<h1 align="center"><b>PANORAMA DA PESQUISA NA PUCPR - 2014</b></h1>
<span class="corpo-texto-explicativo">
<P class="lt3">CIP - Centro Integrado de Pesquisa, implantado. Esse sistema tem como objetivo Integrar os diversos sistemas de gestão/ bancos de armazenamento de dados: CEP/CEUA, IC (PIBIC, PIBITI e PIBIC Jr), Grupos/Linhas de Pesquisa e Banco de Projetos, visando a otimização da utilização da informação inserida pelos docentes pesquisadores.</P>
<P class="lt3">No âmbito da pesquisa a PUCPR se destacou no ranking da Folha de São Paulo, em 2013, foi considerada a 47ª. universidade do país, 3ª. universidade privada e a 1<SUP>a.</SUP> universidade privada do Estado do Paraná.</P>
<P class="lt3">Tem em seu corpo docente, 542 doutores, sendo 325 em tempo integral, dos quais 208 estão inseridos nos programas stricto sensu. Possui aproximadamente 1314 alunos nos programas de pós-graduação stricto sensu.</P>
<P class="lt3">Possui 15 Programas de Pós Graduação Stricto Sensu (PPG) em nível de Mestrado, 11 em nível de Doutorado, sendo a grande maioria com nota 4, e seis PPG com nota 5 pela Coordenação de Aperfeiçoamento de Pessoal de Nível Superior do Ministério da Educação (CAPES).</P>
<P class="lt3">Conta com um total de 46 bolsistas Produtividade em Pesquisa (PQ) e Produtividade em Desenvolvimento Tecnológico e Extensão Inovadora (DT). Destes 46 são do CNPq, sendo: 2 pesquisadores nível 1C, 2 pesquisadores nível 1B, 6 pesquisadores nível 1D e 36 pesquisadores nível 2; 21 da Fundação Araucária.</P>
<P class="lt3">Possui 98 Grupos de Pesquisa cadastrados e certificados junto ao CNPq. De acordo com o Censo do CNPq.</P>
<P class="lt3">Disponibiliza em seu site o Banco de Projetos que é uma ferramenta de busca que permite acessar dados dos projetos de pesquisa realizados na Instituição a partir de palavras-chave, área de conhecimento ou nome do pesquisador.</P>
<P class="lt3">Realiza anualmente a Mostra de Pesquisa e Inovação com objetivo de favorecer a integração da Pesquisa e o Setor Produtivo em geral (empresas, indústrias, órgãos governamentais e ONG´s).</P>
<P class="lt3">Conta com a Agência PUC de Inovação: trata-se de um novo arranjo institucional para incrementar a atuação da PUCPR como agente de promoção do desenvolvimento socioeconômico. Atua na transferência do conhecimento científico e tecnológico para o setor produtivo, mediante alianças e parcerias estratégicas entre a Universidade e as Empresas.</P>
<P class="lt3">A Agência PUC de Ciência, Tecnologia e Inovação está organizada em Rede junto as Escolas da PUCPR, comporta 10 Núcleos, quais sejam: saúde, biotecnologia, pesquisa clínica, gestão, engenharias, educação e humanidades, ensino à distância, agrárias, direito e TIC.</P>
<P class="lt3">Fazem parte também da Agência, o Escritório de Transferência de Tecnologia e o Escritório de Gestão Integrada de Projetos de Parceria U - E.</P>
