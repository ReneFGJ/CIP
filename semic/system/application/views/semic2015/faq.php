			<br />
			<li>
				<p align="justify">
					<strong> Quantas apresentações devo assistir para ter a declaração de participação no evento caso não eu não seja aluno PIBIC/PIBITI e queira a declaração como ouvinte?</strong>
					<br/>
					O aluno que quiser participar como ouvinte deverá assistir obrigatoriamente pelo menos 50% das apresentações oral, palestras, minicurso e/ou mesa redonda.
					<br />
					*** Sessões de pôster NÃO contabilizam como frequência para completar os 50% de participação no evento.
				</p>
			</li>
			<br />
			<li>
				<p align="justify">
					<strong>Sou aluno PIBIC/PIBITI e irei apresentar, terei direito à declaração?</strong>
					<br/>
					Sim, alunos que apresentarão seus trabalhos no evento receberão a declaração de conclusão e participação no programa PIBIC/PIBITI, porém caso queiram a declaração de participação no evento SEMIC 2015, deverão obrigatoriamente participar em 50 % do evento, assim receberão duas declarações.
				</p>
			</li>
			<br />
			<li>
				<p align="justify">
					<strong>Quantas apresentações deverei assistir para participar 50% do evento?</strong>
					<br/>
					Para ter 50% de presença o aluno deverá assistir pelo menos 07 (sete) blocos de apresentação. Seja oral, palestra, minicurso e/ou mesa redonda, porém, a participação somente será contabilizada caso o aluno permaneça durante o bloco inteiro (início ao fim) de apresentação. A equipe de apoio está terminantemente proibida de abrir qualquer tipo de exceção, caso o aluno queira sair da sala antes de finalizar o bloco. Nosso sistema é altamente resolutivo e contempla saídas para ir ao banheiro, tomar água etc, porém o aluno não poderá permanecer por um período superior a 15 minutos fora da sala, pois sua entrada será automaticamente cancelada pelo sistema. Entradas e saídas nas salas durante as apresentações poderão atrapalhar os colegas, portanto evitem ao máximo.
				</p>
			</li>
			<br />
			<li>
				<p align="justify">
					<strong>Posso validar o certificado do SEMIC como horas complementares? Quantas horas é válido?</strong>
					<br/>
					Cada aluno deverá apresentar a declaração de participação no programa e/ou ouvinte na coordenação de seu curso de graduação para validar as horas. A coordenação do PIBIC não determina a quantidade de horas a serem validadas.
				</p>
