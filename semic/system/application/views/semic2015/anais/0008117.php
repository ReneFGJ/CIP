			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O Programa Segundo Tempo (PST) é um programa do governo federal através do Ministério do Esporte em parceria com estados e municípios que busca democratizar o acesso à pratica e à cultura do esporte, promovendo o desenvolvimento integral de crianças e adolescentes na formação da cidadania e melhoria da qualidade de vida desses indivíduos que prioritariamente se encontram em áreas de vulnerabilidade social e matriculados na rede pública de ensino.					<B>Objetivo</B>: O objetivo desse estudo foi analisar o desenvolvimento do processo de gestão do Programa Segundo Tempo no município de São José dos Pinhais no período de 2010 a 2012.					<B>Metodologia</B>: O estudo foi de caráter qualitativo exploratório e os instrumentos utilizados foram entrevista semi-estruturada com dois membros da coordenação do PST no município para ter um aprofundamento e compreensão do problema proposto no estudo.					<B>Resultados</B>: Verificamos que o perfil de gestão do PST se caracterisou  como descentralização parcial, porém esta descentralização e autonomia prevista para o município nas diretrizes do programa com maior ênfase  na implementação do programa, não tendo pouca continuidade na sua execução.					<B>Conclusões</B>: Conclui-se que durante a implementação do programa houve uma parcial descentralização e a autonomia entre as esferas governamentais, e a partir da execução do PST, o Ministério do Esporte teve uma ação mais de fiscalizadora, enquanto que a secretaria municipal de esporte e lazer do município teve uma atuação de execução do projeto pedagógico do convênio realizado na fase de implementação do PST.				</P>
			</div><B>Palavras-chave</B>: Gestão. Esporte. Programas sociais.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The program Second Time (PST) is a federal program through the Ministry of Sports in partnership with states and municipalities seeking to democratize access to practice sport and culture, promoting the integral development of children and adolescents in the formation of citizenship and improving the quality of life of individuals who are primarily in socially vulnerable areas and enrolled in the public school system.					<B>Objectives</B>: The aim of this study was to analyze the development of the Second Half Program management process in São José dos Pinhais in the period 2010 to 2012.					<B>Methods</B>: he study is exploratory qualitative and the instrument used was a semi-structured interview with two members of the coordination of the PST in the city to have a deeper understanding of the problem and proposed in the study.					<B>Results</B>: We found that the PST management profile was a partial profile of decentralization, which occurred more emphasis on the preparation and implemtanção phase compared the implementation phase.					<B>Conclusion</B>: It is concluded that during implementation of the program there was a partial decentralization and autonomy between spheres of government and from the implementation of the PST, the Ministry of Sports had more action supervisory,, while the municipal department of sport and recreation in the municipalityWe had a pedagogical project execution performance of the agreement made in the implementation of the PST phase.				</P>
