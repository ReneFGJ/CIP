			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Existe uma tendência mundial de repensar e melhor desenvolver mecanismos de controle interno nas empresas, aumentando a transparência dos procedimentos e diminuindo os riscos de práticas indesejadas, principalmente, no âmbito penal. Daí a importância de se estudar o instituto do criminal compliance. No Brasil, tal fato ganhou ainda mais notoriedade com o julgamento da Ação Penal 470 (julgamento do mensalão), ocasião em que o STF condenou os dirigentes do “núcleo financeiro”, reconhecendo a relevância penal com base em descumprimento de obrigações de caráter administrativo.					<B>Objetivo</B>: Busca-se analisar sob qual(is) fundamento(s) pode-se atribuir a posição de garante do(s) responsável(is) pelo cumprimento das regras (compliance officer) dentro da empresa.					<B>Metodologia</B>: Por ser tema novo (praticamente desregulamentado no Brasil), existe certa carência de boa bibliografia nacional e dificuldade de acesso às internacionais. O trabalho é pautado por estudos de grandes doutrinadores sobre o tema especificamente, e sobre assuntos tangentes, coligindo o que se considera mais correto em cada ponto. Com isso, tem-se uma estruturação da opinião dos sábios, capaz de elucidar teoricamente o como se passa no plano normativo.					<B>Resultados</B>: Do cruzamento das informações teóricas, com as normas jurídicas e a jurisprudência, o fundamento da posição de garante dos compliance officers advém da existência de deveres jurídicos extrapenais. Trata-se de situação que será cada vez mais explorada pelo Direito na luta contra os crimes econômicos.					<B>Conclusões</B>: Pode-se dizer que o novo padrão regulatório se orienta pelo dever de colaboração (veicula os deveres de diligência e cuidado, lealdade e sinceridade) ao funcionamento do mercado, que oferece ao Estado as condições de prevenção da criminalidade no âmbito empresarial.				</P>
			</div><B>Palavras-chave</B>: Compliance. Garantidor e Responsabilidade			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: There is a global tendency to rethink and better develop companies’ internal control mechanisms, improving the transparency of procedures and lowering risk of undesired practices, mostly, on criminal ground. Therefore the importance to studying the criminal compliance institute. In Brazil, such fact adquired even more notoriety with the judgement of AP 470 (mensalão), at which time STF convicted the directors of the "financial core", recognizing the criminal relevance based on breach of administrative character obligations.					<B>Objectives</B>: The focus is to analyze under what reason(s) can be assigned the position of guarantor responsible(s) for observance of the rules (compliance officer) within the company.					<B>Methods</B>: Being new theme (almost deregulated in Brazil), there is a certain lack of good national bibliography and difficult access to international. The work is guided by scholars of great studies on the subject specifically, and on tangent topics, collecting what is considered more accurate at each point. Thus, it has been structuring the opinion of the wise, able to theoretically elucidate what happens in legislative terms.					<B>Results</B>: From the intersection of theoretical information with the legal framework and case law, the foundation of the position of guarantor of compliance officers arises from the existence of out-criminal legal duties. It’s a situation that is increasingly exploited by the law in the fight against economic crimes.					<B>Conclusion</B>: It can be said that the new regulatory standard is guided by the duty of cooperation (conveys the duty of diligence and care, loyalty and sincerity) the operation of the market, which offers the States the conditions of crime prevention in the business domain.				</P>
