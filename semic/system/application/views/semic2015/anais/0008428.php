			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O desenvolvimento da ciência contábil está atrelado ao aperfeiçoamento das relações econômicas: sendo estas mais complexas, requerem que a contabilidade suporte tamanha demanda. Dessa forma, a prática contábil passa gradativamente a utilizar-se de tecnologia da informação para se adequar ao cenário em que se insere. Não só para o contexto empresarial, os produtos gerados pela contabilidade são usados também em caráter fiscalizatório pela Administração Pública. Como reflexo desse modelo informatizado de prestação de contas, em 2007 o Governo implementa o Sistema Público de Escrituração Digital (SPED) e em 2010 iniciaram os trabalhos da chamada Escrituração Fiscal Digital Social, para que em 2012 fosse instaurado o que hoje é conhecido por eSocial. O e-Social é uma plataforma que vai unificar o envio de informações trabalhistas e previdenciárias ao fisco por empregadores em geral.					<B>Objetivo</B>: Como objetivo, o presente trabalho vislumbra evidenciar como as empresas de contabilidade de Londrina estão se preparando para atender ao eSocial - estabelecendo uma conjuntura, além de identificar a receptividade do programa quanto à medidas internas cabíveis.					<B>Metodologia</B>: Para atender a estes critérios, como pesquisa de caráter original, foi aplicado um questionário misto no período de setembro de 2014 a junho de 2015, com característica majoritariamente de mensuração, que pretende estabelecer um cenário abrangente em uma amostra não-probabilística por conveniência.					<B>Resultados</B>: A situação se mostra equilibrada – 28,57% da amostra se define parcialmente inapta ao programa, quanto 26,19% se diz apta. Os escritórios atribuem ao sistema de controle interno a medida mais crucial para adequação ao eSocial.					<B>Conclusões</B>: A pesquisa mostra que a adequação ao eSocial depende de um sistema gerencial que comporte o programa. Ou seja, a tecnologia pela tecnologia.				</P>
			</div><B>Palavras-chave</B>: ESocial. Empresas de Contabilidade. SPED			<BR>
