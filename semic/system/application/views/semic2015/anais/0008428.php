			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The development of accounting science is linked to the improvement of economic relations: which were more complex, require accounting support such demand. Thus, the accounting practice gradually begins to use technology to suit the scenario in which it operates. Not only for the business environment, the products generated by accounting are used also in fiscalization by the Government. Reflecting this accountability computerized model, in 2007 the Government implements the Public Digital Bookkeeping System (SPED) and in 2010 began the work of the Fiscal Social Digital Bookkeeping, so that in 2012 was deployed what is now known for eSocial. The e-Social is a platform that will unify the sending of labor and social security information to the tax authorities by employers in general.					<B>Objectives</B>: As a goal, this work intends to evidence as Londrina accounting firms are preparing to meet the eSocial - establishing a situation and identify the reactivity of the program as the appropriate internal measures.					<B>Methods</B>: To meet these criteria, such as original research study, it applied a mixed questionnaire from September 2014 to June 2015, mainly with measurement feature, which aims to establish a comprehensive scenario in a non-probabilistic convenience sample. The situation shows balanced - 28.57% of the sample is defined partially unfit for the program, as 26.19% is said apt.					<B>Results</B>: Offices attribute to the internal control system the most crucial measure to fit the eSocial.					<B>Conclusion</B>: Research shows that matching the eSocial depends on a management system carrying the program. So, technology by technology.				</P>
