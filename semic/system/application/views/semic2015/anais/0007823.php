			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Os distúrbios do metabolismo mineral e ósseo são frequentes em portadores de doença renal crônica e estão associados com elevada morbidade e mortalidade. O transplante renal pode corrigir muitas destas alterações, contudo a recuperação da função renal dificilmente é completa. A constatação da prevalência de hiperparatireoidismo secundário (HPTs) e de hipovitaminose D após transplante renal nessa população é de suma importância para o seguimento dos pacientes.					<B>Objetivo</B>: Determinar se o transplante renal pode corrigir alterações metabólicas ocasionadas pela doença renal crônica					<B>Metodologia</B>: Foi realizado um estudo retrospectivo de um grupo de pacientes transplantados renais estáveis, incluindo pacientes [e]#8805; 18 anos de idade, com ao menos uma dosagem de paratormônio (PTHi) nos últimos seis meses de acompanhamento. Foram analisadas, fosfatase alcalina, cálcio sérico, fósforo sérico, dosagem de 25 hidroxivitamina D3, bem como calculada taxa estimada de filtração glomerular, além de eco cardiograma transtorácico para avaliação da presença de hipertrofia de ventrículo esquerdo. Foram estudados 313 pacientes transplantados renais.					<B>Resultados</B>: A prevalência de hipovitaminose D (25OH D3 [e]lt; 30 ng/ml) foi de 60,5% e a de hiperparatireoidismo secundário (PTHi [e]rt; 65 pg/ml) foi de 42,8%.					<B>Conclusões</B>: Apesar do transplante renal conseguir reverter grande parte das anormalidades no metabolismo mineral e ósseo, há alterações que ainda persistem. O estudo apontou uma elevada prevalência de HPTs e de hipovitaminose D em pacientes transplantados renais				</P>
			</div><B>Palavras-chave</B>: Transplante renal. desordens metabólicas			<BR>
