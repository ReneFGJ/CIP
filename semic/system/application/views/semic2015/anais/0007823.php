			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Mineral and bone metabolism disorders are frequent in the chronic renal disease population and they are both associated with high morbidity and mortality. Kidney transplantation can correct many of these disorders, however the recovery of renal function is hardly complete. The aim of this study was to evaluate the prevalence of secondary hyperparathyroidism and hypovitaminosis D after kidney transplantation					<B>Objectives</B>: Determine whether the renal transplantation may correct metabolic changes caused by chronic kidney disease					<B>Methods</B>: Retrospective study of a group of stable renal transplants patients. Were included patients with [e]#8805; 18 years, with at least one level of parathyroid hormone (iPTH) within the last six months of follow-up. Were analysed, alkaline phosphatase, serum calcium, serum phosphate, 25 hidroxyvitamin D3, as well calculated the estimated glomerular filtration rate. Also to evaluate the presence of left ventricular hypertrophy was analysed the transthoracic echocardiography. We analysed 313 kidney transplant patients.					<B>Results</B>: The prevalence of hypovitaminosis D (25OH D3 [e]lt; 30 ng/ml) was 60,5% and the prevalence of secondary hyperparathyroidism (PTHi [e]rt; 65 pg/ml) was 42,8%.					<B>Conclusion</B>: Despite of reverting most of the abnormalities in bone and mineral metabolism, some disorders can persist after the kidney transplant. Our study found a high prevalence of secondary hyperparathyroidism and hypovitaminosis D in kidney transplant patients.				</P>
