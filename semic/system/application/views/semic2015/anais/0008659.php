			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Segundo a United Nations Children’s Fund – Unicef as crianças que possuem deficiência, seja ela física, sensorial, intelectual estão entre as mais estigmatizadas e marginalizadas de todas as crianças do mundo.					<B>Objetivo</B>: Este projeto tem como objetivo a proposta de indicadores das áreas de Educação, Saúde e Assistência Social sobre crianças e adolescentes com deficiência no Estado do Paraná, a partir de diversas fontes de dados.					<B>Metodologia</B>: Para a construção dos indicadores e identificação das variáveis necessárias, foi construído um plano tabular, considerando a seguinte questão: “Em que medida, as crianças e adolescentes com deficiência do estado do Paraná têm seus direitos fundamentais assegurados no que se refere à rede de serviços das políticas básicas de Saúde, Educação e Assistência Social, disponíveis para seus territórios de moradia?”.					<B>Resultados</B>: Como resultado foram propostos 97, porém ainda se mostram insuficientes tendo em vista a não disponibilidade de dados específicos sobre deficiência. E mesmo aqueles existentes, não se encontram disponíveis para a sociedade a despeito de todo o esforço que vem sendo feito para a maior transparência.					<B>Conclusões</B>: Como trabalho futuro está prevista a coleta de dados a de questionários, para que os indicadores excluídos desta proposta possam ser construídos. Os indicadores propostos no escopo deste projeto estão sendo apropriados por três projetos de dissertação de mestrado desenvolvidos no Programa de Pós-graduação em Tecnologia em Saúde pela PUCPR.				</P>
			</div><B>Palavras-chave</B>: Indicadores, Crianças e Adolescentes com Deficiência, Direitos Humanos			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: According to the United Nations Children´s Fund - UNICEF children who have disabilities, whether physical, sensory, intellectual are among the most stigmatized and marginalized of all the world´s children.					<B>Objectives</B>: This project objective to proposed indicators of Education, Health and Social Assistance, considering children and adolescents with disabilities, in the State of Paraná, from several data sets.					<B>Methods</B>: For the construction of indicators were considered the question: "How disability’s children and adolescents have their fundamental rights guaranteed in relation to network services the basic policies of Health, Education and Social Assistance, available to their home territories?".					<B>Results</B>: As a result 97 were proposed, but still are insufficient in view of the unavailability of specific data on disability. And even those existing, are not available to society despite all the efforts being made for greater transparency.					<B>Conclusion</B>: As future work is planned to collect data from questionnaires so that the indicators excluded from this proposal can be built. The indicators proposed in the scope of this project are being appropriated for three master´s thesis projects developed at the Graduate Program in Health Technology by the PUCPR.				</P>
