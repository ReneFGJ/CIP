			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O Tumor Venéreo Transmissível (TVT) é uma neoplasia de fácil disseminação e ocorre através do coito, mordeduras, lambeduras e dessa forma a implantação de células tumorais na membrana das mucosas, neste sentido, o aparecimento das lesões  ser observadas em localizações variadas, como cavidades oral e nasal, na pele e nas tonsilas, porém o local mais acometido é na região genital.  A classificação é feita quanto aos seus três subtipos linfocitoide, plasmocitoide e misto, através de exames citopatológicos e histopatológicos, mas o mais utilizado é o exame citopatológico, o qual foi escolhido para ser utilizado nessa pesquisa.					<B>Objetivo</B>: Calssificar o tipo de TVT mais frequente na região.					<B>Metodologia</B>: Neste estudo, foram realizados exames em 6 cães, os quais apresentavam sintomas e sinais característicos de tumor venéreo transmissível, porém só foi possível a confirmação e classificação de 4 amostras de cães portadores da neoplasia genital primária.					<B>Resultados</B>: Quanto à classificação do TVT, o tipo plasmocitoide foi o de maior ocorrência, seguido pelo misto e nenhum caso do tipo linfocitoide. Dessa forma, como esse tumor apresenta particularidades morfológicas, as quais podem interferir no comportamento e no tratamento, a sua classificação acaba sendo de grande importância, pois através de estudos, foi comprovado que o tipo plasmocitoide além de ser mais comum, é o que apresenta maior resistência ao tratamento com Sulfato de Vincristina. Após a classificação do tipo de tumor de cada animal, eles foram submetidos à quimioterapia com sulfato de vincristina e assim observados até a cura total do tumor. Quanto ao tratamento, os animais necessitaram em média de quatro a sete quimioterapias para remissão da massa.					<B>Conclusões</B>: O subtipo plasmocitóide foi o mais frequente dentre as amostras utilizadas no estudo.				</P>
			</div><B>Palavras-chave</B>: TVT. Tumor. Citologia			<BR>
