			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The tumor Transmissible Venereal ( TVT ) is a neoplasm and is easily spread through sexual intercourse , bites , licks and thus the implantation of tumor cells in the mucous membrane , in this sense, the appearance of lesions is observed in various locations , such as wells oral and nasal , skin and in tonsils , but the most affected location is the genital area . The classification is made as to its three linfocitoide subtypes , plasmacytoid and mixed through cytological and histological examinations , but the most used is the Pap test , which was chosen to be used in this research.					<B>Objectives</B>: Rate the most common type of TVT in the region.					<B>Methods</B>: In this study, tests were performed in 6 dogs, which showed characteristic symptoms and signs of transmissible venereal tumor, but confirmation and classification of 4 samples of dogs primary genital neoplasia was made possible.					<B>Results</B>: Regarding the classification of TVT , the plasmacytoid type was the most frequent , followed by mixed and no cases of linfocitoide type. Thus, as this tumor presents morphological characteristics , which may interfere with the behavior and treatment , their classification turns out to be of great importance, because through studies it has been proven that plasmacytoid type and is more common , is the one with increased resistance to treatment with Vincristine sulfate . After classification of each animal tumor type , they underwent chemotherapy with vincristine sulfate and thus observed to the full cure of the tumor. As for treatment, the animals needed an average of four to seven chemotherapy for mass remission.					<B>Conclusion</B>: The plasmacytoid subtype was most frequent among the samples used in the study.				</P>
