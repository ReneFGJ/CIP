			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O Tumor Venéreo Transmissível (TVT) é uma neoplasia que se dissemina através do coito, mordeduras e lambeduras gerando a implantação de células tumorais na membrana das mucosas. Dessa forma, o tumor é mais comumente localizado em genitais, porém pode ser encontrado em localidades variadas como cavidades oral e nasal, pele e tonsilas. O aspecto macroscópico da tumoração aliada ao histórico do paciente é altamente sugestivo de TVT, porém o diagnóstico da afecção depende de análises histopatológicas e citológicas, sendo a última portadora da melhor relação custo-benefício.  Tal tumor possui características morfológicas constantes que permitiram sua subclassificação em 3 diferentes tipos de acordo com o percentual de células encontradas, podendo ser linfocitoide, plasmocitoide e misto.					<B>Objetivo</B>: O atual estudo objetiva a identificação dos subtipos de TVT encontrados da região de Toledo, PR.					<B>Metodologia</B>: No atual estudo foram utilizados apenas 4 cães provenientes da rotina hospitalar da PUCPR-Toledo, não havendo contribuição externa ao projeto de veterinários da localidade. Após serem submetidos ao PAAF e diagnosticados com TVT deu-se início a terapêutica com sulfato de vincristina. Após o diagnóstico de TVT uma análise minuciosa da lâmina foi realizada com a finalidade de tipificar a tumoração em plasmocitóide, linfocitóide ou mista.					<B>Resultados</B>: O tipo plasmocitoide foi identificado em 3 casos sendo portanto o de maior ocorrência. O TVT misto foi encontrado em apenas um caso, sendo ausente casos TVT linfocitóide.					<B>Conclusões</B>: O baixo número amostral foi decorrente da diminuição de casos de TVT atendidos no Hospital Veterinário da PUCPR-Toledo associado a não contribuição de clínicos da região no projeto. O TVT mais encontrado coincide com dados literários epidemiológicos que afirmam ser o TVT plasmocitóide o de maior ocorrência.				</P>
			</div><B>Palavras-chave</B>: Canino. TVT. Citologia.			<BR>
