			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The Transmissible Venereal Tumor (TVT) is a cancer that spreads through intercourse, bites and licks generating the deployment of tumor cells in the mucous membrane. Thus, the tumor is more commonly located in genitals, but can be found in various locations such as the oral and nasal cavities, skin and tonsils. The gross appearance of the tumor together with the patient´s history is highly suggestive of TVT, but the diagnostic of the condition depends on histological and cytological analysis, the last bearer of the best value for money. This tumor has constant characteristics which permitted their morphological subclassification in 3 different types according to the percentage of cells found, can be lymnphocytoide, plasmocytoid and mixed.					<B>Objectives</B>: The current study aims to identify the morfological types of TVT found in the Toledo region.					<B>Methods</B>: In the current study it was used only four dogs from the hospital routine PUCPR-Toledo, with no external veterinary contribution to the project. After being submitted to FNA and diagnosed with TVT was begun therapy with vincristine sulfate. After the diagnostic of TVT thorough analysis of the blade was held in order to typify the mass in plasmacytoid, limphocytoide or mixed.					<B>Results</B>: The plasmacytoid type was identified in 3 cases and therefore the most frequent. The mixed TVT was found in only one case, absent cases TVT linfocitóide.					<B>Conclusion</B>: The low sample size was due to the decrease in cases of TVT treated at PUCPR-Toledo Vet Hospital associated with non-clinical contribution of the region in the project. The TVT found more literary coincides with epidemiological data that claim to be the TVT plasmacytoid the most frequent.				</P>
