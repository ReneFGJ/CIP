			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Peltophorum dubium (Spreng.) Taub is a medicinal plant used in the form of juice to trate impinges, infectious, inflammatory and gastric diseases.					<B>Objectives</B>: The aim of this study was realize the phytochemical screening of the leaves of Peltophorum dubium (Spreng.) Taub and an evaluation of the antimicrobial potential of its methanolic extract and fractions (aquous, dichlormethylene, ethyl acetate and hexane).					<B>Methods</B>: The leaves were collected, dried and reduced to powder to prepare the methanolic extract (maceration). The extract was lyophilized and this made the phytochemical screening for detection of carbohydrates, reducing sugars, tannins, saponins, flavonoids, steroids, alkaloids and glycosides. The evaluation of the antimicrobial potential by agar disk diffusion technique was performed with methanolic extract and fractions forward to bacteria of great clinical importance.					<B>Results</B>: The phytochemical screening identified the presence of tannins, saponins, glycosides and carbohydrates in methanolic extract and microbiological evaluation showed that it significantly inhibited the development of Staphylococcus aureus bacteria at a concentration of 12 mg, with 7.33 mm inhibition zone and among the tested fractions, the aqueous fraction inhibited the development of Staphylococcus aureus (9.33 mm) and Pseudomonas aeruginosa (11.11 mm), and the ethyl acetate fraction formed an inhibitory halo for Staphylococcus aureus (9.67 mm) and Samonela typhimurium (10.67 mm), both at a concentration of 12mg.					<B>Conclusion</B>: It was possible to confirm the etnomedicinal use for the leaves of Peltophorum dubium (Spreng.) Taub with antimicrobial purpose, but further studies are necessary.				</P>
