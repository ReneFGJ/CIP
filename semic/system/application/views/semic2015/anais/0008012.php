			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Peltophorum dubium (spreng.) taub é uma planta medicinal utilizada na forma de suco para combate de impinges, doenças infecciosas, inflamatórias e gástricas.					<B>Objetivo</B>: O objetivo desse trabalho foi realizar a triagem fitoquímica das folhas de Peltophorum dubium (Spreng.)Taub e uma avaliação do potencial antimicrobiano do seu extrato metanólico e frações (aquosa, diclorometano, acetato de etila e hexânica).					<B>Metodologia</B>: As folhas foram coletadas, secas e reduzida a pó para preparo do extrato metanólico (maceração). O extrato foi liofilizado e deste realizada a triagem fitoquímica para detecção de carboidratos, açúcares redutores, taninos, saponinas, flavonoides, esteroides, alcaloides e glicosídeos. A avaliação do potencial antimicrobiano pela técnica de disco-difusão em ágar foi realizada com o extrato metanólico e suas frações frente a bactérias de grande importância clínica.					<B>Resultados</B>: A triagem fitoquímica identificou a presença de taninos, saponinas, glicosídeos e carboidratos no extrato metanólico e a avaliação microbiológica evidenciou que o mesmo inibiu de forma significativa o desenvolvimento da bactéria Staphylococcus aureus na concentração de 12 mg, com halo de inibição de 7,33 mm, e dentre as frações testadas, a fração aquosa inibiu desenvolvimento de Staphylococcus aureus (9,33 mm) e Pseudomonas aeruginosa (11,11 mm), e a fração acetato de etila formou um halo inibitório para Staphylococcus aureus (9,67 mm) e Samonela typhimurium (10,67 mm), ambas na concentração de 12mg.					<B>Conclusões</B>: Foi possível confirmar o uso etnomedicinal das folhas de Peltophorum dubium (spreng.) taub com finalidade antimicrobiana, porém estudos complementares fazem-se necessários.				</P>
