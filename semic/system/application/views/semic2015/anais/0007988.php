			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A osteoporose é importante morbidade na vida da mulher menopausada e existem diversos fatores que podem levar a ela tais como hábitos de vida, uso de medicamentos, alterações hormonais dentre outros.					<B>Objetivo</B>: O objetivo desse trabalho foi avaliar quais os reais riscos dessas mulheres de desenvolver osteoporose com base na história familiar e pessoal delas, bem como analisar se as participantes do estudo apresentam tais fatores e expor de forma indireta ao profissional da saúde aquilo que pode ser mudado no curso dessa patologia e que não pode ser esquecido.					<B>Metodologia</B>: O estudo foi observacional de delineamento transversal com 200 pacientes. Foi aplicado um questionário de 19 perguntas objetivas nos ambulatórios da Santa Casa de Curitiba nas mulheres menopausadas. Os dados foram descritos por frequências e percentuais, arquivados no Microsoft Excel 2010, analisados estatisticamente com o Programa SPSS e os testes T student e Kruskal-Wallis, organizados em planilha Excel e representados em tabelas.					<B>Resultados</B>: Dentre os resultados encontrados, a maioria não demonstrou influência genética na saúde do osso (81,5%), não foi submetida à ooforectomia antes dos cinquenta anos (95%) nem notou alteração importante na estatura (90%) ou apresentou quedas em frequência importante (84%). Grande parte não faz atividade física de forma satisfatória (51,5%), mas recebe luz solar adequadamente (68,5%).					<B>Conclusões</B>: A história familiar não influencia no metabolismo ósseo do adulto, enquanto os hábitos de vida devem ser abordados pelo médico para que possam ser modificados quando necessário, já que são passíveis de mudança e alteram a saúde do tecido ósseo.				</P>
			</div><B>Palavras-chave</B>: Osteoporose. menopausa. após a menopausa. saúde do osso			<BR>
