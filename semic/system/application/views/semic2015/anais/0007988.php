			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Osteoporosis is an important morbidity in the life of menopausal women and there are several factors that can lead to it such as lifestyle habits, medication use and hormonal changes.					<B>Objectives</B>: The aim of this study was to evaluate what are the real risks that these women have of  developing  osteoporosis based on their family and personal history  and analyze whether they present such factors and exposed indirectly to the health professionals  what can be changed in the course of this disease and that can´t be forgotten.					<B>Methods</B>: An observational cross-sectional study with 200 patients. A questionnaire of 19 objective questions was applied in the clinics of Santa Casa de Curitiba in postmenopausal women. Data were presented as frequencies and percentages, filed in Microsoft Excel 2010, statistically analyzed SPSS Program and T student test and Kruskal-Wallis, organized in an Excel spreadsheet and represented in tables.					<B>Results</B>: The majority did not show genetic influence on bone health (81.5%), was not submitted to oophorectomy before the age of fifty (95%), did not notice a major change in stature (90%) or showed important number of falls (84%). Much does not exercise in a satisfactory way (51.5%), but is used to receiving adequate sunlight (68.5%).					<B>Conclusion</B>: Family history does not influence the adult bone metabolism, while living habits should be approached by the doctor so they can be modified as necessary as they are subject to change and alter the health of the bone tissue.				</P>
