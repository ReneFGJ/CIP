			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Em uma análise sobre a atualidade, é possível perceber que a palavra corrupção
encontra-se cada vez mais em evidência.
Constantemente na mídia, ouvimos falar em casos de escândalos de corrupção
envolvendo principalmente a administração pública e em virtude disso é que os meios
acadêmicos se movem para pesquisar a possível origem deste fenômeno social que
causa enorme prejuízo a toda sociedade.					<B>Objetivo</B>: Portanto, se faz necessária a presente
pesquisa com a finalidade de apontar as possíveis causas que originam a corrupção,
como ela se desenvolve na administração pública em virtude da ausência do princípio
da objetividade nos atos de agentes públicos, quais seriam os motivos, se seriam
também algo cultural e quais seriam possíveis mecanismos para coibir tais ações.					<B>Metodologia</B>: Para o apontamento de resultados, foram utilizadas doutrinas e pesquisas de
campo por meio de entrevistas com autoridades, pessoas da área do direito e
populares. Entretanto, além da utilização de doutrinas, foram utilizados artigos, e
citações de sites eletrônicos.					<B>Resultados</B>: Através das pesquisas de campo, mediante entrevistas, pôde-se observar a
divergência de opiniões no que tange a origem da corrupção, porém entrevistados
demonstraram grande insatisfação com os atos de corrupção praticados atualmente e
os constantes escândalos envolvendo a Administração Pública.					<B>Conclusões</B>: Com relação às entrevistas realizadas com autoridades que atuam diariamente,
direta ou indiretamente com casos de corrupção, pôde-se obter um grande conteúdo de
conhecimento com relação às opiniões dessas autoridades, a conceituação ou visão do
que seriam atos de corrupção e as soluções sugeridas para este grande problema				</P>
			</div><B>Palavras-chave</B>: Palavras-chave. corrupção. administração pública. agente público. soluções.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: In an analysis on the current, it is possible to realize that the word corruption is increasingly in evidence.
Constantly in the media, we heard about cases of corruption scandals involving mainly the public administration and as a result is that the academics move to search for the possible origin of this social phenomenon that causes immense damage to the whole society					<B>Objectives</B>: . Therefore, it is necessary to present research with the purpose of pointing out the possible causes that give rise to the corruption, as it develops in the public administration in the absence of the principle of objectivity in the acts of public officials, which would be the reason, it would be also something cultural and what would be possible mechanisms to discourage such actions.					<B>Methods</B>: For the pointing of results, were used doctrines and field research through interviews with authorities, people of the area of law and popular. However, in addition to the use of doctrines, were used articles and citations of electronic sites.					<B>Results</B>: Through field research, interviews, I could observe the divergence of views regarding the source of the corruption, but interviewees demonstrated great dissatisfaction with the acts of corruption practiced today and the constant scandals involving Public Administration.					<B>Conclusion</B>: With respect to the interviews carried out with authorities that operate daily, directly or indirectly with cases of corruption, it was possible to obtain a large content of knowledge with respect to the views of these authorities, the conceptualization or vision of what would be acts of corruption and the suggested solutions to this great social problem and cultural.				</P>
