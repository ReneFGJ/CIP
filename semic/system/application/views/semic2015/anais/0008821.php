			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: No presente trabalho, analisam-se os verbos de dizer não canônicos da língua portuguesa. Para tanto, promove-se um pesquisa para investigar o comportamento desses verbos em relação aos verbos de dizer ditos oficiais. Desse modo, verificam-se o contexto de uso, o significado, a predicação verbal, a estrutura do complemento e o papel temático de cada verbo de dizer encontrado, mediante o estudo de gramáticas descritivas e normativas da língua portuguesa.					<B>Objetivo</B>: Este trabalho pretende analisar os contextos de uso dos verbos de dizer não canônicos baseando-se nos fundamentos da valência sintática e da sua distribuição, averiguando as semelhanças do comportamento quando comparados aos verbos de dizer canônicos e avaliando as restrições semânticas que o emprego desses verbos implica.					<B>Metodologia</B>: Foi elaborada uma tabela visando a compreender sintática e semanticamente os verbos de dizer não canônicos, realizando testes de comutação com todos os 25 verbos encontrados.					<B>Resultados</B>: Destes, observou-se que uma parte era oriunda de notícias sobre comportamento e outra sobre esportes. Verificou-se que o comportamento sintático dos verbos dicendi não-canônicos equivale ao dos canônicos, e que há também equivalência semântica nos contextos analisados. O que os diferencia é o contexto discursivo e a intenção comunicativa do enunciador.					<B>Conclusões</B>: Percebeu-se que nos gêneros textuais o emprego de verbos de dizer não canônicos é propício. Em uma análise mais acurada em outros contextos, seria viável uma ampliação desse número, dada a riqueza desse tipo de verbo na língua portuguesa.				</P>
			</div><B>Palavras-chave</B>: verbos de dizer, listagem canônica, restrições semânticas, verbos não canônicos.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: In this study, we analyze the unofficial reporting verbs in the Portuguese language. Thus, a survey was carried out to investigate the behavior of these in relation to the official ones. Hence, we verified the context of use, the meaning, the verbal predication, the complements’ structure, and the thematic role of each non-official verb, according to the fundamentals of descriptive and normative Portuguese language grammars.					<B>Objectives</B>: This study aims to examine the contexts of use of unofficial reporting verbs based upon the foundations of the syntactic valence and its distribution, confirming the similarities of such verbs when compared to official verbs and evaluating the semantic constraints that their use cause.					<B>Methods</B>: A table was created in order to understand syntactically and semantically non-official reporting verbs, by means of commutation tests with all the 25 unofficial verbs.					<B>Results</B>: Among the verbs found, we verified that of them arose from behavioral context and the other from sports context. It was verified that unofficial reporting verbs have the same syntactic behavior of the official verbs, and there is also a semantic equivalence in analysed contexts. The difference is in the discoursive context and in the communicative intention of the speaker.					<B>Conclusion</B>: We noticed that in the analyzed genres the use of unofficial reporting verbs was propitious. Therefore, in a more accurate analysis in other contexts, an extension of that number, given the variety of this kind of verbs in the Portuguese language, would be viable.				</P>
			</div><B>Keywords</B>: reporting verbs, official listing, semantic constraints, unofficial reporting verbs.</td>
