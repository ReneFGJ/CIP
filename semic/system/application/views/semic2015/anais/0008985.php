			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Introdução: O presente projeto de pesquisa envolve um estudo para o entendimento do MERCADO LIVRE DE ENERGIA, bem como, as Vantagens e Desvantagens de se tornar um Consumidor Livre. Os estudos aqui apresentados são um importante ponto de partida para a pesquisa sobre os consumidores livres de energia elétrica e seu comportamento no mercado.					<B>Objetivo</B>: Objetivos: apresentou-se a aplicação de um modelo para tomada de decisão no setor de comercialização de energia elétrica, pelo qual um grande consumidor poderá optar pelos mercados cativo ou livre, utilizando a metodologia de opções reais. Tratou-se ainda, com particular ênfase os consumidores livres (CL), visando assim permitir ao potencial CL bases para um possível estudo de migração para o ambiente de contratação livre.					<B>Metodologia</B>: Métodos: levantamento bibliográfico buscando fundamentação teórica pertinente ao tema da comercialização de energia elétrica no Ambiente de Contratação Livre, bem como a analise pratica de atos normativos, como leis, decretos, resoluções e portarias, sobretudo os que tratam do mercado livre. ANEEL e a CCEE.					<B>Resultados</B>: Resultados: Neste sentido, o estudo apresentou uma metodologia de comparação entre os dois ambientes, ambiente regulado (mercado cativo) e ambiente de livre contratação (mercado livre) para que assim o consumidor vislumbrasse uma ferramenta de apoio na tomada de decisão.					<B>Conclusões</B>: Conclusão: Conclui-se que uma análise de viabilidade econômica a migração de mercado pode ser bem vantajosa. Mas outros fatos importantes além do ganho financeiro devem ser levados em consideração, é imprescindível avaliar todas estas questões antes de tomar uma decisão de migrar de mercado de energia.				</P>
			</div><B>Palavras-chave</B>: Comercialização de energia eletrica. Mercado livre. Vantagens.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Introduction: this research project concerns a study for the comprehension of the FREE/OPEN ENERGY MARKET, as well as, the advantages and disadvantages of becoming a free consumer. The studies presented here are an important starting point for the research about the free consumers and the eletric power and its Market performance					<B>Objectives</B>: Objectives: presented the application of a model for decision-making in the energy marketing industry, by which a major consumer can choose for either the captive or free markets, by making use of a methodology of real options. Yet handled, with particular emphasis the free consumers, thus aiming to enable the potential free consumers foundations for a possible migration study for the free Market.					<B>Methods</B>: Methodologies: literature seeking relevant theoretical background to the subject of sale of eletric energy in the Free Market, as well as the analyzation of the practice of normative acts, such as, laws, decrees, resolution and ordinances, especially the ones that deal with the free Market ANEEL, CCEE.					<B>Results</B>: Achievements: In this sense, the study presented a methodology of comparison between both settings, controlled settings (captive Market) and the free setting of contracting (free Market), so that this way the consumer can envision a support tool in decision making.					<B>Conclusion</B>: Conclusion: It can be concluded that economic feasibility analysis Market migration can be very advantageous. Still, other important facts besides the financial gain should be taken into consideration, being essential to evaluate all theses issues before making a decision to migrate in the energgy Market.				</P>
			</div><B>Keywords</B>: Commercialization of eletric energy. Free market or open market. Advantages.</td>
