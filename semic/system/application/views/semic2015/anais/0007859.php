			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Visa uma abordagem histórica da educação no Japão oitocentista, levando em conta o contexto educacional pre Restauração e a revolução educacional a qual o Japão é submetido durante a segunda metade do século XIX.					<B>Objetivo</B>: Buscar informações quanto ao desenvolvimento cultural japonês, no aspecto da educação. Com a base de pensamento iluminista podemos nesse aspecto fazer alguns questionamentos: Quais são os valores aproveitados da proposta iluminista na elaboração da proposta educacional Meiji? O que muda na educação após a promulgação da Constituição Meiji? Qual é o reflexo dessas mudanças na sociedade?					<B>Metodologia</B>: A metodologia preponderante para embasar esta pesquisa está situada na escola francesa de historiografia, a Escola dos Annales, pois a história faz-se com documentos escritos, uma vez que a análise do presente fornece o mapa e o compasso da pesquisa histórica. Entre o passado e o presente não há separação estanque.					<B>Resultados</B>: Observamos então que no auge do governo de Mutsuhito, e um ano após a promulgação da Constituição japonesa a educação era exaltada como princípio básico para a formação da unidade nacional e identidade.					<B>Conclusões</B>: Observa-se no ocidentalismo japonês características próprias de desenvolvimento e que no campo da educação é primado pelo desenvolvimento humano. O iluminismo (Aufklärung) de Kant parece pertencer ao viés filosófico do discurso do Imperador em sua indicação aos japoneses de buscar a educação primordialmente, bem como no respeito as hierarquias sociais. Posso indicar que Mutsuhito, o Imperador Meiji, pode ser considerado um déspota esclarecido aos moldes ocidentais, mas que preservou suas características mais tradicionais.				</P>
			</div><B>Palavras-chave</B>: Iluminismo no Japão, Educação no Japão, História da Educação japonesa.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: In search of a historical approach to education in nineteenth-century Japan, taking into account the educational context pre Restoration and educational revolution which Japan is submitted during the second half of the nineteenth century.					<B>Objectives</B>: To seek information as to Japanese cultural development, in the education aspect. With the base of Enlightenment thinking in this regard can make some questions: What are the values of the Enlightenment proposed leveraged in developing the educational proposal Meiji? What changes in education after the promulgation of the Meiji Constitution? What is the impact of these changes in society?					<B>Methods</B>: The predominant methodology to support this research is located in the French school of historiography, the Annales school, because the story is done with written documents, since the analysis of this provides the map and the compass of historical research. Between the past and the present there is no watertight separation.					<B>Results</B>: Results: We observed that the government peak of Mutsuhito, and one year after the enactment of the Japanese Constitution education was exalted as a basic principle for the formation of national identity and unity.					<B>Conclusion</B>: It is observed in the Japanese Westernism characteristics of development and in education is primacy for human development. The Enlightenment (Aufklärung) Kant seems to belong to the philosophical bias Emperor´s speech in its statement to the Japanese to seek education primarily and respect social hierarchies. I can indicate that Mutsuhito, Emperor Meiji, can be considered an enlightened despot to Western molds, but preserved its more traditional features.				</P>
