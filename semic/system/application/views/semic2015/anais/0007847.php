			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O projeto Modateca é uma pesquisa de iniciação científica teórico-prática, com o intuito de estruturar e organizar a Modateca do Curso de Design de Moda da Escola de Arquitetura e Design da PUCPR. O espaço funciona como um local de pesquisa histórica, social, comportamental e de tendências, voltado para a manutenção cultural tendo como foco principal o campo da moda.					<B>Objetivo</B>: O projeto visa organizar e unir os diversos conhecimentos sobre a moda e atuar como espaço de elaboração e propagação do conhecimento a partir de ações interdisciplinares entre pesquisa, ensino e extensão, incentivando a gestão de moda, contribuindo com o desenvolvimento das atividades e qualidade do design de moda.					<B>Metodologia</B>: Metodologicamente, o trabalho foi desenvolvido através da organização, manutenção e atualização do acervo, atividades técnicas/operacionais de suporte, apoiadas em fundamentação teórica.					<B>Resultados</B>: Foram recebidos e catalogados: um vestido de noiva do ano de 1952, trinta reconstituições de trajes históricos femininos e masculinos de período compreendido de 3000 a.C. à 1789 d.C. e vinte revistas manequim datadas entre anos de 1967 e 1998. Obtiveram-se ainda como doações 119 revistas de moda e 36 trajes sendo eles roupas de festa, vestidos de noiva, roupas feitas sob medida e com modelagens diferenciadas e fantasias de carnaval. Um artigo foi publicado na Revista de Iniciação Científica, Tecnológica e Artística do Senac de São Paulo, e outro artigo sob forma de pôster foi escrito e aprovado, será apresentado no 11º Colóquio de Moda, em Curitiba, PR.					<B>Conclusões</B>: A Modateca é um projeto de vida longa, que permanecerá na faculdade, permitindo que os acadêmicos, professores e a comunidade em geral possam usufruir do acervo, que estará sendo sempre atualizado.				</P>
			</div><B>Palavras-chave</B>: Modateca. Design de Moda. organização de acervo de pesquisa de moda			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Modateca project is a theoretical and practical scientific initiation research with the purpose of structuring and organize the Modateca of the Fashion Design Course at School of Architecture and Design of PUCPR. The spot serves as a place of historical research, social, behavioral and trends facing the culture maintenance, mainly focusing on the field of fashion.					<B>Objectives</B>: The project aims to organizeand unite the diverse knowledge about fashion, act as a development space and spread knowledge starting from interdisciplinary actions among research, teaching and extension, encouraging fashion management, contributing to the development of activities and the quality fashion design.					<B>Methods</B>: Methodologically, the work was developed through the organization, maintenance and updating of the acquis, technical / operational support activities, supported by theoretical foundation.					<B>Results</B>: Foram recebidos e catalogados: um vestido de noiva do ano de 1952, trinta reconstituições de trajes históricos femininos e masculinos de período compreendido de 3000 a.C. à 1789 d.C. e vinte revistas manequim datadas entre anos de 1967 e 1998. Obtiveram-se ainda como doações 119 revistas de moda e 36 trajes sendo eles roupas de festa, vestidos de noiva, roupas feitas sob medida e com modelagens diferenciadas e fantasias de carnaval. Um artigo foi publicado na Revista de Iniciação Científica, Tecnológica e Artística do Senac de São Paulo, e outro artigo sob forma de pôster foi escrito e aprovado, será apresentado no 11º Colóquio de Moda, em Curitiba, PR.					<B>Conclusion</B>: A Modateca é um projeto de vida longa, que permanecerá na faculdade, permitindo que os acadêmicos, professores e a comunidade em geral possam usufruir do acervo, que estará sendo sempre atualizado.				</P>
