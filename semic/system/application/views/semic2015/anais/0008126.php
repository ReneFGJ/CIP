			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O trabalho a seguir aborda uma análise sobre o periódico Jornal das Moças, com foco em suas publicações ao longo dos anos entre 1914 e 1918, com ênfase na questão da segurança alimentar e os aspectos da Primeira Guerra Mundial que influenciaram direta, ou indiretamente na publicação da revista e de seus conteúdos voltados para a área alimentícia.					<B>Objetivo</B>: O objetivo da presente pesquisa é realizar o levantamento e fichamento dos conteúdos de uma Revista Feminina no que diz respeito a tudo que envolve o preparo e consumo dos alimentos e o espaço reservado aos alimentos, levando-se em conta questões relativas ao que hoje se reconhece como Segurança Alimentar no período que compreende a Primeira Guerra Mundial.					<B>Metodologia</B>: A partir do levantamento e fichamento de todas as matérias que se relacionavam ao tema alimentação que estivessem presentes nos volumes do Jornal das Moças entre 1914 e 1918 procurou-se estabelecer uma relação entre os tópicos, englobando também a área da propaganda alimentícia, e da figura da mulher empreendedora inserida na sociedade, como a quituteira; assim como a forma de tratamento que ela recebia a partir da revista e seu posicionamento sobre as questões mundiais que estavam em fase de transição.					<B>Resultados</B>: 					<B>Conclusões</B>: 				</P>
