			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A acupuntura é uma prática da Medicina Tradicional Chinesa (MTC) que tem sido cada vez mais aceita e incorporada à rotina clínica. A conscientização sobre a acupuntura como opção terapêutica resultou até hoje na realização de muitas pesquisas, bem como na melhor compreensão das aplicações desta terapia e de seus mecanismos de ação.					<B>Objetivo</B>: O objetivo deste trabalho foi avaliar os efeitos locais da acupuntura em equinos saudáveis através da técnica de termografia infravermelho.					<B>Metodologia</B>: Foram avaliados dez animais saudáveis através da acupuntura e termografia no Regimento da Polícia Montada Coronel Dulcídio em Curitiba, PR. Durante essa avaliação, foi mensurada a temperatura do corpo do animal durante quinze minutos, distribuídos em 12 momentos diferentes de tempo. Os acupontos avaliados nesse trabalho foram o Bexiga 11 (B-11), Fígado 1 (F-1), Bai hui lombar e Bexiga 67 (B-67).					<B>Resultados</B>: Na avaliação do acuponto B-67 se observou variação de temperatura a partir do segundo minuto, apresentando uma queda de 1,7°C durante 15 minutos. Já o acuponto F-1 apresentou a queda de temperatura se iniciando no terceiro minuto, totalizando 2,98°C. Na análise dos pontos B-11 e Bai hui lombar, esses não apresentaram alterações relevantes nas imagens de termografia.					<B>Conclusões</B>: Conclui-se que na utilização de acupuntura nos pontos Ting, o F-1 e o B-67 provocaram uma maior influência na circulação periférica, por se tratar de pontos mais superficiais e de importante ação energética na medicina chinesa. Trazer informações a respeito do mecanismo de ação da acupuntura se faz extremamente necessário porque muitos desses mecanismos permanecem desconhecidos, devendo ser abordados em futuros estudos.				</P>
			</div><B>Palavras-chave</B>: Acupuntura. Equinos. Termografia. Infravermelho. Medicina tradicional chinesa.			<BR>
