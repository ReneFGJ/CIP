			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The acupuncture is one of the Traditional Chinese Medicine practices that has been approved and is becoming part of the clinical routine. The awareness of acupuncture as a therapeutical choice had resulted until today on a high number of researches as well as a better understanding about the applications of this therapy and its action mechanism.					<B>Objectives</B>: The aim of the present study was to analyze the spot effects of acupuncture on healthy equines through the infrared thermography techniques.					<B>Methods</B>: Ten healthy horses were evaluates through acupuncture and thermography at the Regimento da Polícia Montada Coronel Dulcídio in the city of Curitiba, PR. During these examinations, the animal’s body temperature was measured during 15 minutes, in 12 different moments. The following acupoints were analyzed Bladder 11 (BL-11), Liver 1 (LIV-1), lombar Bai hui e Bladder 67 (BL-67).					<B>Results</B>: In the evaluation of the acupoint BL-67 the temperature variation from the second minute had a decrease of 1,7°C during the period of 15 minutes. The acupoint LIV-1 had a temperature decrease that started at the third minute, in a total of 2,98°C. Analyzing the point BL-11 and lombar Bai hui, they did not present any relevant modification in the thermography image.					<B>Conclusion</B>: In conclusion, the acupuncture on the Ting points, the LIV-1 and BL-67 had a higher influence at the peripheral circulation, for being a superficial point and having an important energetic action at the Chinese medicine. Bringing information about the action mechanism of the acupuncture is extremely necessary, since a lot of those mechanism are still unknown, and shall be investigates in further studies.				</P>
