			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Para melhor compreender uma realidade é importante a disponibilidade de dados que permitam a construção de um diagnóstico.					<B>Objetivo</B>: Este projeto teve como objetivo modelagem e implementação de uma base de dados que integre diversas fontes, sobre crianças e adolescentes com deficiências no Estado do Paraná.					<B>Metodologia</B>: Para tal foram inicialmente identificadas as variáveis necessárias, suas respectivas fontes e a necessidade de compatibilização. Foi adotado o modelo relacional, sendo implementada no SGBD MySql. Para a representação foi adotado o formato: Código do IBGE para o município; Grupo ao qual pertente a variável; Subgrupo ao qual pertence a variável; Código da varável; Ano e Valor.					<B>Resultados</B>: Foram identificadas 225 variáveis, sendo 145 oriundas do Instituto Paranaense de Desenvolvimento Econômico e Social (IPARDES), 7 do Índice de Educação Básica (IDEB), 8 do Índice de Desenvolvimento Humano Municipal (IDHM) e 65 do censo do Instituto Brasileiro de Geografia e Estatística (IBGE). A partir da identificação das variáveis foram identificados os possíveis grupos e subgrupos para melhor organizar o conjunto de dados, tais como Educação, População, Saúde. Por exemplo, para o grupo População foram identificados como subgrupos população censitária, população estimada, etc. A partir do subgrupo foram listadas as respectivas variáveis.					<B>Conclusões</B>: A partir do desenvolvimento deste projeto fica clara a não preocupação por parte dos profissionais da área de Tecnologia da Informação quanto aos diversos usos que as bases modeladas nos diversos sistemas possam ter.				</P>
			</div><B>Palavras-chave</B>: Bases de dados, Crianças e Adolescentes com Deficiência, Direitos Humanos			<BR>
