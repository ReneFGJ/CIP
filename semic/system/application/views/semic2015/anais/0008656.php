			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: For better understanding a reality, it is important data for diagnosis construction.					<B>Objectives</B>: This project aimed to modeling and implementing a database that integrates several data sources about children and adolescents with disabilities on Paraná State.					<B>Methods</B>: For such were initially identified the necessary variables, their respective sources and the need for compatibility. The relational model was implemented on MySQL. For the representation was adopted the format: IBGE code for the city; Group which one the variable belongs; Subgroup that owns the variable; Variable Code; Year and Value.					<B>Results</B>: Were identified in total 225 variables, 145 from the IPARDES, 7 from IDEB, 8 from HDI and 65 from IBGE. The variables were organized in groups and sub-groups to be better organized. Were identified, such as education, population health. For the population group were identified as subgroups population census, estimated population, etc. From their respective sub-group, variables were listed.					<B>Conclusion</B>: From the development of this project is a clear no concern from Information Technology professionals about several kinds of data uses when they are modeling the databases.				</P>
