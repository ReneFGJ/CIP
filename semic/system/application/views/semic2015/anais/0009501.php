			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Um processo de resposta a desastre pode ocorrer de várias maneiras, conforme a necessidade do evento em questão. Essas ações de resposta, normalmente, são fundamentadas em manuais ou guias elaborados pelas entidades que prestam esse serviço de assistência humanitária. Entretanto é difícil estabelecer um processo de resposta a desastre totalmente prescritivo, uma vez que cada evento tem suas características. Desta maneira, é possível afirmar que os processos de resposta a desastres exigem certo grau de flexibilidade. Para isso é necessário avaliar estes processos para determinar sua real eficiência, eficácia e efetividade.					<B>Objetivo</B>: O objetivo do projeto é definir as principais características dos processos de resposta a desastre como sistemas baseados em processos flexíveis e relacionar com um conjunto de diretrizes de engenharia de organizações.					<B>Metodologia</B>: Utilizou-se da revisão da literautra tanto para as definições de processos flexíveis quanto para as ações de resposta a desastres. Em seguida, para caracterizar o processo de resposta a desastre como um processo flexível, foi usado o método da abordagem por processos de Cambridge (Cambridge Process Approach).					<B>Resultados</B>: O processo criado para a avaliação da flexibilidade foi submetido a uma avaliação de especialistas na área que avaliaram a  eficiência, eficácia e efetividade do mesmo. Em seguida aplicou-se o processo em um caso real de resposta a um desastre.					<B>Conclusões</B>: O processo criado se mostra viável para a determinação do grau de flexibilidade de um processo de resposta a desastre, uma vez que foi desenvolvido a partir de referenciais já existentes e de acordo com um procedimento bem estruturado. Os resultados encontrados no caso estudado reforçam a confiabilidade do processo desenvolvido.				</P>
			</div><B>Palavras-chave</B>: flexibilidade de processos; Cambridge Process Approach; resposta a desastres			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: A disaster response process may be executed in many different ways, according to the necessity of the event it is linked to. These response actions are usually based on manuals or guides published by entities that provide assistance in these kinds of situations. However, it is difficult to establish a prescriptive disaster response process, as each event has its own characteristics. Hence, it is possible to state that disaster response processes demand flexibility, and it is needed to evaluate these processes to determine their real efficiency and effectiveness.					<B>Objectives</B>: The objective of this project is to define the main characteristics of disaster response processes as flexible processes and to relate them to a set of enterprise engineering guidelines.					<B>Methods</B>: The method that was applied was based on a literature review of process flexibility and disaster response, as well as the Cambridge Process Approach for defining a process that is able to determine the flexibility of the process.					<B>Results</B>: Specialists in the area evaluated the process that was created to assess the flexibility of disaster response processes. This process was also applied to evaluate the response given to a real event.					<B>Conclusion</B>: It is shown that the process is viable for determining the degree of flexibility of the disaster response process, as it was developed based on already existing guidelines for disaster response, together with a well-structured process. Results reassure the validity of the approach.				</P>
			</div><B>Keywords</B>: process flexibility; Cambridge Process Approach; disaster response</td>
