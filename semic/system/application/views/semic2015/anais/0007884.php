			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: In order to prevent criminal practices and cross-contamination in the use of extracted teeth, the Human Teeth Bank (HTB) was established. It´s preserves the legality of the search and the health of students. In the course of Dentistry, many disciplines using extracted teeth in clinical and laboratory practice, forcing academics to make provision teeth. This reality makes necessary the implementation of BDH, which should be organized to meet this demand.					<B>Objectives</B>: The objective was to analyze the functionality of HTB PUCPR.					<B>Methods</B>: It was a descriptive quantitative approach .Data were collected in the material that the bank stores (teeth) and all documents existing there, since its establishment in 2006 until the year 2014.					<B>Results</B>: The results showed that 63.35% of the projects that used teeth, occurred between the years 2006 to 2008. The bank provided 2081dentes to 80% of the PUCPR projects, whose prevalence was male researchers, and specialty dentistry.  54 students took stage on the HTB. In the 1134 Clarified Donation Terms analyzed, 1088 are of the year 2011 and thereafter suffered a decrease in the number of donations (only 15 in 2012, 27 in 2013 and 42 in 2014). As for donors, 57.41% are women, 73.81% Caucasian and ages were kept between 40 and 50 years (37.13%).					<B>Conclusion</B>: We conclude that the HTB-PUCPR retains its functionality according to the goals that have been proposed for their institution.				</P>
