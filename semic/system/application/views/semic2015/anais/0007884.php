			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A fim de impedir práticas criminosas e contaminações cruzadas na utilização de dentes extraídos, foram instituídos os Banco de Dentes Humanos (BDH), que preservam a legalidade da pesquisa e a saúde dos estudantes. No curso de Odontologia, muitas disciplinas utilizam dentes extraídos nas práticas clínicas e laboratoriais, obrigando os acadêmicos a providenciarem os dentes, o que torna necessária a implantação de BDH, que deve ser organizado de modo a suprir essa demanda.					<B>Objetivo</B>: O objetivo foi analisar a funcionalidade do BDH da PUCPR.					<B>Metodologia</B>: Realizou-se uma pesquisa descritiva de abordagem quantitativa. Os dados foram coletados no material que o banco armazena (dentes) e em todos os documentos lá existentes, desde a sua instituição em 2006 até o ano de 2014.					<B>Resultados</B>: Os resultados obtidos mostraram que 63,35 % dos projetos que utilizaram dentes, ocorreram entre os anos de 2006 a 2008. Que foram fornecidos 2081dentes para 80% de projetos da PUCPR, cuja prevalência foi de pesquisadores do sexo masculino, e a especialidade dentística. Estagiaram no banco 54 alunos. Nos 1.134 Termos de Doação Esclarecida analisados, 1088 são do ano de 2011, tendo a partir daí sofrido um decréscimo no número de doações, (2012 apenas 15; 2013 foram 27 e 2014, 42 doações). Quanto aos doadores, 57,41% são de mulheres, 73,81% leucodermas cujas idades se mantiveram entre 40 e 50 anos (37,13%).					<B>Conclusões</B>: Conclui-se que o BDH da PUCPR mantém a sua funcionalidade de acordo com os objetivos que foram propostos para a sua instituição.				</P>
			</div><B>Palavras-chave</B>: Dentes. Banco de dentes humanos. Doação de dentes. Ética.			<BR>
