			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Biofilmes são diretamente vinculados à manifestação de virulência microbiana.					<B>Objetivo</B>: Neste estudo, se buscou avaliar, ainda que de forma preliminar, a influência exercida por substitutos de açúcar (adoçantes) no status metabólico de alguns microrganismos de importância odontológica.					<B>Metodologia</B>: Para tanto, foram utilizados a levedura Candida albicans SC5314, a bactéria cariogênica Streptococcus mutans ATCC®25175™ e a bactéria colonizadora primária (iniciadora de placa dental) S. sanguinis ATCC®10556™. Esses microrganismos foram crescidos como biofilmes na presença de aspartame, ciclamato de sódio, sacarina e xilitol, em concentrações próximas àquelas observadas na saliva de usuários. Após 72 h, os biofilmes foram avaliados quanto às suas condições metabólicas pelo teste de redução de MTT sem ativadores.					<B>Resultados</B>: Foi verificado que o S. mutans ATCC®25175™ apresenta baixa atividade metabólica quando da ausência de açúcares fermentáveis, mas que pode se mostrar proporcionalmente aumentada quando do desafio com aspartame e ciclamato. Contudo, sacarina pode reduzir a atividade metabólica de C. albicans SC5314 e xilitol pode fazer o mesmo com S. sanguinis ATCC®10556™.					<B>Conclusões</B>: Esses resultados podem colaborar não entendimento da fisiologia microbiana na presença dos xenobióticos ensaiados.				</P>
			</div><B>Palavras-chave</B>: Adoçantes. biofilmes. atividade metabólica			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Biofilms are directly linked to the expression of microbial virulence.					<B>Objectives</B>: In this study, we evaluated, even in a preliminary way, the influence exerted by sugar substitutes (sweeteners) in the metabolic status of some microorganisms of dental importance.					<B>Methods</B>: Therefore, we used the yeast Candida albicans SC5314, the cariogenic bacterium Streptococcus mutans ATCC®25175 ™ and the early colonizer bacterium (dental plaque initiator) S. sanguinis ATCC®10556 ™. These microorganisms were grown as biofilms in the presence of aspartame, sodium cyclamate, saccharin and xylitol in concentrations close to those observed in the saliva of users. After 72 h, biofilms were evaluated for their metabolic conditions by MTT reduction test without activators.					<B>Results</B>: It was found that S. mutans ATCC®25175 ™ has low metabolic activity when the absence of fermentable sugars, but may show proportionately increased when the challenge with aspartame and cyclamate. However, sugar can reduce the metabolic activity of C. albicans SC5314 and xylitol can do the same with S. sanguinis ATCC®10556 ™.					<B>Conclusion</B>: These results can contribute no understanding of microbial physiology in the presence of xenobiotics tested.				</P>
			</div><B>Keywords</B>: Sweetners. biofilms. metabolic activity</td>
