			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Os direitos fundamentais ganharam especial guarida com o advento da Constituição de 1988, que abrangeu em larga escala o rol de liberdades e garantias, tanto individuais quanto coletivas. Todavia, mais de 25 anos após a promulgação da Constituição da República, observa-se que a lista de direitos fundamentais constitucionais ainda não alcançou um satisfatório grau de efetividade. Diante desse incômodo fático, o presente trabalho guarda foco para a problemática relativa à efetivação desses direitos constitucionais, com especial atenção ao direito à educação, como alavanca para o desenvolvimento das potencialidades humanas.					<B>Objetivo</B>: Compreender o contexto problemático que permeia a efetivação do direito à educação, como também verificar a validade do argumento da reserva do possível, quando contraposto à realização do mínimo existencial.					<B>Metodologia</B>: A pesquisa se valeu do método dedutivo, eis que utilizou-se, em um primeiro momento, de conceitos teóricos e abstratos, para, posteriormente, verificar a aplicabilidade e validade da argumentação amparada nas teorias estudadas e utilizada no caso concreto.					<B>Resultados</B>: A partir da consulta a doutrinas e jurisprudência, discorreu-se sobre a reserva do possível como o orçamento que o Estado dispõe para realização de políticas públicas, contraposto ao mínimo existencial, reconhecido como um núcleo de direitos mínimos que cada ser humano necessita para obter uma vida digna.					<B>Conclusões</B>: Nessa contraposição dialética, os objetivos gerais da República, estabelecidos pela Lei Maior em seu art. 3º, bem como a teoria da força normativa da Constituição, indicam que o Estado está vinculado à concretização do mínimo existencial, devendo guardar máxima prioridade para a efetivação desse núcleo de direitos.				</P>
			</div><B>Palavras-chave</B>: DIREITO FUNDAMENTAL À EDUCAÇÃO. MÍNIMO EXISTENCIAL. RESERVA DO POSSÍVEL.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The fundamental rights got privileged position within the 1988 Brazilian Constitution, which embraced a large amount of individual and collective freedoms and guaranties. However, after more than 25 years from its promulgation, the 
 constitutional fundamental rights have not yet achieved a satisfactory effectiveness level. Before this factual nuisance, the present work focus on the problematic relating to the effectiveness of those rights, especially the ones that assure education to children and teenagers as a path leading to development of human potential.					<B>Objectives</B>: Understand the problematic context that permeates the effectiveness of the right to education, as well as verifying the validity of the argument of the reserve of contingencies, when opposed to the realization of the existential minimum.					<B>Methods</B>: The research was based on the deductive method, as in a first moment, theoretical and abstract concepts were used, in order to, afterwards, verify the applicability and validity of the argumentation supported in the studied theories and its use in a concrete case.					<B>Results</B>: In this direction, based on legal doctrine and jurisprudence, it has been written about reserve of contingencies; the budget State has to execute the public policy, counter posed to the existential minimum, a minimum of rights that humans need to live with dignity					<B>Conclusion</B>: In this dialectic, the general objectives of Federative Republic of Brazil and the theory of normative constitutional power indicate that the State is bound to existential minimum achievement, having these nuclear rights priority on its substantiation.				</P>
