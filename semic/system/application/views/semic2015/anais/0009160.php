			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Celebrity endorsement is a recurrent comunication strategy in Brasil.					<B>Objectives</B>: The objective of this study was to investigate in an exploratory way the existence of a pattern on the use of the communication strategy based on celebrity endorsement, considering the largest advertisers in the Brazilian market, as advertising spending reports released by IBOPE last year.					<B>Methods</B>: Content analysis was performed based on 115 campaign ads without repetition, eight of the top ten advertisers in 2014, as follows: Via Retail (Casas Bahia), Unilever Brazil (Lifebuoy), Box (GFC), Ambev (Skol) Hypermarcas (Neo Quimica), Petrobras (GFC), Telefonica (VIVO) and BCO Bank of Brazil (GFC). Standards were created to categorize the ads and celebrity profile.					<B>Results</B>: The results show the temporality of endorsement usage. Considering 2014, World Cup year, the sport theme was taken up by at least half of the top advertisers and the use of sports celebrities, especially football, was significant. Another highlight is related to the celebrity expertise based on experience. Moreover, excessive repeated use of the same celebrities was recorded for different brands. In general, major celebrities endorse three brands at the same time, reaching up to a dozen. This suggests major criticism about the effectiveness of this strategy based on the exposure of competing messages.					<B>Conclusion</B>: 				</P>
