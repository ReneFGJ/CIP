			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O uso de endosso de celebridades como estratégia de comunicação tem sido prática recorrente no Brasil.					<B>Objetivo</B>: O objetivo deste estudo foi investigar de forma exploratória a existência de um padrão sobre o uso da estratégia de comunicação apoiada no endosso de celebridades, considerando as maiores empresas anunciantes no mercado brasileiro, conforme relatórios de investimento publicitário divulgado pelo IBOPE no último ano.					<B>Metodologia</B>: Foi realizada análise de conteúdo com base em 115 anúncios sem repetição de campanha, de oito dos dez maiores anunciantes publicitários em 2014, sendo: Via Varejo (Casas Bahia), Unilever Brasil (Kibon), Caixa (GFC), Ambev (Skol), Hypermarcas (Neo Quimica), Petrobras (GFC), Telefônica (VIVO) e BCO Banco do Brasil (GFC). Foram criados padrões para categorizar os anúncios conforme presença e perfil de celebridades.					<B>Resultados</B>: Os resultados evidenciam a temporalidade dos perfis de endosso. Em ano de Copa do Mundo, o tema esporte foi absorvido por, pelo menos, metade dos principais anunciantes e o uso de celebridades esportivas, especialmente futebol, foi expressiva. Outro elemento interessante é a característica de expertise com base na experiência. Por outro lado, foi registrada repetição excessiva do uso das mesmas celebridades para diferentes marcas. Em geral, as principais celebridades endossam 3 marcas ao mesmo tempo, podendo chegar até uma dezena. Tal fato sugere maior crítica sobre a eficiência desta estratégia considerando a exposição de mensagens concorrentes.					<B>Conclusões</B>: 				</P>
