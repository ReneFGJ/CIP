			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Os objetivos do preparo químico mecânico dos canais radiculares são eliminar tecido pulpar e microrganismos, além de dar forma ao canal, promovendo condições para uma obturação de qualidade.					<B>Objetivo</B>: Avaliar in vitro o efeito do uso do hidróxido de cálcio, como medicação intracanal, no selamento apical dos cimentos obturadores AH Plus e Fillapex.
A hipótese nula testada é de que o hidróxido de cálcio não influencia no selamento apical dos dois cimentos obturadores.					<B>Metodologia</B>: Sessenta e quatro pré-molares inferiores extraídos foram preparados com sistema Profile .04 e divididos em quatro grupos. Dois grupos (1 e 3) receberam medicação intracanal com hidróxido de cálcio por 15 dias. Os cimentos foram manipulados, e os canais obturados com guta-percha e AH Plus (grupos 1 e 2), ou MTA Fillapex (grupos 3 e 4). O método de filtração de fluido foi utilizado para medir a infiltração permitida pelos cimentos obturadores. O volume de fluido foi calculado pela observação do movimento da bolha de ar e expresso em [e]#956;L/min.10psi. As medidas foram tomadas em intervalos de dois minutos, em um período de 8 minutos. A média desses valores foi calculada para cada espécime.Os dados foram submetidos a análise estatistica utilizando ANOVA, seguido do teste de Tukey para múltiplas comparações.					<B>Resultados</B>: Os resultados mostram que o volume de infiltração permitido pelo cimento AH Plus com e sem hidróxido de cálcio não diferiu. Já o cimento MTA Fillapex com hidróxido de cálcio apresentou menor infiltração, diferindo estatisticamente do MTA Fillapex sem hidróxido de cálcio (p=0,0001).					<B>Conclusões</B>: O uso da medicação intracanal prévia promoveu melhora na capacidade de selamento do cimento MTA Fillapex.				</P>
			</div><B>Palavras-chave</B>: Filtração de fluido. cimentos endodonticos			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: To evaluate the effect of the calcium hydroxide dressing in leakage of two endodontic sealers.					<B>Objectives</B>: To evaluate the effect of the calcium hydroxide dressing in leakage of two endodontic sealers.					<B>Methods</B>: Sixty four mandibular premolars with a single root canal were prepared with rotary Profile .04 instruments and divided into four groups. In two groups (1 and 3), a CH dressing was placed for 15 days. The sealers were mixed and root canals were obturated with gutta-percha and AHPlus (groups 1 and 2) or MTA Fillapex (groups 3 and 4). The roots were placed in a fluid filtration model and the leakage was accessed.  Statistical analysis was performed using  ANOVA and Tukey.					<B>Results</B>: The results showed that is no difference in the leakage volume for AHPlus sealer with or without calcium hydroxide.  MTA Fillapex with calcium hydroxide showed less leakage values that without calcium hydroxide.					<B>Conclusion</B>: MTA Fillapex improved leakage in groups that calcium hidroxyde are used.				</P>
