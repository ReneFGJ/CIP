			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O uso de revisões sistemáticas caracteriza-se por um grande rigor na descrição do questionamento e na aplicação de análises qualitativas e quantitativas, com intuito de facilitar e identificar estudos de maior relevância. Principalmente quando da aplicação de um método clínico ou de uma nova ferramenta diagnóstica ou terapêutica, o uso da meta-análise é de grande valor dentro de um grande montante de publicações controversas e com valoração variável.					<B>Objetivo</B>: Com o intuito de aplicar uma meta-análise na literatura que aborda o uso da fitoterapia na medicina veterinária principalmente no tema “mastite” realizou-se uma revisão sistemática.					<B>Metodologia</B>: Foi possível estabelecer uma estratégia de análise, a seleção de critérios de inclusão e de exclusão além dos termos de busca. Analisando-se 14 bancos de dados obteve-se ao final 91 publicações relevantes para o tema no período de 1982 a 2015.					<B>Resultados</B>: Porém somente 16 publicações abordaram a mastite em animais juntamente com a fitoterapia. As plantas mais citadas foram: [e]lt;I[e]rt;Baccharis dracunculifolia[e]lt;/I[e]rt;, Mimosa [e]lt;I[e]rt;Tenuifolia[e]lt;/I[e]rt;, [e]lt;I[e]rt;Ocimum basilicum[e]lt;/I[e]rt;, [e]lt;I[e]rt;Salvia officinalis[e]lt;/I[e]rt; e o [e]lt;I[e]rt;Thymus vulgaris[e]lt;/I[e]rt;. Os compostos das plantas mais citados foram os flavonoides e os óleos essenciais. Não foi possível fazer em sua plenitude a extração de todos os dados relevantes das publicações, assim como a análise crítica e interpretação destes dados, por limitação do tempo.					<B>Conclusões</B>: Conclui-se que o pequeno número de meta-análises existentes na literatura médico veterinária deve-se em parte à necessidade de tempo considerável para a sua confecção.				</P>
			</div><B>Palavras-chave</B>: Meta-análise. Medicina veterinária. Fitoterapia. Mastite. Revisão sistematizada			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The use of systematic reviews is characterized by a great rigor in the description of the questioning and applying qualitative and quantitative analysis, with a view to facilitating and identifying the most relevant studies. Especially when the application of a clinical method or a new diagnostic or therapeutic tool is pursued, the use of meta-analysis is valuable to distinguish a large amount of controversial publications with variable ratings.					<B>Objectives</B>: In order to apply a meta-analysis in the literature that addresses the use of phytotherapy in veterinary medicine especially on the theme "mastitis" a  systematic review was carried out.					<B>Methods</B>: It was possible to establish a strategy of analysis, the selection criteria for inclusion and exclusion in addition to the search terms. Analyzing 14 databases we obtained at the end 91 publications relevant to the topic in the 1982-2015 period.					<B>Results</B>: But only 16 publications addressed mastitis in animals along with phytotherapy. The plants most cited were: [e]lt;I[e]rt;Baccharis dracunculifolia[e]lt;/I[e]rt;, Mimosa [e]lt;I[e]rt;Tenuifolia[e]lt;/I[e]rt;, [e]lt;I[e]rt;Ocimum basilicum[e]lt;/I[e]rt;, [e]lt;I[e]rt;Salvia officinalis[e]lt;/I[e]rt; e o [e]lt;I[e]rt;Thymus vulgaris[e]lt;/I[e]rt;. The compounds of the mentioned plants were flavonoids and essential oils. It was not possible to do in its fullness the extraction of all relevant data of publications as well as critical analysis and interpretation of these data, due to limitation of time.					<B>Conclusion</B>: We conclude that the small number of meta-analyzes in veterinary medical literature is in part  due to the considerable need for time for its preparation.				</P>
			</div><B>Keywords</B>: Meta-analysis. Veterinary medicine. Phytotherapy. Mastitis. Systematic review.</td>
