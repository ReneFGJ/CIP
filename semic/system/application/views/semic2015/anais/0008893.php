			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O presente trabalho propõe-se a retratar o panorama doutrinário
acerca da disciplina da morte dignamente no Brasil, apresentando para tanto a
morte atrelada às demais áreas do conhecimento, os direitos fundamentais à
vida e à morte, a disciplina dos institutos jurídicos que guarnecem a busca por
um morrer com dignidade.					<B>Objetivo</B>: Analisar os principais aspectos teóricos
sobre o direito à morte digna desenvolvidos na doutrina brasileira, os direitos
fundamentais do homem e verificação se o direito à morte digna enquadra-se
enquanto direito fundamental.					<B>Metodologia</B>: Por meio da revisão de bibliografias que
desenvolveram e dedicaram especial atenção ao tema, na colheita de livros e
artigos e em seus fichamentos por resumos.					<B>Resultados</B>: Tem-se inegável a
tutela do direito à vida e em decorrência desse direito, um direito a morrer com
dignidade, pois não seria justificável que ao final de sua vida o paciente em
estado terminal fosse desprovido daquilo que norteia o direito, a dignidade da
pessoa humana. O tema percorre os principais institutos jurídicos que abarcam a
possibilidade de proporcionar aos pacientes terminais que deixem a vida com
dignidade e respeito a sua autonomia individual, quais sejam, a eutanásia, a
distanásia e a ortotanásia, que por meio da medicina atuam nos pacientes com
doenças crônicas ou degenerativas, ou pacientes em estado terminal, atrelados
a utilização dos cuidados paliativos e aos testamentos vitais em disposições de
última vontade.					<B>Conclusões</B>: Conclui-se pela ortotanásia, aliada aos cuidados
paliativos e aos testamentos vitais, como a forma mais adequada de oferecer
dignidade aos pacientes destituídos de tratamentos curativos capazes de
proporcionar recuperação ou cura, de modo que evite a não abreviação nem
prolongamento desmedido da vida e da doença, proporcionando ao paciente a
atenção e o cuidado que necessita, aliado ao respeito à sua vontade em se
submeter ou não a tratamentos extraordinários, sendo esta escolha decorrente
de sua autonomia individual.				</P>
			</div><B>Palavras-chave</B>: Dignidade. morte. autonomia			<BR>
