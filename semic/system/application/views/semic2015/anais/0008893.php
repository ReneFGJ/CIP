				<P>
					<B>Introduction</B>: This paper proposes to portray the panorama doctrinal
about the subject of death with dignity in Brazil, performing for both
Death linked to other areas of knowledge, the fundamental rights to
life and death, the discipline of legal institutions which line the search for
one die with dignity.					<B>Objectives</B>: Analyze the main theoretical aspects
on the right to dignified death developed in the Brazilian doctrine, rights
fundamental human verification and the right to a dignified death fits
as a fundamental right.					<B>Methods</B>: Through the bibliographic review that
They developed and paid special attention to the issue, the harvest of books and
articles and their summaries by fichamentos					<B>Results</B>: Has undeniable
protection of the right to life and as a result of that right, a right to die with
dignity, it would not be justifiable to the end of his life the patient
terminal state was devoid of what guides the rights, the dignity of
human person. The theme runs through the main legal institutions that cover the
possibility of providing terminal patients to leave life with
dignity and respect for their individual autonomy, namely euthanasia,
dysthanasia and orthothanasia, which act by means of medicine in patients with
chronic or degenerative diseases, or terminally ill patients, trailers
the use of palliative care and living wills in provisions
last will.					<B>Conclusion</B>: The results confirmed the orthothanasia, combined with care
palliative and living wills, as the most appropriate way to provide
dignity to patients devoid of curative treatments capable of
porporcionar recovery or curing, so as not to prevent or Abbreviation
excessive prolongation of life and disease, helping the patient to
attention and the care you need, coupled with respect for their willingness to be
submit or not the extraordinary treatments, which is due choice
their individual autonomy.				</P>
