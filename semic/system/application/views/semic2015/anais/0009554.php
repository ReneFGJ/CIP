			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Esta pesquisa busca refletir os desafios encontrados durante o período de caminhada do povo no deserto, a lamentação de Moisés que se encontra cansado pelo peso de conduzir o povo (Nm 11, 16-30). O Senhor pede que ele reúna setenta anciãos para dividir com ele a tarefa de conduzir o povo, assim eles levarão com Moisés a carga e ele não mais levará sozinho (v. 17).					<B>Objetivo</B>: 1) Determinar a causa da crise e as queixas do povo; 2) Destacar o papel de Moisés e dos setenta anciãos diante das queixas; 3) Apresentar os avanços e vantagens da democratização do poder de Moisés e o significado do dito profético de Moisés “Oxalá que todo povo fosse profeta”; 4 Elaborar uma reflexão hermenêutica-teológica, contextualizada da perícope no contexto do Livro dos Números com elementos de atualização para os leitores de hoje					<B>Metodologia</B>: Leitura atenta do texto e comparação com outros discursos do Pentateuco; delimitação e contexto histórico; análise literária; destaque das ideias teológicas chaves e elementos de atualização					<B>Resultados</B>: O enredo de conflito contra a autoridade mosaica por falta de variedade de alimento pode descrever-se com destaque para: a escuta de Moises (v.10); o lamento mosaico e sua queixa (v. 15); a rejeição do Senhor (v. 20); A escolha dos setenta anciãos para ajudar Moisés na condução do povo, fazendo assim a partilha do poder (v. 24); A ausência de Eldad e Medad, sendo que mesmo assim eles profetizaram, mostrando o conflito entre o evento novo e a antiga fé (v. 26); a adesão de Moisés a ação do Espírito do Senhor a ponto de dizer: oxalá todo povo fosse profeta (v. 29) Deus manda as codornizes, indicação ao atendimento divino da queixa do povo e intercessão de Moisés (v. 31-33).					<B>Conclusões</B>: Faltava
no povo o sentido da vida, do caminhar com Deus. O murmúrio demonstra a
insatisfação de não possuir o mesmo projeto que Deus. A condução da comunidade
sozinho é pesada, mas quando partilhada fica leve, o mesmo acontece com a
profecia e o Espírito de Deus não se pode aprisioná-los a uns poucos é preciso
deixá-lo realizar a sua missão.				</P>
			</div><B>Palavras-chave</B>: Moisés. Anciãos. Poder. Liderança. Conflito.			<BR>
