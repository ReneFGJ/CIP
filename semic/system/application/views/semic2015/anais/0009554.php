			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: This research seeks to reflect the challenges encountered during the walk period of the people in the desert, mourning Moses who is tired by the burden of leading the people (Num 11, 16-30). The Lord asks him to gather seventy elders to share with him the task of leading the people, so they will lead the charge to Moses and he will take no longer alone (v. 17)					<B>Objectives</B>: 1) To determine the cause of the crisis and the complaints of the people; 2) Emphasize the role of Moses and the seventy elders on complaints; 3) Present the progress and democratization of the advantages of the power of Moses and the meaning of the prophetic saying of Moses "Would that all the people were prophets"; 4 Develop a hermeneutical and theological reflection, contextualized the text in the context of the Book of Numbers with updated elements for today´s readers.					<B>Methods</B>: attentive reading of the text and compared to other speeches of the Pentateuch; delimitation and historical context; literary analysis; highlight the key theological ideas and update elements.					<B>Results</B>: The plot of conflict against the Mosaic authority for lack of variety of food can be described with emphasis on: listening Moses (v.10); the lament mosaic and his complaint (v. 15); the rejection of the Lord (v. 20); The choice of the seventy elders to assist Moses in the conduct of the people, thus making the sharing of power (v. 24); The absence of Eldad and Medad, and yet they prophesied, showing the conflict between the new event and the ancient faith (v. 26); Moses the accession of the action of the Spirit of the Lord as to say: may all the people were prophets (v. 29) God sends quail, indicating the divine service of the people´s complaints and intercession of Moses (v 31-33.)					<B>Conclusion</B>: Missing the people the meaning of life, the walk with God. The murmur demonstrates the dissatisfaction of not having the same design as God. The community driving alone is heavy, but when it gets shared light, so does the prophecy and God´s Spirit can not bind them to a few it takes
let him carry out his mission.				</P>
