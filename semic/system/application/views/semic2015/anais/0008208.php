			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O Bullying é um fenômeno muito antigo, que está presente em todas as escolas do nosso país, mas que só passou a ser estudado no Brasil nas últimas décadas. Os profissionais da educação devem possuir conhecimento, pois ele, ocorre frequentemente nas escolas através das relações interpessoais dos alunos.					<B>Objetivo</B>: O presente estudo teve como objetivos; identificar os comportamentos dos alunos nas escolas pesquisadas; verificar a incidência e os principais locais onde ocorrem os casos de bullying nas escolas.					<B>Metodologia</B>: Participaram do estudo 60 alunos do ensino fundamental de duas escolas públicas do Município de São José dos Pinhais.O estudo foi de caráter quantitativo. O instrumento utilizado foi um questionário com questões fechadas em duas escolas municipais do município de São José dos Pinhais.					<B>Resultados</B>: Os resultados apontaram que mais de um quarto dos alunos da escola já sofreram bullying. Os locais em que os alunos mais sofreram bullying nas escolas foram  as salas de aula e o recreio.					<B>Conclusões</B>: Assim concluiu-se que há uma frequência significativa de ocorrências de bullying nas escolas pesquisadas e que o local mais vulnerável à ocorrências de bullying nessas escolas é a sala de aula.				</P>
			</div><B>Palavras-chave</B>: Bullying. Criança. Escola			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Bullying is a very old phenomenon, which is present in all schools of our country, but that only began to be studied in Brazil in recent decades. Education professionals must possess knowledge, as it often occurs in schools through the interpersonal relationships of the students.					<B>Objectives</B>: This study aimed; identify the behaviors of students in schools surveyed; check the incidence and the main sites where there cases of bullying in schools.					<B>Methods</B>: Study participants were 60 elementary school students from two public schools in São José dos Pinhais.O study was quantitative character. The instrument was a questionnaire with closed questions in two public schools in São José dos Pinhais.					<B>Results</B>: The results showed that more than a quarter of the students have suffered bullying. Places where more students suffered bullying in schools were the classrooms and the playground.					<B>Conclusion</B>: Thus it was concluded that there is a significant frequency of bullying incidents in schools surveyed and the most vulnerable place to bullying occurrences in these schools is the classroom.				</P>
