			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Com o grande crescimento dos bancos de dados dos serviços de saúde e para utilizá-los de maneira relevante para as ciências da saúde são necessários métodos que identificam padrões específicos entre eles. Para tanto utilizamos como ferramenta a mineração de dados, que consiste justamente na extração de informações em grandes bases de dados. Diversos métodos de mineração foram desenvolvidos.					<B>Objetivo</B>: o objetivo deste trabalho foi identificar o melhor logaritmo para empregar em serviços de saúde que trouxesse mais benefícios ao paciente, prevenindo doenças e consequentemente diminuindo custos ao sistema.					<B>Metodologia</B>: Inicialmente, foi realizada uma revisão de literatura, com artigos disponíveis na Biblioteca Virtual da PUCPR, bem como em outros bancos de dados, para atualização referente aos algoritmos para reconhecimento de padrões para análise de  risco em questões de saúde.					<B>Resultados</B>: Foram encontrados os seguintes temas: mineração temporal; representação das sequências temporais; medidas de similaridade para sequências; operações de mineração; regras calendric; padrão de mineração de dados com reusable time ontology; mineração de dados médicos para padrões causais e temporais.					<B>Conclusões</B>: Concluiu-se que para mineração de dados em eventos assistenciais na saúde suplementar há necessidade de associação de várias técnicas, em especial as relacionadas a associação por similaridade e associação temporal.				</P>
			</div><B>Palavras-chave</B>: Algoritimos. Padrões. Custos assistenciais.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: With the large growth of the databases of health services and to use them in a relevant way to the health sciences are needed methods that identify specific patterns between them. For such use as a data mining tool, which consists exactly in the extraction of information in large databases. Several mining methods were developed					<B>Objectives</B>: The aim of this study was to identify the best logarithm for use in health services to bring more benefits to the patient, preventing diseases and thus reducing costs to the system.					<B>Methods</B>: Initially, a literature review was conducted, with articles available in the Virtual Library of PUCPR, as well as in other databases, to update related to algorithms for pattern recognition for risk analysis on health issues.					<B>Results</B>: The following items were found: temporal mining; representation of temporal sequences; similarity measures for sequences; mining operations; calendric rules; standard data mining with reusable ontology team; Mining medical data for causal and temporal patterns.					<B>Conclusion</B>: We concluded that to evaluate health care process with data mining it is necessary a combination of techniques, specially those related to similarities association and temporal association.				</P>
			</div><B>Keywords</B>: Algoritms. Pattern. Assistential costs.</td>
