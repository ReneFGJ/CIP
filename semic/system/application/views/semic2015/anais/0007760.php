			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O debate bioético no mundo dominado pela ciência e pela técnica deve levar em conta a especificidade antropológica do ser humano.  A fim de colaborar nessa reflexão, a CNBB produziu, em 2010, um estudo: Questões de Bioética, com colaboração de profissionais de diversos campos do saber.					<B>Objetivo</B>: O que se deseja no presente trabalho é apresentar uma breve análise da morte como sentido para a vida no documento da CNBB 98 – Questões de Bioética, à luz da escatologia e da bioética. Assim, o que se intenta com esse estudo é apontar caminhos que auxiliem para a solução dos problemas que envolvem a dignidade do ser humano.					<B>Metodologia</B>: A metodologia deste projeto de pesquisa é qualitativo-bibliográfico.					<B>Resultados</B>: Mais do que mero ser biológico, o homem é também espírito que se abre ao eterno. A visão da eternidade futura ilumina a vida do homem no tempo histórico e orienta suas decisões no aqui e agora. A morte, entendida como limiar da vida futura, dá sentido à existência no tempo presente e forja uma nova matriz ética, baseada no respeito à dignidade de todo ser humano.					<B>Conclusões</B>: A realidade resultante das escolhas éticas feitas no presente da vida são prefiguração do que se viverá na vida plena e glorificada na eternidade.				</P>
			</div><B>Palavras-chave</B>: Teologia. teologia moral. escatologia. morte. bioética			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The bioethical debate in a world dominated by science and technique should take into account the anthropological specificity of the human being. In order to collaborate in this reflection, CNBB produced in 2010 a study: Bioethics Issues, in collaboration with professionals from various fields of knowledge.					<B>Objectives</B>: The aim of this paper is to present a brief analysis of death as meaning to life in the document of CNBB 98 - Bioethics Issues in the light of eschatology and bioethics. So the study attempts to point out ways to help solving problems that involve the dignity of the human being.					<B>Methods</B>: The methodology of this research project is qualitative-bibliographical.					<B>Results</B>: The vision of future eternity illuminates the life of man in historical time and guides its decisions here and now. Death, understood as the threshold of the afterlife, gives meaning to existence at the present time and forges a new ethical matrix based on respect for the dignity of every human being.					<B>Conclusion</B>: The resulting reality of the ethical choices made in this life are prefiguration of what will be lived in full and glorified life in eternity.				</P>
			</div><B>Keywords</B>: Theology. moral theology. eschatology. death. bioethics</td>
