			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O vazamento de linha de grampos com fistulização consiste em uma complicação pós-operatória de cirurgia bariátrica e pode ocorrer em até 7% dos casos. O quadro clínico apresentado pelo paciente obeso é silencioso e atípico, o que exige alto grau de suspeição diagnostica e agilidade para intervenção.A Endoscopia Digestiva Alta (EDA) é o procedimento de escolha para o diagnóstico e a terapêutica das complicações.					<B>Objetivo</B>: O objetivo foi relatar o caso de uma paciente que apresentou fistula gástrica como complicação pós-bariatrica, bem como as intervenções e acompanhamentos via endoscópica realizados.					<B>Metodologia</B>: Apresentou-se o caso de uma mulher de 32 anos que foi submetida à gastroplastia por Sleeve que desenvolveu como complicação a fistulização gástrica, diagnosticada após queda do quadro geral da paciente com necessidade de reintervenção cirúrgica sem sucesso. Após estabilização de quadro de sepse de foco abdominal com drenagem de coleção de parede e colocação de dreno, procedida a EDA com realização de septotomia do orifício fistuloso e acompanhamento com subsequentes procedimentos endoscópicos para avaliação da evolução da fístula.					<B>Resultados</B>: No momento, continua em observação e atenta-se para necessidade de nova intervenção cirúrgica para tentativa de fechamento do orifício remanescente.					<B>Conclusões</B>: Conclui-se que a fístula decorrente de complicação pós-bariátrica carece de padronização de tratamento e conduta. Possui, atualmente, o procedimento de Endoscopia Digestiva Alta como principal aliado para diagnóstico, acompanhamento da evolução e intervenção terapêutica.				</P>
			</div><B>Palavras-chave</B>: Cirurgia bariátrica. complicações. fístula. endoscopia digestiva alta. septotomia			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The gastric leak in the staple line with fistula is a post-operatory complication in bariatric surgery that can come about 7% of patients. Obese patient has a silent and atypical clinic, which demands a higher diagnostic suspicion and agility to intervention. Upper Digestive Endoscopy (UDE) is the first choice as a procedure to diagnosis and therapeutic for complications.					<B>Objectives</B>: The main objective was to relate the case of a patient which presented gastric fistula as a post-bariatric complication, as well as the intervention and the management with endoscopic procedures.					<B>Methods</B>: A case of a 32 years old women who was submited to Sleeve gastroplasty and become with gastric fistula as complication has been presented. The diagnostic has been done after worsening of overall health status, with the pressing need of a new surgical intervention, with no success. After sepsis health status stabilization with abdominal focus by wall collection drainage and positioning of drain, the UDE has been proceeded with the accomplishment of septotomy of fistulous hole and follow-up by subsequent endoscopic procedures to fistula`s evolution evaluation.					<B>Results</B>: Nowadays, patient still in observation and comes up to the necessity of a new surgical intervention for the left over hole closure.					<B>Conclusion</B>: It can be concluded that the treatment and management of fistula arising from post-bariatric complication lacks of uniformity. The Upper Digestive Endoscopic procedure is main ally to monitoring the evolution and to therapeutic intervention.				</P>
			</div><B>Keywords</B>: Bariatric surgery. complications. fistula. upper digestive endoscopy. septotomy</td>
