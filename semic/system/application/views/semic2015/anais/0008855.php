			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A manutenção da posição ereta trata-se de uma tarefa complexa que envolve um intricado relacionamento de informações tanto sensoriais como motoras. Desta forma se torna necessário a criação de um protocolo que integre diferentes formas de avaliação biomecânica, assim criando a possibilidade de controlar todas as variáveis que possam interferir na manutenção do equilíbrio após uma perturbação do mesmo.					<B>Objetivo</B>: Portanto, o presente estudo tem como proposta criar um protocolo afim de analisar os mecanismos de manutenção da postura em indivíduos hígidos durante perturbação do equilíbrio.					<B>Metodologia</B>: O sistema biomecânico proposto consiste em uma associação de alguns sistemas já existentes na biomecânica, para o presente estudo os sistemas utilizados para a criação do protocolo foram: Eletromiógrafo de quatro canais da marca EMG System com eletrodos de superfície; Plataforma de Força OR6-7AMTI (Advanced Mechanical Thechnologies); Plataforma móvel; Cinemetria Sistema de captura de imagens (VICON) Computador compatível com o sistema de aquisição de dados; Material para tricotomia e assepsia da região de posicionamento dos eletrodos de EMG.					<B>Resultados</B>: Foi possível elaborar um protocolo envolvendo todos os parâmetros necessários para avaliar uma pessoa em situação de desequilíbrio.					<B>Conclusões</B>: Como forma de tentar avaliar quais são as estratégias de manutenção da postura perante perturbação do equilíbrio, concluímos que o protocolo proposto com variados componentes biomecânicos é capaz de analisar parâmetros de atividade muscular envolvidos no equilíbrio.				</P>
			</div><B>Palavras-chave</B>: Equilíbrio. Avaliação. Biomecânica.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Maintaining the upright position it is a complex task that involves the relationship of both sensory information such as motor. Thus, it becomes necessary to create a protocol that integrates different forms of biomechanical evaluation, in order to controlling all the variables that can interfere on maintaining balance after a sudden perturbation task.					<B>Objectives</B>: To create a protocol in order to analyze the maintenance posture mechanisms in healthy individuals during sudden perturbation.					<B>Methods</B>: The proposed biomechanical system consists on: electromyographic system with four channels with surface electrodes; force platform (AMTI Advanced Mechanical thechnologies); mobile platform; and capture system (VICON).					<B>Results</B>: It was possible to create a new balance protocol including all the biomechanics features necessary to evaluate a sudden perturbation test.					<B>Conclusion</B>: In order to assess the maintenance strategies of the body during sudden perturbation, we conclude that the protocol proposed with biomechanical components is able to analyze muscle activity parameters involved in balance.				</P>
