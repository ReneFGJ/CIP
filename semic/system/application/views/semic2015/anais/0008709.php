			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A presente pesquisa é uma oportunidade de refletir sobre um dos aspectos da gestão educacional, no que diz respeito ao gerenciamento dos recursos financeiros da educação básica, e o Fundo de Manutenção e Desenvolvimento da Educação Básica e de Valorização dos Profissionais da Educação, conhecer suas características e aplicação, bem com gerenciamento dos recursos financeiros.					<B>Objetivo</B>: A pesquisa objetiva caracterizar o FUNDEB, investigar sua efetivação e verificar como estão sendo utilizados os recursos financeiros destinados à educação.					<B>Metodologia</B>: A pesquisa adotada é qualitativa, tendo como principal instrumento a análise documental, com o estudo realizado por meio da consulta aos sites oficiais, o que possibilitou a verificação da aplicação dos recursos financeiros destinados à educação básica.					<B>Resultados</B>: O FUNDEB foi criado pelo governo para atingir as suas metas do Plano Nacional de Educação, pelo qual cada estado e o Distrito Federal têm um fundo que deve ser aplicado na educação básica. Os recursos são oriundos dos impostos e transferências dos estados, do Distrito Federal e dos municípios, conforme artigo 68 da Lei de Diretrizes e Bases da Educação Nacional nº 9.394/96. Os recursos públicos são originários de receita de impostos próprios da União, dos Estados, do Distrito Federal e dos Municípios; receita de transferências constitucionais e outras transferências; receita do salário-educação e de outras contribuições sociais; receita de incentivos fiscais e outros recursos previstos em lei.					<B>Conclusões</B>: Podemos concluir que atualmente o FUNDEB é o principal instrumento de distribuição de recursos para a educação, porém é considerado insuficiente para atender à demanda por universalização e qualidade do ensino e está abaixo das metas de investimento na educação propostas pelos Planos Nacionais de Educação, tanto do governo, como da sociedade.				</P>
			</div><B>Palavras-chave</B>: Fundeb. educação básica. recursos financeiros.			<BR>
