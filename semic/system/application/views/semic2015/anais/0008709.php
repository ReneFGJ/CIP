			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: This research is an opportunity to reflect on one aspect of educational administration, with regard to the management of financial resources of basic education, and the FUNDEB, meet your characteristics and its application, and also know about management of the financial resources.					<B>Objectives</B>: This study is sought to characterize the FUNDEB, identify their goals and see how the funds are being used for education					<B>Methods</B>: The research adopted is qualitative; the main instrument to document analysis, this study was carried out by consulting the official sites, which enabled the observation of the use of educational financial resources.					<B>Results</B>: The FUNDEB was created by the government to achieve the goals of the National Plan of Education, in which each State, Municipal and Federal District have a financial resource that have to be destined to education. The financial resources comes from taxes and states transferences, from Federal District and Municipal, according to the article 68 from Law Guidelines of National Education n 9.394/96. The public resources are from the taxes of its own, from the states from municipal and from the District Federal; revenues for education and other social contributions; income tax incentives and other resources provided by law.					<B>Conclusion</B>: We can conclude that currently the FUNDEB is the principal instrument of distribution of educational resources, but it is not considered enough to meet the demand of universalization and quality of the education, and its goals are below than what was supposed in the National Education Plan, both government and society.				</P>
