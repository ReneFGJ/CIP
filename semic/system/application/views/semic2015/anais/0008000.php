			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Sistemas embarcados são limitados em relação a energia disponível a eles. Normalmente estes sistemas possuem uma bateria e devem realizar a sua operação de maneira energeticamente eficiente. De qualquer maneira, estes sistemas são normalmente conectados à internet e expostos a um crescente número de ameaças. O levantamento do consumo energético de técnicas de detecção de intrusão permite efetuar a melhoria do sistema de acordo com a energia requisitada pela técnica estudada. A abordagem mais estudada na literatura para a detecção de intrusão consiste em utilizar um algoritmo de aprendizagem de máquina. Este algoritmo requer um conjunto de tarefas, que permitem efetuar a sua detecção.					<B>Objetivo</B>: Estabelecer o consumo energético dos módulos de um sistema de detecção de intrusão e também de trechos específicos do mesmo. Permitindo assim efetuar melhorias ao método utilizado para a detecção de acordo com o consumo energético.					<B>Metodologia</B>: Instrumentar o código do sistema de detecção de intrusão, e obter dados do consumo energético de módulos ou de trechos específicos do programa.					<B>Resultados</B>: Os dados obtidos, mostram o consumo energético da CPU, nos módulos de aquisição, extração e detecção. E também dos classificadores do sistema de detecção, os quais foram medidos em duas plataformas diferentes, garantindo assim a confiabilidade dos resultados obtidos.					<B>Conclusões</B>: Este projeto elencou o consumo energético de técnicas estudadas por um estudante de mestrado da PUCPR. Diversos trechos específicos do sistema foram elencados e seu consumo energético estabelecido. A partir dos valores obtidos neste projeto espera-se que seja possível efetuar melhorias ao sistema considerando o consumo energético de cada técnica, abordagem ainda não relatada na literatura.				</P>
			</div><B>Palavras-chave</B>: Sistemas embarcados. Consumo energético. Técnicas de detecção.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Embedded systems are limited according to the energy available to them. Normally these systems are battery-powered and must perform its operations by an energy-efficient manner. Anyway, these systems are normally connect to the internet and exposed to a crescent number of threats. The definition of the energy consumption of the intrusion detection techniques allows performing improvements according to the energy demanded by the technique studied. The most studied approach on the literature consists on using a machine-learning algorithm. This algorithm requires a set of tasks, which allows its detection.					<B>Objectives</B>: Define the energy consumption from the modules of a intrusion detection system and also from specific sections of it. Allowing then to perform improvements to the used method according to the energy demanded by it.					<B>Methods</B>: Instrument the code from the intrusion detection system, and obtain the energy consumption data from the modules and from the system sections.					<B>Results</B>: The data obtained, show the energy consumption from the system, the acquisition, extraction and detection module. And also from the detection system classifiers, which were measured on two platforms allowing the reliability from the data obtained.					<B>Conclusion</B>: This Project has listed the energy consumption from the studied techniques from a master degree student from PUCPR. Many specific sections from the system were listed and its energy consumption established. From the energy consumption values obtained on this project it is expected that improvements can be made to the system considering the energy consumption from the used technique, approach not yet reported on the literature.				</P>
			</div><B>Keywords</B>: Embedded systems. Energy consumption. Technical detection.</td>
