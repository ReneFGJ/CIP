			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A Rede Mãe Paranaense é uma estratégia do Governo que propõe a organização da atenção materno-infantil, nas ações do pré-natal, parto, puerpério e acompanhamento do crescimento e desenvolvimento das crianças até um ano de vida, visando a redução da mortalidade materna e infantil. Propõe um conjunto de ações de cuidado da gestante e das crianças, incluindo o cuidado em saúde bucal.					<B>Objetivo</B>: O objetivo deste trabalho foi analisar a eficácia da atenção secundária em saúde bucal na Rede Mãe Paranaense em municípios de diferentes portes populacionais no Estado do Paraná.					<B>Metodologia</B>: O instrumento de pesquisa foi enviado por e-mail para os gestores de 136 municípios do estado do Paraná, sendo que 105 responderam a um questionário composto por perguntas que abordavam ações de estruturação da rede de atenção em saúde materno-infantil. Para esta pesquisa, foram analisadas duas perguntas deste questionário, sobre a estruturação dos serviços de atenção especializada em saúde bucal. Os dados foram tabulados e analisados por meio de distribuição de frequências.					<B>Resultados</B>: Do total, a maioria (38,85%) relatou existir serviços especializados de nível secundário para os cuidados a saúde bucal, mas que são insuficientes. Do mesmo modo, 36,75% relataram a existência de serviços ambulatoriais especializados de nível secundário para os cuidados à saúde bucal que operem de forma integrada com a APS, mas que também são insuficientes.					<B>Conclusões</B>: É necessário que seja incentivado para maior integração das modalidades de Atenção à Saúde Bucal (atenção primaria e especializada) para estruturação de redes de atenção integrada, otimizando os trabalhos de Saúde Bucal da Rede Mãe Paranaense e consequentemente atendendo melhor as gestantes.				</P>
			</div><B>Palavras-chave</B>: rede de atenção materno infantil; saúde bucal			<BR>
