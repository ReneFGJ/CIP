			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Mother Paranaense Network is a Government strategy that proposes the organization of maternal and child care in the prenatal actions, childbirth, postpartum and monitoring the growth and development of children under one year of life, aimed at reducing maternal mortality and children. It proposes a set of care actions of pregnant women and children, including care in oral health.					<B>Objectives</B>: The objective of this study was to analyze the effectiveness of secondary care in oral health in Network Mother Paranaense in cities of different population sizes in the state of Paraná.					<B>Methods</B>: The survey instrument was sent by e-mail to managers of 136 municipalities in the state of Paraná, and 105 answered a questionnaire with questions that addressed structuring actions in the treatment network in maternal and child health. For this research, we analyzed two questions of the questionnaire on the structure of specialized care services in oral health. Data were tabulated and analyzed using frequency distribution.					<B>Results</B>: Of the total, the majority (38.85%) reported there secondary level of specialized services for the care of oral health, but that is insufficient. Similarly, 36.75% reported the existence of secondary specialized outpatient services for care oral health that operate seamlessly with the APS, but are also insufficient.					<B>Conclusion</B>: .It needs to be encouraged for greater integration of modes of Attention to Oral Health (primary and specialized care) for structuring integrated care networks, optimizing the Oral Health Network works of Mother Paranaense and therefore better serve pregnant women.				</P>
