			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The health professional, for a correct clinical practice, must not only know which drug is indicated for specific disease, but rather as the substance acts in the body and how the body responds to its effects - this concept is extremely important for understanding the importance of Study of pharmacokinetics and pharmacodynamics, since this is the basis for the understanding adverse effects, toxicity, and therapeutic failures interaçõe. The advancement of information technology coupled with the growing number of scientific publications requires a transformation in the way students learn clinical pharmacology and pharmacotherapy.					<B>Objectives</B>: The objective of this work consisted in the development of educational content related to Pharmacodynamics and Pharmacokinetics themes for a virtual learning environment.					<B>Methods</B>: Important sources of clinical pharmacology were used for texts graphics, images and layouts elaboration. The prepared material was sent to experts in the field for validation.					<B>Results</B>: It was obtained a bibliographic content that addressed the basic aspects related to the principles of pharmacology, but of relevance to clinical practice and excellent concept , according to the experts in pharmacology.					<B>Conclusion</B>: It was obtained a bibliographic content that addressed the basic aspects related to the principles of pharmacology, but of relevance to clinical practice and excellent concept , according to the experts in pharmacology.				</P>
