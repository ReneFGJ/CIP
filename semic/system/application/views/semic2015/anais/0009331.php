			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O profissional da saúde, para uma prática clínica correta, não deve apenas saber qual medicamento é indicado para determinada doença, mas sim como a substância age no organismo e como o organismo responde aos seus efeitos - esse conceito é o pilar para compreendermos a importância do estudo da farmacodinâmica e farmacocinética, pois esta representa a base para a compreensão dos efeitos adversos, toxicidade, interaçõe e falhas terapêuticas. O avanço da tecnologia de informação aliada ao crescente número de publicações científicas exige uma transformação no modo pelo qual os estudantes aprendem farmacologia clínica e farmacoterapia.					<B>Objetivo</B>: O objetivo do presente trabalho consistiu na elaboração do conteúdo didático referente aos temas Farmacodinâmica e Farmacocinética para um ambiente virtual de aprendizagem.					<B>Metodologia</B>: Foram utilizadas fontes de renome na área de farmacologia para confecção dos textos e exposição visual aplicada ao assunto para um melhor entendimento na forma de texto, gráficos, imagens e esquemas.					<B>Resultados</B>: Foi obtido conteúdo que abordou os aspectos básicos relacionados aos princípios da farmacologia, mas de relevância para a prática clinica e com conceito de excelente de acordo com os especialistas em farmacologia que o analisaram.					<B>Conclusões</B>: O conteúdo didático desenvolvido abordou os temas relacionados aos pilares da farmacologia – a farmacodinâmica e a farmacocinética. Foi possível desenvolver um texto abordando aspectos básicos relacionados aos princípios da farmacologia, mas com um enfoque direcionado a prática clinica, relevantes para desenvolver competências relacionadas a seleção de medicamentos, escolha de esquemas posológicos que garantam que o medicamento escolhido alcance concentrações adequadas no alvo de ação e reconhecimento de fatores modificadores de resposta clínica.				</P>
			</div><B>Palavras-chave</B>: Farmacologia clinica. e-learning			<BR>
