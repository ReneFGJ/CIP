					<B>Introdução</B>: O uso de hormônios vegetais tem obtido resultados significativos em plantas bem nutridas, sendo um momento oportuno para o estudo dos efeitos destes compostos na produção da soja .					<B>Objetivo</B>: avaliar o uso de diferentes concentrações de 2,4-D e estrato bruto aquoso de tiririca (Cyperus rotundus) para avaliar a suscetibilidade a cultura da soja (Glycine Max) a doenças como também o uso dos dois tratamentos como hormônio vegetaL					<B>Metodologia</B>: O delineamento experimental empregado foi em blocos casualizados utilizando 4 doses de extrato bruto aquoso de tiririca 25, 50, 75 e 100% e 5 doses de 2,4 D diluído 1, 2, 3, 4, 5ml/l-1 com quatro repetições incluindo a testemunha. Com aplicações á cada 15 dias até o inicio do florescimento. As variáveis avaliadas foram altura de planta, diâmetro de colmo,número de vagens, número de vagens com 1 grão, número de vagens com 2 grãos, número de vagens com 3 grãos, peso de mil grãos e produtividade.					<B>Resultados</B>: 					<B>Conclusões</B>: 				</P>
