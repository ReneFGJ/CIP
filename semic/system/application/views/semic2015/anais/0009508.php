			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Com o grande desenvolvimento da equinocultura brasileira e com a crescente da utilização de biotecnologias da reprodução, como a inseminação artificial, existe um grande aumento no uso de métodos de refrigeração de sêmen.					<B>Objetivo</B>: Deste modo, o objetivo específico do presente projeto, foi comparar a refrigeração do sêmen equino em geladeira convencional e caixa comercial de transporte de sêmen equino.					<B>Metodologia</B>: Foram realizadas 9 colheitas de sêmen, de garanhões diferentes, e cada ejaculado foi submetido a uma avaliação microscópica (motilidade e vigor), mensuração de pH e patologia espermática. Os métodos estatísticos utilizados para comprovar o experimento foram Kruskal-Wallis e Dunns para a motilidade e vigor, ANOVA e Tukey para pH e patologia espermática.					<B>Resultados</B>: A motilidade só foi diferente antes da refrigeração (p[e]lt;0,001), sendo igual estatisticamente entre geladeira e caixa comercial (p[e]rt;0,05). O vigor não diferiu antes ou depois da refrigeração pelos dois métodos (p[e]rt;0,05). O pH, após acrescido o diluente, se manteve semelhante(p[e]rt;0,05). A patologia não teve alterações significativas (p[e]rt;0,05) mesmo após a refrigeração em geladeira ou caixa.					<B>Conclusões</B>: A redução de motilidade após a refrigeração é esperada, porém não houve diferença em nenhum parâmetro quanto ao método de refrigeração, demonstrando a viabilidade do uso da geladeira no lugar da caixa comercial.				</P>
			</div><B>Palavras-chave</B>: equino, semen, regrigeração			<BR>
