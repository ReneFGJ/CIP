			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: With the great development of Brazilian Equine and the growing use of reproductive biotechnologies such as artificial insemination, there is a large increase in the use of semen cooling methods.					<B>Objectives</B>: The specific objective of this project was to compare the cooling of equine semen in conventional fridge and commercial cash equine semen transport.					<B>Methods</B>: Nine semen were performed crops, different stallions, and each ejaculate was subjected to microscopic evaluation (Sperm motility), pH measurement and sperm morphology. Statistical methods used to prove the experiment were Kruskal-Wallis and Dunns for motility and vigor, ANOVA and Tukey for pH and sperm morphology.					<B>Results</B>: The motility was not different before refrigeration (p [e]lt;0.001), being statistically equal between refrigerator and commercial box (p[e]rt; 0.05). The force did not differ before or after cooling by the two methods (p[e]rt; 0.05). The pH after the diluent increased, remained similar (p[e]rt; 0.05). The morphology sperm had no significant changes (p[e]rt; 0.05) even after cooling in the refrigerator or box.					<B>Conclusion</B>: The reduction in motility after cooling is expected, but there was no difference in any parameter on the cooling method, demonstrating the feasibility of using the refrigerator in place of commercial box. Finally, it concluded that conventional refrigerator is feasible for cooling equine semen for 24 hours without offering great risk the integrity of sperm.				</P>
