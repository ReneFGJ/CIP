			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Esta pesquisa contemplará o levantamento dos dados necessários para o dimensionamento da rede de coleta de esgoto e levantamento planialtimétrico, além do dimensionamento do biodigestor adequado às condições verificadas, sendo este o primeiro estudo da implantação de um reator anaeróbico na região, podendo fundamentar outros estudos à respeito do desenvolvimento do sistema na região.					<B>Objetivo</B>: a) Efetuar o Levantamento Topográfico da região;
b) Dividir a área de estudo em Bacias de Esgotamento Sanitário;
c) Cadastrar as edificações de acordo com as Bacias de esgotamento Sanitário em que se encontram;
d) Estimar o volume de esgoto gerado em cada Bacia de Esgotamento Sanitário;
e) Coletar as amostras do solo e das águas para identificar o grau de poluição;
f) Dimensionar a rede coletora de esgoto e o biodigestor, que irá atender a bacia de esgotamento sanitário delimitada para o estudo;
g) Instalar sensores eletrônicos no solo e nas águas, para coletar informações instantâneas do grau de poluição, objetivando a implantação de monitoramento remoto.					<B>Metodologia</B>: 3.1.  LEVANTAMENTO PLANIALTIMÉTRICO: as curvas de nível foram representadas a cada 0,50 centímetros. 
3.2.   PRÉ DIMENSIONAMENTO DA REDE DE COLETA DE EFLUENTES: o traçado da rede segue a NBR 9649 – Projeto de Redes de Esgoto. 
3.3.  DIMENSIONAMENTO DO BIODIGESTOR: o sistema será composto por uma combinação sequencial dos sistemas anaeróbios e aeróbios, a associação de reatores UASB e filtros biológicos percoladores (FBP)					<B>Resultados</B>: Sistema em condomínio, composto por 15 residências com 4 pessoas, sendo a contribuição total de esgoto de 7.800 l/dia com DBO 346,15 mm/litros.
Três Reatores UASB, em paralelos com volume útil unitário de 1.300 litros, seguidos por Filtros Anaeróbios com volume útil unitário de 2.080 litros.
o lodo gerado é de 883,55 litros, devendo ser retirado 90% a cada 8 meses.					<B>Conclusões</B>: O sistema adotado mostrou-se eficiente pois atende as necessidades da comunidade da Ilha das Peças, reduzindo a carga de DBO em 80%.				</P>
			</div><B>Palavras-chave</B>: Saneamento básico. reator anaeróbico. tratamento de efluentes			<BR>
