			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: This research will include the collection of data necessary for the design of the network of sewage collection and planialtimetric survey in addition to the sizing of the digester suitable to conditions that arise, and this is the first study the implementation of an anaerobic reactor in the region , and may support other studies to about the development of the system in the region.					<B>Objectives</B>: a) Make the Topographical Survey of the region;
b ) Divide the study area in Sewage basins ;
c ) Sign the buildings according to the sewage basins Sanitary where they are located ;
d ) Estimate the volume of sewage generated in each basin Sewage ;
e) Collect soil and water samples to identify the degree of pollution;
f ) Scaling the sewage disposal system and the digester , which will suit the basin sewage defined for the study ;
g ) Install electronic sensors in soil and water , to collect instant information on the degree of pollution , aiming the remote monitoring deployment.					<B>Methods</B>: 3.1. SURVEY planialtimetric : The contours were represented every 0.50 centimeters .
3.2 . PRE SIZING OF WASTEWATER COLLECTION NETWORK : The design of the network follows the NBR 9649 - Sewerage Project.
3.3 . SIZING the biodigester : the system will consist of a sequential combination of anaerobic and aerobic systems , the combination of UASB reactors and trickling biological filters ( BPF )					<B>Results</B>: System in condominium, consisting of 15 residences with 4 people, with a total contribution of sewage 7,800 l / day with BOD 346.15 mm / liter. Three reactors UASB in parallel with unit working volume of 1300 liters, followed by Anaerobic Filters with unit working volume of 2,080 liters. The generated sludge is 883.55 liters, must be removed 90% at 8 months.					<B>Conclusion</B>: The adopted system was efficient as it serves the needs of parts of the Island community, reducing the BOD load by 80%.				</P>
