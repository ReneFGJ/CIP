			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O presente projeto de pesquisa envolve um estudo para o entendimento do neuromarketing frente ao Código de Defesa do Consumidor (CDC). Complementando-se especificamente acerca da possibilidade das técnicas do neuromarketing influenciar os consumidores que se utilizam da internet como meio para o consumo.					<B>Objetivo</B>: Questiona-se se é possível influenciar os consumidores que utilizam o comércio eletrônico como fonte principal do consumo através das técnicas do neuromarketing. E, em que medida estas técnicas ultrapassam as regras estabelecidas pela legislação vigente.					<B>Metodologia</B>: Foi realizado levantamento bibliográfico buscando fundamentação teórica consistente e firmada em estudos científicos, sempre tendo como orientação o Código de Defesa do Consumidor. Estudou-se a internet como ambiente de consumo, o comportamento do consumidor virtual. E, também se analisou as técnicas do neuromarketing frente ao Código de Defesa do Consumidor.					<B>Resultados</B>: Neste sentido, o estudo se debruçou sobre como as técnicas de neuromarketing poderiam influenciar o consumidor no ambiente virtual e, ainda, sobre a análise relacionada às influências no momento da compra. Finalmente, pode-se buscar os limites ao neuromarketing por meio da legislação vigente no país. Devido à característica de vulnerabilidade do consumidor, as técnicas do neuromarketing trazem ainda mais desequilíbrio na relação de consumo.					<B>Conclusões</B>: Conclui-se que com as técnicas do neuromarketing é possível influenciar o consumidor quando o mesmo se utiliza do ambiente virtual para realizar suas compras, de modo que a legislação vigente realiza uma tentativa de afastar essas técnicas abusivas, visando um equilíbrio na relação de consumo.				</P>
			</div><B>Palavras-chave</B>: Direito do Consumidor. Comércio Eletrônico. Neuromarketing.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The project is related to a study to understand the neuromarketing and Consumer Protection Code (CDC). The study is based on the possibility of neuromarketing techniques to influence consumers on internet.					<B>Objectives</B>: Is it possible to influence the consumers using e-commerce as the main source of consumption through neuromarketing techniques? And to what extent these techniques go beyond the rules established by the Law? These questions are the focuses this study.					<B>Methods</B>: The study was conducted based on the literature seeking consistent theoretical basis and taking account scientific studies, always having as guidelines the Consumer Protection Code. We studied the Internet as a consumer environment and the virtual consumer behavior. We also analyzed the neuromarketing techniques and the Consumer Protection Code.					<B>Results</B>: The study looked into how neuromarketing techniques could influence the consumer in the virtual environment and also analysed the influences related to the time of purchase. Finally, one can seek the limits on neuromarketing by current legislation in Brazil. Due to consumer vulnerability, the neuromarketing techniques bring further imbalance in consumer relationship.					<B>Conclusion</B>: It is concluded that the neuromarketing techniques can influence the consumer when it uses the virtual environment to make their purchases, so that Brazilian legislation tries to avoid the abuse these techniques, seeking a balance in consumer relationship.				</P>
			</div><B>Keywords</B>: Consumer Law. e-commerce. Neuromarketing.</td>
