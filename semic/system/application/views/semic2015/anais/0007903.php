			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: In this undergraduate research it is discussed the importance of insects as a tool to maintain the ecological balance, considering the perspective of Brazilian environmental law and the constitutional provision (article 225) that assures to everyone the right to an ecologically balanced environment and to a healthy life.					<B>Objectives</B>: The purpose of this research was to verify the importance of insects.					<B>Methods</B>: It was investigated by an interdisciplinary approach the function of the insect for ecology, the approach used by the Environmental Law, and the understandings adopted by the national courts. Thus, we examined the biological definition of insects, the existence of standards that explicitly discipline this subject, and the existence of trials involving the theme.					<B>Results</B>: It has been found that in the Brazilian legal system there is no specific law that deals with the protection of insects as an essential subject to maintain the ecological balance of the environment, neither regulation issued by the administrative bodies linked to the National Environmental System (SISNAMA) on the subject. Is has been verified that the Environmental Crimes Law is applicable as a basis for analysis of actions that deal with the subject. Finally, in the research of previous law cases in the Courts of Rio Grande do Sul, Paraná, São Paulo and Rio de Janeiro, there was a lack of results that deal with cases in which there is death of insects.					<B>Conclusion</B>: It has been envisioned the need of developing deeper studies about the subject, taking into account that it is of a theme of great value for the Environmental Law.				</P>
