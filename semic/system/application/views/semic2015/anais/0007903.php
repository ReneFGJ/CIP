			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Esta pesquisa de iniciação científica aborda a importância dos insetos como instrumento de manutenção do equilíbrio ecológico, a partir da perspectiva do Direito Ambiental brasileiro e da previsão constitucional (art. 225) de que todos possuem o direito a um meio ambiente ecologicamente equilibrado e a uma vida sadia.					<B>Objetivo</B>: Para tanto, o propósito foi verificar a importância dos insetos e como objetivo desta pesquisa.					<B>Metodologia</B>: Investigou-se a abordagem interdisciplinar da função dos insetos para a ecologia e o posicionamento adotado pelo Direito Ambiental, assim como foram investigados os entendimentos adotados pelos Tribunais nacionais. Assim, examinou-se a definição biológica de insetos, a existência de norma que discipline explicitamente a matéria, e a existência de julgados que envolvam o mérito em questão.					<B>Resultados</B>: Aferiu-se que no ordenamento jurídico brasileiro não há lei específica que exerça proteção aos insetos como sujeitos essenciais à manutenção do equilíbrio ecológico do meio ambiente, tampouco regulamento emitido pelos órgãos administrativos vinculados ao Sistema Nacional do Meio Ambiente (SISNAMA) acerca do assunto. Vislumbrou-se a aplicabilidade da Lei de Crimes Ambientais como fundamento para análise de ações que tratam sobre o assunto. Por fim, em pesquisa de jurisprudência nos Tribunais do estado do Rio Grande do Sul, Paraná, São Paulo e Rio de Janeiro, constatou-se a inexistência de julgados que deliberem acerca das penalidades aplicadas aos casos em que há morte dos insetos.					<B>Conclusões</B>: Vislumbra-se a necessidade de desenvolver estudos mais profundos sobre o assunto, tendo em vista que esse se apresenta um tema de grande valia para o Direito Ambiental.				</P>
			</div><B>Palavras-chave</B>: Direito Ambiental. insetos. Fauna.			<BR>
