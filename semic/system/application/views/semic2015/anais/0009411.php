			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O foco é a investigação da relação entre técnica, medicina e Ética no pensamento de Hans Jonas, analisando a sua concepção a respeito dessa relação que está muito presente nas discussões atuais no que tange ao universo da filosofia da técnica.					<B>Objetivo</B>: tentar conceber, mediante a tese de Jonas, como devemos enfrentar alguns dilemas éticos voltados ao âmbito da medicina humana, por exemplo, a questão ética que envolve testes de novos medicamentos em seres humanos, quando o indivíduo voluntaria-se para a aplicação de determinado medicamento, ainda não testado, mesmo concordando com a aplicação da substância, ainda sim seria ético “testar sua vida” desta forma em prol das demais que utilizarão (ou não) essa medicação, sacrificar uma pelas demais.					<B>Metodologia</B>: Foi utilizado como material da pesquisa as obras de Hans Jonas referentes ao tema da técnica: Principio Responsabilidade e Técnica, Medicina e Ética, os quais foram levantados bibliograficamente no período anterior ao relatório parcial com fichamentos usando como auxílio textos de comentadores do Brasil que tem como foco de estudo também o trabalho de Jonas.					<B>Resultados</B>: Como resultado dessa pesquisa, podemos conceber muito melhor a preocupação de Jonas em alertar aos demais membros da espécie humana a importância da preservação do planeta e as cautelas que devemos tomar no que tange a vida, sobretudo a vida humana e a preservação da natureza, o filósofo tem uma missão de nos colocar para a reflexão para que abramos os olhos para os problemas que estamos causando desenvolvendo a técnica pela técnica	de forma desenfreada.					<B>Conclusões</B>: A técnica é uma ferramenta importante esteve sempre presente durante a trajetória humana, desde a sua concepção com os primeiros hominídeos, porém hoje ela tomou proporções que estão além do controle de seus criadores, os humanos, e cabe a nós pensarmos a respeito e utilizá-la com responsabilidade.				</P>
			</div><B>Palavras-chave</B>: Hans Jonas. Técnica. Tecnologia. Biotecnologia. Medicina.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The focus is the investigation of the relationship between technology , medicine and ethics at the thought of Hans Jonas , as he conceives this relationship that is very present in current discussions regarding the universe of technical philosophy.					<B>Objectives</B>: The goal is to try to conceive, by Jonas thesis, as we face some ethical dilemmas facing the area of human medicine, for example, the ethical issue that involves testing new drugs in humans when the individual volunteers to the application of particular drug, not yet tested, even agreeing to the application of the substance, but still it would be ethical "test life" as it is for the benefit of others who use (or not) this medication, sacrifice one for the other					<B>Methods</B>: bibliographic search					<B>Results</B>: As a result of this research, we can design much better Jonah´s concern to alert the other members of the human species the importance of preserving the planet and the precautions we should take when it comes to life, especially human life and the preservation of nature, philosopher has a mission to put us to the reflection to open our eyes to the problems we are causing developing the technique by technique rampant.					<B>Conclusion</B>: The technique is an important tool has always been present in human history, from its conception to the first hominids, but today she took proportions that are beyond the control of its creators, humans, and it´s up to us to think about and used it responsibly.				</P>
