			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O entendimento do comportamento do crescimento dendrométrico em fases iniciais de espécies arbóreas nativas ainda é pequeno, uma vez que sua variabilidade é alta e o número de estudos na área é escasso.					<B>Objetivo</B>: Assim, o objetivo do presente projeto de pesquisa é avaliar as taxas de germinação e crescimento, bem como produzir mudas de seis espécies florestais nativas do Paraná.					<B>Metodologia</B>: Foram escolhidas 6 (seis) espécies florestais: pitanga, Bracatinga, Ipê Amarelo, Aroeira salsa, Araçá e Paineira Rosa. Para cada espécie foram testados 3 (três) substratos: Trimix, Carolina II e Macplant. Foram medidos os DAC, altura e número de folhas semanalmente. Os resultados foram comparados pela ANOVA, seguido do teste de Tukey, com P = 0,05.					<B>Resultados</B>: Das espécies avaliadas, apenas 4 (apresentaram germinação suficiente para a realização das análises estatísticas. Nas três variáveis avaliadas não foi observada diferença significativa entre os substratos, tampouco houve interação entre espécies e substratos.					<B>Conclusões</B>: A paineira apresentou maior crescimento em DAC e altura, enquanto que em número de folhas ela empatou com a aroeira, estatisticamente.				</P>
			</div><B>Palavras-chave</B>: Diâmetro. Crescimento. Altura.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Dendrometric growth behavior on the early stages of native tree species knowledge is still small, since variability is high and the number of studies in the area is scarce.					<B>Objectives</B>: The objective of this research was to evaluate the germination and growth rates as well as Parana State native species seedlings produce.					<B>Methods</B>: Pitanga, Bracatinga, Ipe Amarelo, Aroeira salsa, Araçá and Paineira Rosa was select for this experiment. For each species were tested three (3) substrates: Trimix, Carolina II and Macplant. DAC, height and number of leaves were measured weekly. The results were compared by ANOVA followed by Tukey test, P = 0.05.					<B>Results</B>: Of species assessed, only 4 had enough germination to perform the statistical analyzes. In the three variables evaluated was no significant difference between the substrates, there was neither interaction between species and substrates.					<B>Conclusion</B>: Paineira presented the highest DAC and height growth, while the leaf number tied with Aroeira, statistically.				</P>
			</div><B>Keywords</B>: Diameter. Growth. Height.</td>
