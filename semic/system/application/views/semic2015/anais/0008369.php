			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A imunização constitui uma das formas mais eficazes no combate de doenças imunopreviniveis, porém os imunobiológicos podem provocar Eventos Adversos Pós-Vacinação (EAPV), alguns causados por erros de imunização, que podem interferir na adesão da população ao programa de imunização.					<B>Objetivo</B>: Identificar a ocorrência e caracterizar os tipos de Eventos Adversos Pós-Vacinação decorrentes de prováveis erros de imunização, notificados no Sistema de Informação de Eventos Adversos Pós-Vacinação, no Brasil e no Paraná, no período de 2003 a 2013.					<B>Metodologia</B>: Pesquisa descritiva, documental, quantitativa, em que foram utilizados dados do Sistema de Notificação de Eventos Adversos PósVacinação (SI-EAPV), no período de 2003 e 2013, no Brasil e no Paraná. Foram incluídos casos confirmados de EAPV decorrentes de prováveis erros de imunização, segundo o Manual de EAPV, do Programa Nacional de Imunização (PNI). Os dados foram organizados em tabelas com auxílio dos programas computacionais Microsoft Excel® 2013 e Epiinfo® versão 7.1.4					<B>Resultados</B>: Os estados com mais registros dos EAPV estudados foram São Paulo e Minas Gerais, Santa Catarina, Rio Grande do Sul e Paraná e três não notificaram durante quatro anos consecutivos, indicativo de subnotificação, 44,4% dos EAPV estavam relacionados a vacina BCG, que o evento de maior ocorrência foi abscesso quente e que a faixa etária mais atingida foi a de crianças menores de um ano de idade.					<B>Conclusões</B>: Concluiu-se que os erros de imunização são frequentes e o número de casos notificados aumentou ao longo do período estudado. Constatou-se, ainda, que estes eventos estão relacionados as ações de enfermagem durante a vacinação.				</P>
			</div><B>Palavras-chave</B>: Imunização. Saúde pública. Enfermagem.			<BR>
