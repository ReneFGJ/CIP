			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Immunization is one of the most effective ways to combat immunopreventable diseases, but the immunobiologicals can cause Adverse Events Following Immunization (AEFI), some caused by immunization errors, which can interfere with adhesion of the population to the immunization program.					<B>Objectives</B>: To identify the occurrence and characterize the types of Post-vaccination Adverse Events resulting from immunization likely errors on System database in Brazil and Paraná, from 2003 to 2013.					<B>Methods</B>: Search descriptive, documentary, quantitative, where we used data of the Post-Vaccination Adverse Events Reporting System (ISAEFI) in 2003 and 2013, in Brazil and Paraná. AEFI confirmed cases were included due to probable immunization errors, according to the AEFI Manual, the National Immunization Program (NIP). Data were organized in tables with the help of computer programs Microsoft Excel® 2013 and Epiinfo® 7.1.4.					<B>Results</B>: The states with more records of AEFI studied were São Paulo and Minas Gerais, Santa Catarina, Rio Grande do Sul and Parana and three did not report for four consecutive years, indicative of underreporting, 44.4% of AEFI were related to BCG vaccine that the most frequent event was hot abscess and the most affected age group was that of children under one year of age.					<B>Conclusion</B>: Conclude that immunization errors are frequent and the number of reported cases has increased over the study period. It found also that these events are related nursing actions during vaccination.				</P>
			</div><B>Keywords</B>: Immunization. Public health. Nursing.</td>
