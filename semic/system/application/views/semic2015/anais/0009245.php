			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O presente projeto de pesquisa faz uma análise jurídica e social sobre a
licitação do metrô curitibano, visando evidenciar e facilitar, por exemplo, alguns
conceitos do Direito Administrativo para a compreensão da população desta capital.
O projeto concretizou-se através de pesquisas baseadas em doutrinas,
jurisprudências, artigos científicos e, especialmente, com os entrevistados que
responderam algumas perguntas indagadas. A participação do público alvo da
licitação foi de extrema relevância.
A realização e desenvolvimento da pesquisa se deu, incialmente, por meio de
pesquisas teóricas, reuniões com o professor orientador, conversa, informal, com
Paulo Scalco assessor técnico e Juarez Varallo Pont, superintendente de
Administração da Secretaria Municipal de Planejamento e Administração. Também
foi utilizado um publico alvo – pouco mais de 30 entrevistados, o qual são residentes
dos bairros CIC, Portão e Novo Mundo.					<B>Objetivo</B>: O objetivo deste trabalho consiste em evidenciar para a população curitibana o que é o Direito Administrativo, o que é a Licitação e qual a opinião do público alvo quanto a implantação do metrô.					<B>Metodologia</B>: O método utilizado no presente projeto de pesquisa baseou-se em pesquisas
jurisprudências, pesquisas doutrinárias e pela internet. Num segundo momento, o
mecanismo passou a ser a pesquisa com a população curitibana.					<B>Resultados</B>: 					<B>Conclusões</B>: 				</P>
