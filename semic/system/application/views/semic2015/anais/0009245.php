				<P>
					<B>Introduction</B>: This research project is a legal and social analysis of the bidding Curitiba subway, aiming to highlight and facilitate, for example, some Administrative Law concepts for understanding the population of this capital.
The project materialized through doctrines-based research, jurisprudence, scientific articles, and especially with the respondents. They answered some questions. The participation of the target audience bidding was extremely relevant.
The construction and development of the research took place, initially through
theoretical research, meetings with the guiding teacher, talk, informal, with
Paulo Scalco technical advisor and Juarez Varallo Pont, superintendent
Municipal Administration for Planning and Administration. Also
It used a target audience - just over 30 respondents, who are resident
neighborhoods CIC, Portão and Novo Mundo.					<B>Objectives</B>: The method used in this research project was based on case law research, doctrinal research and the Internet. Secondly, the mechanism became the research with the Curitiba population.					<B>Methods</B>: The objective of this work is to emphasize to the Curitiba population which is the Administrative Law, which is the bid and what the target audience of opinion on the implementation of the subway.					<B>Results</B>: 					<B>Conclusion</B>: 				</P>
