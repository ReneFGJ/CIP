			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Com a evolução tecnológica atual, observa-se o desenvolvimento de tecnologias que buscam de um modo geral, melhorar o bem estar das pessoas. Neste contexto, recebe atenção especial os sistemas especialistas voltados ao apoio à profissionais da saúde, em processos de análise e tomada de decisão.					<B>Objetivo</B>: Assim, o objetivo deste projeto de pesquisa é, através de uma revisão de literatura, buscar por conceitos que auxiliem na construção de ontologias aliadas a modelos de aquisição de conhecimento para sistemas especialistas, no processo de planejamento de processo de implantodontia.					<B>Metodologia</B>: Como método de pesquisa foi utilizada a bibliometria da revisão sistemática da literatura, em três journals com relevância ao tema e alto fator de impacto.					<B>Resultados</B>: Fora obtido como resultado, uma base com 627 artigos selecionados previamente, que após o processo de busca resultou em 25 artigos selecionados que serviram como sustentação teórica para um artigo que está em elaboração para publicação em revista e indiretamente, para outro internacional que já fora publicado.					<B>Conclusões</B>: 				</P>
			</div><B>Palavras-chave</B>: Sistemas agentes. sistemas especialistas. ontologias.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: With present technological advances, was observed the development of technologies that improve the welfare of people. In this context, today receives special attention to the expert systems software, to support healthcare professionals in analysis and decision making process.					<B>Objectives</B>: The goal of this research project is, through a literature review, search for concepts that helps in building ontologies combined with knowledge acquisition models, for expert systems in dental implant planning process.					<B>Methods</B>: As a research method was used bibliometrics systematic review of the literature in three journals with relevance to the theme and high impact factor.					<B>Results</B>: It was obtained as result, a set with 627 articles previously selected from this journals, specifically. After all the steps of the search process is finished, was resulted in 25 selected articles that was served as theoretical support to an article that is being prepared for publication in a journal and, in indirectly way to other international publication presented in Serbia on January, 2014.					<B>Conclusion</B>: 				</P>
			</div><B>Keywords</B>: Agents systems. expert systems. ontologies.</td>
