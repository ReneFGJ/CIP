			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Intelligence can be defined as the totality of individual´s cognitive skills, referring to the ability to resolve and identify problems. One of the theories designed to study the
intelligence is the theory of Fluid Intelligence (Gf) and Crystallized
Intelligence (Gc). The Gf is related to the ability to react in
unprecedented situations, Gc refers to the solution of problems
closely related to cultural and educational experiences. Currently, the
failures of public education have been so identified, which indicate
that their teaching and learning has less quality than private schools
has. Because of that, it is considered relevant to investigate
individual factors related to intelligence and learning. Therefore, this
research presents relevant because it seeks to investigate and
understand aspects of intelligence in public and private schools.					<B>Objectives</B>: To evaluate the crystallized intelligence and fluid
intelligence in elementary school students through standardized
neuropsychological instruments, and compare the performance of
the groups in these instruments..					<B>Methods</B>: This study was carried out
with two samples of students, one for the students of the public
schools and other for students of the private schools. The instrument
chosen for the evaluation was the Wechsler Intelligence Scale for
Children WISC-IV. Specifically these subtests: Cubes and Matrix
Reasoning to evaluate crystallized intelligence; Vocabulary and
Information to assess fluid intelligence.					<B>Results</B>: It was expect to find
a significant difference between the performance of public and
private school in the evaluation of crystallized intelligence. Instead, it
was believed that the groups will not be differentiated in the
performance of subtestes which assess fluid intelligence. The results
obtained indicated that the groups are not differentiated in the
performance of fluid intelligence subtests. However, about the
subtests that assess the crystallized intelligence, the results showed
that there is an significant difference between the groups in the
Vocabulary subtest.					<B>Conclusion</B>: From the results one might think
that stimulation of fluid intelligence strategies in schools can be a
measure of effective intervention to compensate for the flaws
inherent to the Brazilian public school, which interfere with the
crystallized intelligence-dependent content, which is not always
transmitted properly.				</P>
