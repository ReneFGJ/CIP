			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A inteligência pode ser definida como a totalidade de
habilidades cognitivas de um indivíduo, referindo-se à capacidade de
resolver e identificar problemas. Uma das teorias destinadas a estudar a
inteligência é a teoria da Inteligência Fluida (Gf) e Inteligência Cristalizada
(Gc). A Gf está relacionada à habilidade de raciocinar em situações
inéditas, já a Gc refere-se à solução de problemas intimamente
relacionados às vivências culturais e educacionais. Em tempos em que as
falhas do ensino público têm sido tão apontadas, indicando um processo
de ensino e aprendizagem de qualidade inferior, quando comparado às
escolas da rede particular, considera-se relevante a investigação dos
fatores individuais relacionados à inteligência e à aprendizagem. Logo, a
presente pesquisa se apresenta relevante na medida em que busca
investigar e compreender aspectos da inteligência em escolares da rede
pública e privada de ensino					<B>Objetivo</B>: Avaliar a inteligência cristalizada e
a inteligência fluida em alunos do ensino fundamental através de
instrumentos neuropsicológicos padronizados e, comparar o desempenho
dos grupos nestes instrumentos.					<B>Metodologia</B>: Este estudo foi desenvolvido
com duas amostras de alunos, uma referente aos alunos da rede pública
e outra da rede privada. O instrumento escolhido para a avaliação foi a
Escala de Inteligência Wechsler para Crianças - WISC-IV,
especificamente os subtestes: Cubos e Raciocínio Matricial para avaliar a
inteligência cristalizada e, Vocabulário e Informação para avaliar a
inteligência fluida.					<B>Resultados</B>: Esperava-se encontrar diferença
significativa entre o desempenho de escolares da rede pública e privada
na avaliação da inteligência cristalizada. Ao contrário, acreditava-se que
os grupos não se diferenciaram no desempenho de tarefas que avaliam a
inteligência fluida. Os resultados obtidos comprovaram esta segunda
hipótese, visto que os grupos não se diferenciaram nos subtestes de
inteligência fluida. Já nos subtestes que avaliam a inteligência cristalizada,
foi encontrada diferença significativa entre os grupos no subteste
Vocabulário.					<B>Conclusões</B>: A partir dos resultados encontrados pode-se
pensar que estratégias de estimulação da inteligência fluida em escolares
pode ser uma medida de intervenção eficaz para compensar as falhas
inerentes ao ensino público brasileiro, as quais interferem no conteúdo
dependente da inteligência cristalizada, o qual nem sempre é transmitido
de maneira adequada.				</P>
