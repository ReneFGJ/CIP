			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: É notória a vital importância do Ensino Superior para capacitação profissional de uma nação e, portanto, para o desenvolvimento do país. Para tanto, há que se verificar a extensão da abrangência das Instituições de Ensino Superior para todas as classes sociais no intuito de promover a inclusão.					<B>Objetivo</B>: O presente trabalho tem por objetivo levantar e analisar dados no tocante ao Ensino Superior no País, bem como as políticas de inclusão no Ensino Superior, além das informações relativas ao sistema de meritocracia para o ingresso no Ensino Superior nos meios de comunicação.					<B>Metodologia</B>: A pesquisa tem caráter dedutivo, interdisciplinar, e será desenvolvida por meio de pesquisa bibliográfica e análise documental, por meio da utilização de dados disponibilizados pelo próprio Ministério da Educação, bem como artigos veiculados pela mídia, em especial na internet. É evidente a divisão de opiniões acerca do tema, tanto entre os estudantes como entre a população em geral. Por isso faz-se necessária uma observação minuciosa dos dados disponíveis para considerar os avanços na questão educacional. A análise dos dados colhidos terá o intuito de concluir os pontos positivos e negativos da implementação dos sistemas de cotas e programas de inclusão do governo no que tange o Ensino Superior.					<B>Resultados</B>: O impacto de tais programas de inclusão somente será notado em um longo espaço de tempo, quando estes profissionais já encontrarem-se no mercado de trabalho, porém o que se pode verificar é um aumento significativo de ocupação dos bancos das universidades, demonstrando a cada dia mais o interesse da população em se profissionalizar.					<B>Conclusões</B>: Ressalta-se também por meio deste trabalho a influência das mídias sociais para a divulgação de dados e informações aos estudantes e a população em geral, bem como sua repercussão em relação a gama da população alcançada.				</P>
			</div><B>Palavras-chave</B>: Ensino Superior, inclusão, cotas, meritocracia, mídias sociais			<BR>
			<BR>
