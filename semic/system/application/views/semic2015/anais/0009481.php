			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: These findings emphasize the vital importance of higher education to professional training of a nation and therefore for the development of the country. Therefore, it is necessary to ascertain the extent of the scope of higher education institutions to all social classes in order to promote inclusion.					<B>Objectives</B>: This study aims to survey and analyze data with respect to higher education in the country as well as the inclusion of policies in higher education, in addition to information about the meritocracy system for entry into higher education.					<B>Methods</B>: Research has deductive character, interdisciplinary, and will be developed through literature and document analysis through the use of data provided by the Ministry of Education, as well as reports in the media, especially the Internet. The division of opinion on the subject, both among students and among the general population is evident. Therefore it is necessary a thorough observation of the data available to consider advances in educational issue. The analysis of data collected have in order to complete the strengths and weaknesses of the implementation of systems of quotas and government inclusion programs regarding higher education.					<B>Results</B>: It is also highlighted through this work the influence of social media for the dissemination of data and information to students and the general population as well as their impact in relation to the range achieved population.					<B>Conclusion</B>: 				</P>
