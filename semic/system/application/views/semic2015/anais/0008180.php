			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Este projeto de pesquisa tem por tema analisar as questões ambientais no Passeio Público de Curitiba. É um Projeto que envolve diferentes áreas do conhecimento: Turismo, Biologia e Bioética. Será desenvolvida de forma conjunta e interdisciplinar entre os alunos de graduação e os Professores do programa de Pós Graduação em Bioética da PUCPR.					<B>Objetivo</B>: Esta pesquisa tem como objetivo principal formular analises acerca do comportamento e do perfil que compõem os visitantes de um parque urbano: o Passeio Público. Elaborar avaliações sobre a percepção desses visitantes em relação ao lugar em que estão,  bem como realizar o levantamento dos referenciais históricos do parque urbano e sua importância como espaço de lazer aos moradores da cidade e visitantes.					<B>Metodologia</B>: A metodologia utilizada para a pesquisa a de coleta de dados quantitativa através da aplicação de questionário com 384 pessoas, bem como observações locais dos comportamentos e do perfil dos indivíduos do local					<B>Resultados</B>: Os resultados obtidos sobre o espaço público foram referentes a utilização desse parque urbano principalmente para atividades referentes ao lazer, visitação ao zoo, descanso do trabalho, dentre outros.					<B>Conclusões</B>: Esta foi uma pesquisa multidisciplinar sobre a percepção dos moradores de Curitiba e  visitantes sobre a sua percepção do primeiro Parque urbano de Curitiba, PR. Mais do que pesquisar sobre este tema, o processo de pesquisa se constituiu em um aprendizado sobre organização de pesquisa, trabalho em grupo e ética em pesquisa.				</P>
			</div><B>Palavras-chave</B>: Passeio Público. comportamento. parque urbano. perfil.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: This research project has the theme analyze environmental issues in the Passeio Público (Public Garden) of Curitiba. It is a project that involves different areas of knowledge Tourism, Biology and Bioethics. It will be developed jointly and interdisciplinary way between undergraduates and the Graduate Program in Bioethics Professors of PUCPR.					<B>Objectives</B>: This research aims to formulate analysis of the behavior and profile that make visitors of an urban park : the Passeio Público ( Public Garden) . Prepare reviews of the perception of these visitors about the place they are in, as well as to survey the historical references of the urban park and its importance as a recreational space for city residents and visitors.					<B>Methods</B>: The methodology used to research the quantitative data collection through interviews with 384 people, as well as local observations of the behavior of individuals and the local profile.					<B>Results</B>: The results of the public space were related to use of this urban park primarily for activities related to leisure, visiting the zoo, rest from work, among others.					<B>Conclusion</B>: This was a multidisciplinary research on the apperception of the residents of Curitiba and visitors about their perception of the first Curitiba, PR Urban park. More than research on this topic, the research process consisted in learning about research organization, group work and research ethics.				</P>
			</div><B>Keywords</B>: Passeio Público. behavior. urban park. profile.</td>
