			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A pesquisa buscou realizar uma análise econômica na abordagem do âmbito penal em relação ao mundo globalizado. Sendo que a globalização trouxe tantos avanços tecnológicos que permitiram o desenvolvimento de novas técnicas e meios que facilitaram a prática de ilícitos.					<B>Objetivo</B>: Tendo como objetivo analisar os fatores que levam um indivíduo a praticar uma conduta criminosa, partindo da premissa de que este sujeito responde a incentivos, utilizando para tal, os métodos econômicos.					<B>Metodologia</B>: Foram utilizados métodos de pesquisa usando as opinião de vários doutrinadores e personalidades do direito, bem como bastante utilizado a teoria de Gary Becker para os resultados obtidos.					<B>Resultados</B>: Em relação aos resultados obtidos, foi possível verificar alguns fatores que levam aos indivíduos optarem pelo crime, utilizando como exemplo a falta de oportunidades de emprego com salários dignos, onde a pessoa escolhe assumir o risco, por acreditar que compensa. Assim como foi apontado que os países subdesenvolvidos e em desenvolvimento tem um índice maior de criminalidade.					<B>Conclusões</B>: O sistema penal brasileiro é deficiente em muitos setores, há carência nas agências de polícia, no sistema judiciário e também no sistema penitenciário que são evidentes e estão cada vez mais contribuindo para que os indivíduos optem pelo crime. Assim sendo, de acordo com Becker, a pena de multa se mostrará muito mais eficiente, para determinados crimes, de acordo com o ordenamento jurídico brasileiro, pois haveria uma contribuição para o Estado, que tem muitas despesas para o aprisionamento e manutenção do indivíduo em presídios.				</P>
			</div><B>Palavras-chave</B>: Análise econômica do Direito. Criminalidade. Globalização.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: This research seeks to conduct an economic analysis on the approach to criminal law in relation to the globalized world. Since globalization has brought many technological advances that allowed the development of new techniques and means that facilitated the practice of crimes.					<B>Objectives</B>: With the objective to analyze the factors that cause an individual to commit crimes, based on the premise that this individual responds to incentives, using, therefore, economic methods.					<B>Methods</B>: Research methods were employed using the opinion of many jurists and authors, specially Gary Becker´s theory to obtain the results.					<B>Results</B>: Regarding the results, it was possible to verify some factors that lead individuals to opt for crime, using as an example the lack of employment opportunities with decent wages, where a person chooses to take the risk, believing that it pays off. As it was pointed out that the underdeveloped and developing countries have a higher crime rate.					<B>Conclusion</B>: The brazilian penal system is deficient in many areas, there is a lack of police agencies, problems in the judiciary system and also in the prisonal system, evidently contributing to increase the decision to commit crimes. Therefore, according to Becker, the penalty fine will prove much more efficient for certain crimes, according to the brazilian legal system, as there would be a contribution to the State, that has many expenses capturing and maintaining people in prisons.				</P>
			</div><B>Keywords</B>: Economic analysis of Law. Criminality. Globalization.</td>
