			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O presente trabalho traz como tema principal o desenvolvimento de uma pesquisa voltada para a utilização dos brinquedos LEGO como material de auxílio a aprendizagem em um ambiente hospitalar. Esta pesquisa está integrada no grupo de pesquisa do programa de Mestrado e Doutorado em Educação - PPGE - PUCPR, no projeto LEGO FAVORECENDO PROPOSTA PEDAGÓGICA JUNTO A ESCOLARES EM TRATAMENTO DE SAÚDE.					<B>Objetivo</B>: A pesquisa tem como objetivo oferecer atividades pedagógicas com auxílio do material da Lego no aprendizado dos escolares que estão hospitalizados, buscando ajudar os profissionais da educação que atuam no ambiente hospitalar na exploração de um caminho inovador para chegar ao aprendizado.					<B>Metodologia</B>: Pesquisa de abordagem qualitativa do tipo pesquisa-ação realizada em um hospital de Curitiba.					<B>Resultados</B>: 					<B>Conclusões</B>: É claramente notável, ao final do trabalho, que a pedagogia hospitalar tem uma participação fundamental na recuperação da criança que está em tratamento de saúde, e os materiais Lego além de favorecer grandemente o trabalho do pedagogo é aceito pelos alunos com muita facilidade.  							Com as visitas aos diversos locais e contextos onde os profissionais da educação atuam e a participação de cursos sobre a LEGO como material pedagógico e sobre a mediação e tutoria em um ambiente virtual, tínhamos como foco levar esse conhecimento para a prática no atendimento do Hospital Cajurú e domiciliar. As demais componentes do grupo, ou seja, colegas PIBITI e PIBIC tiveram a oportunidade de levar o material da Lego ao hospital e conseguiram resultados relevantes, assim foi possível perceber a relevância do material Lego para aprendizagem.								
Apesar de não ter sido como o esperado e de não conseguirmos chegar aos resultados almejados, aprofundamos o nosso conhecimento, em relação as diversas e novas possibilidades que as tecnologias trazem e a inclusão dessas tecnologias em diferentes contextos.				</P>
			</div><B>Palavras-chave</B>: LEGO. Educação não Formal. Escolar hospitalar. Ação docente			<BR>
