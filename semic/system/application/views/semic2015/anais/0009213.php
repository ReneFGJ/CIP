			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A toxoplasmose é causada pelo [e]lt;i[e]rt;Toxoplasma gondii[e]lt;/i[e]rt;, protozoário coccídio de caráter intracelular obrigatório, com ciclo biológico complexo e que acomete o homem e praticamente todas as espécies de animais de sangue quente. A infecção pode ocorrer pela ingestão de oocistos, taquizoítos, ou bradizoítos. O cão, apesar de não ser hospedeiro definitivo, contribui na disseminação mecânica deste protozoário. Considerando a sorologia como um método de diagnóstico simples e economicamente viável, deve-se ressaltar que a Reação de Imunofluorescência Indireta (RIFI) é o método mais comumente utilizado na investigação epidemiológica desta zoonose.					<B>Objetivo</B>: A soro-ocorrência é fundamental para o controle e profilaxia da doença, assim sendo, o objetivo do presente estudo foi detectar a ocorrência de anticorpos anti-[e]lt;i[e]rt;Toxoplasma gondii[e]lt;/i[e]rt; em cães atendidos no Hospital Veterinário.					<B>Metodologia</B>: Para tanto, foram coletadas 175 amostras de sangue de cães, provenientes do Atendimento de Animais de Companhia. Os cães foram selecionados aleatoriamente, independentemente de raça, sexo ou idade.					<B>Resultados</B>: Das 175 amostras inclusas no estudo, 43 apresentaram resultado positivo para anticorpos contra o [e]lt;i[e]rt;Toxoplasma gondii[e]lt;/i[e]rt;, representando 24,6% de soropositividade.					<B>Conclusões</B>: Do ponto de vista de Saúde Pública, a infecção na população canina significa que a área envolvida representa um nicho ecológico para o parasita e, consequentemente, um risco para a população humana.				</P>
			</div><B>Palavras-chave</B>: População de cães. Reação de Imunofluorescência Indireta. Toxoplasmose.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Toxoplasmosis is a parasitic disease caused by the protozoan [e]lt;i[e]rt;Toxoplasma gondii[e]lt;/i[e]rt;, the obligate intracellular protozoan. The parasite can infect humans and virtually all other warm-blooded animals. The infection can occur by ingestion of oocysts. Dogs may act as mechanical vectors of oocysts. The serology can be a simple and economically viable diagnostic method for measuring seroprevalence. The Indirect Immunofluorescence (IFA) are most commonly used in toxoplasmosis epidemiological studies.					<B>Objectives</B>: The seroprevalence is critical for the control and prevention of the disease. This study aimed to investigate the occurrence of anti-[e]lt;i[e]rt;Toxoplasma gondii[e]lt;/i[e]rt; antibodies in dogs treated at the Veterinary Hospital.					<B>Methods</B>: It was collected 175 samples of blood serum of dogs, of different races, gender and ages.					<B>Results</B>: Of the 175 samples included in the study, 43 were positive for antibodies to [e]lt;i[e]rt;Toxoplasma gondii[e]lt;/i[e]rt;, representing 24,6% of seropositivity.					<B>Conclusion</B>: From the viewpoint of public health, the infection in the canine population means that the ambient is an ecological niche for the parasite, representing a risk for the human population.				</P>
			</div><B>Keywords</B>: Dogs. Indirect Immunofluorescence. Toxoplasmosis.</td>
