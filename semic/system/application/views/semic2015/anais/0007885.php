			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Para o ensino da Odontologia, muitas disciplinas solicitam de seus alunos dentes humanos para as práticas clínicas e laboratoriais, sem os quais não podem cursá-las. Essa demanda por dentes tem induzido os estudantes à prática do crime de comércio ilegal de dentes, além de outros atos ilícitos. Para impedir essas práticas, foram instituídos os Bancos de Dentes Humanos (BDH).					<B>Objetivo</B>: O objetivo desta pesquisa foi analisar a necessidade dos estudantes de uma IES de dentes humanos extraídos para seus estudos.					<B>Metodologia</B>: Foi aplicado um questionário aos estudantes (n=120) com quatro questões fechadas e uma aberta. Os dados coletados das perguntas fechadas foram tabulados numa planilha do software Excel for Windows para a realização da análise estatística por distribuição de frequência com um nível de confiança de 95%. As respostas da questão aberta foram submetidas à análise de conteúdo de Bardin (2002).					<B>Resultados</B>: Os resultados demonstraram que são solicitados dentes nos quinto, sexto e sétimo períodos acadêmicos. A disciplina que mais solicita é a Endodontia (100%). Os dentes são coletados em consultórios particulares e com outros estudantes e, após utilizá-los, os acadêmicos os guardam em suas casas. O estudo revelou que o comércio ilegal de dentes é praticado.					<B>Conclusões</B>: Conclui-se que um terço da população pesquisada tem necessidade de dentes para seus estudos e que sente muita dificuldade em obtê-los. E que essa demanda por dentes continua incentivando a prática criminosa do comércio ilegal de dentes e pondo em risco a saúde dos estudantes e a de seus familiares.				</P>
			</div><B>Palavras-chave</B>: Dentes. Banco de dentes. Doação de dentes. Ética.			<BR>
