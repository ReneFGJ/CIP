			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: For teaching dentistry, many disciplines request of the students human teeth to the clinical and laboratory practice, without which they cannot pursuing this disciplines. This demand for teeth has led students to the practice of teeth illegal trade, and other illegal acts. To prevent such practices, banks Human Teeth (BDH) were established.					<B>Objectives</B>: The objective of this research was to analyze the needs of students from a one school of extracted human teeth for their studies.					<B>Methods</B>: A questionnaire was given to students (n = 120) with four closed questions and one open. The data collected from the closed questions were tabulated in an Excel spreadsheet software for Windows for statistical analysis by frequency distribution with a 95% confidence level. The answers to the open question were submitted to Bardin content analysis (2002).					<B>Results</B>: The results showed that teeth are requested in fifth, sixth and seventh academic periods. The discipline that most requests is the Endodontics (100%). Teeth are collected in private practice and with other students and, after using them, scholars keep them in their homes. The study revealed that the teeth illegal trade  is practiced					<B>Conclusion</B>: It is concluded that a third of the surveyed population needs teeth to their studies and feeling great difficulty in obtaining them. And this demand for teeth still encouraging the criminal practice of teeth illegal trade  and endangering the health of students and their families.				</P>
