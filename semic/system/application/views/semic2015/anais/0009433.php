			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A técnica de dissecção de fibras brancas é um método clássico utilizado por vários anatomistas e permitiu a identificação e a compreensão da conectividade cerebral. Com a possibilidade de se visualizar feixes de substância branca do encéfalo in vivo por imagens de RM, utilizando tractografia baseada em imagens de tensor de difusão, os fundamentos clássicos obtidos das dissecções anatômicas se tornaram ainda mais relevantes e atuais.					<B>Objetivo</B>: O objetivo do presente estudo foi analisar imagens cerebrais obtidas por ressonância magnética a partir da técnica de imagens de tensor de difusão e estabelecer padrões de definição de ROIs (Region of Interest), pensando uma metodologia para abordagem de cinco tractos específicos da substância branca.					<B>Metodologia</B>: O estudo foi realizado a partir da obtenção de um banco de imagens de RM de indivíduos jovens e sadios, com aquisição apropriada. Para a reconstrução dos tractos (tractografias) foi utilizado o sistema [e]lt;i[e]rt;Syngo Multimodality Workplace[e]lt;/i[e]rt;  MAGNETOM Amira (Siemens AG – München).					<B>Resultados</B>: Foram definidos os pontos (ROI) que permitem a reconstrução dos seguintes tractos de associação: Fascículo Longitudinal Superior, Fascículo Arqueado, Fascículo Frontooccipital Inferior, Fascículo Uncinado, Cíngulo.					<B>Conclusões</B>: O estudo demonstra alguns aspectos da subjetividade do método na reconstrução dos tractos, estando não só na dependência do correto posicionamento dos ROIs, mas também em características de pós-processamento, sendo necessário excluir algumas fibras que se dirigem a outros pontos, e incluir outras que, por critérios individuais (singularidade), apresentam características relevantes próprias.				</P>
			</div><B>Palavras-chave</B>: Tractografia. Substância branca. Ressonância magnética. Imagem de tensor de difusão.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The white fibers dissection technique is a classic method used by several anatomists and allowed the identification and understanding of brain connectivity. With the ability to visualize white matter bundles of the brain in vivo with MRI using tractography based on DTI (diffusion tensor images), the classical foundations obtained from anatomical dissections became even more relevant and current.					<B>Objectives</B>: The aim of this study was to analyze brain images taken by MRI from diffusion tensor imaging technique and to establish ROI setting standards (Region of Interest) for five specific white matter tracts approach.					<B>Methods</B>: The study was conducted obtaining  DT-MRI from 30 young and healthy individuals with appropriate acquisition. For the reconstruction of the tracts (tractografias) we used the system Syngo Multimodality Workplace MAGNETOM Amira (Siemens AG - München).					<B>Results</B>: The seeds (ROI) were defined to allow the reconstruction of the following association tracts: superior longitudinal fasciculus, arcuate fasciculus, Inferior frontooccipital fasciculus, uncinate fasciculus and cingulus.					<B>Conclusion</B>: The study shows some aspects of the subjectivity of the method in the reconstruction of tracts, not only dependent on the correct positioning of the ROI, but also in post-processing features.  It is necessary to exclude some fibers that address other tracts, and to include others by individual criteria (uniqueness).				</P>
			</div><B>Keywords</B>: Tractography. White matter. MRI. DTI.</td>
