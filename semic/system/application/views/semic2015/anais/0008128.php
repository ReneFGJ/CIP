					<B>Introduction</B>: When choosing alimentation as a historical subject of study, it was found that this action belongs to the routine of everyday life, goes beyond simply eating as a biological necessity, but  rather that eating, being at the table, preparing food, encompasses much more to fill a physiological need but becomes a social act. As they sat at the table, people gather around the food and at this point there is social interaction, information exchange and interpersonal relationships. With this assumption, the study of history of alimentation comes into play as a means of analysis of historical transformations in the civilizing process of society.					<B>Objectives</B>: From this premise, the present research seeks to rescue the regional cuisine values by contract revenues from a pontagrossense family between the years 1900 to 1950 in search of new approaches to the female world. Its general objective is to recognize the preparation ‘food spaces of a family group of women pontagrossenses the first half of the twentieth century from the writing book of recipes that belonged to them as an essentially female space.					<B>Methods</B>: Searched behind the reading and deepening of the subject-matter about of the alimentation’s history in Brazil and history of women in being worked, or the first half of the twentieth century, identify the titles of revenue and systematize them from a rating for a later stage be possible to analyze the contents of revenues, and everything about space food.					<B>Results</B>: 					<B>Conclusion</B>: 				</P>
