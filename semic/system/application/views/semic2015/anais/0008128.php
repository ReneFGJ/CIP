			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Ao escolher a alimentação como tema de estudo histórico, descobriu-se que esta ação pertencente à rotina da vida cotidiana, vai além do simplesmente comer como forma biológica, mas sim que alimentar-se, estar à mesa, preparar os alimentos, engloba muito mais que suprir uma necessidade fisiológica mas converte-se em um ato social. Ao sentarem-se à mesa, as pessoas se reúnem em volta do alimento e neste momento há interação social, troca de informações, relações interpessoais. Com este pressuposto, o estudo da história da alimentação entra em cena como meio de análise das transformações históricas no processo civilizador da sociedade.					<B>Objetivo</B>: A partir dessa premissa, a atual pesquisa busca resgatar os valores da cozinha regional pelos cadernos de receitas de uma família pontagrossense entre os anos de 1900 a 1950 em busca de novos enfoques sobre o mundo feminino. Tem como objetivo geral reconhecer os espaços de preparação dos alimentos de um grupo familiar de mulheres pontagrossenses da primeira metade do século XX a partir dos cadernos de receitas que a elas pertenceram como um espaço essencialmente do feminino.					<B>Metodologia</B>: Buscou-se assim a partir da leitura e aprofundamento da temática da história da alimentação no Brasil e história das mulheres no contexto a ser trabalhado, ou seja, primeira metade do século XX, identificar os títulos das receitas e sistematizá-las a partir de uma classificação para numa etapa futura ser possível a análise dos conteúdos das receitas, e tudo o que cerca o espaço da comida.					<B>Resultados</B>: 					<B>Conclusões</B>: 				</P>
