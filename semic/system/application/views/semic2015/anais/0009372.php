			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A Hipertensão Arterial Sistêmica (HAS) é um dos fatores de risco cardiovasculares mais importantes e seu tratamento com fármacos antihipertensivos reduz a incidência de infarto (IAM), acidente vascular encefálico (AVE) e óbito A população idosa é particularmente suscetível aos eventos adversos associados ao tratamento medicamentoso da HAS e as diretrizes diferem quanto às recomendações de alvo de nível tensional nesta população.					<B>Objetivo</B>: Essa revisão da literatura foi conduzida com o intuito de avaliar as evidências científicas disponíveis para o embasamento de recomendação de alvo de Pressão Arterial (PA) em pacientes idosos.					<B>Metodologia</B>: Buscou-se na base de dados PUBMED por estudos randomizados que tivessem reportado o impacto do tratamento medicamentoso da HAS sobre IAM, AVE e óbito em pacientes com 60 anos ou mais. Os dados extraídos dos estudos foram sintetizados em tabelas do programa Excel e descritos de forma narrativa.					<B>Resultados</B>: A busca resultou em 9585 artigos, dos quais 19 foram incluídos para a revisão, em que 11 avaliaram a terapia farmacológica em comparação ao placebo, 4 testaram o efeito de diferentes classes de antihipertensivos em idosos e 4 compararam  alvos distintos de PA.					<B>Conclusões</B>: Esta revisão concluiu que o tratamento da hipertensão em idosos com mais de 60 anos de idade iniciando em pressão arterial sistólica [e]rt; = 160 mmHg ou pressão arterial diastólica [e]rt;= 90 mmHg  almejando alvo de pressão arterial sistólica [e]lt;= 150 [e]rt;= 140 mmHg e pressão arterial diastólica [e]lt; 90 mmHg reduz eventos cardiovasculares e não aumenta de forma significativa os eventos adversos. Para alvos inferiores a esses níveis, a análise dos estudos não permite concluir benefício adicional.				</P>
			</div><B>Palavras-chave</B>: Hipertensão arterial. idosos. infarto agudo do miocárdio. acidente vascular encefálico. morte.			<BR>
