			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Hypertension is one of the most important cardiovascular risk factors and antihypertensive treatment reduces acute myocardial infarction, stroke and death rates. Elderly patients are particularly susceptible to adverse events associated with antihypertensive drug treatment and there is disagreement in current guidelines as for the optimal blood pressure levels in this population.					<B>Objectives</B>: The aim of this literature review was to analyse current evidence for endorsing blood pressure target recommendation in elderly patients.					<B>Methods</B>: We conducted a PUBMED data base search for randomised trials that reported antihypertensive treatment impact on acute myocardial infarction, stroke and death in patients aged 60 or more. Data was extracted into excel tables and described narratively.					<B>Results</B>: The search resulted in 9585 articles. Nineteen  were included in the revision, from which 11 analysed pharmacological treatment against placebo, 4 tested the effects of different classes of antihypertensive drugs in elderly and 4 compared different blood pressure targets.					<B>Conclusion</B>: This review concluded that blood pressure treatment in people aged 60 or more starting at a systolic blood pressure level [e]rt;= 160 mmHg or diastolic blood pressure [e]rt;= 90 mmHg aiming a target [e]lt;= 150 [e]rt;= 140 mmHg for systolic blood pressure  and [e]lt; 90 mmHg for diastolic blood pressure reduces cardiovascular events and does not significantly increases adverse events. There is no support for lower blood pressure targets.				</P>
			</div><B>Keywords</B>: Hypertension. elderly. acute myocardial infarction. stroke. death</td>
