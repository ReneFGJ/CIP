			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Após a extração dentária, o processo alveolar sofre um processo de atrofia fisiológica que pode levar a alterações, com perda de volume da crista alveolar, impossibilitando ou dificultando a instalação de implantes. Alguns autores vêm preconizando a utilização de biomateriais para acelerar a regeneração óssea alveolar. O Bio-Oss® é um osso de origem bovina, desproteinado, usado frequentemente em cirurgias de aumento de rebordo alveolar e levantamento de seio maxilar, servindo como arcabouço para crescimento do novo tecido ósseo.					<B>Objetivo</B>: O objetivo do presente estudo foi avaliar, histológica e histomorfometricamente, o processo de reparo ósseo e o potencial regenerativo do osso bovino anorgânico (Bio-Oss) em defeitos cirurgicamente criados na calvária de coelhos.					<B>Metodologia</B>: Foram criados 2 defeitos circulares de 7 mm de diâmetro no osso frontal e parietal de 20 coelhos albinos (New Zealand). Os defeitos foram aleatoriamente preenchidos com Bio-Oss (grupo-teste) ou mantidos com o preenchimento por coágulo (grupo-controle). Os animais foram mortos 60 dias após os procedimentos cirúrgicos e os defeitos avaliados através de análises histológica e histomorfométrica.					<B>Resultados</B>: Uma maior porcentagem de formação óssea foi encontrada no grupo- controle, estatisticamente superior ao grupo-teste. Com relação ao preenchimento ósseo, o grupo-teste apresentou a maior média de preenchimento, com diferença estatisticamente significativa em relação ao grupo-controle.					<B>Conclusões</B>: O uso do Bio-Oss leva a um atraso no processo de reparo ósseo, porém, promove um aumento do volume de tecido formado após 60 dias, em coelhos, em comparação ao reparo observado em áreas onde o biomaterial não foi utilizado.				</P>
			</div><B>Palavras-chave</B>: Regeneração óssea. osso bovino inorgânico. reparo ósseo			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: After dental extraction, the alveolar process undergoes a process of physiologic atrophy which may lead to changes, with volume loss of the alveolar crest, preventing or hindering the installation of implants. Some authors come from advocating the use of biomaterials to accelerate the alveolar bone regeneration. The Bio-Oss ® is a deproteinated bovine bone, often used in surgeries increased alveolar edge and lifting of the maxillary sinus, serving as a scaffold for new bone tissue growth.					<B>Objectives</B>: The objective of the present study was to evaluate the histological process of bone repair and regenerative potential of inorganic bovine bone (Bio-Oss) in surgically created defects in the rabbits calvaria.					<B>Methods</B>: Two circular 7 mm in diameter defects were created in the frontal and parietal bones of 20 albino rabbits (New Zealand). The defects were randomly filled with Bio-Oss (test group) or kept with the padding for clot (control group). The animals were killed 60 days after surgical procedures and the faults assessed by histological and histomorphometric analysis.					<B>Results</B>: A greater percentage of bone formation was found in the control group, statistically superior to the test group. With respect to bone filling the test group showed the highest average fill, with statistically significant difference compared to the control group.					<B>Conclusion</B>: The use of Bio-Oss leads to a delay in the process of bone repair, however, promotes an increase in the volume of tissue formed after 60 days, in rabbits, in comparison to repair observed in areas where the biomaterial was not used.				</P>
