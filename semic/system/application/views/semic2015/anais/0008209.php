			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Nas ultimas décadas, muito se tem discutido sobre o fenômeno Bullying, em suas diversas vertentes. O professor tem o papel significativo na formação educacional dos alunos é um exemplo a ser seguido, é um formador de opinião. Ele tem um grande poder de intervenção, podendo contribuir para mudar direta ou indiretamente o comportamento de seus alunos.					<B>Objetivo</B>: A ideia principal desta pesquisa é a figura do professor, como educador e seu papel nas estratégias que se utilizadas frente às ocorrências de bullying, também procura verificar o conhecimento sobre o tema.					<B>Metodologia</B>: A presente pesquisa foi realizada em duas escolas do Município de São José dos Pinhais, sendo uma instituição particular e outro da rede estadual de ensino, totalizando uma amostra de vinte professores que lecionam no ensino fundamental que abrange o 1º ano ao 9º ano. O estudo tem um caráter quantitativo, e o instrumento utilizado foi um questionário adaptado de Carvalho (2012), já utilizado em outros estudos.					<B>Resultados</B>: Os resultados apontaram um bom interesse e conhecimento sobre o bullying, ressaltando que este fenômeno está inserido no contexto escolar, sendo o professor uma peça chave no combate e na prevenção junto com a direção e os pais, assim os mesmo se utilizam de estratégias como palestras educativas, conversas com os alunos e realização de atividades lúdicas.					<B>Conclusões</B>: 				</P>
			</div><B>Palavras-chave</B>: Professor. escola. bullying			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: In recent decades, much has been discussed about the bullying phenomenon in its various forms. The teacher has a significant role in the educational background of students is an example to be followed, it is a trendsetter. He has great power intervention and can contribute directly or indirectly to change the behavior of their students.					<B>Objectives</B>: The main idea of [e]#8203;[e]#8203;this research is the teacher´s figure as an educator and his role in the strategies that are used in the face of bullying occurrences also seeks to verify the knowledge on the subject.					<B>Methods</B>: This survey was conducted in two schools in São José dos Pinhais, being a private institution and one of the state schools, totaling a sample of twenty teachers who teach in elementary schools covering the 1st to 9th grade year. The study has a quantitative character, and the instrument was a questionnaire adapted from Carvalho (2012), already used in other studies.					<B>Results</B>: The results showed a good interest and knowledge about bullying, emphasizing that this phenomenon is embedded in the school context, being the teacher a key player in the fight and prevention along with the direction and parents, so even if using strategies such as lectures educational, conversations with students and carrying out recreational activities.					<B>Conclusion</B>: 				</P>
