			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A proteção de dados pessoais merece uma atenção redobrada na atualidade, pois remete a uma questão cada vez mais comum no cotidiano dos indivíduos. Com o advento da internet e a grande evolução tecnológica, torna-se constante o uso de dados pessoais nas redes e, por conseguinte, aumentam-se os problemas relacionados ao seu uso. Alguns países da Europa criaram leis específicas para diminuir os conflitos existentes a respeito desse assunto, trazendo artigos em suas próprias Constituições. No Brasil o marco civil da internet foi aprovado apenas em 2014 e existe um anteprojeto de lei de proteção de dados pessoais.					<B>Objetivo</B>: O presente projeto teve como objetivo analisar as normas brasileiras existentes nos tempos atuais, e se elas de fato solucionam os problemas de quem aciona o judiciário.					<B>Metodologia</B>: Utilizou-se o método dedutivo, através de documentação indireta, envolvendo a pesquisa documental e bibliográfica.					<B>Resultados</B>: Verificou-se a existência de algumas normas gerais que podem ser utilizadas para a proteção de dados pessoais, como normas de direito constitucional e de direito civil, bem como a existência de algumas decisões dos tribunais tentando solucionar os conflitos que foram levados até a Justiça.					<B>Conclusões</B>: A partir da coleta dos resultados ficou comprovada a falta de uma legislação específica sobre a proteção de dados pessoais, o que dificulta o julgamento pelos tribunais, deixando a pessoa muito vulnerável a práticas abusivas e ao entendimento do poder judiciário.				</P>
			</div><B>Palavras-chave</B>: proteção de dados pessoais – legislação – Poder Judiciário			<BR>
