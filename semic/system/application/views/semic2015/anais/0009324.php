			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Personal data protection deserves careful attention in the news since it refers to a question increasingly common in daily life. With the advent of internet and the great technological evolution, it is the constant use of personal data in networks and, therefore, are increased the problems related to its use. Some European countries have created specific laws to reduce conflicts on this subject, bringing items in their own constitutions. In Brazil, the civil Law “marco civil da internet” was only approved in 2014 and there is a draft of personal data protection law.					<B>Objectives</B>: This project aims to analyze existing standards in Brazil nowadays, and they actually solve the problems of who drives the judiciary.					<B>Methods</B>: We used the deductive method, through indirect documentation, involving documentary and bibliographic research.					<B>Results</B>: It was found that there are some general rules that can be used for the protection of personal data, such as standards of constitutional law and civil law, and the existence of some decisions of the courts trying to resolve the conflicts that have been brought up to Justice.					<B>Conclusion</B>: From the collection of the results was proven the lack of specific legislation on the protection of personal data, which makes the judgment by the courts, leaving the person very vulnerable to abuse and understanding of the judiciary.				</P>
