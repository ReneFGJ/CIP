			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O termo Rede Inteligente (RI) possui uma série de valores e características que são universais. Entre esses valores estão um sistema mais confiável, seguro, econômico, eficiente e ambientalmente correto.					<B>Objetivo</B>: Foi proposto o desenvolvimento de um sistema, cujo objetivo é o controle de um, ou mais, aparelhos ligados à rede elétrica utilizando da rede de comunicação WiFi para controlar.					<B>Metodologia</B>: Foi desenvolvido um aplicativo na plataforma Android capaz de ligar ou desligar um determinado equipamento. Foram utilizados um kit de desenvolvimento Arduino e um módulo de comunicação WiFi CC3000.					<B>Resultados</B>: Os resultados obtidos ocorreram de acordo com os propostos e esperados. De maneira simples foi construído um sistema em que é possível, por exemplo, acender e apagar uma lâmpada e obter seu estado atual.					<B>Conclusões</B>: Os resultados obtidos ocorreram de acordo com os propostos e esperados. De maneira simples foi construído um sistema em que é possível, por exemplo, acender e apagar uma lâmpada e obter seu estado atual. Sendo assim, é possível construir um sistema mais robusto que é capaz de determinar o tempo em que um determinado dispositivo permanece ligado ou desligado, de acordo com a tarifação da rede elétrica do horário para, assim, de uma maneira inteligente enconomizar energia elétrica.
Este sistema poderia ainda, se adicionado um sensor adequado, apresentar relatórios de consumo do equipamento em questão. Sendo assim, se estivesse ocorrendo um consumo maior do que o esperado, um alerta pode ser emitido para poder detectar possíveis problemas.				</P>
			</div><B>Palavras-chave</B>: Smart grid. Redes inteligentes. Rádio CC3000. Automação residencial.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The term Intelligent Network (IN) has a series of values and characteristics that are universal. Among these values are a more reliable, safe, economical, efficient and environmentally friendly system.					<B>Objectives</B>: It was proposed the development of a system whose aim is the control of one or more devices connected to the mains using the WiFi communication network to control.					<B>Methods</B>: An application was developed on the Android platform able to turn on or off certain equipment. An Arduino development kit and a communication module WiFi CC3000 were used.					<B>Results</B>: The results occurred in accordance with the expected and proposed. Simply built a system where it is possible, for example, turn on and off a lamp and get their current state.					<B>Conclusion</B>: Therefore, it is possible to construct a more robust system which is able to determine the time that a particular device remains switched on or off in accordance with the charging time of the electrical network to thus in an intelligent way enconomizar electricity.
This system could also be added an appropriate sensor, provide consumer reports the equipment in question. So if you were going higher consumption than expected, an alert may be issued in order to detect possible problems.				</P>
			</div><B>Keywords</B>: Smart Grid. Smart Networks. Radio CC3000. Home Automation.</td>
