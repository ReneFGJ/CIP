			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O objetivo da osteotomia periacetebular de Bernese-Ganz, descrita em 1983, é mudar a biomecânica patológica do quadril que leva a dano intra-articular e, consequente, artrose desta articulação.  A reorientação do acetábulo displásico aumenta a superfície de carga enquanto mantém ou melhora a estabilidade articular.					<B>Objetivo</B>: O objetivo desse trabalho é avaliar o resultado radiográfico dos pacientes submetidos a osteotomia periacetabular de Ganz.					<B>Metodologia</B>: Para a realização deste trabalho, foram avaliadas as radiografias de oito pacientes operados, nove quadris, em um hospital terciário de Curitiba, Paraná. Através do Programa de Computador Surgimap, Versão 2.1.8, foram avaliados os ângulos Centro borda, ou CE de Wiberg, e o índice acetabular pré e pós operatórios desses pacientes. 
Para a comparação dos resultados do ângulo CE e do índice acetabular antes e após a cirurgia, foi usado o teste não-paramétrico de Wilcoxon. Valores de p[e]lt;0,05 indicaram significância estatística. Os dados foram analisados com o programa computacional IBM SPSS Statistics v.20.					<B>Resultados</B>: Este trabalho, apesar do baixo número de casos avaliados, mostrou, em cada caso, um bom resultado pós operatório na cobertura acetabular, trazendo os valores para a faixa de normalidade.					<B>Conclusões</B>: A avaliação radiográfica dos pacientes submetidos a osteotomia periacetabular de Ganz apresentou bons resultados, porém, ainda há necessidade de uma amostra maior para que haja diferença estatisticamente significativa entre os valores pré e pós operatórios.				</P>
