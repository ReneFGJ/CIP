			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The goal of periacetebular osteotomy Bernese -Ganz described in 1983, it is to change the pathological hip biomechanics which leads to intra -articular damage and consequential, arthrosis this articulação. The reorientation of dysplastic acetabulum increases the load surface while maintaining or improving articular stability.					<B>Objectives</B>: The objective of this study was to evaluate the radiographic findings of patients who underwent osteotomy periacetabular					<B>Methods</B>: For this work , radiographs eight patients operated, nine hips, were evaluated in a tertiary hospital in Curitiba, Paraná . Through Surgimap Computer Software , Version 2.1.8 evaluated the center edge angle , CE Wiberg , and the pre- and postoperative acetabular index of these patients.
For comparison of results and CE angle of the acetabular index before and after surgery , it used the non- parametric Wilcoxon test . P values [e]#8203;[e]#8203;[e]lt; 0.05 were considered statistically significant . Data were analyzed with the computer program IBM SPSS v.20 .					<B>Results</B>: This work, despite the low number of cases evaluated, showed, in each case , a good postoperative outcome in acetabular coverage , bringing the values [e]#8203;[e]#8203;for normal range					<B>Conclusion</B>: Radiographic evaluation of patients undergoing periacetabular Ganz osteotomy had good results , however, there is still a need for a larger sample so that there is statistically significant difference between pre and postoperative .				</P>
