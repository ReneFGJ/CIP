			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The Federal Government through the Ministry of Sports (ME) implemented from 2003 the sporting and social program called Second Time, which is being considered by the institution as a major anchor of national initiatives in sport and recreation policy which presents the following proposals: the reversal of the current situation of injustice, exclusion and social vulnerability; sports and recreation as a right of each and duty of the State; universalization and social inclusion and democratization of management and participation.					<B>Objectives</B>: Identify and analyze the main difficulties, the limiting and aspects with implementing sports program Second Time (PST) in São José dos Pinhais-PR, in the period 2010-2012 from the perspective of social actors involved in such management.					<B>Methods</B>: The research was qualitative descriptive type and the data collected through semi-structured interviews.					<B>Results</B>: For the general coordinator of the PST in the city, the main barriers are related to the structures, staff and little program runtime. As for the pedagogical coordinator, they are linked with the lack of autonomy and the involvement of trainees and teachers. The main facilitators are provision of sporting goods, the city´s initiative in bringing the agreement, the dietary supplement and meetings of teachers.					<B>Conclusion</B>: Based on the results we presented it was found that the program in the municipality introduced a dynamic aspects involving satisfactory and constraints in implementation that need more research to enhance the new agreements that may be effective in this or in another municipality.				</P>
