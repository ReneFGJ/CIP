			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O Governo Federal através do Ministério do Esporte (ME) implantou a partir de 2003 o Programa esportivo e social denominado Segundo Tempo, o qual vem sendo considerado pela referida instituição como uma das principais iniciativas na âncora da política nacional de esporte e lazer que apresenta as seguintes propostas: a reversão do quadro atual de injustiça, exclusão e vulnerabilidade social; o esporte e lazer como direito de cada um e dever do Estado; da universalização e inclusão social e da democratização da gestão e da participação.					<B>Objetivo</B>: Identificar  e analisar as principais dificuldades, os limitadores e os aspectos satisfatórios na execução do programa esportivo Segundo Tempo (PST) no município de São José dos Pinhais, PR, no período de 2010 a 2012 na perspectiva dos atores sociais envolvidos na referida gestão.					<B>Metodologia</B>: A pesquisa foi qualitativa do tipo descritiva e os dados coletados através de entrevistas semiestruturadas.					<B>Resultados</B>: Para o coordenador geral do PST no município, as principais barreiras relacionam-se com as estruturas, funcionários e pouco tempo de execução do programa. Já para o coordenador pedagógico, estão ligadas com a falta de autonomia, e o envolvimento dos estagiários e professores. Os principais facilitadores são a oferta de materiais esportivos, a iniciativa do município em trazer o convênio, o reforço alimentar e os encontros dos professores.					<B>Conclusões</B>: Com base nos resultados apresentados constatou-se que o referido programa no município apresentou aspectos uma dinâmica que envolve aspectos satisfatórios e limitadores na sua execução que necessitam de mais pesquisas para potencializar os novos convênios que possam se efetivar neste ou em outro município.				</P>
			</div><B>Palavras-chave</B>: Políticas Públicas. Esportes. Programa Segundo Tempo			<BR>
