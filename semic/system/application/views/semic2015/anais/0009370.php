					<B>Introduction</B>: The scientific field has been developing rapidly with technological
advances, especially in the field of research involving biotechnology, such as:
genetic manipulation involving human embryos.					<B>Objectives</B>: But starting from the right does
not develop at the same speed because there are still few boundaries and
sanctions. In addition, ethics or bioethics this very defined by custom and not
practice, and because it is a new branch there are not many parameters yet.
But it should not be forgotten the principle of human dignity, which must guide
all relations, both legal as scientific as it aims to protect the human being by its
essential fundamental rights. The biolaw is the branch of law relating to
bioethics to the characteristics of law, as this serves to standardize the norms of
a society, so there are limits. Indeed, it is the legislative, judicial and doctrinaire
production involving bioethics, therefore, these two branches can not be
analyzed separately. Ethics must be analyzed in a multidisciplinary context, it
should analyze the moral values and principles, but in this case, the dimension
of scientific studies in the field of improvement of human health. So it´s a new
slant on life and human dignity, for it seeks a new response to modern science.
Thus, there is the need to create new laws, these are security and the limits of a
society. Since the law seeks to organize a safe conduct for all based on the
values of this society, that is, based on bioethics.					<B>Methods</B>: The principle of human dignity
is the least that should be respected by others and the state itself as it is a
foundation of the Federative Republic of Brazil. Protect the right to life, which is
the foundation of all other rights, being an inalienable right.					<B>Results</B>: 					<B>Conclusion</B>: 				</P>
