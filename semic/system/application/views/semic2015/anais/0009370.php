			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O campo científico vem se desenvolvendo rapidamente com os avanços
tecnológicos, principalmente no campo da de pesquisas que envolvem a
biotecnologia, como por exemplo: a manipulação genética envolvendo
embriões humanos.					<B>Objetivo</B>: Mas em contra partida o direito não se desenvolve com a
mesma velocidade, pois ainda há poucas delimitações e sanções. Além disso,
a ética ou a bioética esta muito definida pelo costume e não pela prática, e por
se tratar de um ramo novo ainda não há muitos parâmetros. Porém não deve
ser esquecido o principio da dignidade da pessoa humana, o qual deve nortear
todas as relações, tanto jurídicas quanto científicas, pois visa proteger o ser
humano por seus direitos fundamentais essenciais. O biodireito é o ramo do
direito que relaciona a bioética com as características do direito, pois esse tem
a função de normatizar as normas de uma sociedade, para que haja limites.
Aliás, é a produção legislativa, judicial e doutrinária que envolve a bioética,
sendo assim, esses dois ramos não pode ser analisados separadamente. A
ética deve ser analisa em um contexto multidisciplinar, pois deve analisar os
valores e princípios morais, mas nesse caso, na dimensão dos estudos
científicos na área da melhoria da saúde humana. Portanto, é uma nova
vertente sobre a vida e a dignidade humana, pois busca uma nova resposta
para ciência moderna. Sendo assim, há a necessidade da criação de novas
leis, essas são a segurança e os limites de uma sociedade. Já que o direito
busca organizar uma conduta segura para todos com base nos valores dessa
sociedade, ou seja, com base na bioética.					<B>Metodologia</B>: O princípio da dignidade humana é o
mínimo que deve ser respeitado pelas demais pessoas e o próprio Estado, pois
é um fundamento da República Federativa do Brasil. Proteger o direito a vida,
que é o fundamento de todos os outros direito, sendo um direito irrevogável.					<B>Resultados</B>: 					<B>Conclusões</B>: 				</P>
