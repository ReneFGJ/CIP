			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Estudos sobre a qualidade têm demonstrado grande importância no marketing de relacionamento, influenciando medidas como satisfação, lealdade e resultados financeiros.					<B>Objetivo</B>: O objetivo deste estudo é avaliar a influência da qualidade de ensino no relacionamento dos alunos com a marca da IES e os fatores moderadores que mais impactam nessa relação.					<B>Metodologia</B>: Após a criação de uma nova escala para mensurar a qualidade de ensino a partir de quatro fatores (método de ensino, a qualificação do docente, a estrutura da IES e o relacionamento entre alunos e professores) foi realizado um levantamento.					<B>Resultados</B>: Análises de equações estruturais verificaram a explicação de impacto de 75,9% da Qualidade de ensino sobre a Qualidade do Relacionamento com a Marca. Verificou-se ainda por teste T de Student e análises de testes two-way Anova que alunos não bolsistas, homens e aqueles que estão no início do curso são mais sensíveis a essa relação. Ainda, que existe uma queda na percepção de Qualidade de Ensino e Qualidade de Relacionamento para alunos dos períodos finais em relação aos alunos de períodos iniciais.					<B>Conclusões</B>: O estudo verificou que para criar relacionamento entre aluno e marca da IES deve-se investir na excelência do ensino. Não há relacionamento sem a entrega adequada do principal serviço oferecido por uma instituição de ensino, ou seja, um ensino de qualidade. Falhas na entrega de serviço e expectativas não atendidas levam a relacionamentos de baixa intensidade.				</P>
			</div><B>Palavras-chave</B>: Marcas. Qualidade do Ensino. Qualidade do Relacionamento com marcas.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Studies have shown that quality holds great importance in relationship marketing, influencing measures as satisfaction, loyalty and financial results.					<B>Objectives</B>: The aim of this study is to evaluate the influence of the education quality in   students’ relationships with the University brand.					<B>Methods</B>: After the creation of a new scale for measuring the Education Quality based on four factors (teaching methods, teachers’ qualification, the IES structure and relationship between students and teachers) a survey was conducted.					<B>Results</B>: Results from structural equations indicated that the Quality of education have 75.9% of explanatory power on Brand Relationship Quality. It was further demonstrated by Student’s T-tests and two-way Anova tests that students without scholarships, men and those who are at the beginning of the course students are more sensitive to relationship. Also, there is a decline in the perception of Education Quality and Brand Relationship Quality in students of the final periods compared to students in initial periods.					<B>Conclusion</B>: The study found that to create relationship between student and the University Brand, it should invest in teaching excellence. There is no development of positive relationships without the proper delivery of the service core offered by an educational institution, an education with high levels of quality. Therefore, flaws in service delivery and unmet expectations will lead to low-level relationships.				</P>
