			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O risco de mercado no agronegócio está relacionado as variações decorrentes do preço de comercialização das commodities. A realização de estudos de tendências, análise do ambiente e de possíveis mudanças é fundamental para avaliar os riscos de mercado, traduzidos em riscos de preço. Este estudo está inserido no contexto de uma pesquisa que tem, como um dos objetivos principais, disseminar para as cooperativas agroindustriais ferramentas quantitativas de medição, análise e acompanhamento dos riscos financeiros e de mercado.					<B>Objetivo</B>: Esse trabalho visa a obtenção de modelos para a previsão do preço de commodities. Se objetiva inicialmente estimar um modelo ARIMA e seguidamente o modelo GARCH. O objetivo é desenvolver ferramentas que auxiliem no cálculo dos riscos de preços, utilizando sempre do apoio da literatura acerca do assunto e também de softwares para cálculos e analises de séries temporais, no caso o GRETL.					<B>Metodologia</B>: Para tanto, foram usados modelos de séries temporais ARIMA e GARCH.					<B>Resultados</B>: Como resultados, foram obtidos modelos satisfatórios para a estimação do preço da Soja considerando os modelos ARIMA e GARCH.					<B>Conclusões</B>: A partir dos modelos que foram estimados, ficou constatado que tais modelos se mostram adequados a previsão de preços de comercialização da soja em cooperativas no Paraná.				</P>
			</div><B>Palavras-chave</B>: Séries temporais			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Market risks of agribusiness is related to variations of prices of commercialization of commodities. Studies of trends, environment analysis and likely changes are mandatory to assessment of market risks, which can be translated into price risks. This study is part of a research that aims at making available for cooperatives quantitative tools of measurement and analysis of market risks.					<B>Objectives</B>: This study aims at developing models for price forecasting of commodities of Paraná. Initially, the goal is estimate a ARIMA model and then a GARCH model. The main goal is to develop tools that aid in calculation of risk in prices using the literature support and especific software to time series – GRETL.					<B>Methods</B>: For this, time series models of ARIMA and GARCH were used.					<B>Results</B>: As main results, best fitted models have been estimated for forecasting soybean prices using ARIMA and GARCH.					<B>Conclusion</B>: With the estimated models, it was possible to verify that they are suitable for forecasting prices of commercialization of soybeans in cooperatives of Paraná.				</P>
