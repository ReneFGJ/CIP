			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Com o avanço do mercado equino, as biotecnologias aplicadas à espécie  tiveram um grande salto, pois estas são as principais ferramentas para o melhoramento genético destes animais. Dentre essas biotecnologias a utilização o sêmen refrigerado derrubou barreiras geográficas. Dentre as muitas vantagens que o sêmen refrigerado proporciona, algumas dificuldades podem aparecer nesse processo, sendo a peroxidação um fator muito importante na refrigeração de sêmen. Sendo assim os cuidados com a exposição do sêmen ao oxigênio pode ser um fator que aumente ou diminua a fertilidade de alguns garanhões.					<B>Objetivo</B>: O presente projeto teve como propósito o objetivo de avaliar a influência do oxigênio em diferentes proporções na refrigeração do sêmen equino em caixas de transporte comerciais a uma temperatura de cinco graus Celsius, comparando motilidade, vigor, ph e patologia em três grupos distintos: sêmen armazenado com 80% de ar no frasco, sêmen armazenado com 50% de ar no frasco e sêmen armazenado na ausência de ar no frasco.					<B>Metodologia</B>: Neste experimento foi comparada a influência do oxigênio sobre a motilidade, vigor, morfologia e ph seminal em três grupos distintos: 20% sêmen e 80% ar no frasco, 50% sêmen e 50% ar no frasco e sêmen refrigerado na ausência de ar.					<B>Resultados</B>: 					<B>Conclusões</B>: Não foi encontrado diferença estatística na motilidade, vigor, ph e morfologia espermática entre grupos avaliados, entretanto mais estudos poderiam chegar a resultados diferentes avaliando um número maior de animais.				</P>
			</div><B>Palavras-chave</B>: equino, semen, refrigeração			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: With the advancement of equine market, biotechnologies applied to the species had a big jump, because these are the main tools for breeding these animals. Among these biotechnologies using refrigerated semen brought down geographical barriers. Among the many advantages that provides cooled semen, some difficulties may appear in this process, peroxidation being a very important factor in cooling semen. Therefore the care of semen exposure to oxygen can be a factor that increases or decreases the fertility of some stallions.					<B>Objectives</B>: This project aimed to to evaluate the influence of oxygen in different proportions in the refrigeration of equine semen on commercial shipping boxes at a temperature of five degrees Celsius, compared motility, vigor, and disease ph into three distinct groups: stored semen with 80% air in the bottle, semen stored with 50% air in the vial and semen stored in the absence of air in the bottle.					<B>Methods</B>: In this experiment we compared the influence of oxygen on motility, vigor, morphology and seminal ph into three groups: 20% semen and 80% air in the bottle, 50% semen and 50% air in the bottle and cooled semen in the absence of air.					<B>Results</B>: 					<B>Conclusion</B>: We found no statistical difference in motility, vigor, pH and morphology between groups assessed, however more studies could reach different results with a greater number of animals.				</P>
