			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O controle PID (proporcional, integral e derivativo) é ainda hoje predominante no meio industrial. Mais que 90% de todas as malhas existentes são do tipo PID (ou mesmo proporcional e integral, PI). Porém devido aos problemas de desempenho normalmente resultantes de uma sintonia pobre, a utilização de métodos de otimização eficientes tornou-se uma solução. Sabe-se que UAVs (Veículos Aéreos Não-Tripulados ou também denominados drones, do inglês Unmanned Aerial Vehicles) apresentam acoplamentos e comportamento não-linear. No entanto, o projeto de sistemas de controle PID podem ser promissores para aplicações em UAVs.					<B>Objetivo</B>: Neste projeto é apresentado o funcionamento de UAVs, bem como um estudo sobre o controlador PID, tendo como principal objetivo a otimização do Controlador PID através da metaheuristica de otimização denominada Grey Wolf Optimizer (GWO).					<B>Metodologia</B>: O algoritmo GWO é baseado no comportamento dos lobos cinzentos, tanto na hierarquia de liderança como no método de caça. Ele é utilizado para minimizar a função objetivo, conforme os valores das variáveis do sistema.					<B>Resultados</B>: A sintonia do controlador PID utilizando o algoritmo GWO foi possível usando um simulador do drone AR Parrot em ambiente computacional Matlab.  No entanto, a validação experimental no drone (real) ainda está em fase de pesquisa e desenvolvimento.					<B>Conclusões</B>: Metaheuristicas podem ser eficientes na sintonia de controladores e em especial para o controle de sistemas complexos e com comportamento não-linear, tais como drones. No entanto, a obtenção de uma função de transferência adequada para este tipo de processo não é uma atividade simples, pois o sistema tem dinâmica não-linear e é instável em malha aberta.				</P>
			</div><B>Palavras-chave</B>: Controlador pid. otimização. veículos aéreos não-tripulados.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The PID control (proportional, integral and derivative) is still prevalent in the industrial environment. More than 90% of all meshes that exist are PID (proportional, integral or even PI).However, due to performance problems normally resulting from a poor tuning, the efficient use of optimization methods became a solution. It is known that UAVs (Unmanned Aerial Vehicles) have couplings and nonlinear behavior. However, the PID control system design may be promising for applications in UAVs.					<B>Objectives</B>: In this project the operation of UAVs is presented as well as a study of the PID controller, with the main objective to optimize the PID controller through the optimization metaheuristic called Grey Wolf Optimizer (GWO).					<B>Methods</B>: The GWO algorithm is based on the behavior of gray wolves, both in leadership hierarchy as in the method of hunting. It is used to minimize the objective function as the values of system variables.					<B>Results</B>: It was possible to tune PID controller using the GWO algorithm in an AR drone simulator using Matlab computational environment. However, the practical validation of the optimized PID in the real drone is in the development phase.					<B>Conclusion</B>: Metaheuristics can be effective in controllers tuning and especially for the control of complex systems and nonlinear behavior, such as drones. However, obtaining an appropriate transfer function for this type of process is not a simple activity, since the system is non-linear dynamic and is unstable in open loop.				</P>
			</div><B>Keywords</B>: Pid controller. optimization. unmanned aerial vehicles.</td>
