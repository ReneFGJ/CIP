			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The need to interact socially part of any professional activity. Some occupations, such as psychologist, involving work processes that refer to the quality of social interaction, and the valued professionals
more assertive, more empathetic, more motivated, have fewer health problems and greater satisfaction with their work.					<B>Objectives</B>: Evaluate the repertoire of psychology students skills at different levels graduation.					<B>Methods</B>: Participated in this study psychology students at the beginning (1st semester), middle (5th. Semester) and end of the course (10th semester). The instruments used were the Social Skills Inventory and the Skills Questionnaire and Interpersonal Communication Skills.					<B>Results</B>: 114 students completed the questionnaire, 96 were women and 18 were men. 14 students made before graduation. The results showed that students in the last period have no index higher social skills compared to students of the first period or the middle of the course, indicating that the psychology course, by itself, does not seem socais develop skills in students. However, students do not have this perception, as 68% indicated "master" communication skills and interpersonal skills and 59% argue that the course has disciplines that develop these skills. Probably, it takes more targeted strategies for developing these skills and competencies in psychology students and to discuss with them, concepts of these skills and competencies.					<B>Conclusion</B>: It seems important that the degree in psychology promotes the development of social skills through courses or vivencias, concomitant technical and professional training, forming, thus, professionals with greater chance of career success.				</P>
