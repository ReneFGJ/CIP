			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A necessidade de interagir socialmente faz parte de qualquer atuação profissional. Algumas ocupações, como a de psicólogo, envolvem processos de trabalho que remetem a qualidade da interação social, sendo valorizados os profissionais
mais assertivos, mais empáticos, mais motivados, apresentam menos problemas de saúde e maior satisfação em relação ao seu trabalho.					<B>Objetivo</B>: Avaliar o repertório de habilidades de estudantes de psicologia em diferentes níveis da graduação.					<B>Metodologia</B>: Participaram desse estudo estudantes de psicologia do início (1º semestre), meio (5º. semestre) e final do curso (10º semestre). Os instrumentos utilizados foram o Inventário de Habilidades Sociais e o Questionário de Competências e Habilidades de Comunicação Interpessoal.					<B>Resultados</B>: De 114 alunos que preencheram os questionários, 96 eram mulheres e 18 eram homens. 14 alunos fizeram graduação anterior. Os resultados mostraram que os alunos do último período não apresentam índice de habilidades sociais mais alto quando comparados aos alunos dos primeiros períodos ou do meio do curso, indicando que o curso de psicologia, por si só, não parece desenvolver habilidades socais nos alunos. Entretanto, os alunos não tem essa percepção, já que 68% indicaram “dominar” habilidades de comunicação e habilidades interpessoais e 59% defendem que o curso possui disciplinas que desenvolvem essas habilidades. Provavelmente, são necessárias estratégias mais direcionadas para o desenvolvimento dessas habilidades e competências em estudantes de psicologia e até, discutir com eles, conceitos sobre essas habilidades e competências.					<B>Conclusões</B>: Parece importante que a graduação em psicologia promova o desenvolvimento de habilidades sociais, através de cursos ou vivencias, concomitante à formação técnica-profissional, formando, com isso, profissionais com maior chance de sucesso profissional.				</P>
			</div><B>Palavras-chave</B>: Estudantes. habilidades sociais. formação do psicólogo			<BR>
