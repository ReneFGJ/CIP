			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: As mudanças climáticas ocorridas no último século motivaram o surgimento de grupos de pesquisa e de movimentos voltados à defesa do meio ambiente e da melhoria da qualidade de vida. Sob este viés, destacou-se o estudo da ecossocioeconomia, tema abordado neste relatório.					<B>Objetivo</B>: Este trabalho buscou realizar uma discussão acerca do ecodesenvolvimento, com ênfase no movimento Slow Food. a) Contribuir na construção do conhecimento sobre a teoria e prática da ecossocioeconomia, através das leituras, da análise de fichamentos, pesquisas e cadastro de experiências;
b) Contribuir para formação acadêmica de graduandos, mestrandos, doutorandos e pós-doutorandos;
c) Amplicar a base de dados sobre experiências em ecossocioeconomia urbana; e
d) Construir um sítio eletrônico e banco digital de dados com o resultado da pesquisa.					<B>Metodologia</B>: A partir de pesquisas bibliográficas e catalogação de experiências, contribuindo na construção do conhecimento sobre a prática e a teoria da ecossocioeconomia.					<B>Resultados</B>: Foram catalogadas seis experiências norte-americanas de ecogastronomia, organizadas com mais outras 113 experiências de ecossocioeconomia, em um banco de dados para facilitar a consulta e filtragem de informações.					<B>Conclusões</B>: A expectativa por meio desta pesquisa foi, através dos seus resultados, contribuir para a melhor compreensão e clareza a respeito do papel da ecossocioeconomia no espaço gastronômico, como agente de desenvolvimento sustentável e preservação de valores socioambientais.				</P>
			</div><B>Palavras-chave</B>: Ecossocioeconomia urbana. Mudanças climáticas. Ecogastronomia. Análise de experiências.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Climate change in the last century led to the emergence of research groups and movements aimed at protecting the environment and improving the quality of life.					<B>Objectives</B>: Under this bias, the highlight was the study of ecossocioeconomia, topic covered in this report. This study attempts to make a discussion of eco-development, with emphasis on the Slow Food movement. a) To contribute to the construction of knowledge about the theory and practice of ecossocioeconomia, through readings, the fichamentos analysis, research and experience of registration;
b) contribute to academic education of undergraduate, masters, doctoral and post-doctoral students;
c) amplify the database of experiences in urban ecossocioeconomia; and
d) Build an electronic site and digital database with the search result.					<B>Methods</B>: From literature searches and experiences of cataloging, contributing to the construction of knowledge about the practice and the theory of ecossocioeconomia.					<B>Results</B>: We were cataloged six North American experiences of eco-gastronomy, organized over 113 other experiences ecossocioeconomia in a database for easy reference and filtering information.					<B>Conclusion</B>: The expectation through this research was, through its results contribute to better understanding and clarity about the role of ecossocioeconomia the dining space, such as sustainable development and preservation agent of social and environmental values.				</P>
			</div><B>Keywords</B>: Urban Ecossocioeconomia. Climate changes. Eco-gastronomy. Experiences analysis.</td>
