			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A demanda cada vez maior de tempo para ensino de estudantes e profissionais das áreas biomédicas e a evolução das expectativas de alunos com o conteúdo apresentado dificulta o ensino teórico ortodoxo de disciplinas como a farmacologia clínica. Ferramentas de e-Learning são uma nova forma de expor conteúdo que pode incrementar o ensino e otimizar a aprendizagem por meio da maior interatividade.					<B>Objetivo</B>: Tendo em vista tais pontos, o objetivo do presente projeto foi criar uma plataforma virtual, online, para aprendizagem específica dos assuntos englobados pela farmacologia clínica, com foco no aluno e sem a necessidade de um mentor específico para cada assunto abordado, voltado a estudantes da área da saúde. O trabalho também se propôs a avaliar a usabilidade dessa ferramenta.					<B>Metodologia</B>: O ambiente virtual criado utiliza um sistema de código-aberto para gerenciamento de conteúdo (WordPress) associado a plug-ins de código aberto para manutenção de cadastro e perfis com o BuddyPress, e para o gerenciamento da plataforma e-Learning com o Wp-Pro-Quis, bem como uma associação de outros plug-ins e funcionalidades adicionais possíveis de serem implementadas. Para a avaliação de usabilidade foi utilizado um formulário com obtenção de dados, criado através do Google para entrevista rápida com os usuários que atingiram a etapa final de utilização do funil de páginas desenvolvida no site.					<B>Resultados</B>: Desenvolvemos uma plataforma de estudo online voltada para o estudo de farmacologia clínica,  viável e desenvolvida com um pequeno investimento financeiro inicial.					<B>Conclusões</B>: Com a conclusão do projeto, obtivemos uma plataforma de estudo online de conteúdos acadêmicos de nível superior, viável e desenvolvida com um pequeno investimento financeiro inicial.				</P>
			</div><B>Palavras-chave</B>: E-learning. farmacologia			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The increasing demand of time for teaching students and professionals in the biomedical areas and the actual expectations of students about a different way to teach theorical content  of disciplines have been a challenge to teachers. E-learning method is consider an important tool to enhance learning when a content is exposed, considering the interactivity of this strategy.					<B>Objectives</B>: Considering  these point , the aim of this project was create a virtual platform for specific learning in clinical pharmacology, focusing on Health area student and without  the need for a specific mentor for each subject matter.					<B>Methods</B>: This project uses an open source system for content management (WordPress) associated with open source plug-ins to register new users and manage profiles with BuddyPress, and for the management of a simple e-Learning platform with the Wp-Pro-Quiz, as well as a combination of other plug-ins and additional features able to be implemented. For the usability evaluation was used a form with data collection, created by Google for quick interview with users who have reached the final stage of use of the funnel pages developed on the site.					<B>Results</B>: The development of an viable online study platform of pharmacology academic contents  was achieve and can be developed across platforms with a small initial investment.					<B>Conclusion</B>: With the completion of the project, it is clear that the development of an online study platform of academic contents of academic education is viable and can be developed across platforms with a small initial investment. Thereby, the present project showcases an inexpensive e-learning model ready to be explored and enhanced.				</P>
