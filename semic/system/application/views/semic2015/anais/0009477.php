			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Recursos educacionais sobre interfaces digitais são importantes para a discussão, problematização e práticas educacionais em torno de sistemas interativos, em especial para o resgate e compreensão da história das transformações que as interfaces sofreram ao longo do desenvolvimento das interfaces de interação entre sistemas, seus desenvolvedores, desenvolvedoras e seus usuários e usuárias.					<B>Objetivo</B>: Esta pesquisa tem como tema a história do Design de Interação. O objetivo do projeto foi o desenvolvimento de uma coleção de Recursos Educacional Abertos (REA) sobre as diferentes abordagens em torno das trajetórias históricas da Interação Humano-Computador (IHC).					<B>Metodologia</B>: Este projeto foi realizado a partir da leitura e análise de artigos de autores com diferentes propostas de compreensão história do Design de Interação e da IHC: as 4 perspectivas propostas por John Kammersgaard, as 3 ondas de Susanne Bodker, e os 3 paradigmas em Harrisson, Tatar e Sengers.					<B>Resultados</B>: Com o objetivo de auxiliar professores, estudantes e pesquisadores na leitura destes artigos, foram desenvolvidos três (03) Recursos Educacionais Abertos (REA): dois mapas conceituais e uma apresentação em slides, cada um referente a um artigo.					<B>Conclusões</B>: A coleção foi finalizada e será disponibilizada em um repositório e em uma plataforma aberta, para permitir o acesso, utilização e reutilização dos mesmos.				</P>
			</div><B>Palavras-chave</B>: Recursos Educacionais Abertos. Design de Interação. Interação Humano-Computador. História			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Educational resources on digital interfaces are fundamental to the discussion, questioning and educational practices around interactive systems, in particular for the ransom and the understanding of the history of transformations that the interfaces have suffered along the development of interaction between systems interfaces, its developers, developers and their users and users.					<B>Objectives</B>: This research has as its theme the history of interaction Design. The project objective was the development of a collection of Open Educational Resources (OER) about the different approaches around the historical trajectories of Human-Computer Interaction (HCI).					<B>Methods</B>: This project was carried out from the analysis of articles of authors with different proposals for understanding the history of interaction Design and HCI: the 4 perspectives proposed by John Kammersgaard, the 3 waves of Susanne Bodker, and the 3 paradigms into Harisson, Tatar and Sengers.					<B>Results</B>: Aiming to help teachers, students and researchers in reading these articles, were developed three (03) Open Educational Resources (OER): two topic maps and a slide presentation, each concerning an article.					<B>Conclusion</B>: The collection has been completed and will be made available in a repository and an open platform, to allow the access, use and reuse of the same.				</P>
			</div><B>Keywords</B>: Open Educational Resources. Interaction Design. Human-Computer Interaction. History</td>
