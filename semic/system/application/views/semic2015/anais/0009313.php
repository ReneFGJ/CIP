				<P>
					<B>Introduction</B>: The plant object of this study, the cattail ([e]lt;I[e]rt;Typha angustifolia L.[e]lt;/i[e]rt;), a very common plant on river banks, swamps and marshes, occurs throughout tropical and subtropical America, in this work we used the fibers formed in the ear after flowering.					<B>Objectives</B>: The plant object of this study, the cattail ([e]lt;i[e]rt;Typha angustifolia L.[e]lt;/i[e]rt;), a very common plant on river banks, swamps and marshes, occurs throughout tropical and subtropical America, in this work we used the fibers formed in the ear after flowering					<B>Methods</B>: The trials were conducted to determine the moisture content, ash, hydrophobicity,
flutuosidade, cold water solubility, fat content and waxes, fibers, oil sorption capacity and marine in deionized water and filtering through test glass column. All analyzes were performed according to prevailing
regulations.					<B>Results</B>: The literature suggests that the main disadvantage
of vegetable fibers is the sorption lot of water compared to oil. In the case
of cattail fiber, the amount of water sorbed in relation to oil was low, 1.1%.
Thus, the fiber showed a great potential as a sorbent for the oil industry,
not only by the amount of oil sucked, but also for its preference for oil,
which is due to its high content of lipids and waxes					<B>Conclusion</B>: The change of deionized water to seawater no significant effect on the results of sorption, indicating that the behavior of a particular sorption barrier would be the same in any medium. There was no significant difference in adsorption between static and dynamic systems. The fibers had a low ash content and a tendency to fast firing, constituting a favorable given the possibility for energy reuse of fibers after oil sorption.				</P>
