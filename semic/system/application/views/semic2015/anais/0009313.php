			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A planta objeto deste estudo, a Tabôa ([e]lt;i[e]rt;Typha angustifólia L.[e]lt;/i[e]rt;), uma planta muito frequente em margens de rios, brejos e pântanos, ocorre em toda a América tropical e subtropical, neste trabalho foram utilizadas as fibras formadas na espiga após a floração.					<B>Objetivo</B>: Realisamos análises químicas e físicas das fibras, avaliamos a capacidade de sorção/retenção de óleos. Com o objetivo de obter um produto sorvente alternativo de baixo custo para limpeza de águas oleosas. Desenvolver métodos de uso deste material.					<B>Metodologia</B>: Os ensaios foram realizados para determinar o teor de umidade, cinzas, hidrofobicidade,
flutuosidade, solubilidade em água fria, teor de lipídios e ceras, fibras, capacidade de sorção de óleo em água deionizada e marinha, e de filtração através de teste de coluna de vidro. Todas as analises foram realizadas seguindo as normas vigentes.					<B>Resultados</B>: A literatura aponta que a principal
desvantagem das fibras vegetais é a sorção de grande quantidade de água em
relação ao óleo. No caso da fibra de Tabôa, a quantidade de água sorvida em
reeolação ao óleo foi baixa, de 1,1%. Sendo assim, a fibra apresentou um
excelente potencial como sorvente para o setor de óleo, não só pela quantidade
de óleo sorvido, como também pela sua preferência pelo óleo, que se deve a seu
alto teor de lipídios e ceras.					<B>Conclusões</B>: A mudança de água deionizada para água marinha não exerceu efeito significativo sobre os resultados de sorção, indicando que o comportamento de uma determinada barreira de sorção seria a mesma em qualquer meio. Também não houve uma diferença significativa na sorção entre os sistemas estático e dinâmico. As fibras apresentaram um baixo teor de cinzas e uma tendência de queima rápida, constituindo um dado
favorável para a possibilidade reaproveitamento energético das fibras após sorção do óleo.				</P>
