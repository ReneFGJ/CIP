					<B>Introduction</B>: A phenomenon of recent decades observable in health in Brazil is known as legalization. It is due to the initiative of the population to make the bringing of litigation proceedings to the service assurance and fundamental constitutional rights as to the completeness of health care, aiming the effectiveness of the right to access to medicines that are not provided spontaneously by the Single System health (SUS). This phenomenon gained amplitude in the light of the rights of society instituted in the period to the Constitution of 1988. The AF of legalization brings impacts to health costs in the country because the resources to meet the legal demands can interfere with the public policy of budget Cheers.					<B>Objectives</B>: This work aims to analyze the effects of the new law (Federal Decree 7508, published in June 28, 2011, which regulates the access to medicines in SUS) on the legalization of AF.					<B>Methods</B>: he study will be conducted through the survey the number and type of shares (legal action) that determine the supply of drugs, proposed by the CEMEPAR and Health Attorney.					<B>Results</B>: 					<B>Conclusion</B>: Through this work, we observed that Umuarama Paraná is the city with the highest percentage of people with lawsuits, with 80.86% of requested drugs, are not in the RENAME, and respiratory diseases are the leading most the legalization of people to obtain their medicines.				</P>
