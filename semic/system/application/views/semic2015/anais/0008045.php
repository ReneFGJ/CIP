			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Um fenômeno das últimas décadas observável na área da saúde no Brasil é conhecido como judicialização. É decorrente da iniciativa da população de tornar litigiosa a propositura de processos judiciais visando o atendimento de garantia e direitos fundamentais consagrados constitucionalmente quanto à integralidade da atenção em saúde, visando a efetividade do direito ao acesso a medicamentos que não sejam providos espontaneamente pelo Sistema Único de Saúde (SUS). Tal fenômeno ganha amplitude, à luz da sociedade de direitos instituídos no período posterior à Constituição de 1988. A judicialização da AF traz impactos aos custos da saúde no país, pois os recursos destinados a atender as demandas judiciais podem interferir no orçamento de políticas públicas de saúde.					<B>Objetivo</B>: Este trabalho tem como objetivo analisar as repercussões do novo ordenamento jurídico (Decreto Federal 7.508, publicado em 28 de junho de 2011, que regulamenta o acesso a medicamentos no SUS) sobre a judicialização da AF.					<B>Metodologia</B>: O estudo será conduzido por meio do levantamento do número e tipo de ações (medidas judiciais) que determinem o fornecimento de medicamentos, propostas junto ao CEMEPAR e a Procuradoria de Saúde.					<B>Resultados</B>: 					<B>Conclusões</B>: : Por meio deste trabalho, foi possível verificar que Umuarama é a cidade do Paraná com maior percentual de habitantes com ações judiciais, sendo que 80,86% dos fármacos solicitados, não estão na RENAME, e que as doenças respiratórias são as que levam a maioria das pessoas a judicialização para obtenção de seus medicamentos.				</P>
			</div><B>Palavras-chave</B>: Direito a saúde, medicamentos, Assistência Farmacêutica.			<BR>
