			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Atualmente o nível de atividade física, bem como a motivação intrínseca de escolares nas aulas de educação física, têm sido temas recorrentes de estudos e publicações científicas					<B>Objetivo</B>: Frente a isso, este estudo teve como objetivo comparar o nível de motivação e de esforço nas aulas de educação física tradicional e com uso da tecnologia exergames.					<B>Metodologia</B>: Para a realização desse estudo foram necessário dois grupos para a intervenção, onde 20 alunos participaram das aulas com exergames e 14 tiveram aulas com os conteúdos tradicionais das disciplina de Educação Física. A idade média dos alunos é respectivamente de 7,35 anos e 8,21 anos. Para avaliar o nível de motivação foi utilizada uma escala elaborada pelo pesquisador, já a percepção de esforço foi avaliada com a escala de Borg adaptada e o nível de atividade física foi mesurada pelo software SOFIT.					<B>Resultados</B>: Os resultados mostram que a percepção de esforço dos alunos nas aulas de Educação Física tradicional ou com os Jogos Virtuais Ativos não apresentam diferenças significantivas entre as aulas, porém, os níveis de atividade física são bem diferentes, pois as aulas tradicionais apresentam nível moderado e vigoroso e as de exergames, nível leve e vigoroso.					<B>Conclusões</B>: O nível de motivação se mostra equilibrado, sendo que os alunos se apresentavam sempre motivados ou muito motivados para fazer as atividades desenvolvidas nas aulas de exergames ou tradicional.				</P>
			</div><B>Palavras-chave</B>: escolares, nível de motivação, exergames			<BR>
