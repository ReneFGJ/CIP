			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Currently the level of physical activity as well as the intrinsic motivation of students in physical education classes have been recurring themes of studies and scientific publications.					<B>Objectives</B>: Because of that, this study aimed to compare the level of motivation and effort in traditional physical education classes and use of exergames technology					<B>Methods</B>: To carry out this study were necessary two groups for intervention, where 20 students participated in the classes with exergames and 14 took classes with traditional contents of Physical Education. The average age of students is respectively 7.35 years and 8.21 years. To assess the level of motivation was used a scale developed by the researcher, as the perception of effort was assessed with the Borg scale adapted and the level of physical activity was gauged from the SOFIT software.					<B>Results</B>: Because of that, this study aimed to compare the level of motivation and effort in traditional physical education classes and use of exergames technology.					<B>Conclusion</B>: The level of motivation shown balanced, and the students always had motivated or very motivated to do the activities in exergames classes or traditional.				</P>
