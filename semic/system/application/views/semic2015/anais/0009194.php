			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O confisco se destaca no cenário brasileiro em virtude de ser o maior meio utilizado pelo poder estatal como modalidade de combate ao crime, por isto será estudado em seu sentido clássico.					<B>Objetivo</B>: O presente trabalho de iniciação científica se circunscreve à temática da extensão do confisco clássico como efeito da condenação penal. Frente a evolução da criminalidade contemporânea, algumas modalidades de combate ao crime se destacam: a) confisco; b) lavagem de capitais; c) recuperação de ativos e suas respectivas diferenças.					<B>Metodologia</B>: Trata-se de uma pesquisa qualitativa e bibliográfica.					<B>Resultados</B>: A doutrina espanhola apresentou o conceito de “ganancias netas e brutas” e a doutrina portuguesa a da perda alargada. Ocorre que em muitos casos, há a inversão do ônus da prova quanto aos bens supostamente obtidos (deixando o encargo ao acusado) que desvincularia o Ministério Público de sua obrigação legal, como órgão acusatório, de provar a origem ilícita dos bens, prejudicando, em todo caso, novamente, o princípio da presunção de inocência.					<B>Conclusões</B>: 				</P>
			</div><B>Palavras-chave</B>: Confisco. Condenação Penal. Direito Penal			<BR>
