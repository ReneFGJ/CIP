			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The asset forfeiture is known on the Brazilian scenery because it is the State`s most used power to combat criminality, so it will be studied on its classic sense. The extension of the loss of property is also an important theme, because it can reach even other people who didn’t exactly commit the crime.					<B>Objectives</B>: The goal of this ongoing effort is based on the extension of the classic confiscation as an effect of the criminal condemnation. Because of the evolution of the criminality, some models of crime combat were created? a) asset forfeiture; b) money laundry; c) recovery of financial assets and its differences.					<B>Methods</B>: This is a qualitative and bibliographic research.					<B>Results</B>: The Spanish scholars presented the concept of “ganancias netas e brutas” and the Portuguese scholars presented the “perda alargada”. What happens is that, in my cases, there’s and inversion of the obligation to proof the origin of the property, what would erase the obligation of the Public Ministry as an acusation organ. What would hurt the presumption of innocence.					<B>Conclusion</B>: 				</P>
