			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A degradação dos recursos hídricos é crescente. As atividades antrópicas causam alterações nas características físicas, químicas e biológicas das águas, que provocam perda da biodiversidade e resultam em grandes prejuízos econômicos.					<B>Objetivo</B>: O presente trabalho tem como objetivo avaliar a qualidade da água do rio do Ouro, situado em Ouro Verde do Oeste - PR.					<B>Metodologia</B>: Foram realizadas quatro coletas nos meses de outubro/dezembro de 2014 e março/maio de 2015, utilizando um coletor do tipo Hand Net com malha de 500µm. Dessa forma, foram coletadas quatro amostras por ponto nos diversos fluxos de água do rio (correnteza, remanso) e margens, para a padronização das mesmas.					<B>Resultados</B>: Calculados os valores de riqueza, abundância, diversidade de Shannon e equitabilidade não constatou-se diferenças significativas entre os pontos, porém, de acordo com os resultados obtidos, os locais de melhor controle ambiental – P4 de maio e P2 de março, foram os que apresentaram maiores valores para o índice riqueza (22), equitabilidade (2,221) e diversidade de Shannon (0,8671). Em contrapartida, os pontos mais impactados apresentaram resultados variados, prevalecendo abundância (P2 de outubro = 672) e dominância (P3 de maio= 0,6193).					<B>Conclusões</B>: O cálculo das métricas biológicas (riqueza de taxa, Diversidade, Equitabilidade e BMWP) possibilitou observar a boa resposta da estrutura da comunidade de macroinvertebrados bentônicos na avaliação da qualidade da água do Rio do Ouro, ressaltando que estes indicadores biológicos são sensíveis às variações físicas e químicas que ocorrem nos corpos hídricos e possibilitam uma análise completa da situação ecológica em que se encontra o ambiente de estudo				</P>
			</div><B>Palavras-chave</B>: Bioindicadores. macroinvertebrados. recursos hídricos. impactos ambientais.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The degradation of water resources is increasing. Human activities cause changes in physical, chemical and biological characteristics of the water, causing loss of biodiversity and result in large economic losses.					<B>Objectives</B>: This study aims to evaluate the quality of the Gold River water, situated in West Green - PR.					<B>Methods</B>: Four samples were taken in October / December 2014 and March / May 2015, using a type Hand Net collector mesh of 500[e]#956;m. Thus, four samples were collected by step in the various river water flows (current, backwater) and margins for the standardization of the same.					<B>Results</B>: Calculated the richness, abundance, diversity of Shannon and equitability found no significant differences between the points, however, according to the results obtained, the locations of better environmental control - P4 May and P2 March, were the They showed higher values for the wealth index (22), evenness (2.221) and Shannon diversity (0.8671). In contrast, the most affected spots showed mixed results, prevailing abundance (P2 October = 672) and dominance (P3 May = 0.6193).					<B>Conclusion</B>: The calculation of biological metrics (taxa richness, diversity, and Equitability BMWP) made it possible to observe the good response of the benthic macroinvertebrate community structure in the evaluation of the Gold River water quality, noting that these biological indicators are sensitive to physical variations and chemicals that occur in water bodies and provide a comprehensive review of the ecological situation in which we find the learning environment.				</P>
			</div><B>Keywords</B>: Bioindicators. macroinvertebrates. water resources. environmental impacts.</td>
