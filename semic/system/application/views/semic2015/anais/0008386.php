			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Com o advento da Lei 12.529/11 surgiu no direito brasileiro a análise prévia, onde um ato de concentração deve ser submetido à análise do Conselho Administrativo de Defesa Econômica (CADE) antes de sua consumação. Apesar do instituto ser importante instrumento de controle de estruturas, trouxe com ele o chamado gun jumping, que consiste na antecipação dos atos de concentração. Com isto, surge à vida empresarial e aos operadores do direito um problema que merece atenção, visto que o limite entre a prática de atos lícitos e ilícitos não é claro, restando muitas dúvidas acerca de quais atos podem ser praticados sem caracterizar a infração.					<B>Objetivo</B>: O objetivo do projeto é analisar casos no direito brasileiro e norte-americano para identificar as práticas mais comuns de gun jumping e seus efeitos à concorrência.					<B>Metodologia</B>: A presente pesquisa tem caráter bibliográfico, sendo feita a partir de textos publicados.					<B>Resultados</B>: A partir dos textos analisado, mesmo sendo impossível elaborar uma lista exaustiva de quais são os atos que podem ser considerados ilícitos, foi elaborada uma lista com alguma práticas que podem servir de referência para os interessados, como por exemplo a troca de informações concorrencialmente sensíveis, união de ativos, restrição de negócios etc.					<B>Conclusões</B>: Em uma concentração deve-se ter muito cuidado com os atos praticados, pois na realização de atos padrão pode ocorrer o gun jumping e mesmo que a prática não resulte em nenhum prejuízo, a antecipação deve ser evitada, pois por ter o potencial de causar um dano é punível independentemente da concretização do mesmo.				</P>
			</div><B>Palavras-chave</B>: CADE. análise prévia. gun jumping.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: With the enactment of Law 12529/11 came under Brazilian law prior analysis, where a merger shall be subject to examination by the Conselho Administrativo de Defesa Econômica (CADE) before its consummation. Although the institute is important control structures instrument, brought with it the so-called gun jumping, which is the anticipation of the merger. With this, it comes to business life and law professionals a problem that deserves attention, since the boundary between the practice of lawful acts and unlawful is unclear, leaving doubt as to which actions can be practiced without characterizing the offense.					<B>Objectives</B>: The aim of the project is to analyze cases in the Brazilian and US law to identify the most common practices of gun jumping and its effects on competition.					<B>Methods</B>: For this study we used a bibliographic method, from published texts.					<B>Results</B>: From the texts analyzed, even being impossible to establish an exhaustive list of what are the acts that may be considered illegal, a list was drawn up some practices that can serve as a reference for stakeholders, such as the exchange of competitively sensitive information, union assets, business restriction etc.					<B>Conclusion</B>: In a concentration, the parties must be very careful with the actions taken, because in performing standard acts can occur gun jumping and even though the practice does not result in any damage, the anticipation should be avoided because by having the potential to cause injury is punishable regardless of pursuing it.				</P>
