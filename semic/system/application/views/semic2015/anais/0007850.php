			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A canola está se tornando uma boa alternativa de produção, pois além da produção de óleo vegetal vem sendo utilizada como rotação de culturas na estação de inverno no sul do Brasil, agregando importância econômica e social. Embora o Estado conte com o zoneamento agrícola para a cultura, há necessidade de se realizar estudos sobre as épocas de semeadura mais apropriadas para o seu cultivo no Oeste do Paraná.					<B>Objetivo</B>: O objetivo do trabalho foi avaliar o desempenho de cinco híbridos de canola em seis diferentes épocas de semeadura.					<B>Metodologia</B>: O delineamento experimental utilizado foi o de blocos casualizados em esquema fatorial 5 x 6, constando de cinco híbridos (hyola 61, hyola 76, hyola 411, hyola 433, hyola 571) e seis épocas de semeadura (24/02/2014; 12/03/2014; 02/04/2014; 23/04/2014; 14/05/2014; 04/06/2014), com tres  repetições. O experimento foi instalado no mês de fevereiro, na unidade experimenta da PUCPR – Campus Toledo. Foi avaliado o desempenho dos híbridos quanto à emergência, florescimento, maturação, altura de planta, massa de síliquas por planta, massa por síliqua, massa de grãos por síliqua, produtividade, peso de mil grãos.					<B>Resultados</B>: Os resultados demonstram que todos os híbridos avaliados apresentam desempenho semelhante nas condições edafoclimáticas em que foi desenvolvido o experimento.					<B>Conclusões</B>: É possível concluir que a produtividade é superior quando os híbridos são semeados nas épocas de 23 de abril e 14 de maio .				</P>
			</div><B>Palavras-chave</B>: Brassica napus. Massa de mil grãos. Produtividade. Teor de óleo. Zoneamento Agrícola.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Canola is becoming a good alternative production, because besides the production of vegetable oil, has been used as crop rotation in the winter season in southern Brazil, bringing economic and social importance. Although the state rely on the agricultural zoning for culture, it is necessary to carry out research into the most appropriate sowing dates for its cultivation in western Paraná.					<B>Objectives</B>: Thus, the goal of this study was to evaluate the performance of five canola hybrids in six different sowing dates.					<B>Methods</B>: The experimental design was a randomized complete block in a factorial 5 x 6, consisting of five hybrids (Hyola 61, Hyola 76, Hyola 411, Hyola 433, Hyola 571) and six sowing dates (24/02/2014; 12 / 03/2014; 02.04.2014; 04.23.2014; 05.14.2014; 06.04.2014), with three repetitions. The experiment was conducted in February, in the unity of experience PUCPR - Toledo Campus. The hybrid performance as the emergency was evaluated, flowering, maturity, plant height, mass of siliques per plant, weight per pod, grain weight per pod, productivity, thousand grain weight.					<B>Results</B>: The results showed that all hybrids have similar performance at conditions in which the experiment was developed.					<B>Conclusion</B>: However, it is possible to conclude that productivity is higher when hybrids are seeded in times of April 23 and May 14.				</P>
			</div><B>Keywords</B>: Brassica napus. Thousand grain weight. Productivity. Oil content. Agricultural zoning.</td>
