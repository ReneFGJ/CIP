			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O aborto nada mais é que a interrupção intencional ou não da gestação, que resulta na morte do feto. O aborto espontâneo pode ser causado por fatores naturais ou químicos, alheios a vontade da parturiente. O aborto provocado é intencional, recorrendo as gestantes ao uso de fármacos e cirurgias, que por muitas vezes resultam em morte materna, sendo esta a quarta causa de morte materna no Brasil. Estudos sobre aborto de acordo com as características das mulheres podem ajudar a identi[e]#64257;car subgrupos com necessidade especial de serviços e orientação para prevenir gravidez não planejada.					<B>Objetivo</B>: O presente estudo objetivou estimar a prevalência de mulheres que praticaram o aborto provocado em uma instituição universitária localizada na região oeste do Paraná.					<B>Metodologia</B>: Foram convidados a participar do estudo discentes de cursos diversos, que após o preenchimento do termo de livre esclarecido foram submetidos a um questionário estruturado para ser auto respondido, contendo apenas perguntas fechadas.					<B>Resultados</B>: Dentre os 300 entrevistados não houve nenhum caso de aborto provocado registrado, divergindo com o perfil bibliográfico dos levantamentos epidemiológicos. Tendo em vista que 69,69 % do publico entrevistado eram mulheres e 30,30 % eram homens, aonde  47,12% concorda com o aborto em alguns casos, levando em conta que 84,50 adotam cristianismo como religião.					<B>Conclusões</B>: Não houve nenhum entrevistado que tenha praticado o aborto espontâneo.				</P>
			</div><B>Palavras-chave</B>: Aborto . Epidemiologia. Gravidez			<BR>
