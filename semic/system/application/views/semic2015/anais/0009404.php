			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Abortion is nothing more than a deliberate interruption of pregnancy or not, that results in the death of the fetus. Spontaneous abortion can be caused by natural factors or chemical unrelated to the will of the mother. Induced abortion is intentional, using pregnant women to the use of drugs and surgery, which often result in maternal death and charged as the third and fourth most common cause of maternal mortality in Brazil. Studies on abortion according to the characteristics of women can help identify subgroups with special needs services and guidance to prevent unplanned pregnancy.					<B>Objectives</B>: This study aimed to estimate the prevalence of women who had induced abortion in a university located in the western region of Paraná					<B>Methods</B>: They were invited to join the students study various courses, which after filling the free savvy term underwent a structured questionnaire to be self answered, containing only closed questions.					<B>Results</B>: Among the 300 respondents there were no cases of induced abortion record, diverging with the bibliographic profile of epidemiological surveys. Considering that 69.69% of the public interviewed were women and 30.30% were men, where 47.12% agreed with abortion in some cases, taking into account that 84.50 adopt Christianity as a religion.					<B>Conclusion</B>: There was no respondent who has practiced abortion.				</P>
