			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O processo KDD representa uma alternativa para apoiar a decisório, mas não constitui prática regular nas atividades da Saúde.					<B>Objetivo</B>: Esta pesquisa tem como objetivo avaliar ferramentas de KDD, a partir de estudos de caso, envolvendo situações de gestão na área da Saúde.					<B>Metodologia</B>: Foram realizados dois experimentos para avaliar especificamente uma estratégia para pós-processar padrões considerando temporalidade. O algoritmo avaliado foi AssocTemp. O primeiro objetivou identificar beneficiários de alto custo em operadores de saúde. Para esta avaliação os resultados obtidos foram analisados por especialistas em auditoria de contas médicas. O segundo estudo de caso foi sobre situações que contribuem para atrasos em procedimentos cirúrgicos. Este segundo conjunto de resultados foi avaliado por gestores de um hospital de Curitiba.					<B>Resultados</B>: A partir das duas avaliações foi possível identificar a necessidade de geração de duas novas medidas, a implementação de uma janela máxima e mínima para análise das regras.					<B>Conclusões</B>: A partir dos resultados obtidos, fica clara a necessidade de que as estratégias propostas pelo processo KDD sejam constantemente avaliadas sob a perspectiva da área do conhecimento para a qual se esteja aplicando.				</P>
			</div><B>Palavras-chave</B>: KDD, Pós-Processamento, Mineração de dados Temporal			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The KDD process is an alternative to support the decision making process, but it is not a regular practice in health care activities.					<B>Objectives</B>: This research aims to evaluate KDD tools, from case studies, involving management situations in healthcare.					<B>Methods</B>: Two experiments were conducted a specifically evaluate a strategy for post-process standards considering temporality. The algorithm evaluated was AssocTemp. The first aimed to identify beneficiary’s costly health operators. For this evaluation, experts, in auditing medical bills, analyzed the results. The second case study was on factors that contribute to delays in surgical procedures. Managers of a hospital in Curitiba evaluated this second set of results.					<B>Results</B>: From the two assessments was possible to identify the need to generate two new measures, the implementation of a maximum and minimum window for review of rules.					<B>Conclusion</B>: There is a clear need that the KDD strategies are constantly evaluated considering specific knowledge area for which they are applying.				</P>
			</div><B>Keywords</B>: KDD, Post-Processing, Temporal Data Mining</td>
