			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Atualmente, preocupa-se muito com questões ambientais, principalmente quanto aos gases poluentes que são emitidos pelos meios de transporte de grande porte, por exemplo. Esses gases possuem nitrogênio e enxofre em suas composições, os quais são derivados do combustível fóssil.					<B>Objetivo</B>: No intuito de amenizar essa forma de agressão ambiental, utiliza-se a hidrodessulfurização (HDS) para diminuir a concentração desses componentes no óleo diesel; em complemento, estudou-se o método de adsorção com carvão ativado (puro e impregnado com cobre ou paládio), a fim de melhorar remoção dos compostos sulfonados e nitrogenados.					<B>Metodologia</B>: Para isso, foram realizados ensaios de adsorção com carvão puro, impregnado com cobre e impregnado com paládio. Para os três adsorventes foram avaliados o tempo de equilíbrio e isotermas em batelada.					<B>Resultados</B>: Com os dados obtidos, adotou-se o tempo de equilíbrio de 24 h e, então, montou-se a curva da isoterma com o intuito de descrever o comportamento adsortivo e analisar qual modelo, dentre Langmuir e Freundlich, melhor o descreve.					<B>Conclusões</B>: Os resultados evidenciam que para a maioria dos ensaios, a melhor modelagem matemática foi de Frendlich.				</P>
			</div><B>Palavras-chave</B>: Óleo Diesel. Carvão Ativado. Adsorção. Isoterma.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Nowadays there is a big preoccupation about polluting gases that are expelled by trucks and buses.					<B>Objectives</B>: In these gases there are nitrogen and oxygen compounds, which are from fossil fuel. It is used the hydrodesulfurization (HDS) to decrease the concentration of these compounds in the diesel oil and not to be aggressive to the environment. Adsorption with activated coal (pure or impregnated with copper or palladium) is used as complement of HDS and through this method nitro and sulfur compounds are removed from the fuel.					<B>Methods</B>: For this purpose, adsorption experiments were conducted with pure carbon impregnated with copper and impregnated with palladium. For the three adsorbents were evaluated equilibrium isotherms of time and batch.					<B>Results</B>: With the results the time of 24 h was adopted for forward experiments, in order to construct the isotherm curve, that describes the adsorptive behavior, and determine which model between Langmuir and Freundlich corresponds better to the experiment.					<B>Conclusion</B>: Major of the experiments show that Freundlich is the better mathematical model.				</P>
			</div><B>Keywords</B>: Diesel Oil. Activated Coal. Adsorption. Isotherm.</td>
