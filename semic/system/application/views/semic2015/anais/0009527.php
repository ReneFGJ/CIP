			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A utilização de instrumentos geotécnicos para monitoramento de áreas instáveis é importante, pois através deles é possível prever e analisar a ocorrência de movimentos de massa, de modo que haja tempo hábil para tomadas de decisão que previnam possíveis deslizamentos. Como principais agentes causadores destes movimentos podem ser apontados a presença de água no solo e a poropressão. A fibra óptica surge como uma nova tecnologia empregada na otimização do monitoramento geotécnico.					<B>Objetivo</B>: Neste sentido, o principal objetivo deste projeto foi a instalação e realização de testes em campo de uma instrumentação geotécnica baseada na tecnologia de fibra óptica, para medição da poropressão no solo.					<B>Metodologia</B>: Foi realizado a instalação de piezômetros com base na tecnologia de fibra óptica na encosta OLAPA localizada no município de Morretes, para o monitoramento regular do nível d’agua e poropressão da encosta. Para isto foi utilizado o equipamento convencional chamado PIU. Ainda, foi realizado o monitoramento do mesmo instrumento a partir da nova tecnologia.					<B>Resultados</B>: Após a obtenção dos dados do instrumento, verificou-se a proximidade dos resultados comprovando a eficácia da tecnologia de fibra óptica como base para o monitoramento geotécnico.					<B>Conclusões</B>: As diferenças entre as leituras convencionais e com a tecnologia de fibra óptica obtidas mostraram-se dentro de um erro aceitável.				</P>
			</div><B>Palavras-chave</B>: Monitoramento geotécnico. fibra óptica. piezômetro			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The use of geotechnical instruments for monitoring of unstable areas is important because through them one can predict and analyze the occurrence of mass movements, so that there is time for decision-making to prevent possible landslides. As main agents of these movements can be pointed out the presence of water in the soil and porepressure. The optical fiber comes as a new technology used in the optimization of geotechnical monitoring.					<B>Objectives</B>: In this sense, the main objective of this project was the installation and testing in the field of a geotechnical instrumentation based on fiber optic technology for measuring the porepressure in the soil.					<B>Methods</B>: : It carried out the installation of piezometers based on fiber optic technology in Olapa hillside in the Morretes city, Parana State, for the regular monitoring of the water level and porepressure slope. For this it was used the conventional equipment called PIU. In addition, it was carried out monitoring of the same instrument from the new technology.					<B>Results</B>: After obtaining the data from the instrument, it is the proximity of the results proving the effectiveness of fiber optic technology as a basis for geotechnical monitoring.					<B>Conclusion</B>: The differences between the readings and the conventional fiber optic technology obtained were within an acceptable error.				</P>
			</div><B>Keywords</B>: Geotechnical monitoring. optical fiber. piezometer</td>
