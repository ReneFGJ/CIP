			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Com a recente popularização dos UAVs (Veículos Aéreos Não-Tripulados, do inglês Unmanned Aerial Vehicle), ou simplesmente drones, houve um grande interesse na comunidade científica para atender às novas possibilidades existentes na área da robótica colaborativa. A robótica colaborativa é um importante ramo de pesquisa da robótica, pois possibilita que múltiplos agentes executem ações complexas ou mesmo impossíveis de serem executadas por um único robô. Assim, fica evidente que a comunicação entre robôs é uma questão essencial para a execução destas atividades colaborativas de forma eficiente.					<B>Objetivo</B>: Os objetivos foram de estudar e projetar um protótipo visando a comunicação entre um UAV (do tipo drone) e robôs móveis, com o intuito de realizarem tarefas colaborativas.					<B>Metodologia</B>: Inicialmente é feito um estudo dos principais meios e protocolos de comunicação, em seguida desenvolve-se um protótipo para estabelecer a comunicação entre os agentes.					<B>Resultados</B>: A comunicação só pôde ser estabelecida em um sentido, de forma que o robô móvel possa enviar comandos ao drone.					<B>Conclusões</B>: A escolha errada do drone a ser utilizado limitou o sistema de comunicação a um único sentido. Para solucionar este problema, sugere-se o  desenvolvimento de todo o sistema (Hardware e Software) desde seu estágio inicial, possibilitando a comunicação da maneira como foi proposta.				</P>
			</div><B>Palavras-chave</B>: Robótica colaborativa. sistemas de comunicação. veículos aéreos não-tripulados.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: With the recent popularization of UAVs (Unmanned Aerial Vehicles) , or just drones , there was great interest in the scientific community to bring new possibilities in the collaborative robotic’s field. The collaborative robotic is an important robotic’s research theme because it enables multiple agents to perform complex, or even impossible, actions to be executed by a single robot. Due this, it is evident that communication between robots is the key for the efficient implementation of these collaborative activities.					<B>Objectives</B>: The objective of this project is to conduct the study and design of a prototype aimed at communication between a UAV and mobile robots in order to perform collaborative tasks .					<B>Methods</B>: The study of communication forms and its advantages and limitations was adopted in this design. Initially, it is made a study of the connectivity media and communication protocols, then develops a prototype for establishing communication between agents.					<B>Results</B>: The communication could only be established in one direction, allowing just the mobile robot send commands to the drone.					<B>Conclusion</B>: The development of the full system (hardware and software ) is in a initial stage after this project. However, the development of a full system is a very complex task and it is beyond the scope of this project. In this context, The wrong choice of drone being used, limited the communication system to a single direction. To solve this problem, it is recommended the development of the whole system (hardware and software) since early stages, enabling the communication the way it was proposed.				</P>
			</div><B>Keywords</B>: Collaborative robotics. unmanned aerial vehicles. communication systems.</td>
