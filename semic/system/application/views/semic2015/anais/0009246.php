			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A proposta da Unesco e a orientação do MEC para a graduação no curso de Medicina para o século XXI estabelecem a importância da formação humanística do profissional de saúde. Contudo, ao concluir o curso, o profissional mostra um perfil predominantemente técnico e com formação ética insatisfatória.					<B>Objetivo</B>: Verificar a auto-percepção de acadêmicos de uma escola privada de medicina sobre temas relacionados à Ética e Bioética; Analisar o padrão de conhecimento destes acadêmicos em relação à fase do curso (Pré-Clínica, Clínica e Internato Médico); Comparar os dados destes achados entre a universidade pública e privada no estado do Paraná.					<B>Metodologia</B>: Aplicação de questionário contendo questões de múltipla escolha sobre a formação de acadêmicos referentes aos temas de ética e bioética oferecidos durante o período de graduação do curso médico.					<B>Resultados</B>: Os resultados mostram que a percepção dos acadêmicos referentes aos conteúdos de ética e bioética é de que o ensino dessas disciplinas ao longo da graduação é insuficiente e, de acordo com a maioria dos entrevistados, as mesmas deveriam ser obrigatórias ou ofertadas ao longo do curso.					<B>Conclusões</B>: Observou-se a deficiência no conhecimento sobre temas relacionados à ética e bioética, sendo insuficiente para responder aos dilemas morais apresentados pelos novos avanços das ciências e não atendendo às necessidades atuais de formação do profissional médico.				</P>
			</div><B>Palavras-chave</B>: Ética Médica. Bioética. Ensino. Medicina.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The recommendations from both UNESCO and Education Ministry from Brazil highlight the importance of humanistic-based formation of health professionals from the XXI century. However, at the end of the course, the  professionals usually show  a predominant technical and poor ethics training profile.					<B>Objectives</B>: This study aimed to assess the knowledge of medical undergraduate students from different stages of the medical course (Pre-Clinical, Clinical and Medical Internship) about topics concerning ethics and bioethics as well as to compare these findings from a public and private university from Parana state.					<B>Methods</B>: At this cross-sectional study, a structured questionnaire was used to asses the students’ knowledge about ethics and bioethics issues.					<B>Results</B>: It was observed that students from medical internship reported that the topics from these disciplines are unsatisfactory (Fisher-Freeman-Halton Test, p = 0.0001). The students think that 
These disciplines should be obligatory or offered throughout the course. Regarding knowledge on specific contents of Ethics and Bioethics, it was found that students receive more information concerning regulatory aspects and human rights. However, the major doubts of the students lay down aspects about changing the paradigm from 
cure to care.					<B>Conclusion</B>: There is a lack of knowledge of medical students on topics related to ethics and bioethics, being this knowledge insufficient to accomplish for the requested medical profile to deal with moral dilemmas presented by science advances.				</P>
