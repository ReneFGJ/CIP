			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O projeto “Karl Rahner em diálogo com a cultura moderna”
investigou a relação do teólogo alemão com a sociedade de seu tempo. O seu
modo de fazer teologia está em estreita relação com a compreensão de mundo,
de ser humano e com os novos desafios enfrentados pelo discurso cristão na
modernidade.					<B>Objetivo</B>: Investigar o interesse do teólogo alemão em dialogar
com a cultura moderna tendo em vista o primado da razão em detrimento da fé.
Por que Karl Rahner quer falar do Deus cristão a essa cultura? Como e onde
estabelece pontos de diálogo entre a sua teologia e a modernidade?					<B>Metodologia</B>: A pesquisa é de cunho teórico. Foram realizadas leituras e fichamentos de obras
do autor estudado e de comentadores e confronto das informações e saberes
apreendidos.					<B>Resultados</B>: Rahner encontra o ponto de diálogo com a cultura
moderna no ser humano. Este, central no discurso moderno, é o lugar da
revelação e do externar-se, do dizer-se de Deus. Preocupado com o
racionalismo científico, Karl Rahner propõe, com bases antropológicas, um novo
modo para o discurso teológico. Mais coerente com seu tempo e mais adequado
ao diálogo com a cultura moderna, o teólogo fala de Deus com a linguagem da
ciência.					<B>Conclusões</B>: A virada antropológica realizada por Rahner na teologia
estabeleceu novas bases para dizer a fé cristã: a humanidade. Falar de Deus é
falar do lugar onde ele se encontra: a carne humana, a proximidade do mistério
divino no mundo. Isso dá credibilidade e espaço para, ainda hoje, falar da fé
cristã em todos os ambientes, sobretudo nas atuais e desafiantes realidades.				</P>
			</div><B>Palavras-chave</B>: Karl Rahner. Diálogo. Modernidade. Autocomunicação.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The project "Karl Rahner in dialogue with modern culture"
investigated the German theologian relationship with the society of his time. Your
way of doing theology is closely related to understanding the world of human
beings and with the new challenges facing the Christian discourse on modernity.					<B>Objectives</B>: To investigate the German theologian interest in dialogue with
modern culture in view of the primacy of reason over faith. Why Karl Rahner
wants to speak of the Christian God to that culture? How and where to establish
points of dialogue between his theology and modernity?					<B>Methods</B>: The research is
of theoretical nature. Fichamentos readings and works of the author studied and
commentators and comparison of information and knowledge learned were held.					<B>Results</B>: Rahner is the point of dialogue with modern culture in humans. This,
central in modern discourse, is the place of revelation and up-express, of saying
to God. Concerned about the scientific rationalism, Karl Rahner proposes, with
anthropological bases, a new way for theological discourse. More consistent with 
your time and best suited to dialogue with modern culture, theologian speaks of
God with the language of science.					<B>Conclusion</B>: The anthropological turn
performed by Rahner´s theology established new bases to tell the Christian faith:
mankind. To speak of God is to speak of the place where he is: human flesh, the
proximity of the divine mystery in the world. This gives credibility and room for
even today speak of the Christian faith in all environments, especially in the
current and challenging realities.				</P>
			</div><B>Keywords</B>: Karl Rahner. Dialogue. Modernity. Self-communication.</td>
