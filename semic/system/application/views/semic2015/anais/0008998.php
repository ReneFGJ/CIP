			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O presente trabalho aborda a lavagem de dinheiro e o fortalecimento da atuação dos servidores públicos, que desencadeou na alteração da Lei 9.613/1998, com a entrada em vigor da Lei 12.683/2012. O referido dispositivo determina o afastamento automático do servidor público indiciado, não deixando margem a apreciação judicial do caso concreto para verificar sua necessidade. O estudo abrange algumas questões da ADIN 4911 contra o art. 17-D da Lei 9.613/1998. No mais, o impasse se perfaz ante a punição antecipada fruto de subjetivismo da autoridade policial, há apontamentos sobre a ofensa aos princípios do contraditório e da ampla defesa, presunção de inocência, segurança jurídica, razoabilidade e da proporcionalidade e das competências constitucionais do Ministério Público e do Poder Judiciário. Além disso, destaca a imprescindibilidade da cooperação jurídica internacional e privada para repreender a expansão da lavagem de capitais.					<B>Objetivo</B>: A principal finalidade do relatório científico é analisar a constitucionalidade do Artigo 17-D da Lei de Lavagem de dinheiro que prevê o afastamento do servidor indiciado na lavagem de dinheiro. Além disso, ressaltar a importância da cooperação jurídica internacional e privada devido à complexidade do crime.					<B>Metodologia</B>: Apurou-se por meio de pesquisa em doutrinas específicas sobre o tema, reconhecendo, o necessário estudo sobre as questões relacionadas à constitucionalidade do dispositivo, abordando reflexões sobre o tema utilizando-se de análises  de jurisprudências e artigos científicos para a formação de conclusões críticas.					<B>Resultados</B>: 					<B>Conclusões</B>: 				</P>
