			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Ligas acadêmicas de medicina são atividades extracurriculares desenvolvidas pelos estudantes de medicina. Elas são organizações acadêmicas, sem fins lucrativos, afiliadas aos respectivos e seus respectivos hospitais escolas.					<B>Objetivo</B>: O objetivo específico desse projeto de pesquisa foi desenvolver um sistema organizacional para a Liga do Trauma do Hospital Universitário Cajuru.					<B>Metodologia</B>: O trabalho consiste na programação de um web site onde todas as informações referentes ao interesse e organizações dos acadêmicos participantes da liga acadêmica. O site foi programado em linguagem C#, usando plataforma ASP.NET, ferramenta Microsoft Visual Studio 2013 e Microsoft SQL Server Express 2008 para seu desenvolvimento					<B>Resultados</B>: Com o auxílio de um profissional da área, foi elaborado no período de um ano, o website.					<B>Conclusões</B>: Com o intuito de facilitar a organização dos estudantes, o web site possui todas as informações referentes ao interesse e organizações dos acadêmicos participantes de ligas acadêmicas, tais como, apresentação de novidades, organização de escalas e gerenciamento de trocas de plantão.				</P>
			</div><B>Palavras-chave</B>: Ligas acadêmicas. medicina. web site. organização de escalas.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Academic medical alloys are one of the most important extracurricular activities by medical students. They are academic, nonprofit, affiliated to their respective hospitals and schools.					<B>Objectives</B>: The specific objective of this research project was to develop an organizational system for Trauma League University Hospital Cajuru.					<B>Methods</B>: The work consists of programming a web site where all the information regarding the interest of academics and organizations participating in an academic league will need during your internship. The site was programmed in C # using ASP.NET platform, Microsoft Visual Studio 2013 and Microsoft SQL Server Express 2008 tool for development.					<B>Results</B>: With the help of a professional in the area, was developed in the period of one year, the website. They were raised prerequisites defined together with the managers of the league, fellows and systems analyst.					<B>Conclusion</B>: In order to facilitate the organization of students, the web site has all the information regarding the interest of the participating organizations and scholars from academic leagues, such as presentation of news, organizing the call of duty management changes.				</P>
			</div><B>Keywords</B>: Academic leagues. medicine. websites. scales of organization.</td>
