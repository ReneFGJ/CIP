			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Pretendemos explicitar nessa pesquisa o papel da
filosofia na obra Tractatus Lógico-Philosophicus de Ludwig
Wittgenstein, atentando para as influências na temática do “místico”.
Seu pensamento articulou questionamentos direcionados as
questões eminentemente tradicionais, sobretudo, as do âmbito ético.
O trato inovador de sua filosofia permitiu a caracterização das até
então questões, ou problemas, como pseudoquestões,
pseudoproblemas. A origem desses problemas na compreensão de
Wittgenstein se dá precisamente porque repousamos no mau
entendimento da lógica de nossa linguagem. Portanto, o que motivou
seu Tractatus será o empreendimento de estabelecer as condições
de uma linguagem com sentido, dizível.					<B>Objetivo</B>: A organização de
suas ideias parte de dois pressupostos últimos, a concepção de
mundo e de linguagem; filósofo concebe a linguagem como
representadora da totalidade das proposições, e mundo a totalidade
dos fatos empíricos.					<B>Metodologia</B>: A possibilidade dessa configuração,
entre essas duas dimensões mundo e linguagem, se dá
isomorficamente; quer dizer que existe uma condição comum e
fundamental para tal relação, é o compartilhamento de uma mesma
forma.					<B>Resultados</B>: a forma lógica, que por sua vez não pode ser dita,
mas se mostra. A filosofia sob a ótica tractariana é encarregada de
ser crítica de nossa linguagem, de discernir por meio de determinados
critérios a genuinidade de suas proposições, para que não sejam
utilizadas equivocadamente.					<B>Conclusões</B>: Do próprio interior da
linguagem o filósofo realiza um exame de reconhecimento daquilo que
pertence as suas possibilidades de abordagem clara, com sentido; em
suma, se trata de um exercício de análise lógica. Ao ter sido
esclarecido o dizível, simultaneamente, evidencia-se o indizível,
contudo, conforme salienta Ludwig Wittgenstein, deve-se calar a falar daquilo que  não se pode dizer.				</P>
			</div><B>Palavras-chave</B>: Wittgenstein. Linguagem. Mística. Tractatus.			<BR>
