			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O aumento das pesquisas na análise do comportamento sobre a compreensão do comportamento simbólico vem crescendo nos últimos anos.					<B>Objetivo</B>: Este estudo teve como objetivo verificar se as relações autoclíticas interferem na formação e velocidade de classes de equivalência de estímulos, e se as relações autoclíticas são formadas a partir da formação de classe de equivalência de estímulos.					<B>Metodologia</B>: Participaram da pesquisa seis crianças que foram divididas aleatoriamente em dois grupos. O Grupo 1 passou por 5 etapas: Screening (nomeação de estímulos), modelagem de relações autoclíticas (era modelado o repertório verbal para que a criança utilizasse o autoclítico relacional “Vai com”/”Combina com”), treino de simetria, teste de equivalência e teste de relações autoclíticas. E o Grupo 2 passou pelas mesmas etapas, exceto a modelagem de relações autoclíticas.					<B>Resultados</B>: O Grupo 1, nos testes de equivalência, acertou noventa por cento do teste, considerando que todos conseguiram realizar os testes. no grupo 2, apenas o participante P6 conseguiu alcançar uma taxa de acertos para ser considerada uma relação confiável de equivalência entre estímulos. os participantes P4 e P5 não alcaçaram oitenta por cento de taxa de acertos nos testes de equivalência de estímulos. Os resultados obtidos evidenciam que a formação de classes de equivalência de estímulos é influenciada pelo treino individual de relações autoclíticas.					<B>Conclusões</B>: Não obstante, nota-se que a velocidade de formação das classes equivalentes foi mais rápida, comparando o tempo de formação entre os grupos, quando houve treino  anterior (Grupo 1) às tarefas de equivalência com autoclíticos relacionais, demonstrando que uma história de treinamento neste operante verbal pode facilitar e acelerar o processo de formação de classes de equivalência de estímulos.				</P>
			</div><B>Palavras-chave</B>: Comportamento verbal. Autoclítico relacional. Equivalência de estímulos			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The increase in research in behavior analysis on the understanding of symbolic behavior has been growing in recent years					<B>Objectives</B>: This study aimed to verify if the autoclitic relations interfere in the formation and speed of stimulus equivalence classes, and the autoclitic relationships are formed from the stimulus equivalence class formation					<B>Methods</B>: The participants were six children who were randomly divided into two groups. Group 1 underwent five stages: Screening (naming stimuli), modeling autoclitic relations (was modeled verbal repertoire so the child utilizasse relational autoclitic "Go to" / "Matches"), symmetry training, testing autoclitic equivalence test and relations. And the Group 2 passed through the same steps, except the modeling autoclitic relations					<B>Results</B>: Group 1, the equivalency testing hit ninety percent of the test, whereas all able to perform the tests. in group 2, only the participant P6 has achieved a hit rate to be considered a reliable relationship of equivalence between stimuli. participants P4 and P5 not alcaçaram eighty percent hit ratio in stimulus equivalence tests. The results show that the formation of stimulus equivalence classes is influenced by individual training autoclitic relations					<B>Conclusion</B>: However, note that the speed of forming the equivalence classes was more rapid, comparing the time of formation of the groups, when there has been previous practice (group 1) with the equivalent relational autoclitics jobs, demonstrating that a training history in this verbal operant can facilitate and accelerate the formation of stimulus equivalence classes.				</P>
			</div><B>Keywords</B>: Verbal behavior. autoclitic relational. stimulus equivalence</td>
