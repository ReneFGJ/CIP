			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O projeto objetiva verificar a influência da procrastinação acadêmica na autoavaliação de desempenho discente. Para atender o objetivo do estudo, foi realizada uma pesquisa quantitativa de levantamento com 469 casos. O instrumento de pesquisa foi pautado nos estudos de Costa (2007), Freitas e Arica (2008) e Amaro (2014). O questionário foi aplicado aos discentes de uma universidade privada de Curitiba, PR, ligados à área de Ciências Sociais Aplicadas, especificamente nos Cursos de Ciências Contábeis, Administração, Ciências Econômicas, Marketing e Turismo. Após a revisão da literatura sobre o tema e a coleta e tratamentos dos dados obtidos no levantamento, na sequencia, decorreu-se na análise dos resultados. A analise dos dados se deu por meio de estatística descritiva; no que compete aos testes multivariados, utilizou-se a análise fatorial exploratória para validar a dimensionalidade das escalas, análise fatorial confirmatória para validar o modelo de mensuração e para testar as hipóteses do estudo utilizou-se a modelagem de equações estruturais com auxilio dos softwares IBM SPSS® e IBM AMOS®.					<B>Objetivo</B>: Verificar a influência da procrastinação acadêmica na autoavaliação de desempenho discente.					<B>Metodologia</B>: Pesquisa descritiva de abordagem quantitativa					<B>Resultados</B>: Os resultados revelam que a procrastinação acadêmica exerce influência negativa e significativa na autoavaliação de desempenho discente, aceitando a hipótese do estudo.					<B>Conclusões</B>: Conclui-se que a procrastinação exerce uma influência negativa e significativa na autoavaliação de desempenho discente. Esta pesquisa contribui para o avanço dos estudos sobre procrastinação ao elucidar o impacto deste fenômeno na própria percepção de desempenho do estudante.				</P>
			</div><B>Palavras-chave</B>: Procrastinação. Ensino superior. Autoavaliação de desempenho.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The project aims to investigate the influence of academic procrastination in self-assessment of student performance. To meet the objective of the study, a quantitative research survey with 469 cases was performed. The research instrument was marked in the studies of Costa (2007), Freitas and Arica (2008) and Amaro (2014). The questionnaire was administered to students of a private university in Curitiba, PR linked to the area of [e]#8203;[e]#8203;Social Sciences, specifically in courses of Accounting, Management, Economics, Marketing and Tourism. After reviewing the literature on the subject and the collection and treatment of the data obtained in the survey, in sequence, it is held on the analysis of the results. The data analysis was made through descriptive statistics; Racing in the multivariate tests, we used exploratory factor analysis to validate the dimensionality of the scales, confirmatory factor analysis to validate the measurement model and to test the hypotheses of the study used the structural equation modeling with the help of IBM software SPSS and IBM AMOS®.					<B>Objectives</B>: Verify the influence of academic procrastination in self-evaluation student performance					<B>Methods</B>: Descriptive design approach					<B>Results</B>: The results showed that academic procrastination exerts a significant negative influence on the self-assessment of student performance , accepting the hypothesis of the study.					<B>Conclusion</B>: It concludes that procrastination has a negative and significant influence on the self-evaluation student performance. This research contributes to the advancement of studies on procrastination to elucidate the impact of this phenomenon in the very perception of student performance.				</P>
			</div><B>Keywords</B>: Procrastination. Higher education. Assessment student performance.</td>
