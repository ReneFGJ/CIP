			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A Alimentação Complementar é de suma importância no desenvolvimento da criança nas primeiras fases de sua vida, podendo implicar em repercussões a curto e longo prazo. É sabido que um adequado hábito alimentar durante os primeiros anos pode prevenir desnutrição, deficiência de micronutrientes e também obesidade.					<B>Objetivo</B>: Analisar o emprego da alimentação complementar em diferentes grupos da população de Curitiba, PR, procurando identificar os períodos de aleitamento materno exclusivo e complementado; o início e tipo de alimentação complementar e a avaliação antropométrica.					<B>Metodologia</B>: Foi realizado um estudo observacional transversal, através da avaliação de um questionário e dados antropométricos (peso e altura) de 13 crianças de 0 a 2 anos de idade que frequentam a rede particular de ensino de Curitiba, PR, comparando os achados com os dados  de Peso x Idade; Estatura x Idade e Índice de Massa Corporal (IMC) x Idade  recomendados pela Organização Mundial de Saúde (OMS).					<B>Resultados</B>: Foram avaliadas 13 crianças, todas com idade superior a 12 meses. A média de idade das mães foi de 30,2 anos (23-24), 92,31% tinham mais de 10 anos de escolaridade e 61,54% com renda familiar superior a 3 salários mínimos. O leite materno foi oferecido a 76,92% das crianças e mantido por mais de 6 meses para 46,15% delas. Na avaliação antropométrica, 84,62% das crianças encontravam-se eutróficas (análise do IMC recomendado para a idade).					<B>Conclusões</B>: O baixo índice de excesso de peso nessa população (15,38%) pode indicar que a boa escolaridade e condição socioeconômica da mãe (e família) seja um fator de proteção para obesidade e outras complicações relacionadas à alimentação da criança.				</P>
			</div><B>Palavras-chave</B>: Comportamento alimentar. transtornos da nutrição do lactente. cuidado do lactente.			<BR>
