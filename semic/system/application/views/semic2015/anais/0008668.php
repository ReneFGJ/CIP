			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Complementary feeding is very important in child development in the early stages of your life and can result in short and long-term repercussions. It is known that proper eating habits in the early years can prevent malnutrition, micronutrient deficiency and also obesity.					<B>Objectives</B>: Analyze the use of complementary feeding in different groups of the population of Curitiba, PR, seeking to identify periods of exclusive and complemented breastfeeding; the beginning and type of complementary feeding and anthropometric assessment.					<B>Methods</B>: A cross-sectional observational study was conducted by evaluating a questionnaire and anthropometric data (weight and height) of 13 children between 0-2 years of age attending private schools of Curitiba, PR. The findings were compared with the data recommended by the World Health Organization (WHO): Weight x Age; Height x Age and Body Mass Index (BMI) x Age.					<B>Results</B>: 13 children were evaluated, all over the age of 12 months. The average age of the mothers was 30.2 years (23-24), 92.31% were over 10 years of schooling and 61.54% with household income greater than 3 times the minimum wage. Breast milk was given to 76.92% of children and maintained for 6 months or longer to 46.15% of them. In anthropometric assessment, 84.62% of children were eutrophic (analysing the BMI recommended for the age).					<B>Conclusion</B>: Low Overweight rate in this population (15.38%) may indicate that good education and socioeconomic status of the mother (and family) is a protective factor for obesity and other complications related to children´s nutrition.				</P>
