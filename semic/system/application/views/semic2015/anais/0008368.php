			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O centro histórico e bairros mais antigos da cidade de Curitiba apresentam diversos exemplares de arquitetura desenvolvida em diferentes períodos e, portanto, com diferentes linguagens. Ainda assim, o Art Déco, visto como manifestação arquitetônica limiar entre ecletismo historicista e o modernismo, se destaca na paisagem da cidade					<B>Objetivo</B>: O presente trabalho visa levantar exemplares do movimento Art Déco em Curitiba em diversas tipologias de edificações a fim de contribuir com o acervo histórico cultural das ruas e da cidade e buscar, através da qualificação e relevância de tais obras, a possibilidade de preservação das mesmas.					<B>Metodologia</B>: A primeira etapa da pesquisa consistiu em coleta de informações e leituras bibliográficas para estudo e caracterização inicial das obras. O segundo passo foi percorrer a cidade em busca das obras de cunho Déco espalhadas na cidade. A caracterização das obras marcou a terceira etapa da pesquisa e se deu através da pontuação de elementos da Arquitetura Déco presentes em cada edificação.					<B>Resultados</B>: Foi criado um banco de dados que apresenta fotos e detalhes característicos Déco, localização e uso atual do imóvel, preservação, indicação fiscal, entre outros. Uma tabela com 13 itens que abrange traços externos nas edificações caracteriza uma a uma com os componentes presentes a fim de ter um resumo breve das impressões Déco em cada obra.					<B>Conclusões</B>: A preservação das obras depende da constante manutenção e cuidado, além de incentivos públicos ao melhor aproveitamento de comércio e serviços nas mesmas uma vez que sua localização e eventualmente sua dimensão, podem ser atraentes às incorporadoras imobiliárias e empresas privadas.				</P>
			</div><B>Palavras-chave</B>: Art Dèco. Arquitetura. Preservação. Patrimônio. Curitiba.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The historic center and the oldest neighborhoods of Curitiba city have several copies architecture developed in different periods and therefore with different languages. Still, the Art Deco architectural threshold seen as manifestation of historicist eclecticism and modernism stands out in the landscape of the city.					<B>Objectives</B>: This paper aims to raise exemplary Art Deco movement in Curitiba in several buildings typologies in order to contribute to the cultural record of the streets and the city and seek, through training and relevance of such works, the possibility of preserving them.					<B>Methods</B>: The first stage of the research consisted of collecting information and bibliographic readings for study and initial characterization of the works. The second step was to travel around the city in search of nature works Deco scattered in the city. The characterization of the works marked the third stage of research and took over the scoring elements Deco architecture present in each building.					<B>Results</B>: It was created a database that features photos and details characteristic Deco, current location and use of the property, preservation, tax statement, among others. A table with 13 items covering external traits in buildings features one by one with the components present in order to have a brief summary of Deco prints in each work.					<B>Conclusion</B>: The preservation of the works depends on constant maintenance and care, and public incentives for better use of trade and services under the same since their location and eventually their size, may be attractive to real estate developers and private companies.				</P>
			</div><B>Keywords</B>: Art Dèco. Architecture. Preservation. Heritage. Curitiba.</td>
