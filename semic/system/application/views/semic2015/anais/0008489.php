			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Em 1943, o Venerável Papa Pio XII publicou a encíclica 
Mystici Corporis, abordando a visão de Igreja como o corpo místico de 
Cristo. Já há alguns anos, o debate no campo da eclesiologia se 
desenvolvia entre os teólogos de modo intenso. Posteriormente, o 
Concílio Vaticano II tratou dessa questão na constituição dogmática 
Lumen Gentium. Vinte anos depois do encerramento do Concílio, em 
1985, teve lugar em Roma uma Assembleia Especial do Sínodo dos 
Bispos, com o objetivo de refletir sobre os frutos da Assembleia Conciliar. 
Nessa ocasião, tomou-se por diretriz uma leitura eclesiológica da Igreja 
enquanto comunhão. Vê-se, pois, que a eclesiologia foi o objeto de uma 
rica reflexão no século passado. Certamente, o Brasil não ficou de fora 
desse debate. A contribuição dos teólogos brasileiros no campo da 
eclesiologia nesse período nunca foi, porém, rastreada e estudada em 
seu conjunto. Foi para suprir essa lacuna que foi desenvolvido este 
projeto. Para tanto, foram mapeados os artigos dessa área publicados 
nos principais periódicos brasileiros no campo da teologia. O presente 
relatório mostrará os resultados obtidos para o período de 1940-1955. 
Objetivos: O presente projeto teve como objetivo mapear a produção 
eclesiológica no Brasil entre 1940 e 1955, detectando os temas e ênfases 
característicos, bem como propondo elementos em vista da periodização 
histórica da eclesiologia.					<B>Objetivo</B>: O presente projeto teve como objetivo mapear a produção 
eclesiológica no Brasil entre 1940 e 1955, detectando os temas e ênfases 
característicos, bem como propondo elementos em vista da periodização 
histórica da eclesiologia.					<B>Metodologia</B>: Esta é uma pesquisa quantitativa do 
tipo “descritiva”, na qual foi realizada um registro e análise de produção 
realizada em um período e tempo especifico. Para tanto o itinerário 
percorrido iniciou-se com o conhecimento dos periódicos editados no 
Brasil e o seu levantamento entre 1940 e 1955; seguido da identificação e 
caraterização das publicações no campo da eclesiologia presentes nos 
periódicos, formando, por fim, o banco de dados necessário para a 
análise que será apresentada em seguida. Para o período de pesquisa 
proposto foi mapeada a Revista Eclesiástica Brasileira (REB), única 
revista teológica existente naquela década; fundada em 1940.					<B>Resultados</B>: Dos vinte artigos relacionados à eclesiologia encontrados 
entre o período de 1940 a 1955 na REB, nove citam em seus títulos ou 
subtítulos a encíclica Mystici Corporis. Os demais artigos relacionam a 
eclesiologia à arquitetura, ao primado romano, à formação do clero, ao 
papel do leigo e ao ecumenismo. Conclusão: A presente pesquisa 
possibilita o início do desenvolvimento da história da eclesiologia 
brasileira, pois mapear os dados existentes é o primeiro patamar para 
pesquisas posteriores. Sendo assim, conclui-se que a eclesiologia no 
Brasil entre 1940 e 1955 apresentou discussões e reações à encíclica 
Mystici Corporis.					<B>Conclusões</B>: 				</P>
