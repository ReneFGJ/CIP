			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: In 1943, the Venerable Pope Pius XII published the encyclical Mystici Corporis, about the Church as the mystical body of Christ. For several years before, the debate in the field of ecclesiology has been developed among theologians intensely. Subsequently, the Second Vatican Council addressed this topic in the Dogmatic Constitution Lumen Gentium. Twenty years after the Council, in 1985,a Special Assembly of the Synod of Bishops  took place in Rome, aiming to reflect on the fruits of the Conciliar Assembly. On that occasion, an ecclesiological reading of the Church as communion was taken by guideline. One sees, then, that the ecclesiology was the subject of a rich reflection in the last century. Indeed, Brazil was not out of this debate. The contribution of Brazilian theologians in the field of ecclesiology in this period never was, however, tracked and studied in its entirety. It was to fill this gap that this project was developed. Therefore, the articles published on this area in the main Brazilian publications in the field of theology were mapped. This report will show its results for the period from 1940 to 1955.					<B>Objectives</B>: This project aimed to map the ecclesiological production in Brazil between 1940 and 1955 by detecting the characteristic themes and emphasis and proposing elements in view of the historical periodization of ecclesiology.					<B>Methods</B>: This is a "descriptive" quantitative research, which was conducted a record and analysis of the production undertaken in a specific period time. For this purpose the itinerary covered began with acknowledge of the magazines published in Brazil between 1940 and 1955; followed by the identification and characterization of the publications in the field of ecclesiology present in the magazines, forming finally the database required for the analysis that is given below. For the proposed period it was used the "Revista Eclesiástica Brasileira" - Brazilian Ecclesiastical Magazine (REB), the only existing theological magazine in that decade, founded in 1940.					<B>Results</B>: Among the twenty articles related to ecclesiology found between the period from 1940 to 1955 at REB nine mention in their titles or subtitles the encyclical Mystici Corporis. The other articles relate ecclesiology to architecture, the Roman primacy, the formation of clergy, the role of the laity and ecumenism. Conclusion: This research allows the begin of development of the history of Brazilian ecclesiology, because to map the existing data is the first level for further research. Therefore, it appears that the ecclesiology in Brazil between 1940 and 1955 had discussions and reactions to the encyclical Mystici Corporis.					<B>Conclusion</B>: 				</P>
