			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Cryptococcosis is an urban disease that affects both humans and animals, and is caused by encapsulated yeast, the Cryptococcus neoformans, which can be found in the feces of birds, but also in other environments contaminated with feces as wood, leaf and land. The disease is more common in people and animals immuno-compromised. The main form of manifestation of pathogenesis is fungal meningitis, may also affect the respiratory system and other organs.					<B>Objectives</B>: This study aimed to investigate the occurrence of the etiologic agent in farms located near the city of Toledo, Paraná, in order to get information on possible contamination of niches for people and animals.					<B>Methods</B>: 42 properties were visited in the Toledo, PR, and was collected bird excreta samples at different locations during the months of march until april/2015. The collected material was analyzed in the HV-PUCPR Microbiology Lab, Sabouraud Dextrose Agar through cultivation estuga at 37 °C for 10 days, and read by optical microscopy.					<B>Results</B>: There was no fungal growth on the samples tested, 100% of the tests were negative for the presence of Cryptococcus sp.					<B>Conclusion</B>: The result of the analysis indicates that the area investigated is free of the occurrence of Cryptococcus neoformans, and consequently of cryptococcosis in humans and animals.				</P>
			</div><B>Keywords</B>: Respiratory disease. epidemiology. fungus. cryptococcosis. Toledo.</td>
