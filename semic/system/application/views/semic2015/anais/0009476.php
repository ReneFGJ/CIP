			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A criptococose é uma doença urbana que acomete tanto seres humanos como os animais, e é causada por uma levedura capsulada, o Cryptococcus neoformans, que pode ser encontrado em fezes de aves, mas também em outros ambientes contaminados com excretas como madeira, folha e solo. A doença é mais comum em pessoas e animais imunologicamente comprometidos. A principal forma de manifestação da patogenia é a meningite fúngica, podendo acometer também o sistema respiratório e outros órgãos.					<B>Objetivo</B>: Este trabalho teve como objetivo investigar a ocorrência do agente etiológico em propriedades rurais situadas próximas à cidade de Toledo, Paraná, a fim de obter informações sobre possíveis nichos de contaminação para pessoas e animais.					<B>Metodologia</B>: Foram visitadas 42 propriedades na região de Toledo, PR e coletadas amostras de excretas de aves em diferentes locais durante os meses de março e abril de 2015. O material coletado foi analisado no Laboratório de Microbiologia do HV-PUCPR, através de cultivo Ágar Sabouraud Dextrose em estuga a 37ºC durante 10 dias e leitura por microscopia óptica.					<B>Resultados</B>: Não houve crescimento fúngico em nenhuma das amostras avaliadas, isto é, 100% das análises foram negativas para a presença do Cryptococcus sp.					<B>Conclusões</B>: O resultado das análises indica que a região ru-urbana investigada encontra-se livre da ocorrência de Cryptococcus neoformans, e consequentemente da criptococcose em humanos e em animais.				</P>
			</div><B>Palavras-chave</B>: Doença respiratória. epidemiologia. fungo. criptococcose. Toledo			<BR>
