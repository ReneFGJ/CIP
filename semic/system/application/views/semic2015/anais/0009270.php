			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A correta aplicação de um termo, palavra ou expressão é fundamental na compreensão de uma ideia ou conceito, principalmente as palavras e expressões polissêmicas. Essa é a principal razão para o desenvolvimento de um glossário em EA – Enterprise Architecture, um tema relativamente novo no meio acadêmico, que pode ser definido como a arquitetura coerente e global de uma empresa, que compreende tanto de TI – Tecnologia da Informação e elementos de negócios (Winter, 2010).					<B>Objetivo</B>: O objetivo deste estudo é sintetizar os conceitos existentes e derivar novos conceitos em EA para a aplicação e a difusão do conhecimento adquirido em uma base de dados, a qual está disponível para consulta pelo EAWT - PUCPR Enterprise Architecture Working Team da Pontifícia Universidade Católica do Paraná PUCPR.					<B>Metodologia</B>: O presente estudo é exploratório realizado usando da análise qualitativa de 16 artigos em EA, os quais foram selecionados pelo EAWT - PUCPR.					<B>Resultados</B>: Foram analisados 16 artigos e destes foram derivados 236 termos e expressões, que estão disponíveis para consulta no site www.eawt.com.br acessando o item artigos. Os temas e conteúdo dos artigos analisados são variados, tais como: definição de conceitos básicos; funções e métodos; ontologias; benefícios e valor corporativo; implementação; modelos empresariais de operação; análise de pesquisas acadêmicas anteriores; stakeholders e relações da EA.					<B>Conclusões</B>: O projeto cumpriu os objetivos propostos e possui a possibilidade de exercer continuidade no tema. Pode-se concluir com as análises dos artigos que há uma falha de pesquisas base em EA e ausência de uma definição para o tema, fatores que podem estar dificultando o desenvolvimento do tema no Brasil.				</P>
			</div><B>Palavras-chave</B>: Glossário; Enterprise Architecture; Processos de negócios; Tecnologia da Informação.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The correct application of a term, word or expression is fundamental in understanding an idea or concept, especially the words and polysemous expressions. It is the main reason for the development of a glossary in EA - Enterprise Architecture, a relatively new topic in academics, which can be defined as the coherent and comprehensive architecture of a company that understands both IT - Information Technology and business elements (Winter, 2010).					<B>Objectives</B>: The aim of this study is to synthesize the concepts and derive new concepts in EA for the application and the dissemination of knowledge acquired in a database, which is available for consultation by the EAWT - PUCPR Enterprise Architecture Working Team from PUCPR – Pontifical University Catholic of Paraná.					<B>Methods</B>: This exploratory study was carried out through a qualitative analysis of 16 articles in EA, which were selected by the EAWT - PUCPR.					<B>Results</B>: There were 16 articles analyzed and 236 terms and expressions were derived, and presented in the site www.eawt.com.br accessing the item “artigos” . The themes and content of the articles vary, which include: definition of basic concepts; functions and methods; ontologies; benefits and business value; Implementation; operation business models; previous academic research analysis; EA stakeholders and relationships.					<B>Conclusion</B>: The project has achieved the proposed objectives and has the possibility to exercise continuity in theme. Finally, through the analysis of the articles, it was concluded that there is a lack of basic research in EA and absence of a clear definition for the theme, factors that may be hindering the development of the subject in Brazil.				</P>
			</div><B>Keywords</B>: Glossary, Enterprise Architecture; Business Processes; Information Technology.</td>
