			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: As much debated in schools in the classroom, lectures or educational videos bullying often continues to be practiced and can leave marks, both physical and emotional on the victims. Thus, it is of utmost importance to analyze and identify bullying actions that occur in schools, can from this analysis structuring actions that assist in reducing violence among peers.					<B>Objectives</B>: This research sought to identify the places where they occur actions and bullying behaviors.					<B>Methods</B>: The transversal study and analysis of all data collected in the questionnaire were performed using SPSS version system 17. The instrument was a questionnaire adapted from Barros (2012) being conducted by the evaluator, applied in physical education class from 6 of a school state in Curitiba. 57 students participated in this research with 21 boys (36.84%) and 36 girls (63.15%).					<B>Results</B>: The results indicated the playground as the place where most bullying occurs (24.6%), the name-calling and profanity are assaults commonly performed by assessed (36.84%), students confess having done evil once or more once against third parties (40.4%), and which can be regarded as a "justification" when these action is irritate each other (49.1%).					<B>Conclusion</B>: We conclude that the students answered the evaluation form contained afraid that the answers were brought to the school board and mainly to lack a complete understanding of what bullying is because students fail to discern "zoações" and "play" attitudes which are detrimental to the development of these actions of the victim				</P>
