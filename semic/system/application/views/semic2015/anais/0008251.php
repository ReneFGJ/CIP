			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Por mais debatido nas escolas em sala de aula, palestras ou vídeos educacionais o bullying muitas vezes continua a ser praticado, podendo deixar marcas, tanto físicas quanto emocionais nas vítimas. Desta forma, é de extrema importância analisar e identificar as ações de bullying que ocorrem nas escolas, podendo a partir desta análise estruturar ações que auxiliam na diminuição da violência entre pares.					<B>Objetivo</B>: Esta pesquisa buscou identificar os locais onde ocorrem ações e os comportamentos de bullying.					<B>Metodologia</B>: O estudo de caráter transversal e todas as análises dos dados coletados no questionário foram realizados no sistema SPSS versão 17. O instrumento utilizado foi um questionário adaptado de Barros (2012) sendo realizado pelo avaliador, aplicado na aula de educação física do 6º de uma escola Estadual em Curitiba. Participaram desta pesquisa 57 alunos sendo 21 meninos (36,84%) e 36 meninas (63,15%).					<B>Resultados</B>: Os resultados indicaram o recreio como o lugar onde mais ocorre o bullying (24,6%), os xingamentos e os palavrões são as agressões mais executadas pelos avaliados (36,84%), os alunos confessam ter feito maldades uma vez ou mais de uma vez contra terceiros (40,4%), e o que pode ser considerado como uma “justificativa” a estas ação é quando irritam uns aos outros (49,1%).					<B>Conclusões</B>: Conclui-se que os alunos responderam a avaliação de forma contida com medo que as respostas fossem levadas à direção da escola e principalmente à falta um entendimento completo sobre o que é bullying devido aos alunos não conseguirem discernir “zoações” e “brincadeiras” de atitudes que são prejudiciais para o desenvolvimento da vítima destas ações.				</P>
			</div><B>Palavras-chave</B>: Bullying. Escola. Curitiba.			<BR>
