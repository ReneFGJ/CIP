					<B>Introduction</B>: Besides all the difficulties in relation to urbanism, environment, politic, economic, there is the quality of life to citizens. In this context, the discussion about public					<B>Objectives</B>: Identify concepts of public intelligence focusing on strategic digital city.					<B>Methods</B>: The research methodology consists on gathering bibliographic data, analyzing and registering material about these topics and identifying concepts of public intelligence into a context of strategic digital city.					<B>Results</B>: By linking the concepts and simulating an application of them, it is possible to realize that “Public Intelligence” and “Strategic Digital City” are very close to each other. It is possible to affirm that as the cities managers use information and communication technology in order to improve the public service, the Public Intelligence is inserted in the context of Digital City. However, the fact that the Digital City has characteristic instruments, it does not mean that there is the process of knowledge and interaction between social actors and politicians presents in a specific context.					<B>Conclusion</B>: In other words, the city manager has to fulfill his or her commitments in an ethical way, discussing the citizens’ life quality of his or her city and explaining the process of his or her decisions in order to help these citizens to constrict a better public service. After all, only in this way is possible to build an effective contextualization between Public Intelligence and Strategic Digital City.				</P>
