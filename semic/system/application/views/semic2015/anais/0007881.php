			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Além dos desafios, dificuldades e dilemas enfrentados pelos munícipios, tanto com relação às questões urbanas, rurais, ambientais, sociais, físico-territoriais e legais, quanto por razões políticas, financeiras e econômicas, há, ainda, a preocupação com a garantia de qualidade de vida diferenciada aos cidadãos. Nesse contexto, a discussão sobre a inteligência pública aplicada em municípios, prefeituras, projetos de cidade digital e modelos de cidade digital estratégica tem total valia.					<B>Objetivo</B>: Identificar conceitos de inteligência pública no contexto da cidade digital estratégica.					<B>Metodologia</B>: A metodologia da pesquisa adotada para a pesquisa consistiu essencialmente na coleta de dados bibliográficos, análise e registro de material, identificação conceitos de inteligência pública dentro de um contexto de cidade digital estratégica.					<B>Resultados</B>: Por meio da aproximação conceitual e “simulação” de aplicação, percebe-se a afinidade entre os temas “Inteligência Pública” e “Cidade Digital Estratégica”. A utilização das tecnologias da informação e da comunicação por parte de gestores, melhorando serviços públicos e beneficiando a sociedade, insere a Inteligência Pública no contexto da Cidade Digital. Entretanto, o simples fato de haver instrumentos característicos da Cidade Digital, não significa de fato a existência de processos de conhecimento e interatividade entre os vários atores sociais e políticos presentes em determinado contexto.					<B>Conclusões</B>: Como conclusão, para que seja efetiva a contextualização da Inteligência Pública à Cidade Digital, além de o “gestor” ter de cumprir com seus compromissos de forma ética, discutindo os cenários da vida local e expondo as etapas de seus processos decisórios, o cidadão precisa colaborar ativamente para a efetivação de sua função diante da sociedade, buscando e utilizando todos os mecanismos de participação em seu contexto social, a fim de contribuir para a melhoria dos serviços públicos, o que trará incessantes desafios às cidades.				</P>
			</div><B>Palavras-chave</B>: Cidade Digital. Cidade Digital Estratégica. Inteligência. Inteligência Pública. Inteligência Organizacional.			<BR>
