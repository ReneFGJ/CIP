			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Este estudo visa analisar o escoamento de ar via Dinâmica dos Fluidos Computacional (DFC) no interior de instalações avícolas industriais. Sabe-se que a carne de frango é a segunda carne mais consumida no mundo (Agricultural Outlook 2015-2024) e que o Brasil é o maior exportador mundial e o terceiro maior produtor (Relatório Anual da UBABEF, 2014). Assim, desenvolver pesquisas no campo da avicultura é essencial para o Brasil tornar-se uma referência. Neste sentido, este trabalho visa aplicar um modelo para predizer o escoamento turbulento do ar no interior de aviários industriais.					<B>Objetivo</B>: O objetivo principal do projeto é estudar o comportamento do escoamento transiente no interior de aviários, utilizando o pacote computacional Ansys CFX versão 14.5, que tem como fundamento o método dos volumes finitos (Patankar, 1980), para discretizar o domínio computacional.					<B>Metodologia</B>: Aplicar a DFC usando um código comercial denominado Ansys® CFX versão 14.5 do seguinte modo: (i) construir malhas no ICEM CFD, (ii) exportá-las para o CFX-Pre no qual as condições de contorno são introduzidas, (iii) fazer a simulação do problema no CFX-Solver até atingir um critério de convergência e (iv) analisar os resultados fornecidos pelo CFX-Post.					<B>Resultados</B>: Foram traçadas linhas de função corrente, campo vetorial de velocidade e perfis de velocidade.					<B>Conclusões</B>: Foram feitas análises usando a velocidade de entrada transiente, variando com o tempo, de forma a simular os exaustores ligados ou desligados. Notou-se que deve-se ter cuidado ao simular o ligamento ou desligamento dos exaustores porque velocidades muito baixas podem trazer avarias para os frangos, porém pode ser usado tal artifício com moderação obedecendo cada faixa etária do frango que exige valores pré-determinados de velocidade do ar.				</P>
