			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: This study aims to analyze the airflow via Computational Fluid Dynamics (CFD) within industrial poultry facilities. It is known that chicken meat is the second most consumed meat in the world (Agricultural Outlook 2015-2024) and that Brazil is the largest exporter and the third largest producer (Annual Report of UBABEF, 2014). Thus, developing research in the field of poultry is essential for Brazil to become a reference. Thus, this work aims at applying a model to predict the turbulent flow of air inside industrial poultry.					<B>Objectives</B>: The main objective of the project is to study the behavior of the transient flow inside poultry houses, using the computational package Ansys CFX version 14.5, which is founded on the finite volume method (Patankar, 1980), to discretize the computational domain.					<B>Methods</B>: Apply the DFC using a commercial code called Ansys® CFX version 14.5 as follows: (i) build meshes in ICEM CFD, (ii) export them to the CFX-Pre in which the boundary conditions are introduced, (iii) make the simulation of the problem in CFX-Solver until a convergence criterion and (iv) analyze the results provided by the CFX-Post.					<B>Results</B>: Streamlines field and vector velocity profiles were drawn.					<B>Conclusion</B>: Analyzes were performed using the transient input speed varying with time, to simulate the exhaust fans on or off. It was noted that one should be careful to simulate the ligament or shutdown of hoods for very low speeds can bring damage to the chickens, but can be used such a device in moderation obeying each age group of the chicken that requires pre-determined speed values air.				</P>
