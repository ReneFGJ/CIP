			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Atualmente as empresas vêm buscando meios e instrumentos que as habilitem gerenciar seus processos de maneira sustentável. Sendo assim, faz-se indispensável a atuação de uma gestão de operações eficaz, que seja capaz de delinear o processo e, a partir de seus indicadores de desempenho, tomar decisões de maneira acertada e com pronta solução dos problemas.					<B>Objetivo</B>: O presente projeto de pesquisa tem por objetivo reconstruir o modelo do sistema de produção na forma de sua estratégia de operações a partir de informações de seu desempenho.					<B>Metodologia</B>: Por meio de revisão sistemática da literatura, compilação e pesquisa de conceitos para fundamentação teórica e análise univariada de dados estatísticos das operações de serviço de um programa de pós-graduação de uma universidade, analisou-se como os indicadores afetavam o conceito final do sistema de avaliação da Coordenação de Aperfeiçoamento Pessoal de Nível Superior (CAPES).					<B>Resultados</B>: A partir da análise, foi possível determinar quais e como cada indicador atuava de modo a obter o conceito final da CAPES, considerando a estrutura de contaúdo da estratégia de operações como framework de análise.					<B>Conclusões</B>: Portanto, um modelo de sistema produtivo, seja ele aplicado a serviços ou manufatura, pode prover dados interessantes e que, se bem analisados, proporcionam agilidade e assertividade na tomada de decisões.				</P>
			</div><B>Palavras-chave</B>: Estratégia de operações em serviços. Ensino superior. Análise univariada.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Nowadays, comapnies are seeking ways and means for managing their processes in a sustainable way. Therefore, it is essential for an effective operations management system, to be able to outline its processes and, from its performance indicators, making assertive and agile decisions for responsive problem solving.					<B>Objectives</B>: This research project aims to reconstruct the model of production system, based on operations strategy content analysis, from its performance information.					<B>Methods</B>: Through systematic literature review, theoretical concepts compilation and synthesis and univariate analysis of statistical data from a service operations of an university graduate program, an analysis of indicators affecting the final grade given by the assessment exercise of CAPES higher education funding agency was promoted.					<B>Results</B>: From the analysis, it was possible to determine which and how each indicator was acting in order to obtain the final concept of CAPES, based on an operations strategy content.					<B>Conclusion</B>: Therefore, a model of productive system, whether applied to services or manufacturing could provide interesting data and that if well analyzed, provide agility and assertiveness in decision-making.				</P>
			</div><B>Keywords</B>: Service operations strategy. Graduate higher education. Univariate analysis</td>
