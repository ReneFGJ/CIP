			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Pesquisa da Organização Mundial da Saúde mostra que o álcool pode ser considerado a substância mais consumida no Brasil e no mundo. Em se tratando dos jovens, este consumo apresenta uma incidência ainda maior constituindo em grande preocupação para a saúde pública. O consumo excessivo de álcool está aumentando cada vez mais e se tornando um problema significativo principalmente por envolver jovens e adolescentes cada vez mais cedo.					<B>Objetivo</B>: O estudo teve objetivos: Conhecer a frequência de consumo de álcool entre um grupo de jovens universitários, Traçar o perfil sócio demográfico do pesquisado e conhecer os motivos que levam o jovem a beber.					<B>Metodologia</B>: Para a coleta de dados foi utilizado um questionário estruturado aplicado para um grupo de jovens universitários de cursos de graduação da escola de saúde e biociências de uma Universidade privada do Paraná. Os dados foram analisados utilizando a freqüência simples.					<B>Resultados</B>: O estudo mostrou que  a maioria dos jovens iniciou o consumo de álcool cedo, a bebida de escolha foi os destilados e a cerveja, grande parte bebe mensalmente e por motivos de diversão.					<B>Conclusões</B>: conclui-se quede fato a maioria dos jovens participantes consomem com uma certa freqüência bebida alcoólica, principalmente para se divertir e encontrar amigos. Embora os dados mostrem uma freqüência não tão alta no consumo, é preciso que ações sejam desenvolvidas por profissionais de saúde, particularmente o enfermeiro, para uma sensibilização desses jovens para o uso responsável e seguro do álcool.				</P>
