				<P>
					<B>Introduction</B>: World Health Organization research shows that alcohol can be considered the most consumed substance in Brazil and worldwide. In the case of young people, this consumer has a greater incidence constituting major concern for public health. Excessive alcohol consumption is increasing more and more and becoming a significant problem mainly involve young people and adolescents at an earlier age.					<B>Objectives</B>: The study objective: To know the frequency of alcohol use among a group of university students, Tracing the demographic profile of the researched partner and know the reasons that lead young people to drink.					<B>Methods</B>: To collect data we used a structured questionnaire administered to a group of university students of health school graduate and life sciences private University of Paraná. Data were analyzedusingthesimples					<B>Results</B>: The study showed that most young people started early consumption of alcohol, the drink of choice was the spirits and beer, much monthly drinks and fun reasons.					<B>Conclusion</B>: : it was concluded that in fact most of the young participants to consume a certain frequency alcoholic beverage, mainly to have fun and meet friends. Although the data show a rate not as high in consumption, it is necessary that actions be developed by health professionals, particularly nurses, to raise awareness of these young people for responsible and safe use of alcohol.				</P>
