			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Dissolution test has become an important tool for characterizing the performance of drugs . Also, when a correlation in vitro/in vivo is possible, dissolution can be used as a test to reflect the bioavailability of the product in humans and thus determine the actual bioequivalence of different drugs . Propafenone hydrochloride , a regulatory drug heartbeat, does not have in any pharmacopoeia a dissolution methodology by ultraviolet spectrophotometry.					<B>Objectives</B>: The aim of this work was to develop a dissolution methodology by ultraviolet spectrophotometry to propafenone hydrochloride coated tablets.					<B>Methods</B>: SStandard solutions were subjected to a scanning spectrophotometer spectrum between 200-500 nm in order to determining the wavelength where absorption of the drug occurs . The tablets were analyzed in the following dissolution media: 0.1 N HCl ; citrate buffer pH 4.5 and phosphate buffer pH 6.8 to determine the medium with better solubility . Also, were evaluated the apparatus and speeds: Apparatus I (Baskets) in 50 and 100 rpm and Apparatus II (paddles) also in 50 and 100 rpm.					<B>Results</B>: Propafenone hydrochloride showed UV absorption at a wavelength of 250 nm . The tests demonstrated that 0.1 N HCl using the paddle apparatus rotating speed of 100 rpm showed a more discriminating dissolution profile .					<B>Conclusion</B>: The results show that the conditions for the dissolution test for propafenne hydrochloride coated tablets are 0.1 N HCl an dissolution medium using Apparatus II (paddles ) at 100 rpm and analysis by UV in wavelength of 250 nm.				</P>
