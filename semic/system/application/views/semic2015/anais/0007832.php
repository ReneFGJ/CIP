			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Ensaios de dissolução tem se tornado uma importante ferramenta para caracterizar a performance de medicamentos. Ainda, quando é possível uma correlação in vitro/in vivo, dissolução pode ser usada como um teste para refletir a biodisponibilidade de um produto em humanos e, dessa forma, determinar a real bioequivalência de diferentes medicamentos. O cloridrato de propafenona, um fármaco regulador dos batimentos cardíacos, não possui em nenhuma farmacopéia uma metodologia de dissolução por doseamento em ultravioleta.					<B>Objetivo</B>: O presente trabalho teve como objetivo desenvolver metodologia analítica para ensaio de dissolução para comprimidos revestidos de cloridrato de propafenona usando método espectrofotométrico.					<B>Metodologia</B>: Soluções padrões foram submetidas à um espectro de varredura no espectrofotômetro entre 200 – 500 nm para determinação do comprimento de onda onde ocorre absorção do fármaco. Os comprimidos foram analisados nos meios de dissolução HCl 0,1 N; tampão citrato pH 4,5 e tampão fosfato pH 6,8 a fim de determinar o meio com melhor solubilidade. Os aparatos e velocidades utilizados foram I (Cestas): 50 e 100 rpm e Aparato II (Pás): 50 e 100 rpm.					<B>Resultados</B>: O cloridrato de propafenona apresentou absorção em ultravioleta no comprimento de onda de 250 nm. A avaliação dos meios de dissolução demostrou que houve a geração de um perfil de dissolução mais discriminativo em HCl 0,1 N utilizando o aparato pás na velocidade de giro de 100 rpm.					<B>Conclusões</B>: Os resultados obtidos demonstram que as condições para o ensaio de dissolução de comprimidos revestidos de cloridrato de propafenona são o meio HCl 0,1 N com aparto II (pás) à 100 rpm e leitura em ultravioleta no comprimento de onda de 250 nm.				</P>
			</div><B>Palavras-chave</B>: Perfil de dissolução. espectrofotometria. comprimidos			<BR>
