			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The equipment called "AQUACLOCK" was designed by academics guided by Professor Fernanda Ostrovki Sales for the project "Mission Health II" of the Pontifical Catholic University of Parana. Aquaclock is a device that allows assist the elderly in their routine hydration and administration of various medications, aiming to improve their quality of life.					<B>Objectives</B>: Prototype development (software), easy to use, to the configuration of AQUACLOCK, smart individual cooler, with alarms and indicators to aid the administration of drugs.					<B>Methods</B>: Research enabled the choice of a development system which produces an intuitive software to the user, compatible with the most widely used operating systems.					<B>Results</B>: A software for device configuration "AQUACLOCK" with a set of four screens was developed: A screen with the main menu, a screen on with the development team information, a settings screen and a screen with the configuration options of the compartments alarm.					<B>Conclusion</B>: A simple and intuitive software has been developed, designed for use by anyone with the least difficulty possible. The program has compatibility with most current operating systems, as researched in the early design, and makes the device configuration "AQUACLOCK" quickly and effectively. As a possible use of this equipment, the program may be made available in pharmacies, the attendant quickly make the dispenser programming for each specific need of the user.				</P>
