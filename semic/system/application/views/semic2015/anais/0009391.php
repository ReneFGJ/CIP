			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O equipamento chamado “AQUACLOCK” foi idealizado por acadêmicos orientados pela professora Fernanda Ostrovki Sales para o projeto “Missão Saúde II” da pontifícia universidade católica do Paraná. Aquaclock é um equipamento que permite auxiliar o idoso na sua rotina de hidratação e administração de medicamentos diversos, visando o aperfeiçoamento de sua qualidade de vida.					<B>Objetivo</B>: Desenvolvimento de software, de fácil utilização, para a configuração do AQUACLOCK, cooler individual, inteligente, com alarmes e indicadores para auxílio a administração de medicamentos.					<B>Metodologia</B>: Pesquisas permitiram a escolha de um sistema de desenvolvimento que produzisse um software intuitivo ao usuário, compatível com os sistemas operacionais mais utilizados.					<B>Resultados</B>: Foi desenvolvido um software para configuração do dispositivo “AQUACLOCK" com um conjunto de quatro telas. Uma tela com o menu principal, uma tela de sobre com as informações da equipe de desenvolvimento, uma tela de configurações e uma tela com as opções de configuração do alarme de cada um dos compartimentos.					<B>Conclusões</B>: Foi desenvolvido um software simples e intuitivo, idealizado para ser utilizado facilmente por qualquer pessoa. O programa possui compatibilidade com a maior parte dos sistemas operacionais atuais e permite a configuração do dispositivo “AQUACLOCK" de maneira rápida e eficaz. Como possível utilização deste equipamento, o programa poderá ser usado em farmácias, para que o próprio atendente faça a programação do equipamento de acordo com a necessidade específica de medicamentos do usuário.				</P>
			</div><B>Palavras-chave</B>: Protótipo. Software. Saúde do idoso.			<BR>
