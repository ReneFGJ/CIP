			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A reabsorção óssea é um processo com muitas etapas no qual a osteoprotegerina (OPG) desempenha um importante papel.					<B>Objetivo</B>: O propósito deste estudo foi verificar a hipótese que a expressão de osteoprotegerina (OPG), é significante elevada em pacientes com periodontite apical sintomática					<B>Metodologia</B>: Vinte pacientes com lesões periapicais foram divididos em dois gupos: grupo sintomático (SYM) composto por 10 pacientes e o grupo assintomático (ASYM) com 10 pacientes. Todos os dentes dos pacientes tinham sido endodonticamente retratados pelo menos 2 anos antes da cirurgia periapical. As lesões periapicais foram coletadas, processadas e cortadas em secções de 4-[e]#956;m. Pelo menos duas secções foram processadas pelo exame histológico usando hematoxilina e eosina as demais foram usadas para imunohistoquímica. A fração da área de células OPG positivas foi mensurada e o percentual de áreas OPG imunopositivas relativas à area totais do campo microscópio foi calculado. O teste t de Student foi realizado.					<B>Resultados</B>: O percentual imunopositivo de área de OPG não foi significativamente diferente entre os grupos. Os macrófagos são as células que mais expressaram OPG em ambos os grupos.					<B>Conclusões</B>: A maior expressão de OPG em lesões assintomáticas pode sugerir uma tentativa de controle do organismo em evitar a expansão da periodontite apical crônica.				</P>
			</div><B>Palavras-chave</B>: OPG. imunohistoquímica. lesão periapical.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Bone resorption is a multi-step process in which OPG play important role.					<B>Objectives</B>: The purpose of this study was to examine the hypothesis that the expression of osteoprotegerin (OPG) are significantly elevated in patients with symptomatic apical periodontitis.					<B>Methods</B>: Twenty patients with periapical lesions were divided into two groups: a symptomatic (SYM) group of 10 patients and an asymptomatic (ASYM) group of 10 patients. All teeth had been endodontically retreated at least 2 years before periapical surgery. The periapical lesions were collected and serially cut into 4-[e]#956;m-thick sections. At least two sections were processed for histological examination using hematoxylin-eosin staining, and the remaining sections were processed for immunohistochemistry. The area fraction of OPG-positive cells was measured and the percentage of OPG-immunopositive areas relative to the total area of the microscopic field was calculated.  The Student´s t-test were performed.					<B>Results</B>: The percentage of immunopositive area for OPG  was not significantly different between the groups. Macrophage-like cells predominantly expressed OPG as determined   by   immunohistochemistry   analysis					<B>Conclusion</B>: The greatest expression of OPG in asymptomatic lesions may suggest an attempt  to prevent the spread of chronic apical periodontitis.				</P>
			</div><B>Keywords</B>: OPG. immunohistochemistry. periapical lesion.</td>
