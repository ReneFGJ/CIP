			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Este projeto visa analisar a contribuição do PNAIC – Pacto Nacional pela Alfabetização na Idade Certa no desenvolvimento da alfabetização e letramento nas séries iniciais bem como na formação continuada dos professores alfabetizadores.					<B>Objetivo</B>: Compreendendo o poder da escrita, leitura e linguagem na sociedade, a pesquisa teve como foco observar e discutir as diferentes estratégias que os docentes utilizam no desenvolvimento do trabalho com crianças na aquisição da alfabetização e letramento nas instituições públicas de ensino e as propostas do governo para alfabetizar essas crianças até os nove anos de idade.					<B>Metodologia</B>: Nesse sentido, a proposta desta pesquisa foi colocar em discussão se o PNAIC, programa criado pelo Governo Federal é algo inovador para a alfabetização ou se ele é apenas um recurso para o professor mediatizar a sua prática.					<B>Resultados</B>: 					<B>Conclusões</B>: Considerando os resultados e o que foi discutido até agora, entendo que ser alfabetizado é mais do que decodificar e codificar os textos, ou seja, é poder estar inserido em práticas diversas de leitura e escrita e vivenciá-las de forma autônoma e ser letrado é compreender a escrita na sociedade atual que é essencialmente grafocêntrica.				</P>
			</div><B>Palavras-chave</B>: Alfabetização e letramento. PNAIC. Formação continuada.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: This project aims to analyze the contribution of PNAIC - National Pact for Literacy in the Middle One in the development of alphabetization and literacy in the early grades and continuing training of literacy teachers.					<B>Objectives</B>: Understanding the power of reading, writing and language in society, the research focuses observe and discuss the different strategies that teachers use in the development of work with children in the acquisition of alphabetization and literacy in public education and the government´s proposals for alphabetize these children up to the age of nine.					<B>Methods</B>: In this sense , the proposal of this research was put into discussion the PNAIC , a program created by the Federal Government is something innovative for alphabetization or if it is just a resource for the teacher mediate their practice.					<B>Results</B>: 					<B>Conclusion</B>: Considering the results and what has been discussed so far, I believe that being literate is more than decoding and encoding the texts, ie it is able to be inserted in a number of reading and writing practices and experience them autonomously and be literate is understand writing in today´s society which is essentially grafocêntrica.				</P>
			</div><B>Keywords</B>: Alphabetization e literacy. PNAIC. Continuing education.</td>
