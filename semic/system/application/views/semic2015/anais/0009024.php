			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The urban trees is related to the presence of trees in public spaces such as parks, streets, avenues , gardens and squares. For the identification of plant species are used flower characteristics and / or fruit, however, they are not present all year round, making it difficult to identify at certain times .					<B>Objectives</B>: This study aims to make the identification of tree species used in afforestation neighborhood  Santa Maria streets. Develop an identification key using the external morphology of the leaves.					<B>Methods</B>: The study was developed in Jardim Santa Maria in the city of Toledo, was a survey of tree species and subsequent preparation of the key.					<B>Results</B>: They identified 23 species , distributed in 15 botanical families , totaling 865 individuals. The most representative species was the sibipiruna (Poincianella pluviosa ) with 271 individuals , native . The identification key was prepared simply, but with a series of choices between two parallel and mutually exclusive claims , making the choices of the statements containing the morphological characters of the sheet present in the material analyzed, will allow the user to identify the correct name tree species present in the neighborhood Santa Maria.					<B>Conclusion</B>: It is recommended that this study be expanded to afforestation of Toledo city streets , with the elaboration of an identification key with the inclusion of more vegetative characters , helping work that requires identification tree species in periods that the plant does not present flowers and / or fruits.				</P>
