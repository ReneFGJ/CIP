			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A arborização urbana está relacionada com a presença de árvores em espaços públicos como parques, ruas, avenidas, jardins e praças. Para a identificação das espécies vegetais utilizam-se características da flor e/ou fruto, no entanto, elas não estão presentes o ano todo, o que dificulta a identificação em determinadas épocas.					<B>Objetivo</B>: Identificar as espécies arbóreas utilizadas na arborização de ruas do Bairro Santa Maria. Elaborar uma chave de identificação utilizando a morfologia externa das folhas.					<B>Metodologia</B>: O estudo foi desenvolvido no Bairro Jardim Santa Maria, na cidade de Toledo, foi realizado um levantamento das espécies arbóreas e posterior elaboração da chave de identificação.					<B>Resultados</B>: Foram identificadas 23 espécies, distribuídos em 15 famílias botânicas, totalizando 865 indivíduos. A espécie mais representativa foi a sibipiruna (Poincianella pluviosa) com 271 indivíduos, nativa. A chave de identificação foi elaborada de forma simples, mas com uma série de escolhas, entre duas afirmações paralelas e mutuamente excludentes, realizando as escolhas das afirmações que contenha os caracteres morfológicos da folha presentes no material analisado, permitirá o usuário identificar do nome correto da espécie arbórea presente no bairro Santa Maria.					<B>Conclusões</B>: Recomenda-se que este estudo seja expandido para a arborização de ruas da cidade de Toledo, com a elaboração de uma chave de identificação com a inclusão de outros caracteres vegetativos, auxiliando trabalhos que necessite de identificação de espécies arbóreas em períodos que a planta não apresente flores e/ou frutos.				</P>
			</div><B>Palavras-chave</B>: Arbóreas. chave dicotômica. sibipiruna.			<BR>
