			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Dapsone is a sulfonamide which has bacteriostatic against Mycobacterium leprae, inhibiting protein synthesis by competitive antagonism with paraaminobenzoic acid. Given that the determination of the power of antimicrobials is important in quality control of pharmaceutical preparations and that there is no official method for dapsone gel analysis in the pharmaceutical form justifies this work.					<B>Objectives</B>: Develop a microbiological analysis methodology, through the diffusion plate method for the dapsone gel.					<B>Methods</B>: To prepare the formulation the following constituents were used: dapsone, carbopol, propyleneglycol, sodium hydroxide and water. For the development of microbiological assay were performed several tests alternating some parameters: microorganism, inoculum concentration, initial diluent, final diluent, solutions concentration, method, culture media and incubation time.					<B>Results</B>: From the trials with dapsone gel it was possible to establish some parameters for microbiological analysis however the technique was not developed because it was not possible to obtain inhibition zones. There was uniform bacterial growth, but the inhibition zones, which should be formed around disks were not crisp. Therefore, we tested the method using enrofloxacin solution.					<B>Conclusion</B>: For microbiological analysis of dapsone gel, under the conditions tested, it was not possible to develop an optimal method from microorganisms available in PUCPR. However the method using enrofloxacin test showed good results and gave a new project (PIBIC 2015/2016) of scientific research, entitled microbiological assay development for the analysis of enrofloxacin.				</P>
