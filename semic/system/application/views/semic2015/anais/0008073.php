			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A dapsona é uma substância da classe das sulfonamidas que apresenta ação bacteriostática contra o Mycobacterium leprae, inibindo a síntese proteica por antagonismo competitivo com o ácido para-aminobenzóico. Tendo em vista que a determinação da potência dos antimicrobianos é importante no controle de qualidade das preparações farmacêuticas e que não há um método oficial para análise de dapsona na forma farmacêutica gel, justifica-se a realização deste trabalho.					<B>Objetivo</B>: Desenvolver metodologia de análise microbiológica, através do método de difusão em placa para o doseamento de gel de dapsona a 5 %.					<B>Metodologia</B>: Para o preparo da formulação foram utilizados os seguintes constituintes: dapsona, carbopol, propilenoglicol, hidróxido de sódio e água. Para o desenvolvimento do ensaio microbiológico foram realizados diversos testes alternando-se alguns parâmetros: microrganismo, concentração do inóculo, solução diluente inicial, solução diluente final, concentração das soluções, método, meios de cultura e tempo de incubação.					<B>Resultados</B>: A partir dos ensaios realizados com o gel de dapsona, foi possível estabelecer alguns parâmetros para a análise microbiológica, entretanto a técnica não foi desenvolvida, pois não foi possível obter halos de inibição. Verificou-se o crescimento bacteriano uniforme, mas os halos de inibição, que deveriam se formar ao redor dos discos ou poços, não ficaram nítidos. Sendo assim, testou-se a metodologia empregando solução de enrofloxacino.					<B>Conclusões</B>: Para análise microbiológica do gel de dapsona, nas condições testadas, não foi possível desenvolver uma metodologia ideal a partir dos microrganismos disponíveis na PUCPR. No entanto, o teste de metodologia utilizando enrofloxacino apresentou bons resultados e originou um novo projeto (PIBIC 2015/2016) de iniciação científica, intitulado desenvolvimento de ensaio microbiológico para análise do enrofloxacino.				</P>
			</div><B>Palavras-chave</B>: Dapsona. difusão em ágar. doseamento microbiológico			<BR>
