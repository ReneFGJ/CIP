			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O presente projeto de pesquisa envolve um estudo através da Teoria dos Sistemas Operativos de Nicklas Luhmann que demonstra por meio dos sistemas meio/entorno, autorreferenciabilidade e autopoiése que a pessoa jurídica, muito embora tendo por traz de si pessoas físicas possui a capacidade de auto se organizar e gerir.					<B>Objetivo</B>: Geral e Específicos conforme propostos no projeto inicial e plano do aluno					<B>Metodologia</B>: os propostos no projeto inicial e plano do aluno					<B>Resultados</B>: Compreende-se a partir desse estudo que em que pese a admissão da Constituição e da lei infraconstitucional para a responsabilização penal do ente coletivo, deve se verificar a possibilidade efetiva de enquadrar o ente jurídico na estrutura que envolve o direito penal brasileiro, notadamente na Teoria do Delito.					<B>Conclusões</B>: A implementação do programa de Compliance, de forma impositiva para as empresas que apresentam riscos, sejam eles ambientais ou financeiros, traria, mesmo que a longo prazo, grandes resultados, sejam no tocante à redução ou até mesmo para excluir tais riscos.
Contudo, para uma efetiva aplicação do programa de Compliance, mas que um Compliance Officer, é necessário que o Estado crie órgãos que venham a fiscalizar a implementação dos programas, bem como sua efetividade.
Assim, sabe-se que o excesso de preciosismo e rigorismo no Direito pode ser um entrave ao desenvolvimento sustentável. Por isso, entende-se necessário encontrar um meio termo entre a aplicação das regras básicas do Direito Penal e a implementação de programas de Compliance a fim de evitar e minimizar os riscos.				</P>
