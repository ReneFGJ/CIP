			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The present project of research study through of Social Systems Theory 
of Nicklas Luhmann that demonstrates by means of the systems of device, re-entry and 
autopoiesis, that the legal entity is able to self-organizing. Thus, seeks to analyze the 
criminal  liability  of  the  legal  entity  in  environmental  crimes,  even  though  this  entities 
have a constitutional and infraconstitutional protection in Brasilian Law, this system find 
difficulties to liability the legals entities, because these regulations to be natural person. 
Also,  it  is  carried  out  an  analysis  through  of  compliance  programs					<B>Objectives</B>: The 
general  objective  of  this  research  is  check  the  application  of  complice  programs  in 
companies with environmental risks	and verify if the complice programs can remove or 
mitigate the criminal liability.					<B>Methods</B>: Through, an analytical method the bibliographic 
research  was  done  and  divided  in  four  steps.  The  first  was  developed  an  analysis  of 
application  of  Luhmann´s  theory,  explaining  about  the  possibility  of  collective  being 
included  as  part  autonomously  in  a  criminal  prosecution.  The  second  topic  seeks  to 
understand  that  it  modes  the  liability  of  legal  entity  is  regulated  in  Brazilian  law.  The 
third to very which are the environmental crimes regulations in Brazilian law. Finally, in 
the fourth topic to examine the Compliance Programs and the figure of the Compliance 
Officer.					<B>Results</B>: The results showed  that,  notwithstanding  the  Constitution  brings  the 
forecast  of  the  criminal  liability  to  legal  entity,  the  Brasilian  Legal  System  have  a 
specific  Criminal  law  that  imposes  barriers  for  the  criminal  liability  to  legal  entity, 
because, this law was created to natural person.					<B>Conclusion</B>: It is necessary to frame 
legal  entity  in  the  structure  that  involves  the  brazilian  criminal  law,  only  after  this 
alterations that the law will be able to be responsible with effectiveness the legal entity. 
Lastly, concludes that, the results occur when have the implementation in the imposing 
form of Complice Programs to companies with environmental and economic risks, this 
results are identified to reduction or elimination of risks to these companies. Therefore, 
it  will  be  necessary  that  the  State  creates  agencies  that  come  to  fiscalize  the 
implantation of programs and its effectiveness.				</P>
