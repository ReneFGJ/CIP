			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Diversos fatores estão relacionados à obesidade infantil, dentre eles o fator gênico tem sido apontado como importante preditor. O polimorfismo gênico FTO está associado a maiores prevalências de obesidade na idade adulta. Sabe-se que a obesidade também está associada a ativação do sistema nervoso autônomo. Assim, qual seria a prevalência de FTO em crianças com sobrepeso / obesidade e sua relação com a atividade autonômica?					<B>Objetivo</B>: O objetivo do presente estudo foi associar atividade do Sistema Nervoso Autônomo com o polimorfismo SNP rs9939609 do gene FTO em crianças com sobrepeso e obesidade.					<B>Metodologia</B>: Foram avaliadas 142 crianças classificadas em eutrofia, sobrepeso e obesidade. Determinou-se a variabilidade de frequência cardíaca e o polimorfismo do gene FTO.					<B>Resultados</B>: Identificou-se que indivíduos com sobrepeso e obesidade apresentam predominância na prevalência do polimorfismo SNP rs9939609 do gene FTO com [e]lt;i[e]rt;Odds ratio[e]lt;/i[e]rt; de 1,8 (95% IC 1,5 a 2,13) quando comparados a indivíduos eutróficos. Observou-se em análise multivariada que indivíduos com sobrepeso / obesidade com polimorfismo SNP rs9939609 do gene FTO apresentam (F=21,3; df=3; p=0,001) maior atividade simpática para as variáveis LF (F=12; df=2; p=0,0001) e índice triangular (F=13; df=4; p=0,002) com [e]lt;i[e]rt;Odds ratio[e]lt;/i[e]rt; de 2,2 (95% IC 1,8 a 2,8) .					<B>Conclusões</B>: Conclui-se que crianças com sobrepeso / obesidade apresentam 1,8 mais chances de apresentar polimorfismo SNP rs9939609 do gene FTO. A ativação simpática ocorre 2,2 mais vezes em crianças com sobrepeso / obesidade com presença de polimorfismo.				</P>
			</div><B>Palavras-chave</B>: Sobrepeso / obesidade infantil. Variabilidade de frequência cardíaca. Polimorfismo gênico FTO.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Several factors are related to childhood obesity, including the genetic factors have been identified as an important predictor. The FTO gene polymorphism is associated with higher prevalence of obesity in adulthood. It is known that obesity is also associated with activation of the autonomic nervous system. So what is the prevalence of FTO in overweight / obesity and its relationship to autonomic activity?					<B>Objectives</B>: The aim of this study was to associate activity of the autonomic nervous system with SNP rs9939609 polymorphism of the FTO gene in children with overweight and obesity.					<B>Methods</B>: The 42 children classified as normal weight, overweight and obesity were evaluated. It was determined heart rate variability and polymorphism of the FTO gene.					<B>Results</B>: It was found that overweight and obesity have predominance in the prevalence of SNP rs9939609 polymorphism of the FTO gene with an [e]lt;i[e]rt;Odds ratio[e]lt;/i[e]rt; of 1.8 (95% CI 1.5 to 2.13) when compared to normal individuals. It was observed in multivariate analysis that overweight / obesity with SNP rs9939609 polymorphism of the FTO gene present (F = 21.3, df = 3, p = 0.001) higher sympathetic activity for LF variables (F = 12, df = 2 p = 0.0001) and a triangular index (F = 13; df = 4, p = 0.002) with an [e]lt;i[e]rt;Odds ratio[e]lt;/i[e]rt; of 2.2 (95% CI 1.8 to 2.8).					<B>Conclusion</B>: We conclude that children with overweight / obesity have 1.8 more chances to present SNP rs9939609 polymorphism of the gene FTO. Sympathetic activation occurs 2.2 times more in overweight / obesity with the presence of polymorphism				</P>
			</div><B>Keywords</B>: Overweight / childhood obesity. Heart rate variability. FTO gene polymorphism.</td>
