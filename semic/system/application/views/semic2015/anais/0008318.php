			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Em decorrência do aumento dos índices demográficos a produção agrícola passou, com o advento da tecnologia, por processos de modernização e mecanização, com o intuito de atender o contingente populacional. Por conseguinte, acompanhando as novas formas de produção, o uso de agrotóxicos vem aumentando anualmente. 
O uso exacerbado de agrotóxicos ocasionam impactos ambientais. Não obstante, os casos de doenças relacionadas ao uso destes produtos aumentaram consideravelmente, tais fatores levam a cabo debates sobre os benefícios engendrados por seu uso versus seus problemas socioambientais. Como meio para amenizar os malefícios ocasionados pelo uso em demasia dos defensivos há quem defenda um maior rigor no controle do uso de agrotóxicos, como já é feito em alguns países. 
A importância do presente estudo está em entender o surgimento do agrotóxico em uma análise histórica, os incentivos fiscais concedidos, as consequências e impactos, as instituições que fiscalizam e a comparação com legislação estrangeira a fim de encontrar um ponto de equilíbrio ente os benefícios e os malefícios que se dão pelo uso dos agrotóxicos.					<B>Objetivo</B>: O objetivo principal da presente pesquisa é analisar o uso do agrotóxico no Brasil e porque ocorre de forma desmedida, utilizando-se de fontes bibliográficas, pesquisa de campo e notícias. Sendo que no transcorrer da pesquisa, como já relatado no relatório parcial entendeu-se necessário estender aos objetivos específicos a análise comparativa com legislações estrangeiras, as consequências e impactos do uso e as instituições que fiscalizam e monitoram.					<B>Metodologia</B>: A pesquisa encontra substrato num conjunto bibliográfico e por meio de entrevista a fim de proporcionar uma correta formulação de pensadomento acerca do tema e da verificação dos objetivos. 
De início, é importante destacar a conceituação do tema: diferenciação de agrotóxicos e defensivos agrícolas, tratamento jurídico do tema, fiscalização e registro dos agrotóxicos no Brasil e por fim a comparação com legislação estrangeira.
A metodologia aplicada segue o método descritivo pautada em bibliografia, leitura de notícias e também uma pesquisa de campo com profissional especializado na área de agrotóxicos para assegurar uma rica base de estudo					<B>Resultados</B>: 					<B>Conclusões</B>: 				</P>
