			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A presente pesquisa é uma oportunidade de refletir sobre um dos aspectos da gestão educacional, no que diz respeito ao gerenciamento dos recursos financeiros da educação básica, e o Plano Nacional de Educação 2001 – 2010, conhecer suas metas e a aplicação dos recursos financeiros, para isso foi necessário analisar suas metas e estratégias, bem com gerenciamento dos recursos financeiros.					<B>Objetivo</B>: A pesquisa objetiva caracterizar o PNE, identificar suas metas e estratégias e verificar como estão sendo utilizados os recursos financeiros destinados à educação.					<B>Metodologia</B>: A pesquisa adotada é qualitativa, tendo como principal instrumento a análise documental.  Este estudo foi realizado por meio da consulta aos sites oficiais, o que possibilitou a identificação das metas atingidas no Plano Nacional de Educação no período analisado.					<B>Resultados</B>: Os planos plurianuais da União, dos Estados, Municípios e Distrito Federal serão elaborados de maneira que deem suporte as metas do PNE, e também deverão divulgar a progressiva realização de suas metas e objetivos, para que assim a sociedade civil acompanhe e tenha conhecimento do andamento das metas.  O Plano Nacional da Educação teve vigência de dez anos, possui no total 295 metas distribuídas em 11 áreas, sendo aproximadamente 27 metas para cada uma. A meta de financiamento e gestão da educação é a maior com 44 metas, também a qual possui mais vetos. O Plano Nacional da Educação é o único que abrange todas as etapas da escolarização. Para atingir as metas o governo criou diversos programas e ações entre elas o FUNDEB e o FUNDEF.					<B>Conclusões</B>: Podemos concluir que o PNE 2001-2010, teve muitas metas, porém não estabeleceu medidas para a realização das mesmas, tão pouco para que a sociedade pudesse fiscalizar.  Os dispositivos que foram vetados colaboraram para que o plano não conseguisse atingir seus objetivos, perdendo o caráter de Plano, tornando-se uma carta de intenções				</P>
			</div><B>Palavras-chave</B>: Plano Nacional da Educação. Metas. Recursos Financeiros.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The management of financial resources of education and the national education  2001-2010					<B>Objectives</B>: This study sought to characterize the PNE, identify their goals and strategies and see how the funds are being used for education.					<B>Methods</B>: The research adopted is qualitative, the main instrument to document analysis. This study was conducted by consulting the official sites, which enabled the identification of targets hit in the National Education Plan in the period analyzed.					<B>Results</B>: The multi-annual plans of Federal, State, Municipal and Federal District shall be drawn up so that support the goals of the NAP and should also disclose the progressive realization of your goals and objectives, so that the civil society track and has knowledge the progress of the goals. The National Education Plan was effective for ten years has total 295 goals divided into 11 areas, with about 27 targets for each. The goal of financing and management of education is the largest with 44 goals, which also has more vetoes. The National Education Plan is the only one that covers all stages of schooling. To achieve the goals the government has set up several programs and actions including the FUNDEB and FUNDEF.					<B>Conclusion</B>: We conclude that the PNE 2001-2010, had many goals, but did not establish measures to carry out the same, so little so that society could monitor. Devices that were vetoed contributed to the plan failed to achieve their goals, losing the character Plan, making it a letter of intent.				</P>
