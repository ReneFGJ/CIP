			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A presente pesquisa aborda a temática cidade a sua legibilidade contextualizando parques urbanos como paisagem cultural, e está voltada a determinação de indicadores - constituindo valores visuais, formais e culturais, e sua representação digital, no contexto da cidade de Curitiba, PR.					<B>Objetivo</B>: O estudo tem por objetivo levantar e espacializar atributos de morfologia urbana, os Parques e Bosques da cidade, determinando indicadores em componentes do meio natural e antrópico, com o objetivo de chegar a uma nova metodologia que os qualifiquem quanto a paisagem cultural.					<B>Metodologia</B>: Com intenção de abranger as informações e resultados desta pesquisa, há uma variedade de métodos utilizados, iniciando-se pelo embasamento teórico sobre o tema, compreendendo o estado da arte sobre Patrimônio Cultural. A partir da análise e sintetização destas informações, foram pesquisadas metodologias de conceituação de Patrimônio Cultural Material e Imaterial. A análise dos objetos de estudo, foi realizada através de coleta de dados aliado a avaliação subjetiva dos mesmos, sintetizando os dados em uma tabela, baseado nos indicadores de qualidade, relação e significação e, por fim, a qualificação dos mesmos quanto a Patrimônio Cultural Material e Imaterial.					<B>Resultados</B>: Como resultados há a unificação de todo o embasamento teórico e da análise subjetiva dos parques e bosques de Curitiba, PR, representados através de gráficos 3D por meio do software SketchUp.					<B>Conclusões</B>: Neste trabalho buscou-se definir atributos da morfologia urbana para qualificar a herança cultural dos parques e bosques de Curitiba, e identificar modelos de representação digital que representassem e possibilitassem a análise das relações entre a cidade e a sua significação.				</P>
			</div><B>Palavras-chave</B>: Design da informação; Legibilidade urbana; Patrimônio cultural; Parques urbanos.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: This research addresses the thematic; cities and it’s readability, contextualizing urban parks as cultural landscape, and it’s aimed at identifying  indicators - constituting visual , formal and cultural values, and its digital representation in the context of the city of Curitiba PR.					<B>Objectives</B>: The study aims to raise and spatialize attributes of urban morphology , the Urban Parks and Forests , determining indicators for the  components of natural and man-made environment, in order to reach a new methodology that qualifies them as cultural landscapes.					<B>Methods</B>: On the intention of incresing the information and results of this research , there is  a variety of used methods , starting with the theoretical background on the topic , including the state of the art on Cultural Heritage . From the analysis and synthesis of this information , methodologies  of Material and Immaterial Cultural Heritage concepts were researched. The analysis of the objects of study , was conducted through data collection combined with subjective evaluation thereof, synthesizing the data in a table, based on quality, relationship and meaning indicators, and  finally, their qualification as Material and Immaterial Cultural Heritage.					<B>Results</B>: As a result there is the unification of the whole theoretical basis and subjective analysis of the parks and forests of Curitiba, represented by 3D graphics through the SketchUp software.					<B>Conclusion</B>: In this paper we sought to define attributes of urban morphology to qualify the cultural heritage of parks and Curitiba groves, and identify digital representation of models that represent and make possible the analysis of the relationship between the city and its significance.				</P>
			</div><B>Keywords</B>: Information Design; Urban readability; Cultural Heritage; Urban parks.</td>
