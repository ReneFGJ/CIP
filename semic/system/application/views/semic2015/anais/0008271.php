			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Introdução: O projeto ofereceu uma formação continuada por meio de um curso online para professores que atuam ou pretendem atuar com escolares em tratamento de saúde nos contextos hospitalar e domiciliar. Além da formação pedagógica buscou-se perceber as diferentes realidades vivenciadas pelos participantes em sua atuação com escolares em tratamento de saúde, por meio do registro de suas atividades feitos em portfolio digital.					<B>Objetivo</B>: Objetivos: Analisar os portfólios criados pelos participantes do curso, acompanhando e registrando o processo como tutora deste módulo.					<B>Metodologia</B>: Método: A metodologia desta pesquisa foi baseada em uma abordagem qualitativa, tipo pesquisa-ação, tendo uma intervenção em que pesquisador e o grupo interagiram em todo processo de pesquisa tanto no aspecto online como presencialmente no trabalho pedagógico no HUC.					<B>Resultados</B>: Resultados: Em ambas as propostas do projeto os resultados foram satisfatórios. No curso online, a maioria dos inscritos fizeram boa avaliação dos módulos e metodologia utilizada. Quanto a avaliação dos portfolios apresentados pelos participantes, foi possível conhecer diversas propostas aplicadas em variadas regiões do país, para o atendimento dos escolares em tratamento de saúde.					<B>Conclusões</B>: Conclusão: Este projeto é resultado de estudos e acompanhamentos, que exigiram muita análise e reflexão. Uma de suas grandes vantagens foi o contato com profissionais da pedagogia hospitalar e outras áreas, o que com certeza contribuiu para minha formação e crescimento pessoal e aumentou o interesse pelo contexto hospitalar e também pela área de pesquisa. Aliar a pesquisa e o trabalho pedagógico hospitalar é motivador para os próximos desafios.				</P>
			</div><B>Palavras-chave</B>: Formação Pedagógica. Escolar em tratamento de saúde. Pedagogia Hospitalar.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Introduction: The project offered a continuing education through an online course for teachers who work or intend to work with school health treatment in hospital and home settings. In addition to teacher training sought to understand the different realities experienced by participants in its activities with schoolchildren in health care, by recording their activities made in digital portfolio.					<B>Objectives</B>: Objective: Analyze the portfolios created by the participants of the course, following and recording the process as guardian of this module.					<B>Methods</B>: Method: The methodology of this research was based on a qualitative approach, like action research, with a speech in which researcher and the group interacted throughout the research process both online and in person appearance in the pedagogical work in the HUC.					<B>Results</B>: Results: In both proposed project the results were satisfactory. In the online course, most subscribers made good evaluation of the modules and methodology used. As the evaluation of portfolios submitted by participants, it was possible to apply various proposals in various regions of the country, to attend the school in health care.					<B>Conclusion</B>: Conclusion: This project is the result of studies and monitoring, which required much analysis and reflection. One of its great advantages was the contact with professionals in hospital pedagogy and other areas, which certainly contributed to my education and personal growth and increased interest in the hospital setting and also by the research area. Combine research and teaching hospital work is motivating for the next challenges.				</P>
			</div><B>Keywords</B>: Teacher Training. School in health care. Hospital pedagogy.</td>
