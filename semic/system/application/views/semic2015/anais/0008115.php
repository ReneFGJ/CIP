			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A síndrome coronariana aguda(SCA) é uma doença decorrente da diminuição abrupta do fluxo de sangue através das artérias coronárias devido a uma obstrução. Diversos fatores influencia na mortalidade desses pacientes, um deles é a anemia. Desta forma o uso de Enoxaparina para o tratamento da SCA quando prescrito com dose em excesso exerceria um efeito ambíguo, pois aumentaria o risco de hemorragias e assim levaria a anemia.					<B>Objetivo</B>: Avaliar se a Enoxaparina esta sendo usada de forma correta.					<B>Metodologia</B>: Foi realizada uma análise descritiva, quantitativa e retrospectiva de uma amostra de pacientes internados no Hospital Santa Casa de Misericórdia de Curitiba, PR. Nestes prontuários foram analisados o Escore TIMI de cada paciente e realizado uma classificação das doses de Enoxaparina em: adequada(1mg/kg, SC, de 12/12 horas), subdose([e]lt;0,95mg/kg), leve excesso(1,05-1,20mg/kg) e  grande excesso([e]rt;1,2mg/kg).					<B>Resultados</B>: No nosso trabalho o número de pacientes submetidos a dose correta de Enoxaparina foi de 1,8%, contra 28,3% com subdose e 69,9% com excesso de dose. Dos pacientes que fizeram o uso de subdose de Enoxaparina, 40,5% eram baixo risco pelo Escore TIMI, enquanto os pacientes que fizeram o uso de grande excesso de Enoxaparina, 59,5% eram pertencentes ao grupo de risco intermediário. Dos pacientes que tiveram grande excesso de dose obtiveram uma variação de Hemoglobina, durante o período de internamento de 5,33, enquanto os pacientes submetidos a subdose de Enoxaparina tiveram uma variação de hemoglobina de 5,25(p=0,89).					<B>Conclusões</B>: A maioria dos pacientes não receberam a dose adequada de Enoxaparina e isso influencia na mortalidade dos pacientes.				</P>
