			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Acute coronary syndrome (ACS) is a disease due to the sudden decrease in blood flow through the coronary arteries due to an obstruction. Several factors influence the mortality of these patients one of them is anemia. Thus the use of enoxaparin for treating ACS with excess when prescribed dose exert an ambiguous effect since it would increase the risk of bleeding and thus lead to anemia					<B>Objectives</B>: To assess whether Enoxaparin is being used correctly.					<B>Methods</B>: a descriptive analysis quantitative and retrospective of a sample of patients admitted to the Hospital Santa Casa de Curitiba, PR was performed. These charts were analyzed the TIMI score of each patient and performed a classification of enoxaparin doses in: adequate (1 mg / kg, SC, 12/12 hours), sub-dose ([e]lt;0,95mg / kg), slight excess (1, 05-1,20mg / kg) and large excess ([e]rt; 1.2 mg / kg).					<B>Results</B>: In our study the number of patients undergoing the correct dose of enoxaparin was 1.8% compared with 28.3% and subdose 69.9% excess dose. Of the patients who did the use of sub-dose of Enoxaparin, 40.5% were low risk by the TIMI score, while patients who made use of large excess of Enoxaparin, 59.5% were owned by the intermediate-risk group. Of the patients who had large excess dose obtained a variation of hemoglobina 5,33; hospitalization during the period while patients undergoing suboptimal dose of enoxaparin had a hemoglobin variant of 5,25 (p = 0.89).					<B>Conclusion</B>: Most patients did not receive the appropriate dose of Enoxaparin and this influences the mortality of patients.				</P>
