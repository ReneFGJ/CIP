			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A obesidade é um problema de saúde mundial. Os tratamentos não cirúrgicos para obesidade não possuem bons resultados. A cirurgia bariátrica é apontada como o único método confiável e efetivo para o tratamento. No entanto algumas complicações pós operatórias podem ocorrer, dentre elas o desenvolvimento de fístulas. O retardo no tratamento de uma fístula pode levar a sepse e morte.					<B>Objetivo</B>: O objetivo fundamental deste estudo é relatar o caso de uma paciente submetida a cirurgia bariátrica que desenvolveu fístula gastrocutânea e foi tratada endoscopicamente com prótese auto-expansiva e correlacionar com as informações existentes na literatura sobre esta terapêutica.					<B>Metodologia</B>: Foram pesquisados prontuários de pacientes que realizaram a cirurgia bariátrica no Hospital Sugisawa de Curitiba e evoluíram com fístula no pós operatório. Foi escolhido o prontuário com mais informações hospitalares para redação do relato de caso.					<B>Resultados</B>: Paciente do sexo feminino, 42 anos, com comorbidades. Foi submetida a cirurgia bariátrica (bypass gástrico em Y de Roux) e no segundo pós operatório foi evidenciado o desenvolvimento de uma fístula gastrocutânea. No 17o pós operatório foi inserida endoscopicamente a prótese auto-expansiva. Depois de 4 semanas de tratamento a prótese foi retirada depois de confirmada a cicatrização do orifício fistuloso.					<B>Conclusões</B>: Conclui-se que o tratamento de escolha para tratar fístulas desenvolvidas após a cirurgia bariátrica é a prótese auto-expansiva, no entanto, sugere-se novos estudos relacionados a terapêutica desta complicação.				</P>
			</div><B>Palavras-chave</B>: Fístula. cirurgia bariátrica. complicações. prótese auto-expansiva. stent. tratamento.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Obesity is a global health problem. The non-surgical treatments for obesity have unsatisfactory results. Bariatric surgery is indicated as the only reliable and effective method to treat the obesity. However, some postoperative complications may occur, including the development of fistulas. The delay of the beginning of the treatment of fistulas can lead to sepsis and death.					<B>Objectives</B>: The primary objective of this study is to report the case of a patient who underwent bariatric surgery, developed a gastrocutaneous fistula and was treated endoscopically with self-expanding prosthesis, correlating with the existing information in the literature on this therapy.					<B>Methods</B>: Medical charts of patients who underwent bariatric surgery in Sugisawa Hospital of Curitiba and developed fistula after surgery were analyzed. The chart that was considered the most complete was used for the study.					<B>Results</B>: A female patient, 42 years old, with comorbidities, was submitted to bariatric surgery (gastric bypass Roux-en-Y) and in the second postoperative day was observed the development of a gastrocutaneous fistula. In the 17th day after the surgery, a self-expanding prosthesis was inserted endoscopically. After 4 weeks, the implant was removed after confirming the healing of the fistula’s hole.					<B>Conclusion</B>: It was concluded that the treatment of choice for treating fistulas that developed after bariatric surgery is the self-expanding prosthesis, however, it is suggested futures studies to analyze the treatment of this complication.				</P>
			</div><B>Keywords</B>: Fistula. bariatric surgery. complications. self-expanding prosthesis. stent treatment.</td>
