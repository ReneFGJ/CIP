			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O REA vem expandir e flexibilizar o acesso ao ensino e a aprendizagem, congregando seu licenciamento de maneira aberta e livre para ser modificado, alterado e construído de forma colaborativa.					<B>Objetivo</B>: O objetivo dessa pesquisa é investigar e desenvolver uma proposta de Recurso Educacional Aberto que tenha por objetivo auxiliar no aprendizado de crianças, alunas do ensino fundamental, com necessidades educacionais especiais.					<B>Metodologia</B>: O método escolhido para a pesquisa foi o investigativo utilizando questionário quantitativo.					<B>Resultados</B>: A interação com o aplicativo foi satisfatória pois apresenta 3 tipos de interação simultânea. Foi pensado dessa forma para facilitar a fixação da informação. Trazendo para a realidade do público alvo e adaptação do conteúdo. Onde apresentamos o sinal de libra, associado a letra do alfabeto e associado a imagem que tem a inicial correspondente a letra estudada naquele momento. Libra, escrita e imagem, voltados na mesma interface para o usuário.					<B>Conclusões</B>: A interação do aplicativo é de grande valia para a comunidade em questão. A diretora da escola conseguiu avaliar a proposta do aplicativo e reconheceu ser de grande ajuda para o processo de alfabetização. Onde apresenta poucas opções que auxiliem nesse processo.				</P>
			</div><B>Palavras-chave</B>: Design de interfaces. rea. aplicativo			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The REA is expanding and flexible access to education and learning, bringing its licensing open and free way to be modified , changed and built collaboratively.					<B>Objectives</B>: The purpose of this research is to investigate and develop a proposal for Open Educational Resource whose purpose assist in learning children, pupils of elementary school with special educational needs.					<B>Methods</B>: The method of this research was quantitative with investigative questionnaire application and creation of interface for the application in question.					<B>Results</B>: The interaction with the application was satisfactory because it has three types of interaction simultaneous . It was thus thought to facilitate attachment information .
Bringing the reality of the target audience and adaption of content. where present the pound sign , associated letter of the alphabet and associated with image which has the corresponding initial letter studied at that time. Libra, writing and image , facing the same interface to the user.					<B>Conclusion</B>: The application interaction is of great value to the community in question . The school principal was able to evaluate the application proposal and be recognized great help in the literacy process. Which has few options assist in this process.				</P>
