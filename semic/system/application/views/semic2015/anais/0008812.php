			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Muitas das patologias apresentadas por uma edificação ao logo de sua vida útil são causadas por problemas relacionados ao projeto e execução de suas fundações. Diferentemente de outros materiais comumente utilizados pelo Engenheiro Civil o solo se mostra o mais desafiador em vista dificuldade de se mensurar algumas variáveis que o envolve, distanciando, em algumas situações, a teoria da realidade.					<B>Objetivo</B>: Por sua vez este trabalho busca realizar ensaios de cisalhamento com misturas de solos com diferentes granulometrias, buscando uma melhor aproximação com a realidade. Além disso, busca testar e ativar a nova prensa adquirida pela PUCPR.					<B>Metodologia</B>: : Foram realizados um total de trinta e seis ensaios em amostras de areia de granulometria controlada. Estes ensaios envolveram uma única velocidade de cisalhamento correspondente a 2 mm/min. Cada mistura das amostras foram consolidadas sob seis níveis de tensões normais. Os procedimentos adotados seguiram as orientações da norma da Sociedade Americana de Ensaios de Materiais D-3080 ASTM (2004).					<B>Resultados</B>: Foram obtidos valores para o ângulo de atrito entre 34,95º e 38,12º.					<B>Conclusões</B>: Os resultados indicam o comportamento das misturas granulométricas agem como uma transição das granulométricas puras. Em comparação com pesquisas anteriores mostra a necessidade de realizar um trabalho comparativo entre a nova e antiga prensa.				</P>
			</div><B>Palavras-chave</B>: Resistência ao cisalhamento dos solos. Cisalhamento direto. Resistência das areias.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Many of the pathologies presented by a building to the right of life are caused by problems related to the design and implementation of their foundations. Unlike other materials commonly used by Civil Engineer, the soil proves the most challenging in view of the difficulty of measuring some variables that involves distancing, in some situations, the theory of reality.					<B>Objectives</B>: In turn this work seeks to accomplish shear tests with mixtures of soils with different grain sizes, seeking a better approximation to reality. It also seeks to test and activate the new press acquired by PUCPR.					<B>Methods</B>: They performed a total of thirty-six trials in controlled particle size sand samples. These trials involved a single corresponding shear speed to 2 mm / min. Samples of each mixture were consolidated under six levels of normal stresses. The procedures adopted following the guidelines of the standard of Materials Testing American Society ASTM D-3080 (2004).					<B>Results</B>: Values were obtained for the friction angle between 34,95º and 38,12º.					<B>Conclusion</B>: The results indicate the behavior of particle size mixtures act as a transition from pure grain size. Compared with previous research shows the need for a comparative study between the old and new press.				</P>
			</div><B>Keywords</B>: Soil strength. Direct shear test. Sand resistance.</td>
