			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O estudo desenvolvido teve como enfoque a análise da importância do ensino de Óptica na educação básica.					<B>Objetivo</B>: O ensino da física, mais precisamente a Óptica Física, vem sendo essencial durante gerações para a disseminação da História da Ciência. As dificuldades associadas à implementação desse conteúdo nas salas de aula, aliadas à forma de como o professor a apresenta, contribuem para a construção de conhecimento dos estudantes envolvidos. Abordar esse conteúdo com certeza envolve o conhecimento da Ciência Física. No entanto, passar esses conhecimentos de geração em geração, envolve muito mais do que somente “saber” um conteúdo específico. Grandes Físicos influenciaram gerações com suas descobertas, um deles foi Michael Faraday, que com o uso de velas realizou experimentos para explicar fenômenos ópticos.					<B>Metodologia</B>: A metodologia desenvolvida estava relacionada a leitura e analise dos conceitos físicos, no período de investigação. Tendo como base a Aprendizagem Significativa e a história da Ciência.					<B>Resultados</B>: A leitura das palestras de Faraday, e a maneira com que apresentou diversas descobertas relacionando conceitos de luz. Demonstra como é possível, de maneira bastante didática explicar fenômenos físicos do Universo.					<B>Conclusões</B>: Com esse fim, levando em consideração tais descobertas na metodologia científica, deve-se aprimorar a metodologia abordada pelo professor, tornando-a adequada para a aprendizagem do aluno.				</P>
			</div><B>Palavras-chave</B>: Ensino de Física, História da Ciência, Óptica			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The study conducted was to focus the analysis of the importance of teaching optics in basic education.					<B>Objectives</B>: The physics education, specifically the Physical Optics, has been essential for generations for the dissemination of the history of science. The difficulties associated with the implementation of that content in the classroom, together with the way of how the teacher presents, contribute to the construction of knowledge of the students involved. Addressing this content certainly involves knowledge of Physical Science. However, passing such knowledge generation to generation, it involves much more than just "know" a specific content. Great Physicists influenced generations with his findings, one was Michael Faraday, who with the use of candles conducted experiments to explain optical phenomena.					<B>Methods</B>: The methodology was related to reading and analysis of physical concepts in the investigation period. Based on the Meaningful Learning and the history of science.					<B>Results</B>: The reading of lectures by Faraday, and the way he made several discoveries relating concepts of light. Demonstrates how you can, in a very didactic way to explain physical phenomena of the universe.					<B>Conclusion</B>: To this end, taking into account these findings in scientific methodology, should improve the methodology addressed by the teacher, making it suitable for student learning.				</P>
			</div><B>Keywords</B>: Teaching of Physics, History of Science, Optics.</td>
