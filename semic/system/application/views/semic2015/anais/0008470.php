			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A doença arterial coronariana (DAC) e a doença hepática gordurosa não alcoólica (DHGNA) estão cada vez mais presentes na atualidade e são patologias com grande impacto na saúde pública ocidental; ambas estão intimamente relacionadas ao grau de dislipidemia sérica. Além compartilharem fatores de risco em comum, bem como as suas respectivas fisiopatologias, essas doenças mantêm intrínseca relação com a síndrome metabólica.					<B>Objetivo</B>: A partir de uma amostragem de 147 pacientes, internados no Hospital Santa Casa de Misericórdia de Curitiba, no período de Agosto de 2014 a Julho de 2015, buscou-se correlacionar a gravidade de DAC e de DHGNA, que cada indivíduo por ventura poderia apresentar em concomitância.					<B>Metodologia</B>: Para isso, utilizou-se um questionário, o qual foi complementado com exames laboratoriais e com ecografia de abdome superior.					<B>Resultados</B>: Há uma maior prevalência de doença arterial coronariana grave associada a graus elevados de doença hepática gordurosa não alcoólica.					<B>Conclusões</B>: Independentemente dos desfechos, é de suma importância fazer uma avaliação objetiva e detalhada da provável relação entre as duas doenças, para poder interferir corretamente no tratamento, visando obter melhor prognostico.				</P>
			</div><B>Palavras-chave</B>: Doença Hepática Gordurosa Não Alcoólica. Doença Arterial Coronariana. Correlação. Gravidade.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Coronary artery disease (CAD) and non-alcoholic fatty liver disease (nafld) are  increasingly present at present and are pathologies with great impact on public health; both are closely related to the degree of Dyslipidemia.  In addition share common risk factors, as well as their respective fisiopatologias, these diseases remain intrinsic relationship with metabolic syndrome.					<B>Objectives</B>: From a sample of 147 patients, admitted at the Hospital Santa Casa de Misericórdia of Curitiba, in the period of August 2014 to July 2015, sought to correlate the severity of DAC and NAFLD, that every individual eventually could present concomitantly.					<B>Methods</B>: For that, it was used a questionnaire, which was supplemented with laboratory tests and ultrasound upper abdomen.  Regardless of the outcomes, it is important to do an objective evaluation and detailed the likely relationship between the two diseases, to be able to interfere correctly in the treatment, in order to obtain better prognosis.					<B>Results</B>: There is a higher prevalence of severe coronary artery disease associated with high degrees of non-alcoholic fatty liver disease.					<B>Conclusion</B>: Regardless of the outcomes, it is important to do an objective evaluation and detailed the likely relationship between the two diseases, to be able to interfere correctly in the treatment, in order to obtain better prognosis.				</P>
			</div><B>Keywords</B>: Non-Alcoholic Fatty Liver Disease. Coronary Artery Disease. Correlation. Gravity.</td>
