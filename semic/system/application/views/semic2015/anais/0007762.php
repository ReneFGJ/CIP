			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A pesquisa deste tema refere-se à relação entre a organização corporativa de uma forma geral, e a espiritualidade possível nestes ambientes.					<B>Objetivo</B>: Como ambiente corporativo entendemos como sendo o local de reunião dos funcionários/colaboradores, em todas as suas possíveis hierarquias dentro das organizações, em que se reúnem, neste mesmo e único local, para a execução de suas atribuições profissionais. Sobre a ótica da espiritualidade, para esta pesquisa, não a abordamos como sendo algo vinculado a alguma denominação religiosa, mas tendo como foco a espiritualidade como elemento construtor das relações humanas, uma vez inseridas, neste caso, em uma realidade obrigatoriamente monetária e material. Confrontamos para isso duas literaturas de temas, a princípio, opostos: “INVOLUÇÕES CORPORATIVAS – Perspectivas críticas sobre a gestão de pessoas comunicação e cultura nas organizações” e “VIDA SEGUNDO ESPÍRITO”. Utilizamos também uma terceira obra: “Antropologia Filosófica”, como forma de baliza, para orientar a inter-relação antropológica entre os dois primeiros temas.					<B>Metodologia</B>: Recortes da pesquisa, de cunho qualitativo e bibliográfico.					<B>Resultados</B>: Tendo definidos os elementos e a ótica de abordagem, fizemos a identificação de um e de outro aspecto dentro das citadas obras, traçando assim paralelos e cruzamentos possíveis entre a espiritualidade e as suas possíveis relações nas organizações corporativas.					<B>Conclusões</B>: Esses cruzamentos e paralelismos se deram num âmbito especulativo, de forma a construir um cenário em que se possam perceber as dificuldades dentro de uma organização corporativa, como uma forma possível de construção de espiritualidade própria e particular em cada ambiente.				</P>
			</div><B>Palavras-chave</B>: Organizações Corporativas. Espiritualidade. Ambiente Corporativo.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: This research relates to the relationship between the corporative organization in general, and the possible spirituality in both environments.					<B>Objectives</B>: As a corporate environment we understand to be the place for staff / employees in all possible hierarchies within organizations that come together in this one and only place to perform their professional duties. On the perspective of spirituality, for this research, not the approach as being linked to a religious denomination, but focusing on spirituality as a builder element of human relations, once inserted in this case on a compulsory monetary and material reality. To confront this two literatures themes, at first sight opposed, "Involutions CORPORATE - critical perspectives on people management and communication culture in organizations" and "SECOND LIFE SPIRIT". We also use a third work, "Philosophical Anthropology" as a pattern, to guide the anthropological interrelation between the first two themes.					<B>Methods</B>: Scraps of research, qualitative and bibliographical.					<B>Results</B>: Having defined the elements and the optical approach, we made the identification about aspects within the mentioned works, so plotting parallel and possible intersections between spirituality and their possible relationships in corporate organizations.					<B>Conclusion</B>: These intersections and parallels are given in a speculative context, in order to construct a scenario in which they can realize the difficulties within a corporative organization, as a possible form to build private and own spirituality in each environment.				</P>
