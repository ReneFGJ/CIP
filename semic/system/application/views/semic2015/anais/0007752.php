			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O transplante tem sido o tratamento de escolha para a maioria de pacientes com doença renal em estágio terminal, porém autores defendem a necessidade de uma terapia que atue na melhora da função renal sem os riscos existentes no transplante renal					<B>Objetivo</B>: O estudo foi desenvolvido com o objetivo de realizar uma análise experimental da eficiência do tratamento em conjunto de células-tronco mesenquimais e estatina em lesão renal de isquemia/reperfusão					<B>Metodologia</B>: Quarenta e cinco ratos winstar foram randomizados em 3 grupos com 15 animais em cada. O Grupo 1, grupo controle, foi submetido apenas ao procedimento cirúrgico de isquemia/reperfusão renal sem nenhum medicamento, com exceção de analgesia. Já o Grupo 2 recebeu células-tronco mesenquimais logo após o procedimento com a utilização de estatina no período de 30 dias. O Grupo 3 , por sua vez, não foi submetido à cirurgia, recebendo células-tronco mesenquimais, e também estatina no mesmo período do grupo anterior.
As coletas de sangue periférico foram realizadas, por punção, jugular em momentos do estudo no Centro Cirúrgico do Laboratório Experimental de Tecnologia Celular com analise de creatinina.					<B>Resultados</B>: As dosagens foram coletadas nos dias 0 e 30 : G1 (0,60 ± 0,15 e 0,86 ± 0,13) e G2 (0,61 ± 0,13 e 0,64 ± 0,06). Houve diferença estatisticamente significativa tanto para a comparação dos grupos G1 e G2, 30 dias após o procedimento cirúrgico (p [e]lt;0,001), quanto na comparação dos grupos G1 e G3.					<B>Conclusões</B>: A associação da administração da sinvastatina com as células-tronco mesenquimais demonstrou ser efetiva no auxílio da recuperação da função renal após procedimento de isquemia/reperfusão, possivelmente devido à ação renoprotetora e antiinflamatória.				</P>
			</div><B>Palavras-chave</B>: Transplante renal. isquemia/reperfusão renal. substâncias antioxidantes.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Renal transplantation has been the treatment of choice for most patients with end-stage kidney disease; however, the necessity of a therapy that acts on the improvement of renal function without risks in the procedure still remains as the main point of this kind of surgery.					<B>Objectives</B>: The study was developed in order to perform experimental analysis of the treatment efficiency with mesenchymal stem cells and statins in renal ischemia / reperfusion.					<B>Methods</B>: Winstar forty five rats were randomized in 3 groups divided in 15 animals in each one. The first one, G1, the control group, was submitted only to the surgery of renal ischemia / reperfusion without any medication, except analgesics. The second group, G2, on the other hand has received mesenchymal stem cells after the procedure with the use of statins within 30 days. On last one, G3, in turn, the surgery was not perfomed, receiving mesenchymal stem cells and statin at the same time as the previous group.
Peripheral blood samples were collected by puncture jugular vein at the Surgical Center of Mobile Technology Experimental Laboratory with creatinine analysis.					<B>Results</B>: Measurements were collected on days 0 and 30: G1 (0.60 ± 0.15 and 0.86 ± 0.13) and G2 (0.61 ± 0.13 and 0.64 ± 0.06). There was a statistically significant difference between both groups G1 and G2, 30 days after surgery (p [e]lt;0.001). as well as G1 and G3.					<B>Conclusion</B>: The administration of simvastatin in association with mesenchymal stem cells proved to be effective in the renal function recovery after ischemia / reperfusion procedure, possibly due to its kidney protection effect and anti-inflammatory action.				</P>
			</div><B>Keywords</B>: Kidney transplantation. Renal ischemia / reperfusion. antioxidants.</td>
