			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Políbios, historiador grego genuíno de Megalópolis, membro da federação Aquéia, participou do conflito romano-macedônio no qual preferiu manter se neutro junto de seu pai Licortas. Porém após a submissão de toda Grécia pelas forças romanas, foi mandado para o exílio em Roma, onde escreveu sua maior obra: Histórias, a qual é dedicada a compreender como e com que Constituição em menos de 53 anos os romanos conseguiram submeter todo o mundo conhecido até então.					<B>Objetivo</B>: Além de compreender o contexto histórico vivenciado pelo autor e as principais características da historiografia antiga até sua época, através da analise de sua obra Histórias, buscou-se levantar características que permitam compreender a concepção de História adotada por ele.					<B>Metodologia</B>: A metodologia empregada consistiu em revisão bibliográfica e análise minuciosa da fonte primária em questão; a obra Histórias.					<B>Resultados</B>: Sob uma posição privilegiada, em uma época caracterizada pelo êxito romano e o declínio grego, Políbios empreende em sua narrativa uma história pragmática, que tem como características o objeto de investigação os fatos políticos, a preocupação em atribuir as causas das vitórias e dos fracassos dos acontecimentos, além de ser útil para o presente e de narrar os feitos como eles realmente ocorreram.					<B>Conclusões</B>: Com uma concepção cíclica e aberta da história, no sentido de que a degeneração inevitável das instituições se dá de forma imprevisível, Políbios faz da história ensinamento para lidar com as situações do presente.				</P>
			</div><B>Palavras-chave</B>: História. Políbios. Pragmática.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Polybius, Greek historian of Megalopolis, a member of Achaea Federation, attended the Roman-Macedonian conflict in which preferred to keep it neutral with his father Licortas. But after submission of all Greece by Roman forces, he was sent into exile in Rome, where he wrote his greatest work: Histories, which is dedicated to understanding how and with what Constitution in less than 53 years the Romans were able to submit all over the world known until then.					<B>Objectives</B>: In addition to understanding the historical context experienced by the author and the main characteristics of ancient historiography, through the analysis of his Histories, sought to lift characteristics that enable understanding of the conception of History adopted by it.					<B>Methods</B>: The methodology consisted of a literature review and analyze the primary source in question; the Histories.					<B>Results</B>: Under a privileged position in a time characterized by the Roman success and the Greek decline, Polybius undertakes in his narrative a pragmatic history, whose features the research object political facts, the concern in tracing the causes of the victories and failures of events as well as being useful for the present and narrate the deeds as they actually occurred.					<B>Conclusion</B>: With a cyclic and open conception of history, in the sense that the inevitable degeneration of institutions occurs unpredictably, Polybius makes history tool to help in present situation.				</P>
			</div><B>Keywords</B>: History. Polybius. Pragmatic.</td>
