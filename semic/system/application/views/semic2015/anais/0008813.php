			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Neste trabalho de iniciação científica, estudam-se os verbos dicendi não canônicos do português brasileiro e, para isso, são abordados os conceitos de valência, transitividade, regência, ordem canônica, papéis temáticos, hierarquia temática, sintagmas e restrições semânticas, com base nos conceitos apresentados por Bechara (2009), Perini (2008), Neves (2002), Ilari e Basso (2014) entre outros autores.					<B>Objetivo</B>: O objetivo desta pesquisa é analisar a distribuição de verbos dicendi não-canônicos do português brasileiro, e comparar os seus contextos de aplicação e uso, observando a possibilidade de comutação entre eles e, caso a comutação não seja possível, especificar qual é a restrição selecional que se apresenta (sintática e/ou semântica).					<B>Metodologia</B>: Foi elaborada uma tabela considerando-se as características semânticas dos verbos, e os testes de valência foram feitos com verbos de carga semântica semelhante, nos enunciados levantados em 17 textos, sendo 8 artigos jornalísticos, 7 notícias e 2 textos retirados de mangás.					<B>Resultados</B>: 					<B>Conclusões</B>: Observou-se que os gêneros textuais selecionados favorecem o uso de verbos dicendi, e o estudo mais aprofundado, compreendendo outros gêneros além desses, pode vir a mostrar-se muito produtivo em pesquisas futuras, devido, inclusive, à variedade de verbos introdutores de discurso no português brasileiro.				</P>
			</div><B>Palavras-chave</B>: Verbos de dizer não-canônicos. transitividade. restrições semânticas e sintáticas.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: In this scientific research work, non-canonical Brazilian Portuguese verbs are studied and, therefore, the study examines the valency concepts, transitivity, verbal regency, canonical order, thematic roles, thematic hierarchy, phrases and semantic restrictions, based on concepts presented by Bechara (2009), Perini (2008), Neves (2002), Ilari and Basso (2014) and other authors.					<B>Objectives</B>: The objective of this research is to analyze the distribution of Brazilian Portuguese non-canonical dicendi verbs, and to compare their contexts of application and role, noting the possibility of a switching between them, and when the switching is not possible, there is the aim to specify which kind of selectional restriction is present in the sentence (syntactic and / or semantic).					<B>Methods</B>: A table was drawn up considering the semantic features of verbs, and valence tests were made with similar semantic load of verbs. The corpus has 17 texts: 8 newspaper articles, 7 stories and 2 texts taken from mangas (Japanese comic strip).					<B>Results</B>: 					<B>Conclusion</B>: It was observed that the selected text genres favor the use of non-canonical dicendi verbs, and a further study, including other genres, may ultimately prove to be very productive in future research about the variety of reporting verbs in Brazilian Portuguese.				</P>
			</div><B>Keywords</B>: Non-canonical reporting verbs. transitivity. semantic and syntactic constraints.</td>
