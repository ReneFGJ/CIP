			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: As políticas de transferencia monetária condicionada -  Conditional Cash Transfer (CCTs), são políticas com objetivo erradicar ou reduzir a pobreza ou, também, amenizar seus efeitos.  Na America Latina ocorreu  uma difusão  deste programa social no período 1990-2014.					<B>Objetivo</B>: Analisar o processo de decisão envolvido na adoção de políticas de transferencia monetária condicionada. Identificar os elementos da política de tipo CCT: competência (responsáveis pela formulação e implementação), cobertura (população-alvo); objetivos e noções de pobreza. Verificar a relação entre implementacao de CCTs e a ideologia política de governantes da América Latina.					<B>Metodologia</B>: Coleta de dados sobre 22 países da América Latina e os programas de transferência monetária condicionada implementados. Classificação e comparação dos dados. Fonte: site e documentos oficiais de governos e de CCTs.					<B>Resultados</B>: Governos com posições ideológico- partidárias de direita ou de esquerda adotaram politicas de combate à pobreza com características  semelhantes. Verificou-se  similaridade entre os objetivos, público - alvo e condicionalidades. Esta convergencia resultou de um  processo de difusão deste tipo de politica na regiao. Portanto, para a implementação desta política , a ideologia políticas de governantes não foi significativa.					<B>Conclusões</B>: 				</P>
			</div><B>Palavras-chave</B>: Política pública social. difusão de políticas. convergência de políticas. ideologia política			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Conditional cash transfer policies - (CCTs) are designed to eradicate or reduce poverty or mitigate its effects. In Latin America there was a dissemination of this social program in the period 1990-2014.					<B>Objectives</B>: Analyze the decision process involved in conditional cash transfer policies. Identify the elements of the CCT: competence (responsible for the formulation and implementation), coverage (target population); and goals. Examine the relationship between implementation of CCTs and the political ideology of governments.					<B>Methods</B>: Search data on conditional cash transfer programs implemented in 22 Latin American countries. Classification and comparison of data. Source: site and official documents of governments and on CCTs.					<B>Results</B>: Governments of right-wing parties and left-wing parties  have implemented similar policies to combat poverty . There was similarity between the objectives, target - population and conditionalities. This convergence resulted from a process of international diffusion. Therefore the political ideology and the poltical party of governments  was not significant to implement condicional cash transfere programs.					<B>Conclusion</B>: 				</P>
			</div><B>Keywords</B>: Social policy. policy diffusion. policy convergence. political ideology</td>
