			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Cerebral palsy is a comorbidity of multiple causes with an incidence of 7 / 1,000 live births in third world countries. These patients have varying degrees of sequelae, however all had some motor impairment level due to spasticity. This clinical condition is a challenge for the doctor as your response to non-invasive methods is restricted. The botulinum toxin has showed good results, but must be reapplied every six months to maintain the effects. However, selective neurotomy is a revolution in the treatment, with excellent long-term results demonstrated in studies.					<B>Objectives</B>: To get a better psychomotor development of these patients, we propose a literature review demonstrating the implementation and results of the surgical selective neurotomy technique in patients with chronic encephalopathy.					<B>Methods</B>: Literature review on the treatment of cerebral palsy focused on selective neurotomy in the upper limbs through PubMed, LILACS, Scielo and Bireme.					<B>Results</B>: Selective neurotomy shows promise in the treatment of spasticity in patients with cerebral palsy, being effective also in children and adults. However, studies of this surgery technique are still few.					<B>Conclusion</B>: At the end of the research it became clear the relevance of surgical treatment in patients with spasticity, not only in cases of chronic encephalopathy, as well as in other comorbidities. Unfortunately, even with promising results in the long term, there are few articles that cover their relevance.				</P>
			</div><B>Keywords</B>: Spasticity. seletive neurectomy. cerebral palsy</td>
