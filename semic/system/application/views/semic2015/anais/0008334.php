			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A paralisia cerebral é uma comorbidade de causas multifatorias com incidência de 7/1.000 nascidos vivos em países de terceiro mundo. Estes pacientes apresentam graus diferentes de sequelas, porem todos apresentaram algum nível de comprometimento motor devido a espasticidade. Esta condição clinica é um desafio para o médico, pois sua resposta a métodos não invasivos é restrita. Dentre os mais aplicados, esta a toxina botulínica com bons resultados, porém deve ser reaplicada a cada seis meses para manutenção de resultados. Contudo, a neurotomia seletiva representa uma revolução no tratamento, com resultados excelentes a longo prazo demonstrados em trabalhos na literatura.					<B>Objetivo</B>: Para buscar um melhor desenvolvimento psicomotor destes pacientes, propomos uma revisão de literatura demonstrando a aplicação e resultados da técnica cirúrgica de neurotomia seletiva em pacientes portadores de encefalopatia crônica.					<B>Metodologia</B>: Revisão de literatura sobre o tratamento da paralisia cerebral com enfoque na neurotomia seletiva em membros superiores por meio do PubMed, LILACS, Bireme e Scielo.					<B>Resultados</B>: A neurotomia seletiva mostra-se promissora no tratamento da espasticidade de pacientes portadores de paralisia cerebral, mostrando-se eficaz não só em crianças como em adultos. Contudo, estudos realizados com esta técnica cirurgia ainda são poucos.					<B>Conclusões</B>: final da pesquisa ficou claro a relevância do tratamento cirurgico em pacientes com espasticidade, não apenas em casos de encefalopatia crônica, como também em outras comorbidades. Infelizmente, mesmo com resultados a longo prazo promissores, ainda são poucos os artigos que abrangem sua relevância.				</P>
			</div><B>Palavras-chave</B>: Espasticidade. neurectomia seletiva. paralisia cerebral.			<BR>
