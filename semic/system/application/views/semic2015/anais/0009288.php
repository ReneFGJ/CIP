			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A tractografia (imagem por tensor de difusão) é um método de visualização dos feixes (tratos) de fibras nervosas in vivo por meio da ressonância magnética. Essa técnica permite visualizar a orientação e a integridade dos feixes de substância branca por meio da mensuração da difusão das moléculas de água nas fibras neurais.					<B>Objetivo</B>: Realizar uma revisão de literatura sobre os princípios e as aplicações da tractografia no sistema nervoso humano, bem como identificar as possíveis aplicações futuras, visando entender como a técnica pode ser útil para o diagnóstico e acompanhamento de doenças.					<B>Metodologia</B>: A busca foi feita nas bases de dados Medline, sendo incluídos sessenta e oito (68) artigos nessa revisão após a seleção feita pelos autores.					<B>Resultados</B>: A tractografia mostrou ter aplicação nas mais diversas doenças neuropsiquiátricas e na neurocirurgia. A técnica tem aplicação fundamental no planejamento cirúrgico para ressecções tumorais ou de biópsias, diagnóstico precoce de doenças neoplásicas ou degenerativas assintomáticas do sistema nervoso, avaliação da extensão de doenças, acompanhamento da sua evolução, resposta ao tratamento e prognóstico. Entretanto, a tractografia ainda apresenta algumas limitações, como em áreas do cérebro em que há o cruzamento de fibras, em situações onde várias fibras estão se prolongando em mais de uma direção, a falta de homogeneidade do campo magnético, o movimento da cabeça e artefatos.					<B>Conclusões</B>: Cada vez mais a tractografia vem se mostrando útil e com diversas aplicações nas doenças que acometem o sistema nervoso central.				</P>
			</div><B>Palavras-chave</B>: Tractografia. Tensor de Difusão. Ressonância Magnética			<BR>
