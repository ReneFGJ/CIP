			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Tractography (diffusion tensor imaging) is an in vivo visualization method of nerve fiber bundles (tracts) by MRI. This technique allows the viewing of the orientation and integrity of white matter bundles by measuring the diffusion of water molecules in nerve fibers.					<B>Objectives</B>: To conduct a literature review of the principles and applications of tractography in the human nervous system as well as identify possible future applications, in order to understand how the technique can be useful for the diagnosis and monitoring of diseases.					<B>Methods</B>: A search was carried out in Medline and included sixty-eight (68) review articles after the selection made by the authors.					<B>Results</B>: Tractography was shown to have application in various neuropsychiatric disorders and neurosurgery. The technique has important application in surgical planning for tumor resections or biopsies, early diagnosis of neoplastic or asymptomatic degenerative diseases of the nervous system, evaluating the extent of disease, monitoring of its evolution, response to treatment and prognosis. Meanwhile, tractography still has some limitations, as in areas of the brain where there is crossing fibers, in situations where several fibers are extending in more than one direction, the lack of homogeneity of the magnetic field, the movement of the head and artefacts					<B>Conclusion</B>: Tractography has proven useful and with potential applications in several diseases that affect the central nervous system.				</P>
