			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A presente pesquisa científica tem como projeto central analisar a concepção de felicidade no pensamento de Santo Agostinho. A partir da análise do pensamento agostiniano referente a este tema, faremos também uma relação desse pressuposto felicidade com o homem contemporâneo. Isso, por entendermos que a felicidade é um ideal buscado a todo custo nos dias de hoje por parte do homem, entretanto, um questionamento nos levou a desenvolver a presente pesquisa; ou seja, que compreensão o homem atual tem no que se refere a felicidade, e que meios o mesmo usa com o intuito de alcança-la. É esse questionamento que orientará toda ela. 
	Assim, esta pesquisa está estruturada da seguinte forma: em um primeiro momento expomos a concepção de felicidade para Santo Agostinho; depois apresentamos sinteticamente uma visão do homem contemporâneo, e de que forma ele busca a felicidade. Assim, tivemos informações importantes para relacionar o pensamento agostiniano referente ao tema com o homem de hoje, a fim de saber se se aproxima ou se há um distanciamento muito grande da concepção de felicidade para Santo Agostinho, em relação ao homem atual. 
	Por fim, sem a pretensão de esgotar o tema nessa pesquisa, nosso objetivo final é deixar alguns questionamentos e tentar apontar alguns caminhos para que a humanidade possa melhor entender e resgatar esse ideal que infelizmente está tão distorcido nos dias de hoje.					<B>Objetivo</B>: O objetivo desta pesquisa é estudar a concepção agostiniana de felicidade, esta que por sua vez foi pensada por Agostinho a partir da perspectiva cristã.    Depois, buscamos fazer uma análise do homem atual, partindo de um teórico contemporâneo cujo nome é Gilles Lipovetsky , a fim de entender se e como esse homem busca tal pressuposto, e a partir disso tentar descobrir se o mesmo ainda almeja e se pode atingir esse ideal de felicidade proposto por Santo Agostinho. Nossa pretensão com o presente trabalho, não é dar uma resposta definitiva acerca da felicidade para o homem atual, mas apenas deixar alguns questionamentos, e apontar alguns possíveis caminhos que devemos trilhar para alcança-la.					<B>Metodologia</B>: Levantamento bibliográfico e documental sobre o tema da pesquisa, leitura e releitura das obras de Santo Agostinho, principalmente a obra Confissões; assim como seus principais comentadores;
