					<B>Introduction</B>: This scientific research is central project to analyze the concept of happiness at the thought of St. Augustine. From the analysis of the Augustinian thought regarding this issue, we will also respect this assumption with happiness contemporary man. This, because we believe that happiness is an ideal sought at all costs these days by the man, however, questioning led us to develop this research; namely that understanding the current man has with regard to happiness, and that means it uses in order to reach it. It is this question that will guide all of it.
Thus, this research is structured as follows: at first we expose the concept of happiness for St. Augustine; after briefly present a vision of contemporary man, and how he seeks happiness. So we had important information to relate the Augustinian thought concerning the issue with the man of today in order to know whether approaching or if there is a sharp departure from the conception of happiness to St. Augustine, from the current man.
Finally, with no claim to exhaust the subject in this research, our ultimate goal is to leave some questions and try to point out some paths for humanity to better understand and rescue this ideal that unfortunately is so distorted today.					<B>Objectives</B>: The objective of this research is to study the Augustinian conception of happiness, this in turn was thought by Augustine from a Christian perspective. Then we seek to analyze the current man, from a contemporary theorist whose name is Gilles Lipovetsky, in order to understand if and how this man seeks such an assumption, and from there try to find out if it still craves and can achieve this ideal of happiness proposed by St. Augustine. Our intention with this work is not to give a definitive answer about happiness for the current man, but only leave some questions, and point out some possible paths we must walk to reach it.					<B>Methods</B>: Bibliographical and documentary survey on the subject of research, reading and rereading of St. Augustine´s works, especially the Confessions work; as well as its principal commentators;
