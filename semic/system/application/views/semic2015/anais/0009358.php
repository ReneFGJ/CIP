			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A compactação do solo consiste em um dos fatores relacionados à ação impeditiva das raízes, dificultando as culturas em expressar seu máximo potencial produtivo.  Benefícios importantes têm sido observados na estruturação do solo em médio e longo prazo com a utilização de plantas de cobertura com alto potencial de fixação de carbono e que possuam sistema radicular volumoso e agressivo, com capacidade de crescer em solos com alta resistência à penetração, criando poros por onde as raízes da cultura subsequente possam crescer.					<B>Objetivo</B>: O objetivo do projeto consiste na avaliação no processo de recuperação de mata ciliar degradada e qual a relação desse processo associado a utilização de diferentes tipos de adubação verde.					<B>Metodologia</B>: O experimento esta situado no município de Fazenda Rio Grande, na Fazenda Experimental Gralha Azul da Pontifícia Universidade Católica. Os trabalhos vêm decorrendo desde maio de 2011. A área total do experimento é de 0,31 há, sendo 50 parcelas de 25 m² cada (5X5), divididos em 10 tratamentos com 5 repetições cada um. Para a avaliação das características físicas do solo, foram coletadas amostras do solo com o auxílio de anéis volumétricos das parcelas que estão sendo tratadas com a adubação verde.					<B>Resultados</B>: Os valores obtidos indicam que não há compactação na área experimental estudada, e os tratamentos com adubação verde se assemelham à testemunha.					<B>Conclusões</B>: Na análise estatística não se observou diferença significativa nos tratamentos avaliados, seja na densidade do solo ou a porosidade total, o que indica continuidade no processo de recuperação das propriedades físicas do solo.				</P>
			</div><B>Palavras-chave</B>: Física do solo. Recuperação de áreas degradadas. Adubação verde.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Soil compaction is one of the factors related to the inhibitive action of roots, hindering cultures to express its maximum productive potential. Significant benefits have been observed on the soil structure in the medium and long term with the use of cover crops with high carbon fixation potential and possessing massive and aggressive root system, with ability to grow in a high penetration resistance soils, creating pores where the roots of subsequent culture can grow.					<B>Objectives</B>: The goal of the project is to assess the degraded riparian forest recovery process and what the relationship of this process associated with the use of different types of green manure.					<B>Methods</B>: The experiment is situated in the municipality of Fazenda Rio Grande, in the Experimental Farm Crow Blue Catholic University. The works come elapsing since May 2011. The total area of the experiment is 0.31 there, 50 installments of 25 m² each (5X5), divided into 10 treatments with 5 repetitions each. For the evaluation of soil physical properties, soil samples were collected with the aid of volumetric rings of parcels being treated with green manure					<B>Results</B>: The evaluated parameters indicate that there is no compression in the study area, indicating that treatments with green manure resemble the witness.					<B>Conclusion</B>: Statistical analysis there was no significant difference in the treatments evaluated, either in soil density or porosity, which indicates continuity in the recovery process of the physical properties of the soil.				</P>
			</div><B>Keywords</B>: Soil physics. Recovery of degraded areas. Green manure.</td>
