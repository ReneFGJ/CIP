			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A proteína C-reativa (PCR) corresponde a uma proteína de fase aguda predominantemente sintetizada pelo fígado e regulada por citocinas pró inflamatórias, tais como fator de necrose tumoral alfa (TNF-[e]#945;) e interleucina seis (IL-6). A resposta da fase aguda refere-se a uma reação complexa e não específica do animal que ocorre após qualquer lesão tissular.					<B>Objetivo</B>: O presente estudo teve como objetivo detectar alterações séricas de PCR em cadelas portadoras de tumores mamários, avaliar estas alterações quanto aos valores encontrados antes e após a mastectomia e com isso verificar se a retirada do tumor pode resultar na diminuição dos níveis desta proteína inflamatória.					<B>Metodologia</B>: Foi realizado a coleta do sangue por meio de punção da veia jugular e armazenado em tubos sem anticoagulante, em seguida o sangue foi dessorado e armazenado em ependorfes a -20ºC até análise. A mensuração das concentrações séricas de proteína C-Reativa foram realizadas utilizando ELISA com o kit "C-Reactive Protein ELISA kit" da fabricante MILLIPORE, de acordo com os procedimentos dispostos no manual.					<B>Resultados</B>: As amostras coletadas apresentaram concentração media (±DP) de, logo antes da cirurgia 4,89 (± 2,99), logo após a cirurgia 5,19 (± 3,75), 24 horas após a cirurgia 32,78 (± 24,53b), 7 dias após a cirurgia 15,20 (± 19,79), 14 dias após a cirurgia 8,92 (± 6,39) e 30 dias após a cirurgia 8,69 (± 7,36). Não houve diferença na concentração sérica de animais hígidos e animais com tumores mamários. Ocorreu um aumento significativo da concentração sérica de PCR tendo seu pico 24 horas após a cirurgia.					<B>Conclusões</B>: Não há diferença na concentração sérica de animais hígidos e animais com tumores mamários; 
Ocorre um aumento significativo 24 horas após a cirurgia;
A retirada do tumor não resulta em diminuição das concentrações séricas de PCR, pelo menos 30 dias após a cirurgia.				</P>
