			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: C-reactive protein (CRP ) is an acute phase protein predominantly synthesized by the liver, is regulated by pro- inflammatory cytokines, such as, tumor necrosis factor (TNF - [e]#945; ) and interleukin six ( IL -6 ) . The acute phase response refers to a complex reaction and not specific to any animal that occurs after tissue injury .					<B>Objectives</B>: Measure the serum levels of CRP before and after surgery bitches with mammary tumors undergoing mastectomy.
Estimate the time these levels had to be modified and when they returned to normal.
To assess these changes by means statistics.					<B>Methods</B>: Was collected the blood by puncture of the jugular vein and stored in tubes without anticoagulant , then the blood was dessorado and stored in ependorfes in the -20 ° C until analysis. The measurement of serum concentrations of C- reactive protein were performed using ELISA with the kit " C- Reactive Protein ELISA kit " of MILLIPORE manufacturer in accordance with the procedures provided in the manual.					<B>Results</B>: The samples showed average concentration (± SD), just before the surgery 4.89 (± 2.99), just after the surgery 5.19 (± 3.75), 24 hours after surgery 32.78 (± 24,53b) 7 days after surgery 15.20 (± 19.79) 14 days after surgery 8.92 (± 6.39) and 30 days after surgery 8.69 (± 7.36). There was no difference in serum of healthy animals and animals with mammary tumors. A significant increase in serum CRP having its peak 24 hours after surgery.					<B>Conclusion</B>: There is no difference in serum of healthy animals and animals with mammary tumors; A significant increase occurs 24 hours after surgery; The removal of the tumor did not result in a decrease in serum CRP levels, at least 30 days after surgery.				</P>
