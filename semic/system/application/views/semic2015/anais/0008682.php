			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: a sepse, designada como uma manifestação clínica grave, ocorre pela decomposição da matéria orgânica, causada por diferentes tipos de microorganismos. É uma das principais causas de morte em UTI e no Brasil a taxa de óbitos é em torno de 65%. A glutamina, aminoácido presente em abundância no organismo humano, quando está envolvida em estados hipermetabólicos, como na sepse, tem sua concentração reduzida, devido à sua alta taxa de captação e utilização por diversos tecidos em relação à sua síntese e liberação pelo músculo esquelético.					<B>Objetivo</B>: constitui-se em avaliar o uso da glutamina em ratos submetidos a sepse abdominal com sua utilização por via parenteral no período pré sepse.					<B>Metodologia</B>: foram utilizados 20 ratos separados em grupo A (n=9) e grupo B (n=11). O primeiro grupo foi submetido à sepse abdominal por ligadura e perfuração do ceco e o segundo foi submetido à sepse abdominal e ofertado glutamina intraperitoneal. Ocorreu a eutanásia 48 horas após o início da sepse, com coleta de fígado, pulmões, rim e intestino delgado para análise histológica.					<B>Resultados</B>: a análise do fígado demostrou diminuição da intensidade de esteatose hepática, redução dos níveis de tumefação e da sua congestão. Na avaliação pulmonar houve melhora em todos os parâmetros (congestão dos capilares dos septos alveolares, grau de congestão das arteríolas e artérias dos septos interlobulares e interlobares, quantidade de neutrófilos em septos alveolares e quantidade de neutrófilos em torno às arteríolas e artérias). Nos rins a administração da glutamina diminui o edema intersticial renal. Na análise do intestino houve redução da descamação epitelial					<B>Conclusões</B>: a administração de glutamina em ratos apresentou resultados satisfatórios em relação ao grupo controle, com preservação de alterações histopatológicas significativas				</P>
			</div><B>Palavras-chave</B>: Sepse. ratos. glutamina. alterações histopatológicas			<BR>
