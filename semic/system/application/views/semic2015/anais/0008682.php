			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: sepsis, designated as a severe clinical manifestation, is the decomposition of organic matter caused by different types of microorganisms. It is a leading cause of death in ICU and in Brazil the rate of deaths is around 65%. The glutamine amino acid present in abundance in the human body when it is involved in hypermetabolic states such as in sepsis, has its low concentration, due to its high rate of uptake and use by various tissues relative to their synthesis and release by skeletal muscle.					<B>Objectives</B>: the objective of this study consists in evaluating the use of glutamine in rats undergoing abdominal sepsis with its use  parenteral  pre sepsis period.					<B>Methods</B>: 20 rats separated in group A were used (n = 9) and group B (n = 11). The first group underwent abdominal sepsis by cecal ligation and puncture and the second was submitted to abdominal sepsis and offered intraperitoneal glutamine during about three daysbefore sepsis					<B>Results</B>: The analysis of the liver demonstrated decrease in the intensity of hepatic steatosis, reduction of swelling levels and its congestion. With pulmonary there was improvement in all parameters (congestion of the capillaries of the alveolar septa, degree of congestion of arterioles and interlobular arteries and interlobular septa, number of neutrophils in alveolar septa and quantity of neutrophils around the arterioles and arteries). In the kidneys the administration of glutamine decreases renal interstitial edema. The analysis showed a reduction bowel epithelial desquamation.					<B>Conclusion</B>: Glutamine administration in rats presented satisfactory results in the control group, with significant histopathological changes.				</P>
