			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O presente trabalho de pesquisa tem como tema Festas religiosas populares na Tradição Oriental,					<B>Objetivo</B>: cujo objetivo é perceber as relações existentes entre as festas do ano litúrgico bizantino e as tradições culturais existentes nas comunidades.					<B>Metodologia</B>: Para o presente trabalho efetuou-se um recorte sobre a festa da Páscoa, que é a principal dentre as constantes no calendário religioso. A pesquisa se deu por meio de levantamento bibliográfico sobre o tema e também por meio da participação nos ritos das comunidades ucranianas.					<B>Resultados</B>: Após o levantamento da bibliografia e da pesquisa de campo, se deu a análise do material encontrado para a busca do estabelecimento de possíveis relações existentes entre aquilo que se celebra e aquilo que se vive nas tradições culturais. Os resultados obtidos foram os seguintes: 1) compreensão do conceito fundamental de música sacra litúrgica para os orientais; 2) revisão de literatura sobre música sacra litúrgica  oriental.					<B>Conclusões</B>: Foi possível discutir a respeito do lugar da música na liturgia da tradição oriental  e perceber como ela contribui para enriquecer a experiência religiosa das comunidades.				</P>
