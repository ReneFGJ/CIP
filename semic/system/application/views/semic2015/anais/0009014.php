			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Chronic Kidney Disease is a status of persistent renal dysfunction  , characterized by irreversible loss of nephrons, leading to the need of renal replacement therapy , including dialysis and kidney transplant. Peritoneal dialysis is one of the most frequent renal replacement therapies used in pediatric patients. Peritonitis is the most important complication, resulting in increase of morbidiy and, technique failure.					<B>Objectives</B>: The aim of this study is to describe the peritonitis episodes in the pediatric population from a Multicenter Peritoneal Dialysis Cohort .					<B>Methods</B>: Prospective study analyzing the BRAZPD II Cohort, composed incident and prevalente patients on peritoneal dialysis followed in 122 brazilian centers. We evaluated peritonitis episodes , clinical and demographic characteristics of a subgroup of patients from 0 to 18 years old.					<B>Results</B>: We analyzed data from 361 incident patients who stayed at least 90 days on peritoneal dialysis. They were followed by a mean period of 13 months. Chronic glomerulonephritis was the underlying  kidney disease in 23% of the patients and in 43% the cause was unknown. During the follow up, 85 patients presented at least one peritonitis episode, after median time of 29 months on dialysis. .Staphyloccoccus aureus and Coagulase negative Staphylococcus were responsable for  38% of the positive cultures. Fungal  Peritonitis occurred in 1.8% of patients					<B>Conclusion</B>: The main data on our study are in agreement with global published studies.We emphasize the need of prevention of peritonitis , since it is the most importante cause of technique failure on peritoneal dialysis.				</P>
