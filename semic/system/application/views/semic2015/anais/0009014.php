			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A Doença Renal Crônica é um estado de disfunção renal persistente, que se caracteriza por perda irreversível dos néfrons, sendo necessária terapia de substituição renal que inclui métodos dialíticos ou transplante renal. A diálise peritoneal é uma das formas de tratamento de substituição renal mais utilizadas em pacientes pediátricos. Sua principal complicação é a peritonite. A peritonite em crianças em diálise peritoneal crônica continua contribuindo para morbidade significativa e pode levar à falha técnica irreversível, além de reduzir a longevidade desta modalidade terapêutica.					<B>Objetivo</B>: O objetivo do trabalho é descrever os episódios de peritonite na população pediátrica de uma Coorte Brasileira Multicêntrica de pacientes em Diálise Peritoneal.					<B>Metodologia</B>: Estudo prospectivo realizado a partir da coorte BRAZPD II, composta de pacientes incidentes e prevalentes em diálise peritoneal de 122 centros nacionais. Foram avaliados os episódios de peritonite e características clínicas e demográficas de um subgrupo de pacientes de 0 a 18 anos.					<B>Resultados</B>: Avaliou-se 361 pacientes de 0 a 18 anos,incidentes em diálise peritoneal e que permaneceram pelo menos 90 dias na terapia. Os pacientes foram seguidos durante um tempo médio de 13 meses. Glomerulonefrite crônica foi a doença renal subjacente em 23% dos pacientes e 43% de etiologia desconhecida. Durante o seguimento, 85 pacientes desenvolveram pelo menos um episódio de peritonite, após uma mediana de tempo de 29 meses em diálise .Staphyloccoccus aureus e Staphylococcus coagulase negativo foram responsáveis por 38% das culturas positivas.  Peritonite fúngica ocorreu em 1,8% dos pacientes					<B>Conclusões</B>: Os principais dados encontrados são compatíveis com a literatura mundial. Ressaltamos a importância de se evitar infecção pois é a principal causa de falência do método dialítico.				</P>
			</div><B>Palavras-chave</B>: Peritonite. Diálise Peritoneal. Criança. Adolescente			<BR>
