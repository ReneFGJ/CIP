			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Introdução: Depois da publicação do parágrafo único do art. 442 da CLT que garante não possuir vinculo empregatício entre cooperativas e associados nem entre tomadores de serviço e os mesmos, não importando qual seja o ramo de atividade da sociedade cooperativa, passaram a existir várias pseudo-cooperativas fundadas com o exclusivo objetivo de diminuir custos administrativos das empresas. Neste momento os empregados saem lesados, pois são obrigados a se tornarem cooperados, porém continuam exercendo as mesmas atividades, desta vez sem receber seus direitos trabalhistas e assim permanecem, muitas vezes porque precisam do trabalho para sobreviver e sofrem ameaças de perder o emprego caso não aceitem essa condição.					<B>Objetivo</B>: Objetivo: Tentar mostrar financeiramente que a ilicitude não traz vantagens para os tomadores de decisões das empresas, tentando resgatar o verdadeiro significado das cooperativas de trabalho para a economia e a sociedade.					<B>Metodologia</B>: Método: Construir uma folha de pagamento de uma empresa LTDA e comparar com todos os valores que deverão ser pagos pela pseudo-cooperativa quando comprovada a fraude trabalhista.					<B>Resultados</B>: Resultados: A pseudo-cooperativa quando comprovada a fraude gasta praticamente o dobro que pagaria para seus empregados se estivesse trabalhando com a licitude, ou seja, ela gasta R$ 794.387,14 a mais.					<B>Conclusões</B>: Conclusão: Este meio utilizado pelas empresas de reduzir seus custos não é a melhor tomada de decisão para a empresa, existem outras formas lícitas que podem trazer resultados efetivos, pois esta prática que foi estudada por esta pesquisa apenas traz prejuízos. O grande problema são as políticas sobre as cooperativas que são falhas, e por este motivo, abrem brecha para muitos empresários conseguirem tirar vantagem com esse tipo de ação.				</P>
			</div><B>Palavras-chave</B>: Cooperativas de Trabalho			<BR>
