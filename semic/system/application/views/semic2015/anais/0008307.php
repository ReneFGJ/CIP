			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Introduction: After the publication of the sole paragraph of art 442 of the Consolidation of Labor Laws (CLT), which guarantees doesn’t have employment bond between cooperatives and associates neither between borrowers and the associates, no matter what the business activity of the cooperatives is, some illegal cooperatives came into existence with the exclusive aim of reducing administrative costs. At this time, the employees are injured because they are required to become members, but are still carrying out the same activities, this time without receiving their labor rights and so remain, often because they have to work to survive and suffer threats of losing their jobs if they don’t accept this condition.					<B>Objectives</B>: Trying to show financially that the unlawfulness don’t bring benefits to the decision makers of companies, trying to rescue the true meaning of work cooperatives for the economy and the society.					<B>Methods</B>: Method: Build a payroll of a Private Limited Company (Lda.) and compare all the values that should be paid by the illegal cooperatives when proven fraud.					<B>Results</B>: Results: When the illegal cooperatives proven fraud, they spends almost the double what they pay for their employees if you were working with lawfulness, in other words, the illegal cooperatives spend R$ 794.387,14 more.					<B>Conclusion</B>: Conclusion: This form used by the companies to reduce their costs isn’t the best decision-making for the company, there are other legal forms that can bring effective results, as this practice was studied by this research only brings losses. The big problem the policy on cooperatives that are flawed and therefore, gives opening for many business owners can take advantage with this type of action.				</P>
