			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O presente projeto de Iniciação Científica destina-se a estudar a Lei 12.965, de 23 de Abril de 2014, mais conhecida como Marco Civil da Internet. Com ela cria-se uma legislação específica para regular as atividades na internet, antes disciplinadas de forma esparsa em diversos diplomas legais.					<B>Objetivo</B>: O projeto tem como objetivo estudar sobre o marco civil que originou a Lei 12.965/2014 e os principais direitos e deveres do internauta, bem como averiguar regulamentações e outros dispositivos que tratam de regrar o uso na internet.					<B>Metodologia</B>: O método da pesquisa é o dedutivo, no qual se verifica as premissas para se chegar a uma determinada conclusão. Do ponto de vista dos procedimentos técnicos, pode ser definida como pesquisa bibliográfica, valendo-se de outros meios, como o eletrônico.					<B>Resultados</B>: Aprofundou-se o estudo em alguns direitos do internauta, principalmente aqueles previstos no artigo 7º da Lei do Marco Civil, e os deveres foram deduzidos a partir dos principais crimes cometidos no meio virtual. Foi visto que muitas condutas podem se tornar crimes, assim devendo o internauta tomar cuidado em suas ações em rede. A palestra, constituindo parte do projeto, foi um sucesso; elaborou-se uma cartilha intitulada ´´cartilha do jovem internauta``,a partir da qual foi apresentado o tema a alunos do ensino médio.					<B>Conclusões</B>: Ante a doutrina pesquisada, verificou-se que a Lei do Marco Civil da Internet muito beneficiou a sociedade da informação, estabelecendo princípios, garantias, direitos e deveres para o uso da Internet no Brasil, porém traz algumas lacunas e termos duvidosos em sua redação, a qual deve ser trabalhada.				</P>
			</div><B>Palavras-chave</B>: Internet. direitos. deveres			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The Scientific Initiation project is intended to study the Law 12,965, of April 23, 2014, better known as Mark Civil Internet. With it creates is a specific legislation to regulate the activities on the Internet before disciplined scattered throughout several legal instruments.					<B>Objectives</B>: The project aims to study on the civil mark that originated the Law 12,965/2014 and the main rights and duties of Internet users, as well as investigate regulations and other provisions addressing the regrar using the Internet.					<B>Methods</B>: The research method is deductive, in which one sees the premises for reaching a particular conclusion. From the point of view of technical procedures, it can be defined as literature, making use of other means, such as electronic.					<B>Results</B>: Deepened the study in some Internet user´s rights, particularly those provided for in Article 7 of the Civil Mark Act, and duties were deducted from major crimes committed in the virtual environment. It has been seen that many behaviors can become crimes, so should the internet user be careful in their network actions. The lecture, constituting part of the project was a success; produced is a booklet entitled ´´cartilha the young internet user``, from which the theme was presented to high school students.					<B>Conclusion</B>: Before the doctrine searched, it was found that the Civil Mark Internet Law greatly benefited the information society, establishing principles, guarantees, rights and duties for the use of the Internet in Brazil, but has some gaps and uncertain terms in his writing, which must be worked.				</P>
