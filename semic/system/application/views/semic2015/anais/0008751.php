			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A composição das populações de plantas daninhas em um agroecossistema é reflexo de suas características edafoclimáticas e das práticas agronômicas adotadas, como manejo do solo e aplicação de herbicidas.					<B>Objetivo</B>: O objetivo da pesquisa foi avaliar a composição e distribuição da flora de plantas daninhas emergidas em agroecossistema de soja transgênica e convencional, assim como verificar a similaridade da comunidade infestante entre os dois sistemas de produção de soja em diferentes épocas da safra e verificar a possível seleção de biótipos resistentes ao herbicida glyphosate.					<B>Metodologia</B>: O experimento foi conduzido durante a safra agrícola de 2014/15 na Fazenda Experimental Gralha Azul/PUCPR, no município de Fazenda Rio Grande, PR, em duas áreas de produção de soja, sendo uma transgênica e outra convencional em condições de plantio direto. Foram realizados: o estudo fitossociológico da comunidade de plantas daninhas utilizando-se o método do quadrado inventário; o índice de diversidade biológica e o índice de similaridade das áreas experimentais.					<B>Resultados</B>: Com base nos resultados obtidos, verifica-se que houve um baixo índice de similaridade (40,0%) entre as áreas de soja convencional e transgênica. Na área de soja transgênica, a espécie azevém se destacou pela maior densidade, frequência, dominância e IR, em relação às demais espécies presentes, o que leva a uma menor equitabilidade, demonstrando indícios de seleção de biótipos de azevém resistentes ao herbicida glyphosate.					<B>Conclusões</B>: Está ocorrendo seleção de biótipos de azevém nas áreas de soja transgênica.				</P>
			</div><B>Palavras-chave</B>: Soja. levantamento fitossociológico. seleção. resistência. planta daninha.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The composition of the populations of weeds in an agroecosystem is reflecting characteristics of soil, climate and agronomic practices adopted, such as soil tillage and herbicide application.					<B>Objectives</B>: The objective of the research was to evaluate the composition and distribution of emerged weeds in agroecosystems from transgenic and conventional soybean, as well as verify the similarity of the weed community between the two production systems soybean crop at different times and check the possible selection of biotypes glyphosate resistent.					<B>Methods</B>: The experiment was carried out during the season of 2014/15 at Gralha Azul Experimental Farm/PUCPR, in Fazenda Rio Grande County, PR, in two areas of soybean production, being a transgenic and other  one conventional in no-till system. Were performed: the phytosociological study of weeds using the inventory square method; the rate of biological diversity and similarity index of experimental areas.					<B>Results</B>: Based on these results, it appears that there was a low level of similarity (40%) among the areas of conventional and transgenic soybean. In the area of transgenic soybean, ryegrass species stood out for greater density, frequency, dominance and IR, compared to the other species present, which leads to a lower evenness, showing evidence of selection of resistant ryegrass biotypes to glyphosate.					<B>Conclusion</B>: It is occurring selection of biotope ryegrass in transgenic soybean area.				</P>
			</div><B>Keywords</B>: Soybean. Phytosociological survey. Selection. Resistence. Weed.</td>
