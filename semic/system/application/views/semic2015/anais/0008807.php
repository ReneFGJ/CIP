			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A associação de compósitos reforçados por fibra de vidro e sistema CAD/CAM (Computer Aided Design/ Computer Aided Manufacturing) na construção de retentores intrarradiculares personalizados permite a padronização na construção dos mesmo e consequente ganho em precisão e adaptação.					<B>Objetivo</B>: O presente estudo avaliou por análise de microtomógrafo a espessura da película de cimentação nos terços apical, médio e cervical de retentores intrarradiculares de compósitos reforçados por fibra de vidro construídos com tecnologia CAD/CAM.					<B>Metodologia</B>: Vinte (20) pré-molares monorradiculares humanos foram padronizados pelo seccionamento da coroa e distribuídos aleatoriamente em dois grupos (n=10) de acordo com o material de confecção dos retentores intrarradiculares: PF (pinos pré-fabricados de fibra de vidro) e CRFV (pinos de fibra de vidro personalizados por CAD/CAM). A análise da espessura de cimento foi realizada por Sistema de Microtomográfia de Raio X (mod. 1172, Skyscan). As imagens foram geradas a partir de 1300 secções axiais e 500 secções laterais em torno do longo eixo das raízes. As espessursa de cimento em micrômetros (µm) foram obtidas a partir de 10 medições em cada terço da raiz.					<B>Resultados</B>: A ANOVA a dois critérios revelou área de cimentação inferior para o retentor CRFV em relação ao PF (p[e]lt;0,05) nos terços médio e cervical, não havendo diferença entre ambos no terço apical (p[e]rt;0,05).					<B>Conclusões</B>: Concluiu-se que a construção de retentores de fibra de vidro utilizando o sistema CAD/CAM permitiu melhor adaptação dos mesmos, o que resultou em espessuras de cimento menores que aquelas com retentores pré-fabricados.				</P>
			</div><B>Palavras-chave</B>: Pino. CAD/CAM. Cimento. Espessura.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The combination of glass fiber reinforced composites and CAD/CAM (Computer Aided Design/Computer Aided Manufacturing) to construct custom intracanal posts allows standardization of the process with consequent gain in accuracy and adaptability.					<B>Objectives</B>: This study evaluated by analysis of microtomography the thickness of the cementing film in the apical, middle and cervical thirds of glass fiber reinforced posts constructed   with CAD / CAM technology.					<B>Methods</B>: Twenty (20) human premolars had their crowns sectioned and were distributed randomly into two groups (n = 10) according to the construction material of the posts: PF (pre-fabricated glass fiber post) and CRFV (glass fiber posts customized by CAD/CAM). The analysis of the cement thickness was performed by microtomography system X-ray (mod. 1172, Skyscan). The images were generated from 1,300 axial sections and 500 side sections around the long axis of the root. The cement thickness (um) were obtained from 10 measurements in each third of the root.					<B>Results</B>: The two-way ANOVA revealed a lower cement thickness of CRFV compared to PF (p [e]lt;0.05) in the middle and cervical thirds, with no difference between them in the apical third (p[e]rt; 0.05).					<B>Conclusion</B>: The construction of glass fiber posts using the CAD/CAM system has better adaptation, resulting in lower cement thicknesses than those with prefabricated posts.				</P>
			</div><B>Keywords</B>: Post. CAD/CAM. Cement. Thickness.</td>
