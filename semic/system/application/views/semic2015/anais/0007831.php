			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Ensaios de dissolução tem se tornado uma importante ferramenta para caracterizar a performance de medicamentos. Para garantir que um novo método analítico gere informações confiáveis e interpretáveis sobre a amostra, ele deve sofrer uma avaliação denominada validação, sendo essencial para definir se a metodologia desenvolvida está completamente adequada aos objetivos a que se destina. Na literatura não foram encontrados relatos sobre métodos de ensaios de dissolução de comprimidos revestidos de desloratadina.					<B>Objetivo</B>: O presente trabalho teve como objetivo validar a metodologia de dissolução para comprimidos revestidos de desloratadina, metodologia esta desenvolvida anteriormente.					<B>Metodologia</B>: As condições de dissolução utilizadas foram 900 mL de HCl 0,1 N como meio de dissolução, utilizando pás (aparato II) à 100 rpm e análise por UV à 280 nm. O método foi validado de acordo com os requerimentos regulatórios através dos parâmetros especificidade, linearidade, precisão e exatidão.					<B>Resultados</B>: O método demonstrou especificidade, linearidade (R2: 0,9994), precisão (DPR [e]lt; 2 %) e exatidão ([e]rt; 99 %).					<B>Conclusões</B>: Os resultados obtidos demonstraram que o método proposto é específico, pois não houve interferência dos excipientes na quantificação da desloratadina no comprimento de onda de 280 nm, linear na faixa de 1,11 à 8,33 µg/mL, preciso, com uma precisão média de 100,09 % (DPR: 1,04 %) e exato.				</P>
			</div><B>Palavras-chave</B>: Dissolução. validação. desloratadina.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Dissolution test has become an important tool for characterizing performance of drugs. To ensure a reliable and interpretable information about the sample from an analytical method, it must undergo a so-called validation evaluation, which is essential to determine if this methodology is completely appropriate to the objectives to which they relate. In the literature there were no reports of dissolution methodology to coated tablets desloratadine.					<B>Objectives</B>: The aim of this work was to validate the dissolution methodology for coated tablets desloratadine, a methodology previously developed.					<B>Methods</B>: The dissolution conditions used were 900 ml of 0.1 N HCl as dissolution medium, using paddles (apparatus II) at 100 rpm and analyzed by UV at 280 nm. The method was validated in accordance with regulatory requirements through parameters such as specifity, linearity, precision and accuracy.					<B>Results</B>: The method demonstrated specificity , linearity (R2: 0.9994) , precision (RSD [e]lt;2%) and accuracy ([e]rt; 99%).					<B>Conclusion</B>: The results demonstrated that the proposed method is specific, since there was no interference of the excipients in the quantification of desloratadine at a wavelength of 280 nm, linear over the range 1.11 to 8.33 g / ml, precise, with an average accuracy of 100.09 % (RSD : 1.04% ) and accurate.				</P>
			</div><B>Keywords</B>: Dissolution. validation. desloratadine</td>
