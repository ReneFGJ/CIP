			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The practice of physical activity can be considerably influenced by environmental considerations. Among these, the availability of spaces where people can be physically active is very important for the cities.					<B>Objectives</B>: The aim of this study was to describe and compare the profile of the visitors and activity levels in Fitness Zone (FZ) in Curitiba, PR in the years 2012 and 2015.					<B>Methods</B>: The profile of the users variables were gender, age, household income, education, walking, company to attend FZ and met new people in the neighborhood after the implementation of FZ. The variables for use patterns were: weekly frequency, length of stay (min / day) and time of use (months). Physical activity levels were categorized into: inactive (0-9 min / week), insufficiently active (10-149 min / week) and active ([e]#8805; 150 min / week). The year the collection was divided in 2012 and 2015 to compare the data. Associations were analyzed with the chi-square test (x²) for proportion and heterogeneity in SPSS 20.0 software and the significance level maintained at 5%.					<B>Results</B>: The major practice of walking was positive associated with the year 2015 (p [e]lt;0.05), whereas minor time spent in the FZ showed inverse association with 2012 (p [e]lt;0.05).					<B>Conclusion</B>: These results can guide interventions to promote greater use of FZ and the practice of physical activity performed at these locations.				</P>
