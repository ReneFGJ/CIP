			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A prática de atividade física pode ser influenciada consideravelmente por aspectos ambientais. Entre estes, a disponibilidade de espaços onde as pessoas possam ser fisicamente ativas é de grande importância para as cidades.					<B>Objetivo</B>: O objetivo deste estudo foi descrever e comparar o perfil dos frequentadores e os níveis de atividade nas academias ao ar livre (AAL) de Curitiba, PR nos anos de 2012 e 2015					<B>Metodologia</B>: Foram comparadas 10 AAL do ano 2012 e 2015, no total 255 participantes foram intencionalmente entrevistados. Sendo 172 em 2012 e 83 em 2015. As variáveis do perfil dos frequentadores foram: sexo, faixa etária, renda domiciliar, escolaridade, prática de caminhada, companhia para frequentar a AAL e conheceu novas pessoas no bairro após a implantação da AAL. As variáveis para padrão de uso foram: frequência semanal, tempo de permanência (min/dia) e tempo de utilização (meses). Os níveis de atividade física foram categorizados em: inativos (0 a 9 min/sem), insuficientemente ativos (10 a 149 min/sem) e ativos ([e]#8805; 150 min/sem). O ano da coleta foi dividido em 2012 e 2015 para comparação dos dados. As associações foram analisadas com o teste de qui-quadrado (x²) para proporção e heterogeneidade no software SPSS 20.0 e o nível de significância mantido em 5%.					<B>Resultados</B>: A maior prática de caminhada foi associada positivamente com o ano de 2015 (p[e]lt;0,05), enquanto o menor tempo de permanência na AAL apresentou associação inversa ao ano de 2012 (p[e]lt;0,05).					<B>Conclusões</B>: Estes resultados podem guiar intervenções para promover o maior uso das AAL e a prática de atividade física realizada nestes locais.				</P>
			</div><B>Palavras-chave</B>: Atividade física, Academias ao Ar Livre, Adultos			<BR>
