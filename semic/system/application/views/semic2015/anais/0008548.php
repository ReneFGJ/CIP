			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Quadrirotores é uma classe de helicópteros de uma área maior denominada UAVs (Veículos Aéreos Não-Tripulados, do inglês Unmanned Aerial Vehicle) que utiliza quatro rotores para propulsão, geralmente dispostos em formato de cruz. O controle de movimento e feito por meio da mudança da velocidade relativa entre os quatro rotores.					<B>Objetivo</B>: Este projeto aborda o desenvolvimento de um controlador baseado em Computação Natural a ser utilizado em  UAVs, com a finalidade de coletar dados por meio de câmera acopladas em um quadrirotor.					<B>Metodologia</B>: Primeiramente, são detalhados os conceitos gerais de sistemas nebulosos e controle nebuloso (fuzzy control), que em seguida são utilizados para desenvolver um modelo matemático do comportamento não linear de um UAV fabricado pela empresa Parrot denominado AR Drone. Baseado no modelo matemático obtido, foi utilizado um simulador em ambiente computacional Matlab/Simulink e outro em ambiente Gazebo, para validar o método de controle desenvolvido.					<B>Resultados</B>: A estratégia de controle nebuloso adotada é baseada na combinação de quadro parâmetros: altura, arfagem, rolagem e guinada. Estes parâmetros incrementam ou reduzem a potência de cada motor do AR Drone de forma a atender as especificações de projeto e seguir uma trajetória.					<B>Conclusões</B>: 				</P>
			</div><B>Palavras-chave</B>: Controle nebuloso. veículos aéreos não-tripulados.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Unmanned aerial vehicles (UAVs) has attracted a great amount of attention among scientists over the last decades, due to, the widespread area of applications, e.g. near-area surveillance, crop dusting firefighting, exploration both in military and commercial in- and outdoor applications, and so on.					<B>Objectives</B>: This work deals with development of a controller based on a Natural Computing called fuzzy system to be used in UAVs, in order to collect data through videos using quadrirotor.					<B>Methods</B>: First, it details the design concepts of a fuzzy controller.  which are then used to develop a mathematical model of the nonlinear behavior of a Parrot AR Drone. In this drone,  the four rotors are symmetrically distributed respect to the central cabin. Based on the mathematical model, a simulator using Matlab/Simulink and Gazebo computational environments was used to validate the control method.					<B>Results</B>: The fuzzy control strategy implements the logic used to control the UAV. The controller decisions are taken based on the combination of the four parameters: height, pitch, roll and yaw. These actions increase or reduce the power of each of the motors in order to get the specifications and to follow a trajectory.					<B>Conclusion</B>: 				</P>
			</div><B>Keywords</B>: Fuzzy control. unmanned aerial vehicles.</td>
