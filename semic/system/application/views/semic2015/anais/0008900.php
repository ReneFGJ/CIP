			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O objetivo desta pesquisa é uma oportunidade de refletir sobre os aspectos da gestão educacional, no que diz respeito ao gerenciamento dos recursos financeiros da educação básica, e o Plano Nacional de Educação 2014-  2024. Para tanto, se fez necessário analisar as metas e estratégias estabelecidas no Plano Nacional de Educação, no que diz respeito ao financiamento da educação e os avanços obtidos a partir da aplicação dos mesmos nos diversos níveis da educação básica. A referida Lei será implementada nos próximos dez anos.					<B>Objetivo</B>: A pesquisa objetiva em caracterizar o PNE, identificar suas metas e estratégias e em verificar como estão sendo utilizados os recursos financeiros destinados à educação.					<B>Metodologia</B>: Trata-se de uma pesquisa de qualidade qualitativa, com análise documental e bibliográfica. Fundamentada em Bogdan e Biklen.					<B>Resultados</B>: O PNE é constituído por 20 metas e 243 estratégias específicas. As metas são destinadas para a educação básica a educação superior a valorização, formação e remuneração dos profissionais da educação, incluindo a meta de investimento em educação em conformidade do PIB.					<B>Conclusões</B>: Para o cumprimento das metas dos PNE 2014-2024 é necessária uma manutenção de mecanismos que permitam equiparar as desigualdades contrastadas mediante a imensa demanda do país e as especificidades que emanam as diferentes regiões do Brasil.				</P>
			</div><B>Palavras-chave</B>: Plano Nacional da Educação. Metas. Recursos Financeiros.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: This research is an opportunity to reflect on aspects of educational administration, with regard to the management of financial resources of basic education, and the National Education Plan 2014- 2024. Therefore, it was necessary to analyze the goals and strategies established in the National Education Plan, with regard to the funding of education and advances obtained from the application of the same at all levels of basic education. This law will be implemented over the next decade.					<B>Objectives</B>: This study sought to characterize the PNE, identify their goals and strategies and see how the funds are being used for education.					<B>Methods</B>: This is a qualitative quality research, with documental and bibliographical analysis. Based on Bogdan and Biklen.					<B>Results</B>: The NAP consists of 20 goals and 243 specific strategies. The goals are meant for basic education to higher education enhancement, training and remuneration of professionals in education, including education on investment target in accordance GDP.					<B>Conclusion</B>: To achieve the goals of the NAP 2014-2024 a maintenance mechanisms is necessary to enable equate inequality contrasted by huge demand of the country and the specifics emanating from different regions of Brazil.				</P>
