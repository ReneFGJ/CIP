			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Este estudo tem como objeto os saberes elementares matemáticos da Escola Primária nas primeiras décadas do século XX.					<B>Objetivo</B>: O objetivo da pesquisa foi buscar vestígios dos saberes elementares matemáticos (Aritmética, Geometria e Desenho) em fontes que circularam no estado do Paraná no período de 1903 a 1953, momento em que as escolas primárias do estado seguiam princípios advindos do Ensino Intuitivo e da Escola Nova.					<B>Metodologia</B>: Trata-se de um estudo histórico desenvolvido na perspectiva da história cultural, cujas fontes foram encontradas no Instituto de Educação do Paraná Professor Erasmo Pilotto. Essas fontes referem-se, principalmente, à Revista do Ensino, uma publicação do Rio Grande do Sul que teve grande circulação no Paraná, desde os anos de 1940. Inicialmente, foi realizado o inventário do material localizado, em seguida, a digitalização das capas e sumários dos artigos. Posteriormente foram identificados artigos que traziam vestígios dos saberes matemáticos elementares sendo os mais freqüentes os relacionados à Aritmética.					<B>Resultados</B>: As análises dos artigos indicam que os saberes elementares da escola primária passavam por uma transformação metodológica expressiva.					<B>Conclusões</B>: As revistas saindo do ensino meramente intuitivo propunham atividades de uma escola ativa em que os educadores preocupavam-se com as etapas de desenvolvimento da criança, propondo formas de um ensino mais concreto e menos memorístico e abstrato, com atividades mais dinâmicas e mais relacionadas aos interesses da criança, contrapondo-se ao tradicional ensino mecânico e memorístico da organização didática tradicional, exercícios repetitivos e descontextualizados da vida infantil.				</P>
			</div><B>Palavras-chave</B>: Saberes elementares matemáticos. Ensino Primário. História Cultural.			<BR>
