				<P>
					<B>Introduction</B>: This paper studied the Mathematical basic knowledge Primary School in the early decades of the twentieth century.					<B>Objectives</B>: The objective of the inquiry went to look for tracks of there knew elementary mathematicians (Arithmetic, Geometry and Drawing) in fountains that circulated in the state of the Paraná in the period from 1903 to 1953, moment in which the elementary schools of the state were following beginnings resulted from the Intuitive Teaching and from the New School.					<B>Methods</B>: It is the question of a historical study developed in the perspective of the cultural history, whose fountains they were found in the Institute of Education of Paraná Professor Erasmo Pilotto. These fountains refer, mainly, to the Magazine of the Teaching, a publication of Rio Grande do Sul that had great circulation in the Paraná, from the years of 1940. Initially, there was carried out the inventory of the located material, next, to scanning of the capes and summary of the articles. Subsequently there were identified articles that were bringing tracks of elementary mathematicians knew when the made a list ones be the most frequent to the Arithmetic.					<B>Results</B>: The analyses of the articles indicate that you know them elementary about the elementary school they were suffering a transformation methodological expressive.					<B>Conclusion</B>: The magazines going out from the merely intuitive teaching were proposing activities of an active school in which the educators were preoccupying with the stages of development of the child, proposing the forms of a more concrete teaching and fewer memory and abstractly, with activities more dynamic and more made a list to the interests of the child, being in opposition to the traditional mechanical teaching and memory of the traditional educational organization, repetitive exercises and decontextualized of the childlike life.				</P>
