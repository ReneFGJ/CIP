			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The question about the meaning of life is never fully subsided. It is a decisive issue in the life of each person, it involves existence as a whole. Actually, this question arises acutely in today´s society deeply changed. Several are the proposals of meaning, many inconsistent and contradictory.					<B>Objectives</B>: Our objective is to theoretically investigate the condition of the crisis and the search for existential meaning of human, from the actually crisis of meaning which passes the society and present the experience of faith in Jesus Christ as a concrete propose of sense to life.					<B>Methods</B>: In order to achieve the proposed objectives, collect the necessary bibliography about our theme and proceeded with the reading and analysis of the material. We organize our study, which is of theoretical nature, in two main areas: first, we studied how the current context of crisis and the search for existential meaning is; later, we research on the experience of the Christian faith as a propose meaning.					<B>Results</B>: As a theoretical result, we note that have meaning in life is to have a direction; it means having an ethical and spiritual way. Ignore this sense may have pain more extreme: that of an existential emptiness; nihilism is the term generally used to describe the overall sickness of our time: the crisis of meaning. In the other side, we observed that the deep and authentic meaning of life starts from the experience of the encounter with the person of Jesus Christ; it is he who gives a decisive direction to life.					<B>Conclusion</B>: Then, we conclude that the experience of Christian faith remains a forceful response to the problem of sense of crisis. It can be considered a sure foundation capable of supporting the building of one´s life and leads it to the plenitude.				</P>
