			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O questionamento acerca do sentido da vida nunca é totalmente aquietado. Trata-se de uma questão decisiva na vida de cada pessoa, envolve a existência como um todo. Atualmente, essa questão se coloca de modo agudo na sociedade atual profundamente modificada, e diversas são as propostas de sentido, muitas delas inconsistentes e contraditórias.					<B>Objetivo</B>: Temos como objetivo investigar teoricamente a condição da crise e da busca de sentido existencial do ser humano, a partir da crise atual de sentido pela qual passa a sociedade, e apresentar a experiência da fé em Jesus Cristo como uma proposta concreta de sentido para a vida.					<B>Metodologia</B>: A fim de atingir os objetivos propostos, coletamos a bibliografia necessária acerca de nosso tema e procedemos com a leitura e análise do material. Organizamos nosso estudo, que é de caráter teórico, em dois eixos principais: primeiro, estudamos como está o atual contexto da crise e da busca de sentido existencial; posteriormente, pesquisamos sobre a experiência da fé cristã como proposta de sentido.					<B>Resultados</B>: Como resultado teórico, notamos que ter sentido na vida é ter uma direção, um rumo; significa ter um caminho ético e espiritual. Ignorar este sentido pode comportar a dor mais extrema: a de um vazio existencial; o niilismo é o termo geralmente usado para designar a doença geral do nosso tempo: a crise de sentido. Por outro lado, observamos que o sentido profundo e autêntico da vida se dá a partir da experiência do encontro com a pessoa de Jesus Cristo; é Ele quem dá um rumo decisivo à vida.					<B>Conclusões</B>: Assim, chegamos à conclusão de que a experiência da fé cristã continua sendo uma resposta contundente à problemática da crise de sentido. Ela pode ser considerada um alicerce seguro capaz de sustentar a edificação da própria vida e leva-la à plenitude.				</P>
			</div><B>Palavras-chave</B>: Crise de sentido. Niilismo. Fé cristã. Sentido			<BR>
