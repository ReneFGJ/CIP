			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A erliquiose é uma doença causada pela bactéria Ehrlichia canis, sua transmissão ocorre durante o repasto sanguíneo, através do carrapato Rhipicephalus sanguineus. A anaplasmose trombocitopenia canina é induzida por Anaplasma platys essas podem ocorrer nos cães e já tem evidencias da ocorrência em humanos.					<B>Objetivo</B>: O presente estudo teve como objetivo avaliar a epidemiologia molecular da [e]lt;i[e]rt;E. canis[e]lt;/i[e]rt; e A. platys em cães domiciliados da região de Toledo, PR.					<B>Metodologia</B>: Os cães foram selecionados aleatoriamente, foram realizadas coletas de 197 amostras de sangue, essas foram armazenadas até a realização da técnica de Reação em cadeia da polimerase (PCR).					<B>Resultados</B>: A epidemiologia de [e]lt;i[e]rt;E. canis[e]lt;/i[e]rt; foi de 6,09% (12/197) e de A. platys de 5,58% (11/197).					<B>Conclusões</B>: Com a realização do trabalho, os resultados demonstraram que há presença de infecção da [e]lt;i[e]rt;E. canis[e]lt;/i[e]rt; e A. platys nos cães avaliados no estudo. A técnica de PCR apresenta uma grande importância para o diagnóstico das enfermidades, pois os animais podem apresentar a doença subclínica, onde essa apresenta um risco para outros cães.				</P>
			</div><B>Palavras-chave</B>: Anaplasmose. Erliquiose. Ocorrência e População de Cães.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The ehrlichiosis is a disease caused by [e]lt;i[e]rt;Ehrlichia canis[e]lt;/i[e]rt; by transmission occurs during the blood meal by Rhipicephalus sanguineus, anaplasmosis is canine thrombocytopenia induced by Anaplasma platys these may occur in dogs and already have evidence occurring in humans.					<B>Objectives</B>: This study aimed to evaluate the molecular epidemiology of [e]lt;i[e]rt;E. canis[e]lt;/i[e]rt; and A. platys in dogs from the Toledo, PR.					<B>Methods</B>: The dogs were randomly selected samples were taken from 197 blood samples, these were stored until the completion of the polymerase chain reaction (PCR).					<B>Results</B>: The epidemiology results of [e]lt;i[e]rt;E. canis[e]lt;/i[e]rt; was 6.09% (12/197) and A. platys of 5.58% (11/197).					<B>Conclusion</B>: With the completion of the work, the results showed that there is presence of infection of bacterial agent in dogs studied. The PCR technique has a great importance for the diagnosis of ehrlichiosis and anaplasmosis, since the animals may have subclinical disease where this presents a risk to other dogs.				</P>
