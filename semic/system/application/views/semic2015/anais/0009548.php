			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Buscamos entender como foi construído o pensamento de Walter Benjamin (1892-1940) em relação aos conceitos de direito e poder, justiça e violência e sobretudo no que diz respeito a noção de "mera-vida".					<B>Objetivo</B>: Analisar a ideia de mera-vida na crítica à violência nos escritos de Walter Benjamin.					<B>Metodologia</B>: Utilizamos a pesquisa bibliográfica de textos tanto do próprio pensador quanto dos pesquisadores e comentadores de sua obra. Nossas leitura consistiram em interpretação crítica de conceitos ora se valendo de métodos comparativos ora se valendo de métodos analíticos.					<B>Resultados</B>: Para realizar a pesquisa tomamos como principal referência o ensaio "Crítica da violência e do poder" ("Kritik der Gewalt" em alemão), elaborado por Benjamin em 1921 e que tem, ao longo dos anos e sobretudo na atualidade, provocou amplas discussões e influenciou trabalhos de autores como Hannah Arendt, Jacques Derrida,  Slavoj Zizek e Giorgio Agamben.					<B>Conclusões</B>: A tradução do termo alemão "Gewalt" parece ilustrar o problema apontado por Benjamin em relação a mera-vida. "Gewalt" pode significar tanto poder (de direito ou do Estado) quanto violência. Assim, o autor destaca a ambivalência das relações de poder/violência sobre a vida natural, a mera-vida, e abre a perspectiva da discussão política sobre o tema na esfera atual da chamada biopolítica.				</P>
			</div><B>Palavras-chave</B>: Violência poder mera-vida filosofia Walter Benjamin			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: We try to understand how it was built the thought of Walter Benjamin (1892-1940) in relation to the concepts of right and power, justice and violence, and particularly in respect the notion of "simply-life".					<B>Objectives</B>: To analyze the idea of simply-life in the critique of violence in the writings of Walter Benjamin					<B>Methods</B>: We use the bibliographical research with texts from both the thinker himself as researchers and commentators of his work. Our readings consisted of critical interpretation of concepts sometimes making use of comparative methods sometimes making use of analytical methods					<B>Results</B>: To conduct the research take as main reference the essay "Critique of violence and of power" ("Kritik der Gewalt" in German), written by Benjamin in 1921 and has, over the years and especially today, caused extensive discussions and has influenced works of authors such as Hannah Arendt, Jacques Derrida, Slavoj Zizek and Giorgio Agambem.					<B>Conclusion</B>: The translation of the german term "Gewalt" seems to illustrate the problem pointed out by Benjamin relative to simply-life. "Gewalt" can mean power (form the legal or from the State) and can mean violence. Thus, the author emphasizes the ambivalence of the relations of power/violence on the natural life, the simply-life, and opens the perspective of political discussion on the subject in the current sphere of biopolitics .				</P>
			</div><B>Keywords</B>: Violence power simply-life philosophy Walter Benjamin</td>
