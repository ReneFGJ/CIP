			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: As substâncias químicas produzidas por plantas, quando liberadas no meio ambiente, podem prejudicar ou beneficiar o desenvolvimento de outros organismos, esse modo de ação denomina-se alelopatia.					<B>Objetivo</B>: O presente trabalho objetivou avaliar a influência do extrato aquoso de plantas sobre a germinação e desenvolvimento inicial do trigo.					<B>Metodologia</B>: Para avaliação do efeito alelopático foram feitos extratos plantas daninhas o capim amargoso Digitaria insularis, e a buva Conyza bonariensis, para o extrato aquoso de adubo verde utilizou-se nabo forrageiro Raphanus sativuse capim barachiaria, Brachiaria ruziziensis. O trabalho foi realizado em duas etapas, sendo a primeira no Laboratório de Fitopatologia e, a segunda, em casa de vegetação da Pontifícia Universidade Católica do Paraná, campus Toledo. O delineamento experimental em ambos os ensaios foram inteiramente casualizado com 5 tratamentos. Os dados obtidos foram submetidos à análise de variância pelo teste de Tukey ao nível de 5%.					<B>Resultados</B>: Observou-se que os diferentes estratos aquosos testados não interferiram nas variáveis analisadas da cultura do trigo, sendo, portanto possível utilizar esses adubos verde com intuito de realizar a rotação de cultura para posteriormente ser semeada a cultura do trigo, sem haver uma interferência alelopática.					<B>Conclusões</B>: 				</P>
			</div><B>Palavras-chave</B>: alelopatia, adubo verde, Triticum aestivum			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Chemicals produced by plants when released in the environment can harm or benefit the development of other organisms, this mode of action is called allelopathy.					<B>Objectives</B>: This study aimed to evaluate the effect of aqueous extract of plants on the germination and early growth of wheat.					<B>Methods</B>: To evaluate the allelopathic effect were made weed extracts the bitter grass Digitaria insularis, and hairy fleabane Conyza bonariensis, for the aqueous extract of green manure was used radish Raphanus sativuse barachiaria grass, Brachiaria ruziziensis. The study was conducted in two stages, the first being in Phytopathology Laboratory and the second, in a greenhouse at the Pontifical Catholic University of Parana, Toledo campus. The experimental design in both trials were randomized with 5 treatments. The data were submitted to analysis of variance by Tukey test at 5%.					<B>Results</B>: It was observed that the different strata aqueous tested did not interfere in the variables of the wheat crop, it is therefore possible to use these green fertilizers aiming to carry out crop rotation later to be sown to wheat crop, without an allelopathic interference.					<B>Conclusion</B>: 				</P>
			</div><B>Keywords</B>: allelopathy, green manure, Triticum aestivum</td>
