			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Entender o processo de tomada de decisão dos magistrados brasileiros é condição necessária para que se possa pensar em ferramentas para tornar o processo decisório cada vez mais legítimo e independente, e desta forma colaborar para a efetividade da prestação jurisdicional no Brasil. Este é o objetivo maior do projeto intitulado “Limites e Possibilidades da Eficácia da Prestação Jurisdicional no Brasil”, financiado pelo Conselho Nacional de Justiça (CNJ), ao qual esta pesquisa está vinculada					<B>Objetivo</B>: A investigação objetivou identificar e examinar os modelos atitudinal, legal e estratégico de tomada de decisão que descrevem o comportamento de juízes norte-americanos, especialmente os “justices” da Suprema Corte norte-americana, e os analisar tomando em conta o Supremo Tribunal Federal e a realidade brasileira.					<B>Metodologia</B>: Tomando por base o constitucionalismo contemporâneo e, neste, o fenômeno do ativismo jurídico, é feito um estudo bibliográfico dos modelos comportamentais de tomada de decisão descritos por doutrinadores norte-americanos e busca-se entender em que medida e sob que condições estes modelos tendem a se reproduzir no Brasil.					<B>Resultados</B>: O estudo revelou a inexistência de um método perfeito a ser adotado nas tomadas de decisões. Cada um deles apresenta aspectos positivos e negativos, que quando adotados pelo órgão responsável por proferir uma decisão acabam permitindo que decisões com mesmo embasamento jurídico possam apresentar resultados diferentes aos litigantes.					<B>Conclusões</B>: A ausência de um modelo de tomada de decisão judicial perfeito conduz à conclusão de que o modelo estratégico, combinado com o legal, parece ser aqueles que melhor se ajusta ao processo decisório levado a cabo pelos Ministros do Supremo Tribunal Federal.				</P>
			</div><B>Palavras-chave</B>: Modelo de decisão judicial. Modelo Atitudinal. Modelo Legal. Modelo estratégico.  Supremo Tribunal Federal.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Understand the decision-making process of the Brazilian judiciary is a necessary condition so that you can think of tools to make the decision-making process increasingly legitimate and independent and thereby contribute to the effectiveness of judicial services in Brazil. This is the greater objective of the project entitled “Limites e Possibilidade da Eficácia  da Prestação Jurisdiconal no Brazil"  (Limits and Possibilities of effectiveness of adjudication in Brazil” funded by the National Council of Justice (CNJ), to which this research is linked.					<B>Objectives</B>: The research aimed to identify and examine the attitudinal, legal and strategic decision-making models that describe the behavior of American judges, especially Justices of the Supreme Court of United States, and analyzes them taking into account the Brazilian Supreme Court and the Brazilian reality					<B>Methods</B>: Based on the contemporary constitutionalism and on the legal activism, it is made a study of the behavioral models of decision-making process described by American scholars and seeks to understand to what extent and under what conditions these models tend to be reproduced in Brazil.					<B>Results</B>: The study revealed the lack of a perfect method to be adopted in decision-making. Each has strengths and weaknesses, which allow that similar cases may produce different results.					<B>Conclusion</B>: The absence of a perfect judicial decision-making model leads to the conclusion that the strategic model, combined with the legal one, seems to be those that best fits the decision-making process carried out by the Justices of the Federal Supreme Court.				</P>
			</div><B>Keywords</B>: Judicial decision-making models. Attitudinal Model. Legal Model. Estrategical Model. Federal Supreme Court.</td>
