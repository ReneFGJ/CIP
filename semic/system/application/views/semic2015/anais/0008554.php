			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O estudo focaliza os saberes elementares matemáticos da escola primária no período compreendido entre 1953 e 1971, ´anos em que importantes transformações ocorrem no  ensino da matemática  escolar.					<B>Objetivo</B>: O objetivo do estudo foi investigar como as “modernas” propostas para o ensino da
Matemática impactaram os saberes elementares da Escola Primaria no estado do
Paraná, no período de 1953 a 1971.					<B>Metodologia</B>: No primeiro momento, foram inventariados documentos
localizados no Instituto de Educação do Paraná Erasmo Pilotto onde se obteve 55
números da Revista do Ensino, uma publicação do Rio Grande do Sul que teve forte
circulação no estado do Paraná. Em sequência, as fontes foram digitalizadas tendo
em vista a socialização do material a outros pesquisadores. O material obtido
possibilitou a produção de um artigo para o XII Seminário Temático, organizado pelo
Grupo de Pesquisa de História da Educação Matemática no Brasil, do qual teve o
intuito de discutir propostas modernas de ensino de Matemática no período
delimitado.					<B>Resultados</B>: Os artigos referentes aos saberes elementares matemáticos , selecionados nas revistas, apresentam orientações e modelos de atividades matemáticas apropriadas para um ensino ativo e uma escola ativa que valoriza a participação do aluno, além de exercícios que envolvem simbologia da matemática moderna.					<B>Conclusões</B>: A análise das fontes inventariadas mostra vestígios da introdução da
teoria dos conjuntos no ensino primário do estado, especialmente nos anos 1960 e
de forma mais efetiva nos anos 1970, modernidade trazida pelo Movimento da
Matemática Moderna, reforma que teve grande repercussão na matemática escolar
dos anos iniciais no Brasil e que no estado do Paraná foi intensamente difundida
pelo NEDEM- Núcleo de Estudos e Difusão do Ensino da Matemática coordenado				</P>
			</div><B>Palavras-chave</B>: Ensino Primário. Educação Matemática. Movimento da Matemática Moderna.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The study´s main focus are the elementary mathematics knowledge of the primary school in the period between 1953 and 1971, years that important changes occurred in the scholar education of mathematics					<B>Objectives</B>: The aim of this study is to investigate how the "modern" proposals for the teaching of mathematics impacted the elementary knowledge of Primary School in the state of Paraná, in the period from 1953 to 1971.					<B>Methods</B>: At first, were inventoried documents located in the Paraná Institute of Education Erasmo Pilotto where was gotten 55 numbers of the Revista da Educação, a publication of Rio Grande do Sul that had strong circulation in the state of Paraná. In sequence, the sources were scanned with a view to socialization of the material to other researchers. The material obtained has enabled the production of an article for the XII Seminário Temático organized by the Mathematics Education History Research Group in Brazil (GHEMAT), which aimed to discuss proposals of the modern mathematics teaching in the delimited period.					<B>Results</B>: The analysis of the inventoried sources shows traces of introduction of set theory in primary education in the state, especially in the 1960s and more effectively in the 1970s, modernity brought by the Modern Mathematics Movement, reform that had great repercussion in school mathematics the years early in Brazil and in the state of Paraná was intensely disseminated by NEDEM- Center for Studies and Mathematics Education Broadcasting coordinated by Professor Antonio Osny Dacol, then Director of the State of Paraná School.					<B>Conclusion</B>: The articles related to elementary mathematics knowledge, chosen in the magazine, shows orientations and models of mathematics activities appropriate for an active teaching and an active school who valorizes the student’s participation, besides exercises with modern mathematical symbology.				</P>
			</div><B>Keywords</B>: Primary Education. Mathematics Education. Modern Mathematics Movement.</td>
