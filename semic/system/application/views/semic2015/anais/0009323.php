			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A identificação de propriedade de materiais se faz cada vez mais necessário quando se deseja modelar o seu comportamento quando submetido a condições físicas distintas. Para a identificação das propriedades radiativas de matérias é comum se fazer uso de um equipamento denominado espectrômetro Infravermelho (FTIR) que permite a realização de medições com um nível satisfatório de sinal e em tempos relativamente rápidos de ensaio, quando comparado a outras técnicas.					<B>Objetivo</B>: O objetivo deste trabalho é ter-se uma rotina de identificação de propriedades radiativas a partir de medições obtidas em um FTIR.					<B>Metodologia</B>: Para isto fez-se o estudo de uma rotina de identificação que utiliza o método de minimização de Gauss fornecendo os valores de espessura ótica e de albedo para cada comprimento de onda.					<B>Resultados</B>: Foram utilizadas medições de transmitância e refletância disponíveis para o Acetato de Celulose que é um material do tipo nanocelular e obteve-se convergência para os valores de espessura ótica e albedo.					<B>Conclusões</B>: O método, apesar de ser relativamente lento em função de inúmeras iterações necessárias para a convergência apresentou repetibilidade para as amostras ensaiadas.				</P>
			</div><B>Palavras-chave</B>: Identificação de propriedades. Radiação. Método de Gauss			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Nowadays the material property identification becomes necessary to model their behaviour when subjected to different physical conditions. To identify the radiative properties of materials it is common to make use of a device called Fast Transform Infrared spectrometer (FTIR), which allows realizing measurements with a satisfactory signal level and relatively fast, compared to other techniques.					<B>Objectives</B>: The objective is to preparer a radiative property identification routine to use from measurements obtained on a FTIR spectrometer apparatus.					<B>Methods</B>: To this, it was made the study of an identification routine that uses the Gaussian minimization method providing the optical thickness and albedo for each wavelength.					<B>Results</B>: Transmittance and reflectance measurements were used to available cellulose acetate, which is a nanocelular material and it had been obtained convergence to the optical thickness and albedo values. An error analysis using six measurements is also presented.					<B>Conclusion</B>: This method, despite being relatively slow due to numerous iterations required for convergence, presented repeatability to this presented analysis.				</P>
			</div><B>Keywords</B>: Property identification. Radiation. Gauss method</td>
