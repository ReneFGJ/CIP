			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The animal is considered obese when presenting more than 30% body fat, in most cases, obesity is related to low proportion of energy expenditure with high consumption of food. From the time the animal has an excessive accumulation of fat, there are individual´s body adaptations to meet the needs, cardiovascular changes may cause risk of heart failure and sudden death of the animal in response arrhythmias. Electrocardiography is a complementary test used to detect heart rhythm disturbances.					<B>Objectives</B>: This study aimed to evaluate the electrocardiogram in obese dogs, carrying out the measurement of the electrocardiographic parameters, as well as the pace, heart rate and the presence of arrhythmias in these patients.					<B>Methods</B>: Were used in the evaluations electrocardiographic 10 obese and 10 lean dogs dogs age, sex and different races, from the Pontifical Catholic University of Paraná.					<B>Results</B>: There was no significant difference between the electrocardiographic parameters evaluated among obese and normal dogs.					<B>Conclusion</B>: In this study there were no significant changes in the findings of electrocardiographic tests, with the need for more research on the topic discussed.				</P>
