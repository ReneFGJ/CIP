			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O animal é considerado obeso quando apresenta mais de 30% de gordura corporal, na maioria dos casos a obesidade está relacionada a baixa proporção de gasto de energia com alto consumo de alimento. A partir do momento em que o animal tem um acúmulo excessivo de gordura, ocorrem adaptações do corpo do indivíduo para suprir as necessidades, as alterações cardiovasculares podem causar riscos de falência cardíaca e morte súbita do animal como resposta as arritmias. A eletrocardiografia é um exame complementar utilizado para detectar distúrbios do ritmo cardíaco.					<B>Objetivo</B>: O presente estudo teve o objetivo de avaliar o eletrocardiograma em cães obesos, realizando-se a mensuração dos parâmetros eletrocardiográficos, bem como do ritmo, frequência cardíaca e a presença de arritmias nesses pacientes.					<B>Metodologia</B>: Foram utilizados nas avaliações eletrocardiográficas 10 cães obesos e 10 cães magros de idade, sexo e raças variados, provenientes da Pontifícia Universidade Católica do Paraná.					<B>Resultados</B>: O ritmo mais comumente observado em todos os cães do estudo foi a arritmia sinusal com marcapasso migratório e nenhum dos casos apresentou arritmia atrial ou ventricular. Os resultados das frequências cardíacas nos animais estão dentro do parâmetro normal, não havendo diferença na frequência cardíaca entre cães obesos e cães com peso normal. A análise dos complexos e intervalos não mostrou diferença significativa entre cães obesos em relação aos normais.					<B>Conclusões</B>: No presente estudo não houveram alterações significativas nos achados dos exames eletrocardiográficos, havendo a necessidade de mais pesquisas sobre o tema abordado.				</P>
			</div><B>Palavras-chave</B>: Obesidade. cães. arritmia			<BR>
