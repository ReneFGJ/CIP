			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O presente trabalho de pesquisa verifica se a convergência midiática e a migração dos telejornais para a web modificaram, de alguma forma, o modo como a linguagem é constituída e aplicada neste meio. As constantes mudanças tecnológicas e a popularização da internet têm alterado diferentes aspectos da vida social, incluindo a comunicação. No jornalismo, as plataformas buscam diversas maneiras para adaptarem-se as novas demandas. No entanto, algumas delas ainda encontram entraves nesse processo, como é o caso da televisão.					<B>Objetivo</B>: Sendo assim, esta pesquisa busca verificar se a narrativa audiovisual sofreu alguma modificação ao migrar dos telejornais para a web, ou se ainda segue os antigos padrões dos meios televisivos.					<B>Metodologia</B>: Para que isso se tornasse viável, além de um estudo aprofundado sobre o panorama do ciberjornalismo nos dias de hoje, foi realizada a decupagem de  cinco horas diárias de produção do periódico online lusitano “Tv Algarve – Uma Região... Com vida!”, durante cinco dias.					<B>Resultados</B>: A partir desta decupagem, foi feita uma análise com base em critérios propostos por Palacios (1999), para denominar webjornalismo, na qual se verificou que o webtelejornalismo, feito hoje, apresenta poucas características específicas de seu meio.					<B>Conclusões</B>: Deste modo, tornou-se possível comprovar a hipótese de que, embora o webtelejornalismo exija uma narrativa mais imersiva e interativa, a linguagem utilizada nas produções feitas para a web ainda apresentam características que são próprias do telejornalismo tradicional. Pode-se notar também, que em muitos casos, as webtv’s realizam apenas transposições dos conteúdos transmitidos na televisão, para o meio digital, dando continuidade as formas de narrativas tradicionais.				</P>
			</div><B>Palavras-chave</B>: Ciberjornalismo. webtv. narrativas. convergência			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: This research work verifies that media convergence, and the migration of newscasts to the web have changed, in some way, the way in which language is constituted and applied in this medium.  The constant changes in technology and the popularization of internet has changed various aspects of social life, including communication. In journalism, the platforms seek different ways to adapt to the new demands. However, some of them are still in the process barriers, such as television.					<B>Objectives</B>: Therefore, this research aims to verify whether the audiovisual narrative has undergone some modification to migrate from TV news to the web, or still follows the old standards of television media.					<B>Methods</B>: For this to become viable, in addition to a thorough study of the landscape of online journalism these days, was carried out decoupage for five hours a day of production of the online journal Portuguese "TV Algarve – Uma região... Com vida!” for five days.					<B>Results</B>: From this decoupage, an analysis was made based on criteria proposed by Palacios, to name web journalism, in which it was found that the telejournalism for web made today, has few specific characteristics of their environment.					<B>Conclusion</B>: Thus, it has become possible to prove the hypothesis that although the telejournalism for web requires a more immersive and interactive narrative, the language used in productions made for the web still have characteristics that are unique to the traditional television journalism. It may be noted also that in many cases, the webtv´s realize just transpositions of what is reported on television, to digital, continuing forms of traditional narratives.				</P>
			</div><B>Keywords</B>: Ciber journalism. webtv. narratives. convergence</td>
