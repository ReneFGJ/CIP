			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: In contemporary society, the demand for learning a second language has increased considerably. So many schools have been investing in bilingual education. As a new segment, there rises the question whether teachers are prepared for this new teaching model.					<B>Objectives</B>: This research aims to investigate whether teacher training is aligned with the needs of such teaching.					<B>Methods</B>: A field research involving teachers of elementary schools offering bilingual education in Curitiba – Portuguese and English - was performed. To this end, questionnaires were used to collect data.					<B>Results</B>: The analysis of such data has revealed that teachers consider important continuing education for a good teaching performance in bilingual schools and they take courses in this area in the schools where they work or abroad. Also, one third of the teachers have a major in another area and are working in bilingual schools temporarily. It was also observed that most of the schools investigated follow the standards required for monolingual education and, in spite of advertising bilingual educational, they offer a rather low number of classes and/or their extracurricular activities in English.					<B>Conclusion</B>: 				</P>
