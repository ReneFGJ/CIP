			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Na sociedade contemporânea, as exigências para aprender uma segunda língua tem aumentado consideravelmente. Por isso, diversas instituições de ensino investiram na oferta de educação bilíngue. Como um novo segmento, é coerente questionar se os professores estão preparados para este novo modelo de ensino.					<B>Objetivo</B>: Assim, esta pesquisa visa investigar se a formação dos professores está de acordo com as necessidades desse tipo de ensino.					<B>Metodologia</B>: Foi realizada uma pesquisa de campo envolvendo profissionais docentes do Ensino Fundamental I de instituições que oferecem ensino bilíngue em Curitiba – português e inglês. Para isso, foram utilizados questionários para coletar dados que possibilitassem a análise e considerações a respeito do tema pesquisado.					<B>Resultados</B>: Esses dados revelaram que os professores consideram a formação continuada muito importante para o bom desempenho em escolas bilíngues e eles frequentam os cursos oferecidos nas próprias instituições onde trabalham ou no exterior. A pesquisa também revelou que um terço dos professores são graduados em outra área e trabalham em escola bilíngue temporariamente. Além disso, observou-se que as instituições de ensino bilíngue pesquisadas, em sua maioria, seguem os padrões exigidos pelo ensino regular. No entanto, por serem instituições de ensino bilíngues, percebeu-se que oferecem pouca carga horária semanal para aulas de língua inglesa, bem como para atividades extracurriculares em língua inglesa.					<B>Conclusões</B>: 				</P>
