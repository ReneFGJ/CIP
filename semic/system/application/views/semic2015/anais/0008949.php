			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: In Brazil is collected daily between 180 and 250 thousand tons of solid waste. It seems insignificant but "butt" cigarette residue is the most common waste on the planet and is present in all public places.					<B>Objectives</B>: The aim was to develop techniques for reusing components of the "butt" of cigarette and hydro-seeding in the recovery of degraded areas and slopes in addition to produce and study the feasibility of a domestic pesticide for plants.					<B>Methods</B>: From the butts and after composting nine treatments were prepared in different proportions of fertilizer and soil and then placed in plastic containers and held sowing mimosa lettuce. After 45 days were evaluated biometric variables of the plant. For hydro-seeding, added "butt" of cigarettes, water, chemical fertilizers and B. brizantha seeds and grass Tanzania. Seeds were sown on polyethylene tray containing soil type substrate being kept in a greenhouse with room temperature. An affected area by erosion for application of the product was chosen. The pesticide preparation used up 80 ´butts "cigarette, water, detergent and alcohol.					<B>Results</B>: After analyzing the results, it was found that the fertilizer made exhibit efficacy on plant development, hydro-seeding is an alternative to reuse of "butts" cigarettes, it helps to fix the seed in high slope locations and processes erosive and it was also concluded that the cellulose is the residue present after more cigarette consumption.					<B>Conclusion</B>: 				</P>
