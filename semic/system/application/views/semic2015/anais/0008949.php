			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Diariamente, são coletadas no Brasil entre 180 e 250 mil toneladas de resíduos sólidos urbanos. Parece insignificante, mas a “bituca”, resíduo do cigarro, é o lixo mais comum no planeta, e está presente em todos os lugares públicos.					<B>Objetivo</B>: O objetivo do trabalho foi o desenvolvimento de técnicas de reutilização dos componentes da “bituca” de cigarro e de hidro-semeadura na recuperação de encostas e áreas degradadas, além de produzir e estudar a viabilidade de um pesticida doméstico para as plantas.					<B>Metodologia</B>: A partir das bitucas e, após compostagem, foram elaborados nove tratamentos em diferentes proporções de adubo e solo e em seguida, colocados em recipientes plásticos e realizada a semeadura de alface mimosa. Após 45 dias foram avaliadas as variáveis biométricas da planta. Para a hidro-semeadura, adicionou-se “bituca” de cigarro, água, adubo químico e sementes de Brachiaria brizantha e Capim Tanzânia. A semeadura foi realizada sobre bandeja de polietileno contendo substrato do tipo solo sendo mantidas em casa de vegetação com temperatura ambiente. Após, foi escolhida uma área afetada pela erosão para aplicação do produto. Para a preparação do pesticida utilizaram-se 80 “bitucas” de cigarro, água, detergente e álcool.					<B>Resultados</B>: Após análise dos resultados, verificou-se que o adubo confeccionado apresentou eficácia no desenvolvimento das plantas, a hidro-semeadura é uma alternativa para a reutilização das “bitucas” de cigarros, pois ajuda a fixar a sementes em locais de alta declividade e com processos erosivos e também foi possível concluir que a celulose é o resíduo com maior presença após o consumo do cigarro.					<B>Conclusões</B>: 				</P>
			</div><B>Palavras-chave</B>: Bituca de cigarro. hidro-semeadura. pesticida			<BR>
