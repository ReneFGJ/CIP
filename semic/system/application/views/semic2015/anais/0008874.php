			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A responsabilidade judicial – social e política – tem como pano de fundo o ativismo judicial e a atuação política do Judiciário no Estado contemporâneo, especialmente nas democracias ocidentais e, entre elas, o Brasil. Quando abordado este tema, majoritariamente entende-se o comportamento do Poder Judiciário como ativista quando, de forma deliberada, o juíz escolheria desbordar do campo de atuação da tecnicidade jurídica para adentrar outras esferas. O ativismo seria, portanto, uma consequência da judicialização da política.					<B>Objetivo</B>: Investigar a judicialziação da política e o ativismo como consequência de fatores culturais e socio-antropológicos do cidadão brasileiro e indicar suas consequências para o estudo da responsabilidade judicial do magistrado.					<B>Metodologia</B>: A pesquisa baseou-se em estudo bibliográfico, composto de análises teóricas da estrutura do Poder Judiciário como instituição, explanações doutrinárias sobre os conceitos de ativismo judicial, judicialização da vida e da política, protagonismo do Poder Judiciário e outros, assim como obras de aporte filosófico, cultural e sócio antropológico quando da responsabilidade do cidadão. Utilizou o método dedutivo e uma abordagem preponderantemente qualitativa.					<B>Resultados</B>: Denotou-se que ocorre uma transferência de responsabilidades do cidadão para o Poder Judiciário ao estilo contemplado pelo conceito da má-fé de Sartre, onde o cidadão busca libertar-se de sua própria liberdade de tomar decisões, entre outras conclusões. Como manifestação jurídica deste fenômeno, se observou que o princípio constitucional da obrigatoriedade da prestação jurisdicional (vedação do non liquet) torna o juiz refém das demandas propostas pelo particular, onde a responsabilidade caso a decisão culmine em exceder o plano jurídico ou interferir na esfera de atuação de outros poderes não pode ser compreendida como intencional por parte do magistrado.					<B>Conclusões</B>: Mostrou-se necessário apresentar a compreensão do fenômeno da judicialização da política como sendo de responsabilidade da histórica postura cultural do cidadão brasileiro na prática de transferência de suas responsabilidades para o Poder Judiciário.				</P>
