					<B>Introdução</B>: A Qualidade de vida no trabalho, atualmente, pode ser definida como uma construção multidimensional que se refere à satisfação do sujeito relacionada à busca do equilíbrio entre a vida pessoal, profissional, sentimento de pertencer ao grupo de trabalho, a sensação de ser valorizado e respeitado. Podendo ser incluído, o cuidado do bem estar e do ambiente como fundamental para garantir uma melhor produtividade e qualidade no trabalho.					<B>Objetivo</B>: Levantar o escore da Qualidade de Vida dos trabalhadores de um restaurante de Curitiba, utilizando o inventário TQWL-42 (2009), com proposta tipo Likert e identificar os escores obtidos de QVT  correlacionando com as funções dos trabalhadores do restaurante pesquisado.					<B>Metodologia</B>: Os dados foram coletados individualmente no ambiente de trabalho e em três locais: escritório, filial e matriz. O inventario era entregue ao sujeito após o pesquisador ter explicado o objetivo da pesquisa e o colaborador que aceitava participar assinava o TCLE , assim sendo, o instrumento foi aplicado durante o horário de trabalho dos colaboradores e levou em média de 30 a 40 minutos a resposta.					<B>Resultados</B>: 					<B>Conclusões</B>: 				</P>
