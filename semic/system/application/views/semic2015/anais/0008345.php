			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The tobacco, or smoking as it is popularly known is produced in different regions of Brazil, it serves as an important income generator for small and medium producers ..					<B>Objectives</B>: The objective of this study was to evaluate the efficiency of different substrates in the production of tobacco seedlings and disease in seedlings .					<B>Methods</B>: he experiment consisted of five treatments and four repetitions . The treatments coconut fiber , pine bark , Plantmax , substrate for orchids and substrate for flowers, and the second by four replications. Tobacco , Nicotiana tabacum , was sown in plastic trays of 200 cells after 25 days and transferred to such treatments. They evaluated the number of leaves, plant height , the amount of fresh and dry matter , and to evaluate the amount and intensity of diseases that affect the production. The analysis was done by SISVAR statistical analysis program , and compared by Tukey test at 5 % probability.					<B>Results</B>: By analyzing all the variables, the number of leaves, fresh matter and due to the lower number of diseases coconut substrate showed the best results . In plant height the substrate which achieved better results was the orchids in dry matter no significant difference					<B>Conclusion</B>: So there is a good choice of substrate must also observe its cost , due to this we recommend the coconut fiber substrate .				</P>
