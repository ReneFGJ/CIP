			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O tabaco, ou fumo como é popularmente conhecido é produzido em diferente regiões brasileiras, serve como importante gerador de renda para pequenos e médios produtores.					<B>Objetivo</B>: O objetivo do presente trabalho foi avaliar a eficiência de diferentes substratos na produção de mudas de fumo e incidência de doenças nas plântulas.					<B>Metodologia</B>: O delineamento experimental foi constituído de 5 tratamentos e 4 repetições. Sendo os tratamentos fibra de coco, casca de pinus, Plantmax, substrato para orquídeas e substrato para flores, e o segundo pelas quatro repetições. O tabaco, Nicotiana tabacum, foi semeado em bandejas de polietileno de 200 células e após 25 dias transferido para os referidos tratamentos. Foram avaliados o número de folhas, a altura das plantas, a quantidade de matéria fresca e seca, além de avaliar a quantidade e intensidade das doenças que incidem na produção. A análise foi feita pelo programa de análise estatística SISVAR, e comparadas pelo teste de Tukey com 5% de probabilidade.					<B>Resultados</B>: Ao analisar todas as variáveis, o número de folhas, a matéria fresca e por apresentar o menor número de doenças o substrato de coco foi o que apresentou os melhores resultados. Na altura das plantas o substrato que obteve melhores resultados foi o de orquídeas, na matéria seca não houve diferença significativa.					<B>Conclusões</B>: Para que haja uma boa escolha do substrato deve-se também observar o seu custo, devido a isso recomenda-se o substrato de fibra de coco.				</P>
			</div><B>Palavras-chave</B>: Substratos. tabaco. doenças			<BR>
