			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Com a necessidade de entender os processos térmicos e de escoamento de fluido que ocorrem em um motor de ignição por centelha com injeção direta foram adotados os métodos de simulação computacional CFD (Computacional Fluid Dynamics) para a avaliação de propriedades relacionadas a esse processo como pressão, velocidade do fluido e temperatura.					<B>Objetivo</B>: O trabalho tem por objetivo simular os processos térmicos e de escoamento dos fluidos em um motor de combustão interna utilizando um código computacional livre.					<B>Metodologia</B>: O código computacional livre OpenFoam foi a ferramenta utilizada para o pré-processamento e processamento dos dados. O pós-processamento foi realizado no software Paraview.					<B>Resultados</B>: O objetivo foi concluído parcialmente. Não foi possível analisar completamente um ciclo que ocorre nos motores de combustão interna, com o movimento das válvulas de admissão e de escape, porém foi possível identificar movimentos como o do pistão, propriedades de velocidade e pressão que ocorrem dentro da câmara de combustão.					<B>Conclusões</B>: Dessa forma os resultados obtidos computacionalmente podem ser analisados e comparados com os já existentes extraídos em testes físicos podendo apontar quais modelos representam melhor os processos térmicos de um motor de combustão interna.				</P>
			</div><B>Palavras-chave</B>: Motor de combustão interna. dinâmica dos fluidos computacional			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: With the growing need to understand the thermal and fluid flow processes which occur in an spark ignition engine with direct injection, the methods of computer simulation CFD (Computacional Fluid Dynamics) were used, in order to assess the properties related to this process like pressure, speed and temperature of fluid.					<B>Objectives</B>: The study aims to simulate the thermal and flow of fluids in an internal combustion engine using a free computer code.					<B>Methods</B>: The free computer code Open Foam was the tool used to the preprocessing and data processing. The postprocessing was made with the Paraview software.					<B>Results</B>: The goal was partially completed. It was not possible to completely analyze a cycle that occurs in internal combustion engines, the movement of the intake and exhaust valves, though it was possible to identify movements as the piston speed properties and pressure occurring within the combustion chamber.					<B>Conclusion</B>: This way, the obtained results by computer processes can be analyzed and compared with the extracted in existing physical tests may point models which best represent the thermal processes of an internal combustion engine.				</P>
			</div><B>Keywords</B>: Internal combustion engine. computational fluid dynamics</td>
