			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A inovação empresarial vem cada vez mais ganhando espaço dentro das organizações, sendo tanto em forma de pesquisa, quanto em lançamento de novos produtos, serviços ou processos. Consta-se que sem a inovação não é possível que qualquer corporação tenha competitividade e consiga se sobressair perante seus concorrentes.					<B>Objetivo</B>: O trabalho possui dois objetivos, sendo estes: objetivo geral e objetivo específico. O objetivo geral é compreender quais são os fatores mais relevantes que levam a ocorrência de uma inovação e o específico se refere aos assuntos que devem ser abordados para que se chegue ao objetivo geral. Os assuntos em questão são as fases da inovação, seus diferentes tipos e classificações.					<B>Metodologia</B>: Para realizar esta pesquisa foi-se utilizado apenas de levantamento bibliográfico de artigos, livros e revistas.					<B>Resultados</B>: A partir deste projeto pode-se obter como resultado o fato de que as empresas tratam a inovação como aprendizado organizacional, para que a mesma possa ser replicada por outros setores e departamentos da empresa. Além disso, as organizações buscam criar uma cultura que possibilite que a inovação ocorra.					<B>Conclusões</B>: Através da análise de quatro casos de inovação em empresas brasileiras conclui-se que a maior parte destes é influenciada pela visão da alta administração e pelas condições organizacionais encontradas na organização.				</P>
			</div><B>Palavras-chave</B>: Inovação Empresarial. Empresas Brasileiras. Inovação			<BR>
