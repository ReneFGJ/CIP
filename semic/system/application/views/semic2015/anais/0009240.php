			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Business innovation is increasingly gaining ground inside of the organizations, as much as in the form of research, as in launching new products, services or processes. It appears that without innovation It’s not possible to any corporation to be competitive and able to stand out against their competitors.					<B>Objectives</B>: The work has two objectives, namely: general objective and specific objective. The general objective is to understand what are the most important factors that leads to the occurrence of an innovation and the specific refers to the issues that must be addressed in order to reach the general objective. The issues at stake are the stages of innovation, it’ different types and classifications.					<B>Methods</B>: To conduct this research has been used only a literature review of articles, books and magazines.					<B>Results</B>: From this project it can be obtained as a result the fact that companies treat innovation as organizational learning, so that it can be replicated in other sectors and departments. Beside that, organizations also seek to create a culture that allows that innovation occurs.					<B>Conclusion</B>: Beside the analysis of four cases of innovation in Brazilian companies it is concluded that most of these are influenced by the vision of the senior management and the organizational conditions found in the organization.				</P>
