			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Durante o preparo do canal radicular podem ocorrer iatrogênicas  como o transporte e perda do trajeto original do canal radicular podendo comprometer o resultado do tratamento.					<B>Objetivo</B>: O objetivo do presente estudo foi avaliar por meio de tomografia computadorizada cone bean (TCCB) o transporte (TRA) e a capacidade de centralização (CC) do terço apical de canais curvos após o emprego de diferentes sistemas automatizados.					<B>Metodologia</B>: Foram utilizados as raízes mesiais de vinte e quatro primeiros molares inferiores (48 canais) que foram distribuídos em um grupo controle (n=3) e três grupos experimentais (n=15) de acordo com o sistema utilizado para a modelagem do canal radicular: reciprocante com instrumentos ProTaper F2 (PTr), reciprocante com instrumentos Wave One (WO) e rotatório com instrumentos ProTaper Universal (PT). Imagens da TCCB pré e pós-instrumentação foram utilizadas para avaliar o transporte (TRA) e a capacidade de centralização (CC) pela análise linear voxel a voxel. Os dados foram submetidos à análise estatística  por meio do teste do qui-quadrado de Pearson e o teste de diferença entre duas proporções com nível de significância de 5%.					<B>Resultados</B>: Quanto ao TRA e CC os grupos WO e PTr não apresentaram diferença entre si, porém apresentaram diferença estatisticamente significante (p[e]lt;0,05) em relação ao grupo PT.					<B>Conclusões</B>: Conclui-se que, no terço apical da raiz mesial de primeiro molares inferiores, todas as técnicas utilizadas produziram transporte na região apical considerados como aceitável com desvios tendendo a ocorrer em direção ao lado externo da curvatura e que a cinemática reciprocante promoveu menor desvio e canais mais centralizados.				</P>
			</div><B>Palavras-chave</B>: Preparo do canal radicular. tomografia computadorizada cone beam. instrumentos rotatórios. instrumentos reciprocantes.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: During root canal preparation, apical transportation and failure of mantaining the original path of the root canal may occur, compromising the outcome of the treatment.					<B>Objectives</B>: The aim of this study was to access apical transportation (AP) and centering ability (CA) of the apical third in curved root canals instrumented with different mechanized systems, by means of cone-beam computed tomography (CBCT).					<B>Methods</B>: Mesial roots of twenty-four first mandibular molars with three canals each, were divided into three experimental groups (n=15) according to the system used for instrumentation of the root canals: GI - ProTaper F2 with reciprocating motion (PTr); GII - WaveOne with reciprocating motion (WO); GIII - ProTaper Universal (PT) with rotary motion; three canals were used as control. Pre and post - instrumentation CBCT images were obtained to evaluate apical transportation and centering ability by linear analysis voxel to voxel. Data were statistically analyzed using Pearson chi-square test with significance level of 5%.					<B>Results</B>: No significant differences were found in apical transportation and centering ability between WO and PTr, however, PT was statistically different (P [e]lt; .05) compared to WO and PTr.					<B>Conclusion</B>: In conclusion, all techniques produced apical transportation in some level and the deviation tended to occur towards the opposite side of the curvature. Instruments operated with reciprocating cinematic promoted less deviation remaining more centralized inside the root canal.				</P>
			</div><B>Keywords</B>: Root canal preparation. computerized cone beam tomography. rotary instruments. reciprocating instruments</td>
