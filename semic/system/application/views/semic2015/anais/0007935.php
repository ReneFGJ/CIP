			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O evangelista Lucas narrou uma passagem importante na vida de Jesus (Lc 7,36-50). Ao participar de uma refeição na casa de Simão, um fariseu, entrou uma mulher da cidade, chamada de pecadora. Chorando e com suas lágrimas lavou os pés de Jesus, enxugou-os com os seus cabelos e depois ungiu-os com perfume. Jesus a acolheu e a perdoou. Diante da indignação do fariseu, Jesus contou a parábola dos dois devedores perdoados, ensinando que quem demonstra muito amor, recebe mais perdão.					<B>Objetivo</B>: O objetivo desta pesquisa é estudar, analisar e interpretar a passagem bíblica de Lc 7,36-50 em que Jesus acolhe e perdoa uma mulher considerada pecadora. Entender o valor e eficácia do gesto praticado por Jesus para a sociedade da sua época e, ao mesmo tempo, atualizar esta mensagem para a realidade de hoje.					<B>Metodologia</B>: Para a realização desta pesquisa foi utilizada a metodologia de análise e interpretação de textos, privilegiando o método Histórico Crítico e os seus elementos essenciais, além do uso de outros métodos e abordagens para interpretação de textos bíblicos.					<B>Resultados</B>: Através da análise foi possível compreender a situação da mulher na época de Jesus e entender o alcance da atitude de Jesus em relação à mulher. Jesus não olhou para o pecado da mulher e nem se deteve ao rigor da Lei, mas valorizou o gesto de amor praticado pela mulher e a acolheu e a perdoou dos seus pecados.					<B>Conclusões</B>: O gesto de Jesus nos orienta a agirmos com misericórdia e compaixão diante das pessoas marginalizadas que sofrem discriminações e termos a mesma atitude de acolhida e perdão. É a prática do amor e do perdão que é capaz de integrar e redimir as pessoas que são excluídas na realidade de hoje.				</P>
			</div><B>Palavras-chave</B>: Mulher. Jesus. amor. perdão. Lucas.			<BR>
