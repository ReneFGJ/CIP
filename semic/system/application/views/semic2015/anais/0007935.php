			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The evangelist Luke narrated an important passage on Jesus’ life (Lk 7:36-50). Participating in a meal in Simon’s (a Pharisee) house, a woman from town who was labeled a sinner walked in. Weeping, with her tears she began to wet Jesus’ feet and with her own hair she dried them and anointed them with ointment. Jesus welcomed and forgave her. Upon the Pharisee’s indignation, Jesus told the parable of the two debtors, teaching that, he who demonstrates a great amount of love, receives more forgiveness.					<B>Objectives</B>: The main objective of this research is to study, analyse and interpret the following biblical passage Lk 7:36-50, in which Jesus welcomes and forgives a woman considered a sinner. To understand the value and efficiency that gesture practiced by Jesus had upon the society of His time and at the same time bring this message up-to-date for today’s reality.					<B>Methods</B>: In the preparation of this research the methodology of analysis and text interpretation was used favouring the critical-historical method and its essential elements moreover, more methods and approaches for biblical texts interpretations were used.					<B>Results</B>: Through the analysis it became possible to comprehend the situation of women during Jesus’s time, also to understand the grasp of His attitude in relation to women. Jesus did not look to the woman’s sin nor he attained himself to the rigidity of the law, nevertheless he prized the love gesture demonstrated by the woman and welcomed her and forgave her of all her sins.					<B>Conclusion</B>: Jesus’ gesture advocates for us to act mercifully and compassionately before people who are marginalized who suffer discrimination and for us to have the same attitude of welcoming and forgiveness. The practice of love and forgiveness is capable of integrate and redeem people who are excluded on today’s society.				</P>
			</div><B>Keywords</B>: Woman. Jesus. love. forgiveness. Luke.</td>
