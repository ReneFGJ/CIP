			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A insuficiência renal aguda (IRA) é considerada uma doença de pacientes hospitalizados muito comum em Unidades de Terapia Intensiva (UTI). Pode ser definida como perda da função renal, de maneira súbita, independentemente da etiologia ou mecanismos, provocando acúmulo de substâncias nitrogenadas (ureia e creatinina), acompanhada ou não da diminuição da diurese.					<B>Objetivo</B>: Avaliar a associação de distúrbios hemodinâmicos, o uso de medicamentos, idade,  uso de sonda vesical, ventilação mecânica, sepse e tempo de internação com a IRA.					<B>Metodologia</B>: Trata-se de um estudo retrospectivo realizado realizada por meio da analise de prontuários de 104 pacientes internados na UTI do Hospital Irmandade da Santa Casa de Londrina no ano de 2014.					<B>Resultados</B>: Como resultado, percebeu-se ausência de associação entre IRA e gênero, da mesma forma entre IRA e o uso de múltiplos medicamentos e de sonda vesical. Quanto ao tempo de internação, ao número de óbitos, idade, ventilação mecânica, sepse e distúrbios hemodinâmicos (hemorragias e choque), apresentaram associação com a IRA.					<B>Conclusões</B>: A insuficiência renal aguda é uma doença de incidência significativa em pacientes submetidos a cuidados intensivos e o controle dos fatores de risco, aspectos hemodinâmicos e uso de drogas nefrotóxicas mostra-se essencial para evitar a doença.				</P>
			</div><B>Palavras-chave</B>: Insuficiência renal aguda. Unidade de terapia intensiva. medicamentos			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The Acute Renal Failure can be defined as a sudden loss of renal function, regardless of its etiology or mechanisms, causing an accumulation of nitrogen based substances(urea and creatinine) and it can be accompanied by diuresis reduction.					<B>Objectives</B>: the objective of analyzing the hemodynamic aspects, the medicine intake and the risk factors that led the analyzed patients to develop Acute Renal Failure.					<B>Methods</B>: This prospective study, based on the analysis of medical records of 104 patients admitted at Irmandade Santa Casa de Londrina Hospital’s intensive care unit in the year of 2014					<B>Results</B>: As a result, no significant relationship was found between gender and Acute Renal Failure, as well as between the use of medication and the insertion of an urinary catheter. The literature thesis regarding death, age, mechanical ventilation, admission period, sepsis and hemodynamic aspects were confirmed.					<B>Conclusion</B>: The Acute Renal Failure is a disease, with significant incidence in patients submitted to intensive care and risk factor control, hemodynamic aspects and the use of nefrotoxic drugs showed to be essential to avoid the disease.				</P>
