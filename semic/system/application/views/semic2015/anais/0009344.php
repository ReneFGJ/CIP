			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Competitive karate is a sport that requires high speed demands in carrying out scams. A form of technical skill execution is an important factor in major speed gains on the realization of punches. Thus, understanding the speed of behavior in carrying out different technical skills can help improve sports performance.					<B>Objectives</B>: The objective of the study was to identify the influences of the punch speed of execution in three lower limb positioning strategies (MI).					<B>Methods</B>: We evaluated 15 athletes of the Brazilian national karate. The subjects performed 15 punches in three different bases of support of the feet, 5 punches fixed base (heels in contact with the ground), 5-based heel displacement (foot tip half) and 5 in free base (status fight). For the image capture was used a camera model Cássio EXILIM with adjustable rate 420Hz (frames per second).					<B>Results</B>: It was identified that the maximum speeds achieved in the situation C were higher than situations A and B (F = 31.4; df = 2; [e]#931;² = 408.4, p = 0.0001). Happen influential technical adjustments to the maximum speed of an anatomical point relative to each other, these technical adjustments identified emphasize the importance of the lower limbs to the standard engine and the ultimate speed of execution in karate punch.					<B>Conclusion</B>: We conclude that the strategy in free base, similar to the situation of struggle (situation C) showed higher rates of stroke [e]lt;i[e]rt;gyako zuki[e]lt;/i[e]rt;.				</P>
