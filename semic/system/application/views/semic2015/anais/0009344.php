			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O karate competitivo é uma modalidade esportiva que necessita de elevadas demandas de velocidade na realização de golpes. A forma de execução da habilidade técnica é importante fator em maiores ganhos de velocidade na realização de socos. Logo, a compreensão do comportamento de velocidade na realização de diferentes habilidades técnicas pode auxiliar na melhora do desempenho esportivo.					<B>Objetivo</B>: Assim, o objetivo do estudo foi identificar a influencia na velocidade de execução do soco em três estratégias de posicionamento do membro inferior (MI).					<B>Metodologia</B>: Foram avaliados 15 atletas da seleção brasileira de karate. Os sujeitos executaram 15 socos em três diferentes bases de apoios dos pés, sendo 5 socos em base fixa (calcanhares em contato com o solo), 5 em base com deslocamento do calcanhar (meia ponta de pé) e 5 em base livre (situação de luta). Para a captura de imagem foi utilizada uma câmera modelo Cássio EXILIM com taxa ajustável de 420Hz (quadros por segundo).					<B>Resultados</B>: Identificou-se que as velocidades máximas obtidas na situação C foram superiores às situações A e B (F=31,4; df=2; [e]#8721;²=408,4; p=0,0001). Acontecem adequações técnicas influenciadoras na velocidade máxima de um ponto anatômico em relação ao outro, estas adequações técnicas identificadas ressaltam a importância dos membros inferiores para o padrão motor e a velocidade final na execução no soco do karate.					<B>Conclusões</B>: Conclui-se que a estratégia em base livre, similar a situação de luta (situação C) apresentou maiores velocidades do golpe [e]lt;i[e]rt;gyako zuki[e]lt;/i[e]rt;.				</P>
			</div><B>Palavras-chave</B>: Artes marciais. Cinemática. Medição de velocidade.			<BR>
