			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O presente trabalho parte de duas concepções acerca do processo de identificação que pode ser compreendido a partir de dois autores, quais sejam: Chantal Mouffe e Jacques Rancière					<B>Objetivo</B>: desenvolver um encadeamento lógico a partir da identificação de três dimensões do sujeito no espaço político: o individual, o coletivo e a própria democracia, sem, todavia, propor um fechamento em torno da temática que envolve identidade e exclusão					<B>Metodologia</B>: o método é o analítico, que parte da análise interna das obras dos autores, proporcionando meios para o entendimento conceitual da lógica interna de seus pensamentos					<B>Resultados</B>: Ambos os autores possuem uma visão de democracia onde existe um dissenso que, também, pode ser entendido como conflito, embora discordem de alguns elementos. Dessa forma, as definições de política, político, polícia e democracia são colocadas como chaves de leitura para a compreensão de diversos fatores relativos a ordem social, em que suas combinações e encaixes, ou desencaixes, darão uma pista de como a sociedade democrática se estrutura e influencia a identidade política dos sujeitos.					<B>Conclusões</B>: 				</P>
			</div><B>Palavras-chave</B>: Identificação. Jacques Rancière. Chantal Mouffe. Exclusão. Identidade.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: This study is based on two conceptions of the identification process that can be understood from two authors, namely: Chantal Mouffe and Jacques Rancière					<B>Objectives</B>: To develop a logical sequence based on the identification of three dimensions of the subject in the political space: the individual, the collective and democracy itself, without, however, propose a lock around the theme involving identity and exclusion					<B>Methods</B>: the method is analytical, that part of the internal analysis of the works of authors, providing means for conceptual understanding of the internal logic of his thoughts					<B>Results</B>: Both authors have a vision of democracy where there is a dissent that, too, can be understood as a conflict, but disagree with some elements. Thus, the policy settings, political, police and democracy are placed as reading keys to understanding a number of factors relating to social order in which their combinations and fittings, or disembeddings, will give a clue as to how a democratic society is structured and influences the political identity of the subjects					<B>Conclusion</B>: 				</P>
