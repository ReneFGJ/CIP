			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A presente pesquisa de abordagem qualitativa do tipo bibliográfica sobre o uso da Arte Sequencial e suas múltiplas linguagens no ensino da Língua Portuguesa. Esta pesquisa contou com o aporte teórico sobre a linguagem das Histórias em Quadrinhos buscando seus benefícios para o processo de ensino-aprendizagem.					<B>Objetivo</B>: Em sua problematização apresenta o seguinte questionamento: como estão sendo explorada a linguagem da Arte Sequencial no âmbito escolar, quais os aspectos desta linguagem estão sendo utilizadas nas disciplinas de Língua Portuguesa pelos professores.					<B>Metodologia</B>: Por meio desta pesquisa foi possível observar que as Histórias em Quadrinhos são uma estratégia envolvente para obter bons resultados no Ensino da Língua Portugesa, visto como um recurso pedagógico, esta ferramenta pode contribuir para aquisição de novos conhecimentos e de incentivo a leitura.					<B>Resultados</B>: O estudo desenvolvido neste trabalho contou com a fundamentação teórica dos seguintes autores: Freire (2006); Ramos (2012); Eisner (1995);  Morin (2013) entre outros.					<B>Conclusões</B>: Foi percebido durante esta pesquisa que os próprios professores desconheciam o uso da linguagem dos quadrinhos. Muitos se quer a entendiam como uma linguagem, a utilização do termo “Arte Sequecial” fez com que muitos professores se atentassem, pois elevando-a ao status de “arte” ela poderia ser colocada em seus planejamentos sem maiores questionamentos e poderiam dar maior atenção a linguagem.				</P>
			</div><B>Palavras-chave</B>: Educação. Histórias em Quadrinhos.			<BR>
