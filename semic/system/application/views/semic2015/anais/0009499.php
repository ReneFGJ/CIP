			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A população vem crescendo em taxas altíssimas em todo o planeta. Este crescimento impacta diretamente o meio ambiente, visto que com o aumento da população, vem o grande impacto que esta pressão faz sobre as áreas naturais, tais como exploração das florestas, dos rios e da fauna.					<B>Objetivo</B>: Em vista de conhecer o atual estado de uso do solo, o presente trabalho mapeou o uso do solo do município de Fazenda Rio Grande, PR.					<B>Metodologia</B>: O mapeamento foi realizado pela técnica da digitalização em tela sobre imagens de satélite. Foram extraídas 22 imagens do programa Google Earth Pro. Estas imagens foram georreferenciadas e inseridas em um Sistema de Informações Geográficas. Posteriormente estas imagens foram interpretadas e digitalizadas					<B>Resultados</B>: resultando em 8 tipologias distintas com suas respectivas áreas. Área Urbana (2.736 ha), Agropecuária (3.532 ha), Floresta Ombrófila Mista Montana (2.427 ha), Floresta Ombrófila Mista Aluvial (1.517 ha), Formação Pioneira Flúvio-lacustre (várzeas) (801 ha), Corpos d´água (1233 ha), Reflorestamentos (177 ha) e Solo Exposto (217 ha).					<B>Conclusões</B>: O trabalho cumpriu seus objetivos e mapeou o Município de Fazenda Rio Grande em oito tipologias, onde destacaram-se principalmente dois usos, o agropecuário, com 31 % e as áreas recobertas por vegetação natural com 41%.				</P>
			</div><B>Palavras-chave</B>: Uso do Solo. Fazenda Rio Grande. PR. Floresta Ombrófila.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The population is growing at very high rates around the world. This growth directly impacts the environment, as with the increase in population comes the great impact that this pressure is on natural areas, such as exploitation of forests, rivers and wildlife.					<B>Objectives</B>: In order to meet the current state of land use, this study mapped land use in the municipality of Fazenda Rio Grande, PR by the digitalization method.					<B>Methods</B>: The mapping was carried out by the screen scanning technique on satellite images. 22 images Google Earth Pro program were extracted. These images were georeferenced and inserted into a Geographic Information System. Later these images were interpreted and digitized,					<B>Results</B>: resulting in 8 different types with their respective areas. Urban area (2,736 ha), Agriculture (3,532 ha), Rain Forest Mista Montana (2,427 ha), Mixed Alluvial Rain Forest (1,517 ha), Pioneer Training fluvial-lacustrine (wetlands) (801 ha), watermark Bodies (1233 ha) Reforestation (177 ha) and Solo Exposed (217 ha).					<B>Conclusion</B>: The study met its goals and mapped the municipality of Fazenda Rio Grande in eight types where the highlights were two main uses, agricultural, with 31% and the areas covered by natural vegetation with 41%.				</P>
			</div><B>Keywords</B>: Land Use. Fazenda Rio Grande. PR. Rain forest.</td>
