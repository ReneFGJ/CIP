			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Esta pesquisa tem como pressuposto a análise reflexiva da Carta aos Hebreus, como iluminação para os processos formativos dos presbíteros, com atenção especial aos presbíteros da Igreja no Brasil.					<B>Objetivo</B>: A partir de Hebreus 12, 1-2, é possível identificar e delinear os traços principais do sacerdócio de Cristo e, assim, apresentar a configuração de vida a Cristo como referência e meta para os presbíteros e os envolvidos no processo de formação presbiteral.					<B>Metodologia</B>: Utilizando métodos exegéticos, a presente pesquisa pretende analisar a presença do sacerdócio nas Sagradas Escrituras e, em particular, na Carta aos Hebreus, para justificar o fato de Jesus ser Sumo Sacerdote constituído por Deus, segundo a ordem de Melquisedec.					<B>Resultados</B>: 					<B>Conclusões</B>: Assim procedendo, chega-se à imprescindível necessidade de os presbíteros orientarem suas vidas ao sacerdócio de Cristo. Chega-se à conclusão, mediante a análise dos resultados obtidos, de que a identidade do presbítero somente pode ocorrer mediante uma profunda relação com Jesus Cristo.				</P>
			</div><B>Palavras-chave</B>: Formação presbiteral. Carta aos Hebreus. Sacerdócio. Igreja.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: This research presupposes a reflective analysis of the Letter to the Hebrews, as
lighting for the formative processes of the elders, with special attention to
elders of the Church in Brazil.					<B>Objectives</B>: So, understanding oh of the Letter, and  from Hebrews 12, 1-2, it is possible to identify and delineate the traits main priesthood of Christ and thus present Christ to life setting
as a reference and target for elders and those involved in the training process
Priestly.					<B>Methods</B>: Using exegetical methods, the present study aims to analyze the
presence of the priesthood in the Holy Scriptures and in particular by the Charter to
Hebrews, to justify the fact that Jesus was high priest ordained by God,
after the order of Melchizedek.					<B>Results</B>: 					<B>Conclusion</B>: In doing so, one comes to the essential
need for priests orient their lives to the priesthood of Christ. Chegase
the conclusion by analyzing the results obtained, that the identity of the
presbyter can only occur through a deep relationship with Jesus Christ.				</P>
