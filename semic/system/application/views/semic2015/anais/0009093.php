			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A preocupação quanto ao teor de enxofre nos combustíveis está cada vez maior e o processo atual de hidrodessulfurização utilizado nas refinarias não é ideal para baixas quantidades de enxofre, como consequência novas formas de ultradessulfurização estão em vista.					<B>Objetivo</B>: Encontrar um modelo matemático que possa descrever o processo de adsorção, discretizá-lo e realizar um treinamento laboratorial de testes e análises a serem feitas para validar o modelo com confiabilidade e segurança.					<B>Metodologia</B>: A discretização foi realizada utilizando o método dos volumes finitos e o treinamento laboratorial abordou testes para obtenção de curva de ruptura, cinética e isoterma de adsorção.					<B>Resultados</B>: Foram estudados três modelos, dos quais foi obtida a equação discretizada para o modelo de equilíbrio e foi possível acompanhar o desenvolvimento e validação do modelo de difusão nos poros.					<B>Conclusões</B>: Um modelo que representa a adsorção foi desenvolvido na pesquisa. Com um modelo validado é possível obter a simulação do processo que torna mais rápido e fácil o desenvolver um projeto de coluna de adsorção.				</P>
			</div><B>Palavras-chave</B>: Modelagem. Adsorção. Diesel. Carvão Ativado.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Concerns with the sulfur content in fossil fuels is increasing and the current hydrodesulphurization process used in refineries is not ideal for low sulfur content, as consequence, new ways of ultradesulphurization are in sight.					<B>Objectives</B>: Find a mathematical model that can describe the adsorption process, discretize it and perform laboratory training for tests and analysis to validate the model.					<B>Methods</B>: The finite volumes method was used to discretize the model and the laboratory training addressed obtaining the breakthrough curve, the kinetics and the isotherm of adsorption process.					<B>Results</B>: Three models were studied from which the equilibrium method was discretized and it was possible to follow the development and validation of the pore diffusion model. The breakthrough curves were well represented by the models for sulfur and nitrogen compounds.					<B>Conclusion</B>: A model that represents the adsorption was developed during the research. With a validated model its possible to obtain the simulation of the process that makes the development of a project easier and quicker.				</P>
			</div><B>Keywords</B>: Modeling. Adsorption. Diesel. Activated Carbon.</td>
