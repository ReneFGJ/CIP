			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Imagens subaquáticas são afetadas pela redução de contraste e pela não uniforme das cores devido à absorção e dispersão da luz no meio aquático.					<B>Objetivo</B>: O estudo desenvolvido propõe estudar, implementar e avaliar procedimentos automáticos de aprimoramento de contraste de realce e de cores de imagens subaquáticas.					<B>Metodologia</B>: Várias técnicas de aprimoramento de âmbito geral e especificas para imagens subaquáticas foram implementadas e avaliadas usando a métrica universal UQI (Universal Image Quality Index).					<B>Resultados</B>: Os resultados mostram que existem técnicas gerais e especificas capazes de aprimorar de realce de contraste e de cores,  mas com grau variável de eficiência. Nem todas as técnicas propostas na literatura são realmente eficientes para todos os tipos de imagens com diferentes contraste e coloração (marrom, verde, azul, mistura etc). A dificuldade em avaliar visualmente quais são as mais eficientes fez que, nos objetivos, foi acrescentada a meta de avaliar numericamente as mudanças provenientes de cada técnica avaliadas usando a métrica universal UQI (Universal Image Quality Index).					<B>Conclusões</B>: A pesquisa mostrou a complexidade do tema, mostrou também que as técnicas propostas na literatura não são eficientes para todos os tipos de imagens com diferentes contraste e coloração (marrom, verde, azul, mistura etc). O uso da métrica universal UQI foi muito enriquecedor no que diz respeito a quantificar as mudanças  de contraste e coloração provenientes de cada técnica.				</P>
			</div><B>Palavras-chave</B>: Processamento de Imagens. Imagens Subaquáticas. Contraste. Métrica universal UQI.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Underwater images are affected by reduction of contrast and uneven colors due to light dispersion and absorption in water.					<B>Objectives</B>: The study proposes to study, implement and evaluate automatic color and contrast enhancement methods for underwater images.					<B>Methods</B>: Several general and specific enhancement techniques for underwater images were implemented and evaluated using the universal metric UQI (Universal Image Quality Index).					<B>Results</B>: The results show that there are general and specific techniques to enhance contrast and color.
Not all techniques proposed in the literature are actually effective for all types of images with different contrast and color (brown, green, blue, mixing etc.). To numerically evaluate and not visually which are the most used efficient, the universal metric UQI (Universal Image Quality Index) was used .					<B>Conclusion</B>: The research showed the complexity of the subject and also showed that the techniques proposed in the literature are not efficient for all types of images with different contrast and color (brown, green, blue, mixing etc.). The use of universal meter UQI was very interesting to quantify color and contrast changes for each technique.				</P>
			</div><B>Keywords</B>: Image Processing. Underwater Images. Contrast. Universal metric UQI.</td>
