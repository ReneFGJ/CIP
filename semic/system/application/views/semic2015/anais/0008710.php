			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O número de pacientes que iniciam a terapia renal substitutiva (TRS) está
aumentando. Em análise de julho de 2012 no Brasil, foram estimados 97.586
pacientes em diálise, destes 8,4% em diálise peritoneal (DP). Contudo, assim como
a hemodiálise (HD), esta TRS apresenta altos índices de mortalidade. Diversos
fatores contribuem para tal, como os relacionados à um desequilíbrio corporal,
desencadeado pela doença renal crônica (DRC), como uremia, anemia e distúrbios
minerais e ósseos. Segundo a diretriz Clinical Practice Guideline for Anemia in
Chronic Kidney Disease – KDIGO, a anemia é diagnosticada em adultos acima dos
15 anos de idade, quando os níveis de hemoglobina (Hb) se encontram abaixo de
13g/dL no sexo masculino e 12g/dL no feminino. As etiologias encontradas como
causa da anemia são: déficit relativo na produção de eritropoetina e escassez de
ferro. Na DRC, a anemia, geralmente, se manifesta quando as taxas de filtração
glomerular (TFG) estão abaixo de 70ml/min/1,73m² em homens e 50ml/min/1,73m²
em mulheres. Todavia sua intensidade e prevalência tornam-se mais frequentes com
a piora da função renal, ou seja, em estágios mais avançados de doença. O
diagnóstico preciso e a abordagem terapêutica precoce são fundamentais para a
prevenção de danos causados pela anemia sobre o sistema cardiovascular e
nervoso, e talvez sobre a progressão da DRC.					<B>Objetivo</B>: o objetivo deste trabalho foi analisar o impacto dos cuidados pré-diálise no comportamento da Hb por um período de 12 meses em pacientes incidentes em DP, que não apresentaram
como forma de tratamento TRS anterior - transplante renal ou  HD.					<B>Metodologia</B>: Foi realizado estudo prospectivo, multicêntrico a partir do coorte BRAZPD II. Foram
estudados 4107 pacientes incidentes em DP, os quais foram divididos em dois
grupos, cuidados pré-diálise precoce e tardio, com base nos valores de Hb.					<B>Resultados</B>: . Destes 4107, 2.107 obtiveram cuidados pré-diálise precoce e 2.000 tardio. Dos pacientes em cuidados precoces, 25,6% apresentaram baixos valores de Hb em comparação ao grupo tardio (29,6%). Em ambos os grupos houve redução do número de pacientes com Hb [e]#8804; 10g/dL.					<B>Conclusões</B>: Conclui-se que mais cuidados pré-diálise podem
proporcionar ao paciente um benefício significativo no controle dos valores da
hemoglobina.				</P>
