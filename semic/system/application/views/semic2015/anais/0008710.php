			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The number of patients starting renal replacement therapy (RRT) is increasing.
In analysis of July 2012 in Brazil, 97,586 patients were estimated on dialysis, these
8.4% in peritoneal dialysis (PD). However, just as hemodialysis (HD), this TRS has
high mortality rates. Several factors contribute to this, such as those related to a
bodily imbalance, triggered by chronic kidney disease (CKD), as uremia, anemia and
mineral and bone disorders. According to the Clinical Practice Guideline for Anemia
in Chronic Kidney Disease – KDIGO, anemia is diagnosed in adults over 15 years old
when hemoglobin levels (Hb) are below 13 g/dL in males and 12 g/dL in female. The
etiologies found as cause of anemia are: relative deficit in the production of
erythropoietin and iron shortage. In CKD, anemia, usually manifests itself when
glomerular filtration rates (GFR) are below 70 ml/min/1.73m² for men and 50
ml/min/1.73m² in women. However its intensity and prevalence become more
frequent with worsening of renal function, or in more advanced stages of disease.
Accurate diagnosis and early therapeutic approach are the key to the prevention of
damage caused by anemia on cardiovascular and nervous system, and perhaps on
the progression of CKD.					<B>Objectives</B>: The aim of this study was to analyze the impact of
pre-dialysis care in Hb behavior on a period of 12 months in incident PD patients who
did not present as a form of previous treatment renal transplantation or TRS with HD.					<B>Methods</B>: This is a prospective, multicenter study from BRAZPD II cohort. In this study 4107
incident patients on PD were divided into two groups, pre-dialysis care early and late,
based on the values of Hb.					<B>Results</B>: 4107, 2107 obtained early pre-dialysis care and
2000 the late care. In the patients on early care, 25.6% had low Hb levels compared
to the late group (29.6%). In both groups there was a reduction in the number of
patients with Hb [e]#8804; 10 g/dL.					<B>Conclusion</B>: It is therefore concluded that more pre-dialysis patient
care can provide significant benefits in controlling the hemoglobin.				</P>
