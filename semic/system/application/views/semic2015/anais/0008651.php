			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Chronic diseases are currently the leading cause of death and disability worldwide. The increase in prevalence reflects the process of industrialization, economic development, food globalization, causing increase of harmful health habits such as smoking, diet low in fruits and vegetables, and physical inactivity.					<B>Objectives</B>: The objective of this study was to estimate the prevalence and distribution of Hypertension (SAH), diabetes mellitus type 2 (DM), Depression and risk factors in a subsample of the population of 18 years and more of the urban area of Curitiba, PR.					<B>Methods</B>: The study was conducted with a sample of 284 individuals aged 18 years and over living in the urban area of Curitiba, in which  physical examinations and questionnaires were conducted to assess the prevalence of the chronic conditions studied, as well as risk factors associated with sociodemographic variables, behavioral, access and quality of health services. Preliminary analysis of the data presented in this research was based on estimates of means and frequencies distribution.					<B>Results</B>: Among the participants, 101 people (35.56%) reported having hypertension; 43 (15.14%) reported having been diagnosed with diabetes; and 93 (27.81%) reported having depression. These prevalence rates were considered high.					<B>Conclusion</B>: In addition to the recognition of health problems, it was observed that emotional problems, income gap, quality of life, geographic location, as well as other social differences may be related to quality of life and health.				</P>
			</div><B>Keywords</B>: chronic diseases; prevalence; diabetes, hypertension, depression</td>
