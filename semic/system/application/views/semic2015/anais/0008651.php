			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: As doenças crônicas são atualmente a principal causa de morte e incapacidade no mundo. O aumento de sua prevalência reflete os processos de industrialização, desenvolvimento econômico, globalização alimentar que causam aumento de hábitos nocivos à saúde como tabagismo, dieta pobre em frutas e vegetais e sedentarismo.					<B>Objetivo</B>: O objetivo deste estudo foi estimar a prevalência e a distribuição de Hipertensão Arterial Sistêmica (HAS), Diabete Melito tipo 2 (DM), Depressão e fatores de risco associados em uma subamostra da população de 18 anos e mais da região urbana de Curitiba, PR.					<B>Metodologia</B>: O estudo foi realizado com uma amostra de 284 indivíduos com 18 anos e mais residentes na região urbana de Curitiba, nos quais foram realizados exames físicos e aplicados questionários para avaliar a prevalência das condições crônicas estudadas, assim como fatores de risco associados a variáveis sociodemográficas e comportamentais e acesso e qualidade dos serviços de saúde.  A análise preliminar dos dados apresentada na presente pesquisa foi baseada em estimativas das médias e frequências.					<B>Resultados</B>: Dentre os participantes, 101 pessoas (35,56%), relatavam ter HAS; 43 (15,14%) relatavam terem sido diagnosticadas com DM; e 93 (27,81%) relatavam ter Depressão.  Tais prevalências foram consideradas altas.					<B>Conclusões</B>: Além do reconhecimento dos problemas de saúde, observou-se que problemas afetivos, diferença de renda, qualidade de vida, localização geográfica, assim como outras diferenças sociais podem estar relacionadas a qualidade de vida e saúde da população.				</P>
			</div><B>Palavras-chave</B>: doenças crônicas; prevalência; diabetes;hipertensão;depressão			<BR>
