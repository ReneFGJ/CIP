			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O reator tubular, ou plug-flow é um tipo de reator comumente utilizado na indústria química, em que os reagentes são continuamente consumidos à medida que avançam, ao longo do seu comprimento.					<B>Objetivo</B>: Modelar e simular um reator plug flow não isotérmico.					<B>Metodologia</B>: Neste trabalho, realizou-se a modelagem e simulação para uma reação de primeira ordem exotérmica em um reator tubular encamisado, com a obtenção das equações diferenciais parciais dos balanços de massa e energia do processo reativo. Abordou-se o balanço de massa no estado transiente, e do balanço de energia em estado permanente. Aplicou-se a metodologia de solução numérica de equações diferenciais parciais por diferenças finitas, e da metodologia numérica de solução de equações diferenciais ordinárias por Runge-Kutta.					<B>Resultados</B>: Implementaram-se as metodologias de cálculo para dois estudos de caso no Excel, de forma a determinar a viabilidade do uso da ferramenta para a solução dos mesmos.					<B>Conclusões</B>: Com a utilização do Excel, foi possível a resolução numérica do PRF.				</P>
			</div><B>Palavras-chave</B>: Reator tubular.  equações diferenciais parciais. equações diferenciais ordinárias. diferenças finitas e Runge-Kutta.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Plug flow reactor (PFR) is a kind of chemical reactor commonly utilized on the chemical industries, on which the reactants are continuously consumed as they advance, through it’s length. Thus, the reactants are consumed over time and reactor length. The modeling of these reactors does not involve elementary mathematical devices, requiring the use of numerical methods.					<B>Objectives</B>: Model and simulate a plug flow reactor not isothermal .					<B>Methods</B>: On this paper, it was made the modelling and simulation of an exothermic and first order chemical reaction on a jacketed tubular reactor, with the obtainment of it’s partial differential equations of mass and energy balances. The mass balance was approached on the transient state, and the energy balance was studied on the permanent state. It was applied the finite difference method to obtain the numeric solution of partial differential equations, and the Runge-Kutta method for solving numerically the ordinary differential equations.					<B>Results</B>: The methodologies of calculation were implemented on two case studies on Excel in order to determine the viability of utilization of this tool on the solutions.					<B>Conclusion</B>: With the use of Excel, it was possible to numerically solve the PRF reactor.				</P>
			</div><B>Keywords</B>: Plug flow reactors. partial differential equations. ordinary differential equations. finite difference and Runge-Kutta.</td>
