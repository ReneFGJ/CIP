			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A gestão de projetos é uma tarefa complexa e de extrema importância para as organizações, usada em diferentes contextos. Para tratar dessa diversidade de situações nas quais projetos aparecem e organizar o esforço envolvido no desenvolvimento dos mesmos, diversos referenciais para a gestão de projetos baseado em processos foram propostos. A forma de realização desses processos, no entanto, depende do contexto de cada projeto e de cada organização. Assim, existe um crescente interesse no entendimento das práticas relacionadas à flexibilidade da gestão de projetos.					<B>Objetivo</B>: O objetivo deste trabalho é definir as principais características dos processos de gerenciamento de um projeto como sistemas organizacionais baseados em processos flexíveis e relacionar essas características a um conjunto de diretrizes para iniciativas de engenharia de organizações previamente desenvolvido.					<B>Metodologia</B>: O método usado é a revisão da literatura sobre processos flexíveis e referenciais de gestão de projetos de forma a se elaborar um framework de análise, baseado no Cambridge Process Approach, para o estudo de diferentes projetos em um contexto organizacional.					<B>Resultados</B>: Resultados da aplicação desse procedimento a uma organização que realiza projetos na área de engenharia são apresentados.					<B>Conclusões</B>: Uma análise preliminar dos resultados permite que se conclua que o procedimento desenvolvido permite que se explore questões relacionadas à flexibilidade de processos de gestão de projetos.				</P>
			</div><B>Palavras-chave</B>: Flexibilidade de processos. Cambridge Process Approach. gestão de projetos			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Project management is a complex task and of uttermost important for any organization, applied to different contexts. To address the diversity of situations in which projects appear and organize the effort involved in their development, several bodies of knowledge have been proposed, most of them based on processes. The way of executing these processes, however, depends on the context of each project and organization. Hence, there is an increasing interest in the understanding of practices related to the flexibility of project management.					<B>Objectives</B>: The objectives of this work are to define the main characteristics of project management processes as organizational systems based on flexible processes and to relate these characteristics to a set of enterprise engineering guidelines previously developed.					<B>Methods</B>: The method that was applied comprises a literature review on flexible processes and project management bodies of knowledge, which informs the development of an analysis framework based on the Cambridge Process Approach for the study of different projects in an organizational setting.					<B>Results</B>: Results of the application of this procedure to an organization that develops project in engineering are presented.					<B>Conclusion</B>: A preliminary analysis of these results allows one to conclude that the developed procedure supports the exploration of issues related to the flexibility of project management processes.				</P>
