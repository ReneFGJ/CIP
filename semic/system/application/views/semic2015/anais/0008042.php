			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Optimize a process is to seek the best condition to solve a problem, and that this quest is achieved, the engineering is used to maximize or minimize variables of a function.					<B>Objectives</B>: The objective of this project and to model , simulate and optimize CSTR reactors in batch , implementing modeling in Excel, with the help of numerical methods .					<B>Methods</B>: In this project the applied optimization method uses computer technology with Excel tool to assist in resolving problems related to CSTR reactors and batch aiming choose classic cases of optimization in chemical engineering CSTR reactors and batch in CAPES periodical portal and develop its modeling, optimization and implementation in Excel, doing a comparative with results that was found in the literature, analyzing the feasibility of this tool in the optimization of chemical reactors.					<B>Results</B>: In selected cases, implementation is possible in Excel, obtaining concentration profiles as a function of temperature. With the development of the sheet, there was an analysis of the influence of process variables in the concentration profiles and you can find as a result optimized for problems similar to those determined by others in the literature, with a great concentration of C component fixed at 0.423 for the CSTR reactor and a great concentration of B fixed at 0.610 for the batch reactor.					<B>Conclusion</B>: 				</P>
