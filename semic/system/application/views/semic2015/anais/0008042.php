			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Otimizar um processo significa buscar a melhor condição para solucionar um problema e, para que esta busca seja alcançada, a engenharia trata de maximizar ou minimizar variáveis de uma função.					<B>Objetivo</B>: O objetivo deste projeto e modelar, simular o otimizar reatores CSTR e em batelada, implementando a modelagem em Excel, com auxílio de métodos numéricos.					<B>Metodologia</B>: Neste projeto o método de otimização aplicado utilizará a tecnologia computacional com a ferramenta do Excel para auxiliar na resolução de problemas relacionados aos reatores CSTR e batelada objetivando escolher casos clássicos de otimização em engenharia química de reatores CSTR e em batelada, no portal de periódicos da CAPES e desenvolver sua modelagem, sua implementação e otimização no Excel, realizar um comparativo dos resultados encontrados com o que se encontra na literatura, analisando a viabilidade desta ferramenta na otimização de reatores químicos. Nos casos escolhidos, foi possível a implementação em Excel, obtendo perfis de concentração em função da temperatura.					<B>Resultados</B>: Com o desenvolvimento da planilha, realizou-se uma análise da influência das variáveis de processo nos perfis de concentração sendo possível encontrar como resultados otimizados para os problemas similares aos determinados por outros encontrados na literatura, com uma concentração ótima do componente C fixada em 0,423 para o reator CSTR e uma concentração ótima de B fixada em 0,610 para o reator batelada.					<B>Conclusões</B>: 				</P>
			</div><B>Palavras-chave</B>: modelagem reator CSTR, modelagem reator em batelada			<BR>
