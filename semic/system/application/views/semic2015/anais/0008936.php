			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Considerado como um instrumento estratégico responsável pela promoção de produtores rurais, o Fomento Florestal não somente impulsiona tais produtores ao mercado como também proporciona, vantagens econômicas, sociais e ambientais.					<B>Objetivo</B>: O estudo foi conduzido, visando analisar as estratégias envolvidas nos programas de fomento florestal realizados no estado do Rio Grande do Sul, por meio de pesquisa junto à 31 empresas no setor florestal localizadas no estado do Rio Grande do Sul.					<B>Metodologia</B>: Posteriormente a identificação do perfil de cada empresa, foi elaborado um questionário contendo perguntas objetivas e discursivas, referente ao suprimento de madeira necessária para a empresa bem como a procedência da matéria prima. Utilizando a ferramenta do Google Forms®, foi possível colocar o questionário em mídia digital para envio via link, sendo efetivamente enviado somente no dia 26 de maio de 2015, ficando disponível para resposta por duas semanas.					<B>Resultados</B>: Duas empresas responderam ao questionário e outras quatro mandaram e-mail fornecendo algumas informações. Ambas as empresas que responderam trabalham com Papel e Celulose e mencionaram que tem uma boa visão acerca das políticas de fomento, contudo estas empresas atualmente não realizam fomento e/ou não desejam ampliar os mesmos.Com os dados, os e-mails e sites disponíveis foi realizada o levantamento indireto das informações almejadas pelo estudo, sendo possível concluir que apenas as empresas que responderam à pesquisa desenvolveram fomento florestal.					<B>Conclusões</B>: Este fato justifica a dificuldade das mesmas em ampliar suas respectivas bases florestais, bem como sua potencializar a visibilidade no mercado, e junto as comunidades próximas.				</P>
