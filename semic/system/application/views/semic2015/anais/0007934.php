			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Esta pesquisa é um estudo e análise das recorrências do verbo periblépomai, utilizado por Marcos em seis momentos de sua obra (3,5; 3,34; 5,32; 9,8; 10,23; 11,11), aparecendo novamente no evangelho de Lucas (6,10). Contudo, evidentemente Lucas só o tenha empregado por se tratar de passagem sinótica, assim comprovando a originalidade de Marcos no uso da expressão.					<B>Objetivo</B>: O intuito da pesquisa é oferecer uma análise do conjunto das perícopes nas quais estão contidas as atitudes de Jesus por meio do referido verbo.					<B>Metodologia</B>: Utilizando métodos sincrônicos e diacrônicos foram seguidos três passos fundamentais: a exegese dos textos para levantar elementos que possam contribuir para a investigação; uma análise teológica para a compreensão do texto à luz da teologia; por fim, foram propostas algumas pistas hermenêuticas com o intuito de compreender como estes textos podem ser visto e aplicados em nossa realidade.					<B>Resultados</B>: A riqueza temática das perícopes que compreendem as recorrências do verbo periblépomai suscita uma hermenêutica importante para a reflexão sobre a cristologia do olhar e, consequentemente uma eclesiologia de visão holística da realidade. Uma vez que o próprio Jesus toma essa atitude de olhar ao redor de si, observando e tomando consciência de tudo a sua volta, fica-nos o convite de tomar a mesma atitude do Mestre, no seguimento e vivência cristãos, conjecturando assim uma eclesiologia da inclusão.					<B>Conclusões</B>: Por conseguinte, os sentimentos de Jesus, expressam a possibilidade de compreender uma cristologia do olhar, pois suas atitudes são referenciais para os discípulos e na convivência e seguimento cristãos, percebemos a urgência de se refletir uma eclesiologia holística, isto é, ir além das aparências para se chegar à integridade e dignidade humana.				</P>
			</div><B>Palavras-chave</B>: Periblépomai. Cristologia. Olhar. Dignidade.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: This research is a study and analysis of recurrences periblépomai verb used by Mark in six moments of his work (3.5; 3.34; 5.32; 9.8; 10.23; 11.11), appearing again in Luke´s Gospel (6.10). However, of course Luke only has employee for being synoptic passage, thus proving the Mark of originality in the use of the expression.					<B>Objectives</B>: The aim of the research is to provide an analysis of all the pericopes in which are contained the attitudes of Jesus by said verb.					<B>Methods</B>: Using synchronous and diachronic methods were followed three basic steps: the exegesis of the text to identify elements that can contribute to the investigation; a theological analysis for understanding the text in the light of theology; Finally, it was proposed some hermeneutical clues in order to understand how these texts can be viewed and applied in our reality.					<B>Results</B>: The thematic richness of pericopes which comprise recurrences of periblépomai verb raises an important hermeneutic for reflection on Christology look and consequently an ecclesiology of holistic view of reality. Since Jesus himself takes this attitude of looking around you, observing and being aware of everything around you, it invites us to take the same attitude of the Master, following and living Christians, so guessing an ecclesiology of inclusion.					<B>Conclusion</B>: Therefore, the feelings of Jesus, express the possibility of understanding a Christology look because their attitudes are references to the disciples and living and follow Christians, we realize the urgency to reflect a holistic ecclesiology, that is, beyond appearances to get to integrity and human dignity.				</P>
