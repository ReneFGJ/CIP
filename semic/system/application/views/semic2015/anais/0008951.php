			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Redes Definidas por Software, um tema relativamente novo no mundo computacional, largamente chamado na literatura de SDN – Software Defined Network, tem como principal diferencial a separação do plano de controle do plano de dados, quebrando o paradigma das redes convencionais. Esta separação permite ao administrador de rede tomar decisões sobre os fluxos da rede via plano de controle, isto motivou o estudo de integração de uma ferramenta de segurança (SDI – Sistema de Detecção de Intrusão) em nível de rede para monitorar e identificar ataques em uma rede SDN.					<B>Objetivo</B>: Implementar um ambiente de simulação para gerar uma rede virtual (SDN) e monitorar esta rede com um SDI, desta forma para cada ataque identificado a ferramenta dispara um alerta para o padrão de ataque ocorrido.					<B>Metodologia</B>: Para gerar o ambiente de rede virtualizada foi necessário utilizar a ferramenta de simulação de redes SDN, chamada Mininet. Esta ferramenta permite criar redes SDN de diferentes proporções, com switches virtuais utilizando o protocolo OpenFlow, que é o protocolo padrão de comunicação das redes SDN. Definido o cenário de simulação, foi necessário comunicar a rede virtual com a rede externa para que o SDI realiza-se o monitoramento do tráfego. Para isto, elencou-se a ferramenta Snort, por ser uma ferramenta de mercado e amplamente conhecida no mundo de segurança computacional.					<B>Resultados</B>: 					<B>Conclusões</B>: 				</P>
