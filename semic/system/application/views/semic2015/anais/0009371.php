			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O gene [e]lt;i[e]rt;PARK2[e]lt;/i[e]rt; ([e]lt;i[e]rt;parkin RBR E3 ubiquitin protein ligase[e]lt;/i[e]rt;) codifica a parquina. Atualmente, diversos estudos mostram que esta proteína está relacionada a diferentes funções, incluindo metabolismo de ácidos graxos, controle do ciclo celular, degradação proteica e supressão tumoral. Porém, como esta proteína está envolvida em todos estes processos ainda não é completamente conhecido.					<B>Objetivo</B>: Para contribuir para a compreensão destes mecanismos, o presente trabalho teve como objetivo selecionar moléculas de siRNA sintéticas para a indução do silenciamento específico e integral do gene [e]lt;i[e]rt;PARK2[e]lt;/i[e]rt; em modelos [e]lt;i[e]rt;in vitro[e]lt;/i[e]rt; em nosso laboratório.					<B>Metodologia</B>: Para tanto, foram adaptados os protocolos de silenciamento desenvolvidos para outra linhagem celular, a THP-1. Assim, células das linhagens CHLA-20 e IMR-32 foram cultivadas e o silenciamento do gene PARK2 foi induzido por meio da utilização de diferentes siRNA específicos para este gene. Para a análise do silenciamento, a expressão do gene PARK2 foi quantificada por qRT-PCR, a partir de cDNA sintetizado de amostras de RNA das células cultivadas.					<B>Resultados</B>: Os resultados permitiram observar na linhagem CHLA-20 um silenciamento parcial do gene PARK2, com diferenças de eficiência entre os dois conjunto de siRNA usados. Por outro lado na linhagem IMR-32 não foi observado silenciamento, talvez devido à elevada expressão do gene alvo nessas células.					<B>Conclusões</B>: Para a confirmação dos resultados e aprimoramento dos modelos [e]lt;i[e]rt;in vitro[e]lt;/i[e]rt; é necessário repetir os experimentos em outras condições. O que poderá contribuir nos estudos do envolvimento do PARK2 em diversas patologias.				</P>
			</div><B>Palavras-chave</B>: [e]lt;i[e]rt;PARK2[e]lt;/i[e]rt;. silenciamento gênico. siRNA			<BR>
