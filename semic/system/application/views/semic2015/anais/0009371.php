			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The [e]lt;I[e]rt;PARK2[e]lt;/I[e]rt; gene (parkin RBR E3 ubiquitin protein ligase) codifies parkin. Currently, many researches show that this protein is related to several functions, including fatty acid metabolism, cell cycle regulation, protein degradation and tumour suppression. However, it remains unclear how this protein is exactly involved in all these processes.					<B>Objectives</B>: Aiming to contribute to the comprehension of these mechanisms, this study selected synthetic siRNAs molecules to induce specific and integral PARK2 gene silencing in [e]lt;I[e]rt;in vitro[e]lt;/I[e]rt; models in our laboratory.					<B>Methods</B>: Silencing protocols developed for other cell line, THP-1, were adapted. Therefore, the cell lines CHLA-20 and IMR-32 were cultivated and [e]lt;I[e]rt;PARK2[e]lt;/I[e]rt; silencing was induced by using different siRNAs, specific for this gene. For the silencing analysis, PARK2 gene expression was quantified through qRT-PCR using cDNA synthesised from the RNA samples of cultured cells.					<B>Results</B>: Results demonstrated partial silencing of PARK2 gene in CHLA-20 line, with variation in efficiency between the two sets of siRNA used. Moreover, silencing was not observed in the IMR-32 line, possibly due to the high target gene expression in these cells.					<B>Conclusion</B>: For confirmation of results and improvement of [e]lt;I[e]rt;in vitro[e]lt;/I[e]rt; models, it is necessary to repeat the experiments under other conditions, contributing to studies of PARK2 involvement in various diseases.				</P>
