			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A pecuária leiteira vem sendo marcada por um intenso processo de modernização, com significativas mudanças nos sistemas de armazenamento e transporte, sendo o resfriamento e a granelização do leite, tendências irreversíveis na produção. Por sua composição, o leite é considerado um dos alimentos mais completos em termos nutricionais, contendo diversos nutrientes com as proteínas. O método de Biureto é uma das metodologias utilizadas para avaliação de concentração total de proteínas em soluções, através da espectrofotometria.					<B>Objetivo</B>: O objetivo foi realizar análises dos perfis proteicos, presentes no leite [e]lt;i[e]rt;in natura[e]lt;/i[e]rt;, pasteurizado e UHT comercial, quando submetidos a diferentes temperaturas para comparação dos perfis nas temperaturas testadas.					<B>Metodologia</B>: Foram utilizadas 30 amostras de leite, divididas em três tipos lácteos, em 10 temperaturas diferentes. Para obter o valor da concentração proteica, foi padronizado uma curva de calibração com concentrações proteicas conhecidas. De cada coleta, foram pipetados 2,5 ml do reagente de biureto para leitura e 0,50 ml de cada amostra a ser dosada.					<B>Resultados</B>: Como resultado, pode ser observado que as concentrações proteicas tiveram variação média (relação entre a concentração inicial e final) não significativa, embora tenham sofrido alterações em valores individuais.					<B>Conclusões</B>: Pode-se concluir que o trabalho apresentou um resultado que corrobora com a literatura pesquisada, demonstrando que o tratamento térmico não altera as concentrações proteicas dos tipos lácteos, nas temperaturas testadas. Por fim, conclui-se que podem ser realizadas novas pesquisas, elevando a temperatura de coleta para valores além dos processamentos que ocorrem no leite UHT.				</P>
			</div><B>Palavras-chave</B>: Espectrofotometria. [e]lt;i[e]rt;in natura[e]lt;/i[e]rt;. Uht.			<BR>
