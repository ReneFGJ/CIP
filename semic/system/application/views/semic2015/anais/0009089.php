			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The dairy industry has been marked by an intense process of modernization, with significant changes in the storage and transport systems, and the bulk milk cooling and milk, irreversible trends in production. By its composition, milk is considered one of the most complete foods nutritionally, containing various nutrients with proteins. The Biuret method is one of methods used to evaluate total protein concentration in solution by spectrophotometry.					<B>Objectives</B>: The aim was to carry out analysis of protein profiles, present in fresh milk, pasteurized and UHT commercial, when subjected to different temperatures to compare the profiles in the tested temperatures.					<B>Methods</B>: They used 30 milk samples, milk divided into three types, at 10 different temperatures. For the amount of the protein concentration was standardized calibration curve with known protein concentrations. From each sample, they were pipetted 2.5 ml of biuret reagent and 0.50 ml for reading each sample to be measured.					<B>Results</B>: As a result, it can be observed that the protein concentrations have average variation (ratio between the initial and final concentrations) not significant, although they have undergone changes in individual values.					<B>Conclusion</B>: It can be concluded that the work presented a result that corroborates the literature, demonstrating that the heat treatment does not alter the protein concentrations of dairy types, the temperatures tested. Finally, it is concluded that further studies can be carried out by raising the temperature to values [e]#8203;[e]#8203;beyond collection of processes that occur in UHT milk.				</P>
