			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The authors point out that sacred music for ukrainians is time to religious experience and memory of religiosity of this people.					<B>Objectives</B>: To investigate the style and the proposal of the ukrainian liturgical song and its understanding constitute the core objective of this research.					<B>Methods</B>: Literature on liturgical song was held, its importance and social significance as well as its features.					<B>Results</B>: Liturgical song reveals apophatism which is evident in a special way in Divine Liturgy and branches in the family and society. The content expressed the corner converges to an evangelization and spreading the faith lived by his descendants. This faith to his particular Church of the catholic rite ukrainian teaches that the believer is an instrument of the Holy Spirit who actively participates in the common prayer. The Holy Spirit acts so that the singer to express well, needs your grace and strength becoming participant´s choir of angels.					<B>Conclusion</B>: The main conclusion of the study is in pneumatic understanding of liturgical song as the essence of worship expression. The singing in this sense has a mystagogical function, catechetical and maintenance and enrichment of faith. The beauty of the song and its contents are important means of evangelization in space of liturgical celebration and in society. The song is part of the life of the ukrainian people where the Divine Liturgy extends to family and society.				</P>
