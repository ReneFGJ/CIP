			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Os autores estudados ressaltam que a música sacra para os ucranianos constitui momento de experiência religiosa e memória da religiosidade do próprio povo.					<B>Objetivo</B>: Investigar o estilo e a proposta do canto litúrgico ucraniano e sua compreensão constituem o núcleo e objetivo da presente pesquisa.					<B>Metodologia</B>: Foi realizada pesquisa bibliográfica sobre o canto litúrgico, sua importância, seu significado social e suas características.					<B>Resultados</B>: O canto litúrgico revela um apofatismo que se evidencia de modo singular na divina liturgia e se ramifica na família e sociedade. O conteúdo expresso pelo canto converge para uma evangelização e divulgação da fé vivida pelos seus descendentes. A Igreja particular do rito ucraniano católico ensina que o fiel é um instrumento do Espírito Santo que participa ativamente na oração comum. O Espírito Santo age de forma que o cantor, para bem expressar, necessita da sua graça e força tornando-se participante do coro dos anjos.					<B>Conclusões</B>: A principal conclusão do estudo consiste na compreensão pneumática do canto litúrgico como essência da expressão do culto. O canto neste sentido tem uma função mistagógica, catequética e de manutenção bem como enriquecimento da fé. A beleza do canto e o seu conteúdo são importantes meios de evangelização tanto no espaço da celebração litúrgica quanto na sociedade. O canto faz parte da vida do povo ucraniano onde a divina liturgia se estende à família e sociedade.				</P>
			</div><B>Palavras-chave</B>: Musica Sacra; Tradição; igreja oriental			<BR>
