			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A Tractografia, do inglês Tractography ou Fiber Tracking, é um método de visualização dos feixes de fibras nervosas através da determinação da difusão da água nos tecidos por ressonância magnética. Os dois principais parâmetros de difusão determinados são a anisotropia fracionada (AF) e difusividade média (DM).					<B>Objetivo</B>: Identificar as principais características dos parâmetros de aquisição de imagens de DTI, de obtenção da Difusividade Média (DM), da Anisotropia Fracionada (AF) e de visualização dos tensores de difusão que permitam a obtenção de imagens de Tractografia de boa qualidade utilizando software de open source.					<B>Metodologia</B>: Foi utilizado o software MITK diffusion como ferramenta para reconstrução de tractografias a partir de imagens de tensor de difusão (DTI) por ressonância magnética. Realizando-se o tutorial do software, identificou-se os procedimentos e os principais parâmetros do método de reconstrução. Uma vez determinados estes parâmetros, foi avaliado o impacto das principais variáveis de reconstrução na tractografia.					<B>Resultados</B>: As técnicas de tensores e q-balls, são as mais importantes da difusão, e as mesmas geram imagens coloridas para facilitar a leitura e o entendimento das visualizações produzidas, que estão relacionadas com os índices de anisotropia da difusão. Os glyphs tornam a identificação de um conjunto de fibras mais fácil, assim consegue-se visualizar, através de elipses, qual o comportamento numa determinada região.					<B>Conclusões</B>: A construção da tractografia a partir de imagens de difusão é feita aplicando-se um processo matemático complexo e com múltiplos parâmetros. A determinação dos melhores valores para cada região a ser observada é fundamental para a qualidade do resultado final.				</P>
			</div><B>Palavras-chave</B>: Tractografia. Difusão. MITK. Ressonância Magnética			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Tractography or Fiber Tracking is a method of nerve fiber bundles viewing by measuring the diffusion of water in tissues by MRI. The two main diffusion parameters determined are the fractional anisotropy (FA) and mean diffusivity (MD).					<B>Objectives</B>: To identify the main characteristics of DTI imaging acquisition parameters, Mean Diffusivity (MD), Fractional Anisotropy (FA) and visualization parameters of diffusion tensor that allow the reconstruction of good quality tractography images using open source software.					<B>Methods</B>: We used the MITK diffusion software as a tool for tractography reconstruction from diffusion tensor imaging (DTI) MRI. By doing the software tutorial, the procedures and the main parameters of the reconstruction method were identified. Once these parameters were determined, the impact of key variables on tractography reconstruction was determined.					<B>Results</B>: The tensor and q-balls techniques are the most important in diffusion, and they generate color images to facilitate the reading and understanding of the produced views, which are related to the diffusion anisotropy rates. The glyphs make identification of one set of fibers easier, thus the behavior in a particular region can be visualized through ellipses.					<B>Conclusion</B>: The construction of tractography from diffusion imaging is done by applying a complex mathematical process and multiple parameters. Determining the best values for each region to be observed is critical to the quality of the final results.				</P>
			</div><B>Keywords</B>: Tractography. DTI. MITK. Magnetic Resonance</td>
