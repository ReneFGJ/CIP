			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Weaning is a critical time in the life of piglets, when they are exposed to several stress factors such as separation from the sow, changes in dietary physical form and composition, changes in the environment, etc., leading to increased susceptibility to intestinal disorders, infections and diarrhea. Thus, it is common to include antimicrobials supplying in-feed sub therapeutic levels used as performance enhancing additives for piglets in the post-weaning phase.					<B>Objectives</B>: The aim of this study was to evaluate the effect of lincomycin as performance enhancing additive on the incidence of diarrhea and performance of pigs in nursery phase.					<B>Methods</B>: For this, 60 newly weaned piglets were used, which were divided into two treatments, each one consisting of ten repetitions of three animals: control group (feed without antibiotic), lincomycin group (feed with 22ppm lincomycin). Feed intake, weight gain and feed conversion was calculated weekly. Every day, in the morning, it was monitored the incidence of diarrhea, by assigning scores related to stool consistency.					<B>Results</B>: Results showed that lincomycin, used as a performance enhancer additive, had no effect on the performance parameters evaluated in this study. However, there was a significant reduction in the incidence of diarrhea in nursery pigs supplemented with lincomycin.					<B>Conclusion</B>: 				</P>
