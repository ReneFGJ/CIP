			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O desmame é um momento crítico na vida dos leitões, quando os animais são expostos a vários fatores estressantes como separação da porca, mudança na forma física e composição da dieta, mudança de ambiente, etc., levando ao aumento da susceptibilidade a desordens intestinais, infecções e diarreias. Dessa forma, é comum a inclusão de antimicrobianos na ração, utilizados como aditivos melhoradores de desempenho, para leitões na fase pós-desmame.					<B>Objetivo</B>: O objetivo deste trabalho foi avaliar o efeito da lincomicina como aditivo melhorador de desempenho sobre a incidência de diarréia e desempenho em leitões na fase de creche.					<B>Metodologia</B>: Para isso, foram utilizados 60 leitões recém-desmamados, os quais foram divididos em dois tratamentos, cada qual composto por dez repetições de três animais: grupo controle (ração sem antibiótico), grupo lincomicina (ração com 22ppm de lincomicina). Semanalmente foi realizado o cálculo do consumo semanal de ração, ganho de peso e conversão alimentar. Diariamente, no período da manhã, foi realizado o monitoramento da incidência de diarréia, com a atribuição de escores relacionados a consistência das fezes.					<B>Resultados</B>: Os resultados demonstraram a lincomicina, utilizada como aditivo melhorador de desempenho, não apresentou efeito sobre os parâmetros de desempenho avaliados neste estudo. No entanto, houve redução significativa na incidência de diarreia de leitões na fase de creche suplementados com lincomicina.					<B>Conclusões</B>: 				</P>
