			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Na dimensão dos Direitos Humanos a educação não pode ser vista apenas como instrumento para conseguir um lugar no mercado de trabalho por questões puramente econômicas subordinadas meramente a um sistema de meritocracias, mas deve-se haver toda uma reflexão acerca do indivíduo e suas ideias como um transformador ativo em todos os âmbitos sociais.					<B>Objetivo</B>: O presente artigo tem como principal objetivo determinar quais são as orientações das diretrizes curriculares nacionais bem como do Paraná para a Educação em direitos Humanos nos cursos de formação de professores das principais Universidades do estado.					<B>Metodologia</B>: A metodologia utilizada será a análise documental a partir dos projetos de cursos de graduação das licenciaturas do estado do paraná.					<B>Resultados</B>: Foi realizado um levantamento dos documentos oficiais que orientam a inserção da temática Direitos Humanos nos cursos de formação de professores, ou seja, percebe-se que mesmo sendo uma política pública de formação de professores ainda tem-se muito que caminhar na implementação dessas abordagens na formação docente inicial.					<B>Conclusões</B>: Os dados indicam que esta legislação tem pouca ou nenhuma aplicação prática sobre os cursos de formação de professores em nível de graduação.				</P>
			</div><B>Palavras-chave</B>: Educação. Direitos Humanos. Diretrizes			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: In the dimension of human rights education can not be seen only as an instrument to achieve a place in the labor market for purely economic issues merely subordinate to a meritocracies system, but should be all a reflection on the individual and his ideas as a transformer active in all social spheres.					<B>Objectives</B>: This article aims to determine which are the guidelines of the national curriculum guidelines and the Paraná for Education in Human Rights in the major teacher training courses from state universities.					<B>Methods</B>: The methodology used is the analysis of documents from the projects of undergraduate degrees of the Parana state.					<B>Results</B>: A survey of official documents that guide the insertion of thematic human rights in teacher training courses, ie, it is clear that even being a public policy of teacher training still has long way to go in implementing these approaches in initial teacher education.					<B>Conclusion</B>: The data indicate that this legislation has little or no practical application on the teacher training courses at the undergraduate level.				</P>
