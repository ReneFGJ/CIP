			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Something indispensable in a manufacturing environment is to generate performance reports, these performance reports allow us to compare different machines, so identify points in the manufacturing line can be improved. There are several indicators that can be used for such purposes, among them stand out from the TEEP (Total Effective Equipment Productivity) and OEE (Overall Equipment Efficiency).					<B>Objectives</B>: Having as objective the communication for industrial equipment and an HMI, it was found that in order to evaluate experimentally the health indicators of the equipment, not only the OEE but also the TEEP, the development of a test bench that held is required simple tasks, such as sorting parts, aiming the simulation of a real factory floor generating data as part quality, quantity and availability of equipment, components necessary to calculate the OEE.					<B>Methods</B>: Initially the main focus of this subproject was in assembling the test bench where it was possible to reach a layout that is able to work without interruption, thereby generating the desired satisfactorily data. The test bench needs to communicate to an information system, such communication being the real purpose of this subproject, providing data countertop operation to the user in an intelligible form, the SCADA system was developed in a specific development platform for this purpose , facilitating the progress of the project.					<B>Results</B>: 					<B>Conclusion</B>: 				</P>
