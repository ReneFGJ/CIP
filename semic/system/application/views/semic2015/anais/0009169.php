			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Algo indispensável em um ambiente de manufatura é a geração de relatórios de desempenho, esses relatórios de desempenho permitem fazer a comparação entre diversas maquinas, assim identificar pontos na linha de manufatura que podem ser melhorados. Existem vários indicadores que podem ser utilizados para esses fins, entre eles destacam-se o TEEP (Total Effective Equipment Productivity) e o OEE (Overall Equipment Efficiency).					<B>Objetivo</B>: Tendo como o objetivo a comunicação entre um equipamento industrial e uma IHM, foi constatado que para podermos avaliar de forma experimental os indicadores de funcionamento do equipamento, não somente o OEE mas também o TEEP, é necessário o desenvolvimento de uma bancada de teste que realizava tarefas simples, como a separação de peças, visando a simulação de um chão de fábrica real gerando dados como a qualidade das peças, quantidade e disponibilidade do equipamento, componentes necessários para o cálculo do OEE.					<B>Metodologia</B>: Inicialmente o enfoque principal desse subprojeto foi na montagem da bancada de testes onde foi possível chegar a um layout que fosse capaz de trabalhar sem interrupções, assim gerando os dados desejados de maneira satisfatória. A bancada de testes precisa se comunicar a um sistema de informação, sendo essa comunicação o real propósito desse subprojeto, disponibilizando dados da operação da bancada para o usuário, de forma inteligível, o sistema SCADA foi desenvolvido em uma plataforma de desenvolvimento específica para esse fim, facilitando o andamento do projeto.					<B>Resultados</B>: 					<B>Conclusões</B>: 				</P>
