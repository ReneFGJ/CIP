			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A citricultura no Brasil, em especial, nas regiões sul e sudeste sempre teve destaque e representou um setor importante da economia dos estados produtores. Com o passar das décadas as cultivares vem sendo substituídas por material genético melhorado, e com isso é necessário ser refeito novos estudos sobre quais são as melhores variedades e onde a cultivar se adapta melhor.					<B>Objetivo</B>: O objetivo do trabalho é avaliar a adaptação de sete cultivares de laranjeira no pomar da Fazenda Experimental Gralha Azul, localizada em Fazenda Rio Grande, PR, durante o período de julho de 2014 a julho de 2015.					<B>Metodologia</B>: A área já estava com o solo preparado e com as cultivares plantadas, após uma seleção, escolhemos 7 cultivares das 10 trabalhadas no primeiro ano do projeto. Foram feitos alguns transplantes de mudas, para alocar 3 plantas de cada cultivar em apenas duas linhas de plantio. Foram analisados dados como altura da planta, diâmetro de copa, floração, frutificação, ocorrência de pragas, quantidade de frutos por planta e por cultivar e análises dos frutos, como peso, altura e diâmetro, pH, acidez e Grau Brix.					<B>Resultados</B>: As cultivares que apresentaram maior crescimento em altura e diâmetro no período de julho de 2014 a julho de 2015 foram as cultivares Lanelate e Valência Midnight, já a cultivar que apresentou menor crescimento no mesmo período foi a Folha Murcha. As duas cultivares que frutificaram foram Jaffa com 3 frutos e Folha Murcha com 7 frutos.					<B>Conclusões</B>: As cultivares apresentaram em maior parte das avaliações um crescimento neste período, mesmo com a forte geada, transplantio e poda realizada em algumas mudas. As cultivares que se destacam são a Jaffa e a Folha Murcha por terem produzido frutos.				</P>
			</div><B>Palavras-chave</B>: Citrus. laranjas. crescimento.			<BR>
