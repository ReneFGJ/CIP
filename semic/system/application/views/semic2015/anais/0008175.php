			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The citrus industry in Brazil, especially in southern and southeastern always stood out and represented an important sector of the economy of the producing states. Over the decades cultivars has been replaced with improved genetic material, and so you need to be redone new studies on what are the best varieties to grow and where it fits best.					<B>Objectives</B>: The objective is to evaluate the adaptation of seven orange cultivars in the orchard of Fazenda Experimental Gralha Azul, located in Fazenda Rio Grande, PR, during the period July 2014 to July 2015.					<B>Methods</B>: The area was already prepared the soil and planted cultivars, after a selection, choose cultivars 7 of 10 worked in the first year of the project. We made some transplant seedlings, to allocate 3 plants of each cultivar in just two crop rows. Data were analyzed as plant height, crown diameter, flowering, fruiting, occurrence of pests, number of fruits per plant and cultivate and analysis of the fruit, such as weight, height and diameter, pH, acidity and Brix.					<B>Results</B>: The cultivars that had high growth in height and diameter from July 2014 to July 2015 were the Lanelate and Valencia Midnight cultivars, as the cultivar that showed lower growth in the same period was the Folha Murcha. Both cultivars were fruitful Jaffa with 3 fruit and Folha Murcha with 7 fruits					<B>Conclusion</B>: The cultivars showed in most evaluations growth in this period, even with the hard frost, transplanting and pruning done in some seedlings. The cultivars that stand out are the Jaffa and Folha Murcha to have borne fruit.				</P>
