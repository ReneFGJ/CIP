			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O crescimento desordenado das metrópoles gera
diversos problemas que atingem todos, e a ecossocioeconomia explora
soluções para estes problemas. Este projeto busca, então, soluções para
a atenuação dos efeitos das intempéries climáticas. A troca de áreas
verdes por asfalto e concreto fazem com que esta nova superfície adquira
e conserve calor por mais tempo, que eleva a temperatura por conta das
ilhas de calor. Uma solução eficaz para adaptar-se às mudanças, reduzir
a velocidade de escoamento e atender ao desenvolvimento sustentável
com benefícios sociais é o telhado verde. O qual atende ao objetivo de
sustentabilidade, propicia melhorias em grande escala e é experiência de
ecossocioeconomia.					<B>Objetivo</B>: A pesquisa desenvolve-se, portanto, a
partir do recolhimento de dados governamentais e independentes,
direcionando-se, principalmente à utilização e benefícios das atividades
de ecossocioeconomia. Dentre os principais objetivos desta pesquisa está
a construção de bases de dados. Também, são objetivos obter dados
acerca do estudo em instituições correlacionadas, entrevistar os atores
envolvidos nos agrupamentos da área de pesquisa, e, finalmente, obter
na literatura o estado da arte do tema.					<B>Metodologia</B>: Inicialmente realizaram-se
pesquisas bibliográficas para compreensão dos conceitos teóricos
intrínsecos e pesquisas exploratórias sobre ações práticas de
sustentabilidade urbana. Com a dificuldade para obtenção de material, foi
necessário pesquisar em eixos transversais e assuntos correlatos para
obter resultados dentro do contexto principal. Na sequência, analisou-se
indicadores disponíveis em fontes confiáveis.					<B>Resultados</B>: Dentre as
experiências catalogadas, as quais ultrapassaram 142, apenas 43 delas
preenchiam os requisitos mínimos para validação e consequente
efetividade da pesquisa. Este baixo número é decorrente da falta de
informações disponíveis ou da inexistência de fontes confiáveis. No entanto, apesar disso, com base na tabela final foi possível fazer uma
análise multivariada por mineração de dados com os métodos de Auto-
Correlação, Análise de Componente Principal, Agrupamento de Dados
(Tree Clustering, K-means), que visaram correlacionar os diversos
indicadores para identificar padrões de associação entre as variáveis.					<B>Conclusões</B>: A partir dos resultados é possível observar que mesmo em
virtude da escassez de informações disponíveis o estudo demonstrou
inovação em uma tentativa inédita na área de ecossocioeconomia,
através de modelagem matemática e mineração de dados.				</P>
			</div><B>Palavras-chave</B>: Ecossocioeconomia. Telhado Verde. Base de Dados. Mineração de Dados.			<BR>
