			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The disorderly growth of cities creates several problems
that affect everyone, and the eco-socioeconomics explores solutions for
these problems. This project seeks for solutions to mitigate the effects
from weather changes. The exchange of green areas by asphalt and
concrete make this new surface acquires and retains heat longer, which
raises the temperature because the heat islands. An effective solution to
adapt to change, reduce the flow velocity and reach sustainable
development with social benefits is the green roof. It reachess the goal of
sustainability, provides large-scale improvements and is regarded as a
eco-socioeconomics.					<B>Objectives</B>: The research develops, therefore, from the
governmental e independents data collection, and is mainly targeting the
utilisation and benefits of the green roof as a eco-socioeconomy solution.
Among the objectives of this research is the construction of a data base
about the subject. Although, the goals are also to collect data from
correlated institutions, interview the authors involved in several groups in
this research area, and, finally, collect in the literature the state of the art of
the theme.					<B>Methods</B>: Initially it was held bibliographical research to understand the intrinsic theoretical concepts and exploratory research into
practical actions of urban sustainability. With the difficulty to obtain
material it was necessary to research on cross-cutting axis and related
matters to achieve results within the main context. Following, it was
analyzed indicators available in reliable sources.					<B>Results</B>: Among the cataloged experiences, which exceeded 142, only 43 of them fulfilled the
minimum requirements for validation and subsequent effectiveness of
research. This low number is due to the lack of information or lack of
reliable sources. However, despite this, based on the final table it was
possible to make a multivariate analysis using data mining with Auto-
Correlation methods, Principal Component Analysis, Data Clustering
(Tree-Clustering, K-Means), that aimed to correlate the various indicators
to identify association patterns among the variables.					<B>Conclusion</B>: From the results it can be observed that even from the lack of available
informations the research has presented innovation in a brand new
attempt in the eco-socioeconomic area, through mathematical modeling
and data mining.				</P>
			</div><B>Keywords</B>: Eco-socioeconomics. Green Roof. Data Base. Data Mining.</td>
