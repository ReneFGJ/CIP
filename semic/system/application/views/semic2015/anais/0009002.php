			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The credit unions have a considerable contribution to the society. They are recognized as instrument with high potential to regional developing and specially to entrepreneurship in small and medium sized firms. But, credit unions are confronted with big challenges in order to grow and to improve services they provide to communities.					<B>Objectives</B>: The present study aimed to study the mechanisms for capital building and loyalty of credit unions.					<B>Methods</B>: The following methods were used: (a) research on official data banks; (b) bibliographical research about capitalization and loyalty of cooperatives; (c) semi structured interviews with specialists of the branch.					<B>Results</B>: Results demonstrated the situation of the credit unions in Brazil and in Paraná State nowadays. The participation of credit unions is considered small in comparison to other forms of financial institutes. A challenge is to increase sector participation in national financial sector. The general lack of knowledge by a good part of the population can be considered a hurdle to sector development. This research identified capital building strategies, that cooperatives use in accordance with their stage of development.					<B>Conclusion</B>: 				</P>
