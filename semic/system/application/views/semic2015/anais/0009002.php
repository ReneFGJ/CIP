			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O cooperativismo de crédito presta considerável contribuição à sociedade. É reconhecidamente um instrumento com excelente potencial para o desenvolvimento regional e em especial do empreendedorismo em pequenas e médias empresas. Entretanto, as cooperativas de crédito defrontam-se com grandes desafios para crescer e aprimorar seus serviços junto às comunidades.					<B>Objetivo</B>: Esse trabalho teve como objetivo estudar os mecanismos de capitalização de cooperativas de crédito, bem como as estratégias de fidelização e principalidade do quadro social (cooperados).					<B>Metodologia</B>: Para isso foram utilizados os seguintes métodos: (a) pesquisa nos bancos de dados oficiais; (b) pesquisa bibliográfica sobre capitalização e fidelidade cooperativa; (c) entrevistas semiestruturadas com especialistas da área de cooperativas.					<B>Resultados</B>: Como resultado foi demonstrado como está a situação do cooperativismo de crédito no Brasil e no Paraná hoje, é visto que a participação do cooperativismo de crédito nas operações de crédito no Brasil ainda é pequena e que o grande desafio é aumentar essa participação, entretanto esse desafio não é fácil de ser superado, pois o brasileiro tem muito pouco conhecimento sobre o modelo cooperativista, isso se mostra como uma das grandes dificuldades a serem superadas para o cooperativismo de crédito alcançar maiores participações no mercado financeiro. No fim chega-se ao objetivo central dessa pesquisa, onde foram observados os principais instrumentos de capitalização e fidelização utilizados hoje pelas cooperativas e quais tem dado os melhores resultados.					<B>Conclusões</B>: Como conclusão é apresentado que não há formulas únicas para atingir um melhor nível de capitalização, mas sim que há estratégias que funcionam melhor para cada estágio e objetivos que as cooperativas tenham.				</P>
			</div><B>Palavras-chave</B>: cooperativas de crédito; fidelidade; capitalização			<BR>
