			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A globalização, liberalização econômica e modernização da tecnologia deram origem a uma criminalidade organizada sofisticada, que trouxe novos delitos e diferentes formas de cometê-los.					<B>Objetivo</B>: A presente pesquisa procura estudar o fenômeno do Criminal Compliance com foco na atividade empresarial, que surge com o objetivo de proteção e prevenção contra práticas criminosas. Será também comentado sobre a importância do tema e seu reflexo na legislação vigente no país.					<B>Metodologia</B>: Trata-se de uma pesquisa qualitativo de caráter bibliográfico.					<B>Resultados</B>: O Compliance visa justamente prevenir e minimizar os delitos cometidos por estas empresas, nos mostrando uma política criminal mais eficiente e avançada.					<B>Conclusões</B>: O instituto começa a ter mais destaque com a Lei nº 9.613/98 (Lei de Lavagem de Dinheiro) e a Lei 12.846/2013 (Lei Anticorrupção). A primeira trouxe alguns deveres de antecipação de possíveis condutas criminosas no âmbito empresarial (principalmente instituições financeiras), atuando como instrumento de autorregulação, com objetivo de facilitar a investigação de tais crimes. A segunda leva em conta, na aplicação das sanções, a existência de mecanismos e procedimentos internos de integridade, como o Compliance.				</P>
			</div><B>Palavras-chave</B>: Compliance. Direito Penal. Compliance officer. Prevenção			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Globalization, economic liberalization and the modernization of technology gave birth to an organized and sophisticated criminality, which resulted in new crimes, and new ways to commit them.					<B>Objectives</B>: This research intends to study the Criminal Compliance phenomenon focusing on business activities, which has the intent of preventing and protecting companies against criminal activities. We will comment on the importance of the subject and its reflexes on our current legislation.					<B>Methods</B>: This research is qualitative and bibliographic.					<B>Results</B>: The Compliance aims to prevent and minimize the offenses committed by these companies, being a more advanced and efficient criminal policy.					<B>Conclusion</B>: It begins to be highlighted with Law nº 9.613/98 (Money Laundering Law) and Law 12.846/2013 (Anti-corruption Law). The first one brought some duties that anticipate possible criminal conducts in the business scope (financial institutions, primarily), acting as an instrument of self-regulation, with the goal of facilitating the investigation of such crimes. The second one takes into account, in the application of sanctions, the existence of mechanisms and internal procedures of integrity, such as Compliance.				</P>
