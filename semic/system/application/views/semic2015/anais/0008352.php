			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Necessitamos conhecer a história da matemática, desde suas origens até os tempos hodiernos, para poder desenvolver o raciocínio logico da matemática e encontrar o ponto axial em que a matemática e a filosofia se entrelaçam. Isso servirá de auxílio na solução dos problemas e questionamentos que tangem os estudantes de matemática.					<B>Objetivo</B>: Queremos argumentar acerca do nosso mundo abstrato da lógica e dos fundamentos da matemática a possibilidade de justificar, a partir da Filosofia, a problemática de nossas dificuldades para com a Matemática. Não apenas, justificá-las, mas, sobretudo, encontrar as respostas necessárias às perguntas de nosso objeto de pesquisa infra exposto. Para isso faz-se necessário o estudo da filosofia matemática em busca da construção de novas perspectivas e abordagens no ensino da matemática.					<B>Metodologia</B>: A partir de uma revisão bibliográfica e documental em livros e na rede mundial de comunicações, possibilitamos o encontro entre a matemática e a filosofia matemática no intento de construir novas perspectivas para o ensino da matemática em todos os seus níveis.					<B>Resultados</B>: Da hermenêutica acerca da filosofia da matemática, impulsionamos a urgência de uma revisão nas grades curriculares de Matemática para torná-la mais prática, acessível e atrativa, evitando o alto índice de evasão dos estudantes.					<B>Conclusões</B>: Urge mudar o modus operandi no ensino da matemática, em todos os seus níveis, nas suas diretrizes curriculares, amparadas pela reflexão possibilitada pela filosofia da matemática e construída a partir da solidez do raciocínio lógico.				</P>
			</div><B>Palavras-chave</B>: Ensino da matemática. Filosofia da matemática e Bertrand Russell.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: We need to know the history of mathematics, from its origins to our times, in order to develop the logical reasoning of mathematics and find the axial point where the mathematics and philosophy intertwine. This will aid in solving the problems and questions that concern the math students.					<B>Objectives</B>: we argue about our abstract world of logic and the foundations of mathematics the opportunity to justify, from the philosophy, the problem of our difficulties with mathematics. Not only justify them, but above all find the necessary answers to the questions of our research infrastructure object exposed. For this it is necessary the study of mathematics philosophy for building new perspectives and approaches in teaching mathematics.					<B>Methods</B>: From a literature and document review in books and on global communications network, we enable the encounter between mathematics and mathematical philosophy in an attempt to build new perspectives for the teaching of mathematics at all levels.					<B>Results</B>: Hermeneutics about the philosophy of mathematics, We push the urgency of a revision in curriculum of mathematics to make it more practical, accessible and attractive, avoiding the high evasion rate of students.					<B>Conclusion</B>: Urges change the modus operandi in teaching mathematics at all levels in their curriculum guidelines, supported by reflection made possible by the philosophy of mathematics and built on the strength of logical reasoning.				</P>
			</div><B>Keywords</B>: Teaching of mathematics. Philosophy of mathematics. and Bertrand Russell.</td>
