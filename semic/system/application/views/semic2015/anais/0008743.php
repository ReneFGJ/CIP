			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Tal estudo traz a aprendizagem colaborativa como proposta e construção do conhecimento, juntamente com o levantamento bibliográfico sobre REA’s. A aprendizagem colaborativa tem por base a troca e interação entre os envolvidos em prol de um objetivo comum. Os Recursos Educacionais Abertos caracterizam-se por diversos materiais pedagógicos e de pesquisa, no qual são demarcados em qualquer tipo de mídia ou suporte, estando eles sob o domínio público, ou licenciado de maneira aberta, oportunizando que sejam modificados por outras pessoas.					<B>Objetivo</B>: Esta pesquisa realizada tem por objetivo de criar Recurso Educacional Aberto para professores.					<B>Metodologia</B>: Foi utilizado como método da pesquisa o levantamento do estado da arte, no qual foram consultados livros dos seguintes autores: MORAIS, RIBEIRO e AMIEL (2011); SANTANA, ROSSANI e PRETTO (2012); SANTOS (2011).					<B>Resultados</B>: 					<B>Conclusões</B>: O levantamento do estado da arte foi essencial, pois ele deu subsidio para que o resultado da pesquisa obtivesse êxito, com a criação do REA e posteriormente publicação destes recursos na mídia				</P>
			</div><B>Palavras-chave</B>: Recursos Educacionais Abertos. Aprendizagem Colaborativa. Formação continuada online.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: This study provides collaborative learning as a proposal and construction of knowledge, along with the literature on REA´s. Collaborative learning is based on the exchange and interaction among stakeholders towards a common goal. The Open Educational Resources are characterized by various teaching materials and research, which are demarcated in any media or support, when they were in the public domain or licensed in an open manner, providing opportunities that are modified by others.					<B>Objectives</B>: This research conducted aims to create Open Educational Resource for teachers.					<B>Methods</B>: Was used as a research method lifting the state of the art, in which the following authors books were consulted: MORAIS, RIBEIRO and AMIEL (2011); SANTANA, ROSSANI and PRETTO (2012); SANTOS (2011).					<B>Results</B>: 					<B>Conclusion</B>: The lifting of the state of the art was essential as it gave subsidies to the search result were successful, with the creation of REA and further publication of these resources in the media.				</P>
			</div><B>Keywords</B>: Open Educational Resources. Collaborative Learning. online Continuing Education.</td>
