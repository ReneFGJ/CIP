			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A poluição dos recursos hídricos é parcialmente evitada através de sistemas adequados de controle e monitoramento da poluição, perante este fato o acompanhamento de parâmetros de qualidade dos corpos hídricos torna-se cada vez mais importante.					<B>Objetivo</B>: O presente trabalho tem o objetivo de monitorar as condições físico-químicas das águas do Rio do Ouro, em Ouro Verde do Oeste, Paraná, comparando os resultados com valores estabelecidos pela legislação vigente, CONAMA 357/05.					<B>Metodologia</B>: O estudo contempla cinco pontos de amostragem, considerando as diferentes interferências no entorno do corpo hídrico. Coletas foram feitas no período de janeiro a junho de 2015. Os parâmetros avaliados: temperatura, turbidez, pH, condutividade elétrica, sólidos totais (ST), fósforo total, nitrogênio amoniacal total e demanda química do oxigênio (DQO).					<B>Resultados</B>: Apesar de não analisarmos a totalidade dos parâmetros citados na legislação, cerca de 60, o Rio do Ouro apresenta conformidade com a legislação vigente CONAMA 257, dos parâmetros analisados, não apenas no semestre citado mas também nos últimos dois anos de monitoramento.					<B>Conclusões</B>: A análise de parâmetros de qualidade das águas nos distintos pontos de coletas, mostram a importância do monitoramento dos recursos hídricos para garantir atenção a qualquer desvio das condições naturais padrões deste curso de água, vital para o abastecimento do município. Assim, o histórico do Rio do Ouro, relativo à qualidade das águas nos leva a crer fortemente que este curso hídrico possui ótima qualidade ambiental.				</P>
			</div><B>Palavras-chave</B>: Controle. hídrico. análises. físico-químico			<BR>
