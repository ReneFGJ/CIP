			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The water pollution resources is partly avoided by appropriate control and monitoring of pollution systems, before this fact monitoring of bodies water quality parameters becomes increasingly important.					<B>Objectives</B>: This study aims to monitor the physical and chemical conditions of the Gold River waters in Ouro Verde do Oeste, Paraná, comparing the results with values established by law, CONAMA 357/05.					<B>Methods</B>: The study includes five sampling points, considering the different interference surrounding the water body. Collections were made from January to June 2015. The parameters: temperature, turbidity, pH, electrical conductivity, total solids (ST), total phosphorus, total ammonia nitrogen and DQO.					<B>Results</B>: Although not analyze all the parameters mentioned in the legislation, about 60, the Rio do Ouro shows compliance with current legislation CONAMA 257, of the parameters analyzed, not only in the semester quoted but also the last two years of monitoring.					<B>Conclusion</B>: The analysis of water quality parameters in the different collection points, show the importance of monitoring water resources to ensure attention to any deviation from the standard conditions of this natural watercourse, vital for supplying the municipality. Thus, the history of Rio do Ouro on the quality of water leads us to believe strongly that this water course has great environmental quality.				</P>
