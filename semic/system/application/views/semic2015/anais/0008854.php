			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The native forest species has considerable growth for recovery use, timber production and forest resources. However, the lack of research on native species decreases the quality and quantity of these seedlings to provide good stands (GONÇALVES et al., 2000).					<B>Objectives</B>: This project aims to study the seedlings of Tabebuia avellanedae (Ipe pink), [e]lt;I[e]rt;Cedrella fissilis[e]lt;/I[e]rt; (pink cedar) and [e]lt;I[e]rt;Cassia leptophylla[e]lt;/I[e]rt; (Barbatimão) growth, based on dendrometric variables and equations fitting to express natural conditions growth behavior.					<B>Methods</B>: The research was conduct at Gralha Azul Experimental Farm (FEGA), where the seedlings were planted and there was subsequently monthly data collecting, as stem height diameter, breast diameter height and height. Exponential, Gaussian and Weibull probability density function were used, in which estimate standard error, adjusted coefficient of determination, the residue and the projection of the model on the observed data were used.					<B>Results</B>: Gauss model presented the better statistical adjustment for Cedro Rosa. For Barbatimão and Ipe rosa species, the best results were obtained by fitting the Gaussian and Weibull models, although still unsatisfactory, due to too early stage of such plants.					<B>Conclusion</B>: 				</P>
