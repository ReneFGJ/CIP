			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A floresta nativa conta com espécies com crescimento considerável para uso em recuperação de áreas, produção de madeira e recursos florestais. Porém, a falta de pesquisa sobre as espécies nativas diminui a qualidade e quantidade dessas mudas para proporcionar bons povoamentos (GONÇALVES et al., 2000).					<B>Objetivo</B>: Este projeto tem como objetivo estudar o crescimento de mudas de Tabebuia avellanedae (Ipê rosa), [e]lt;I[e]rt;Cedrella fissilis[e]lt;/I[e]rt; (Cedro rosa) e [e]lt;I[e]rt;Cassia leptophylla[e]lt;/I[e]rt; (Barbatimão), com base nas variáveis dendrométricas e ajuste de equações para expressar o comportamento de crescimento em condições naturais no campo.					<B>Metodologia</B>: A pesquisa ocorreu na Fazenda Experimental Agrária Azul (FEGA), onde as mudas foram plantadas e posteriormente houve coleta de dados mensais de diâmetro altura do colo, diâmetro altura peito e altura. Foram utilizados os modelos de distribuição de função densidade de probabilidade: Exponencial, Gauss e Weibull, onde foi avaliado o erro padrão da estimativa, o coeficiente de determinação ajustado, o resíduo e a projeção do modelo sob os dados.					<B>Resultados</B>: Para a espécie Cedro rosa, o modelo Gauss apresentou o melhor ajuste. Para as espécies Barbatimão e Ipê rosa, os melhores resultados foram obtidos pelo ajuste dos modelos de Gauss e Weibull, embora ainda insatisfatórios, em função da fase demasiadamente inicial dessas plantas.					<B>Conclusões</B>: 				</P>
			</div><B>Palavras-chave</B>: Modelos. Diâmetro. Altura.			<BR>
