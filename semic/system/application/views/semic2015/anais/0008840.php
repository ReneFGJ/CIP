			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Estudo focado em analisar a formação de tarifas de ônibus em âmbito municipal. O estudo utiliza como âncora o tema da discussão em Curitiba, notadamente a partir do que foi discutido no Tribunal de Contas do Estado.
O serviço público tem no Brasil por força de lei um vínculo inerente do poder
público na atividade, sendo uma forma de efetivação dos direitos fundamentais. A concessão é uma relação administrativa através da qual o Poder Público delega a execução de determinado serviço a um particular selecionado por licitação que assume por sua própria conta e risco a gestão de um projeto concessionário autossustentável através de um sistema remuneratório que se desenrola mediante o pagamento de tarifas pelos usuários. Analisar e estudar a cobrança da tarifa no transporte de passageiros, objetivando elucidar as razões pelas quais o preço da passagem é o atual cobrado, abordando também o ponto de vista social da política tarifária.					<B>Objetivo</B>: Discutir a formação de tarifas de ônibus. Analisar os diferentes tipos de tarifa. Examinar a formação da tarifa.					<B>Metodologia</B>: Pesquisa Teórica a partir de textos de doutrina e julgados.					<B>Resultados</B>: 					<B>Conclusões</B>: 				</P>
