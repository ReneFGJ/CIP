					<B>Introduction</B>: The research is developed to unterstand the logic of tolls in bus services in Brazil. 
The study uses as anchor the topic of discussion in Curitiba, notably from what was discussed in the State Court of Auditors.
The public service has in Brazil by the force of law an inherent link of the government in the activity, being a form of realization of fundamental rights. The award is an administrative relationship through which the Government entrusts the execution of a particular service at a particular selected by bidding that takes for its own risk management from a dealer self-sustaining project through a remuneration system that unfolds upon payment tariffs by users. Analyze and study the collection of the rate in passenger transport, aiming to elucidate the reasons why the fare is the current charged, also addressing the social point of view of the tariff policy.					<B>Objectives</B>: Discuss tolls in public transport services in Brazil					<B>Methods</B>: Theoretical					<B>Results</B>: 					<B>Conclusion</B>: 				</P>
