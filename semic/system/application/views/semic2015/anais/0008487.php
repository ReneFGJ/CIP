			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O esôfago de Barrett (EB) é caracterizado pela substituição do epitélio escamoso estratificado do esôfago por epitélio colunar especializado com células caliciformes, ou seja, metaplasia colunar do tipo intestinal, o que está associado com a progressão para displasia e adenocarcinoma de esôfago.					<B>Objetivo</B>: O objetivo desta pesquisa é determinar a prevalência de displasia de esôfago em pacientes com diagnóstico anatomopatológico de Esôfago de Barrett.					<B>Metodologia</B>: A amostra consistiu em 460 pacientes suspeitos da presença de Esôfago de Barrett pelo exame de endoscopia digestiva alta. A partir destes pacientes buscou-se o diagnóstico definitivo de Esôfago de Barrett mediante análise de exames anatomopatológicos. Por meio de um protocolo foram avaliadas variáveis relacionadas com o diagnóstico e a presença de displasia e adenocarcinoma de esôfago. Por fim, foi realizada uma análise estatística para determinação da prevalência de displasia de esôfago em pacientes com Esôfago de Barrett. Esta pesquisa foi aprovada pelo Comitê de Ética em Pesquisa da Pontifícia Universidade Católica do Paraná (Protocolo CAAE 27691914.0.000.0020).					<B>Resultados</B>: A prevalência de displasia em casos de Esôfago de Barrett foi de 4,3%. A média de idade entre pacientes com Esôfago de Barrett foi de 54,2 anos, aumentando para 61,1 se considerarmos a presença de displasia. Dos 154 casos sem displasia, 128 (83,12%) tinham esofagite e dos 7 casos com displasia, 3 (42,86%) tinham esofagite, mostrando uma diferença estatisticamente significativa (p=0,023).					<B>Conclusões</B>: Com o presente estudo pode-se concluir que o esôfago de Barrett tem um potencial risco de evolução para displasia e consequentemente para adenocarcinoma, justificando assim um acompanhamento nestes pacientes.				</P>
