			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A mielomeningocele (MMC) é uma anomalia congênita resultante do defeito no fechamento do tubo neural, processo embriológico com ocorrência durante a 4º semana de gestação, que acomete, principalmente, a região lombo-sacra. A MMC é o subtipo de espinha bífida com maior relevância social, resultando em acometimento urológico, nefrológico, gastrintestinal e muito comumente ortopédico.					<B>Objetivo</B>: O referente estudo visa avaliar a reposição de Ácido Fólico antes e durante a gestação, em função das crescentes evidências do seu auxílio na prevenção das anomalias resultantes de Defeito do Tubo Neural (DTN).					<B>Metodologia</B>: Foram avaliados 24 pacientes por meio de um instrumento contendo 23 ítens.					<B>Resultados</B>: Dentre os resultados desta pesquisa destaca-se o fato de apenas 3 dos 23 pacientes terem realizado a reposição de Ácido Fólico antes e durante a gestação até, no mínimo, o 2º mês gestacional., equivalendo a apenas 13% dos entrevistados.					<B>Conclusões</B>: É notória a necessidade da utilização do ácido fólico antes e durante a gestação para a prevenção desta anomalia congênita. Evidencia-se assim a importância de maior conscientização da população e realização de campanhas preventivas.				</P>
			</div><B>Palavras-chave</B>: MIELOMENINGOCELE, DISRAFISMO ESPINHAL			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The Myelomeningocele (MMC) is a defect resulting from congenital abnormalities in neural tube closure, embryological process occurring during the 4th week of pregnancy, affecting mainly the lumbosacral region. The MMC is the spina bifida subtype with the greatest social impact, resulting in urological , nephrological, gastroenterological and usually orthopedical affections.					<B>Objectives</B>: The referent study aims to evaluate the replacement of folic acid before and during pregnancy, according to the increasing evidence of their aid in the prevention of anomalies resulting from Neural Tube Defect (NTD).					<B>Methods</B>: A total of twenty four patients responded to 23 questions..					<B>Results</B>: Among the results of this the fact that only 3(13%) out of 23 patients have done the folic acid supplementation before at least 2 months of pregnancy.					<B>Conclusion</B>: The research emphasizes the relevance of the necessity of folic acid supplementation before and during pregnancy in order to prevent this congenital anomaly thus the importance of increasing public awareness and prevention campaigns				</P>
