			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Síndrome metabólica (SM) é um transtorno complexo que engloba diversos fatores, incluindo obesidade central, hipertensão arterial sistêmica (HAS) e diabetes mellitus tipo s (DM). A cirurgia bariátrica tem demonstrado resultados excelentes na melhora da síndrome metabólica e na diabetes mellitus.					<B>Objetivo</B>: O presente estudo tem por objetivo avaliar os resultados da técnica de cirurgia bariátrica de derivação gástrica em Y de Roux na  melhora em longo prazo da SM e da DM.					<B>Metodologia</B>: Foi realizado um estudo retrospectivo de 910 pacientes submetidos a gastroplastia com derivação gástrica em Y de Roux (GDYR) pela clínica gastrovida. Destes pacientes foram selecionados pacientes com critérios de SM e DM cujo tempo de acompanhamento foi superior a 5 anos e inferior a 10 anos. Os dados foram colocados em planilha de excel e a média foi avaliada.					<B>Resultados</B>: Dos 910 pacientes submetidos a GDYR apenas  352 apresentavam tempo de acompanhamento maior do que 5 anos. Destes, 60,23% apresentavam SM, porém apenas 27,8% (59 pacientes) tinham acompanhamento realizado por mais de 5 anos. Observamos nesta população melhora da SM em 93,22% dos pacientes e da DM em 87,5% dos pacientes.					<B>Conclusões</B>: A melhora da SM e da DM após cirurgia bariátrica (GDYR) já está bem documentada, porém existe dificuldade na aquisição de resultados em longo prazo devido ao baixo índice de acompanhamento dos pacientes. No presente estudo observamos excelente resultado em longo prazo porém ainda com baixo número de pacientes. São necessários estudos adicionais para confirmar que a melhora da DM e SM pós procedimento de GDRYR é prolongada				</P>
			</div><B>Palavras-chave</B>: cirurgia bariátrica, síndrome metabolica, diabetes mellitus, estudos em longo prazo			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Metabolic syndrome (MS) is a complex disease envolving a series of factors including central obesity, hypertension and type 2 diabetes (DM). Bariatric surgery has demonstrated excellent results in the  improvement of MS and type 2 diabetes.					<B>Objectives</B>: The aim of the present study is to evaluate the results of roux-em Y gastric bypass in the long term improvement of MS and type 2 diabetes.					<B>Methods</B>: A retrospective study evaluated 910 patients subjected to RYGB in gastrovida clinic which had metabolic syndrome and type 2 diabetes and had more than 5 years and less than 10 years of post operative follow up. Data was evaluated by excel.					<B>Results</B>: From the 910 patients who underwent RYGB, only 352 had more that 5 years after surgery. Only 60,23% of patientes had the criteria for MS, however only 27,8% of these patients had a post operative follow up for more than 5 years. We observed an improvement of 93,22% of metabolid syndrome and 87,5 % of the patients had an improvement in type 2 diabetes.					<B>Conclusion</B>: The improvement of MS and type 2 diabetes after RYGB has already been documented, however studies with long term follow up are scarce. In the present study, despite the decreased number of patients we observed excellent results in the ameliorations of MS and type 2 diabetes in a 5 to 10 years follow up.				</P>
			</div><B>Keywords</B>: Bariatric Surgery, metabolic syndrome, type 2 diabetes, long term studies</td>
