			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A Análise de Redes Sociais (ARS) ou Redes Complexas, representada através de um conjunto de atores ligados por arestas formando um grafo, permite distinguir as suas relações e diferenças. O foco da ARS é diferente dos demais métodos que assumem que os atores agem e tomam decisões de forma independente. Nela os atores formam um sistema social, onde suas conexões influenciam o comportamento de cada um.					<B>Objetivo</B>: O principal objetivo desse projeto é desenvolver uma abordagem de seleção dinâmica de classificadores que seja capaz de construir uma rede de conexão entre classificadores e posteriormente aplicar técnicas de análise de redes para selecionar um subconjunto da rede para classificar uma instância de teste.					<B>Metodologia</B>: Um método de seleção baseada em Acurácia e Diversidade desenvolvido previamente, foi implementado e usado em conjunto com Teoria de Redes sociais com o propósito de selecionar o melhor subconjunto de classificadores capazes de prever a classe de instâncias de teste.					<B>Resultados</B>: A análise dos classificadores representada por teoria dos grafos foi bem significativa. A extração de novas informações pode ser estudada para os próximos trabalhos. Não foi possível desenvolver uma ferramenta integrada, portanto foram utilizadas APIs externas, o que dificultou a relação da implementação do algoritmo de seleção de um subconjunto, em favor da análise com a teoria dos grafos.					<B>Conclusões</B>: Os resultados encontrados possibilitam a escolha de métodos de classificação, porém é necessário verificar se as medidas de centralidade para encontrar um subconjunto dentro do grafo são suficientes e eficazes. Infelizmente não foi possível fazer esta comparação, para saber se existiria um ganho relativo entre os resultados.				</P>
			</div><B>Palavras-chave</B>: Teoria de Redes Sociais. Redes complexas. K-Nearest Neighbors. Seleção dinâmica de classificadores. Subconjunto de classificadores.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The Social Network Analysis (SNA) or complex networks, represented by a set of actors connected by edges forming a graph, distinguishes their relations and differences. The focus of the ARS is different from other methods that assume that the actors act and make decisions independently. Her actors form a social system where their connections influence the behavior of each other.					<B>Objectives</B>: The main objective of this project is to develop a dynamic classifier selection approach that is able to build a connection between classifiers and then apply network analysis techniques to select a subset of the network to classify a test instance.					<B>Methods</B>: A selection method based on accuracy and Diversity developed previously, has been implemented and used in conjunction with social networks theory in order to select the best subset of classifiers able to predict the test instances of class.					<B>Results</B>: The analysis of the classifiers represented by graph theory was very significant. The extraction of new information can be studied for the next work. It was not possible to develop an integrated tool, so external APIs were used, making it difficult to regard the implementation of a subset selection algorithm in favor of the analysis with the graph theory.					<B>Conclusion</B>: The findings enable the development of classification methods, but they do not determine  the if the measures used to find a subset within the graph are sufficient and effective. Unfortunately it was not possible to make this comparison, to see if there would be a relative gain in the results.				</P>
			</div><B>Keywords</B>: Social Networks Theory. Complex networks. K-Nearest neighbors. Dynamic selection of classifiers. Ensembles of classifiers.</td>
