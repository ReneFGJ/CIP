			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A reabsorção óssea é um processo com muitas etapas no qual o Receptor Ativador de NF[e]#954;B (RANKL) desempenha um importante papel.					<B>Objetivo</B>: O propósito deste estudo foi verificar a hipótese que a expressão do Ligante de Receptor Ativador de NF[e]#954;B (RANKL), é significante elevada em pacientes com periodontite apical sintomática					<B>Metodologia</B>: Vinte pacientes com lesões periapicais foram divididos em dois gupos: grupo sintomático (SYM) composto por 10 pacientes e o grupo assintomático (ASYM) com 10 pacientes. Todos os dentes dos pacientes tinham sido endodonticamente retratados pelo menos 2 anos antes da cirurgia periapical. As lesões periapicais foram coletadas, processadas e cortadas em secções de 4-[e]#956;m. Pelo menos duas secções foram processadas pelo exame histológico usando hematoxilina e eosina, as demais foram usadas para imunohistoquímica. A fração da área de células RANKL positivas foi mensurada e o percentual de áreas RANKL imunopositivas relativas à area totais do campo microscópio foi calculado. O teste t de Student foi realizado					<B>Resultados</B>: O percentual imunopositivo de área de RANKL não foi significativamente diferente entre os grupos. Os macrófagos são as células que mais expressaram RANKL em ambos os grupos.					<B>Conclusões</B>: A maior expressão de RANKL em lesões sintomáticas pode representar a dinâmica do processo agudo com maior potencial de reabsorção óssea.				</P>
			</div><B>Palavras-chave</B>: RANKL. imunohistoquímica. lesão periapical			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Bone resorption is a multi-step process in which RANKL play important role.					<B>Objectives</B>: The purpose of this study was to examine the hypothesis that the expression of RANKL is significantly elevated in patients with symptomatic apical periodontitis.					<B>Methods</B>: Twenty patients with periapical lesions were divided into two groups: a symptomatic (SYM) group of 10 patients and an asymptomatic (ASYM) group of 10 patients. All teeth had been endodontically retreated at least 2 years before periapical surgery. The periapical lesions were collected and serially cut into 4-[e]#956;m-thick sections. At least two sections were processed for histological examination using hematoxylin-eosin staining, and the remaining sections were processed for immunohistochemistry. The area fraction of RANKL-positive cells was measured and the percentage of RANKL-immunopositive areas relative to the total area of the microscopic field was calculated.  The Student´s t-test were performed.					<B>Results</B>: The percentage of immunopositive area for RANKL was not significantly different between the groups. Macrophage-like cells predominantly expressed RANKL as determined   by   immunohistochemistry   analysis					<B>Conclusion</B>: The greatest expression of RANKL in symptomatic lesions may play the dynamics of the acute process with greater bone resorption.				</P>
