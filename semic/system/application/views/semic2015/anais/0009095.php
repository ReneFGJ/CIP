			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O milho é uma das culturas mais importantes para a economia agrícola mundial. Como aspecto determinante para atingir altas produtividades, o milho necessita de adubações complementares de nitrogênio, por ser um nutriente extraído em grandes quantidades pela cultura.					<B>Objetivo</B>: Este trabalho tem por objetivo determinar a curva de produção do milho em diferentes doses de nitrogênio e avaliar o diferencial vegetativo e produtivo do milho nas diferentes doses.					<B>Metodologia</B>: O experimento foi realizado na Fazenda Experimental da Pontifícia Universidade Católica do Paraná – PUCPR, Campus Toledo, PR. O solo onde foi implantado o experimento é Latossolo Vermelho Eutroférrico. O trabalho foi realizado inteiramente casualizado sendo oito tratamentos com 0, 50, 100, 150, 200, 250 ,300 e 350 quilogramas de nitrogênio por hectare, com quatro repetições, totalizando 32 unidades experimentais. Foi aplicado como fonte de nitrogênio sulfato de amônia, quinze dias antes da implantação da cultura.					<B>Resultados</B>: Foram observadas diferenças significativas (p[e]rt;0,05) nos componentes de rendimento: número de grãos por fileira e comprimento de espiga, além da produtividade, também com efeito significativo em relação as doses aplicadas. A produtividade foi incrementada a medida que ampliou-se a dose de nitrogênio aplicado, porém mesmo na dose máxima não se obteve inflexão da curva.					<B>Conclusões</B>: Conclui se que as maiores produtividades de grãos foram obtidas nos tratamentos que receberam as maiores doses de N incorporado antecipadamente,  não tendo sido observado efetitos negativos mesmo na maior dose. A aplicação de nitrogênio interfere nos componentes da produtividade de grãos e de aspectos morfológicos na planta.				</P>
			</div><B>Palavras-chave</B>: Sulfato de Amônia. componentes de rendimento. Zea mays			<BR>
