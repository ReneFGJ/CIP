			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Corn is one of the most important crops for global agricultural economy. How crucial aspect to achieve high yields, corn needs additional nitrogen fertilization, as a nutrient extracted in large quantities by culture.					<B>Objectives</B>: This study aims to determine the Corn production curve at different doses of nitrogen and evaluate the vegetative and productive differential Corn in different doses.					<B>Methods</B>: The experiment was conducted at the Experimental Farm of the Pontifical Catholic University of Paraná - PUCPR Campus Toledo, PR. The soil where the experiment was implemented is Oxisoil. The study was conducted completely randomized with eight treatments with 0, 50, 100, 150, 200, 250, 300 and 350 kg of nitrogen per hectare, with four repetitions, totaling 32 experimental units. Was used as a source of nitrogen ammonium sulfate, two weeks before the deployment of culture,					<B>Results</B>: Significant differences were observed (p[e]rt; 0.05) in yield components: number of kernels per row and length of the cob, plus productivity also have a significant effect on the applied doses. Productivity was increased as they expanded to applied nitrogen rate, but even at the maximum dose was obtained no inflection of the curve.					<B>Conclusion</B>: concludes that the highest corn grain yield were obtained in the treatments that received the highest doses of N incorporated in advance and had no negative efetitos even at the highest dose. The nitrogen application interfere in grain yield components and morphological aspects at the plant.				</P>
			</div><B>Keywords</B>: Ammonium Sulfate. yield components. Zea mays</td>
