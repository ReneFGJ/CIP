			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A dissecção submucosa endoscópica (ESD) vem sendo empregada como uma alternativa endoscópica ao tratamento cirúrgico de lesões precoces do trato digestivo. Permite a ressecção de neoplasias em bloco, análise histológica precisa, maior taxa de cura, além dos baixos índices de complicações alcançadas.					<B>Objetivo</B>: Nossos estudos objetivaram avaliar os resultados da DES, em neoplasias precoces de esôfago e estômago.					<B>Metodologia</B>: Análise retrospectiva dos casos de neoplasia precoce do esôfago e estômago submetidos a DES no período de janeiro de 2012 à julho de 2015, no Serviço de endoscopia do Hospital Sugisawa de Curitiba, PR.					<B>Resultados</B>: Foram realizadas 30 dissecções. A idade variou de 25 a 92 anos (m=63 ± 8). Dezoito (60%) pacientes eram do sexo masculino. A ressecção em monobloco ocorreu em 100% dos pacientes, sendo curativa 93,3% dos casos (28 pacientes). As lesões ocorreram, majoritariamente, no estômago (22 pacientes) e 8 no esôfago distal. Ocorreu complicações em 6 pacientes, sendo 5 perfurações, três tratadas com endoclipes, uma cirurgicamente e a outra com prótese esofágica; um episódio de sangramento tratado com endoclipe.					<B>Conclusões</B>: Concluímos que a dissecção endoscópica submucosa proporciona elevado índice de cura das neoplasias neoplasias precoces do esôfago e estômago, com altas taxas de ressecção em monobloco, curativas e baixas taxas de complicações.				</P>
			</div><B>Palavras-chave</B>: ESD, câncer precoce, dissecção submucosa			<BR>
