			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Endoscopic submucosal dissection (ESD) has been used as an endoscopic alternative to surgical treatment of early lesions of the digestive tract. Allows neoplasms en bloc resection, accurate histological analysis, higher cure rate, in addition of low levels of complications.					<B>Objectives</B>: Our studies evaluated the results of ESD in early cancer of the esophagus and stomach					<B>Methods</B>: Retrospective analysis of patients undergoing ESD, from January 2012 to July 2015, in Sugisawa Hospital endoscopy service Curitiba, PR.					<B>Results</B>: 30 dissections were performed. The age ranged from 25 to 92 years (mean = 63 ± 8). Eighteen (60%) patients were male. Resection in a single block occurred in 100% of patients, curative 93.3% of cases (28 patients). The lesions occurred mainly in the stomach (22 patients) and 8 in the distal esophagus. Complications occurred in 6 patients, 5 perfurations, three endoclipes treated with surgery and the other with esophageal prosthesis; an episode of bleeding treated with endoclipe.					<B>Conclusion</B>: We conclude that endoscopic submucosal dissection provides high cure rate of early cancer of the esophagus and stomach cancers with high resection rates monobloc, curative and low complication rates.				</P>
			</div><B>Keywords</B>: ESD, submucosal dissection, early cancer</td>
