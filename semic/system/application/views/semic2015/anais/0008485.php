			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A utilização de injetores de combustíveis nos motores de ignição por centelha, permitiu aumentar a eficiência de combustão pela quebra do combustível líquido em pequenas gotículas.					<B>Objetivo</B>: Neste trabalho utilizou-se a simulação numérica para o estudo da injeção de combustível em um ambiente contendo ar à pressão moderada (condições encontradas no motor).					<B>Metodologia</B>: O código computacional utilizado foi o OpenFoam que utiliza a metodologia de volumes finitos para resolver as equações governantes. Os solvers utilizados foram o sprayFoam para a formação do spray e o chemFoam para a solução da cinética química da combustão. A simulação do spray esta condizente com o que se espera da física de formação das gotas de combustível no interior de um recipiente.					<B>Resultados</B>: Verificou-se a formação da mistura ar-combustível e o início/duração do processo de combustão. A simulação da cinética química também foi satisfatória e permitiu compreender melhor a evolução da temperatura durante a combustão.					<B>Conclusões</B>: Dois mecanismos foram comparados e verificou-se que ambos convergiram para a mesma temperatura adiabática de chama, o que permite dizer que a simulação está coerente.				</P>
			</div><B>Palavras-chave</B>: motor de combustão interna, dinâmica dos fluidos computacional, formação de spray			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The using of fuel injectors in engines of spark ignition allowed  the increasing  of efficiency of combustion through the breaking of liquid fuel into small droplets.					<B>Objectives</B>: In this work the numerical simulation for the fuel injection study in an environment containing air at moderate pressure (conditions found in the IC engine).					<B>Methods</B>: The computer code OpenFoam was used for this process, it uses finite volume method for solving the governing equations. The solvers sprayFoam were used to form the spray and the chemFoam for the solution of chemical kinetics of combustion.					<B>Results</B>: The simulation of this spray consists with what is expected of the physical formation of fuel drops inside a vessel. The formation of air-fuel mixture and the start/delay of the combustion process has been verified. The simulation of chemical kinetics was also satisfactory and allowed a better understanding the evolution of the temperature during combustion.					<B>Conclusion</B>: Two mechanisms were compared and it was found that both converge to the same adiabatic flame temperature, which allows to say that the simulation is consistent.				</P>
			</div><B>Keywords</B>: internal combustion engine, computational fluid dynamics</td>
