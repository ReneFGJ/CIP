					<B>Introduction</B>: The relations of production, consumption and interaction between food and society provides reflections on the food insertion into the family routine and special character as a female element, both with regard to the cultivation and especially their preparation, care and presentation.					<B>Objectives</B>: The main objective of this study is to join the culinary universe, and of the feminine space, private, from the systematization of revenue notebooks that belonged to a family group of Ponta Grossa region of the first half of the twentieth century. And as specific objectives, scan the recipes contained in the sources and systematize the recipes contained in cooking books, sources of this research.					<B>Methods</B>: Assuming that the kitchen keeps revealed gustatory memory through recipes, with their respective location dimensions, regional, national and international, this research has the historical period between the years 1900 and 1950 and is the primary source of revenue notebooks belonging to a group of women, mother and her four daughters living in the city of Ponta Grossa in the state of Paraná.					<B>Results</B>: 					<B>Conclusion</B>: 				</P>
