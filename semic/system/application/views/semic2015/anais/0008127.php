			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: As relações de produção, consumo e de interação entre alimento e sociedade, proporciona reflexões sobre a inserção do alimento no cotidiano familiar e em especial como elemento de caráter feminino, tanto no que diz respeito ao cultivo e especialmente sua preparação, cuidado e apresentação.					<B>Objetivo</B>: O objetivo principal desse estudo é o de ingressar no universo da culinária, bem como do espaço do feminino, do privado, a partir da digitalização e sistematização de cadernos de receitas que pertenceram a um grupo familiar da região de Ponta Grossa da primeira metade do século XX. E como objetivos específicos, digitalizar as receitas contidas nas fontes e sistematizar as receitas contidas nos cadernos de culinária, fontes dessa pesquisa.					<B>Metodologia</B>: Partindo do pressuposto que a cozinha guarda a memória gustativa revelada através das receitas, com as suas respectivas dimensões local, regional, nacional e internacional, a presente pesquisa tem como recorte histórico os anos entre 1900 e 1950 e tem como fonte primária os cadernos de receitas pertencentes a um grupo de mulheres, mãe e suas 4 filhas, residentes na cidade de Ponta Grossa no estado do Paraná.					<B>Resultados</B>: 					<B>Conclusões</B>: 				</P>
