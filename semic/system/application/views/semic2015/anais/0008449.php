			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Domos geodésicos são estruturas de coberturas em formato esférico geradas a partir de sólidos platônicos. Por serem muito leves e práticos, são apropriados para cobrirem grandes vãos sem apoios intermediários, como em estádios, ginásios e tanques de armazenamento de combustíveis. Os domos são constituídos por barras retas conectadas entre si por meio de nós formando uma sucessão de elementos triangulares que cobrem total ou parcialmente a superfície esférica. A localização exata dos nós sobre a superfície é essencial para a geração de modelos computacionais de análise estrutural baseados no Método dos Elementos Finitos.					<B>Objetivo</B>: O presente trabalho visa a geração dos elementos de um domo geodésico, localizando os nós sobre a superfície da esfera.					<B>Metodologia</B>: Para a geração dos nós, parte-se de figuras de sólidos platônicos, que são aqueles cujas faces são iguais e cujos ângulos entre as faces correspondem a submúltiplos de 360º. Dentre os diferentes sólidos platônicos, é dado um destaque para o icosaedro, com 12 faces iguais. Fazendo-se algumas subdivisões nas faces do sólido, obtêm-se nós intermediários. Supondo o sólido inscrito no círculo mínimo, a partir do centro deste são lançados vetores nas direções radiais, que passam por todos os nós gerados, até tocarem na superfície da esfera, definindo-se os nós de ligação das barras. Conectando-se os nós, gera-se o domo geodésico.					<B>Resultados</B>: 					<B>Conclusões</B>: O trabalho mostra como se realiza essa geração e apresenta resultados para algumas situações.				</P>
			</div><B>Palavras-chave</B>: Domo geodésico. Sólidos Platônicos. Coberturas. Elementos Finitos			<BR>
