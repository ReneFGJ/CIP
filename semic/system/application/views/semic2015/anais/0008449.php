			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Geodesic domes are roofing structures in spherical shape generated from platonic solids. For being very light and practical, they are suitable to cover large spans without intermediate supports, such as stadiums, gymnasiums and fuel storage tanks. The domes are formed by straight bars connected together by nodes forming a succession of triangular elements that cover all or part spherical surface. The exact location of the knots on the surface is essential for generating computational models based on structural analysis of the Finite Element Method.					<B>Objectives</B>: This study aims at generating element of a geodesic dome, locating the nodes on the surface of the sphere.					<B>Methods</B>: For the generation of geodesic domes, begins with platonic solid figures, which are those whose faces are equal and whose angles between the faces correspond to submultiples of 360 °. Among the different platonic solids, it is given a highlight for the icosahedron, with 12 equal faces. Making some subdivisions in solid faces, they obtain intermediate nodes. Assuming the solid enrolled at least circle from the center of this, some vectors are launched in the radial direction, passing by all nodes generated until they touch the surface of the sphere, setting up the interchanges of the bars. Connecting nodes, it generates the geodesic dome.					<B>Results</B>: 					<B>Conclusion</B>: The work shows how this generation takes place and gives scores for some situations.				</P>
