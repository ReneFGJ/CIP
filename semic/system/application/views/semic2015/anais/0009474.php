			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O Design de Interação é uma área especializada no projeto, pesquisa e debate em torno das interações humanas mediadas por artefatos computacionais. Apesar de ainda ser uma área emergente, a discussão em torno de seus fundamentos teóricos abrange diversas teorias. Algumas propostas de fundamentos teóricos, como o da amanualidade, possuem bases fenomenológicos-existenciais e materialistas-dialéticas. Estes são temas complexos e ainda carecem de materiais que auxiliem seu debate, tanto na pesquisa quanto na educação.					<B>Objetivo</B>: Esse projeto teve como objetivo produzir uma coleção de Recursos Educacionais Abertos (REA) voltados para ensino, aprendizagem e pesquisa sobre o conceito de amanualidade, aplicado a área do Design de Interação.					<B>Metodologia</B>: Foi realizada uma revisão literária sobre os REA, sobre as ideias de Álvaro Vieira Pinto e do conceito de amanualidade utilizado no Design de Interação. Em seguida, foi preparado um planejamento visual e de conteúdo para cada recurso.					<B>Resultados</B>: Foram desenvolvidas digitalmente cinco imagens, uma tabela e uma apresentação de slides. O desenvolvimento dos recursos foi documentado, assim como o processo de produção e a disponibilização para consulta, uso ou edição pela comunidade interessada, através de uma plataforma colaborativa de projetos abertos.					<B>Conclusões</B>: A coleção de REA sobre amanualidade também foi disponibilizada em um repositório de acesso aberto, com todos os arquivos disponíveis em formato aberto e com licença pública de uso, por Creative Commons.				</P>
			</div><B>Palavras-chave</B>: Recursos Educacionais Abertos. Design de Interação. Álvaro Vieira Pinto. Fundamentos teóricos			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The Interaction Design is an area specialized in the design, research and debate on human interactions mediated by computational artifacts. Although still an emerging area, the discussion on its theoretical foundations includes several theories. Some proposals of theoretical foundations, such as handiness (“amanualidade”), has phenomenological-existential and materialistic-dialectical bases. These are complex subjects and require materials that help your debate both in research and in education.					<B>Objectives</B>: The project’s objective was to produce a collection of Open Educational Resources (OER) focused on teaching, learning and research on the concept of handiness, applied to Interaction Design area.					<B>Methods</B>: A literature review was undertaken, about the OER, the ideas of Alvaro Vieira Pinto and the concept of handiness used in Interaction Design. Then it was prepared a visual planning and content for each feature.					<B>Results</B>: Five images, a table and a slide show were developed. The development of the resources have been documented, even as the production process and making available for viewing, use, or edit the interested community through a collaborative platform of open projects.					<B>Conclusion</B>: The OER collection on handiness was also available in an open access repository, with all files available in an open format and with public license to use, by Creative Commons.				</P>
