					<B>Introduction</B>: Mostly by international journals and documents, Curitiba is repeatdly cited as a model for urban planning and public transportation. How much of that is the result of outside interest or of local efforts to market the city is not the focus of h=the research. We try to understand how far ideas developed or implemented here were replicated abroad and what the factors that contribute more for this are. Part of this discussion are the characteristics of the practice of planning, implementing and monitoring transport services that may influence on the way these very same practices are reproduced in different places.					<B>Objectives</B>: Evaluate, limited to to scope of the research, how ideas on urban transportation developed or implemented in Curitiba were transfered to other cities in Brazil and the world. Focus is given to the different roles institutions and professionals play in these processes.					<B>Methods</B>: Secondary review of selected articles published by distinguished academic journals, documents issued by United Nations and the World Bank.					<B>Results</B>: 					<B>Conclusion</B>: 				</P>
