			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A presente pesquisa agrega informações e debate sobre as relações que a cidade de Curitiba estabelece com outras, nacionais e estrangeiras, por meio de projetos, ideias e intervenções relativas ao transporte coletivo, sugerindo uma discussão sob o espectro da transferência de ideias, identificando seus agentes e possíveis causas de difusão e de assimilação desses mesmos projetos, ideias e intervenções. A análise desenvolve-se primeiramente com a contextualização de como se dão as relações de transferência e quais os posicionamentos assumidos pela cidade de origem e pelas cidades que demonstram interesse em assimilar fatores externos em seu planejamento e gestão urbanos. A especificidade desta pesquisa PIBIC, integrante de uma outra, mais ampla, está no estudo das condições estabelecidas para o caso do setorial de transporte coletivo, o qual se mostra com melhores chances de replicabilidade que outros como habitação, saúde, cultura, dentre outros. No caso de Curitiba, tal especificidade ainda conta com outros potenciais que valorizam ainda mais esse setorial que são as inovações no cenário mundial no que diz respeito às soluções de mobilidade urbana. A partir de então  toma-se Curitiba como referência por algumas de suas práticas até que essas sejam aprimoradas e adaptadas tanto em território nacional, como aconteceu nas maiores capitais brasileiras, como internacionalmente: num recorte de aproximadamente 40 anos, desde que o sistema de transporte surgiu, esse modelo está presente, parcial ou integralmente, com maior ou menor influência de Curitiba, em quase 200 cidades do mundo.					<B>Objetivo</B>: Avaliar, no recorte da pesquisa, como ideias de transporte urbano aplicadas em Curitiba foram transferidas para outras cidades do Brasil e do mundo, destacando o papel de instituições e de profissionais nesse processo.					<B>Metodologia</B>: Leitura de material secundário - artigos e documentos - selecionado por meio do uso de palavra chave (Curi