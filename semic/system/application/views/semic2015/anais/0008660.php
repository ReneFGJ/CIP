			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The hygiene and microbiological control of milk and dairy products, are mainly intended to ensure the safety. The microbial load of fresh milk is extremely important in the final quality of dairy products. A pasteurization ensures the elimination of pathogenic microorganisms, and ultra high temperature eliminates virtually all microorganisms					<B>Objectives</B>: The aim of the study was analysis of the profiles of Bacterial Count standards Total fresh milk, pasteurized and UHT commercial, subjected to different temperatures, for comparison of the profiles in the tested temperatures					<B>Methods</B>: We used 10 samples of each milk type totaling 30 samples. It was later performed the microbiological analysis by counting the colonies of total mesophilic or Total Bacterial Count (TBC) held by instillation of the samples in duplicate on agar medium to count (PCA, Oxoid®) in Spread Plate technique incubated at 37 C for 24 to 48 hours. The calculation of CBT was performed in a digital counter colonies on plates with more than 25 colonies identified in all samples collected					<B>Results</B>: The result was that, in all types milk, the influence of temperature was deleterious for bacteria count, with temperatures between 60 and 70 ° C were removed entirely crops					<B>Conclusion</B>: It is concluded that the application of thermal processes in dairy is effective for the elimination of microorganisms, however, when the initial count is high, the application of gradual temperature can hamper the deleterious effects of temperature on the product itself				</P>
