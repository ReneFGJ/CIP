			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A higiene e o controle microbiológico do leite e seus derivados, têm como principal objetivo assegurar a inocuidade. A carga microbiana do leite [e]lt;i[e]rt;in natura[e]lt;/i[e]rt; é de extrema importância na qualidade final dos produtos lácteos. A pasteurização garante a eliminação de microrganismos patogênicos, e a ultra alta temperatura elimina praticamente todos os microrganismos					<B>Objetivo</B>: O objetivo do trabalho foi realizar análises dos perfis dos padrões de Contagem Bacteriana Total de leite [e]lt;i[e]rt;in natura[e]lt;/i[e]rt;, pasteurizado e UHT comercial, submetidos a diferentes temperaturas, para posterior comparação dos perfis nas temperaturas testadas.					<B>Metodologia</B>: Foram utilizadas 10 amostras de cada tipo lácteo, totalizando 30 amostras. Posteriormente foi realizado a análise microbiológica por contagem das colônias de mesófilos totais ou Contagem Bacteriana Total (CBT), realizada pela instilação das amostras em duplicatas em meio ágar para contagem (PCA, Oxoid®), na técnica Spread Plate incubadas à 37o C por 24 a 48 horas. O cálculo da CBT foi realizado em contador de colônias digital, em placas com mais de 25 colônias identificadas, em todas as amostras coletadas.					<B>Resultados</B>: Como resultado obteve que em todos os tipos lácteos, a influência da temperatura foi deletéria para a contagem bacteriana, sendo que as temperaturas entre 60 e 70º C foram as que eliminaram totalmente os cultivos.					<B>Conclusões</B>: Conclui-se que a aplicação de processos térmicos em lácteos é eficaz para a eliminação de microrganismos, entretanto, quando a contagem inicial é elevada, a aplicação de temperatura gradativa pode dificultar a ação deletéria da própria temperatura no produto.				</P>
