			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O envelhecimento populacional é um fenômeno incontestável, caracterizado pelo aumento das doenças crônico-degenerativas e alto consumo de medicamentos. Embora as interações medicamentosas sejam frequentes em idosos, existem carências de estudos fármaco-epidemiológicos sobre a sua prevalência e principalmente os fatores associados à sua ocorrência.					<B>Objetivo</B>: Desta forma, este estudo objetiva caracterizar o perfil de interações medicamentosas e fatores relacionados em idosos atendidos nas Unidades Básicas de Saúde.					<B>Metodologia</B>: Neste estudo transversal, descritivo e observacional, foram entrevistados 492 idosos, levantando dados sobre características demográficas, presença de comorbidades e consumo de medicamentos, para posterior análise das interações medicamentosas potenciais, usando o Drug Interaction Checker.					<B>Resultados</B>: Observou-se alta prevalência da polifarmácia e multimorbidades nesta população de estudo,  especialmente em mulheres (Teste do Qui Quadrado, p[e]lt;0,05). Destaca-se alta prevalência de utilização de medicamentos potencialmente inapropriados e potenciais interações medicamentosas, as quais são mais significantes nos indivíduos com multimorbidades e polimedicados (Teste do Qui Quadrado, p[e]lt;0,05).					<B>Conclusões</B>: A alta prevalência do consumo de medicamentos inapropriados por idosos e potenciais interações medicamentosas, especialmente em mulheres, aponta para a necessidade de políticas públicas direcionadas a esta população.				</P>
			</div><B>Palavras-chave</B>: Polifarmácia. Interação medicamentosa. Idoso.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Population aging is a global phenomenon, which has determined a change in the demographic and morbidity, resulting in a proportional increase in chronic diseases and use of medications. Although drug interactions are frequent in the elderly, there are shortages of drug-epidemiological studies on the prevalence and especially the factors associated with its occurrence.					<B>Objectives</B>: This study aimed to characterize the profile of drug interactions and related factors in elderly assisted in Basic Health Units.					<B>Methods</B>: In this cross-sectional, descriptive and observational study, we interviewed 492 elderly, collecting data on demographic characteristics, comorbidities and drug consumption for analysis of potential drug interactions, using the Drug Interaction Checker.					<B>Results</B>: There was a high prevalence of polypharmacy and multimorbidity in this population, especially in women (Chi square test, p [e]lt;0.05). It highlights a high prevalence of use of potentially inappropriate medications and potential drug interactions, which are more significant in women and were not associated with age (chi square test, p [e]lt;0.05).					<B>Conclusion</B>: The high prevalence of inappropriate drugs for the elderly and potential drug interactions, especially in women, points out the need for public policies considering drugs’ prescription  for this population.				</P>
			</div><B>Keywords</B>: Drug interaction. Elderly. Polypharmacy.</td>
