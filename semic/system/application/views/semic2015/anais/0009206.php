			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A nicotina, princípio ativo do tabaco pode provocar efeitos deletérios ao tecido ósseo, a movimentação ortodôntica está relacionada à remodelação controlada e sequencial por ativação da reabsorção óssea, estudos observaram que a nicotina acelera a movimentação dentária podendo causar efeito direto sobre o metabolismo ósseo.					<B>Objetivo</B>: Considerando a população de tabagistas e a influência do tabaco no metabolismo ósseo este estudo tem como objetivo avaliar as alterações em ratos sob administração de nicotina durante o movimento ortodôntico por meio da análise da expressão do colágeno no osso alveolar.					<B>Metodologia</B>: Foram analisados 96 ratos Wistar divididos em 2 grupos sem movimentação dentária: N-Nicotina e S-Soro Fisiológico e 2 grupos com movimentação dentária: NM-Nicotina e SM-Soro Fisiológico. As substâncias foram administradas por 30 dias e no 30º dia instalou-se dispositivo para induzir movimento dentário. Os ratos foram eutanasiados no 32°, 44° e 58° dia. A expressão do colágeno mensurada com microscopia de polarização coletando porcentagem de fibras colágenas do tipo I no osso alveolar sobre o lado de tração					<B>Resultados</B>: Na comparação entre grupos NM e SM; N e S, independente de tempo, não foi encontrada diferença estatisticamente significante (p[e]rt;0,05). Comparando os mesmos na relação grupo x tempo foi observado que os grupos NM e SM tiveram uma média de porcentagem de colágeno menor quando comparados a N e S (p[e]rt;0,05). No 58º dia o grupo NM apresentou a menor média de porcentagem de colágeno tipo I.					<B>Conclusões</B>: A nicotina não influenciou a expressão do colágeno em ratos submetidos a movimentação dentária.				</P>
			</div><B>Palavras-chave</B>: Nicotina, Ortodontia, neoformação óssea			<BR>
