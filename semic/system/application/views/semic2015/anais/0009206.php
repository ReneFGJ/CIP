			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Nicotine, the active ingredient tobacco can cause deleterious effects on bone tissue, orthodontic movement is related to the controlled remodeling and sequential activation of bone resorption, studies have found that nicotine accelerates tooth movement may cause direct effect on bone metabolism					<B>Objectives</B>: Considering the population of smokers and the influence of tobacco on bone metabolism this study aims to evaluate the changes in rats under administration of nicotine during orthodontic movement through analysis of collagen expression in alveolar bone.					<B>Methods</B>: We studied 96 Wistar rats divided into 2 groups without tooth movement: N-Nicotine and S-Saline and 2 groups with tooth movement: NM-Nicotine and SM-Saline. The substances were administered for 30 days and on the 30th day settled device to induce tooth movement. The mice were euthanized at 32 °, 44 ° and 58 ° day. The collagen expression measured with polarizing microscopy-collecting percentage of collagen fibers type I alveolar bone on the side of traction.er for each factor and the interaction group x time (Power analysis).					<B>Results</B>: Comparing NM and SM groups; N and S, regardless of time, there was no statistically significant difference (p[e]rt; 0.05). Comparing the same ratio in group x time it was observed that the NM, and SM groups had lower average percent collagen compared to N and S (p[e]rt; 0.05). After 58 days the NM group had the lowest average percentage of collagen type I.					<B>Conclusion</B>: Nicotine did not influence the collagen expression in rats submitted to tooth movement.				</P>
