			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A Enfermagem é essencialmente, uma atividade profissional e humana no âmbito do cuidado na saúde. Possui especificidades próprias e distintas, destacando-se pela sua insubstituível singularidade de caráter humanitário. Sabe-se que o ato de humanizar passa pela total valorização do ser humano, manifestando absoluto respeito pela sua individualidade e autonomia.					<B>Objetivo</B>: O objetivo deste trabalho é revalorizar a integração, tanto da Bioética, da Teologia e da Espiritualidade, como às outras ciências humanitárias, no contexto atual da Enfermagem, em resposta a perspectiva dualista do contexto biotecnocientífico/biotecnológico.					<B>Metodologia</B>: A metodologia deste projeto de pesquisa é qualitativo-bibliográfico.					<B>Resultados</B>: A fragmentação do conhecimento omitiu a noção de comunidade humana solidária, permitindo uma visão reducionista e mecanicista do ser humano, desconsiderando os aspectos emocionais, valores, crenças até a própria história da pessoa enferma. Lamentavelmente, cultiva-se o modelo educacional cativo em parâmetros como: eficácia, rentabilidade econômica, esmero na formação técnica, e se subestima a educação em valores éticos, e na formação de caráter. Sabe-se que a tecnologia deve ajudar com seu poder qualitativo, favorecendo o cuidado dos pacientes, mas não pode se tornar o objetivo final.					<B>Conclusões</B>: Tanto a Bioética como a Espiritualidade, passa pelo caminho da humanização no processo do cuidar, transformando-se numa ação solidária que melhora a comunicação e o processo de interação com o meio ambiente. Já a Espiritualidade revela um valor inigualável, insubstituível e transcendente, que perpassando pela vulnerabilidade, sofrimento e dor, pode se transformar em ação catalizadora e libertadora em procura do sentido da vida.				</P>
			</div><B>Palavras-chave</B>: Revelação. Humanização. Profissionais de Enfermagem. Espiritualidade. Cuidar.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Nursing is essentially a professional and human activity in the health care. Has own distinctive characteristics, noted for his humanitarian irreplaceable uniqueness. It is known that the act of humanizing passes by the total value of the human being, showing absolute respect for their individuality and autonomy.					<B>Objectives</B>: The objective is to revalue the integration, both of Bioethics, Theology and Spirituality as other humanitarian sciences in the current context of nursing, in response to dualistic perspective of biotechnoscientific / biotech context.					<B>Methods</B>: The methodology of this research project is qualitative-bibliographical.					<B>Results</B>: The fragmentation of knowledge omitted joint and several human community, allowing a reductionist and mechanistic view of the human being, disregarding the emotional aspects, values, beliefs until the very history of the sick person. Unfortunately, cultivates the captive educational model on parameters such as: effectiveness, economic profitability, the nines in technical training, and underestimates education in ethical values, and character formation. It is known that the technology should help with its qualitative power, favoring the care of patients, but can not become the ultimate goal.					<B>Conclusion</B>: Both Bioethics and Spirituality, passes the path of humanization in the care process, becoming a joint action that improves communication and the process of interaction with the environment. Spirituality already reveals an unmatched value, irreplaceable and transcendent, which traversing by the vulnerability, suffering and pain, can become a catalyst and liberating action in search of the meaning of life.				</P>
			</div><B>Keywords</B>: Revelation. Humanization. Nursing Professionals Spirituality Care.</td>
