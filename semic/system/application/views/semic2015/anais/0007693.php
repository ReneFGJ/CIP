			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Desde a promulgação da Lei de Diretrizes e Base da Educação Nacional (9394/96), houve a busca pela escolarização deste componente curricular, motivando pesquisas por instituições de ensino superior. Nesta feita se propõe um trabalho sobre a produção de quatorze anos, cujas publicações, pesquisas e divulgação da disciplina, visando as práticas da disciplina que formalmente escolarizada, se viu influenciada pela produção dos livros didáticos. Do material didático trabalhado pelos membros do GPER – Grupo de Pesquisa Educação e Religião (DB. gper.com. BR).					<B>Objetivo</B>: com a consequente organização de uma base de dados, disponível em meio virtual, se buscou compreender a estrutura dos livros didáticos, entre os impressos na primeira década do Século XXI, quanto a tipologia de exercícios e imagens.					<B>Metodologia</B>: Esta pesquisa qualitativa do tipo “estado do conhecimento” procurou analisar a produção de um tema organizado em um período estabelecido de tempo, contribuindo para revelar temáticas e metodologias, que podem oferecer elementos para aprimorar a pesquisa neste campo do saber.					<B>Resultados</B>: . Neste estudo foi realizado o mapeamento de exercícios e imagens de livros didáticos brasileiros produzidos no período entre 2001 a 2010. Visando verificar as mudanças culturais nos temas priorizados, emergentes e silenciados, assim como nas tendências teóricas e metodológicas. Este processo foi realizado inicialmente articulando o referencial teórico e posteriormente a identificação dos textos e imagens.					<B>Conclusões</B>: O resultado foi à verificação de que os textos propostos buscam essencialmente explicitar conceitos desenvolvidos nas unidades didáticas. Desta forma o material didático carece de estratégias que favoreçam uma reflexão mais ampla dos temas abordados.				</P>
