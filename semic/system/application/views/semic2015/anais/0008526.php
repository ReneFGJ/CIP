			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O homem presente no mundo moderno é um enorme produtor de Resíduos Sólidos, isto é, produtor de “lixo”. A produção de resíduos sólidos per capita pelos brasileiros está estimada em cerca de 1,4 kg por dia, conforme dados do Instituto Brasileiro de Geografia e Estatística - IBGE e da Associação Brasileira de Empresas de Limpeza Pública e Resíduos Especiais - ABRELPE.					<B>Objetivo</B>: O alvo da presente pesquisa foi identificar a legislação existente no município de Toledo-PR, assim como, as políticas públicas efetivamente implantadas para o controle do manuseio dos resíduos sólidos por parte dos poderes público municipais.					<B>Metodologia</B>: : Os principais formas de pesquisa foram através do estudo de bibliografias, tais como livros, artigos e leis, com a utilização do método lógico-dedutivo para a compreensão de conceitos, nomenclaturas, causas e características da geração e dos resíduos sólidos urbanos.					<B>Resultados</B>: Toledo está situado na região oeste do estado do Paraná, tendo como enfoque a produção agrícola (soja e milho) e agropecuária (suinocultura), contando também com um setor industrial consolidado. Políticas públicas como o Programa Lixo Útil que busca o desenvolvimento sustentável de Toledo, através da coleta seletiva e da reciclagem, resultam na melhoria de vida da população, no aumento da vida útil do aterro sanitário, na maior limpeza do município, e inúmeros outros benefícios. Surge nesse meio, os consórcios intermunicipais, como uma alternativa de maior viabilização econômica de medidas socioambientais, uma visão consolidada pela Lei 12.305/2010.					<B>Conclusões</B>: A constante produção de resíduos, é resultado do crescimento das populações urbanas, das melhores condições econômicas e de um intenso processo de industrialização da sociedade, e para Toledo não é diferente.				</P>
			</div><B>Palavras-chave</B>: Gerenciamento de resíduos sólidos. políticas públicas. município de toledo.			<BR>
