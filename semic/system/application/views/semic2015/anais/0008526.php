			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: This man in the modern world is a huge producer of Solid Waste, that is, producer of "garbage". The production of solid waste per capita by Brazilian is estimated at about 1.4 kg per day, according to data from the Brazilian Institute of Geography and Statistics - IBGE and the Brazilian Association of Public Cleaning Companies and Special Waste - ABRELPE.					<B>Objectives</B>: The aim of this research was to identify existing legislation in the city of Toledo-PR, as well as public policies effectively implemented to control the handling of solid waste by municipal public powers.					<B>Methods</B>: The main forms of research were through the study of bibliographies such as books, articles and laws, using the logical-deductive method for the understanding of concepts, classifications, causes and characteristics of generation and municipal solid waste.					<B>Results</B>: Toledo is located in western Parana State, with focus on agricultural production (soy and corn) and agriculture (swine), also relying on a consolidated industrial sector. Public policies such as the Business Waste Program that seeks sustainable development of Toledo, through the selective collection and recycling, resulting in improving the lives of the population, increased life of the landfill, most cleaning the city, and numerous other benefits . Arises in the interim, the consortiums, as an alternative of greater economic feasibility of environmental measures, a consolidated view by Law 12.305 / 2010.					<B>Conclusion</B>: The constant production of waste, is a result of growth of urban populations, the best economic conditions and an intense process of industrialization of society, and Toledo is no different.				</P>
