			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A dignidade é garantida a todos os sujeitos por meio dos Direitos Humanos, contudo, nem todos têm acesso a esse direito inerente à existência humana.					<B>Objetivo</B>: O presente trabalho tem como objetivo verificar o senso de justiça, com base na tipologia kohlberguiana, de sujeitos ouvintes, e de surdos usuários de Libras.					<B>Metodologia</B>: Participaram 30 jovens-adultos (faixa etária de 17 a 38 anos) sendo 15 ouvintes e 15 surdos, trabalhadores do setor privado. Os dados foram coletados individualmente utilizando dois dilemas, um hipotético e um da vida real.					<B>Resultados</B>: Os resultados foram categorizados segundo a tipologia de Kohlberg, e, em ambos os grupos o estágio do desenvolvimento moral identificado variou entre o 4 (Orientação para a lei e a ordem) e o 5 (Orientação para o contrato social). Havendo contudo, uma diferença nas respostas em função da faixa etária, da idade em que o surdo aprendeu Libras e da metodologia da escola em que os sujeitos surdos estudaram.					<B>Conclusões</B>: Hipotetiza-se a influência da mídia no julgamento moral dos participantes, e os resultados estão de acordo com a proposta da Educação Bilíngue para os surdos.				</P>
			</div><B>Palavras-chave</B>: Cultura surda. surdez. desenvolvimento moral.  kolhberg. dignidade humana. direitos humanos.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Dignity is assured to everyone on Human Rights agreements. However, not every person has access to this right which is inherent to human existence.					<B>Objectives</B>: The following work aimed at verifying the sense of justice, based on the Kohlbergian typology, of hearing people and deaf people which require the use of sign language.					<B>Methods</B>: 30 young-adults (aged 17 to 38) participated, 15 hearing people and 15 deaf people, private sector workers. The data was collected individually using two dilemmas, one hypothetical and the other, based on real life.					<B>Results</B>: The results were categorized according to Kohlberg typology and, in both groups, the moral development stage identified ranged from 4 (Law And Order Orientation) to 5 (Social Contract Orientation). A difference, however, was present on the answers according to the age of the respondent, the age the deaf people learned the sign language and the methodology used on the school in where the deaf people studied.					<B>Conclusion</B>: One hypothesis raised is that the media influences moral judgement and the results are in accordance with the Bilingual Education proposal for the deaf people.				</P>
			</div><B>Keywords</B>: Deaf culture . deafness. moral development. kohlberg. human dignity. human rights.</td>
