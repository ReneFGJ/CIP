			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A população vem crescendo em taxas altíssimas em todo o planeta. Este crescimento impacta diretamente o meio ambiente, visto que com o aumento da população, vem o grande impacto que esta pressão faz sobre as áreas naturais, tais como exploração das florestas, dos rios e da fauna. Em vista de conhecer o atual estado de uso do solo, o presente trabalho mapeou o uso do solo do município de Fazenda Rio Grande					<B>Objetivo</B>: O mapeamento foi realizado pela técnica da classificação supervisionada sobre imagens do satélite Landsat 8					<B>Metodologia</B>: A imagens foi obtida no site da NASA e foi posteriormente georreferenciada para o sistema de coordenadas UTM e inseridas em um Sistema de Informações Geográficas. Posteriormente foram criadas as amostras de treinamento, resultando em 4 tipologias distintas com suas respectivas áreas.					<B>Resultados</B>: Floresta Ombrófila Mista (3.489 ha), Área Urbana/Solo Exposto (5.877,87 ha), Agropecuária (2.132,12 ha) e Água (0,19 ha)					<B>Conclusões</B>: Os resultados não foram satisfatórios pois devido a grande complexidades dos detalhes do uso do solo da região, o método aplicado confundiu classes, empobrecendo os resultados.				</P>
			</div><B>Palavras-chave</B>: Fazenda Rio Grande. Landsat 8			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The population is growing at very high rates around the world. This growth directly impacts the environment, as with the increase in population comes the great impact that this pressure is on natural areas, such as exploitation of forests, rivers and wildlife. In order to meet the current state of land use,					<B>Objectives</B>: this study mapped land use in the municipality of Fazenda Rio Grande.					<B>Methods</B>: The image was obtained on the NASA website and was later geo-referenced to the UTM coordinate system and inserted into a Geographic Information System. Later the training samples were created, resulting in 4 different types with their respective areas.					<B>Results</B>: Mixed Rain Forest (3,489 ha), Urban Area / Solo Exposed (5877.87 ha), Agriculture (2132.12 ha) and water (0.19 ha).					<B>Conclusion</B>: The results were not satisfactory because due to the great complexity of the details of land use in the region, the method applied confused classes, impoverishing the results.				</P>
