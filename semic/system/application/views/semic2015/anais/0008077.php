			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Os brackets cerâmicos desenvolveram-se para melhorar esteticamente o bracket metálico, mantendo a funcionalidade. Porém, a desvantagem deste material é a baixa resistência às fraturas, devido a cerâmica ser ao mesmo tempo dura e friável.					<B>Objetivo</B>: Avaliar com o método de elementos finitos a distribuição dos campos das tensões no bracket cerâmico monocristalino no incisivos central superior na simulação de cargas proporcionadas por dobras de 3ª ordem (torque ortodôntico).					<B>Metodologia</B>: O bracket cerâmico Clarity™ ADVANCED prescrição MBT .022" para incisivo central superior direito, os segmentos de fios de aço inoxidável com secção retangular .0215" x .027" com cantos arredondados, e .021" x .025" de cantos vivos e a região anterior da maxila foram modelados e exportado para o programa Autodesk Simulation Multiphysics®, para a análise pelo método de elementos finitos					<B>Resultados</B>: A distribuição das tensões ocorreu de modo mais pontual quanto comparada às tensões observadas com o fio retangular .021” x .025” com cantos vivos com maior concentração na região mesial e distal do slot.					<B>Conclusões</B>: Neste modelo tridimensional o fio com cantos vivos apresentou distribuição de tensões mais linear, com concentração de tensões nos corners internos e na região média da parede superior no sentido vestíbulo lingual do slot do bracket. No fio com cantos arredondados a distribuição de tensões foi mais pontual e com maior concentração na região mesial e distal do slot do bracket.				</P>
			</div><B>Palavras-chave</B>: Bracket cerâmico. Incisivo. Torque. Análise de elementos finitos.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The ceramic brackets have been developed to improve aesthetically the metal bracket, while maintaining functionality. However, the disadvantage of this material is low resistance to fractures due to ceramic be both hard and crumbly.					<B>Objectives</B>: to evaluate by means of finite element analysis (FEA) the distribution of tensions fields in the monocrystalline ceramic bracket of the maxillary central incisors by applying a third order bends force.					<B>Methods</B>: The maxillary right central incisor Clarity™ ADVANCED Ceramic Bracket, MBT .022-in prescription, the segments of stainless steel wire .0215 x .027-in with rounded contour, and .021 x .025-in with square contour, and the anterior region of the maxillary arch were modeled on DS Solidworks program® 2013, and exported to the Autodesk Simulation Multiphysics® 2013 program to conduct the FEA.					<B>Results</B>: The stress distribution occurred in a more timely manner as compared to the stress observed with rectangular wire .021 "x .025" with square corners, and with the highest concentration in the mesial and distal region of the slot.					<B>Conclusion</B>: In this 3-D model the wire with square corners developed more linear stress distribution and concentration on the inner corners and in the middle region of the upper wall towards the labiolingual part of the bracket slot. In the rounded corners wire the stress distribution was more punctual and more concentration in the mesial and distal region of the bracket slot.				</P>
			</div><B>Keywords</B>: Ceramic bracket. Incisor. Torque. Finite element analysis</td>
