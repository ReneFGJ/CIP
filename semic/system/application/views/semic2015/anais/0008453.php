			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Antropologia Teologica busca estudar o homem em relaçâo com Deus, ela tem seu fundamento na Revelação, Deus que se revelou ao ser humano, Deus que o criou, a relação do homem com as outras criaturas e o fim último do homem ou seja a morte.  Aqui estão as idagações principais do ser humano: Qual a sua origem e qual o seu fim.					<B>Objetivo</B>: Nós estruturamos o objetivo da nossa pesquisa da seguinte forma: Como Objetivo Geral: O homem como imagem e semelhança de Deus.
1. Analisaremos o significado de imagem e semelhança.
2. Analisaremos homem em colaboração com Deus, que nomeia a criação.
3. Faremos uma reflexão do homem em busca de sua identidade original, ou seja, sendo imagem e semelhança do seu criador.					<B>Metodologia</B>: Como métodologia  adotamos o método analítico, tendo como fonte principal o proprio texto bíblico de gênesis,					<B>Resultados</B>: Partindo dos textos bíblicos do Livro de Gênesis, capitulos 1 e 2, para compreender o significado da imagem e semelhança, o ser humano é criado, modelado segundo a vontade do criador.					<B>Conclusões</B>: Concluimos que a nossa pesquisa foi muito frutuosa, já que durante a pesquisa a tema do homem como imagem e semelhança de Deus foi nos envolvendo, é um tema muito rico, gostariamos de continuar nesta pesquisa partindo para o campo da Cristologia, estudando Jesus como imagem perfeita do ser humano, aquilo que Deus projetou para o ser humano aconteceu por excelencia do seu Filho e também Jesus que recupera a dignidade de filho para o seu humano, o estado de graça.				</P>
			</div><B>Palavras-chave</B>: Antropologia Teologica. Homem. Deus.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Theological anthropology seeks to study man in relation with God, it has its basis in Revelation, God who has revealed himself to be human, God who created it, the relationship of man with other creatures and the ultimate end of man that is death.  Here are the main idagações of the human being: How did it get here and what his end.					<B>Objectives</B>: We structure the objective of our research as follows: General Objective: the man as image and likeness of God. 1. We will analyze the meaning of image and likeness. 2. We will look at man in collaboration with God, which names the creation. 3. We´re going to do a reflection of man in search of his original identity, and image of its creator.					<B>Methods</B>: How to adopt the methodology analytic method, having as main source the own biblical text of Genesis.					<B>Results</B>: From the biblical texts of the book of Genesis, chapters 1 and 2, to understand the meaning of the image, the human being is created, modeled according to the will of the creator.					<B>Conclusion</B>: We conclude that our research was very fruitful, since during the research the theme of man as image and likeness of God has been in involving, is a very rich, we would like to continue this research to the field of Christology, studying Jesus as the perfect image of the human being, what God designed for humans because of his son and also honor Jesus that retrieves son´s dignity to your human , the State of grace.				</P>
