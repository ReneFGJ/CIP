			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: It is known that milk is a noble food source of protein, fat and minerals. Can basically be classified into three types as to their state of heat treatment, they are: Raw Milk or In natura that has not undergone any heat treatment, pasteurized milk heats up to 72 ° C for 15 seconds and the ultra high temperature milk (UHT) It is subjected to temperatures ranging 130-150 ° C for a shorter time ranging from 2 to 4 seconds.					<B>Objectives</B>: The aim was to carry out analysis of protein profiles, gifts types of commercial milk, when subjected to different temperatures by SDS electrophoresis technique for comparison of the profiles in the tested temperatures					<B>Methods</B>: 30 liters of milk were used, 10 liters fresh milk, pasteurized and UHT. Each type of milk was subjected to progressive heating process temperatures and collected. After protein extraction was carried out electrophoretic run on polyacrylamide containing SDS at 100V protocol by 50mA. After the gels were stained, destained and analyzed in Image Master VDS, and the data analyzed					<B>Results</B>: The results showed similar behavior among the tested samples, identifying the common electrophoretic profile of the samples because they are the same species					<B>Conclusion</B>: It can be concluded that there was no quantitative protein losses even densitometric differences occurring during the race, which was the structural proteins when the expansion process for denaturing process.				</P>
