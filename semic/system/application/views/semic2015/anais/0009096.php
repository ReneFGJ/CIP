			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: abe-se que o leite é um alimento nobre, fonte de proteínas, gorduras e sais minerais. Pode ser classificado basicamente em três tipos quanto ao seu estado de tratamento térmico, são eles: Leite Cru ou In natura que não sofreu nenhum tratamento térmico, o leite pasteurizado que sofre aquecimento a 72°C por 15 segundos e o Leite Ultrapasteurizado (UHT) é submetido a temperaturas que vão de 130 a 150°C por um tempo menor que varia de 2 a 4 segundos					<B>Objetivo</B>: O objetivo foi realizar análises dos perfis proteicos, presentes tipos de leite comercial, quando submetidos a diferentes temperaturas, através da técnica de Eletroforese SDS, para posterior comparação dos perfis nas temperaturas testadas					<B>Metodologia</B>: Foram utilizados 30 litros de leite, sendo 10 litros leite in natura, pasteurizado e UHT.  Cada tipo de leite foi submetido ao processo de aquecimento em temperaturas progressivas, e coletados. Após extração proteica, foi realizado a corrida eletroforéticas em poliacrilamida, contendo SDS, em protocolo de 100V por 50mA. Após, os géis foram corados, descorados e analisados em Image Master VDS, e os dados obtidos analisados					<B>Resultados</B>: Os resultados obtidos demonstraram comportamento semelhante entre as amostras testadas, identificando o perfil eletroforéticos comum das amostras, por serem da mesma espécie					<B>Conclusões</B>: Pode-se concluir que não houve perdas proteicas quantitativas, mesmo ocorrendo diferenças densitométrica durante as corridas, o que ocorreu pelo processo de expansão estrutural das proteínas quando sobre processo de desnaturação				</P>
			</div><B>Palavras-chave</B>: SDS. Leite In natura. desnaturação			<BR>
