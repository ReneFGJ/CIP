			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A rotatividade dos profissionais de enfermagem é um problema que afeta principalmente a qualidade do atendimento ao paciente. Sendo assim, se justificam esforços no sentido de identificar fatores que promovam uma maior fidelização deste profissional.					<B>Objetivo</B>: Este projeto objetiva a aplicação do processo KDD para apoiar na descoberta de fatores que favoreçam a respectiva fidelização.					<B>Metodologia</B>: Para tal foi construída uma base de dados, a partir da aplicação de um questionário respondido por profissionais da área de enfermagem, que tivessem se desligado da sua atividade nos últimos 12 meses. O formulário foi organizado priorizando além de questões pessoais, perguntas sobre satisfação no trabalho. Para a descoberta de fatores foram adotadas as tarefas de classificação e de descoberta de regras de associação.					<B>Resultados</B>: A partir classificadores foi possível verificar questões relacionadas a como os funcionários são tratados pela chefia, condições de trabalho, uso de equipamentos e salários. As regras de associação mostraram que a honestidade e ética apresentada pelos chefes são de extrema importância no âmbito de trabalho.					<B>Conclusões</B>: Destaca-se também a necessidade que novas propostas de estratégias para facilitar os padrões descobertos pelo processo KDD sejam disponibilizadas para facilitar a sua interpretação.				</P>
			</div><B>Palavras-chave</B>: Mineração de dados. KDD. Profissionais da Enfermagem. Fidelização			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The turnover of nursing professionals is a problem that primarily affects the quality of patient care. So, the efforts are justified to identify factors that promote greater loyalty of this professional.					<B>Objectives</B>: This project objective is that of applying the KDD process to support the discovery of factors which contribute for their loyalty.					<B>Methods</B>: A specific database was built from a questionnaire answered by nursing professionals, which had been dismissed in the last 12 months. The form was organized prioritizing questions about job satisfaction as well as personal issues. For factors discovery the classification and association rules discovery tasks were adopted.					<B>Results</B>: From classifiers, it was possible to verify issues related to how employees to how employees are treaded by management, working conditions, use of equipment and salaries. Association rules showed that honesty and ethics presented by the heads is extremely important in the context of work.					<B>Conclusion</B>: It also highlights the need for new proposals of strategies to facilitate the patterns discovered by the KDD process is made available to facilitate their interpretation.				</P>
			</div><B>Keywords</B>: Data mining. KDD. Nursing Professionals. Loyalty</td>
