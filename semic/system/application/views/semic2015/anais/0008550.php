			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: As ações afirmativas são mecanismos de combate ao desequilíbrio social, com o objetivo principal de proporcionar oportunidades para aqueles que se encontra em determinadas categorias sociais (minorias).
O presente trabalho tem por escopo o estudo acerca dessas políticas igualitárias e suas vertentes históricas e político-filosófica que servem de alicerce para o desenvolvimento dessas medidas.
Neste contexto serão realizados, no desenvolver do trabalho, debates acerca dos princípios sociais, morais e filosóficos contrários e favoráveis à aplicação da medida, bem como será apresentado o contexto histórico para o desenvolvimento das ações afirmativas e aplicabilidade dessa política no Brasil e nos Estados Unidos					<B>Objetivo</B>: O objetivo principal do trabalho consiste nos estudos dos fundamentos político, histórico e filosófico das ações afirmativas, bem como da aplicabilidade dessas medidas no cenário nacional e norte americano.
Sendo assim, serão apresentadas as ideologias que alicerçam os argumentos contrários e a favor da política de ações afirmativas.					<B>Metodologia</B>: Para a elaboração do trabalho, as pesquisas foram realizadas mediante a leitura de diversas obras de conteúdo moral e filosófico acerca do tema, bem como foram utilizados os métodos dialéticos, dedutivos e históricos para a concretização do projeto.					<B>Resultados</B>: 					<B>Conclusões</B>: 				</P>
