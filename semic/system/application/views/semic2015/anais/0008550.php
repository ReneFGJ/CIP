				<P>
					<B>Introduction</B>: Social inequality has always existed in society, but it became a worrying issue after the liberalism breakdown. Under those circumstances, the well fare state was developed, and from there on the state is responsible not only for the economy, but also for all social interaction, aiming for the wellness of the society. 
Therefore, the government originated a series of measures to assure the well fare in the communities. Under this context, the affirmative actions were created in order to reduce inequalities, by offering some benefits to marginalized groups. In face of this privilege offered, some moral issues arisen.
The present work is about the main elements that constitute the affirmative actions. It will be displayed the historical scenario that originated the policy, and will be presented all arguments against and pro for the use of the affirmative actions as a way to decrease inequalities and prejudice. This way, the project enables debates about the philosophies that justify this policy.					<B>Objectives</B>: The main objective of this work is the study about the elements that constitute the affirmative action policy. This way, the project enables reflections about the moral and philosophical background that rings up the policy.
None the less, there will be discussions over the moral, social and philosophical principals that involve the use of affirmative actions, and it will be presented the historical background that gave origin to the policy.					<B>Methods</B>: To achieve the purpose of this project, a series of researches were made in books. The main methods in this work were dialectical, historical and deductive in order to conclude it.					<B>Results</B>: 					<B>Conclusion</B>: 				</P>
