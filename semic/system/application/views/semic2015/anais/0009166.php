			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Esta pesquisa foi desenvolvida para contribuir com os estudos do projeto PELD, na análise do padrão de funcionamento dos ecossistemas em condições naturais, através de cálculo e interpretação da estrutura e da dinâmica das populações vegetais.					<B>Objetivo</B>: O objetivo foi selecionar e ajustar modelos matemáticos para estimar a altura total e comercial das principais espécies ocorrentes em uma floresta natural, em função do diâmetro à altura do peito (DAP), para um remanescente de Floresta Ombrófila Mista Montana, localizada na Fazenda Experimental Gralha Azul no Município de Fazenda Rio Grande no Estado do Paraná.					<B>Metodologia</B>: Para o ajuste das equações foram coletados os dados das quatro espécies (Sassafrás, Erva Mate, Araucária e Cedro).  Foram medidas a altura total e comercial de cada árvore, bem como o seu DAP. Para o ajuste dos dados da altura foram selecionadas e testadas 10 (dez) equações matemáticas, citadas na literatura. Como critério estatístico de seleção das melhores equações foi utilizado o maior coeficiente de determinação ajustado (R2 ajustado), menor erro padrão da estimativa em porcentagem (Syx %) e melhor distribuição dos resíduos.					<B>Resultados</B>: Os resultados e as estatísticas encontradas foram aceitáveis, apesar de algumas equações apresentarem R² baixos e Syx% altos.					<B>Conclusões</B>: O modelo (d) foi o melhor em todos os casos, exceto na estimativa da altura total – Araucária (modelo i).				</P>
			</div><B>Palavras-chave</B>: Floresta natural. Equações. Altura. Espécies.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: This research was developed to contribute to the studies of the PELD project, the operation pattern analysis of ecosystems under natural conditions by calculation and interpretation of the structure and dynamics of plant populations.					<B>Objectives</B>: The objective was to select and adjust mathematical models to estimate the total and commercial height of the main species occurring in a natural forest, according to the diameter at breast height (DAP), for a remnant of rain forest Mista Montana, located in Gralha Azul Experimental Farm in the municipality of Fazenda Rio Grande, state of Paraná.					<B>Methods</B>: For the adjustment of the equation were collected data from four species (Sassafrás, Erva Mate, Araucária and Cedro). Their total and commercial height of each tree as well as your DAP. To adjust the height data were selected and tested ten (10) Mathematical equations described in the literature. As a statistical criterion for selecting the best equation was used the most adjusted coefficient of determination (adjusted R2), the lower standard error of estimate in percentage (Syx%) and better distribution of waste.					<B>Results</B>: The results and statistics found were acceptable, despite some equations have low R² and Syx% higher.					<B>Conclusion</B>: The model (d) was the best in all cases except in the estimation of the total height - Araucaria (model i).				</P>
			</div><B>Keywords</B>: Natural forest. equations. height. species.</td>
