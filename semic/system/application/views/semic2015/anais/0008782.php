			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Com o presente trabalho de pesquisa buscou-se uma compreensão 
sistemática sobre os impactos da cultura pós-moderna, a partir dos conceitos de 
modernidade líquida e hiperconsumo, na constituição da identidade espiritual da 
adolescência. A adolescência é uma fase de transição, mas de forte importância 
na vida humana, pois se firmam valores humanos, éticos e morais que auxiliarão 
no projeto de vida e, consequentemente, na realização pessoal. O adolescente 
conceituado como pós-moderno está envolvido nas culturas líquida e 
hipermoderna. E traz consigo uma série de questionamentos, como também 
uma forte crise de sentido, que se estende aos seus semelhantes e ao mundo. A 
evangelização na Igreja Católica Apostólica Romana se depara com mudanças 
socioculturais relevantes, que influenciam o modo de pensar e viver a fé cristã 
católica, e, não poucas vezes, alteraram os caminhos e distanciam a 
adolescência e juventude do seio religioso institucional.					<B>Objetivo</B>: Compreender a dimensão religiosa no conceito de “modernidade 
líquida” e de “hiperconsumo” no cotidiano da adolescência na Igreja Católica de Cornélio Procópio, PR.  Identificar as transformações sócioculturais da atual sociedade dada como “líquida”, “fragmentada”, “individualista” na estruturação da identidade espiritual na adolescência; Examinar as influências da atual modernidade no incremento da mentalidade religiosa adolescente-juvenil; Refletir sobre o protagonismo religioso dos adolescentes, e consequentemente 
dos jovens, com o auxílio dos aspectos positivos que a modernidade pode ofertar.					<B>Metodologia</B>: Para a realização dessa pesquisa buscamos empregar uma 
metodologia que engloba tanto a investigação bibliográfica como o trabalho de 
campo, no sentido de que visamos uma mútua complementação científica, dos 
dados coletados à contribuição das obras fonte apresentadas como bibliografia.					<B>Resultados</B>: No presente trabalho observamos alguns gráficos que expressam 
um panorama geral da vivência religiosa dos adolescentes da cidade de Cornélio Procópio, PR. Esses gráficos são o resultado de um questionário anônimo realizado em dois colégios e dois grupos de jovens da referida cidade, que reuniram um 
total de 57 entrevistados.					<B>Conclusões</B>: Nesta perspectiva, essa pesquisa aponta possíveis caminhos no 
horizonte pastoral do trabalho com os jovens e adolescentes. A partir dos 
resultados estatísticos da pesquisa obtivemos resultados que revelaram uma 
conformidade da realidade conjuntural da cidade de Cornélio Procópio, com o 
embasamento teórico dos autores que constituem a base bibliográfica dessa 
pesquisa. Essa análise de conjuntura, no possibilitou ver a urgente necessidade 
de ações pastorais que possam ser mais efetivas no processo de formação 
religiosa com a juventude e adolescência. Essas ações pastorais precisam 
ocorrer numa dimensão eclesial e ao mesmo tempo através de um trabalho 
ecumênico.				</P>
