			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O pedilúvio bovino é uma forma de prevenção e tratamento de enfermidades do locomotor distal dos bovinos aplicável ao tratamento individual assim como ao tratamento massal de bovinos. As substâncias atualmente em uso são ou nocivas para a saúde humana ou possuem impacto sobre o meio ambiente.					<B>Objetivo</B>: O objetivo principal do presente estudo foi o desenvolvimento de um modelo de pedilúvio de bancada que permita simular, em pequena escala, a passagem dos animais pelo pedilúvio e pela solução teste, e as condições encontradas comumente a campo.					<B>Metodologia</B>: Através do desenvolvimento de vários croquis e da troca de idéias com profissionais de outras áreas (físicos, engenheiros elétricos e de automação) foi possível obter-se um modelo de pedilúvio de bancada que na sequência deverá passar por testes para a sua validação.					<B>Resultados</B>: As vantagens são sua escala reduzida e o custo menor para a sua utilização em testes [e]lt;I[e]rt;in vitro[e]lt;/I[e]rt;.					<B>Conclusões</B>: Foi possível desenvolver um modelo de pedilúvio de bancada em tempo hábil dentro de um projeto de iniciação científica.				</P>
			</div><B>Palavras-chave</B>: Pedilúvio. Bovino. Soluções. Prevenção. Tratamento			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The cattle footbath is a form of prevention and treatment of diseases of the distal locomotor  system  applicable to individual treatment as well as to mass treatment of cattle. The substances currently in use are either harmful to human health or have impact on the environment.					<B>Objectives</B>: The main objective of this study was the development of a foot bath model that bench to simulate on a small scale, the passage of animals by foot bath and the test solution, and the conditions found commonly in the field.					<B>Methods</B>: By developing various sketches and exchange ideas with professionals from other fields (physicists, electrical engineers and automation) we could built a reduced foot bath model that in the following must pass tests for validation.					<B>Results</B>: The advantages are a reduced scale and lower cost for use in [e]lt;I[e]rt;in vitro[e]lt;/I[e]rt; tests.					<B>Conclusion</B>: It was possible to develop a bench model of cattle footbath in a timely manner within a PIBIT research project.				</P>
			</div><B>Keywords</B>: Footbath. Cattle. Solutions. Prevention. Treatment.</td>
