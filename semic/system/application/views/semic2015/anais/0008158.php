			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A contaminação por hidrocarbonetos é uma ameaça aos ecossistemas aquáticos, superficiais e subterrâneos. A utilização de microrganismos que toleram compostos orgânicos tende a ser uma nova ferramenta no auxílio à descontaminação desses ambientes. Muitos pesquisadores têm-se preocupado com esse problema, empregando técnicas para a descontaminação de áreas degradadas. Os postos de combustíveis normalmente funcionam com instalações antigas, havendo obsolescência de equipamentos acarretando a contaminação do solo, corpos hídricos e lençol freático. A água, uma vez contaminada exige grande investimento para recupera-las e a qualidade para torná-la consumível novamente.					<B>Objetivo</B>: O presente trabalho tem como objetivo avaliar a resistência de organismos após a inserção de hidrocarbonetos policíclicos aromáticos, com intuito de propor um método na descontaminação da água com a utilização de microalgas para degradação dos HPA em meio aquoso.					<B>Metodologia</B>: Para a realização da pesquisa será seguida a proposta:
a) levantar o referencial bibliográfico; 
b) identificar espécies que resistam a degradar os compostos de hidrocarbonetos;
c) analisar o potencial da biorremediação;
d) selecionar microrganismos presentes na natureza; e
e) analisar o crescimento das microalgas em meio de óleo de corte.					<B>Resultados</B>: De acordo com os resultados, o sistema de biorremediação usando microalgas teve conclusões positivas numa observação analítica.					<B>Conclusões</B>: Com as análises mais intensas de cromatografia foi possível determinar o resultado real de mineralização dos HPAs. Também foi possível observar que as espécies de algas são tolerantes à incorporação de hidrocarbonetos no meio, pois nenhuma espécie desapareceu e o crescimento segue ocorrendo.				</P>
			</div><B>Palavras-chave</B>: Biorremediação.  Hidrocarboneto.  Microalgas. HPA.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The hydrocarbon contamination is a threat to the water, surface and underground ecosystems. The use of microorganisms that tolerate organic compounds tends to be a new tool to aid in the remediation of such environments. Many researchers have been concerned with this problem by employing techniques for decontamination of degraded areas. The gas stations typically operate with older installations, with obsolete equipment resulting in the contamination of soil, water bodies and groundwater. The water, once infected requires great investment for them and recover the quality to make it consumable again.					<B>Objectives</B>: The goal of this study aims to evaluate the resistance of organisms to polycyclic aromatic hydrocarbons after insertion, aiming to propose a method in water decontamination using microalgae to degradation of PAHs in water.					<B>Methods</B>: For the research will be followed the proposal:
a) raise the bibliographic reference ;
b) identify species that are resistant to degrade the hydrocarbon compounds ;
c) to analyze the potential of bioremediation ;
d) selecting microorganisms present in nature ; and
e) analyze the growth of microalgae in the midst of cutting oil.					<B>Results</B>: According to the results, the bioremediation system using microalgae had positive findings in an analytical observation.					<B>Conclusion</B>: With the most intense scrutiny chromatography was possible to determine the actual result of mineralization of PAHs. It was also observed that the algal species are tolerant to the incorporation of hydrocarbons in the middle, because no species disappeared and growth follows occurring.				</P>
