			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Hospitais são grandes prestadores de serviços, e como organizações em geral, necessitam estabelecer a forma com que as atividades devem ser executadas para maximizar os resultados e facilitar o andamento de tarefas decorrentes. Para isto, é de suma importância definir processos, ou seja, padronizar a maneira com que as atividades devem ser conduzidas e estabelecer funções e responsáveis para cada etapa do processo.					<B>Objetivo</B>: Identificar e analisar os processos atuais do departamento de Oncologia Clínica do Hospital Erasto Gaertner, localizado em Curitiba, PR.					<B>Metodologia</B>: Foram elaborados modelos do processo que detalham os procedimentos rotineiros dos colaboradores no atendimento dos pacientes, relacionados aos seguintes sub-processos: primeiro atendimento (novos pacientes), consultas de acompanhamento e quimioterapia. Além disso, foram delineadas as rotas executas pelos pacientes, ou seja, o trajeto que o paciente deve percorrer ao receber o atendimento da unidade, com base na formulação do layout atual do hospital, onde estão identificados os locais de uso dos pacientes da Oncologia Clínica.					<B>Resultados</B>: Os modelos de processo obtidos possibilitaram a identificação de oportunidades de melhoria nos aspectos processual e físico, que podem resultar em um melhor uso de recursos, como tempo e espaço disponível para realização do atendimento dos pacientes.					<B>Conclusões</B>: A partir dos resultados do presente projeto, conclui-se que existem alternativas promissoras de melhorias do processo atual do hospital. Tais melhorias, devidamente implementadas, podem diminuir as falhas do processo, e talvez, resultar no aumento do número de tratamentos realizados pela unidade.				</P>
			</div><B>Palavras-chave</B>: Processos da área de saúde. gerenciamento de processos de negócio. modelagem de processos			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Hospitals are major service providers, and how organizations generally need to establish the way in which activities should be performed to maximize the results and facilitate the progress of tasks arising. For this, it is of paramount importance to define processes, ie standardize the way in which activities should be conducted and establish roles and responsible for each step of the process.					<B>Objectives</B>: Identify and analyze the current processes of Clinical Oncology Department of Erastus Gaertner Hospital, located in Curitiba, PR.					<B>Methods</B>: Process models were developed detailing the routine procedures of employees in the care of patients, related to the following sub-processes: primary care (new patients), monitoring and chemotherapy appointments. In addition, the patients were executest routes outlined, namely the path that the patient should go on receiving the service unit, based on the formulation of the current layout of the hospital where the use of local Clinical Oncology Patients are identified .					<B>Results</B>: The obtained process models allowed the identification of opportunities for improvement the procedural and physical aspects of patient handling. Using this models is possible to propose a better use of resources such as time and space available to perform the care of patients.					<B>Conclusion</B>: From the results of this project, it is concluded that there are promising alternatives to the current hospital process improvement. Such improvements, properly implemented, can reduce process failures, and it points out the increasing of the number of treatments performed by the hospital.				</P>
			</div><B>Keywords</B>: Healthcare processes. business processes managament. process modeling</td>
