			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A pesquisa realizada neste trabalho faz parte de um projeto que propõe o desenvolver um equipamento voltado para auxiliar no treinamento de atletas de natação, especificamente nos treinamentos de força e de velocidade, visando alterar minimamente a biomecânica natural dos movimentos dos atletas durante as seções de treino.					<B>Objetivo</B>: Como objetivo geral deste projeto definiu-se o desenvolvimento de um sistema de sincronia de dados e vídeo para apoiar o protótipo de equipamento em desenvolvimento.					<B>Metodologia</B>: Primeiramente foi feita uma revisão bibliográfica a respeito do tema a ser abordado e as possíveis ferramentas e alternativas a serem utilizadas para atingir o objetivo. Para a programação, foi escolhida a plataforma MATLAB por ter sido abordada e estudada durante o curso de graduação, assim apresentando uma maior familiaridade e afinidade. O projeto de pesquisa se dividiu no desenvolvimento de diversos algoritmos com finalidades específicas que, juntos, tem o intuito de realizar a sincronia entre dados e vídeo. Foram criados e testados algoritmos computacionais abordando as ações necessárias para atingir o objetivo do projeto de detectar um flash para sincronia de dados e vídeo, onde os dados são provenientes do sistema de treinamento de força e velocidade e os vídeos de câmeras de uso doméstico.					<B>Resultados</B>: Entre os resultados obtidos destaca-se a importância na otimização do rendimento do algoritmo, pois como o sistema de avaliação biomecânica necessita de uma alta taxa de aquisição de imagens por segundo, o algoritmo deve acompanhar este critério.					<B>Conclusões</B>: Por fim, como trabalhos futuros, sugere-se a integração entre a tecnologia de sincronia de flash e o sistema de treinamento, para que testes mais específicos possam ser feitos com o sistema apresentado neste projeto.				</P>
			</div><B>Palavras-chave</B>: Natação. Biomecânica. Processamento de vídeo. Sincronia de dados e vídeo.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The research carried out in this work is part of masters’ degree project that proposes to develop an equipment to assist coaches in the training of swimming athletes, specifically on strength and speed training, aiming to change minimally the natural biomechanics of the movements during training sessions.					<B>Objectives</B>: The main objective of this work is to develop a synchronization system capable to evaluate data and video in terms of sampling time and synchronize both using a computational algorithm.					<B>Methods</B>: A literature review was conducted as a first step of this project and possible alternatives and tools used to perform the synchronization were evaluated and tested according to the project purpose. The MATLAB platform was chosen as programming platform due the prior knowledge obtained on this programming language during under graduation. The research was divided into the development of several algorithms for specific purposes that together have the intention to perform the sync between data and video. The data set was provided by a system which is under development, where the objective of this prototype in to provide information about strength and velocity of the swimmer during training sessions.					<B>Results</B>: As results highlights the importance in optimizing the performance of the algorithm for biomechanical evaluation system is emphasized due to the high images acquisition rate. In this way, the algorithm should follow this criterion to provide reasonable results for 60 fps (frames per second) videos.					<B>Conclusion</B>: Finally, as further works, the integration between the flash sync technology and the training system in order to perform more specific tests can be made for a better system evaluation.				</P>
			</div><B>Keywords</B>: Swimming. Biomechanics. Video processing. Video and data synchronization.</td>
