			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O presente trabalho é um desdobramento de um projeto maior pertencente à Trilhas Incubadora Social Marista da PUCPR em parceria com a Rede Marista de Solidariedade. Esta pesquisa visa realizar uma revisão teórica no sentido de compreender a contribuição da Psicologia Social para o campo da Economia Solidária, uma estratégia de ação comunitária que surgiu como uma resposta às crescentes consequências negativas provindas do sistema capitalista, como a competição exacerbada, exclusão social e o acúmulo exagerado de riqueza por uma fina camada da sociedade.					<B>Objetivo</B>: Os objetivos dessa pesquisa são identificar e sistematizar as tecnologias sociais utilizadas na prática cotidiana dos Empreendimentos Econômicos Solidários, com ênfase nas práticas da Psicologia, e conhecer as políticas públicas referentes à Economia Solidária no Brasil.					<B>Metodologia</B>: Para realizar o trabalho de revisão realizou-se uma busca na base de dados do Scielo, utilizando o descritor “Economia Solidária”, descritor que foi substituído por “Economia Social” em virtude do insucesso do primeiro tema, após utilização de vários filtros, foram selecionados 21 artigos para análise através do método de Análise de Conteúdo.					<B>Resultados</B>: Dos artigos lidos para o propósito da pesquisa, identificamos que do total 11 artigos mencionavam o uso de tecnologias sociais, 02 artigos citavam em seu conteúdo política pública municipal, estadual ou federal em economia solidária e 14 artigos conceituavam o que é economia solidária, no entanto, muitos desses artigos não eram escritos por psicólogos.					<B>Conclusões</B>: Com a referida pesquisa observou-se que a Psicologia ainda tem muito pouca participação na produção de conhecimento e técnicas de ação relacionados à Economia Social ou Economia Solidária.				</P>
			</div><B>Palavras-chave</B>: Economia Solidária; Psicologia Social; Práticas Psicológicas			<BR>
