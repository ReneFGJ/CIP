			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: This work is a deployment of a larger project of Trilhas Incubadora Social Marista (Social Incubator of PUCPR), in partnership with the Marist Solidarity Network. In order to understand the contribution of Social Psychology to the field of Solidarity Economy, it was made a literature review. The solidarity economy is a community action strategy that has emerged as a response to the growing negative consequences emanating from the capitalist system, such as competition, social exclusion and the excessive accumulation of wealth by upper stratum of society.					<B>Objectives</B>: The objectives were to identify and systematize the social technologies used in everyday practice of Solidarity Economy Enterprises and, focusing on the psychological practices, to know the public policies concerning this strategy in Brazil.					<B>Methods</B>: During the literature review, the descriptor “solidarity economy” had been used to search the subject on the scielo database.  Due to the failure of this descriptor, it was changed to “social economy”. Then, twenty-one articles were selected for analysis based on content analysis method.					<B>Results</B>: We identified that eleven articles mentioned the use of social technologies, two articles mentioned solidarity economy in its municipal, state or federal public policy content and fourteen articles conceptualized what solidarity economy is, however, many of these articles were not written by psychologists.					<B>Conclusion</B>: The results indicated there is a little psychology involvement in the production of knowledge and action techniques related to Social Economy and Solidarity Economy.				</P>
