			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O presente trabalho buscou analisar as concepções das paixões presentes em documentos do século XVII.					<B>Objetivo</B>: O presente trabalho buscou analisar as concepções das paixões presentes em documentos do século XVII.					<B>Metodologia</B>: Utilizou-se o método histórico, fundamentado na leitura e análise de fontes primárias (cartas, sermões e tratados teológicos) e secundárias (textos da tradição clássica e medieval).					<B>Resultados</B>: Discute-se a importância de se realizar estudos deste tipo no âmbito da cultura brasileira, evidenciando a relevância desta abordagem  para que a construção dos saberes e práticas psicológicas no Brasil de hoje aconteça em harmonia com as características peculiares da cultura e sociedade brasileira. Resulta desta investigação, que a elaboração do conhecimento de si parte do pressuposto de que o homem é uma unidade (corpo e alma, razão e fé, sensação e intelecção).					<B>Conclusões</B>: Assim, os sermões, cartas e tratados teológicos fornecem aos historiadores dos saberes psicológicos uma chave para entender o pensamento jesuítico do século XVII sobre o ser humano.				</P>
			</div><B>Palavras-chave</B>: Saberes psicológicos. teologia e psicologia. sermões			<BR>
