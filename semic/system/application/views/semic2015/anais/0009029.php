			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The present study aimed at analyzing the conceptualizations of the passions present in documents a XVII century.					<B>Objectives</B>: The present study aimed at analyzing the conceptualizations of the passions present in documents a XVII century.					<B>Methods</B>: The study used the historical method, based on the reading and analyzing primary sources (sermons, letters and theological treatises) and secondary sources (texts of classic and medieval traditions).					<B>Results</B>: It presents a discussion the importance of this approach for developing psychological knowledge and practice in Brazil that is in harmony with the specific characteristics to the Brazilian culture and society. It results from this inquiry that the elaboration of the experience of knowledge of himself starts from the assumption that man is an unity (body and soul, reason and faith, sensation and intellection).					<B>Conclusion</B>: Thus, sermons, letters and theological treatises gives to Historians of psychological knowledge a key to understand the Jesuit´s way of thinking man on the seventeenth century on the human being.				</P>
