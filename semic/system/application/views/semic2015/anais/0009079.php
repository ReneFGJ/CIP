			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A arbitragem se apresenta como um importante instrumento alternativo para a resolução de conflitos no âmbito do Ordenamento Jurídico brasileiro. A justiça estatal tradicional tem se mostrado muito morosa na entrega de suas decisões, o que não consulta os interesses empresariais.					<B>Objetivo</B>: O objetivo desta pesquisa foi o de estudar soluções alternativas a esta situação e o julgamento arbitral corresponde as expectativas, em especial pelo fato de ser muito mais ágil no seu processamento e julgamento.					<B>Metodologia</B>: O método de pesquisa utilizado foi o dedutivo, a partir da busca de elementos doutrinários e análise de jurisprudências.					<B>Resultados</B>: Como resultado da pesquisa constatou-se uma tendência muito grande na migração da justiça tradicional para a arbitragem, com o aumento do número de julgamento e a acolhida e respeito pelo próprio Judiciário da cláusula que elege o procedimento arbitral em substituição ao processo judicial comum.					<B>Conclusões</B>: Em conclusão, pode-se afirmar que a evolução das relações empresariais alcança agora a escolha da arbitragem como instrumento alternativo ao julgamento estatal.				</P>
			</div><B>Palavras-chave</B>: Direito privado. Arbitragem. Solução alternativa de conflitos empresariais.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Arbitration comes as an important alternative tool for conflict resolution within the Brazilian legal system. The traditional state justice has been very slow in delivering its decisions, which does not refer to business interests.					<B>Objectives</B>: The goal of this research ,was to study alternative solutions to this situation and the arbitration judgment reflects the  expectations, especially due to the fact that  it is much more agile in its processing and judging.					<B>Methods</B>: The research method used, was the  deductive one, starting off by  the search for doctrinal elements and case law analysis.					<B>Results</B>: As a result of the research, very big trend in the migration of traditional justice for arbitration has been found, with the increasing number of judgment and the reception and respect for the judiciary itself of the clause which elects the arbitration procedure to substitute the ordinary legal proceedings.					<B>Conclusion</B>: In conclusion, it can be said, that the development of business relations now reaches the choice of arbitration as an alternative tool to the state trial.				</P>
