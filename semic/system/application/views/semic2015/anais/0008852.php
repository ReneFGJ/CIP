			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Em estudos experimentais em animais após a lesão medular foi necessário as avaliações de suas respostas motoras, assim, foram desenvolvidas escalas para marcar pontuação e interpretar os movimentos realizados após a lesão.					<B>Objetivo</B>: A analise comparativo de escalas que avaliam respostas motoras após a lesão medular se tornou necessária para se obter melhor interpretações e resultados mais precisos.					<B>Metodologia</B>: A escala de Basso, Beattie, Bresnahan (BBB) é uma das escalas mais utilizadas para análise de lesão medular. Outra escala é a escala de Kuhn e Wrathall originalmente usada para se analisar o animal antes da cirurgia e no pós-cirúrgico.					<B>Resultados</B>: Este estudo faz parte de uma pesquisa maior, sendo dependente desta, obtivemos para análise 12 animais para serem avaliados no mesmo tempo de lesão.					<B>Conclusões</B>: Observa-se que a escala BBB é uma escala muito abrangente o que pode dificultar o entendimento e interpretação dos resultados. A escala de Kuhn e Wrathall é de fácil entendimento e objetiva se tornando melhor para avaliação da resposta motora após a lesão medular.				</P>
			</div><B>Palavras-chave</B>: Lesão medular. Escala de Basso. Beattie. Bresnahan. Escala de Kuhn and Wreathall			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: In experimental studies in animals after spinal cord injury was necessary evaluations of their motor responses, thus scoring scales have been developed to score and interpret the movements performed after the injury.					<B>Objectives</B>: The comparative analysis of scales to assess motor responses after spinal cord injury has become necessary to obtain the best performances and more accurate results.					<B>Methods</B>: The scale of Basso, Beattie, Bresnahan (BBB) is one of the most widely used tools for the analysis of spinal cord injury. Another scale is the Kuhn scale and Wrathall originally used to analyze the animal before surgery and after surgery.					<B>Results</B>: This study is part of a larger study, this being dependent, for analysis 12 animals got to be evaluated at the same time of injury.					<B>Conclusion</B>: It is observed that the BBB scale is a very comprehensive range which can hinder the understanding and interpretation of results. The scale of Kuhn and Wrathall is easy to understand and objective becoming better for assessment of motor response after spinal cord injury.				</P>
