			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Peltophorum dubium (Spreng.)Taub, conhecida popularmente como Canafístula, é uma planta utilizada principalmente no controle de processos inflamatórios e distúrbios gastrointestinais.					<B>Objetivo</B>: O objetivo deste estudo foi realizar uma triagem fitoquímica das folhas de Peltophorum dubium (Spreng.)Taub, juntamente com a avaliação do potencial antimicrobiano do extrato e frações das mesmas.					<B>Metodologia</B>: As folhas foram coletadas, secas e reduzida a pó para preparo do extrato aquoso (infusão), que foi liofilizado para então realizar a triagem fitoquímica e a avaliação do potencial antimicrobiano frente bactérias de grande importância clínica.					<B>Resultados</B>: A triagem fitoquímica evidenciou a presença de açúcares redutores, glicerídeos e compostos fenólicos (carboidratos, esteróis e taninos) e a avaliação microbiológica mostrou que tanto o extrato aquoso quanto as frações, não inibiram o desenvolvimento dos microrganismos testados.					<B>Conclusões</B>: A presença de taninos corrobora com o uso popular desta planta para diferentes enfermidades, porém, novos estudos com diferentes formas de extração, partes da planta e/ou concentração fazem-se necessários para justificar seu uso no tratamento de diferentes processos infecciosos.				</P>
			</div><B>Palavras-chave</B>: Canafístula. Avaliação antimicrobiana. Peltophorum dubium (Spreng.) Taub. Plantas medicinais.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Peltophorum dubium (Spreng.) Taub, popularly known as Canafístula, is a plant mainly used in the control of inflammation and gastrointestinal disorders.					<B>Objectives</B>: The aim of this study was to realize a phytochemical screening of the leaves of Peltophorum dubium (Spreng.) Taub along with the evaluation of the antimicrobial potential of the aqueous  extract and  fractions.					<B>Methods</B>: The leaves were collected, dried and reduced to powder for the preparation of the aqueous extract (infusion), which was lyophilized and then perform the phytochemical screening and the evaluation of the antimicrobial potential against bacteria of  clinical importance.					<B>Results</B>: The phytochemical screening showed the presence of reducing sugars, glycerides and phenolic compounds (carbohydrates, sterols and tannins) and microbiological evaluation showed that both the aqueous extract as fractions, not inhibited the development of the tested microorganisms.					<B>Conclusion</B>: The presence of tannins corroborates the popular use of this plant for different diseases, however, further studies with different forms of extraction, plant parts and / or concentration are required to justify its use in the treatment of various infectious processes.				</P>
			</div><B>Keywords</B>: Canafístula. Antimicrobial evaluation. Peltophorum dubium (Spreng.) Taub. Medicinal plants.</td>
