			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Estamos vivendo numa época de avanço técnico e cientifico que promove muitas mudanças o tempo todo, que mal são assimiladas e já se tornam obsoletas. Esses avanços tecnológicos beneficiam e prejudicam a vida do homem, que foi dotado com um poder gigantesco sem ter que pensar em limites éticos.
Hans Jonas nos propõe pensar sobre este problema e discutir esses limites através de suas teses, propor definições para o comportamento humano de forma que o futuro esteja assegurado para os futuros habitantes da Terra.
	Logo, essa pesquisa tem como pretensão a apresentação e análise do pensamento de Hans Jonas, através de suas principais obras, acerca da ética, da técnica e de suas consequências para a natureza e para vida futura.					<B>Objetivo</B>: O presente projeto de pesquisa está inserido em uma pesquisa do Grupo de Estudos, na PUCPR, chamado Ética, Técnica e Natureza em Hans Jonas, e que na proposta do Professor Orientador da presente pesquisa, tem como objetivo geral, analisar a insuficiência das éticas tradicionais, na perspectiva de Hans Jonas, frente aos desafios da civilização tecnológica, para encontrar os fundamentos para um novo Ethos. 
Deste modo inserido em uma pesquisa mais abrangente, apresentam-se aqui, como objetivos específicos os seguintes encaminhamentos. 

1) Analisar porque as éticas tradicionais tornaram-se insuficientes para a civilização tecnológica. 
2) Estudar quais seriam as novas bases para a ética na civilização tecnológica.					<B>Metodologia</B>: Essa pesquisa foi desenvolvida a partir da leitura de autores e comentadores de Hans Jonas. Inicialmente foi feita a leitura e fichamento do livro O Princípio Responsabilidade: ensaio de uma ética para a civilização tecnológica, para identificar as principais ideias do autor.
O segundo momento da pesquisa foi destinado à leitura do livro Técnica, medicina e ética: sobre a prática do princípio responsabilidade, para tentar compreender a aplicação prática da teoria. Não tive pleno sucesso nesta fase, pois mesmo lendo o livro, acredito que o tema é muito extenso e não consegui compreender e seria necessária uma análise mais aprofundada em outro momento.
Em seguida fiz a leitura do livro O princípio vida: fundamentos para uma biologia filosófica, procurando compreender o fenômeno da vida na perspectiva jonasiana. 
A pretensão deste trabalho foi entender as ideias de Hans Jonas, conhecer suas limitações para posteriormente encontrar uma aplicação prática na área de Design.					<B>Resultados</B>: 					<B>Conclusões</B>: 				</P>
