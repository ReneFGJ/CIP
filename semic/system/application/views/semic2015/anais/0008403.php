					<B>Introduction</B>: We are living in an age of technical and scientific advancement that promotes many changes all the time, what harm are assimilated and has become obsolete. These technological advances benefit and harm the life of the man who was endowed with a gigantic power without having to think about ethical boundaries.
Hans Jonas proposes us think about this issue and discuss these limits through his theses, propose definitions for human behavior so that the future is assured for future inhabitants of Earth.
So this research is to claim the presentation and analysis of the thought of Hans Jonas, through his major works, about ethics, technology and its consequences for nature and future life.					<B>Objectives</B>: This research project is part of a research Study Group, the PUCPR, called Ethics, Technology and Nature in Hans Jonas, and that the proposal of Professor Advisor of this research, has the general objective to analyze the failure of traditional ethics from the perspective of Hans Jonas, facing the challenges of technological civilization, to find grounds for a new Ethos.
This mode inserted in a broader research, are presented here as specific objectives the following referrals.

1) review because the traditional ethics have become insufficient for technological civilization.
2) To study what would be the new basis for ethics in technological civilization.					<B>Methods</B>: This research was developed from reading authors and Hans Jonas commentators. Initially it was made book reading and BOOK REPORT The Responsibility Principle: Test of an ethics for technological civilization, to identify the main author of the ideas.
The second phase of the research was for the reading of Technical book, medicine and ethics: on the practice of responsibility principle, to try to understand the practical application of theory. I have not had full success at this stage, because even reading the book, I believe the issue is too long and could not understand and further analysis would be needed at a later time.
Then I read the book The life principle: the making of a philosophical biology, seeking to understand the phenomenon of life on jonasiana perspective.
