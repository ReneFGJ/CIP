			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: This presente paper entitled threats on the Internet had as its starting point, the absence of clear and objective data on the main threats on the internet. Having given this source, several research-mingled, reaching a score of seventeen major crimes. Defined that threats, developed the “Cartilha do jovem internauta” in order to explain the possible causes, rights and duties that Internet users have the young nomenclature was the fact provide for high school students.					<B>Objectives</B>: To highlight the main threats on the internet, done that, aimed at the creation of the "Cartilha do jovem internauta". Finally, present to the students of the second year of high school, seeking the conscious use of the Internet and expose the main threats.					<B>Methods</B>: Using the bibliographic method for analysis of various researchs and views on the subject, providing a broad academic overview about the cybercrime.					<B>Results</B>: The results are divided into three sectors, the first, the conclusion of which the main threats to Internet users. Opted-in to divide them into two groups: the first, the threats to the financial sector, already the second group is for those threats that do not have the main legal good property. In turn, the second result was the drafting of the "Cartilha do jovem internauta", which was later presented to high school students. Thus, the end result has to show students and the dissemination of safe internet use.					<B>Conclusion</B>: Conclude that there is little in statistics on cybercrimes. But still, it is possible to conclude that there are crimes of greater degree of occurrence, it follows also that the young, for often find experienced, ends up being an easy victim to cybercrime. Thuss, increasingly it’s necessary education conscious use of the internet.				</P>
