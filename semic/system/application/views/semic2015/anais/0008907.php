			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O presente trabalho intitulado de ameaças na internet teve como ponto de partida a ausência de dados claros e objetivos sobre as principais ameaças na internet. Tendo origem neste dado, mesclaram-se várias pesquisas, chegando a um resultado de dezessete crimes principais. Definido as ameaças, elaborou a cartilha do jovem internauta com o fim de explicar as possíveis causas, direitos e deveres que os internautas têm, a nomenclatura jovem foi ao fato de apresentar para alunos do ensino médio.					<B>Objetivo</B>: Evidenciar as principais ameaças na internet, feito isso, teve como objetivo a criação da “cartilha do jovem internauta”. Por fim, apresentar para os alunos do segundo ano do ensino médio, buscando o uso consciente da internet e expor as principais ameaças.					<B>Metodologia</B>: Usando do método bibliográfico para análise das mais variadas pesquisas e opiniões sobre o tema, proporcionando ao acadêmico um amplo panorama dos crimes virtuais.					<B>Resultados</B>: Os resultados estão divididos em três setores, sendo o primeiro, a conclusão sobre quais as principais ameaças ao internauta. Optou-a em dividi-las em dois grandes grupos: o primeiro, das ameaças ao setor financeiro, já o segundo grupo fica por aquelas ameaças que não têm como bem jurídico principal o patrimônio. Por sua vez o segundo resultado foi à elaboração da “cartilha do jovem internauta”, que posteriormente foi apresentada aos alunos secundaristas. Sendo assim, como resultado final, tem-se a apresentação aos alunos e a divulgação do uso seguro da internet.					<B>Conclusões</B>: Conclui-se que pouco há em estatísticas sobre crimes informáticos. Porém, ainda assim, é possível concluir que existem crimes de maior grau de ocorrência, conclui-se ainda, que o jovem, por muitas vezes se achar experiente, acaba sendo uma vítima fácil aos delitos informáticos. Sendo assim, faz-se necessário cada vez mais a educação do uso consciente da internet.				</P>
			</div><B>Palavras-chave</B>: Ameaças. internet. cartilha. jovem.			<BR>
