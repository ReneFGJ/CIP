			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A presente pesquisa apresenta o Cadastro Ambiental Rural - CAR, que representa um registro eletrônico de imóveis rurais que consta informações sobre as Áreas de Reserva Legal, de Preservação Permanente, Consolidadas e de uso restrito, cuja principal finalidade é a identificação das terras brasileiras e o monitoramento e combate ao desmatamento. O CAR foi adotado por meio da Lei nº 12.651 de 2012, que reformou o Código Florestal brasileiro e obrigou os proprietários ou possuidores rurais, e as terras ocupadas por povos tradicionais e assentamentos a realizarem a inscrição no CAR					<B>Objetivo</B>: Esta pesquisa visa analisar a incidência do CAR para os Povos Tradicionais e demonstrar a situação atual do cadastro dos imóveis rurais.					<B>Metodologia</B>: A pesquisa se deu por meio de sites, obras bibliográficas e leitura do Novo Código Florestal Lei nº 12.727/12, com a finalidade de conhecer o CAR e verificar as principais mudanças introduzidas no sistema de proteção florestal. Os principais instrumentos de proteção continuaram sendo as Áreas de Proteção Permanente (APP) e as Áreas de Reserva Legal (RL).					<B>Resultados</B>: Na prática a incidência do CAR pressupõe um enfrentamento de interesses, de um lado a razão de criação das Unidades de Conservações e territórios ocupados por Povos Tradicionais, e de outro a proteção ambiental restrita às Reservas Legais e Áreas de Proteção Permanente.					<B>Conclusões</B>: Os comandos normativos acerca do tema não dispõem claramente sobre a sua aplicação e não levam em consideração as diferentes situações territoriais para realização do Cadastro.				</P>
			</div><B>Palavras-chave</B>: Unidades de conservação. territorios tradicionais. cadastro ambiental rural CAR. socioambientalismo			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: This reserch presents the Rural Environmental Registry - CAR, an electronic control of all Brazilian rural properties, which concentrates information about the Legal Reserve Areas of Permanent Preservation, Consolidated and of restricted use, whose main purpose is the identification of Brazilian territory and the monitoring and combating deforestation. The CAR was adapted by Law No. 12.651 of 2012, which reformed the Brazilian Forest Code and forced landowners or owners of lands occupied by traditional peoples and settlements to carry out the registration in the CAR.					<B>Objectives</B>: This reserch aims to analyze the impact of CAR for Traditional Peoples and show the current status of registration of rural properties.					<B>Methods</B>: The research was done through websites, literature works and reading the New Forest Code Law No. 12,727 / 12, in order to know the CAR and verify the main changes introduced in forest protection system. The main hedging instruments remained the Permanent Protection Areas (APP) and Legal Reserve Areas (RL).					<B>Results</B>: The impact of the CAR requires a clash of interests; on the one hand the reason for creation of the Conservation Areas and in the territories occupied by Traditional Peoples, and the other hand the restricted environmental protection in the Legal Reserves and Permanent Protection Areas.					<B>Conclusion</B>: The legal standards does not clearly provide its application and does not take into account the different territorial situations to perform the register.				</P>
			</div><B>Keywords</B>: Conservation units. Rural Environmental Registry – CAR. raditional occupation of land.</td>
