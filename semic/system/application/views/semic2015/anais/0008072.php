			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O ceticismo moderno é definido por amparar-se tão somente nos juízos que se possam derivar da experimentação empírica e da relação de ideias oriundas desta mesma experimentação. Tal escola filosófica encontrou na figura de David Hume a sua maior representação. Os princípios assumidos por Hume o levam a impugnar conceitos básicos como os de substância, causalidade e de sujeito cognoscente, promovendo uma verdadeira revolução naquilo que se refere a forma como entendemos o processo de aquisição do conhecimento, principalmente quando comparados com a cosmovisão clássica aprimorada por Aristóteles. Todavia, quanto ao fundador do Liceu, a esse atribuímos a responsabilidade intelectual no que tange a delimitação do campo de toda a investigação filosófica. Ou seja, com sua ciência metafísica, pretende o estagirita descrever todos os aspectos do conhecimento humano, inclusive aqueles supostamente propostos por Hume. Tendo visto isso, a presente pesquisa procurou debruçar-se sobre tal tensão a fim de um maior esclarecimento sobre a mesma.					<B>Objetivo</B>: Haja vista a amplitude da proposta aristotélica mencionada acima, a presente pesquisa procurou primeiramente identificar possíveis refutações da parte de Hume ao pensamento aristotélico. Posteriormente procurou-se identificar elementos na obra do autor escocês em que conceitos e pressupostos metafísicos não apenas são encontrados, mas fundamentam a elaboração do pensamento cético, mais precisamente o pensamento de David Hume no que se refere a sua crítica ao conceito de causalidade e em sua postura quanto a existência dos milagres.					<B>Metodologia</B>: : A princípio, procuramos fundamentar a nossa pesquisa escolhendo previamente as obras cujas quais nos auxiliariam na investigação da tensão existente entre o pensamento cético representado por David Hume e o pensamento clássico (metafísico) representado por Aristóteles. Para isso, servimo-nos dos próprios autores citados acima. Necessário também foi a leitura de obras que esclarecessem o contexto iluminista e suas influências, sobre tais, dispusemo-nos à leitura de comentadores acompanhada da realização de fichamentos que nos permitiram ter uma maior compreensão do referido contexto.					<B>Resultados</B>: Além de uma maior compreensão acerca do pensamento humeano, bem como do aristotélico, os resultados adquiridos nos proporcionam uma maior compreensão acerca da própria forma como a contemporaneidade (influenciada pelo pensamento iluminista) entende a realidade.					<B>Conclusões</B>: Como conclusão, ressalta-se a amplitude e a abrangência do pensamento aristotélico em detrimento da circunscrição do pensamento humeano àquilo já exposto pela filosofia clássica. David Hume, durante a elaboração de sua epistemologia, apropria-se de elementos impugnados pelos seus próprios fundamentos, fazendo com que a metafísica sobreviva até mesmo em um sistema dito cético.				</P>
