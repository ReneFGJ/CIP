			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A presente pesquisa buscou investigar no campo filosófico e educativo a constituição de uma compreensão crítica a respeito da educação considerada como decadente no pensamento de Nietzsche. Nosso ponto de partida perspectiva compreender a educação como possibilidade de transformação de si no pensamento nietzschiano, que se dá desde a formação do filósofo, quando ele imerso a sua solidão, se preocupava  com o futuro dos estabelecimentos de ensino ou poderíamos afirmar propriamente com a educação alemã da época. Em seu percurso acadêmico Nietzsche se propôs em alguns momentos constituir grupos de estudos com a finalidade de refletir e transformar o espírito educativo na Alemanha do século XIX.					<B>Objetivo</B>: Quanto ao objetivo buscou-se compreender como se constitui a concepção de educação no pensamento nietzschiano a partir das publicações das produções científicas em três periódicos de filosofia.					<B>Metodologia</B>: a metodologia constitui-se em um mapeamento das produções científicas em forma de artigos nos respectivos periódicos; Cadernos Nietzsche, Revista Trágica e Estudos Nietzsche, nos anos de 2006 a 2013.					<B>Resultados</B>: Ante o exposto, teve como resultado básico na compreensão daquilo que vem sendo pesquisado no Brasil sobre a educação em Nietzsche.					<B>Conclusões</B>: 				</P>
			</div><B>Palavras-chave</B>: Nietzsche. Educação. Produção Científica.			<BR>
