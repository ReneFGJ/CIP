			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A presente pesquisa buscou investigar no campo filosófico e educativo a constituição de uma compreensão crítica a respeito da educação considerada como decadente no pensamento de Nietzsche. Nosso ponto de partida perspectiva compreender a educação como possibilidade de transformação de si no pensamento nietzschiano, que se dá desde a formação do filósofo, quando ele imerso a sua solidão, se preocupava com o futuro dos estabelecimentos de ensino ou poderíamos afirmar propriamente com a educação alemã da época. Em seu percurso acadêmico Nietzsche se propôs em alguns momentos constituir grupos de estudos com a finalidade de refletir e transformar o espírito educativo na Alemanha do século XIX.					<B>Objetivo</B>: Quanto ao objetivo buscou-se compreender como se constitui a concepção de educação no pensamento nietzschiano a partir das publicações e das produções científicas consultadas em três periódicos de filosofia.					<B>Metodologia</B>: A metodologia constitui-se em um mapeamento das produções científicas em forma de artigos nos respectivos periódicos; Cadernos Nietzsche, Revista Trágica e Estudos Nietzsche, nos anos de 2006 a 2013.					<B>Resultados</B>: Ante o exposto, se obtém como resultado básico na compreensão daquilo que vem sendo pesquisado no Brasil sobre a educação em Nietzsche.					<B>Conclusões</B>: Por fim, nossos objetivos propostos foram atendidos parcialmente. Ou seja, fomos surpreendidos em relação a não existência de pesquisas em filosofia relacionadas a área da educação nos três periódicos de filosofia selecionados. Por conseguinte, os gráficos apresentam várias temáticas transversais no contexto educativo, mas constituem subsídios insuficientes para uma reflexão acerca de uma educabilidade humana como formação de espíritos livres no pensamento de Nietzsche.				</P>
			</div><B>Palavras-chave</B>: Nietzsche. Educação. Produção Científica.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: This research investigates the philosophical and educational field the creation of a critical understanding about education considered decadent in Nietzsche´s thought. Our starting point perspective understand education as a possibility of transformation of the self in Nietzsche´s thought, which takes place since the formation of the philosopher, when he immersed in his loneliness, worried about the future of schools or we could say properly with the German education of the time. In his academic route Nietzsche proposed constitute study groups in order to reflect and transform the educational spirit in nineteenth-century at Germany.					<B>Objectives</B>: The objective is sought to understand how is the educational concept in Nietzsche´s thought from the publications and scientific productions consulted in three philosophy journals.					<B>Methods</B>: The methodology is a mapping of scientific production in the form of articles in their journals; Notebooks Nietzsche, Tragic Magazine and Nietzsche Studies , in 2006-2013.					<B>Results</B>: Based on the foregoing, we obtain as a result the basic understanding of what is being researched in Brazil on education in Nietzsche.					<B>Conclusion</B>: Finally, our objectives were partially met. In other words, we were surprised over the lack of research in philosophy related to the field of education in the three selected philosophy journals. Therefore, the charts show several cross-cutting themes in the educational context, but are insufficient subsidies for a reflection on a human educability as formation of free spirits in Nietzsche´s thought.				</P>
