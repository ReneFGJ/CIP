					<B>Introduction</B>: This research aimed to investigate the philosophical and educational field the creation of a critical understanding of education considered decadent in Nietzsche´s thought. Our starting point perspective to understand education as a possibility of transformation of the self in Nietzsche´s thought, which takes place since the formation of the philosopher, when he immersed his loneliness, worried about the future of schools or we could say with proper education German of the time. In his academic route Nietzsche proposed at times constitute study groups in order to reflect and transform the educational spirit in nineteenth-century Germany.					<B>Objectives</B>: As for the objective sought to understand how is the educational concept in Nietzsche´s thought from the publications of scientific productions in three philosophy journals.					<B>Methods</B>: The methodology is on a mapping of scientific production in the form of articles in their journals; Notebooks Nietzsche, Magazine Tragic Studies and Nietzsche, in the years 2006-2013.					<B>Results</B>: Based on the foregoing, we had the basic result in the understanding of what is being researched in Brazil on education in Nietzsche.					<B>Conclusion</B>: 				</P>
