			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A perda de implantes osseointegráveis é um mecanismo complexo e multifatorial. Falhas no processo da osseointegração constituem uma das situações mais delicadas na implantodontia. Fatores clínicos sozinhos parecem não explicar totalmente o processo de perda de implantes. A perda dos implantes pode estar relacionada à incapacidade do hospedeiro em estabelecer ou manter a osseointegração. Polimorfismos genéticos são classicamente considerados como fatores de risco a diversas doenças, e mais recentemente, à perda de implantes dentais. Dessa forma, genes que codificam mediadores do metabolismo ósseo do hospedeiro, como o receptor da vitamina D (VDR), que interferem no processo de ocorrência e manutenção da osseointegração, são candidatos funcionais para a investigação de polimorfismos genéticos e sua associação com a perda de implantes dentais.					<B>Objetivo</B>: O objetivo deste estudo foi analisar a associação dos polimorfismos rs731236 e rs7975232 no gene do VDR e a perda de implantes dentais osseointegráveis.					<B>Metodologia</B>: A amostra foi composta por 244 pacientes, divididos em dois grupos: (1) grupo controle, constituído de 163 pacientes, apresentando um ou mais implantes saudáveis e em função por no mínimo seis meses, e (2) grupo teste, consistindo de 81 pacientes que apresentem perda de implantes. O DNA foi obtido a partir de células epiteliais da mucosa jugal. Após a purificação do DNA, os fragmentos gênicos foram amplificados pela técnica da reação em cadeia da polimerase (PCR). A associação entre polimorfismos genéticos e a perda de implantes dentais osseointegráveis foi estimada por meio do teste de qui-quadrado ao nível de significância de 5%.					<B>Resultados</B>: Não foi observada associação entre os polimorfismos no gene VDR e a perda de implantes.					<B>Conclusões</B>: Concluiu-se que não houve associação dos polimorfismos rs731236 e rs7975232 do gene VDR com a perda de implantes dentais; porém, outros polimorfismos nesse gene ainda restam ser investigados.				</P>
			</div><B>Palavras-chave</B>: Implantes dentais. polimorfismos genéticos. VDR			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Implant loss is multifactorial and considered a complex trait and may be partly influenced by genetic factors.					<B>Objectives</B>: Investigate VDR gene polymorphisms and the association with susceptibility to osseointegrated implant loss.					<B>Methods</B>: Genotypying of VDR genetic polymorphisms.					<B>Results</B>: VDR polymorphisms were not associated with implant loss.					<B>Conclusion</B>: Polymorphisms rs731236 and rs7975232 in the VDR gene were not associated with susceptibility to osseointegrated implant loss.				</P>
			</div><B>Keywords</B>: VDR. implant loss. genetic polymorphisms</td>
