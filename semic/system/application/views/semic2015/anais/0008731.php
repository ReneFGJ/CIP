			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A obesidade pode ser considerada um problema de saúde pública. Estima-se que os casos de indivíduos obesos tripliquem a cada ano. Com essa projeção, muitos fazem a opção pela cirurgia bariátrica por apresentarem patologias relacionadas com o excesso de peso e por desejar uma qualidade de vida melhor.					<B>Objetivo</B>: O estudo teve como objetivos:Avaliar a qualidade de vida de pacientes submetidos a cirurgia bariátrica,levantar o perfil pessoal e clínico dos participantes, apontar os motivos que os levaram à realizar a cirurgia e, listar os pontos positivos e negativos citados pelos pacientes, relacionados à intervenção cirúrgica.					<B>Metodologia</B>: Pesquisa qualitativa, realizada com 30 indivíduos que realizaram a cirurgia bariátrica há mais de um ano, através de uma entrevista gravada. Os dados foram analisados segundo a análise de conteúdo.					<B>Resultados</B>: A maioria dos indivíduos pesquisados optou pela cirurgia por questões de saúde, principalmente por apresentar doenças crônicas. Na amostra, o sexo feminino foi prevalente e dentre os participantes a obesidade grau I prevaleceu.					<B>Conclusões</B>: conclui-se que a obesidade está de fato relacionada a muitas patologias e que a cirurgia bariátrica pode ser considerada uma opção de tratamento. Embora os participantes considerem melhora na qualidade de vida após a cirurgia, grande parte deles alerta para a importância de seguir um acompanhamento com a equipe de saúde para que o método seja eficaz..				</P>
			</div><B>Palavras-chave</B>: Enfermagem. Qualidade de vida. Cirurgia bariátrica.			<BR>
