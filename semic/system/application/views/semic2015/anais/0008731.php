			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Obesity may be considered a public health problem. It is estimated that cases of triple obese individuals each year. With this projection, many make the choice of bariatric surgery because they have diseases related to being overweight and want a better quality of life.					<B>Objectives</B>: The study aimed to evaluate the quality of life of patients undergoing bariatric surgery, raising the personal and clinical profile of the participants, pointing out the reasons that led to perform surgery and list the pros and cons cited by patients related to surgical intervention.					<B>Methods</B>: qualitative research among 30 individuals who underwent bariatric surgery for more than a year, through a recorded interview. Data were analyzed using content analysis.					<B>Results</B>: The majority of those surveyed opted for surgery because of health issues, especially for presenting chronic diseases. In the sample, female gender was prevalent among the participants and obesity grade I prevailed.					<B>Conclusion</B>: : it was concluded that obesity is in fact related to many diseases and that bariatric surgery can be considered a treatment option. Although participants consider improved quality of life after surgery, most of them alert to the importance of following a monitoring with the health team for the method to be effective ..				</P>
