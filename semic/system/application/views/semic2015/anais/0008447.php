			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Sugar substitutes (sweeteners) are increasingly consumed and much debate surrounds them.					<B>Objectives</B>: In this study, we evaluated, even in a preliminary way, the influence exerted by sugar substitutes in the formation of biofilms of some microorganisms of dental importance.					<B>Methods</B>: Therefore, we used the yeast Candida albicans SC5314, the cariogenic bacterium Streptococcus mutans ATCC®25175 ™ and the early colonizer bacterium (dental plaque initiator) S. sanguinis ATCC®10556 ™. These microorganisms were grown as biofilms in the presence of aspartame, sodium cyclamate, saccharin and xylitol in concentrations close to those observed in the saliva of users. After 72 h, biofilms were evaluated for their biomass by MTT reduction test with D-glutamine as activator.					<B>Results</B>: It has been found that Candida albicans SC5314 biofilm biomass was lower than the bacterial. Both the S. mutans ATCC®25175 ™, the S. sanguinis ATCC®10556 ™, were not affected in terms of final biomass.					<B>Conclusion</B>: The compilation of the results reported here leads us to infer that tested sugar substitutes do not increase or reduce the formation of biofilms in dental interest.				</P>
			</div><B>Keywords</B>: Sweetners. biofilms. biomass</td>
