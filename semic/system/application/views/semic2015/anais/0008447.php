			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Substitutos de açúcar (adoçantes) são cada vez mais consumidos e muita discussão os cerca.					<B>Objetivo</B>: Neste estudo, se buscou avaliar, ainda que de forma preliminar, a influência exercida por substitutos de açúcar na formação de biofilmes de alguns microrganismos de importância odontológica.					<B>Metodologia</B>: Para tanto, foram utilizados a levedura Candida albicans SC5314, a bactéria cariogênica Streptococcus mutans ATCC®25175™ e a bactéria colonizadora primária (iniciadora de placa dental) S. sanguinis ATCC®10556™. Esses microrganismos foram crescidos como biofilmes na presença de aspartame, ciclamato de sódio, sacarina e xilitol, em concentrações próximas àquelas observadas na saliva de usuários. Após 72 h, os biofilmes foram avaliados quanto às suas biomassas pelo teste de redução de MTT com ativador D-glutamina.					<B>Resultados</B>: Foi verificado que o Candida albicans SC5314 apresentou menor biomassa de biofilme que as bactérias. Tanto o S. mutans ATCC®25175™, quanto o S. sanguinis ATCC®10556™, não sofreram influência em termos de biomassa final.					<B>Conclusões</B>: A compilação dos resultados aqui reportados nos leva a inferir que os substitutos de açúcar ensaiados não aumentam, nem reduzem a formação de biofilmes de interesse odontológico.				</P>
			</div><B>Palavras-chave</B>: Adoçantes. biofilmes. biomassa			<BR>
