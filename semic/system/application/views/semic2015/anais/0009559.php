			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The research shows the relationship between public spaces and risk groups, and its different connections.  The reflection about degradation of the public space and its changes usually conducts risk groups to the place. The current study intend to show forgotten places in Brazilian context that became safe places, with new users.					<B>Objectives</B>: The main objective is to propose alternatives to leisure spaces, in order to the environmental control and the rescue of social risk groups, especially regarding childhood and youth.					<B>Methods</B>: The method used on this research is case studies focused on urban spaces changes. The studies have been done in public spaces with different social, environmental and political issues.					<B>Results</B>: The results shows that public spaces and risk groups only meet when the space becomes degraded and then drug users and prostitutes occupy and transit trough the site.					<B>Conclusion</B>: The conclusion is that urban spaces without some kind of incentive start to become degraded and risk groups start to occupy, and with that, the violence feeling tended to expelled the old users. In the case studies, when the site has been reformed, the framework has been changed and the old users came back.				</P>
			</div><B>Keywords</B>: Risk groups, Public Spaces, Revitalization, Social Landscape</td>
