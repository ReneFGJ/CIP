			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O presente trabalho representa a relação entre os espaços públicos e grupos de riscos, e o que ocasiona esta conexão. A reflexão do entorno com o espaço público, o estado de degradação que o local inicia e as modificações que isto implica, frequentemente conduzindo grupos de riscos para o local. Esta pesquisa, pretende mostrar locais que foram esquecidos no âmbito nacional, e transformam-se em locais inseguros, por conta destes novos grupos de frequentadores.					<B>Objetivo</B>: O objetivo geral do projeto central consiste em propor alternativas para espaços de lazer, visando ao controle ambiental e ao resgate de populações sujeitas a riscos sociais, especialmente voltados a infância e a juventude.					<B>Metodologia</B>: O foco desta iniciação cientifica se dá nos estudos de casos, e as mudanças realizadas para reverter o quadro de situação das praças estudadas. Os estudos feitos, foram em locais públicos com diferentes características, sejam elas: sociais, ambientais ou politicas.					<B>Resultados</B>: Os resultados desta pesquisa acabam por mostrar que o espaços publicos e grupos de riscos só se encontram quando o espaço se torna degradado, e com isto os usuários de drogas e prostitutas acabam habitando ou transitando pelo local.					<B>Conclusões</B>: Em todos os estudos de casos, pode-se perceber que antes da revitalização o local não possuia um incentivo, com isso o local foi entrando em degradação, após isso ocorrer, grupos de riscos começaram a habitar o local, e um sentimento de inseguraça se instalou no mesmo, afastando os antigos usuários do espaço. Para reverter este quadro, nos estudos de casos estudados, foi necessário uma revitalização, com mudanças antrópicas, naturais ou sociais.				</P>
			</div><B>Palavras-chave</B>: Grupos de riscos; Espaços Públicos; Revitalização; Paisagem Socializada; Revitalização.			<BR>
