			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A depressão atinge uma média 5% da população mundial, e um dos fármacos antidepressivos mais utilizados para o seu tratamento é a fluoxetina. Dessa forma, os cirurgiões-dentistas, bem como ortodontistas estão sujeitos a tratar pacientes que fazem seu uso contínuo. Portanto, é de grande relevância estudar a relação do uso deste fármaco com o movimento ortodôntico.					<B>Objetivo</B>: Este estudo objetivou verificar o efeito decorrente da administração de fluoxetina na maturação do colágeno tipo I e III da matriz óssea neoformada durante a movimentação ortodôntica em ratos.					<B>Metodologia</B>: Foram utilizados cento e noventa e dois ratos machos da linhagem Wistar, divididos em quatro grupos: S - solução salina sem movimento ortodôntico; F - fluoxetina sem movimento ortodôntico; SM - solução salina com movimento ortodôntico e FM - fluoxetina com movimento ortodôntico. Nos grupos S e SM, os animais receberam 1 ml de solução salina 0,9% e grupos F e FM foram realizadas aplicações diárias de fluoxetina de 20 mg/Kg. No 30º dia, os animais dos grupos SM e FM foram sedados e instalou-se o dispositivo ortodôntico para induzir a movimentação dentária do primeiro molar superior direito (força 25cN) e no 32º, 37º, 44º e 58º dias os animais dos grupos S, F, SM e FM foram eutanasiados. Para avaliação da porcentagem de colágeno I e III foi quantificado o colágeno imaturo por microscopia de polarização.					<B>Resultados</B>: Não foram observadas diferenças significativas na quantificação de colágeno imaturo entre os grupos nos quatros intervalos de tempo estudados (p[e]rt;0,05).					<B>Conclusões</B>: A administração de fluoxetina associada à movimentação ortodôntica em ratos Wistar não interferiu na neoformação óssea.				</P>
			</div><B>Palavras-chave</B>: Cloridrato de fluoxetina. movimento ortodôntico. osso			<BR>
