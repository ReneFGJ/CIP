			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Depression affects an average 5% of world population and one of the most widely used antidepressant drugs for their treatment is fluoxetine. Thus, dentists and orthodontists are subject to treat patients who make their continued use. It is therefore of great importance to study the relationship of the use of this drug with orthodontic movement.					<B>Objectives</B>: This study aimed to verify the effect of the administration of fluoxetine in the maturation of collagen type I and III of the newly formed bone matrix during orthodontic tooth movement in rats.					<B>Methods</B>: We used one hundred ninety-two male Wistar rats, divided into four groups: S - saline without orthodontic movement; F - fluoxetine without orthodontic movement; SM – saline with orthodontic movement and FM - fluoxetine with orthodontic movement. In S and SM groups, the animals received 1 ml of 0.9% saline and F and FM groups were performed daily applications of 20 mg / kg fluoxetine. On the 30th day, the animals of SM and FM groups were sedated and settled orthodontic device to induce tooth movement of the first upper right molar (25cN force) and 32, 37, 44 and 58 days the animals of the groups S, F, SM and FM were euthanized. To evaluate the percentage of type I and type III collagen, the immature collagen was quantified by polarization microscopy.					<B>Results</B>: There were no significant differences in the quantification of immature collagen between the groups in the four time intervals studied (p[e]rt; 0.05).					<B>Conclusion</B>: Fluoxetine administration associated with orthodontic tooth movement in rats did not interfere with bone formation.				</P>
