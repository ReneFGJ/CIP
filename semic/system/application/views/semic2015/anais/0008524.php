			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Toxoplasma gondii an obligate intracellular protozoan, has the ability to invade all nucleated cells, tissues and hosts, but domestic cats and wild felines are the only definitive hosts. The importance of animal toxoplasmosis due to the fact that infected animals serve as a source of infection to humans directly or indirectly.					<B>Objectives</B>: So the scarcity of information about the presence of antibodies against Toxoplasma gondii in cattle herds in Toledo, PR region, is aimed in this study to determine the presence of antibodies against T. gondii in dairy cattle females whose rural farms in the municipality.					<B>Methods</B>: We collected 315 blood samples. In each farm was collected blood from 10 animals, except when the herd was less than 10 dairy cattle, then proceeded to collect blood at least five animals. The research antibodies against T. gondii was performed by Immunofluorescence Reaction Test (IFAT) with cutoff 1:64.					<B>Results</B>: Of the 315 serum samples of dairy cows analyzed by IFAT 16 (5.08%) cattle have antibodies against protozoan and 299 (94.92%) have proved negative. Of the 49 farms which proceeded to collect samples, 12 (24.48%) were positive researched the agent.					<B>Conclusion</B>: These results show that the protozoan infection requires larger studies to be evaluated, more faithfully, geographical distribution and economic importance and the real importance of transmission to humans.				</P>
