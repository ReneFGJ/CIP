			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O Toxoplasma gondii um protozoário intracelular obrigatório, possui a capacidade de invadir todas as células nucleadas, tecidos e hospedeiros, porém os gatos domésticos e felídeos selvagens são os únicos hospedeiros definitivos. A importância da toxoplasmose animal, decorre ao fato dos animais parasitados servirem de fonte de infecção aos humanos de forma direta ou indireta.					<B>Objetivo</B>: Portanto pela escassez de informações sobre a presença de anticorpos anti - Toxoplasma gondiiem rebanhos bovinos na região de Toledo, PR, objetivou-se, neste estudo, determinar a presença de anticorpos anti - T. gondiiem fêmeas de bovinos leiteiros oriundas de propriedades rurais do município.					<B>Metodologia</B>: Foram coletadas 315 amostras de sangue. Em cada propriedade foi coletado sangue de 10 animais, exceto quando o rebanho apresentava menos de 10 bovinos leiteiros, então, procedeu a coleta de sangue de no mínimo 05 animais em lactação. A pesquisa de anticorpos contra T.gondii foi realizada pela Reação de Imunofluorescência Indireta (RIFI) com ponto de corte 1:64.					<B>Resultados</B>: Das 315 amostras de soro das fêmeas bovinas leiteiras analisadas através da RIFI 16 (5,08%) bovinos apresentaram anticorpos contra o protozoário de Toxoplasma gondii e 299 (94,92%) mostraram-se negativas. Das 49 propriedades onde procedeu a coleta de amostras, 12 (24,48%) foram positivas ao agente pesquisado.					<B>Conclusões</B>: Tais resultados evidenciam que a protozoose necessita de estudos mais amplos para que seja avaliada, com mais fidedignidade, a distribuição geográfica e a importância econômica e a real importância da transmissão aos humanos.				</P>
			</div><B>Palavras-chave</B>: Bovinocultura de Leite. RIFI. Soroprevalência e Toxoplasmose.			<BR>
