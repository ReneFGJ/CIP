			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Atualmente muitos fenômenos físicos conseguem ser descritas com formulações matemáticas. O fenômeno da flambagem será aqui estudado e analisado. Durante muito tempo, muitos pesquisadores têm se empenhado para buscar novas formas, e melhorar, formulações matemáticas que consigam da melhor forma e mais corretamente descrever um fenômeno físico desejado. Dentro disso tem-se o estudo da flambagem, que é um fenômeno muito comum de instabilidade elástica e que ocorre em elementos esbeltos, como vigas, colunas e dutos.					<B>Objetivo</B>: Este trabalho tem como objetivo desenvolver formulação do Método dos Elementos Finitos enriquecido para a análise não linear geométrica de dutos em flambagem com elementos finitos unidirecionais.					<B>Metodologia</B>: O enriquecimento do método numérico que discretiza a equação governante do problema é dado pela construção do espaço matemático empregado polinômios hierárquicos. Mais especificamente, polinômios de Lobatto e de Bardell.					<B>Resultados</B>: Os resultados encontrados mostram uma pequena diferença com o resultado analítico, que é um resultado da quantidade de elementos e iterações usadas no problema. A formulação de elementos finitos hierárquicos foi implementada num código computacional, denominado Flamba2D. Com este trabalho, é possível perceber que a formulação numérica funciona muito bem para a flambagem aqui. Mas também depende muito do grau de complexidade do problema, pois muitas vezes serão necessários cuidados. Ou seja, se for muito complexo, vai ser preciso um computador mais robusto, se deseja alcançar resultados precisos.					<B>Conclusões</B>: 				</P>
			</div><B>Palavras-chave</B>: Flambagem, elementos finitos, formulação enriquecida, duto			<BR>
