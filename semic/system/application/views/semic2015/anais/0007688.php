			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Nowadays many physical phenomenon can be described with mathematical formulations. The buckling is one of them that will be here studied and analyzed. The buckling is a quite common phenomenon that exists in many elements, such as beams.					<B>Objectives</B>: Therewith, in this stage, the objective of this paper is to work and understand how the FEM performs the non-linear analysis of geometrical pipelines for buckling with unidirectional finite element.					<B>Methods</B>: Among this mathematical formulations, it is known that there are various ways that the buckling effect can perform itself, so, then it was also researched the other models available for programming. Here it will be used the Euler-Bernoulli beam assumption. Then, it was studied how to operate the program, examples were solved and its results saved for analysis, and after to homologate the program FLAMBA2D.					<B>Results</B>: The results showed that we have a quite difference between the analytic solution and the numerical solution, which is a result of how many iterations and elements were used in the problem. Within this study, it was possible to see that this numerical formulation works well for the buckling analysis. But depending on the degree of complexity of the problem, it may be needed cautions when solving the problem. It means that if the problem is quite complex, it will be needed powerful computers to solve it, if the result that is wanted has to be precise.					<B>Conclusion</B>: 				</P>
