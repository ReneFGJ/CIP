			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A Educação é uma importante temática e pesquisas sobre queixa escolar passaram a ocupar um espaço de destaque tanto na Psicologia quanto na Educação.					<B>Objetivo</B>: Conhecer as principais queixas para o encaminhamento de crianças e adolescentes à avaliação psicologica					<B>Metodologia</B>: A pesquisa foi desenvolvida no município de Campina Grande do Sul, região metropolitana de Curitiba, onde foram analisados 50 prontuários de alunos de um centro de avaliação psicológica, também foi aplicado o instrumento Teacher Report Form (TRF) para conhecer o funcionamento adaptativo e os problemas de comportamento sob a percepção dos professores de tais alunos, sendo que os escores do TRF foram comparados as medias de queixas.					<B>Resultados</B>: Resultaram três grupos de principais queixa, sendo que a maioria dos alunos (76%) foram encaminhados devidos a dificuldade de aprendizagem. Os dados evidenciaram ainda diferença media estatisticamente relevante entre as principais queixas e as variáveis (ansiedade/depressão, social, problemas de pensamento, agressividade, escala internalizante, escala externalizante e total  de problemas de comportamento).					<B>Conclusões</B>: Esses dados mostram que estes aspectos são facilmente percebidos nos alunos pelos professores, mas ressaltamos o benefícios de instrumentaliza-los no âmbito prático pedagógico.				</P>
			</div><B>Palavras-chave</B>: Queixas escolares. funcionamento adaptativo. problemas de comportamento			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Education is an important issue and research on school complaints have come to occupy a prominent space in both Psychology and in Education.					<B>Objectives</B>: Know the main complaints for referral of children and adolescents psychological evaluation					<B>Methods</B>: The research was conducted in Campina Grande do Sul municipality, metropolitan region of Curitiba, which analyzed records of 50 students of a center for psychological evaluation was also applied the instrument Teacher Report Form (TRF) to meet the adaptive functioning and problems behavior in the perception of the teachers of such students, and the TRF scores were compared the averages of complaints.					<B>Results</B>: Led three groups of main complaint being that the majority of students (76%) were referred due to learning disability. The data also showed statistically significant average difference between the main complaints and variables (anxiety / depression, social problems of thinking, aggression, internalizing scale and full scale externalizing behavior problems).					<B>Conclusion</B>: These data show that these aspects are easily perceived in students by teachers, but we emphasize the benefits of exploits them in the pedagogical practical context.				</P>
