			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Este trabalho tem como enfoque os avanços e desafios dos onze anos da implementação da Lei 10.639/03. Essa lei tem o objetivo de promover uma educação que reconhece e valoriza a diversidade, comprometida com as origens do povo brasileiro.					<B>Objetivo</B>: A presente pesquisa tem o objetivo de identificar possíveis avanços desta lei desde a sua promulgação até o momento.					<B>Metodologia</B>: A pesquisa iniciou-se pela análise do texto da Lei Federal nº 10.639/03. Depois recorreu-se a pesquisa de outras bibliografias, para obtenção dos dados necessários ao andamento da reflexão; os conteúdos disponibilizados consistiam de ensaios, estudos empíricos e entrevistas, dentre outros.					<B>Resultados</B>: A partir de estudos publicados, foram considerados avanços e consequências da implementação dela, bem como se constatou a presença de barragens de peneiramento (MOURA, 1977) que operaram eficazmente para impedir o pleno alcance dos objetivos da lei. Ademais, foram identificados desafios concernentes ao período estudado, dentre eles, o racismo institucional, que surge como um dos fatores causadores da mencionada frustração.					<B>Conclusões</B>: Concluiu-se que a promulgação da Lei Federal nº 10.639/03 trouxe avanços, ao romper com o silêncio pedagógico (nas escolas dos níveis fundamental e médio) a respeito dos povos africanos, das culturas e contribuições deles no processo de fundação do Brasil.				</P>
			</div><B>Palavras-chave</B>: Educação. Silêncio pedagógico. Racismo institucional. Lei 10.639/03.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: This work is to focus the progress and challenges of the eleven years of the implementation of Law 10.639 / 03. This law aims to promote an education that recognizes and values [e]#8203;[e]#8203;diversity, committed to the origins of the Brazilian people.					<B>Objectives</B>: This research aims to identify possible improvements of this law since its enactment so far.					<B>Methods</B>: The research began by analyzing the text of the Federal Law 10.639 / 03. Then resorted to research other bibliographies, to obtain the data needed to progress reflection; the available contents consisted of tests, empirical studies and interviews, among others.					<B>Results</B>: From published studies were considered advancements and consequences of her deployment and it was found the presence of screening dams (MOURA, 1977) that effectively operated to prevent the full achievement of the objectives of the law. Moreover, challenges were identified concerning the period studied, among them institutional racism, which appears as one of the causative factors of that frustration.					<B>Conclusion</B>: It was concluded that the enactment of Federal Law 10.639 / 03 brought advances, to break the silence teaching (in schools of primary and secondary levels) about the African peoples, cultures and their contributions in the foundation process of Brazil.
KEYWORDS: Education. Pedagogical silence. Institutional racism. Law 10.639 / 03.				</P>
