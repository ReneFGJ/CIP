			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The Passeio Publico is the first park of Curitiba and is located in the central region. it
It was opened in the nineteenth century as the first urban park in the city in order
to end a flooded area of Rio Belém, with many mosquitoes and focus
contamination.					<B>Objectives</B>: This research has as objective to analyze the behavior of
Passeio Público  visitors.					<B>Methods</B>: This is a quantitative survey of 384 people. O
research instrument was a questionnaire . Of the total study subjects , 100
were visitors to the Public Garden in loco and the rest was through the questionnaire
online at Qualtrics system. Data were tabulated and presented as graphs and analyzed under the theoretical foundations of the Tourism and environmental awareness.					<B>Results</B>: The space is used for leisure, rest, work.
It has a small zoo with birds, reptiles and turtles. As it is located one
central area , degraded , respondents mostly complained about the lack of
on-site security and hygiene.

It is frequented by locals and visitors as recreational space , even if rarely, by old groups using as sociability space , children going to the playground and see the animals .					<B>Conclusion</B>: It is a space for leisure , rest and work. Rarely locals frequent it. Animals and the green area remain attractive . No need of revitalization , as visitors complain about the lack of security and hygiene of the place . Urban parks are a tourist attraction and the Public Garden , as by Heritage Listed area is still relatively well preserved.				</P>
