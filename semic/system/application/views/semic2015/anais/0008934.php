			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O Passeio Público é o primeiro parque de Curitiba e localiza-se na região central. Ele 
foi inaugurado no século XIX como primeiro parque urbano da cidade com o objetivo 
de acabar com uma área alagada do Rio Belém, com muitos mosquitos e foco de 
contaminação					<B>Objetivo</B>: Esta pesquisa tem como o objetivo analisar o comportamento de 
visitantes do Passeio Público.					<B>Metodologia</B>: Esta é uma pesquisa quantitativa com 384 pessoas. O 
instrumento de pesquisa foi um questionário. Do total de sujeitos pesquisados, 100 
foram visitantes do Passeio Público, em loco e o restante foi através do questionário 
on-line, pelo sistema qualtrics. Os dados foram tabulados e apresentados na forma de gráficos e analisados sob os fundamentos teóricos do Turismo e da apercepção ambeintal.					<B>Resultados</B>: O espaço é usado para o lazer, descanso, trabalho. 
Tem um pequeno zoológico com aves, répteis e tartarugas. Como se localiza uma 
área central, degradada, os respondentes em sua maioria reclamaram da falta de 
segurança no local e higiene.
É freqüentado por moradores e visitantes como espaço de lazer, mesmo que de forma rara, por grupos de terceira idade que usam como espaço de sociabilidades, crianças que vão ao parquinho e ver os animais.					<B>Conclusões</B>: É um espaço de lazer, de descanso e de trabalho. Raramente os moradores o freqüentam. Os animais e a área verde continuam sendo atrativos.  Há necessidade de revitalização, pois os visitantes reclamam da falta de segurança e higiene do local. Os parques urbanos são um atrativo turístico e o Passeio Público,como área Tombada pelo Patrimônio Histórico ainda é relativamente bem preservado.				</P>
			</div><B>Palavras-chave</B>: Percepção ambiental. Turismo em parques urbanos. Bioética ambiental			<BR>
