			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A área habitacional no Brasil sofreu várias intervenções em suas bases e estruturas, além de uma nova composição em sua política nacional desde o início do século XXI, principalmente pela aprovação do Estatuto das Cidades. O Ministério das Cidades elaborou a Política Nacional de Habitação (PNH), o Sistema Nacional de Habitação (SNH) e o Plano Nacional de habitação (PlanHab) para gerenciar recursos e o equacionamento das necessidades brasileiras. Coube aos municípios desenvolverem Planos Locais de Habitação de Interesse Social (PLHIS) para orientar a tomada de decisões, corrigir irregularidades e conseguir recursos para ações habitacionais. Porém em 2009, com a criação do Programa Federal Minha Casa Minha Vida, a política habitacional foi atingida pela falta de planejamento e integração com o novo plano.					<B>Objetivo</B>: Essa pesquisa busca relacionar os PLHIS de cada município, pois eles possuem questões e ações em comum.					<B>Metodologia</B>: O desenvolvimento do trabalho foi dividido em três partes: fundamentação teórica, visitas / entrevistas e análise dos Planos de Habitação. A primeira foi realizada com a leitura de artigos e elaboração de resenhas. A etapa seguinte corresponde a comparação entre realizada entre a teoria e a prática. Por último, a análise dos Planos foi executada com a elaboração de tabelas para cada município: Araucária, Campina Grande do Sul, Colombo, Curitiba, Pinhais, Quatro Barras e São José dos Pinhais					<B>Resultados</B>: Os resultados dessa análise são muito parecidos, pois foram elaborados pela mesma empresa (Ecotécnica Tecnologia e Consultoria Ltda) ou apresentam necessidades em comum.					<B>Conclusões</B>: Foi visível uma falta de integração entre os Planos das cidades vizinhas, pois somente com o planejamento conjunto será possível solucionar problemas comuns.				</P>
			</div><B>Palavras-chave</B>: Programa habitacional, política ambiental; gestão habitacional			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The housing area in Brazil has undergone several interventions in their bases and structures, as well as a new composition in national politics since the early twenty-first century, particularly the approval of the Estatuto da Cidade. The Ministry of Cities has developed a National Housing Policy (PNH), the National System of Housing (SNH) and the National Plan for Housing (PlanHab) to manage resources and equating the Brazilian needs. It was up to municipalities to develop Local Plans of Social Housing (PLHIS) to guide decision-making, correcting irregularities and secure funds for housing stocks. But in 2009, with the creation of the Federal Program Minha Casa Minha Vida), housing policy was struck by the lack of planning and integration with the new plan.					<B>Objectives</B>: This research seeks to relate the PLHIS of each municipality, because they have issues and actions in common.					<B>Methods</B>: The development work was divides into three parts: theoretical foundation, visits / interviews and analysis of Municipal Housing Plans. The first was carried out by reading articles and writing reviews. The next step corresponds to comparison between theory and practice. Finally, analysis of the plans was performed with making tables for each municipality: Araucária, Campina Grande do Sul, Colombo, Curitiba, Pinhais, Quatro Barras and São José dos Pinhais.					<B>Results</B>: The results of analysis are very similar because they were prepared by the same company (Ecotécnica Tecnology and Consulting Ltd.) or have common needs.					<B>Conclusion</B>: A lack of integration between the plans of the neighboring cities, because only will be possible to solve common problems was visible.				</P>
			</div><B>Keywords</B>: Housing programmes, Environmental planning, housing management</td>
