			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Based on the Law and Economics, sought to address the problem of lack of standard regulating the tax debt installment for business in bankruptcy . Between the interim report and the final was the enactment of a specific law , which, however , is not substantially efficient because basically centered in the upper concession period (84 months) to traditional (60 months ).					<B>Objectives</B>: We used the  cooperatio ntheory to find elements that suprissem these gaps in legal enforcement without judicial derail recovery.					<B>Methods</B>: It used the tools of economic analysis of law , found in domestic and foreign literature , to address the problem proposed in the initial project .					<B>Results</B>: The element " balance " was found in Nash , which was based on the reasoning sopesamento between constitutional and tax principles to limit the power of the state and the limited ability to contribute where the company is in bankruptcy protection,  , and that therefore it must be safeguarded by law.					<B>Conclusion</B>: 				</P>
