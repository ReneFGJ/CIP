			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Com base na Análise Econômica do Direito, buscou-se enfrentar o problema da ausência de norma regulando o parcelamento de débitos tributários para empresários em recuperação judicial. Entre o relatório parcial e o final houve a edição da lei específica, a qual, todavia, não é substancialmente eficiente porque centrada basicamente na concessão de prazo superior  (84 meses) ao tradicional (60 meses).					<B>Objetivo</B>: Dessa forma, recorreu-se à teoria da cooperação visando encontrar elementos que suprissem essas lacunas de aplicação legal, sem inviabilizar a recuperação judicial.					<B>Metodologia</B>: Recorreu-se ao instrumental da análise econômica do direito, encontrado em pesquisa bibliográfica nacional e estrangeira, para enfrentar o problema proposto no projeto inicial.					<B>Resultados</B>: Assim, foi encontrado o elemento “equilíbrio”, de Nash, no qual foi baseado o raciocínio de sopesamento entre os princípios constitucionais e tributários do poder de limitar do Estado e a limitada capacidade contributiva em que se encontra a empresa em recuperação judicial, , e que, por isso, deve ser tutelada pelo ordenamento jurídico.					<B>Conclusões</B>: 				</P>
			</div><B>Palavras-chave</B>: Análise econômica do direito. Teoria da cooperação. Recuperação judicial			<BR>
