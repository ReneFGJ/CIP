			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: As Unidades de Terapia Intensiva (UTIs) são unidades que requerem espaço físico diferenciado, equipamento de alta tecnologia e equipe inter e multidisciplinar, sendo crescente o número de pacientes críticos admitidos nessas unidades. Os índices prognósticos podem ser utilizados visando à otimização dos recursos relacionados à UTI e a avaliação da gravidade desses pacientes, um exemplo deles é o Acute Physiology and Chronic Health Evaluation II (APACHE II).
O déficit de vagas na UTI vem sendo associado a um aumento da mortalidade dos pacientes não admitidos, apesar da sua necessidade, e em pacientes que são admitidos, mas precisam esperar por leitos desocupados.					<B>Objetivo</B>: O objetivo deste estudo é identificar o fluxo de admissão em leitos de UTI de pacientes atendidos no Hospital Universitário Cajuru da cidade de Curitiba-PR e identificar o desfecho dos pacientes após trinta dias.					<B>Metodologia</B>: A amostra consistiu em 55 pacientes internados na UTI do Hospital Universitário Cajuru. Foram coletados os dados dos prontuários dos pacientes e, após 30 dias, realizado contato com a família para saber o desfecho clínico. Os dados foram organizados em planilha do Microsoft Excel. A análise de dados foi realizada com o auxílio do programa computacional SPSS v.20.0.					<B>Resultados</B>: Observou-se maior internamento, número de óbitos e média do escore APACHE II entre o gênero masculino. A média de idade das mulheres foi maior que a dos homens.					<B>Conclusões</B>: É possível concluir que os homens provavelmente apresentavam um quadro clínico mais grave que as mulheres. O estudo demonstrou que o escore APACHE II é um bom índice para avaliar o prognóstico.				</P>
			</div><B>Palavras-chave</B>: Hospital Universitário. APACHE.			<BR>
