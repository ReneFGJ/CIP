			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Intensive Care Units (ICUs) require a special physical space, high-tech equipment with an inter and multidisciplinary team, due to the growing number of critically ill patients admitted at these units. Prognostic indexes can be used in order to optimize resources related to the ICU and evaluate the severity of these patients; an example of them is the Acute Physiology and Chronic Health disease Classification System II (APACHE II).
The deficit of beds in the ICU has been associated to the increased mortality of not admitted patients despite of their need, and  the patients who are admitted, but need to wait for unoccupied beds.					<B>Objectives</B>: The goal of this study is to identify the admission flux of patients in ICU beds at Cajuru Hospital University of Curitiba-PR and identify the outcome of these patients after thirty days.					<B>Methods</B>: The sample consisted of 55 patients admitted to the ICU at the Cajuru Hospital University. Data from patient’s charts were collected and, after 30 days, the family has been contacted to find out the clinical outcome. The data were organized in Microsoft Excel spreadsheet. Data analysis was performed with the aid of the computer program SPSS v.20.0.					<B>Results</B>: A higher hospitalization, number of deaths and APACHE II score were observed between males. The average age of women was higher than men´s.					<B>Conclusion</B>: It was concluded that men probably had a more severe clinical picture than the women. The study showed that the APACHE II score is a good index to evaluate prognosis.				</P>
