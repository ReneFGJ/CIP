			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O modelo conceitual é uma ferramenta importante para apoiar o processo de elicitação de requisitos de software. O modelo conceitual contribui para o entendimento de um domínio, principalmente em contextos mais complexos. No entanto, a construção de um modelo conceitual não é uma tarefa trivial.					<B>Objetivo</B>: Esta pesquisa teve o objetivo de desenvolver um Wizard para apoiar a construção de modelos conceituais.					<B>Metodologia</B>: Neste contexto, conceitos e ferramentas existentes foram estudadas e uma integração entre as mesmas foi realizada. As ferramentas estudadas foram: o ENSURE, ambiente para elicitação de requisitos de software, e o OLED, ferramenta para criação de modelos conceituais utilizando a linguagem OntoUML.					<B>Resultados</B>: A integração possibilitou que por meio do processamento de um texto de um domínio fossem identificados os principais conceituais de um modelo e o mesmo fosse criado em uma ferramenta para modelagem conceitual.					<B>Conclusões</B>: Devido a complexidade dos conceitos e ferramentas envolvidos, os objetivos iniciais não foram atingidos. Desta maneira, é necessária a continuação por meio de novos projetos para atingir os resultados almejados.				</P>
			</div><B>Palavras-chave</B>: Modelo conceitual. ontologia. elicitação de requisitos			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The conceptual model is an important tool to support the process of elicitation software requirements. The conceptual model has contributed to the understanding of a domain, especially in more complex contexts. However, the construction of a conceptual model is not a trivial task.					<B>Objectives</B>: This research aimed to develop a Wizard to support the construction of conceptual models.					<B>Methods</B>: In this context, concepts and existing tools were studied and integration between them was held. The tools studied were:  ENSURE, setting up software requirements elicitation, and the OLED, tool for creating conceptual models using the OntoUML language.					<B>Results</B>: The integration enabled through the processing of a text of a domain conceptual main were identified in a model and it was created in a tool for conceptual modeling.					<B>Conclusion</B>: Due to the complexity of the concepts and tools involved, the initial goals have not been achieved. Thus, it is necessary to follow through new projects to achieve the desired results.				</P>
			</div><B>Keywords</B>: Conceptual model. ontology. elicitation requirement</td>
