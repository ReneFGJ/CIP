			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O titânio é o material mais utilizado em implantes dentários devido à sua boa resistência mecânica, baixo módulo de elasticidade, resistência à corrosão e biocompatibilidade quando comparado com outros metais. Entretanto, o titânio não e bioativo. Estudos recentes mostram que alterações na superfície do titânio em escala nanométrica favorecem a formação de tecido ósseo na superfície do implante. Após a formação dos nanotubos de TiO2 na superfície do implante, é necessário realizar um tratamento térmico para garantir a boa adesão da camada de óxido ao metal.					<B>Objetivo</B>: Esse estudo teve por objetivo compreender a influência da temperatura do tratamento térmico na cristalinidade (quantidade das fases anatase e rutilo), e seu efeito na resistência à tribocorrosão (desgaste em ambiente corrosivo).					<B>Metodologia</B>: Amostras de titânio grau 4 foram anodizadas utilizando um eletrólito composto por dietilenoglicol, ácido fluorídrico e uma solução contendo íons Ca e P. As amostras foram caracterizadas por microscopia eletrônica de varredura (MEV), difração de raios-X (DRX) e submetidos aos ensaios de tribocorrosão.					<B>Resultados</B>: Os resultados mostraram que as amostras que possuem maior quantidade da fase rutilo são mais resistentes à tribocorrosão, quando comparadas com as amostras com maior quantidade de anatase.					<B>Conclusões</B>: Portanto tratamentos térmicos que favoreçam o surgimento da fase rutilo são mais indicados quando se busca implantes mais resistentes ao desgaste e corrosão associados				</P>
			</div><B>Palavras-chave</B>: Titânio, biomateriais, nanotubos, tribocorrosão			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Titanium is the most common material used as dental implants due to its good mechanical resistance, low elastic modulus, corrosion resistance and biocompatibility, when compared to other metals. However, titanium is not bioactive. Recent studies has shown that surface modifications on titanium at a nanometric level induce the formation of bone tissue on the implant surface. After producing titanium oxide nanotubes, it is necessary to carry out a heat treatment to guarantee the good adhesion of the oxide onto the metallic substrate.					<B>Objectives</B>: The aim of this study was to understand the influence of temperature heat treatment in the crystallinity (amount of anatase and rutile phases) and its effects on the tribocorrosion resistance (wear inside a corrosive environment).					<B>Methods</B>: Titanium grade 4 samples were anodized using an electrolyte containing diethylene glycol, hydrofluoric acid and an aqueous solution containing Ca and P ions. Samples were analyzed using Scanning Electron Microscopy and X-ray diffraction, and submitted to tribocorrosion tests.					<B>Results</B>: Results show that samples with larger amount of rutile phase were more resistant to tribocorrosion, when compared to samples with more anatase phase.					<B>Conclusion</B>: Thus, heat treatments that favors the crystallization of rutile phase are more suitable to implants with needed resistance when submitted to wear and corrosion process associated.				</P>
			</div><B>Keywords</B>: Titânium, biomaterials, nanotubes, tribocorrosion</td>
