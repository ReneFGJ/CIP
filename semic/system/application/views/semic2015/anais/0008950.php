			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Através dos registros escritos descobrimos e aprendemos culturas, histórias e hábitos diferentes, compreendemos a realidade, o sentido real das ideias, vivências, sonhos, entre outros.					<B>Objetivo</B>: Como objetivos, coloca-se, como objetivo geral, compreender a influência da literatura no desenvolvimento da alfabetização e letramento. Como objetivos específicos, discutir com professores alfabetizadores o papel da literatura no desenvolvimento da alfabetização e letramento, como ocorre este processo na prática educacional e se o órgão responsável oferece estrutura adequada; observar nas práticas os segmentos discutidos.					<B>Metodologia</B>: Diante do fato, pode-se considerar a leitura como uma das mais importantes tarefas que a escola tem que ensinar, mas é importante ressaltar que para isso o professor deve ter consciência da necessidade, além de praticar com eficiência o hábito da leitura, essa prática deve se dar todos os dias, fazendo assim com que os discentes tomem gosto pelo mesmo, e não vejam como uma atividade cansativa e não prazerosa a leitura é papel principal e fundamental para a alfabetização e letramento de nossos alunos.					<B>Resultados</B>: 					<B>Conclusões</B>: Conclui-se que oportunizar para as crianças o mundo das histórias, é oportunizar uma facilitação para a alfabetização futuramente, pois com as histórias as crianças já têm a familiaridade do mundo das letras e a vontade de decifra-las				</P>
			</div><B>Palavras-chave</B>: Educação. literatura. alfabetização. letramento.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Through the written records discovered and learn different cultures, histories and habits, understand the reality, the real meaning of ideas, experiences, dreams, among others.					<B>Objectives</B>: As objectives, arises as a general goal, understand the influence of literature in the development of literacy and literacy. The specific objectives, discuss with literacy teachers the role of literature in the development of literacy and literacy, as this process occurs in educational practice and the responsible body offers adequate structure; observed in the practices discussed segments.					<B>Methods</B>: Given the fact, one can consider reading as one of the most important tasks that the school has to teach, but it is important to note that for this the teacher must be aware of the need, in addition to practicing efficiently the reading habit, this practice It should be given every day, thus making the students take taste for it, and not see it as an exhausting activity and not pleasant reading is fundamental and leading role for literacy and literacy of our students.					<B>Results</B>: 					<B>Conclusion</B>: We conclude that create opportunities for children the world of stories, it is to create opportunities for literacy facilitation future, because with the stories the children already have the familiarity of the letters world and the will to decipher them				</P>
