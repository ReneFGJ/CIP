			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A presente pesquisa situa-se no ramo do Direito Processual Civil.					<B>Objetivo</B>: Tem por objetivo analisar os limites da responsabilidade dos sócios em execução por dívidas da sociedade limitada, ou seja, em que situações os bens particulares dos sócios poderão ser atingidos e expropriados em execução movida em face da sociedade empresária limitada de cujas quotas sejam titulares. Foi analisado o assunto também sob a ótica do Novo Código de Processo Civil.					<B>Metodologia</B>: A pesquisa é bibliográfica e o método, dedutivo. Foram buscados elementos e conceitos na doutrina e em decisões jurisprudenciais sobre a matéria.					<B>Resultados</B>: Dentre os resultados obtidos, destaca-se que os limites para a responsabilização dos sócios coincidem basicamente com os pressupostos para a aplicação do instituto da desconsideração da personalidade jurídica, e que, em se tratando de dívidas trabalhistas ou fiscais, tais limites praticamente deixam de existir, tendo em vista as características dos créditos tutelados.					<B>Conclusões</B>: Conclui-se que os limites para a responsabilização dos sócios dentro de uma sociedade limitada podem extrapolar aqueles definidos na própria lei, sendo que não há normativas claras e explicitas delimitando tais responsabilidades				</P>
			</div><B>Palavras-chave</B>: Execução - Sócios - Responsabilidade patrimonial - Limites			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The research is located in Civil Procedural Law area.					<B>Objectives</B>: The research aims to analyze the limits of liability of the partners in seizure of a limited liability company: in situations in which the private assets of the partners can be expropriated moved in execution in face of a limited liability company of which they are partners. The analysis has also been made from the perspective of the new Civil Procedure Law Code.					<B>Methods</B>: The research was produced bibliographically and the deductive method was used.					<B>Results</B>: Among the results there is emphasis on the limits for the liability of partners that basically coincides with the conditions for application of disregarding an entity, and in the case of labor or tax debts such limits are ignored considering the nature of the protected claims.					<B>Conclusion</B>: It can be concluded that the limits for the liability of the partners in a company can overpass those defined in the law itself, and there is no clear and explicit legislation delimiting such liabilities.				</P>
