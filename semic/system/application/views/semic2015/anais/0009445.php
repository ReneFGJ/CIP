			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: No âmbito do monitoramento ambiental atmosférico por meio de organismos vivos, biomonitoramento, os organismos mais utilizados são as plantas, por conta da fácil coleta e da larga distribuição geográfica.					<B>Objetivo</B>: Este projeto teve como objetivo realizar o mapeamento de bromélias acopladas à rede elétrica em 6 pontos do município de Toledo a fim de juntamente com o Instituto Ambiental do Paraná (IAP), compor um levantamento de dados para o munícipio.					<B>Metodologia</B>: A partir da seleção de 6 pontos no município de Toledo, foi realizada a coleta de exemplares da espécie [e]lt;i[e]rt;Tillandsia recurvata L.[e]lt;/i[e]rt;, popularmente conhecida como bromélia e realizados testes de medições de metais pesados (Cu, Zn e Pb).					<B>Resultados</B>: Dentre os metais avaliados na espécie, as concentrações mais acentuadas foram a de Cobre e Zinco, prevalecendo ainda o Zinco, enquanto que para o Chumbo foi observado apenas alguns traços.					<B>Conclusões</B>: O biomonitoramento utilizando-se a [e]lt;i[e]rt;Tillandsia[e]lt;/i[e]rt;, recurvata mostrou-se uma ferramenta favorável para realização da avaliação da condição atmosférica por metais pesados do município de Toledo.				</P>
			</div><B>Palavras-chave</B>: Biomonitoramento. Bromélias. [e]lt;i[e]rt;Tillandsia recurvata L.[e]lt;/i[e]rt;.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Under the atmospheric environmental monitoring through living organisms, biomonitoring, the most commonly used organisms are plants, due to the easy collection and wide geographic distribution.					<B>Objectives</B>: This project was aimed at making bromeliads mapping coupled to the power grid in 6 points of the city of Toledo in order to along with the Environmental Institute of Paraná (IAP), compose a data collection for the municipality.					<B>Methods</B>: From the selection of 6 points in the city of Toledo, was held to collect specimens of the species [e]lt;i[e]rt;Tillandsia recurvata L.[e]lt;/i[e]rt;, popularly known as bromeliads and carried heavy metals measurements tests (Cu, Zn and Pb).					<B>Results</B>: Among the metals evaluated in the species, the most significant concentrations of the copper and zinc, still prevailing Zinc, while for the lead was only observed some traces.					<B>Conclusion</B>: Among the metals evaluated in the species, the most significant concentrations of the copper and zinc, still prevailing Zinc, while for the lead was only observed some traces.				</P>
