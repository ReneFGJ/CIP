			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O homem é formado a partir das circunstâncias nas quais ele vive e se
desenvolve. A sociedade contemporânea vive de acordo com seus valores, e esses
influenciam diretamente na maneira de pensar e de agir do homem contemporâneo.
A sociedade atual se mostra de forma individualista e utilitarista. A Noção de Pessoa
em Emmanuel Mounier resgata a vocação humana de um desenvolvimento integral,
no qual o homem consegue associar tanto as esferas materiais quanto espirituais do
seu ser e com isso consegue estabelecer um movimento de transcendência do
individuo e que culmina em um ato de relação para com o OUTRO.					<B>Objetivo</B>: O
personalismo comunitário foca-se na relação entre pessoas que se respeitam.
Portanto, nossa pesquisa se voltará para a análise dos pressupostos filosóficos
sobre os quais o autor desenvolve seu pensamento, bem como a estrutura do
projeto de personalização por ele desenvolvido.					<B>Metodologia</B>: A metodologia aplicada
nesta pesquisa se deu pela pesquisa bibliográfica seguida da leitura dos textos de
Emmanuel Mounier e seus comentadores. O passo seguinte foi a produção de
fichamentos, resumos e de consulta a trabalhos publicados na área, que auxiliaram
na produção das análises.					<B>Resultados</B>: Os resultados obtidos no percurso da
pesquisa foram um maior aprofundamento nas correntes teóricas de que Mounier se
utilizou em seu projeto e o entendimento dos seus conceitos basilares do movimento
de “ser” pessoa.					<B>Conclusões</B>: A partir da análise e dos resultados obtidos, verificouse
a importância tanto da pesquisa dos pressupostos filosóficos de Emmanuel
Mounier, quanto da análise dos seus conceitos. Desta forma, foi possível construir e
entender melhor sua linha de pensamento.				</P>
			</div><B>Palavras-chave</B>: Personalismo. pessoa. transcendência.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: A man is formed through the circumstances in which he lives and
develops himself. Present-day society lives according to its values, and these values
directly influence the way of thinking and acting of the contemporary man. This
society presents itself in an individualist and utilitarian way. The concept of person in
Emmanuel Mounier brings back the human calling for a wholesome development, in
which the man can associate both the material spheres and the spiritual spheres of
his being, and with that, he can establish a transcendence movement of the person
that finds its apex in a relational act towards ANOTHER.					<B>Objectives</B>: Communitarian
personalism focuses on the relationships between people who respect each other.
Thus, this research is turned towards the analysis of the philosophical bases with
which the author develops his thinking, as well as the structure of the project of
personalization developed by him.					<B>Methods</B>: The methodology applied in this
research was comprised of bibliographical research followed by the reading of texts
by Emmanuel Mounier and his commenters. The next step was the making of text
reports, summaries and consults to other works published in the field, which helped
in conducting the analyses.					<B>Results</B>: The results obtained in the course of the
research were the acquirement of deeper knowledge about the theoretical currents of
which Mounier made use of in his project and the understanding of his fundamental
concepts of person.					<B>Conclusion</B>: From the analysis and the obtained results, it was
possible to ascertain the importance of both researching the philosophical bases of
Emmanuel Mounier and analyzing his concepts. This way, it was possible to build a
better understanding of his line of thought.				</P>
