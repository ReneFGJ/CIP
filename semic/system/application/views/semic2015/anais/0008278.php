			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A presente pesquisa tem como tema a viabilização da Justiça Restaurativa, num território com elevados índices violentos, como a Vila Torres, localizada no Prado Velho, bairro de Curitiba, PR.					<B>Objetivo</B>: Através do modelo restaurativo de justiça, buscou-se analisar como este novo conceito jurídico pode ajudar na diminuição da violência local, partindo da crítica ao atual modelo tradicional de justiça retributiva. 					<B>Metodologia</B>: O estudo utilizou métodos qualitativos de pesquisa no levantamento de dados estatísticos de instituições como IBGE, IPPUC e a Polícia Civil do Estado do Paraná, e na pesquisa bibliográfica, do qual apresenta autores como Pedro Scuro Neto, Ivonete Granjeiro, Boaventura de Souza Santos, Giorgio Agamben, Thomas Hobbes, Jürgen Habermas, dentre outros. 					<B>Resultados</B>: Foi diagnosticado um elevado índice de homicídios na Vila Torres, bem como a falta de políticas públicas efetivas no local. Foi observável a atuação da justiça tradicional no local e sua ineficiência, pois numa grande maioria houve a reincidência dos infratores. No que tange a Justiça Restaurativa, foi reconhecido sua capacidade baseado na sua aplicação em outros territórios como Nova Zelândia, Estado Unidos, Inglaterra, Colômbia e em alguns locais dentro do Brasil, como Distrito Federal e Porto Alegre. 					<B>Conclusões</B>: Fora concluso, portanto, que a Justiça tradicional que atua no momento é em alguns casos, ineficaz, haja vista que não recupera o infrator, somente o pune. Desta forma, a Justiça Restaurativa poderia viabilizar esta recuperação, pois tem em sua essência a restauração dos envolvidos no ato infracional. Desta forma, o modelo restaurativo tornaria a comunidade Vila Torres menos violenta e mais participativa da sociedade.				</P>
			</div><B>Palavras-chave</B>: Justiça Restaurativa; Vila Torres; Justiça Tradicional; Direitos Humanos.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: This research has as its theme the viability of Restorative Justice, a territory with high violence rates, such as Vila Torres, located at the Prado Velho, Curitiba neighborhood.					<B>Objectives</B>: Through restorative justice model, it sought to analyze how this new legal concept can help in reducing local violence, based on the criticism of the current traditional model of retributive justice. 					<B>Methods</B>: The study used qualitative research methods in the production of statistical data of institutions like IBGE, IPPUC and the Civil Police of Paraná, and literature, which features authors such as Pedro Scuro Neto, Ivonete Granjeiro, Boaventura de Souza Santos, Giorgio Agamben, Thomas Hobbes, Jürgen Habermas, among others.					<B>Results</B>: A high homicide rate in Vila Torres was diagnosed, and the lack of effective public policies in place. The performance of traditional justice was observable in place and its inefficiency, because a majority was recidivism of offenders. With respect to restorative justice, it was recognized his ability based on its application in other territories such as New Zealand, United States, England, Colombia and in some places within Brazil, as the Federal District and Porto Alegre.					<B>Conclusion</B>: Outside conclusion, therefore, that the traditional justice which operates at the moment is in some cases ineffective, given that the offender does not recover, only punishes. Thus, restorative justice could facilitate this recovery as it has at its core the restoration of those involved in the offense. Thus, the model would make the community restorative Vila Torres less violent and more participatory society.				</P>
			</div><B>Keywords</B>: Restorative Justice. Vila Torres. Traditional Justice. Human Rights.</td>
