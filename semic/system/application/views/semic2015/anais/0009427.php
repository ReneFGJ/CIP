			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Plutarco, historiador grego que se dedicou a escrever “Vidas”, segundo suas próprias palavras, tem uma série de textos no qual este nos fornece uma comparação entre grandes figuras gregas e romanas. No caso de César, objeto do presente estudo, a comparação é feita tomando Alexandre como base. César, por sua vez, é colocado por Plutarco como hábil político e general desde o início de sua carreira, destacando-se perante os outros.					<B>Objetivo</B>: Para minha última etapa como graduanda da PUCPR, realizei a análise do texto de maneira deveras meticulosa, buscando encontrar aspectos tanto positivos quanto negativos da figura de César.					<B>Metodologia</B>: Para tanto, em um primeiro momento foi realizado um levantamento bibliográfico extenso acerca do contexto histórico tanto de César quanto de Plutarco, a fim de compreender em que tipo de realidade ambos viviam. Realizada esta etapa, dediquei-me a uma minuciosa análise do texto de Plutarco, buscando elementos que pudessem me auxiliar a compreender que tipo de imagem este criou, nesse texto, de César.					<B>Resultados</B>: De início, percebi que há uma distinção entre a criação da imagem de César por Plutarco e da imagem de César criada em outros textos, a saber, o De Bello Gallico e o Bellum Civile, ambos de autoria de Júlio César. No texto de Plutarco, César é colocado em como bom general e político, mas seus equívocos não são ignorados, pelo contrário, Plutarco os descreve da mesma maneira como descreve os acertos de César. Nos textos De Bello Gallico e Bellum Civile, César opta pelo silêncio em relação à esses erros.					<B>Conclusões</B>: Por fim, vale colocar que Plutarco descreve César de uma maneira mais geral, sem deixar de lado os aspectos negativos da personalidade de César, muito embora percebe-se que o tom da narrativa é mais favorável que desfavorável à Júlio César.				</P>
			</div><B>Palavras-chave</B>: Júlio César, Plutarco, Vidas Paralelas.			<BR>
