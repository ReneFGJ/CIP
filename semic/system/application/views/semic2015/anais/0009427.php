			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Plutarch, Greek historian, dedicated himself to write ‘’Lifes’’, according to his own words, and has a number of texts in which he gives us a comparison between Greeks and Romans. In Caesar’s case, object of the present work, the comparison is made taking Alexander as a role model. Caesar in Plutarch’s texts is described as a skilled politician and general since the beginning of his political career, highlighting himself among the others.					<B>Objectives</B>: For this last stage of my graduation at PUCPR, I analyze vary meticulously Plutarch’s text ‘’Parallel Lifes: Alexander and Caesar’’, trying to find positive and negative aspects of Caesar’s figure.					<B>Methods</B>: For that, at first I made a large research on the historical context of Plutarch and Caesar’s life, trying to answer in which reality the bough lived. After that, I dedicated myself in a meticulous analyzing of Plutarch’s work, seeking for elements that could help me to comprehend what kind of image Plutarch created of Julius Caesar.					<B>Results</B>: At first, I realized that there is a distinction between the creations of Caesar’s image in Plutarch’s texts and the texts written by Caesar, De Bello Gallico e Bellum Civile. In Plutarch’s work, Caesar is described as a skilled general and politician, but his mistakes are not ignored, on the contrary, the mistakes and the successes are described the same way. When analyzing Caesar’s work, a silence is noticed in those moments when Caesar makes a mistake.					<B>Conclusion</B>: Lastly, is important to comment that Plutarch describes Caesar in a more general way, not hiding the negative aspects of Caesar’s personality, even though is noticeable that the tom in the narrative is more friendly than unfriendly to Caesar.				</P>
