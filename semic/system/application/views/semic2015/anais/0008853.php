			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O luto pode ser entendido como um conjunto de comportamentos e sentimentos decorrentes da perda de alguém ou algo, sendo que o nome é usado principalmente para tratar a reação a perdas por morte de um ente querido. Considerando a universilidade do luto e o sofrimento que acarreta nos enlutados, esse fenômeno é constante foco da atenção de profissionais da saúde, principalmente psicólogos.					<B>Objetivo</B>: Identificar a tratativa e entendimento de analistas do comportamento e da proposta Behaviorista Radical sobre o fenômeno do luto.					<B>Metodologia</B>: Busca das publicações do período de 2001 a 2015 nas bases de dado Redalyc, Proquest, Capes, Scielo e Lilacs que abordem a temática do luto sob o enfoque da Análise do Comportamento.					<B>Resultados</B>: Foram encontrados apenas cinco artigos que atendem tais critérios, sendo dois estudos conceituais por pesquisadores irlandeses e três pesquisas norte americanas focadas no método da Ativação Comportamental utilizando ensaio clínico randomizado para avaliar a eficácia de tais aplicações.					<B>Conclusões</B>: Identificou-se assim uma grande lacuna nas produções científicas analítico-comportamentais que abordem o tema, bem como as dificuldades de elaborar estudos experimentais e fiéis aos pressupostos epistemológicos e filosóficos da Análise do Comportamento.				</P>
			</div><B>Palavras-chave</B>: Luto. análise do comportamento. behaviorismo radical.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Grief can be understood as a set of behaviors and feelings resulting from the loss of something or someone, even though the term is usually used to describe one´s reaction towards the death of a loved one. Considering the universality and the suffering it brings to bereaved this phenomenon is target of constant attention from health professionals, specially psychologists.					<B>Objectives</B>: Identify how Behavior Analysis comprehend and treat grief and bereavement according to the proposals of Behavior Analysis.					<B>Methods</B>: The selected method consist in a research on the following databases for articles from the years 2001 to 2015, concerning grief and Behavior Analysis: Redalyc, CAPES, Proquest, Lilacs and Scielo.					<B>Results</B>: Five articles were found that attend to all search criteria from this paper, two conceptual articles from north irish researchers and three applied randomized clinical trials from north american researchers.					<B>Conclusion</B>: It was possible to identify a huge gap on behavior analysis productions that bring up grief as well as the difficulties to elaborate experimental studies that attend to Behavior Analysis´ epistemology and philosofical assumptions.				</P>
