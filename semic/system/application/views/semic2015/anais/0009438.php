			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Todas as ações desenvolvidas durante a formação inicial de professores devem tem como foco a práxis do professor. Para isso torna-se importante desenvolver o aprendizado de conhecimentos teóricos e estimular práticas. Nesse processo a reflexão será uma característica fundamental, pois ao levar o conhecimento a um contexto real, o estudante será estimulado a entender onde e como atuar e retornará à busca de novos conhecimentos que emergem de sua ação.					<B>Objetivo</B>: O objetivo desse estudo foi analisar a prática pedagógica de acadêmicos do 4º ano da Licenciatura em Educação Física, durante as aulas no Projeto PIBID; analisar como os saberes trabalhados na graduação são incorporados pelos acadêmicos em suas aulas e analisar a perspectiva dos acadêmicos frente a sua prática pedagógica, os saberes trabalhados e a sua influência na formação da identidade profissional.					<B>Metodologia</B>: A pesquisa foi do tipo descritiva-qualitativo. Os participantes foram 14 acadêmicos do curso de Licenciatura em Educação Física PUCPR, dos últimos períodos da graduação e com participação no Projeto PIBID. Foi utilizado duas formas de coleta de dados, um questionário com seis questões e um quadro com respostas abertas e a observação de aulas aplicadas por cinco acadêmicos					<B>Resultados</B>: Os resultados demonstram que a maioria dos acadêmicos possui uma percepção consistente do que é ser professor e dos conceitos a serem trabalhos na profissão; indicam que a experiência da participação no PIBID contribui com a formação e com a prática pedagógica e que os saberes pedagógicos, do contexto educacional e dos conteúdos são estudados e aplicados no contexto da escola.					<B>Conclusões</B>: Conclui-se a influencia positiva que o projeto PIBID agrega na formação e na pratica pedagógica e profissional do acadêmico participante do projeto.				</P>
			</div><B>Palavras-chave</B>: Formação. saberes. PIBID			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: All actions taken during the initial teacher training must focuses on the practice of the teacher. For this it is important to develop the learning of theoretical knowledge and encourage practices. In this process the reflection will be a key feature as the lead knowledge to a real context, the student will be encouraged to understand where and how to act and return to the search for new knowledge emerging from their action.					<B>Objectives</B>: The aim of this study was to analyze the pedagogic practice of students of the 4th year of the Degree in Physical Education, during classes in PIBID Project; analyze how knowledge worked at graduation are incorporated by academics in their classes and analyze the perspective of academic front his pedagogical practice, worked knowledge and its influence on the formation of professional identity.					<B>Methods</B>: The research was descriptive-qualitative. Participants were 14 students from the Bachelor´s Degree in Education PUCPR Physics, the last periods graduation and participation in PIBID Project. It used two forms of data collection, a questionnaire with six questions and a frame with open answers and classroom observation applied for five academics.					<B>Results</B>: The results show that most academics have a consistent perception of being a teacher and concepts to work in the profession; indicate that the experience of participation in PIBID helps with training and teaching practice and the pedagogical knowledge, the educational context and content are studied and applied in the school context.					<B>Conclusion</B>: It concludes the positive influence that the project adds PIBID training and educational and vocational practices of project participant academic.				</P>
