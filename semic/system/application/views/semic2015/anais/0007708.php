of Fanconi anemia are heterogeneos. Abnormalities in at least four genes, defined by complementation groups A, B,C, and D, result in increased DNA cross-linking sensitivity that characterizes Fanconi anemia. Group C complementing gene (FACC) was recently cloned.Mutation analysis suggests
