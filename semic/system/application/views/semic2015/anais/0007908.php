			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Foi efetuado um levantamento arquitetônico e hidráulico da edificação (cozinha comunitária), coleta dos dados pluviométricos das cidades de Paranaguá e Ilha do Mel, identificação do déficit máximo anual no mesmo período da coleta de chuva, dimensionamento do reservatório de captação das águas (Método Máximo Déficit Acumulado, de Gomide (1959) e adaptado por Miranda (2007).					<B>Objetivo</B>: a)	Efetuar o Levantamento Arquitetônico e Hidráulico da Cozinha Comunitária da Associação das Mulheres da Ilha das Peças/PR; 
b)	Coletar os dados de precipitações diárias da Cidade de Paranaguá e da Ilha do Mel; 
c)	Identificar o Máximo Déficit Anual, por consumo, no período de dados das precipitações diárias, em um regime inter-anual;
d)	Dimensionar o Reservatório de Captação de Águas Pluviais, utilizando como Método o Máximo Déficit Acumulado, proposto por Gomide (1959) e adaptado por Miranda (2007);
e)	Projetar o sistema predial de tratamento e distribuição das águas pluviais destinadas ao consumo da Cozinha Comunitária da Associação das Mulheres da Ilha das Peças/PR.					<B>Metodologia</B>: A análise do volume de regularização, foi fundamentada nos dados hidrológicos históricos de Paranaguá, pela proximidade da Ilha das Peças. A estação meterológica utilizada, possui cadastro na ANEEL, sob o nº 02548005, possuindo dados histórico de 1948 a 1980.					<B>Resultados</B>: O reservatório a ser implantado será de 10,00 m³, pois atende a demanda e a possibilidade de faltar água é de aproximadamente 1,23%					<B>Conclusões</B>: O método de regularização, mostrou-se eficiente, como recomendação os estudos devem ser complementados para atender as 200 edificações da Ilha das Peças.				</P>
			</div><B>Palavras-chave</B>: Máximo Déficit Acumukado. Água de Chuva. Regularização de Vazão.			<BR>
