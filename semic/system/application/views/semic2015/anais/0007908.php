					<B>Introduction</B>: An architectural and hydraulic lift of the building was made ( community kitchen ) , collecting rainfall data from the cities of Paranagua and Ilha do Mel, identification of the maximum annual deficit in the same period of rainwater harvesting , sizing of the water collection tank ( Maximum Method Accumulated deficit , of Gomide (1959 ) and adapted by Miranda ( 2007) .					<B>Objectives</B>: a) Make the Architectural Survey and Hydraulic Community Kitchen of the Association of Women of the Island of Parts / PR;
b) Collect data daily rainfall of the city of Paranagua and Ilha do Mel;
c) Identify the Yearly deficit, for consumption in the data period of daily rainfall in an inter-annual basis;
d) Scale the Water Catchment Reservoir rainwater, using as method the Maximum Accumulated Deficit proposed by Gomide (1959) and adapted by Miranda (2007);
e) Design the building treatment and distribution system of rainwater for consumption of the Community Kitchen of the Association of Women of the Island of Parts / PR.					<B>Methods</B>: Analysis of settlement volume , was based on historical hydrological data of Paranaguá , the proximity of the parts Island. The meterológica station used , has joined the ANEEL , under number 02548005 , having historical data from 1948 to 1980 .					<B>Results</B>: 					<B>Conclusion</B>: The method of regularization, was efficient, as recommended studies should be supplemented to meet the 200 buildings of the parts Island .				</P>
