			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Este estudo trata da Astronomia na Física Escolar e das novas linguagens na abordagem do ensino dessa disciplina. Visa que seu uso torne-a mais aprazível e lúdica em sala de aula.					<B>Objetivo</B>: A pesquisa teve por objetivo a análise da evolução dos conceitos científicos da astronomia no ensino da Física Escolar, a partir do levantamento bibliográfico de artigos e livros, a fim de instituir novas práticas pedagógicas na formação dos professores. Tendo como base a Aprendizagem Significativa (MOREIRA, 1999), da reflexão diante da informação, em tensão a aprendizagem.					<B>Metodologia</B>: Fora escolhida uma bibliografia principal, esta se trata de “O telescópio na magia natural de Giambattista Della Porta” escrito por Fumikazu Saito (2011), que permitiu a determinação do período a ser analisado, sendo este do século XVI ao XVII, e por consequência os artigos e periódicos necessários para complementação da pesquisa.					<B>Resultados</B>: As leituras realizadas possibilitaram a identificação das contribuições e argumentações que os instrumentos referentes à História da Ciência nos oferece para a prática docente. De forma geral, é claramente perceptível a contribuição do estudo de História da ciência, para a prática docente					<B>Conclusões</B>: A partir do momento em que nós educadores passamos a interpretar a Física além da dimensão matemática, somos capazes de influenciar e convencer nossos educandos que tudo o que é apresentado de forma fragmentada em sala de aula, está interligado e nos permite compreender um pouco melhor os fenômenos do nosso mundo.				</P>
			</div><B>Palavras-chave</B>: Ensino de Física, História da Ciência, Astronomia.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: This study deals with the Astronomy in the School Physical and new languages in the teaching approach of this discipline. Visa that its use makes it more pleasant and playful in the classroom.					<B>Objectives</B>: The research aimed at the development of scientific astronomy concepts in the teaching of school physical from the literature articles and books, in order to establish new teaching practices in teacher training.					<B>Methods</B>: Based on the Meaningful Learning (MOREIRA, 1999), reflection on the information in tension learning. Was chosen a main bibliography, this is about "The telescope in the natural magic of Giambattista Della Porta" written by Fumikazu Saito (2011), which allowed the determination of the period under consideration, which is the sixteenth to the seventeenth century, and consequently articles and periodicals needed for completion of the research.					<B>Results</B>: The readings made it possible to identify the contributions and arguments that instruments relating to the History of Science offers us for teaching practice. In general, it is clearly noticeable contribution to the study of history of science, for teaching practice.					<B>Conclusion</B>: From the moment we passed educators to interpret the physics beyond the mathematical dimension, we are able to influence and convince our students that everything is presented in a fragmented way in the classroom, is interconnected and allows us to understand a little better the phenomena of our world.				</P>
