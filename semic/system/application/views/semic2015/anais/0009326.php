			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Leaf-cutting ants stand out as one of the major problems in national agriculture, the precariousness of combat methods defiinindo it as very important pest in crops, causing significant economic damage.					<B>Objectives</B>: This study aimed to evaluate the bioactivity of aqueous extract of Peschiera australis (dairy-two-brothers) on the control Camponotus rufipes (carpenter ants).					<B>Methods</B>: The aqueous extract was obtained using the extraction method in cold water. He was employed the filter paper technique for the assessment of contaminated number of dead insects. The definitive bioassay were selected pH 10 concentrations six (6) to determine the mortality rate. Data were subjected to analysis of variance and regression testing					<B>Results</B>: The regression identified highly significant difference between treatments. The best results were obtained mortality on the extract with pH 6.0 where the average mortality was 6.25, and the mortality reached 63%. In all other statements at different pH was observed mortality of ants.					<B>Conclusion</B>: Based on the results of the experiment and the conditions were the experiment was conducted can be concluded that aqueous extract of P. australis was effective in controlling ants.				</P>
