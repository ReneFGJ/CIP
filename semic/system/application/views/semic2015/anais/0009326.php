			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: As formigas cortadeiras destacam-se como um dos principais problemas da agricultura nacional, com a precariedade dos métodos de combate são definidas como pragas causadoras de danos econômicos significativos.					<B>Objetivo</B>: Este trabalho teve por objetivo avaliar a bioatividade do extrato aquoso da [e]lt;i[e]rt;Peschiera australis[e]lt;/i[e]rt; (leiteira-dois-irmãos) no controle de [e]lt;i[e]rt;Camponotus rufipes[e]lt;/i[e]rt; (formigas carpinteiras).					<B>Metodologia</B>: O extrato aquoso foi obtido por meio do método de extração em água fria. Foi empregada a técnica de papel de filtro contaminado para a avaliação do número de insetos mortos. No bioensaio definitivo, foram selecionadas 10 concentrações de pH 6 (seis) para determinação da taxa de mortalidade. Os dados foram submetidos à análise de variância e regressão linear.					<B>Resultados</B>: A regressão identificou diferença altamente significativa entre os tratamentos. Os melhores resultados de mortalidade foram obtidos em extrato com pH 6,0 onde a média de mortalidade foi de 6,25, e a mortalidade atingiu 63%. Em todos os outros extratos nos diferentes pH observou-se mortalidade das formigas.					<B>Conclusões</B>: Com base nos resultados e nas condições de realização do experimento podemos concluir que extrato aquoso de [e]lt;i[e]rt;P. australis[e]lt;/i[e]rt; teve efeito no controle de formigas. Recomenda-se o aprofundamento do estudo com extratos mais concentrados obtidos em pH 6,0 usando a água como solvente.				</P>
			</div><B>Palavras-chave</B>: Manejo de pragas. Compostos bioativos. Formigas cortadeiras			<BR>
