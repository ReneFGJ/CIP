			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: As transformações do cenário político-econômico dos espaços
nacionais representa a tendência globalizadora que vem mudando as relações
na sociedade, principalmente aquelas relacionadas ao campo do trabalho. São
mudanças no modo de produção que conduzem a reconfiguração das relações
de trabalho, levando a precariedade e, inclusive, a flexibilização da
regulamentação trabalhista. Dentre outros fatores, a volatilidade da economia,
resultado das mudanças globais, contribuiu para a concretização de colapsos
financeiros que antes representavam meros riscos. O contexto global em
transformação e a crise financeira que atingiu o continente europeu em 2008
acarreta um cenário de aumento do desemprego, especialmente ao grupo dos
mais jovens da população.					<B>Objetivo</B>: A partir disso, a pesquisa se propõe a
analisar este cenário de escassez de emprego ante o aumento evidente desde o
advento da crise financeira e as mudanças decorrentes da globalização.					<B>Metodologia</B>: Para isso, foi proposto partir de abordagens singulares referentes à
temática, nos aprofundássemos na análise proposta.					<B>Resultados</B>: Além de lidar
com um cenário de desvalorização do capital humano e de estreitamento das
ofertas de emprego, decorrentes das transformações do mundo do trabalho, o
jovem ainda tem que enfrentar a sua natural fragilidade frente ao mercado de
trabalho, representado pelas resistências em absorvê-lo, em virtude da tenra
idade e falta de experiência, levando-os a empregos precários, ao
desencorajamento, a baixas perspectivas de futuro e ao eminente risco de
exclusão social.					<B>Conclusões</B>: A verificação do aumento dos índices de
desemprego entre os jovens no continente alarmam para a tendência global de
reconfiguração das formas, relações e dificuldades a serem encontradas no
mercado de trabalho. Em vista da crise financeira, o desemprego parece emergir
como uma dívida inesperada a ser paga pela juventude presente e futura.				</P>
			</div><B>Palavras-chave</B>: Globalização econômica; Precarização; Crise financeira; Desemprego juvenil.			<BR>
