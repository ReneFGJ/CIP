			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Peritoneal dialysis patients present an extremely high mortality rate caused mainly by cardiovascular diseases. Several risk factors contribute to these numbers, making studies that look for new tools of early detection of high risk patients of particular importance.					<B>Objectives</B>: Our aim was to analyse blood pressure behaviour during the year preceding death in peritoneal dialysis patients.					<B>Methods</B>: The study is based in the BRAZPD II cohort, a prospective study involving 122 centers from Brazil and followed between December 2004 and January 2011. Blood pressure was measure every month by a trained nephrology team. Results were reported using mean and standard deviation during the year preceding death. Similarly, we looked at the same data in subgroups of patients divided according to age and diabetes status. All data were performed using the  software SPSS 20.0.					<B>Results</B>: Out of a total of 9905 patients, we selected all 985 individuals who have died along the study period for any cause and that were followed for at least 1 year. We found a progressive  reduction in blood pressure levels (systolic and diastolic) in the six months before death. Such reduction occurred homogeneously in all subgroups analysed.					<B>Conclusion</B>: In conclusion, the observed pattern of reduction in the blood pressure may serve in the future as a tool to identify high risk patients aiming to either reduce the mortality rate and, of particular importance, to offer a better quality of life for the patient and their families when death is inevitable.				</P>
