			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A mortalidade dos pacientes em diálise é extremamente elevada e ocorre principalmente por causas cardiovasculares. Vários fatores de risco contribuem para esses números elevados, o que faz com que estudos que permitam identificação mais precoce dos pacientes de alto risco sejam de alta relevância.					<B>Objetivo</B>: Nosso objetivo foi avaliar pela primeira vez o comportamento da pressão arterial no ano que precede a morte dos pacientes em diálise peritoneal.					<B>Metodologia</B>: Método: Para análise foram utilizados os dados do BRAZPD II, uma coorte prospectiva nacional envolvendo 122 centros, com seguimento de dezembro de 2004 a janeiro de 2011. As aferições da pressão arterial eram mensais e realizadas por equipe previamente treinada. A média da pressão arterial mês a mês no ano que precedeu o óbito foi reportada com seu respectivo desvio padrão. Exploramos também o mesmo comportamento da pressão arterial nos subgrupos com idade maior e menor de 65 anos, e no de diabéticos e não diabéticos. Os dados foram analisados por meio do software SPSS 20.0.					<B>Resultados</B>: Do total de 9905 indivíduos da coorte, foram selecionados 985 que morreram por qualquer causa ao longo do estudo após permanecerem no mínimo 12 meses em acompanhamento. Observamos uma redução progressiva da pressão arterial nos 06 meses que antecedem ao óbito, tanto da pressão sistólica quanto da diastólica, e de maneira homogênea nos subgrupos divididos por idade e diabetes.					<B>Conclusões</B>: Em conclusão, o padrão de redução da pressão arterial encontrado pode vir a servir como ferramenta de monitoramento de pacientes de alto risco de morte com o intuito eventualmente reverter o desfecho e, principalmente, oferecer maior qualidade de vida para o paciente e para a família.				</P>
			</div><B>Palavras-chave</B>: Dialipe peritoneal. Mortalidade. Pressão arterial			<BR>
