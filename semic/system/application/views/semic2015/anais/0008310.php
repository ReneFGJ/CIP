			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A fim de diminuir a sensibilização do paciente, uma das alternativas aplicadas é a terapia com imunoglobulina intravenosa, que tem efeitos sobre a imunidade humoral e celular. Apesar da aplicação para esse fim ser comumente utilizada, a sua eficácia é de aproximadamente 35%, porém de importância clínica significativa para os que respondem, favorecendo a recepção do transplante. Entretanto, a falta de uma ferramenta que possibilite a seleção dos pacientes respondedores, faz com que a escolha por candidatos ocorra de forma randômica e muitas vezes falha.					<B>Objetivo</B>: O objetivo desse estudo foi avaliar, in vitro, o potencial de imunomodulação induzido por essa droga em diferentes dosagens.através da análise da proliferação linfocitária, mediante estímulo de reconhecimento antigênico, e da dinâmica dos linfócitos T CD4+CD25+ e T CD8+CD25+ determinadas por citometria de fluxo.					<B>Metodologia</B>: Foi utilizada a cultura mista de linfócitos e a citometria de fluxo.					<B>Resultados</B>: Entre os pacientes, foi possível estabelecer grupos de pacientes que respondem adequadamente ao tratamento, bem como um outro grupo de pacientes que responde de modo contrário ao desejado.					<B>Conclusões</B>: A metodologia descrita nesse estudo foi capaz de auxiliar, in vitro na seleção de potenciais bom respondedores à terapia.				</P>
			</div><B>Palavras-chave</B>: IVIg. transplante. dessensibilização. cultura mista de linfócitos. citometria de fluxo.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Looking towards allogenic desensitization, one of the therapies available is intravenous immunoglobulin (IVIg), which acts both on humoral and cellular  immune  response.  Although  it  has  been  commonly  used,  its  efficacy  is about 35%, but has major impacts on clinical outcomes on those patients who responds  to  the  treatment, increasing  the  chances  of  a  successful transplantation.					<B>Objectives</B>: Considering the low efficacy, and the lack of a tool to select patients who will respond to this therapy, the objectives of this study were to evaluate, in vitro, the immunomodulatory effects of IVIg in the most commonly  used  dosages.					<B>Methods</B>: For these analyzes were performed  mixed  lymphocyte  reaction  after  allogenic  stimulation and flow  citometry for T  CD4+CD25+  and  T  CD8+CD25+  cells  subsets.					<B>Results</B>: Among the studied patients,  it  was possible  to  define two groups  based on therapy response, wherein one of them shows a response favoring  allograft  transplantation,  and  the  other  reveals  the  opposite  effect.					<B>Conclusion</B>: The methods described by this study were capable to identify good responders to 
the IVIg therapy.				</P>
			</div><B>Keywords</B>: IVIg. transplantation. desensitization.</td>
