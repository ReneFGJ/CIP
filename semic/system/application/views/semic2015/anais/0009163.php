			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Mixed Rain Forest, also popularly known as Araucaria forest is mixed call to contain gymnosperms in consortium with primitive angiosperms genres. It offers well-defined structure and is generally well stratified, comprising an emerging stratum of pines of hearts, reaching an average of 30 meters high and its canopy has about 15-20 meters. Adjustments may represent the mathematical regression of height to diameter of a stand, being called hipsometric relationship. The influence of factors such as soil characteristics, species, density, etc. can affect the behavior of the variables can be seen in the observed values scatterplots about the fitted regression line.					<B>Objectives</B>: The aim of this study is to test mathematical models to estimate the total and commercial height of the trees, based on the height / diameter trees to indicate the best models for Rain Forest Mixed.					<B>Methods</B>: For the calculations, they collected the diameter measured at breast height (DBH), total height (HT) and commercial height (HC). Data were separated into four layers and analyzed ten different designs equations. The best settings were chosen based on the coefficient of determination (R²) and the standard error of estimate (Syx%).					<B>Results</B>: In natural forests there is much variation in heights on the same diameter classes, which tends to undermine the results. In this work we were obtained R² of 0.0038 to 0.9858 and Syx% 17.35 until 50.84.					<B>Conclusion</B>: The adjustment equations for total and commercial height of trees by species showed satisfactory results due to the different characteristics of individuals, because it is Natural Forest.				</P>
