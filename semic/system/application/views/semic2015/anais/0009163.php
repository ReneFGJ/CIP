			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A Floresta Ombrófila Mista, também popularmente conhecida como floresta com araucária é chamada de mista por conter gimnospermas em consorcio com angiospermas de gêneros primitivos. Apresenta estrutura bem definida e é, geralmente, bem estratificada, formada por um estrato emergente de copas de araucárias, que alcançam, em média, 30 metros de altura e seu dossel apresenta cerca de 15-20 metros. Ajustes matemáticos podem representar a regressão da altura em relação ao diâmetro de um povoamento, sendo chamado de relação hipsométrica. A influência de fatores como: características pedológicas, espécie, densidade, entre outras, podem afetar o comportamento das variáveis, podendo ser visto em gráficos de dispersão de valores observados sobre a linha da regressão ajustada.					<B>Objetivo</B>: O objetivo deste trabalho é testar modelos matemáticos para estimar a altura total e comercial das árvores, baseando-se na relação altura/diâmetro das árvores para indicar os melhores modelos para a Floresta Ombrófila Mista.					<B>Metodologia</B>: Para os cálculos, foram coletadas as medidas de diâmetro à altura do peito (DAP), altura total (HT) e altura comercial (HC). Os dados foram separados em quatro estratos e analisados em dez diferentes modelos de equações. Os melhores ajustes foram escolhidos com base no coeficiente de determinação (R²) e no erro padrão de estimativa (Syx%).					<B>Resultados</B>: : Em florestas naturais existe muita variação de alturas em mesmas classes diamétricas, o que tende a prejudicar os resultados. Neste trabalho foram obtidos R² de 0,0038 até 0,9858 e Syx% de 17,35 até 50,84.					<B>Conclusões</B>: O ajuste de equações para altura total e comercial das árvores por espécie apresentaram resultados satisfatórios em função das diferentes características dos indivíduos, por se tratar de Floresta Natural.				</P>
			</div><B>Palavras-chave</B>: Floresta natural. Equações. Altura. Relação h/d			<BR>
