			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Este é um projeto que envolve uma equipe multidisciplinar de professores do Programa de Pós Graduação em Bioética e alunos dos cursos de Nutrição, Psicologia e Veterinária sobre o tema de alimento seguro					<B>Objetivo</B>: Mapear os conhecimentos dos pais ou responsáveis de crianças do 5° ano de 10 turmas de escolas municipais da Região de Curitiba e desenvolver uma dinâmica sobre a temática “alimento seguro” com os alunos.					<B>Metodologia</B>: Esta pesquisa-ação com 184 participantes. O instrumento de pesquisa foi um questionário com questões abertas e fechadas para diagnosticar o conhecimento das famílias sobre higiene, manipulação e consumo dos alimentos Após a entrega do questionário respondido, os dados foram tabulados e informados na forma de gráficos. Esta pesquisa foi aprovada pelo Comitê de Ética e os respondentes assinaram o TCLE. Depois foram realizadas oficinas confeccionada uma Cartilha sobre o tema.					<B>Resultados</B>: A maioria dos participantes desta pesquisa são mulheres (90%) com escolaridade de nível médio completo ou incompleto (45%). Assim, 52% dos entrevistados não sabe a procedência da carne que compra. Segundo Germano e Germano (2011) as carnes bovinas e de frango são os alimentos mais frequentemente relacionados à veiculação de doenças transmitidas por alimentos. Destacando que, dos entrevistados, 54% consomem esse produto 5 ou mais vezes por semana.					<B>Conclusões</B>: Os conhecimentos dos pais ou responsáveis foram identificados a partir de um questionário entregue aos mesmos, contendo questões sobre suas práticas e hábitos no ambiente doméstico sobre manipulação de alimentos e sua alimentação. O conceito de zoonoses é pouco conhecido pelos respondentes o que supões a necessidade de investigar e analisar este tema.
As dinâmicas foram executadas em uma visita as escolas, abrangendo como assunto as principais formas de contaminação de DTAs e também os principais alimentos transmissores. Realizaram-se práticas dentro da sala de aula relacionadas ao assunto para o melhor entendimento dos alunos.				</P>
			</div><B>Palavras-chave</B>: Bioética. Educação. alimento seguro			<BR>
