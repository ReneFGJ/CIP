			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: his is a project involving a multidisciplinary team of Graduate Program in Bioethics teachers and students of nutrition courses , Psychology and Veterinary Medicine on the topic of safe food					<B>Objectives</B>: Map the knowledge of parents or guardians of children of 5th year 10 classes of municipal schools of Curitiba Region and develop a dynamic on the theme " safe food " with students.					<B>Methods</B>: This action research with 184 participants. The research instrument was a questionnaire with open and closed questions to diagnose the knowledge of families about hygiene, handling and consumption of food After delivery of the completed questionnaire , data were tabulated and reported in the form of graphs . This study was approved by the Ethics Committee and the respondents signed informed consent . After workshops were held made [e]#8203;[e]#8203;one primer on the topic.					<B>Results</B>: Most participants in this study were women (90 %) had complete or incomplete high school degree ( 45%). Thus , 52% of respondents did not know the origin of the meat they buy . According to Germano and Germano (2011 ) the beef and chicken are the foods most often related to placement of foodborne illness . Noting that , of those surveyed , 54 % consume this product 5 or more times a week.					<B>Conclusion</B>: The knowledge of parents or guardians were identified from a questionnaire delivered to them, containing questions about their practices and habits in the household on food handling and feeding them. The concept of zoonoses is little known by the respondents what do you suppose the need to investigate and analyze this issue.
The dynamics were performed on a visit schools , including as a subject the main forms of foodborne contamination and also the main transmitters food. There were practices within the classroom related to the subject to a better understanding of the students.				</P>
