			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Esta pesquisa está integrada no grupo de pesquisa do programa de Mestrado e Doutorado em Educação - PPGE - PUCPR, no projeto Formação Pedagógica de Professores em Diferentes Níveis e Contextos, coordenado pela Prof.ª Dr.ª Elizete L. M. Matos, integra-se ao projeto aprovado pelo CNPq, intitulado Lego favorecendo propostas pedagógicas junto a escolares em tratamento de saúde no contexto hospitalar.					<B>Objetivo</B>: O projeto tem como objetivo implementar a utilização do LEGO com escolares em tratamento de saúde no contexto hospitalar bem como  envolver  docente e discente na construção de novos saberes afim de que percebam os benefícios do material aplicado ao escolar colocando  em prática  flexibilidade e reflexão destacando a  importância do brincar enquanto aprendizagem. Levando desta forma, o aluno Pibiti a participar de cursos e aulas e capacitações para que este seja inserido no contexto hospitalar compreendendo a complexidade da pedagogia hospitalar e suas dimensões.					<B>Metodologia</B>: Pesquisa de abordagem qualitativa do tipo pesquisa-ação.					<B>Resultados</B>: 					<B>Conclusões</B>: Com as experiências que tivemos no Hospital Cajuru na primeira etapa do projeto com o material da LEGO, foi possível perceber a importância do material da LOGO no ensinar e no aprender. As visitas e a proposição da utilização do material da LOGO puderam auxiliar na docência dos professores que atuam junto aos alunos que se encontram em tratamento de saúde, ou seja, hospitalizados ou em atendimento domiciliar e na  minha formação como professora e pesquisadora.				</P>
			</div><B>Palavras-chave</B>: LEGO. Educação não Formal. Escolar hospitalar. Ação docente			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: This research is integrated in the research group of the Master´s program and Doctorate in Education - PPGE - PUCPR in Teacher Training project Teachers at Different Levels and Contexts, coordinated by Prof. Dr. Elizete LM Matos, integrates with project approved by CNPq, entitled Lego favoring pedagogical proposals to the school health treatment in hospitals.					<B>Objectives</B>: The project aims to implement the use of LEGO with school health treatment in the hospital setting and engage teachers and students in the construction of new knowledge in order to realize the benefits of that applied to school supplies putting into practice flexibility and reflection highlighting the importance of playing while learning. Taking this way, the student Pibiti to participate in courses and classes and training for this to be inserted in hospitals comprising the complexity of hospital pedagogy and its dimensions.					<B>Methods</B>: Research qualitative approach type action research					<B>Results</B>: 					<B>Conclusion</B>: With the experiences we had in Cajuru Hospital in the first stage of the project with material from LEGO, it was possible to realize the importance of LOGO material in teaching and learning. The visits and the proposition of using LOGO material could assist in teaching teachers who work with students who are in health care, ie, hospitalized or in home care and in my training as a teacher and researcher.				</P>
