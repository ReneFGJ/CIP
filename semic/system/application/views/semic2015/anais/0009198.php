			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Este projeto visa a validação dos materiais de matemática básica presentes no Eureka, o Ambiente Virtual de Aprendizagem(AVA) da Pontifícia Universidade Católica do Paraná, redigido para os alunos que apresentam dificuldades elementares na disciplina de Matemática para que esses consigam atingir uma autonomia ao estudar a distância, com supervisão de um tutor. Portanto, a validação foi realizada de acordo com os requisitos de qualidade do Ministério da Educação para criação de projetos de Educação a Distância (EaD).					<B>Objetivo</B>: O objetivo geral é analisar os materiais didáticos assíncronos disponíveis no Eureka para a disciplina Matemática, com base em critérios de qualidade e através de uma pesquisa qualiquantitativa aplicada aos alunos egressos dos cursos de Engenharia da Escola Politécnica da Pontifícia Universidade Católica do Paraná.					<B>Metodologia</B>: O questionário foi elaborado no software Qualtrics e sua aplicação para alunos de diversos cursos da Escola Politécnica entre o 1º a 3º semestre da universidade juntamente com os alunos integrantes do programa de Habilidades de Núcleo Básico(HNB), sendo único requisito estarem cursando disciplinas de Matemática, Introdução ao Cálculo ou Cálculo 1. Então foi possível coletar os dados sobre as dificuldades e verificar a aceitação dos estudantes em relação a um material assíncrono complementar de matemática básica.					<B>Resultados</B>: Os resultados encontrados foram satisfatórios, sendo que mais de 50% dos respondentes avaliam o material com muito bom ou excelente.					<B>Conclusões</B>: Algumas críticas construtivas foram levantadas durante a aplicação do questionário como, por exemplo, o quesito jogos, que estão disponíveis no material, mas no geral os estudantes ficaram satisfeitos com o material, tanto o material de base teórica como as videoaulas e vídeos complementares disponíveis.				</P>
			</div><B>Palavras-chave</B>: EAD. Eureka. Matemática básica.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: This project aims to validate the basic math materials present in Eureka, the Virtual Learning Environment (VLE) of the Pontifical Catholic University of Paraná, written for students who have basic difficulties in Mathematics so that these are able to achieve a  while studying the distance, under the supervision of a tutor.Therefore, the validation was performed according to the quality requirements of the Ministry of Education to create the Distance Education project (DE).					<B>Objectives</B>: The overall objective is to analyze the asynchronous learning materials available in Eureka for mathematics discipline, based on quality criteria and through a quali-quantitative research applied to students of the Polytechnic School of Engineering courses at the Catholic University of Paraná.					<B>Methods</B>: The questionnaire was prepared in Qualtrics software and its application to students in several courses at the Polytechnic School of the 1st to 3rd semester of university students along with members of the Basic Core Skills program (HNB), and only requirement are studying math disciplines , Introduction to Calculus or Calculus 1. Then it was possible to collect data on the difficulties and verify the acceptance of students in relation to a complementary asynchronous material math básica.					<B>Results</B>: Os results were satisfactory, with more than 50% of respondents assess the material with very good or excellent.					<B>Conclusion</B>: Some constructive criticisms were raised in the questionnaire, for example, the  games, which are available on the material, but overall the students were satisfied with the material, both the theoretical base material such as video lessons and additional videos available .				</P>
