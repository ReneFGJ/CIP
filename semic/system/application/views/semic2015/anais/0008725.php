			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: As políticas de transferência de renda denominadas na nomenclatura original de Condicional Cash Transfer Programs-(CCTs) representam significante parcela das políticas sociais implementadas na América Latina, principalmente a partir da década de 1990. Essas políticas distributivas são caracterizadas por conceder uma transferência monetária a seus destinatários, sob condicionalidades, previamente delimitada pelos programas. A bibliografia dessa temática demonstra que essas políticas são disseminadas pelas Organizações intergovernamentais, como a Organizações das Nações Unidas (ONU) e também instituições financeiras internacionais (IFIs) como o Banco mundial.					<B>Objetivo</B>: Avaliar de que modo, têm ocorrido os processos de difusão e transferência das (CCTs) na América latina. Investigar parâmetros e concepções relacionados aos CCTs tais como: competência (responsáveis pela formulação e implementação), cobertura (população-alvo), objetivos, e noções de pobreza. Compreender o papel dessas organizações, bem como se houve predominância das suas resoluções, para erradicação da pobreza, sobre os planos governamentais dos países estudados.					<B>Metodologia</B>: Revisão bibliográfica. Exame de documentos de governos e, também de sites de Organizações Internacionais selecionadas. catalogação e classificação dos dados.					<B>Resultados</B>: Entre as organizações investigadas, observeru-se que o Banco Mundial e a ONU tiveram importância destacada na elaboração, disseminação e implementação das políticas do tipo (CCTs) na América Latina.					<B>Conclusões</B>: 				</P>
