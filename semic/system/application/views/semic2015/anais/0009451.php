			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Atualmente o setor público convive com restrições fiscais que lhe são impostas, além de uma maior demanda por parte da sociedade de novos serviços públicos. Esta exigência confrontada frente à escassez de recursos tem levado os governos ao redor do mundo, buscar por novos modelos de gestão que consigam conciliar as diferenças no que se referem à arrecadação, custos e despesas. Uma das propostas que vem ganhando espaço é aquela baseada nos modelos de mercado, denominada de Novo Gerenciamento Público, este modelo se refere a um conjunto de ideias e práticas contemporâneas que tentam, na sua essência, usar abordagens do setor privado e de negócios no setor público.					<B>Objetivo</B>: O presente estudo objetivou identificar os fatores condicionantes para a adoção e utilização de informações de gestão de custos em prefeituras do Estado do Paraná.					<B>Metodologia</B>: No intuito de analisar as práticas da gestão de custos nas prefeituras do Estado do Paraná, delineia-se a pesquisa de forma descritiva, survey com abordagem qualitativa. O questionário de pesquisa foi aplicado utilizando a ferramenta Google Docs, e os endereços eletrônicos das prefeituras foram coletados no sitio do Tribunal de Contas da União.					<B>Resultados</B>: Os resultados da pesquisa mostraram que somente 9,68% das prefeituras do estado do Paraná utilizam sistemas de custos.					<B>Conclusões</B>: Conclui-se que, o principal fator que levou a adoção de um sistema de custos nas prefeituras paranaenses foi a necessidade de controle.				</P>
			</div><B>Palavras-chave</B>: Gestão Contábil. Gestão de custos. Prefeituras do Estado do Paraná.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Currently the public sector coexists with fiscal constraints imposed on it, as well as greater demand from new utility company. This requirement faced against the scarcity of resources has led governments around the world, check for new management models who can reconcile the differences as they relate to revenue, costs and expenses. One proposal that has been gaining ground is one based on market models, called New Public Management, this model refers to a set of ideas and contemporary practices trying, in essence, using private sector and business approaches in the industry public.					<B>Objectives</B>: This study aimed to identify the conditioning factors for the adoption and use of cost management information in the State of Paraná municipalities.					<B>Methods</B>: In order to analyze the practices of cost management in the State of Paraná municipalities, outlines to search descriptively, survey with a qualitative approach. The questionnaire survey was applied using the Google Docs tool, and electronic addresses of the municipalities were collected at the site of the Federal Audit Court.					<B>Results</B>: The survey results showed that only 9.68% of the state of Paraná municipalities use systems costs.					<B>Conclusion</B>: We conclude that the main factor that led to the adoption of a system of costs in Paraná municipalities was the need to control.				</P>
