			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Entre os Ensaios-Não-Destrutivos (END), existe um método chamado termografia infravermelha, que analisa a variação temporal da temperatura de um corpo através de um sensoriamento remoto, o qual interpreta as ondas eletromagnéticas emitidas por um corpo e as transforma em imagens térmicas.					<B>Objetivo</B>: O presente trabalho se baseia em realizar uma análise de defeitos em objetos através da termografia ativa, a qual analisa um corpo que sofre uma estimulação externa de temperatura, para que haja uma variação térmica na superfície do objeto, estudando o comportamento da curva de decaimento da temperatura através de uma câmera de infravermelho.					<B>Metodologia</B>: Também é proposto uma formulação numérica para simular o processo térmico através do método de volumes finitos, que consiste na subdivisão do bloco estudado em volumes com distâncias determinadas, e na formulação de equações de balanços de energia de um determinado volume, relacionando-o às temperaturas de seus volumes vizinhos.					<B>Resultados</B>: Obteve-se um modelo numérico que será utilizado para testar o comportamento experimental de diversos materiais, após se realizar uma validação com um material conhecido, em condições controladas.					<B>Conclusões</B>: Esta formulação feita em três dimensões e em regime transitório permite analisar a difusão interna do calor de acordo com as propriedades termofísicas do material e também comparar o resultado desta simulação numérica com os ensaios realizados.				</P>
			</div><B>Palavras-chave</B>: Termografia Ativa. Volumes Finitos. Radiação Térmica.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Among the Non-Destructive Tests (NDT), there is a method called infrared thermography, which analyzes the temporal variation of the temperature of a body through a remote sensing, which interprets the electromagnetic waves emitted by a body and turns them into thermal images.					<B>Objectives</B>: The present work is based on performing an analysis of defects in objects through active thermography, which analyzes a body that undergoes a temperature external stimulation, so that there is a thermal variation in the object´s surface, by studying the behavior of the temperature decay curve using an infrared camera.					<B>Methods</B>: It is also proposed a numerical formulation to simulate the thermal process using finite volumes, which consists in subdividing the block studied in volumes with certain distances, and the formulation of the energy balance equations for a given volume relating the temperature of the neighboring volumes.					<B>Results</B>: It was obtained a numerical model that will be used to test the performance of various experimental materials, after performing validation with a material known under controlled conditions.					<B>Conclusion</B>: This formulation is done in three dimensions and time dependent and then, with this tool, it is possible to analyze the internal heat diffusion in accordance with the thermophysical properties of the material and also comparing the result of numerical simulation performed with the test.				</P>
			</div><B>Keywords</B>: Active thermography. Finite Volumes. Thermal Radiation.</td>
