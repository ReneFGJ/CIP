			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Este relatório final busca realizar uma apresentação sobre o ecodesenvolvimento com ênfase no enfoque metodológico-empírico, através do cadastro e da coleta de experiências no campo da Ecossocioeconomia Urbana					<B>Objetivo</B>: a) Contribuir na construção do conhecimento sobre a teoria e prática da ecossocioeconomia, através de leituras, fichamentos, pesquisas e cadastro de experiências;
b) Contribuir para formação acadêmica de graduandos, mestrandos, doutorandos e pós-doutorandos;
c) Ampliar a base de dados sobre experiências em ecossocioeconomia urbana; e
d) Construir um sítio eletrônico e banco digital de dados com o resultado da pesquisa.					<B>Metodologia</B>: Através do registro das experiências e da catalogação de dados pelo formulário qualitativo, houve a aproximação e investigação em especial de experiências caracterizadas como Economia Solidária.					<B>Resultados</B>: Foram também, desenvolvidos através de inúmeros contatos e trocas de informação, realizados projetos de pesquisa com outros temas pelo viés ecossocioeconômico, como Slow Food.					<B>Conclusões</B>: Por fim, o foco em ecossocioeconomia das organizações de modo mais objetivo irá proporcionar uma visão mais aproximada e estratégica do campo da ecossocioeconomia urbana trazendo, assim como a pesquisa de campo aliada ao cadastro das experiências, uma experiência teórico-prática dessas novas ações de racionalidade instrumental baseadas na ecossocioeconomia urbana. A expectativa é a de que essa pesquisa permita por meio de seus resultados, a compreensão e maior clareza a respeito do papel da ecossocioeconomia no espaço urbano e como elas se desenvolvem.				</P>
			</div><B>Palavras-chave</B>: Ecossocioeconomia urbana. cadastro de experiências. formulário qualitativo. economia solidária			<BR>
