			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A efetividade da prestação jurisdicional no Brasil é um dos grandes problemas apontados em relação à justiça, daí a necessidade de estudarem-se mecanismos alternativos através dos quais se possa realizar a justiça. O pluralismo jurídico propõe uma alternativa célere, eficaz e democrática para resolver conflitos que submetidos a justiça estatal que não encontram soluções justas, seja pela demora ou pela solução inadequada imposta as partes.					<B>Objetivo</B>: Estudar propostas pluralistas previstas ou fomentadas  nas constituições da Colômbia, Venezuela e Peru e apresentar propostas como a justiça indígena, a justiça comunitária e a justiça de paz, como formas alternativas, pacificadoras e baratas para assegurar o acesso à justiça e pacificar conflitos sociais no Brasil.					<B>Metodologia</B>: Estudo essencialmente bibliográfico. Partiu-se de análises teóricas em torno do conceito de pluralismo jurídico, e em realizou-se um estudo comparado a partir das Constituições da  Colômbia, Venezuela e Peru, cotejando-o aos mecanismos alternativos de solução de conflitos inentivados pelo Conseho Nacional de Justiça no Brasil. Finalizou-se com o estudo do multiculturalismo e discussão a respeito da efetividade do pluralismo para a efetividade da prestação  jurisdicional.					<B>Resultados</B>: As Constituições da Venezuela, Colômbia e Peru reconhecem-se como nações plurais, multiculturais e heterogênas. Estas características transcedem a formação nacional e abrem espaço a formas não-estatais de realizar a justiça, permtitindo que costumes e tradições ancestrais sejam utilizadas e praticadas para tornar a prestação jurisdiconal mais efetiva. Justiça comnitária, juízes de paz, entre outros, têm sido utilizados com sucesso nestes países e são mais rápidos, baratos e eficazes, se comparados à justiça estatal.					<B>Conclusões</B>: O pluralismo permite o processo democrático e legítimo da população sendo um eficiente mecanismo alternativo para a solução de conflitos na sociedade contemporânea.				</P>
			</div><B>Palavras-chave</B>: Pluralismo Jurídico. Constituições Colômbia, Venezuela e  Peru. Formas não estatais de Justiça.			<BR>
