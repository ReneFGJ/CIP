			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O teste de dissolução tem se tornado cada vez mais importante no desenvolvimento de formas farmacêuticas sólidas, porque prevê o comportamento de liberação do ativo, além de ser uma ferramenta no controle de qualidade de produtos. A validação deve garantir, através de estudos experimentais, que o método atenda às exigências das aplicações analíticas, assegurando a confiabilidade dos resultados, demonstrando que o método é apropriado à finalidade pretendida. Na literatura não foram encontrados relatos sobre métodos de ensaios de dissolução de comprimidos revestidos de tadalafila.					<B>Objetivo</B>: O presente trabalho teve como objetivo validar a metodologia de dissolução para comprimidos revestidos de tadalafila, metodologia esta desenvolvida anteriormente.					<B>Metodologia</B>: As condições de dissolução utilizadas foram 900 mL de tampão fosfato pH 6,8 + 0,50 % de laurilsulfato de sódio como meio de dissolução, utilizando pás (aparato II) à 100 rpm e análise por UV à 235 nm. O método foi validado de acordo com os requerimentos regulatórios através dos parâmetros especificidade, linearidade, precisão e exatidão.					<B>Resultados</B>: O método demonstrou especificidade, linearidade (R2: 0,9993), precisão (DPR [e]lt; 2 %) e exatidão ([e]rt; 99 %). .					<B>Conclusões</B>: Os resultados obtidos demonstraram que o método proposto é específico, pois não houve interferência dos excipientes na quantificação da tadalafila no comprimento de onda de 225 nm, linear na faixa de 4,44 à 26,67 µg/mL, preciso, com uma precisão média de 100,01 % (DPR: 1,32 %) e exato.				</P>
			</div><B>Palavras-chave</B>: Dissolução. validação. tadalafila.			<BR>
