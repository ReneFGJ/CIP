			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Dissolution test has become increasingly important in the development of solid dosage forms, because provides the drug release behavior, and also to be a tool to quality control. The validation must guarantee , through experimental studies, that the method meets the requirements of the analytical applications, ensuring the reliability of results, showing that the method is suitable for the intended purpose. In the literature there were no reports of dissolution methods to tadalafil coated tablets.					<B>Objectives</B>: The aim of this work was to validate the dissolution methodology for tadalafil coated tablets,methodology previously developed.					<B>Methods</B>: The dissolution conditions used were 900 ml phosphate buffer pH 6.8 + 0.50 % sodium lauryl sulfate as dissolution medium , using paddles (apparatus II) at 100 rpm and analyzed by UV at 235 nm. The method was validated in accordance with regulatory requirements by analyzing the parameters specificity, linearity , precision and accuracy.					<B>Results</B>: The method demonstrated specificity, linearity (R2: 0.9993) , precision (RSD [e]lt;2%) and accuracy ([e]rt; 99%).					<B>Conclusion</B>: The results demonstrate that the proposed method is specific, since there was no interference of the excipients in the quantification of tadalafil at a wavelength of 225 nm in the linear range of 4.44 to 26.67 g/mL, precise, with a average precision 100.01 % (RSD : 1.32% ) and accurate.				</P>
