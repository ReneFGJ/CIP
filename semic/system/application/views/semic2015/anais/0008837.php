			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Diante do contexto da sociedade da informação, caracterizada pela comunicação em rede e pelo constante fluxo de dados, torna-se importante saber quais são os limites jurídicos que circunscrevem essa perspectiva social.					<B>Objetivo</B>: Por isso, o presente trabalho se propõe a investigar a exposição de dados pessoais na internet e quais são os fundamentos legais disponíveis para a proteção das informações divulgadas na rede mundial de computadores.					<B>Metodologia</B>: Em vista disso, com base em uma análise conjunta de registros estatísticos e da bibliografia pertinente ao tema, faz-se a caracterização da sociedade da informação e, em seguida, aponta-se quais diplomas legislativos brasileiros possuem disposições aptas à proteção jurídica dos dados pessoais.					<B>Resultados</B>: A partir da constituição federal, constata-se que o resguardo das informações pessoais configura uma nova dimensão dos direitos à privacidade e sigilo das comunicações; é essencial a tutela da vulnerabilidade do consumidor; bem como exige uma proteção cooperativa em favor da construção de uma República livre, justa e solidária. Tendo por fundamento a legislação vigente, a proteção aos dados pessoais encontra respaldo no Código de Defesa do Consumidor, no decreto nº 7.962/2013 e na lei 12.965/2014 – que formaliza o Marco Civil da Internet. Por este arcabouço infraconstitucional, observa-se a existência direitos básicos e princípios que regulamentam a gestão das informações pessoais registradas em bancos de dados; que definem os limites de atuação das partes envolvidas; e asseguram a efetividade de direitos constitucionais.					<B>Conclusões</B>: Logo, tem-se que a proteção jurídica aos dados pessoais na internet é sistêmica e complexa, pois a integridade da informação requer esforços tanto do cidadão-consumidor que a disponibiliza quanto, do gestor de dados que a utiliza.				</P>
			</div><B>Palavras-chave</B>: Dados Pessoais. Vulnerabilidade do cidadão-consumidor. Privacidade.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: On the context of information society, characterized by network communication and by the constant data flow, it becomes important to know what the juridical limits that circumscribe this social perspective are.					<B>Objectives</B>: Because of it, the present work intends to investigate the exposure of personal data on the internet and what are the legal fundamentals available for protection of the informations publicized on the world wild web.					<B>Methods</B>: In view of this, based on a joint analysis of statistical records and bibliography pertinent to the issue, it makes the characterization of information society and, after that, it points up the brazilian statutes that have suitable provisions for legal protection of personal data.					<B>Results</B>: From the federal constitution, it realizes that the custody of personal informations configures a new dimension to privacy and secrecy communication rights; it is essential for consumer vulnerability protection; and requires a cooperative safekeeping in favor to the building of a free, fair and solidary Republic. Founded upon the current legislation, the protection of personal data is supported on Consumer Defense Code, on the decree nº 7.962/2013 and on the law nº 12.965/2014 - formalizing the civil landmark of the internet. With this infraconstitucional framework, it notes the existence of basic rights and principles regulating the managment of personal informations registered in databases; setting the limits of the actuation of the parts involved; and guaranteeing the effectivity of constitucional rights.					<B>Conclusion</B>: Then, the legal protection of personal data on the internet is sistemic and complex, because the information integrity demands efforts both the citizen-consumer that makes it available as the data manager that uses it.				</P>
			</div><B>Keywords</B>: Personal Data. Citizen-consumer vulnerability. Privacy.</td>
