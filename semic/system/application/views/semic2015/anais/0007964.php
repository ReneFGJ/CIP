			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Uma das características que auxilia na definição do ser humano atual é a individualidade. Dentre os fatores que possibilitam a criação deste indivíduo está a dificuldade de se relacionar, e a fragilidade existente nas relações já estabelecidas. Vivemos uma era de relações frágeis, de relacionamentos não duráveis, na lógica da troca. Com isso, o sacramento do matrimônio, que é o meio pelo qual os noivos recebem a benção de Deus, bem como as incumbências de uma vivência a dois, tendo como referência o modelo de vida cristã está em descrédito.					<B>Objetivo</B>: O objetivo desta pesquisa é refletir, a partir do Sacramento do Matrimônio os aspectos referentes à recepção, a vivência, valorização e a fragilidade das uniões conjugais no contexto atual.					<B>Metodologia</B>: Para alcançar estes objetivos foram necessárias pesquisas bibliográficas no campo das relações existentes na atualidade, também sobre os diversos aspectos que permeiam o sacramento do matrimônio, possibilitando assim uma relação entre ambas.					<B>Resultados</B>: Os resultados teóricos possibilitaram uma melhor compreensão acerca dos modelos de relações estabelecidas na sociedade atual, bem como um aprofundamento nos fundamentos do matrimônio cristão. A relação entre ambos demonstra um distanciamento entre os modelos de vida, se estes não forem bem compreendidos.					<B>Conclusões</B>: Contudo, apresenta também que não é impossível a vivência matrimonial diante da atual conjuntura que vivemos. Se for bem compreendido este ato de amor, além de possível de vivenciá-lo, pode ser também meio para uma sociedade mais próspera e fecunda.				</P>
			</div><B>Palavras-chave</B>: Fragilidade das relações. Matrimônio. Indissolúvel. Aliança.  Vida conjugal.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: One of the features that assists in the definition of the actual human being is individuality. Among the factors that enable the creation of the individual is the difficulty of relating, and the fragility that exists in established relationships. We live in an era of fragile connections, non-lasting relationships, based in the logic of give-and-take.Therefore, the sacrament of marriage, which is the means by which bride and groom receive the blessing of God, as well as the duties of a couple living having as reference the Christian model of life, is in discredit.					<B>Objectives</B>: The aim of this research is to think over, from the Sacrament of Marriage, the aspects relating to reception, experience, valorization and fragility of the marital unions in the current context.					<B>Methods</B>: To achieve these aims, bibliographical research was necessary in the field of relationships today, also on the various issues that permeate the sacrament of marriage, thus enabling a relation between them.					<B>Results</B>: The theoretical results gave possibility to a better understanding of the differnt models of relationships established in the contemporary society, as well as a deepening in the fundamentals of Christian marriage. The relation of both demonstrates a gap between the models of life, if they are not well understood.					<B>Conclusion</B>: However, it also shows that it is not impossible to have the marital living on the current situation which we live. If properly understood, this act of love is not just possible to be lived, but it can also be a means to a more prosperous and fruitful society.				</P>
			</div><B>Keywords</B>: Fragility of relations. Marriage. Indissoluble. Alliance. Married life.</td>
