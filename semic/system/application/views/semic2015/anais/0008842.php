			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Sindrome metabólica  (SM) é um transtorno complexo que engloba diversos fatores, incluindo obesidade central, hipertensão arterial sistêmica (HAS) e diabetes mellitus tipo 2 (DM). A cirurgia bariátrica tem demonstrado resultados excelentes na melhora da síndrome metabólica e hipertensão arterial.					<B>Objetivo</B>: O presente estudo tem por objetivo avaliar os resultados da técnica de cirurgia bariátrica de derivação gástrica em Y de Roux na  melhora em longo prazo da SM e da HAS.					<B>Metodologia</B>: Foi realizado um estudo retrospectivo de 910 pacientes submetidos a gastroplastia com derivação gástrica em Y de Roux (GDYR) pela clínica gastrovida. Destes pacientes foram selecionados pacientes com critérios de SM e HAS cujo tempo de acompanhamento foi superior a 5 anos e inferior a 10 anos. Os dados foram colocados em planilha de excel e a média foi avaliada.					<B>Resultados</B>: Dos 910 pacientes submetidos a GDYR apenas  352 apresentavam tempo de acompanhamento maior do que 5 anos. Destes, 60,23% apresentavam SM, porém apenas 27,8% (59 pacientes) tinham acompanhamento realizado por mais de 5 anos. Observamos nesta população melhora da SM em 93,22% dos pacientes e da HAS em 88,68% dos pacientes.					<B>Conclusões</B>: A melhora da SM e da HAS após cirurgia bariátrica (GDYR) já está bem documentada, porém existe dificuldade na aquisição de resultados em longo prazo devido ao baixo índice de acompanhamento dos pacientes. No presente estudo observamos excelente resultado em longo prazo porém ainda com baixo número de pacientes. São necessários estudos adicionais para confirmar que a melhora da DM e HAS pós procedimento de GDRYR é prolongada.				</P>
			</div><B>Palavras-chave</B>: cirurgia bariátrica, síndrome metabolica, hipertensão, estudos em longo prazo			<BR>
