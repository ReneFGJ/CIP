			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Metabolic syndrome (MS) is a complex disease envolving a series of factors including central obesity, hypertension and type 2 diabetes (DM). Bariatric surgery has demonstrated excellent results in the  improvement of MS and hypertension.					<B>Objectives</B>: The aim of the present study is to evaluate the results of roux-em Y gastric bypass in the long term improvement of MS and hypertension.					<B>Methods</B>: A retrospective study evaluated 910 patients subjected to RYGB in gastrovida clinic which had metabolic syndrome and hypertension and had more than 5 years and less than 10 years of post operative follow up. Data was evaluated by excel.					<B>Results</B>: From the 910 patients who underwent RYGB, only 352 had more that 5 years after surgery. Only 60,23% of patientes had the criteria for MS, however only 27,8% of these patients had a post operative follow up for more than 5 years. We observed an improvement of 93,22% of metabolid syndrome and 88,68 % of the patients had an improvement in Hypertension					<B>Conclusion</B>: The improvement of MS and hypertension after RYGB has already been documented, however studies with long term follow up are scarce. In the present study, despite the decreased number of patients we observed excellent results in the ameliorations of MS and hypertension in a 5 to 10 years follow up.				</P>
			</div><B>Keywords</B>: Bariatric Surgery, metabolic syndrome, hypertension, long term studies</td>
