			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The [e]lt;i[e]rt;Pereskia aculeata[e]lt;/i[e]rt; belongs to the family of cacti and is widely used as an ornamental plant. In addition, in this specie has high content of protein and chemically active compounds, and its leaves is used in popular medicine and consumed in natura or in the form of flour added in food. In addition to the leaves, the fruit also has chemically active compounds, which makes this plant an interesting source for this study.					<B>Objectives</B>: The objective of this work was to develop from the fruits of [e]lt;i[e]rt;P. aculeata[e]lt;/i[e]rt;, a flour and after was determined its basic composition for a possible food application.					<B>Methods</B>: For this, fruits were collected, frozen and lyophilized (dried in reduced pressure and low temperature that maintains the qualities of the fruit). After, the dried sample was mashed to produce the flour. For the flour, they were determined lipid content (3%), fiber (27.7%), vitamin C (32.20 mg / 100g), ash (5.2%), moisture (6.77%) and protein (1.7%) on dry basis.					<B>Results</B>: The results showed a variation in composition of flour from fruit  in relation flour leaves, particularly with respect to protein content, which results much lower content. However, studies show that despite this low level, the fruits have high levels of carotenoids and phenolic compounds (not determined in this study), indicating another feature to the flour obtained in this study.					<B>Conclusion</B>: Thus, the production of flour from  the fruits of P.aculeata, can be an alternative of the exploration of fruit of this specie and use this source as a functional food.				</P>
