			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: [e]lt;I[e]rt;Pereskia Aculeata[e]lt;/I[e]rt; pertence à família das cactáceas e é amplamente utilizada como planta ornamental. Destaca-se nessa espécie, seu alto teor de proteína e compostos quimicamente ativos, sendo suas folhas utilizadas na medicina popular e consumida in natura ou na forma de farinha utilizada em alimentos. Além das folhas, seu fruto também apresenta compostos quimicamente ativos, o que torna essa planta uma fonte interessante para o presente estudo.					<B>Objetivo</B>: Assim, o objetivo deste trabalho foi desenvolver, a partir dos frutos da [e]lt;I[e]rt;P. aculeata[e]lt;/I[e]rt;, uma farinha e realizar sua composição básica para uma possível aplicação alimentícia.					<B>Metodologia</B>: Para isso, frutos foram coletados, congelados e liofilizados (secagem em pressão reduzida e baixa temperatura que mantém as qualidades do fruto) para posterior maceração para produzir uma farinha. Após obtenção da farinha, foram determinados os teores de lipídios (3%), fibras (27,7%), vitamina C (32,20 mg/100g), cinzas (5,2%), umidade (6,77%) e proteínas (1,7%) na base seca.					<B>Resultados</B>: Os resultados obtidos indicaram uma variação na composição básica da farinha do fruto em relação a farinha das folhas, principalmente em relação ao teor de proteína, que apresenta-se muito inferior. Porém, estudos mostram que apesar deste baixo teor, os frutos apresentam altos teores de carotenoides e compostos fenólicos (não determinados no presente estudo), indicando uma outra funcionalidade para a farinha obtido neste estudo.					<B>Conclusões</B>: Desta forma, a obtenção da farinha de frutos da P.aculeata, pode ser uma alternativa de exploração dos frutos e incluí-la em formulações alimentícias funcionais.				</P>
			</div><B>Palavras-chave</B>: [e]lt;i[e]rt;Pereskia aculeata[e]lt;/i[e]rt;, ora-pro-nobis, farinha, alimento funcional			<BR>
