			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Analisamos a questão da subjetividade e da intersubjetividade na fenomenologia de Maurice Merleau-Ponty e sua relação com a psiquiatria fenomenológica de Binwanger. Através da compreensão sobre o modo como o filósofo pensa as relações entre o eu e o outro, foi possível apontar os limites da aplicação do método fenomenológico no campo da psiquiatria, conforme estruturado por Binswanger.					<B>Objetivo</B>: A presente pesquisa analisou o problema da subjetividade e da intersubjetividade na filosofia de Merleau-Ponty, a fim de evidenciar o modo como sua compreensão da alteridade pode contribuir para a fenomenologia psiquiátrica, no sentido de precaver a redução de outrem na psicose a um objeto de investigação.					<B>Metodologia</B>: O método utilizado foi o fenomenológico, que visa fazer uma análise tentando superar o problema das relações entre consciência e natureza e as demais dicotomias que comprometem a concepção da alteridade.					<B>Resultados</B>: Compreendemos que Binswanger elaborou sua daseinsanalyse psiquiátrica, que se trata de um método de investigação para entender e descrever as síndromes e os sintomas perceptíveis na psicopatologia, de modo a preservar o prejuízo naturalista do distanciamento entre médico/paciente.					<B>Conclusões</B>: Por meio da concepção da subjetividade e da intersubjetividade em Merleau-Ponty, demonstramos que essa relação com o outro precisa acontecer de forma a superar o solipsismo da consciência, a fim de que o outro em condição psicótica não seja reduzido a uma mero objeto de investigação.				</P>
			</div><B>Palavras-chave</B>: Fenomenologia. Intersubjetividade. Subjetividade. Psiquiatria.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: We have analyzed the issue of subjectivity and intersubjectivity in the phenomenology of Maurice Merleau-Ponty and its relation to the phenomenological psychiatry Binwanger. Through understanding of how the philosopher thinks the relationship between self and other, we could point out the limits of the application of the phenomenological method in the field of psychiatry, as structured by Binswanger.					<B>Objectives</B>: This research analyzed the problem of subjectivity and intersubjectivity in Merleau-Ponty in order to demonstrate how their understanding of otherness can contribute to psychiatric phenomenology, to guard to another reduction in psychosis to an object research.					<B>Methods</B>: The method used was phenomenological, which aims to analyze trying to overcome the problem of the relationship between consciousness and nature and other dichotomies that compromise the design of otherness.					<B>Results</B>: We understand that Binswanger elaborated his psychiatric Daseinsanalyse, that it is a method of research to understand and describe the syndromes and symptoms noticeable in psychopathology, in order to preserve the naturalistic prejudice to the distance between doctor / patient.					<B>Conclusion</B>: Through the conception of subjectivity and intersubjectivity at Merleau-Ponty, we demonstrate that this relationship with the other needs to happen in order to overcome the solipsism of consciousness, so that the other in psychotic condition is not reduced to a mere object of investigation .				</P>
			</div><B>Keywords</B>: Phenomenology. intersubjectivity. subjectivity. Psychiatry.</td>
