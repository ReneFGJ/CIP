			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A área de estudos de formação de coalisão analisa os grupos formados por agentes quando estes se unem para realizar tarefas com o intuito de maximizar seus lucros e um objetivo global. Embora na literatura sejam encontrados muitos trabalhos descrevendo modelos e aplicações de protocolos descentralizados de formação de coalisão os resultados são dificilmente comparáveis pela forma não padronizada de descrição dos cenários de teste. Um framework desenvolvido para comparação de modelos de formação de coalisão poderia melhorar a qualidade das análises e contribuir para o desenvolvimento de modelos mais eficazes.					<B>Objetivo</B>: Este trabalho tem como objetivo a criação de uma plataforma para auxiliar testes e análise de diferentes cenários de formação de coalisão, assim como gerá-los de forma semiautomática para que modelos possam ser comparados.					<B>Metodologia</B>: O Framework foi desenvolvido em linguagem C++ e permite uma forma flexível de descrição dos cenários e parametrização dos agentes, tarefas e ambiente de execução.					<B>Resultados</B>: As avaliações realizadas em modelos simples mostram que as métricas geradas são bastante úteis para a comparação e análise dos resultados em diferentes cenários.					<B>Conclusões</B>: O presente projeto contriui de forma significativa para a análise comparativa de modelos de formação de coalisão e pode ser facilmente estendido, tendo em vista a arquitetura aberta utilizada, ou seja, outros modelos e métricas podem ser adicionados ao framework em trabalhos futuros como forma de aprimoramento da plataforma.				</P>
			</div><B>Palavras-chave</B>: Formação de coalisão. coordenação descentralizada. sistemas multi-agente			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The area of [e]#8203;[e]#8203;coalition formation studies analyzes the groups formed by agents when they come together to perform tasks in order to maximize your profits and a global goal. Although in the literature are found many papers describing models and applications of coalition formation the results are difficult to compare the non-standard shape description of test scenarios are often used. A framework developed for coalition training models comparison could improve the quality of the analysis and contribute to the development of more efficient models.					<B>Objectives</B>: This work aims to create a platform to support testing and analysis of different coalition scenarios, as well as generate them in a semiautomatic way so that models can be compared.					<B>Methods</B>: The Framework was developed in C ++ language and allows a flexible description of the scenarios and configuration of agents, tasks and execution environment.					<B>Results</B>: Evaluations undertaken in simple models show that the generated metrics are quite useful for comparison and analysis of results in different settings.					<B>Conclusion</B>: This project contributes significantly to the comparative analysis of coalition formation models and can easily be extended, given the open architecture used, ie, other models and metrics can be added to the framework for future work as a way to improve the platform.				</P>
			</div><B>Keywords</B>: Coalition Formation. Descentralized Agent Coordination. Multi-Agent Systems</td>
