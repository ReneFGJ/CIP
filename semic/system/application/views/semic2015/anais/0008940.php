			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: This article discusses the process of media convergence in two radio stations:
Banda B and Band News . The immediacy, multimidiality, the
hypertextuality, the flow of information, interactivity and convergence, have changed  the production, the use of language and  the communication between radio stations and listeners.
These aspects interfere and reflects on characteristics of journalistic products. In contemporary times the information is constant, the news is disseminated in real time, the user is not only consumer of information, it participates, comments, shares and produces news. The radio search the digital environment a way to reinvent itself and stay competitive.					<B>Objectives</B>: In this context, the objective of the project is to discuss the role of contemporary radio and how to check newsrooms journalistic appropriate the new technologies and multiplatform production.					<B>Methods</B>: During the first months , the research included the bibliography , with articles and books  by different authors. It was also necessary to observe the production routines
and changes in the professional profile due to enhancement of convergence.
In recent months, after the meeting content, detailing the results and occurred
analysis of information, as well as the survey and comparison of theories and
authors of the research.					<B>Results</B>: Despite radios present distinct characteristics, it was possible
identify that even when dealing with an AM station and one FM both use
technological elements to attract and keep their loyal following. Therefore, taking into account the breakthrough communication in recent times and the constant speed
information, branch companies should not be limited to its primary services, but,
yes, bring their products to the public in various ways.					<B>Conclusion</B>: Although the two radios since
begins in the convergence scenario, the time is still transitional, definitions and
discoveries.				</P>
