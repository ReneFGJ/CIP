			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Este artigo aborda o processo de convergência midiática em dois veículos de comunicação: rádios Banda B AM e Band News FM. O imediatismo, a multimidialidade, a
hipertextualidade, o fluxo de informação, a interatividade e a convergência, alteram a
produção de conteúdo, a linguagem e até mesmo a comunicação entre receptor e emissor.
Tais aspectos interferem e propeliam uma ruptura que reflete diretamente nas
características e nos produtos jornalísticos. Na contemporaneidade a informação é
constante, as notícias são divulgadas em tempo real, hoje, o usuário não é apenas
consumidor de informação, ele participa, comenta, compartilha e produz notícia. Em meio a esse cenário de mudanças, o rádio busca no meio digital uma forma de se reinventar, pois, na sociedade atual ou o veículo se ajusta ou pode ficar para trás.					<B>Objetivo</B>: Nesse contexto, o objetivo do projeto é discorrer sobre o papel do rádio contemporâneo e verificar como as redações jornalísticas se apropriam das novas tecnologias e da produção multiplataforma.					<B>Metodologia</B>: Durante os
primeiros meses foram realizadas leituras de livros, artigos e revista de vários autores, para
a compreensão dos termos utilizados. Também foi preciso observar as rotinas de produção
e as mudanças ocorridas no perfil profissional devido à potencialização da convergência.
Nos últimos meses, após ter o conteúdo reunido, ocorreu o detalhamento dos resultados e a análise das informações obtidas, bem como, o levantamento e a comparação das teorias e autores da pesquisa.					<B>Resultados</B>: Apesar das rádios apresentarem características distintas, foi possível identificar que mesmo se tratando de uma emissora AM e outra FM as duas utilizam elementos tecnológicos para atrair e manter seu público fiel. Portanto, levando em conta o
grande avanço da comunicação nos últimos tempos e a constante velocidade da
informação, as empresas do ramo não devem se limitar aos seus serviços primordiais, mas, sim, levar seus produtos ao público de diversas formas.					<B>Conclusões</B>: Embora as duas rádios já despontem no cenário da convergência, o momento ainda é de transição, definições e descobertas.				</P>
			</div><B>Palavras-chave</B>: Radio. jornalismo. convergência.			<BR>
