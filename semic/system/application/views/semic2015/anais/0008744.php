			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Um dos grandes desafios para a comunidade envolvida no processo de produção de alimentos é produzir mais com menor custo e menor utilização de produtos fitossanitários, sem agredir ao homem e ao ambiente. Dentre todos os equipamentos utilizados na condução de lavouras, o pulverizador é um dos únicos usado em todo o ciclo das culturas, sendo, também, responsável pela colocação dos produtos fitossanitários no ambiente. Por isso, o domínio da técnica de aplicação desses produtos é tarefa de mais alta importância.					<B>Objetivo</B>: O presente trabalho teve como objetivo avaliar o tempo de uso e as condições de operação de pulverizadores agrícolas quanto ao estado de conservação e funcionamento de alguns componentes vitais à eficácia da pulverização.					<B>Metodologia</B>: O trabalho de campo foi realizado a partir de um questionário que foi aplicado ao proprietário e/ou operador da máquina, sendo dividido em etapas identificatória, qualitativa e quantitativa, onde foram realizadas inspeções em pulverizadores agrícolas de barras na safra 2014/15. Os dados coletados a campo foram submetidos à análise exploratória, por meio de uma análise descritiva e qualitativa. No total foram avaliados 61 pulverizadores. Analisando de forma geral, todas as máquinas avaliadas apresentaram algum tipo de irregularidade. Muitas delas são simples de se resolver, como exemplo, a limpeza de filtros e também casos de maior precariedade, sendo necessário a substituição de alguns componentes.					<B>Resultados</B>: Os resultados mostram a necessidade de programas de inspeção periódica de pulverizadores.					<B>Conclusões</B>: O principal problema encontrado na avaliação qualitativa, foi com relação à adequação do manômetro (65%). Para a avaliação quantitativa o principal problema encontrado foi no item: erro na taxa de aplicação (70%).				</P>
			</div><B>Palavras-chave</B>: pulverizadores, inspeção, tecnologia de aplicação			<BR>
