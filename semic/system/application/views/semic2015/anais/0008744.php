			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: One of the great challenges for the community involved in the food production process is to produce more with less cost and less use of pesticides without harming to man and the environment. Among all the equipment used in the conduct of crops, the spray is one of the only used throughout the crop cycle and is also responsible for placing pesticides on the environment. Therefore, the field of application technique of these products is all-important task.					<B>Objectives</B>: This study aimed to evaluate the time of use and operating conditions of agricultural sprayers on the condition and operation of some vital components to the effectiveness of the sprayer.					<B>Methods</B>: Fieldwork was carried out from a questionnaire that was applied to the owner and / or operator of the machine, divided into identificatory, qualitative and quantitative steps, which were carried out inspections in agricultural sprayers bars in the 2014/15 crop. The data collected in the field were submitted to exploratory analysis, through a descriptive and qualitative analysis. In total 61 were evaluated sprays. Analyzing in general, all the machines evaluated had some type of irregularity. Many of them are easy to solve, for example, filter cleaning and also cases of more precarious, requiring replacement of some components.					<B>Results</B>: The results show the need for periodic inspection of spraying programs.					<B>Conclusion</B>: The main problem encountered in the qualitative evaluation was regarding the appropriateness of the manometer (65%). For quantitative evaluation, the main problem was the item: error in application rate (70%).				</P>
			</div><B>Keywords</B>: sprayers, inspection, application technology</td>
