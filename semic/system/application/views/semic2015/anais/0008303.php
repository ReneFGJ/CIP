			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A Doença Renal Crônica (DRC) é uma doença silenciosa e sua incidência vem
aumentando consideravelmente em todo o mundo. Na atenção secundária os
profissionais de saúde visam diminuir o ritmo da progressão da doença e melhorar
as condições clínicas e psicossociais do indivíduo. O profissional enfermeiro realiza
seu atendimento através da consulta de enfermagem, para isso ele utiliza o
Processo de Enfermagem. Este processo compreende cinco etapas: Histórico;
Diagnóstico de Enfermagem; Planejamento; Implementação e Avaliação.					<B>Objetivo</B>: O objetivo
deste estudo foi reconhecer as intervenções de enfermagem segundo a
Classificação de Intervenções de Enfermagem no “Nursing Interventions
Classification” (NIC) relacionados aos pacientes com doença DRC em tratamento
conservador partindo dos diagnósticos de enfermagem encontrados no “North
American Nursing Diagnosis Association” (NANDA®).					<B>Metodologia</B>: Foi um estudo de caráter
exploratório e descritivo, tendo como propósito observar, descrever, explorar,
classificar e interpretar aspectos importantes na prática clinica de enfermagem aos
portadores de DRC na atenção secundária.					<B>Resultados</B>: De acordo com as intervenções
encontradas para os Diagnósticos de Enfermagem levantados, percebe-se que
alguns cuidados de Enfermagem são primordiais e estão presentes em todos os
diagnósticos, sendo eles: Monitoração de sinais vitais; Controle de administração de
medicamentos; Controle e orientação nutricional; Controle de peso; e Interpretação
de exames laboratoriais. As intervenções agem positivamente na progressão do
ritmo da doença.					<B>Conclusões</B>: As intervenções agem positivamente na progressão do
ritmo da doença. Sendo assim, nota-se a grande relevância deste estudo para que o
profissional possa intervir de maneira adequada e garantir um atendimento de
adequado.				</P>
			</div><B>Palavras-chave</B>: Cuidados Secundários de Saúde. Cuidados de Enfermagem. Planejamento de Assistência ao Paciente. Diagnóstico de Enfermagem. Enfermagem em Nefrologia.			<BR>
