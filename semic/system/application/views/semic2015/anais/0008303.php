			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Chronic Kidney Disease (CKD) is a silent disease and its incidence has
considerably increasing worldwide. In the secondary care
health professionals seek to slow the progression of the disease and improve
clinical and psychosocial conditions of the individual. The nurse practitioner performs
their care through the nursing consultation, for this he uses the
Nursing process. This process consists of five steps: History;
Diagnosis of Nursing; Planning; Implementation and Evaluation.					<B>Objectives</B>: The goal
this study was to recognize the nursing interventions according to
Nursing Interventions Classification in "Nursing Interventions
Classification "(NIC) related to the disease in patients with CKD treatment
Conservative leaving the nursing diagnoses found in "North
American Nursing Diagnosis Association "(NANDA®).					<B>Methods</B>: It was a character study
exploratory and descriptive, with the purpose to observe, describe, explore,
sort and interpret important aspects in clinical nursing practice to
CKD patients in secondary care.					<B>Results</B>: According interventions
found for Nursing Diagnoses raised, it is clear that
some nursing care are paramount and are present in all
diagnostics, namely: Monitoring of vital signs; Management control
medications; Control and nutritional guidance; Weight control; and Interpretation
laboratory tests. Interventions act positively in the progression of
pace of the disease.					<B>Conclusion</B>: Therefore, there is the great importance of this study for the
nursing that can intervene in ensure service for the DRC patients.				</P>
