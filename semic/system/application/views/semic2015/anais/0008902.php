			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The search for a quality of life is increasingly apparent in society, it being of paramount importance for the prevention of chronic diseases. Study and analyze the cause for which mediating variables of physical activity relationship and gender as social support, self-efficacy and satisfaction for physical activity, for example, has an interference intensity lower for females than for males, it makes it clear to know what are the real reasons for this disparity and then succeed more effective interventions to reverse this situation.					<B>Objectives</B>: This project aims to test if the psychosocial variables may explain the difference in levels of physical activity among men and women.					<B>Methods</B>: The data of this project were all from the IPEN-STUDY project, in which 15 cities were selected in 13 countries, in which the minimum sample range was 500 adults. A survey was applied, divided into 12 blocks, where both genders between 18 and 65 years. The sample consisted of 699 participants of both genders. After dichotomizing the level of physical activity in do and do not, 39.9% of men were considered active while women had a total of 24.5%.					<B>Results</B>: In the Mann-Whitney test, satisfaction has become the most influential variable to the level of physical activity in which men possessed a 1-hour average and 37 minutes of physical activity per week while women had a 1-hour average and 5 minutes of physical activity per week, this followed by the odds ratio test, proving that men are twice as likely to be physically active than women, but this relationship is mediated by variables such as social support, the chance became statistically insignificant.					<B>Conclusion</B>: It was therefore concluded that men receive more social support, have higher self-efficacy and greater satisfaction with physical activity compared to women.				</P>
