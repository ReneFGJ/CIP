			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A busca por uma qualidade de vida está cada vez mais aparente na sociedade, sendo ela de suma importância para a prevenção de doenças crônicas. Estudar e analisar a causa pela qua variáveis mediadoras da relação atividade física e gênero, tem uma intensidade de interferência mais baixa para o gênero feminino do que para o gênero masculino, torna mais claro saber quais são os reais motivos para essa disparidade e então suceder intervenções mais eficazes para a reversão deste quadro					<B>Objetivo</B>: Este projeto teve como objetivo testar se as variáveis do ambiente percebido e psicossociais podem explicar a diferença nos níveis de atividade física entre homens e mulheres. Os dados do presente projeto foram oriundos do projeto IPEN-STUDY.					<B>Metodologia</B>: A amostra foi composta por 699 participantes, de ambos os gêneros com idade entre 18 e 25 anos. A atividade física foi categoridada em FAZ e NÃO FAZ. Foram considerados ativos 39,9% e  24,5%, dos homens e mulheres, respectivamente.					<B>Resultados</B>: No teste U de Mann-Whitney, a satisfação foi a variável mais influente para o nível de atividade física.A média de minutos de atividade fisica/semana foi de 1 hora e 37 minutos entre os homens e 1 hora e 5 minutos entre as mulheres. O  teste de odds ratio, comprovou que homens têm o dobro de chance de ser fisicamente mais ativo do que as mulheres. Entretanto quando essa relação é mediada por variáveis como apoio social, essa chance torna-se estatisticamente insignificante.					<B>Conclusões</B>: Concluiu-se então que homens que recebem maior apoio social, possuem maior autoeficácia e maior satisfação com a prática de atividade física quando comparado com as mulheres.				</P>
			</div><B>Palavras-chave</B>: atividade física, apoio social, autoeficacia, adultos;			<BR>
