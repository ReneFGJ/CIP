			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Com a grande necessidade das organizações em se manterem competitivas, e a exigência de uma maior e melhor qualidade nos produtos, os delineamentos experimentais entram expressivamente nestas tomadas de decisão.					<B>Objetivo</B>: O objetivo do trabalho foi o aperfeiçoamento dos conhecimentos e técnicas de melhoria contínua para a resolução de problemas na engenharia, bem como dos conceitos estatísticos voltados a qualidade de processos e produtos.					<B>Metodologia</B>: Inicialmente com a revisão da literatura, descreveu-se seis importantes tipos de delineamentos de experimentos, sendo eles: delineamento ao acaso (aleatorizado); delineamento em blocos; delineamento em quadrados latinos; delineamento fatorial; delineamento fatorial com dois fatores; delineamento fatorial. Visto a importância do Método de Taguchi, em um segundo momento, o foco do trabalho voltou-se para a descrição e aperfeiçoamento dos conhecimentos deste método, e por fim, com base na literatura disponível, reproduziu-se um delineamento pelo Método de Taguchi com auxílio de softwares estatísticos.					<B>Resultados</B>: Com a descrição dos delineamentos, bem como melhor entendimento do Método de Taguchi, foi possível visualizar a importância dos delineamentos experimentais tanto na engenharia, como para a melhoria de processos e produtos na indústria.					<B>Conclusões</B>: A reprodução computacional com base na literatura comprovou a vantagem e facilidade no método, bem como a grandeza e influência que este tem para a engenharia da qualidade.				</P>
			</div><B>Palavras-chave</B>: Taguchi. Delineamento de Experimentos. Qualidade			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: With the high necessity of organizations in remaining competitive, and the requirement for more and better quality in products, the experimental designs come significantly in these decision-making.					<B>Objectives</B>: The objective was the perfectioning of knowledge and continuous improvement techniques to solve problems in engineering, just like the statistical concepts involved with the quality of processes and products.					<B>Methods</B>: Initially with the literature review, it was described six important types of designs of experiments, namely: randomized design (randomized); block design; design in Latin squares; factorial design; factorial design with two factors; factorial design 2^k. Seeing the importance of the Taguchi method, in a second moment, the focus of this study turned to the description and improvement of the knowledge of this method, and finally, based on the available literature, it reproduced a design by the Taguchi method with assistance of statistical software.					<B>Results</B>: With a description of the designs, as well as a better understanding about the Taguchi method, it was possible to visualize the importance of experimental designs for engineering and for the improvement of processes and products in the industry.					<B>Conclusion</B>: The computational representation based on literature has shown the advantage and facility in the method as well as the magnitude and influence this has for engineering quality.				</P>
