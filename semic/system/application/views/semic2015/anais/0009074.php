			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Em suma neste trabalho tratou-se do Direito Empresarial com o intento de abordar a arbitragem, por meio da análise de acórdãos e doutrina. O foro arbitral no âmbito empresarial procura trazer uma maior agilidade o que não ocasione um desgaste no contrato empresarial para os sócios, tanto emocional quanto a manutenção da mortalidade da empresa, onde no contrato seja trazida a clausula respectiva, seja o contrato temporário ou permanente.					<B>Objetivo</B>: O objetivo desta pesquisa foi o de estudar soluções alternativas ao processo judicial estatal.					<B>Metodologia</B>: O método de pesquisa utilizado foi o dedutivo, a partir da busca de elementos doutrinários e análise de jurisprudências.					<B>Resultados</B>: O instituto foi estudado a partir do seu funcionamento no Brasil e aplicação nos conflitos empresariais e societários e a importância de sua utilização na seara extrajudicial.					<B>Conclusões</B>: A busca de solução alternativa para tais conflitos corporativos na melhora na relação entre os socios a promover ambientes colaborativos, assim ao objetivo de reduzir o sofrimento emocional e o índice de mortalidade empresariaL, pois se traz grandes benefícios para a sociedade de uma forma abrangente				</P>
			</div><B>Palavras-chave</B>: Direito Empresarial. Arbitragem. Solução extrajudicial. de conflitos. Mortalidade Empresarial.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: In short this work, is about business law, with the purpose of addressing the arbitration, through the analysis of judgments and doctrine. The arbitral jurisdiction in the business sector seeks to bring greater flexibility which does not cause wear on the enterprise contract for the partners , both emotionally and maintaining mortality of the company, which in its contract clause is brought , whether temporary or permanent contract.					<B>Objectives</B>: The goal of this research ,was to study alternative solutions to national court proceedings.					<B>Methods</B>: The research method used, was the  deductive one, starting off by  the search for doctrinal elements and case law analysis.					<B>Results</B>: The institute has been studied from its operation in Brazil and application in business and corporate disputes and the importance of its use in extrajudicial harvest.					<B>Conclusion</B>: The search workaround for such corporate conflicts in improving the relationship between partners, in order  to promote collaborative environments , thus the goal is reducing the emotional distress and business mortality rate , as it brings great benefits to society in a comprehensive manner				</P>
			</div><B>Keywords</B>: Business Law . Arbitration. Extrajudicial solution. Conflict . Corporate Mortality</td>
