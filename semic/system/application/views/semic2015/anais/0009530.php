			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The corn (Zea mays) is a crop that allows you to work in consortium with some green manure, the main and most used for the west region of Paraná is the Brachiaria ruziziensis, because it has good adaptation and a satisfactory management during harvest.					<B>Objectives</B>: Evaluate the performance of corn, under the influence of the various consortia with green manure.
 Check the influence of intercropping system in physical and chemical characteristics of the soil.					<B>Methods</B>: This work consisted of 8 treatments and 3 repetitions, being the treatments consortia with 6 types of green manure, millet, B. ruziziensis, B. brizantha, alfalfa, labe-labe and pigeon pea, and most of the cropping corn, and a fallow area. Conducting the experiment was realized from September to early January. Data collection was performed as plant height, stem diameter and height of ear insertion in the full development of the crop when it had grain filling, and after harvesting were carried out the data collection that comprised the production of items as well performed the sampling of soil resistance to mechanical penetration. Data were statistically analyzed, and analyzed for their significant differences.					<B>Results</B>: 					<B>Conclusion</B>: 				</P>
