			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A cultura do milho (Zea mays) é uma das culturas que permite o trabalho em consorcio com alguns adubos verdes, o principal e mais utilizado para a região oeste do Paraná é a Brachiaria ruziziensis, por possuir boa adaptação e apresentar um manejo satisfatório durante a safra.					<B>Objetivo</B>: Avaliar o desempenho da cultura do milho, sob influência dos diversos consórcios com adubos verdes.
- Verificar a influencia do sistema de cultivo consorciado nas características físicas e químicas do solo.					<B>Metodologia</B>: O presente trabalho foi composto por 8 tratamentos e 3 repetições, sendo os tratamentos os consórcios com 6 tipos de adubos verdes, milheto, B. ruziziensis, B. brizantha, alfafa, labe-labe e feijão-guandu , e mais o cultivo solteiro do milho, e uma área em pousio.A condução do experimento se compreendeu de setembro a inicio de janeiro. Foram realizadas coletas de dados como altura de planta, diâmetro de colmo e altura de inserção de espiga no pleno desenvolvimento da cultura, quando esta apresentava enchimento de grãos, e após a colheita foram realizados as coletas de dados que compuseram os itens de produção e também realizado a amostragem da resistência à penetração mecânica do solo. Os dados obtidos foram submetidos a análise estatística, e analisado quanto suas diferenças significativas.					<B>Resultados</B>: 					<B>Conclusões</B>: 				</P>
