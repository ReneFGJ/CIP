			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: As constantes evoluções tecnológicas demandam uma quantidade maior de fontes energéticas. Os combustíveis fósseis dominam a matriz energética mundial, pois durante muitos anos foram considerados fontes baratas e de fácil obtenção. Nos últimos anos, entretanto, descobriu-se que essas reservas fósseis não são tão abundantes ou renováveis como se pensava. Essas descobertas aliadas aos crescentes conflitos territoriais pelo domínio das jazidas de petróleo e a percepção do impacto ambiental que o uso indiscriminado dessas fontes gerou trouxe a necessidade de direcionar esforços para geração de energia através de fontes renováveis e menos poluidoras.					<B>Objetivo</B>: Este projeto vislumbra trazer a tona a atual situação das pesquisas e produção de biocombustíveis, buscando incentivar o crescimento da participação das energias renováveis na matriz brasileira.					<B>Metodologia</B>: A pesquisa caracteriza-se como exploratória, descritiva e bibliográfica.					<B>Resultados</B>: Dentre as inúmeras fontes de energia renováveis, que estão no mercado ou estão em desenvolvimento, este estudo focou nos biocombustíveis: biodiesel fabricado com plantas oleaginosas e gorduras de origem animal, bioetanol fabricado com a cana-de-açúcar e a bioeletricidade que usa como matéria prima o bagaço da cana. Os biocombustíveis estudados são viáveis economicamente para produção no Brasil já que o país pode cultivar as matérias primas em abundancia, domina as tecnologias necessárias e possui um numero já considerável de usinas para produção.					<B>Conclusões</B>: Conclui-se que é necessário a criação de políticas governamentais de apoio e incentivo ao desenvolvimento, fabricação e o uso de combustíveis renováveis através de ações que privilegiem a substituição ao menos de parte significativa dos combustíveis fosseis.				</P>
			</div><B>Palavras-chave</B>: Fontes renováveis. Etanol.  Biodiesel			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Constant technological developments require a greater amount of energy sources. Fossil fuels dominate the world energy matrix, because for many years they wereconsidered cheap and easily obtainable sources. In recent years, however, it was iscovered that these fossil reserves are not as abundant or renewable as once thought. These findings combined with the increasing territorial conflict for dominance of petroleum and the perception of the environmental impact that indiscriminate use of these sources generated brought the need to direct efforts to generate energy through renewable sources and less polluting.					<B>Objectives</B>: This project aims to bring out the current scenary of the research and production of biofuels, seeking to encourage the growth of the renewable energy share in the Brazilian matrix.					<B>Methods</B>: The research is characterized as exploratory, descriptive and bibliographical.					<B>Results</B>: Among the many renewable energy sources, which are on the market or under development, this study focused on biofuels: biodiesel made with oilseeds and animal fats, manufactured bioethanol with cane sugar and bioelectricity that uses as raw press the sugarcane bagasse. Biofuels studied are economically viable for production in Brazil since the country can cultivate the raw materials in abundance, mastered the necessary technologies and has an already considerable number of plants for production.					<B>Conclusion</B>: It is concluded that the creation of government policies to support and encourage the development is necessary, manufacturing and the use of renewable fuels through actions that favor the replacement of at least a significant part of the fossil fuels.				</P>
			</div><B>Keywords</B>: Renewable Fuels.  Ethanol. Biodiesel.</td>
