			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O presente trabalho apresenta a relação entre espaço público e os grupos sociais que o utilizam. Fazendo um paralelo entre estes dois personagens da cidade, o significado de ambos modifica-se conforme são esquecidos, degradando o espaço público e colocando o grupo social em situação de risco.  A arquitetura é apresentada como agente modificadora, capaz de resgatar a importância original do espaço e do cidadão usuário deste.					<B>Objetivo</B>: O objetivo geral do projeto central consiste em propor alternativas para espaços de lazer, visando ao controle ambiental e ao resgate de populações sujeitas a riscos sociais, especialmente voltados a infância e a juventude.					<B>Metodologia</B>: A pesquisa aqui articulada mostra através de estudos de caso e análise conceitual desses propulsores de mudanças municipais as transformações observadas em espaços públicos internacionais, organizando os métodos utilizados para a revitalização destes espaços.					<B>Resultados</B>: Verifica-se a importância da inclusão da comunidade na utilização do espaço assim como a delimitação de uso dos mesmos para resultados positivos e longínquos. Estes resultados podem ser observados em todos os casos com exceção da Plaza de la Luna a qual consiste em uma praça multifuncional impessoal.					<B>Conclusões</B>: Apesar da complexidade do tema re-inclusão social através do espaço público que o estudo abrange, em termos gerais os procedimentos metodológicos adotados foram suficientes para o alcance dos objetivos da pesquisa, contudo seriam necessárias análises temporais mais aprofundadas, assim como in loco, para a melhor compreensão da modificações resultantes das revitalizações.				</P>
			</div><B>Palavras-chave</B>: Espaço degradado. Espaço público. Grupos sociais em situação de risco. Revitalização Urbana. Arquitetura			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: This research presents the relationship between public space and social groups. The meaning of both changes when they are forgotten,  degrading the public space and inserting the social group in a risk situation. The architecture is presented as an modifier agent, able to rescue the original value of the space and its user.					<B>Objectives</B>: The main objective is to propose alternatives to leisure spaces, in order to the environmental control and the rescue of social risk groups, especially regarding childhood and youth.					<B>Methods</B>: The method used in this research is case studies in order to make conceptual analyzes of changing makers in the city and its transformations and urban spaces in the international context.					<B>Results</B>: The important communal inclusion is the main observed aspect, as well as the permanent and positive definition of uses. Those results can be observed in all cases excluding the Plaza la Luna, that consist on a multifunctional and impersonal square.					<B>Conclusion</B>: Considering the complexity of the social re-inclusion by the public space, the methodological procedures have been enough to range the research objectives. Altough, it would be necessary longer and deeper analyzes in place to better comprehension of revitalization changes.				</P>
			</div><B>Keywords</B>: Degraded space. public space. social risk groups. urban revitalization. architecture</td>
