			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O relatório final da pesquisa de iniciação cientifica tem como objeto as percepções juvenis, na educação básica, sobre as temáticas: direitos humanos e justiça nas escolas, e está vinculada ao Observatório de Violência nas Escolas - PUCPR, tendo como problema: Quais as percepções de estudantes da educação básica sobre direitos, justiça e convivência nas escolas?					<B>Objetivo</B>: O objetivo do projeto é: Identificar percepções de estudantes da educação básica sobre direitos e justiça e convivência nas escolas.					<B>Metodologia</B>: A pesquisa se orientou na abordagem qualitativa, abrangendo estudo bibliográfico, análise de documentos e dados do banco de dados do observatório de Violências nas Escolas PUCPR. Os dados analisados foram extraídos da pesquisa empírica que envolveu 12 escolas públicas de educação básica, localizadas em Curitiba, PR.					<B>Resultados</B>: As percepções dos estudantes apontam, sobretudo a questão da igualdade ao se referirem as três categorias em análise direitos humanos, justiça e convivência. O direito de igualdade, destacado pelos estudantes, vem sendo foco nas políticas públicas para assegurá-lo, no contexto social na perspectiva da alteridade.					<B>Conclusões</B>: A garantia e efetivação das categorias abordadas, direitos humanos, justiça e convivência poderão ser melhor viabilizados com mudanças curriculares. . Essa intencionalidade requer a escuta e valorização das vozes dos jovens, entendidos como sujeitos de direitos inseridos em um contexto caracterizado pela diversidade, respeitando e valorizando a pluralidade que ai existe.				</P>
			</div><B>Palavras-chave</B>: Direitos Humanos. Justiça. Convivência Violências nas Escolas. Educação Básica. Juventude.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The final report of scientific initiation research has as object youth perceptions in basic education, on the themes: human rights and justice in schools, and is linked to the Observatory of Violence in Schools - PUCPR, with the problem: What Student perceptions of basic education rights, justice and coexistence in schools?					<B>Objectives</B>: The objective of the project is: to identify the feeling of the students regarding rights, justice and violence in schools.					<B>Methods</B>: The research was oriented on the qualitative approach, using bibliography, documents analysis and data from the Violence in Schools Observatory PUCPR database. The analyzed data was taken from the empirical research that took place in 12 public schools of basic education, located in Curitiba, PR.					<B>Results</B>: In general, the students pointed out, above all, the matter of equality related with the themes: human rights, justice and acquaintanceship. The right of equality, proposed by the students, its the foccus of public policies to assure it.					<B>Conclusion</B>: The security and effectiveness of the studied categories, human rights, justice and coexistence can be best made possible with curriculum changes. This intentionality requires listening and valuing voices of young people, defined as subjects of rights entered into a pluralist society, respecting and valuing the plurality that exists there.				</P>
			</div><B>Keywords</B>: Human Rights. Justice. Coexistence. Violence in Schools. Basic Education. Young.</td>
