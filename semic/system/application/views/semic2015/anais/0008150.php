			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A proposta central é passar de uma pastoral bíblica para a animação bíblica de toda ação pastoral. A Palavra de Deus não pode ser um galho ou ramo da ação evangelizadora, mas sim a seiva que perpassa por toda a Igreja. O trajeto para compreender o atual é cenário perpassa desde a Dei Verbum no Concílio Vaticano II, constituição dogmática fundamental para essa novidade. Depois a conferência de Aparecida e sínodo sobre a Palavra de Deus que resultou na publicação da Verbum Domini apresentam claramente a necessidade urgente de uma mobilização bíblica e assim a animação bíblica da Pastoral					<B>Objetivo</B>: Conhecer a pastoral bíblica e a animação bíblica,
Descobrir a importância da animação bíblica da pastoral.					<B>Metodologia</B>: De pesquisa bibliográfica e observação da práxis a partir da Arquidiocese de Londrina.					<B>Resultados</B>: 					<B>Conclusões</B>: A Palavra de Deus deve estar acessível a todos, compreendida por todos e desse modo, ela poderá ser viva por todos. A prática pastoral deve ser a prática de Jesus Cristo e pela fundamentação na Palavra de Deus se chegará a essa realidade.				</P>
			</div><B>Palavras-chave</B>: Animação bíblica. mobilização bíblica. pastoral. Palavra de Deus			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The central proposal is to move from a biblical pastoral to the Biblical animation of all pastoral action. The Word of God can not be a twig or branch of evangelization , but the sap that permeates the whole Church. The path to understanding the current scenario is permeates from the Dei Verbum the Second Vatican Council , Dogmatic Constitution fundamental to this news . After the Aparecida conference and synod on the Word of God that resulted in the publication of Verbum Domini clearly show the urgent need for a biblical mobilization and thus the biblical animation of pastoral					<B>Objectives</B>: Knowing the biblical pastoral and biblical animation,
Discover the importance of biblical pastoral animation.					<B>Methods</B>: Bibliographic research and watching practice from the Archdiocese of Londrina .					<B>Results</B>: 					<B>Conclusion</B>: The Word of God should be accessible to all, understood by all and thus it can be alive for everyone. The pastoral practice should be the practice of Jesus Christ and the foundation in the Word of God come to this reality.				</P>
