			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O uso de células-tronco tem sido uma possível alternativa para pacientes candidatos ao transplante renal sem os riscos que uma cirurgia complexa e de grande porte oferece.					<B>Objetivo</B>: Este estudo foi desenvolvido com o objetivo de realizar uma análise experimental para avaliar os efeitos da isquemia e reperfusão renal sob a eficácia do tratamento utilizando células-tronco mesenquimais.					<B>Metodologia</B>: Trinta ratos Wistar foram separados de maneira aleatória em dois grupos. Grupo 1 (n=15), submetido apenas ao procedimento cirúrgico de isquemia/reperfusão renal sem nenhum medicamento além dos medicamente para dor. Grupo 2 (n=15) recebeu células-tronco mesenquimais logo após do procedimento cirúrgico de isquemia/reperfusão renal.
As amostras de sangue foram retiradas através da veia jugular par a avaliação da função renal pela dosagem de creatinina					<B>Resultados</B>: A dosagem de creatinina nos dias 0, 15 e 30 dos grupos foram: G1 (0,60 + 0,15; 0,69 + 0,09 e 0,86 + 0,13) e G2 (0,56 + 0,10; 0,88 + 0,10 e 0,77 + 0,10), respectivamente. Não foram observadas diferenças estatísticas (p=0,073) entre os grupos estudados.					<B>Conclusões</B>: Assim sendo, utilização isolada de células-tronco mesenquimais não foi efetiva no tratamento dos riscos relacionados ao processo de isquemia/reperfusão, ainda que durante a fase final do experimento foi observada uma recuperação da função renal promovida pela infusão de CDTAs.				</P>
			</div><B>Palavras-chave</B>: Creatinina. transplante renal. medicina regenerativa. reperfusão.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The use of stem cells has been an alternative for patients who are candidates to kidney transplant without the risks involved in a complex surgery					<B>Objectives</B>: This study was conducted to evaluate the effects of ischemia and reperfusion in the efficacy of treatment using mesenchymal stem cells.					<B>Methods</B>: Thirty male Wistar rats were distributed randomly into two groups. Group 1 (n = 15) was subjected to renal ischemia/reperfusion surgery only, without any medication. Group 2 (n = 15) received mesenchymal stem cells after renal ischemia/reperfusion surgery					<B>Results</B>: The creatinine levels were measured by commercial kits for colorimetric method. Creatinine dosages in the days 0,15 and 30 for the groups were: G1 (0.60 ± 0.15, 0.69 ± 0.09 and 0.86 ± 0.13), and G2 (0.56 ± 0.10, 0.88 ± 0.10 and 0.77 ± 0.10) respectively. No statistical differences (P = 0.073) among the groups were observed.					<B>Conclusion</B>: Therefore, the isolated use of mesenchymal stem cells was not effective in the treatment of the risks relating to the process of ischemia/reperfusion, although was observed a recovery of renal function promoted by infusion CDTAs during the final phase of the experiment.				</P>
			</div><B>Keywords</B>: Creatinine. kidney transplant. regenerative medicine. reperfusion</td>
