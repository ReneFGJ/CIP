			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: As dores lombares incidem em cerca de 85% da população em algum momento da vida, sendo, portanto, um grande problema de saúde pública. A dor crônica gera sofrimento biopsicossocial, pois pode levar à incapacidade do indivíduo no controle da dor e da redução de sua capacidade funcional. Assim desencadeia uma redução da qualidade de vida dos indivíduos e contribui para a sua cronicidade. Os programas educativos são uma importante alternativa para aliar tratamento com desenvolvimento de habilidades de aoto cuidado em saúde.					<B>Objetivo</B>: Avaliar as repercussões da dor na funcionalidade e na qualidade de vida de pacientes com dor lombar crônica, participantes de um programa de Escola da Coluna.					<B>Metodologia</B>: Foram avaliados 47 pacientes no período de janeiro a julho de 2015. Foi utilizado o questionário Roland-Morris (RM), Short Form Health Survey (SF-36) e a Escala Visual Analógica de Dor (EVA) para avaliação da qualidade de vida e dor.					<B>Resultados</B>: Dos participantes, 64,6% eram do gênero feminino, com idade média de 33,56 anos, onde 51,1%  não praticavam atividade física e 41,7% apresentaram excesso de peso. Apenas 5, dos 47 pacientes avaliados, iniciaram e concluíram os 10 encontros do programa.					<B>Conclusões</B>: Após a intervenção houve redução da dor e melhora da funcionalidade e da qualidade de vida destes pacientes.				</P>
			</div><B>Palavras-chave</B>: Lombalgia. Escola da coluna. estabilização segmentar. Educação em saúde.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Back pain affect about 85% of the population at some point in life, is therefore a major public health problem. Chronic pain generates biopsychosocial suffering as it can lead to an individual´s inability to control pain and reduce their functional capacity. Thus triggers a reduction in the quality of life of individuals and contributes to its chronicity.					<B>Objectives</B>: This study aimed to assess the effects of pain on function and quality of life of patients with chronic low back pain participating in a column School program.					<B>Methods</B>: For this we used the Roland-Morris (RM), Short Form Health Survey (SF-36) and the Visual Analog Scale of Pain (VAS).					<B>Results</B>: Were evaluatet 47 patients in the period from January to July 2015, and 64.6% were female, mean age of 33.56 years, where 51.1% did not exercise, and 41.7% were overweight. Only 5 of the 47 patients evaluated, initiated and concluded the 10 program meetings.					<B>Conclusion</B>: After the intervention there was a reduction in pain and improved functionality and quality of life of these participants.				</P>
			</div><B>Keywords</B>: Low back pain. Back school. Segmentar estabilization. Health education.</td>
