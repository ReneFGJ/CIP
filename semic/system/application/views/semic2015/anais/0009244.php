			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A proposta da Unesco e a orientação do MEC para a graduação no curso de Medicina para o século XXI estabelecem a importância da formação humanística do profissional de saúde. Contudo, ao concluir o curso, o profissional mostra um perfil predominantemente técnico e com formação ética insatisfatória.					<B>Objetivo</B>: Verificar o conhecimento de acadêmicos de uma escola privada de medicina sobre temas relacionados à Ética e Bioética nas diferentes fases do curso (Pré-Clínica, Clínica e Internato Médico) e comparar os dados destes achados entre a universidade pública e privada no estado do Paraná, PR.					<B>Metodologia</B>: Neste estudo transversal, foi aplicado um questionário estruturado com questões sobre a formação de acadêmicos referentes aos temas de ética e bioética oferecidos durante a graduação em medicina.					<B>Resultados</B>: Observou-se que os acadêmicos do internato relatam que os conteúdos ministrados nessas disciplinas são insatisfatórios (Teste Fisher-Freeman-Halton, p=0,0001). Os acadêmicos afirmam que o ensino dessas disciplinas ao longo da graduação e que as mesmas deveriam ser obrigatórias ou ofertadas ao longo do curso. Quanto ao conhecimento sobre conteúdos específicos de Ética e Bioética, verificou-se que, os alunos recebem mais informações relacionadas a aspectos normativos e aspectos relacionados a Direitos Humanos. Entretanto, as maiores dúvidas dos alunos dizem respeito à mudança de paradigma de curar para cuidar.					<B>Conclusões</B>: Observou-se a deficiência no conhecimento sobre temas relacionados à Ética e Bioética dos acadêmicos de medicina, sendo este conhecimento insuficiente para responder aos dilemas morais apresentados pelos novos avanços das ciências, não atendendo às necessidades de formação do profissional médico.				</P>
			</div><B>Palavras-chave</B>: Ética Médica.Bioética. Educação Médica. Ensino.			<BR>
