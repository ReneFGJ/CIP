			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The recommendations from both UNESCO and Education Ministry from Brazil highlight the importance of humanistic-based formation of health professionals from the XXI century. However, at the end of the course, the  professionals usually show  a predominant technical and poor ethics training profile.					<B>Objectives</B>: This study aimed to assess the knowledge of medical undergraduate students from different stages of the medical course (Pre-Clinical, Clinical and Medical Internship) about topics concerning ethics and bioethics as well as to compare these findings from a public and private university from Parana state.					<B>Methods</B>: At this cross-sectional study, a structured questionnaire was used to asses the students’ knowledge about ethics and bioethics issues.					<B>Results</B>: It was observed that students from medical internship reported that the topics from these disciplines are unsatisfactory (Fisher-Freeman-Halton Test, p = 0.0001). The students think that 
These disciplines should be obligatory or offered throughout the course. Regarding knowledge on specific contents of Ethics and Bioethics, it was found that students receive more information concerning regulatory aspects and human rights. However, the major doubts of the students lay down aspects about changing the paradigm from 
cure to care.					<B>Conclusion</B>: There is a lack of knowledge of medical students on topics related to ethics and bioethics, being this knowledge insufficient to accomplish for the requested medical profile to deal with moral dilemmas presented by science advances.				</P>
