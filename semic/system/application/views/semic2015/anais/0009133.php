Estes resultados estão de acordo com aqueles relatados por Campos e Filho (2010), onde relatam que a Floresta Estacional Semidecidual tem como característica marcante a variação entre dois períodos climáticos, chuvas e as secas. Conforme os mesmos, sua estrutura é formada por camadas, possuindo um estrato arbóreo com dossel (copa) elevado e, logo abaixo, a camada herbácea, composta por plantas de pequeno porte que sofrem intensamente com as ações dos fatores bióticos e abióticos.
Mas, diferentes destes resultados, florestas estacionais semideciduais das regiões Sul e Sudeste do Brasil, em geral, acabam demonstrando destaque para família Myrtaceae, apresentando maior riqueza específica (TABARELLI, MANTOVANI, 1999; JURINITZ, JARENKOW, 2003). Além do que, as famílias Fabaceae, Euphorbiaceae e Lauraceae também se destacaram na maior parte dos levantamentos floristicos neste tipo de formação florestal, apresentando grande riqueza específica (IVANAUSKAS, RODRIGUES, NAVE, 1999; SANTOS, KINOSHITA, 2003).   
Para Liebsch e Acra (2002), a presença de espécies da família Solanaceae pode estar associada ao carater pioneiro na recomposição de áreae que sofreram grande influencia antropica.
A partir do levantamento floristico realizado no presente estudo e de trabalhos realizados em FES (Floresta Estacional Semidecidual) (IVANAUSKAS, RODRIGUES, NAVES, 1999; FONSECA, RODRIGUES, 2000; MARTINS et al, 2003; BERGAMIN, MONDIN, 2006). Pode-se verificar que as espécies registradas na área estudada (Tabela 1), com exceção da Araucaria angustifolia característica de FOM (Floresta Ombrófila Mista), pertencem  a FES. No entanto, todas as espécies podem ser consideradas nativas do município de Toledo donforme descrição de Da Silva, (2008), ao considerar em estudo de Britez, Castella (2004), que mostra que a vegetação do municipio está inserida numa área de transição de FOM-FES. 
A falta de informação sobre a ocupação anteriormente da área, não possibilitou verificar com exatidão a procedência de Araucária angustifólia (Figura 8). Não é possível dizer se a mesma originou-se naturalmente ou teve ocorrência introduzida na área estudada. 


Figura 8: Imagem da espécie arbórea Araucária angustifólia presente no fragmento da Floresta Estacional Semidecidual localizada na Sanga Jacutinga, Campus da PUCPR, município de Toledo/PR, 2015.
	
 
Fonte: o autor, 2015.

A presença desta espécie é significativa, principalmente devido ao registro da mesma atualmente estar na lista de espécies ameaçadas de extinção desde o final de milênio passado (SCHEEREN et al., 1999).
Outras espécies que merecem destaque é a presença de  Tabernaemontana hystrix e Alchornea glandulosa, pois conforme Lorenzi, (2002), estas plantas quando natural indicam que o ambiente apresenta características de Floresta Estacional Semidecidual, podendo proporcionar intensa regeneração espontânea excelente para projetos destinados à recomposição de áreas degradadas.  
Do mesmo modo, muitas outras espécies, típicas de formação vegetacional, foram registradas no presente estudo, como por exemplo: Luehea divaricata Mart., conhecida popularmente como açoita-cavalo. Este vegetal é encontrado mais frequente em florestas mais abertas e/ou formações secundárias (LORENZI, 2002). Já a espécie Rollinia sylvatica, conhecida popularmente como araticum-da-mata, apresenta um complexo mecanismo de proliferação, onde sua dispersão ocorre em áreas abertas ou degradadas. A Ocotea corymbosa, conhecida como canela-fedida, pode ser considerada espécie secundária inicial e frequente em sub-bosques da Floresta Ombrófila Mista. 
	Estes resultados estão de acordo com aqueles observados por Bergamin e Mondin, (2006), onde relataram que o ambiente que apresenta clareiras ao longo do fragmento pode facilita o surgimento de espécies oportunistas. 
Levando em consideração a classificação proposta por Gandolfi, Leitão e Bezerra (1995), as espécies encontradas neste trabalho foram classificadas em grupos ecológicos: pioneiras (P); secundárias iniciais (Si); secundárias tardias (St) e sem caracterização (Sc). Sendo ainda, os grupos relacionados às três categorias de sucessão: fase inicial, média e avançada de sucessão, respectivamente (Tabela 01).
Neste sentido, ao analisar o resultado da distribuição dos diferentes grupos ecológicos das plantas da sanga Jacutinga verificou-se que neste fragmento existe maior predominância de plantas (P) 51,43%, seguidas de (St) 20,00%, (Si) 17,15%  e (Sc) (11,43%) (Figura 9).  

Figura 9: Distribuição percentual do número de espécies amostradas segundo os diferentes grupos ecológicos observados no levantamento fitossociologico na Sanga Jacutinga, Toledo/PR
 
Fonte: o autor, 2015.
Gandolfi, Leitão Filho e Bezerra (1995) definem que espécies pioneiras (P): são dependentes de maior taxa de luminosidade, desenvolvendo adequadamente em clareiras ou nas bordas da floresta, não ocorrendo em sub-bosques. Assim, fica evidente ao comparar com o fragmento avaliado em Toledo, que o mesmo esta no inicio do processo de regeneração. E que a ausência de florescimento e/ou número de indivíduos da mesma espécie pode comprometer a sustentabilidade do fragmento a curto e longo prazo. Pois observou-se que a grande maioria das plantas 96,45% não são frutíferas carnosas, reduzindo a atratividade dos pássaros e mamíferos, prejudicando a disseminação “in loco” das sementes. 
Da mesma forma, o número de indivíduos de espécies pertencentes aos grupos (Si), (St) e (Sc), é o fator principal para o futuro sucesso do fragmento, pois os números não são maiores do que três indivíduos em toda a área. 
	Este estudo foi fundamental dando indícios da atual situação do fragmento e irá contribuir para o aceleramento e sustentabilidade. Novos estudos serão realizados com a finalidade de buscar a produção de novos exemplares que serão introduzidos no local, visto que o mesmo ainda não apresenta auto suficiência de produção de sementes.					<B>Conclusões</B>: O fragmento pode ser considerado um trecho de Floresta Estacional Semidecidual, onde foram identificadas 21 famílias com 33 espécies arbóreas. 
O maior predomínio da vegetação são espécies do grupo ecológico pioneira.
O terreno apresenta-se estável, protegido com gramíneas, mas por consequência fornece lenta proliferação de novos indivíduos. 
A quantidade de indivíduos da mesma espécie pode comprometer a viabilidade do fragmento.
É necessário introduzir novos indivíduos ao fragmento para acelerar a regeneração.				</P>
