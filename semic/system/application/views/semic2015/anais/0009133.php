Ao analisar os resultados da variável “maior riqueza florística das famílias arbóreas”, foi evidenciado uma superioridade para três famílias (Lauraceae, Rutaceae e Fabaceae), com quatro espécies cada, que somadas juntas totalizam 39,90%, do total de espécies identificadas. Este total representa mais de um terço das espécies encontradas apenas para este grupo na sanga Jacutinga (Figura 5). 

Figura 5: Distribuição das famílias com maior riqueza de espécies da Floresta Estacional Semidecidual do Rio Sanga Jacutinga, Toledo – PR. 
 
Fonte: o autor, 2015. 

Não menos importante, as demais famílias que apresentaram quantidades significativas de espécies foram: Cecropiaceae, com 3 espécies (9,00%), seguidas de Euphorbiaceae, Myrtaceae e Solanaceae, cada uma com 2 espécies (18,12%). Nota-se que somadas juntas estas 7 famílias totalizam valores superiores à 67,00% do total de espécies amostradas. Assim, as demais famílias apresentam apenas uma espécie cada, somando 33% do total (Figura 5). 
Este número expressivo de diferentes famílias observadas neste estudo é fundamental para manutenção e sobrevivência deste fragmento, principalmente ligada a sua diversidade. Um dos problemas observados até o momento é a falta de florescimento de muitos indivíduos. Este fenômeno esta sendo observado com atenção, mas é possível que esteja ligado as condições climáticas adversas. Neste sentido, esta sendo elaborado um estudo minucioso com as precipitações e temperaturas ocorridas nos últimos três anos. Como pode ser observado na figura 6 e 7, cujas diferentes imagens das espécies arbóreas da Sanga Jacutinga, não apresentam flores.

