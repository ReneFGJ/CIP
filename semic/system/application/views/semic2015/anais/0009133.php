				<P>
					<B>Introdução</B>: O processo de ocupação no Brasil teve como característica principal a falta de planejamento, tendo como consequência a destruição dos recursos naturais. Ao longo do tempo, os biomas foram se fragmentando e dando espaço para a agricultura, pastagens e urbanização, acarretando na eliminação de florestas, extinção de espécies da fauna e flora, mudanças climáticas locais, erosão do solo e assoreamento dos recursos hídricos (MARTINS, 2009). 
A degradação ambiental é um problema que esta cada vez mais presente nos dias de hoje e direciona-nos a procurar possíveis soluções para reduzi-la ou estabiliza-la. Este processo de degradação pode, muitas vezes, causar danos irreversíveis ao meio ambiente, devido à exploração inadequada dos recursos naturais (JÚNIOR, 2012). 
A agricultura é uma das maiores causadoras de variados impactos ambientais negativos, como: desmatamento contínuo; redução da diversidade; degradação do solo e dos recursos naturais por contaminação química; entre outros. Desta forma, a educação ambiental é um instrumento que esta sendo trabalhado com produtores rurais com o intuito de obter a conscientização destes indivíduos, em aspectos econômicos, sociais e ambientais (RICARDO, 2008). 
A vegetação presente nas margens das nascentes e cursos d’água é fundamental para a preservação ambiental, independente da origem ou da sua denominação (CHAVES, 2009).  
As matas ciliares ou florestas ribeirinhas têm como característica principal a ocorrência ao longo dos cursos d’água ou no entorno das nascentes (RODRIGUES, 2001), e por isso são de vital importância para a proteção de mananciais, pois controlam a chegada de nutrientes, sedimentos e a erosão das ribanceiras. Estas podem atuar na interceptação e absorção da radiação solar, contribuindo para a estabilidade térmica da água, determinando, assim, as características físicas, químicas e biológicas dos cursos d’água (DELITTI, 1989). 
Pelo Código Florestal Brasileiro, Lei Nº 12.651/12, que dispõe sobre a proteção da vegetação nativa, considera as matas ciliares como áreas de preservação permanente (APPs), que tem como objetivo principal proteger os recursos hídricos e a biodiversidade. As APPs em torno de rios e nascentes têm larguras variadas e está relacionada com a largura do curso d’água. 
As matas ciliares, portanto, são florestas que estão ligadas a qualidade de vida e precisam ser preservadas ou restauradas (CASTRO, MELLO e POESTER, 2012), mas a falta de informação e conhecimento a respeito das interações ecológicas dificultam ações concretas e seguras para tal atividade (BARDDAL et al., 2004). Por essa razão, o levantamento fitossociológico é necessário, principalmente nestas áreas em questão, onde o uso e manejo de espécies nativas sejam incentivados, já que a recuperação ambiental com plantas exóticas é um erro, pois estabelece, na prática, uma monocultura que ocupa o espaço de espécies nativas em prejuízo da biodiversidade (KANASHIRO, 2003).
Antes de realizar qualquer alteração ou modificação em uma área florestal, é indispensável ter conhecimento sobre as características do local em estudo, como a biodiversidade existente e o valor fitossociológico dela. Através dessas informações, é possível caracterizar a região, realizar intervenções florestais e conhecer o potencial econômico e florístico da região (SANTOS et al, 2012).
A fitossociologia é considerada a ciência das comunidades vegetais e abrange sobre todos os fenômenos que acontece com as plantas dentro das unidades ecológicas, considerando o solo, vegetação e clima. Ter o conhecimento florístico e fitossociológico das florestas é uma atividade básica e essencial para a conservação e preservação (CHAVES et al, 2013). 
A biodiversidade é a variação entre as espécies de todos os reinos vivos, da qual estas espécies interagem entre si e com o ambiente físico (CASTRO, MELLO e POESTER, 2012).  É considerada o resultado da evolução biológica, que acontece durante milhões de anos, e é um componente que fornece suporte a vida na Terra. A perda da biodiversidade é um problema para a humanidade, pois este é irreversível (JOLY et al, 2011). Mesmo com o intensivo e contínuo desmatamento, o Brasil é um dos países mais ricos do mundo em biodiversidade (MITTERMEIER et al., 2005).  
Atualmente, pode-se observar que a grande maioria dos municípios pequenos já apresentam evidências de degradação ambiental (JÚNIOR, 2012), principalmente em locais onde existe intensa atividade agrícola. 
Desta forma, é necessário a aplicação e o desenvolvimento de técnicas e modelos de recuperação e preservação do meio ambiente, com o restabelecimento de uma vegetação que proteja os recursos naturais, como o solo e os corpos hídricos.
Este projeto tem como objetivo dar continuidade ao projeto de levantamento fitossociológico 2013-2014, que busca determinar o nível de degradação antrópica existente na nascente do Rio Sanga Jacutinga, localizado em Toledo – PR por meio da identificação das espécies florestais nativas ou exóticas existentes neste ambiente para que posteriormente seja realizada estratégias e modelos de recuperação da área degradada.					<B>Objetivo</B>: Realizar o levantamento fitossociológico da comunidade arbórea que compõe um fragmento alterado de Floresta Estacional Semidecidual em torno da nascente do rio Sanga Jacutinga, localizada nas dependências da PUCPR – campus Toledo.					<B>Metodologia</B>: 2.1 OBJETIVO GERAL

Realizar o levantamento fitossociológico da comunidade arbórea que compõe um fragmento alterado de Floresta Estacional Semidecidual em torno da nascente do rio Sanga Jacutinga, localizada nas dependências da PUCPR – campus Toledo. 

2.2 OBJETIVOS ESPECÍFICOS

Identificação o maior número de espécies florestais para fins de futuros projetos de reflorestamento em área degradada no entorno do Rio Sanga Jacutinga; 
Conhecer a diversidade de espécies arbóreas na área estudada;
Determinar o nível de degradação antrópica existente em torno da nascente do Rio Sanga Jacutinga.					<B>Resultados</B>: De posse dos dados finais após nove meses de coleta é possível ter uma dimensão real da situação em que se encontra o fragmento florestal avaliado (figura 3). Após 12 anos de abandono o local apresenta-se estável, sendo o seu solo recoberto por inúmeras espécies de gramíneas. Esta condição fornece adequada proteção contra erosão decorrente tanto da ação das chuvas e/ou ventos. No entanto, esta vegetação esta prejudicando a proliferação natural de novos indivíduos oriundos da dispersão de sementes.
	
