			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Dentre as lesões potencialmente malignas, a leucoplasia bucal é a mais frequente e sua gradação histológica mais amplamente utilizada para determinação do risco de transformação maligna é a de displasia epitelial da OMS-2005, entretanto, ela é subjetiva. O sistema binário de classificação foi elaborado na tentativa de padronizar tornando a classificação reproduzível e não subjetiva. A identificação do perfil de pacientes com lesões de alto risco é importante para uma abordagem mais eficaz.  Considerando que apenas um estudo aponta o perfil dos pacientes com alto risco, esse estudo justifica-se.					<B>Objetivo</B>: Este estudo objetivou verificar o perfil de pacientes com leucoplasia com alto risco de malignização.					<B>Metodologia</B>: Trinta e dois casos com diagnóstico clínico de leucoplasia bucal foram selecionados e cotes histológicos corados por hematoxilina e eosina foram submetidoa a gradação de displasia epitelial de acordo com a OMS (2005) e em seguida classificados em baixo risco e alto risco de malignização, por meio de microscópio ótico. Os dados gênero, idade e localização foram coletados das fichas de biópsia.					<B>Resultados</B>: Doze pacientes apresentaram lesões de alto risco. A média de idade foi de 47 anos, o gênero mais acometido foi o masculino (58%) e as localizações mais frequentes foram mucosa jugal (50%) e rebordo alveolar (25%).					<B>Conclusões</B>: Pacientes portadores de leucoplasia, na quinta década de vida, gênero masculino, com lesões localizadas em mucosa jugal e rebordo alveolar necessitam de uma avaliação e conduta mais minuciosa pois foi o perfil  encontrado nos pacientes com leucoplasia de alto risco.				</P>
			</div><B>Palavras-chave</B>: leucoplasia, gênero, idade, localização			<BR>
