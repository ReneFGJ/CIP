			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Among the potentially malignant lesions , oral leukoplakia is the most common histological grading and its most widely used to determine the risk of malignant transformation of epithelial dysplasia is the WHO - 2005, however, it is subjective . The classification binary system was developed in an attempt to standardize making playable but not subjective classification. The profile identification of patients at high risk of injuries is important for a more effective approach . Whereas only one study shows the profile of patients at high risk , this study is justified .					<B>Objectives</B>: This study aimed to determine the profile of patients with leukoplakia at high risk of malignancy.					<B>Methods</B>: This study aimed to determine the profile of patients with leukoplakia at high risk of malignancy. Thirty- two cases with clinical diagnosis of oral leukoplakia were selected and histological cotes stained with hematoxylin and eosin were submetidoa gradation of epithelial dysplasia according to WHO (2005 ) and then classified into low risk and high risk of malignant transformation through optical microscope. The gender data , age and location were collected from biopsy chips.					<B>Results</B>: Twelve patients had high-risk lesions . The average age was 47 years, the most affected gender was male ( 58%) and the most frequent locations were buccal mucosa ( 50%) and alveolar ( 25%).					<B>Conclusion</B>: Patients with leukoplakia , in the fifth decade of life, male , with lesions located in the buccal mucosa and alveolar ridge require an assessment and more thorough conduct because it was the profile found in patients at high risk of leukoplakia .				</P>
			</div><B>Keywords</B>: leukoplakia , gender, age , location</td>
