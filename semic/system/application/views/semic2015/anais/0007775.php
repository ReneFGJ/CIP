			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Esta pesquisa científica buscou fazer um estudo da sintaxe espacial produzida pelo novo contexto pós-moderno na cidade de Curitiba, sobretudo em seus Eixos Estruturais (Norte, Sul, Leste e Oeste), analisando como as pessoas vivenciam o ambiente e se relacionam com a cidade, considerando seus comportamentos nos edifícios multifuncionais e interiorização dos espaços públicos.					<B>Objetivo</B>: O objetivo consiste em analisar as pessoas vivenciam os espaços e relacionam-se com os mesmo, considerando seus comportamentos nos edifícios multifuncionais e seu entorno.					<B>Metodologia</B>: A metodologia adota seguiu através de um estudo sobre os conceitos, técnicas, estratégias de edifícios multifuncionais, seguida pelo estudo das relações psicológicas ambientais com os edifícios que foram selecionados para aplicação de um questionário, para enfim, a aplicação dos dados levantados no sistema de "Sintaxe espacial", de Hillier e Hanson, com o auxílio do software DepthmapX.					<B>Resultados</B>: Identificação dos pontos onde há maior integração espacial.					<B>Conclusões</B>: Constata-se que os eixos estruturais de Curitiba possuem integração em toda a sua extensão, com maior ou menor intensidade em certos pontos, onde podemos encontrar as galerias de passagem ou as de tipologia fronte a rua.				</P>
			</div><B>Palavras-chave</B>: Sintaxe espacial. Sustentabilidade. Edifícios multifuncionais. Eixos estruturais. Curitiba.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: This research sought to make a scientific study of space syntax produced by the new postmodern context in Curitiba, especially in its structural axes (North, South, East and West), analyzing how people experience the environment and relate to the city, considering its behavior in multifunctional buildings and internalization of public spaces.					<B>Objectives</B>: The aim is to examine people experience the spaces and relate to the same considering its behavior in multifunctional buildings and their surroundings.					<B>Methods</B>: The methodology adopted followed by a study of the concepts, techniques, strategies, multi-functional buildings, followed by the study of environmental psychological relations with the buildings that were selected for application of a questionnaire, for short, the application of data collected in the system of "space syntax" of Hillier and Hanson, with the help of DepthmapX software.					<B>Results</B>: Identification of the points where there is greater spatial integration.					<B>Conclusion</B>: It appears that the structural axis of Curitiba have integration in all its extension, with greater or lesser intensity at certain points where we can find the galleries of passage or the type of front the street.				</P>
			</div><B>Keywords</B>: Space syntax. Sustainability. Multifunctional buildings. Structural axes. Curitiba.</td>
