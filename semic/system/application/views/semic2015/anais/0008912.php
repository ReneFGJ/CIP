			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: This study deals with the judicial intervention in general aspects, and in a more timely fashion the relationship of socio-economic impact of the intervention in legal entity.					<B>Objectives</B>: The article draws the theoretical framework that makes the discussion of the theme as a starting point for the production of knowledge, defining some concepts and then make use of the data collected in the process which allowed an in-depth analysis of the institute of judicial intervention, whose broad objective sense is the study of judicial intervention, and strictly speaking the way in which it is clear the need for intervention in companies, and the consequences of the intervention in the legal entity, the socio-economic impacts generated from the intervention.					<B>Methods</B>: Therefore, was to develop a scientific investigation of the possibility of judicial intervention technique, through case studies, economic impacts of using this tool in concrete situations. In this, will report concretes cases the studies of interventions in companies, highlighting the relevant positive and negative points of the institute and plotting a relationship of judicial intervention and the legal entity.					<B>Results</B>: The data obtained from the research led to a deepening of the relationship between the intervention and the legal entity, obtaining results that affect the social and economic relationship not only of those involved in directly in the relationship, but of every society/community that depends on entity indirectly in their day- to-day.					<B>Conclusion</B>: 				</P>
