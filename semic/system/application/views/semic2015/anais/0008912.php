			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Este estudo trata da intervenção judicial em aspectos gerais, e de forma mais pontual na relação do impacto socioeconômico da intervenção nas pessoas jurídicas.					<B>Objetivo</B>: Para tanto, recorre ao referencial teórico que faz a discussão sobre a temática como ponto de partida da produção de conhecimento, definindo alguns conceitos para então lançar mão dos dados levantados na investigação o que permitiu uma análise aprofundada sobre o instituto da intervenção judicial, cujo objetivo lato senso é o estudo da intervenção judicial, e stricto sensu a forma na qual decorre a necessidade da intervenção nas pessoas jurídicas, e os desdobramentos da intervenção na pessoa jurídica, ou seja, os impactos sociais e econômicos gerados a partir da intervenção.					<B>Metodologia</B>: Portanto, consistiu em desenvolver uma investigação científica acerca da possibilidade do emprego da técnica da intervenção judicial e investigar, através do estudo de casos, os impactos sócios econômicos da utilização dessa ferramenta nas situações concretas. No presente relatório far-se-á uma análise concreta de casos de intervenções em pessoas jurídicas, ressaltando os pontos relevantes positivos e negativos do instituto e traçando uma relação da intervenção judicial e a pessoa jurídica, e em linhas gerais ressaltando quais são os impactos gerados a partir dessa relação.					<B>Resultados</B>: Os dados obtidos a partir da pesquisa propiciou um aprofundamento da relação entre a intervenção e a pessoa jurídica, obtendo-se resultados que afetam a relação social e econômica não só dos envolvidos na diretamente na relação, mas sim de toda comunidade que depende da pessoa jurídica indiretamente no seu dia-a-dia.					<B>Conclusões</B>: 				</P>
			</div><B>Palavras-chave</B>: Direito processual. intervenção judicial. impactos socioeconômico.			<BR>
