			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A justa causa revela-se essencial ao processo penal. Trata-se da necessidade de demonstração na peça inicial (denúncia ou queixa) quanto aos elementos de autoria e materialidade do delito. Dessa forma, a inexistência ou a presença de dúvida quanto a esses indícios torna o juízo de admissibilidade prejudicado. Nos crimes fiscais ocorre uma relativização desse preceito, em especial, quando há pluralidade de sócios no âmbito empresarial. Ocorre que o conjunto probatório elaborado na fase administrativa e pré processual não é suficiente haja vista que, em situações pontuais, o inquérito policial é dispensável e somente o procedimento administrativo fiscal e o contrato social não indicam a provável autoria do delito. Desde o momento em que o acusado é submetido à condição de réu, configuram-se prejuízos, logo, evidente a importância da observância mais rigorosa da demonstração da justa causa					<B>Objetivo</B>: Sob esse enfoque, buscou-se a ligação entre aspectos teóricos e práticos que unem a delimitação de justa causa e sua aplicação aos crimes fiscais praticado pelo sócio administrador.					<B>Metodologia</B>: Para tanto, a utilização do método dedutivo foi essencial para a finalização da pesquisa. Por meio da pesquisa bibliográfica obteve-se as definições, aspectos principiológicos e aprofundamentos sobre a justa causa e a investigação criminal, em geral, temas pacificados no aspecto doutrinário. Acerca da jurisprudência, tal pesquisa resguardou o intuito de superar a legislação e doutrina haja vista sua aplicação divergente e urgente necessidade de mudança em benefício dos réu submetidos a tais condições equivocadamente					<B>Resultados</B>: 					<B>Conclusões</B>: 				</P>
