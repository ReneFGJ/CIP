			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A tecnologia BIM trata de um conjunto de informações que são geradas e administradas durante todo o ciclo de vida de uma obra. A complexidade dos modernos projetos e das tecnologias de construção faz com que o BIM se torne uma ferramenta útil na construção civil. Além de permitir um eficiente processo de construção que economiza tempo e dinheiro, o BIM pode sanar os problemas de coordenação e gerenciamento de projetos, em comparação com as práticas tradicionais, visto que o uso dessa tecnologia pode melhorar a capacidade de integrar todos os membros de equipes de projeto.					<B>Objetivo</B>: O objetivo desta pesquisa é investigar os modelos de implementação da tecnologia BIM nos Cursos de Engenharia Civil e Arquitetura nas universidades que proporcione uma base de conhecimentos das técnicas colaborativas e das ferramentas de modelagem. Como objetivo específico, as pesquisas sobre Universidades atuais de engenharia que já introduziram BIM nos seus cursos permitiram identificar as metodologias que estão sendo aplicadas.					<B>Metodologia</B>: Foi adotado o método de revisão sistemática por ser uma revisão planejada que auxilia na resposta a uma pergunta específica, identificando, selecionando e avaliando de maneira crítica os estudos encontrados, dentro de critérios preestabelecido. Este método resultou na leitura relevante de vários abstracts que permitiram a seleção de dois artigos publicados relevantes que apresentaram as estratégias adotadas nas universidades para implementação inicial desta tecnologia.					<B>Resultados</B>: Resultados: observou-se a pequena quantidade de artigos relacionados ao tema da pesquisa, o que demonstra que a tecnologia BIM ainda está em fase de desenvolvimento dentro das universidades; fator esse que dificulta e ao mesmo tempo estimula a continuação da pesquisa científica.					<B>Conclusões</B>: 				</P>
