			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The BIM technology is a set of information that is generated and managed throughout the life cycle of a work. The complexity of modern designs and construction technology makes BIM becomes a useful tool in construction. In addition to enabling an efficient construction process that saves time and money, BIM can solve the problems of coordination and project management, compared with traditional practices, since the use of this technology can improve the ability to integrate all members of project teams.					<B>Objectives</B>: The objective of this research is to investigate the implementation of models of BIM in Civil Engineering and Architecture courses in universities to provide a base of collaborative technical knowledge and modeling tools. As a specific purpose, research on current engineering universities that have introduced BIM in their courses have identified the methodologies being applied.					<B>Methods</B>: We adopted the method of systematic review to be a review planned that aids in response to a specific question, identifying, selecting and evaluating critically the studies found within pre-established criteria. This method resulted in the relevant reading several abstracts that allowed the selection of two relevant published articles that presented the strategies adopted in universities for initial implementation of this technology.					<B>Results</B>: There was a small amount of articles related to the topic of the research, which shows that the BIM technology is still under development within universities; this factor which makes it difficult and at the same time stimulates the continued scientific research.					<B>Conclusion</B>: 				</P>
