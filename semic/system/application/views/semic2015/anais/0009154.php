			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Este projeto foi desenvolvido com o propósito de contribuir para um dos objetivos do projeto PELD, o qual visa ajustar equações para estimar a altura total e comercial para as principais espécies, para grupo de espécies e para o total da floresta.					<B>Objetivo</B>: testar os principais modelos matemáticos utilizados para estimativa da altura total e comercial das árvores em função do diâmetro a altura do peito (DAP) para um remanescente de Floresta Ombrófila Mista Montana, localizada na Fazenda Experimental Gralha Azul no Município de Fazenda Rio Grande no Estado do Paraná.					<B>Metodologia</B>: Foram classificadas as espécies ocorrentes na região em quatro grupos, de acordo com o DAP máximo da espécie encontrado nas parcelas experimentais do projeto. Para o ajuste dos dados da altura foram selecionadas e testadas 10 (dez) equações matemáticas citadas na literatura. Como critério estatístico de seleção das melhores equações foi utilizado o maior coeficiente de determinação ajustado (R² ajustado), menor erro padrão da estimativa em porcentagem (Syx %) e melhor distribuição dos resíduos.					<B>Resultados</B>: Duas equações se destacaram com relação ao coeficiente de determinação, uma para altura total e outra para altura comercial, porém ambas as equações não se mostraram boas em todos os quesitos analisados.					<B>Conclusões</B>: A estratificação dos dados por espécies de acordo com o DAP máximo não apresentou resultados satisfatórios para estimar as alturas total e comercial das árvores da floresta em questão.				</P>
			</div><B>Palavras-chave</B>: Grupo de espécies. Modelos matemáticos. Altura.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: This study was developed to contribute to the studies of the PELD project, which aims to adjust equations to estimate total and commercial height for the main species, for groups of species and for the total forest.					<B>Objectives</B>: In this way, it aimed to test the main mathematical models used to estimate the total and commercial height of the trees due to the diameter at breast height (DBH) to a fragment of Araucaria Forest, located in Gralha Azul Experimental Farm in the City of Fazenda Rio Grande, state of Parana.					<B>Methods</B>: The species occurring in the region were classified into four groups according with maximum DBHof the species found in experimental plots of the project. To adjust the height data were selected and tested ten (10) mathematical equations described in the literature.					<B>Results</B>: As a statistical criterion for selecting the best equation was used the higher adjusted coefficient of determination (R²adj), the lower standard error of estimate in percentage (Syx%) and the better distribution of residues.					<B>Conclusion</B>: Two equations were more efficient to estimate the heights, one for total height and one for commercial height, but both chosen equations were not good at all analyzed points.				</P>
			</div><B>Keywords</B>: Group of species. mathematical equations.  height.</td>
