			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Estudos geotécnicos são fundamentais para obras de engenharia. O comportamento dos solos, se não verificado, pode causar grandes desastres, tanto urbanos quanto ambientais. O conhecimento da resistência ao cisalhamento do solo local é imprescindível para um bom desenvolvimento de projeto e/ou para monitoramento do local, evitando tais desastres. Ensaios laboratoriais de compressão triaxial são realizados para a obtenção dos parâmetros de resistência ao cisalhamento (ângulo de atrito interno e intercepto coesivo) do solo, desta forma, é possível prever o comportamento do solo, simulando os carregamentos a que este será submetido.					<B>Objetivo</B>: O presente projeto objetiva a determinação de perâmetros de resistência ao cisalhamento.					<B>Metodologia</B>: Para isto, foram realizados ensaios triaxiais drenados (CID), a partir das tensões efetivas de confinamento de 200, 100 e 50 kPa em um solo amostrado nas margens do reservatório da UHE Santa Clara, localizada no município de Candói, PR.					<B>Resultados</B>: Obtiveram-se ângulo de atrito interno 29,8° e intercepto coesivo 2,9 kPa.					<B>Conclusões</B>: Os parâmetros de resistência ao cisalhamento citados poderão ser utilizados pela concessionária responsável pela UHE em questão, em projetos geotécnicos de estruturas de contenção do tipo: muro de arrimo, gabiões, etc. Tais estruturas a serem dimensionadas dependem, evidentemente, de projetos adequados de engenharia.				</P>
			</div><B>Palavras-chave</B>: Ensaios triaxiais. Ensaios de laboratório. Resistência ao cisalhamento			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Geotechnical studies are fundamental to engineering works. The behavior of soils, if not checked, can cause major disasters, both urban and environmental. Knowledge of the local soil shear strength is essential to a successful development project and/or location of monitoring, avoiding such disasters. Laboratory triaxial compression tests are performed to obtain the shear strength parameters (internal friction angle, cohesive intercept) of the soil, it is thus possible to predict the behavior of the soil, simulating the loads to which it will be subjected.					<B>Objectives</B>: This project aims the determination of shear strength parameters.					<B>Methods</B>: For this, drained triaxial tests (CID) were performed, from the confining effective stress of 200, 100 and 50 kPa in a sampled soil on the banks of the Santa Clara (HPP) plant reservoir, in Candói city, Parana State.					<B>Results</B>: There was obtained internal friction angle of 29.8 °and 2.9 kPa cohesive intercept.					<B>Conclusion</B>: The shear strength parameters cited may be used by the HPP in geotechnical projects. Such structures are dimensioned depend of course suitable engineering projects.				</P>
			</div><B>Keywords</B>: Triaxial tests. Laboratory tests. shear strength</td>
