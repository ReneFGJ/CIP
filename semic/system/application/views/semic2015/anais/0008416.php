			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: One of the paradigms about the biopatolgia of parodontitis if reports that high levels of pro-inflammatory mediators originating from the host are always present at sites characterized as periodontalmente patients, therefore, must be assumed that anti-inflammatory drugs should have an influence on the control of the disease.					<B>Objectives</B>: The objective of this work was to analyze the response caused by periodontal disease induced by ligatures in Wistar rats treated with ibuprofen by biochemical tests for evaluation of the serum level of calcium and aspartate aminotransferase (AST).					<B>Methods</B>: Periodontal Disease was induced with bandages on the lower left first molars in a sample of 96 rats were divided into 4 groups according to the treatment: I7-Ibuprofen for 7 days; Non-Ibuprofen for 14 days; C7-Control for 7 days; C14-Control for 14 days					<B>Results</B>: In analysing the treatment with ibuprofen with 7 and 14 days, the test for serum levels of calcium and the activity of the enzyme AST, the group treated with Ibuprofen showed no significant difference. The results show statistically significant differences in weight reduction of animals regardless of treatment and time used					<B>Conclusion</B>: Within the limits of the present study, we conclude that the use of ibuprofen associated with periodontitis induced by bandages showed no significant differences in the variables AST and calcium levels showing similar behavior to the control group.				</P>
			</div><B>Keywords</B>: Periodontitis. anti-inflammatory. aspartate aminotransferase. calcium.</td>
