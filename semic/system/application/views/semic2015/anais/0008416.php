			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Dentre os paradigmas sobre a biopatolgia das periodontites se relata que níveis elevados de mediadores pró-inflamatórios originados no hospedeiro estão sempre presentes em sítios caracterizados como periodontalmente doentes, sendo assim, deve ser considerado que anti-inflamatórios deveriam ter uma influência no controle da doença					<B>Objetivo</B>: O objetivo deste trabalho foi analisar a resposta causada por doença periodontal induzida por ligaduras em ratos Wistar tratados com ibuprofeno mediante testes bioquímicos para avaliação do nível sérico de cálcio e aspartato aminotransferase (AST).					<B>Metodologia</B>: Foi induzida Doença Periodontal com colocação de ligaduras no primeiro molar inferior esquerdo em uma amostra de 96 ratos divididos em 4 grupos de acordo com o tratamento: I7 – Ibuprofeno por 7 dias; I14 – Ibuprofeno por 14 dias; C7 – Controle por 7 dias; C14 – Controle por 14 dias.					<B>Resultados</B>: Ao analisarmos os tratamentos com ibuprofeno com 7 e 14 dias, os testes para nível sérico de cálcio e para a atividade da enzima AST, o grupo tratado com Ibuprofeno não apresentou diferença significativa. Os resultados demonstram diferenças estatisticamente significantes na redução do peso dos animais independentemente do tratamento e tempo utilizado.					<B>Conclusões</B>: Dentro dos limites do presente estudo, podemos concluir que o uso do ibuprofeno associado a periodontite induzida por ligaduras não apresentou diferenças significativas nas variáveis AST e níveis de cálcio demontrando comportamento semelhante ao grupo controle.				</P>
			</div><B>Palavras-chave</B>: Periodontite. anti-inflamatório. aspartato aminotransferase. cálcio.			<BR>
