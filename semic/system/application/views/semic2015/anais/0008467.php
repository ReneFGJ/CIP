			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: As doenças cardiovasculares são uma das principais causas de óbito ao redor
do globo segundo dados  obtidos da OMS. Com base nessa epidemiologia, é de extrema
importância conhecer o perfil dos pacientes afetados por essa entidade e a eficácia
do tratamento instituído					<B>Objetivo</B>: O presente estudo tem como intuito avaliar o impacto da orientação médica como forma de aumentar a sobrevida destes pacientes.					<B>Metodologia</B>: O delineamento da pesquisa foi realizado através do acompanhamento dos pacientes por um período de 12 meses via ligações telefônicas após sua inclusão e assinatura do Termo De Consentimento Livre Esclarecido, com base nos critérios de inclusão e exclusão.					<B>Resultados</B>: Obteve-se um montante de 240 pacientes ao longo do estudo, onde foi possível estabelecer uma relação entre os eventos cardiovasculares e óbitos. Observou-se uma prevalência de 5% de eventos cardiovasculares no montante.					<B>Conclusões</B>: A análise dos resultados obtidos permitiu concluir que
mesmo com acesso a um acompanhamento médico adequado os índices de
morbimortalidade ainda continuam alarmantes.				</P>
			</div><B>Palavras-chave</B>: Risco cardiovascular. epidemiologia. óbito cardiovascular.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Cardiovascular diseases are a leading cause of death around the globe according to data obtained from the who. Based on epidemiology, it is extremely important to know the profile of patients affected by this entity and the effectiveness of the treatment.					<B>Objectives</B>: Therefore, the present study has as objective to evaluate the impact of medical advice as a way to increase the survival of these patients.					<B>Methods</B>: The design of the survey was conducted through monitoring of patients for a period of 12 months via phone calls after their inclusion and signing the consent form Free Clarified, on the basis of the criteria of inclusion and exclusion. Produced a total of 240 patients over the course of the study, where it was possible to establish a relationship between the cardiovascular events and deaths.					<B>Results</B>: There was a prevalence of 5% of cardiovascular events in the amount.					<B>Conclusion</B>: The analysis of the results obtained made it possible to conclude that even with access to appropriate medical follow-up rates of morbidity and mortality are still alarming.				</P>
			</div><B>Keywords</B>: Cardiovascular risk. epidemiology. cardiovascular death.</td>
