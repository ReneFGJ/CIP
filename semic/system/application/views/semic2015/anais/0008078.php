			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Os brackets cerâmicos desenvolveram-se para melhorar esteticamente o bracket metálico, mantendo a funcionalidade. Porém, a desvantagem deste material é a baixa resistência às fraturas, devido a cerâmica ser ao mesmo tempo dura e friável.					<B>Objetivo</B>: o objetivo da presente pesquisa foi avaliar, com o método de elementos finitos (MEF) os efeitos da aplicação de torque no bracket cerâmico pré-ajustado.					<B>Metodologia</B>: O bracket cerâmico Clarity™ ADVANCED prescrição MBT .022", com canaleta metálica, para incisivo central superior direito, os segmentos de fios de aço inoxidável com secção retangular .0215" x .027" de cantos arredondados, e .021" x .025" de cantos vivos e a região anterior da maxila foram modelados e exportado para o programa Autodesk Simulation Multiphysics®, para a análise pelo MEF.					<B>Resultados</B>: Nos brackets com canaleta metálica e fio retangular de cantos vivos observou-se maior concentração de tensões na área superior/cervical, na região média da parede superior vista no sentido sagital da canaleta do bracket. Com o fio de cantos arredondados as tensões estão localizadas nas paredes horizontais e vertical da canaleta dos brackets, mas não nos corners.					<B>Conclusões</B>: Neste modelo tridimensional o fio retangular .021” x .025” com com cantos vivos apresentou distribuição de tensões mais homogênea e o fio retangular .0215”x .027” com cantos arredondados, distribuição de tensões menos homogênea na canaleta do bracket.				</P>
			</div><B>Palavras-chave</B>: Bracket cerâmico. Canaleta metálica. Incisivo Torque. Análise de Elementos Finitos.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: In general, adult patients are more demanding in the selection of braces by choosing ceramic brackets that cause less cosmetic interference. However, a drawback of this material stems from the fact the ceramic is both hard and crumbly, making it less resistant to fractures.					<B>Objectives</B>: The aim of this study was to evaluate, by means of finite element method (FEM) the effects of torque or third order bends in the ceramic bracket.					<B>Methods</B>: A maxillary right central incisor Clarity™ metal-reinforced ADVANCEDTM ceramic brackets, MBT prescription .022-in, the segments of stainless steel wires with rectangular section .0215 x .027-in rounded corners, a .021" x .025" of squared corners and the anterior maxilla were modeled and exported to Autodesk Simulation Multiphysics® program for analysis by the MEF.					<B>Results</B>: In brackets with metal-reinforced with square corners there was a higher concentration of stresses on the cervical area, in the middle region of the upper wall seen in the sagittal direction of the bracket slot. With the rounded corners wire, the stress was located in the horizontal and vertical walls of the slot, but not in the corners.					<B>Conclusion</B>: In this three-dimensional model the .021 x .025-in wire with square edges presented more homogeneous distribution of stresses and the .0215 x .027-in with rounded corners, presented less homogeneous distribution of stresses into the bracket slot.				</P>
			</div><B>Keywords</B>: Ceramic bracket. Metal reinforced. Incisor Torque. Finite Element Analysis</td>
