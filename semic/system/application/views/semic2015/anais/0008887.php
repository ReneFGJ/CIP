			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Muitas áreas urbanas estão pré-dispostas a sofrer alterações e muitas destas mudanças são decorridas pelo crescimento demográfico.  Um estudo de caso que prevê esta mudança é a atual Linha Verde da Cidade de Curitiba, PR. O crescimento demográfico curitibano fez com que a urbanização sucumbisse o trecho da BR-116 Rodovia Regis Bittencourt, e com isso o planejamento urbano de Curitiba precisou tomar diversas medidas para desafogar o trânsito e dar novamente o sentido de via rápida e de ligação interbairros.					<B>Objetivo</B>: Apresentar um estudo de caso que facilitasse a compreensão da Previsão de Cenários Urbanos, analisando estrategicamente a área pelo seu aspecto socioeconômico e as demandas do mercado imobiliário					<B>Metodologia</B>: Por meio da evolução dos cenários detectados e pelas mudanças de zoneamento ao longo dos anos, e analisando os cenários estabelecidos pelos mesmos, pode-se analisar e discutir os cenários futuros					<B>Resultados</B>: Analise dos cenários urbanos utilizando o estudo de caso da Linha Verde como instrumento de gestão urbana do ambiente construído.					<B>Conclusões</B>: Pode-se concluir que a mudança da Lei No 13.909/2011, onde houve a mudança de zoneamento e a reestruturação da antiga BR-116 trouxe e ainda trará grandes benefícios a população que tem contato com a Linha Verde. Observou-se também a transformação do principal Eixo estrutural urbano de Curitiba, que mudando o seu uso e consequentemente o seu cenário, convida a participar ainda mais da cidade,				</P>
			</div><B>Palavras-chave</B>: Cenários, Análise, Urbano, Estratégias.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Many urban areas are predisposed to be changed and many of these changes are elapsed by population growth. A case study that provides this change is the current Green Line Curitiba, PR City. The Curitiba´s population growth has meant that urbanization succumbed stretch of the BR-116 highway Regis Bittencourt, and with it the urban planning Curitiba had to take several measures to ease traffic and again give the sense of highway and between neighborhoods connection.					<B>Objectives</B>: To present a case study that would facilitate the understanding of Urban Scenario Forecasting, strategically analyzing the area for its socio-economic aspect and the demands of the real estate market.					<B>Methods</B>: Through the evolution of the detected scenarios and the zoning changes over the years, and analyzing the scenarios established by them, can analyze and discuss future scenarios					<B>Results</B>: Analysis of urban settings using the Green Line case study as urban management tool of the built environment.					<B>Conclusion</B>: : It can be concluded that the change of Law No. 13,909 / 2011, where there was a change in zoning and the restructuring of the former BR-116 brought and will bring great benefits to people who have contact with the Green Line. There was also the transformation of the main urban structural axis of Curitiba, which changed its use and therefore its scenery, invites you to participate even more in the city.				</P>
			</div><B>Keywords</B>: Scenarios, Analysis, Urban Strategies.</td>
