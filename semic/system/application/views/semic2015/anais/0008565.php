			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Compósitos laminados têm grande relevância na engenharia devido as suas aplicações, como na construção de fuselagens de aeronaves, cascos de embarcações, carrocerias de carros e estruturas em concreto armado. No estudo dos compósitos laminados deste trabalho, emprega-se análises numérico-computacionais via elementos finitos baseados na notação strain gradient para avaliação do comportamento dessas estruturas. A formulação strain gradient permite que termos espúrios que causam erros de travamento (erro numérico) sejam identificados e removidos antes da implementação computacional.					<B>Objetivo</B>: O objetivo principal do trabalho é avaliar os resultados de tensões, deformações e deslocamentos em estruturas formadas por compósitos laminados.					<B>Metodologia</B>: Para as análises foram escolhidos modelos distintos, como vigas engastadas e placas apoiadas, variando condições de contorno, distribuição de força e refino de malha. Mas a principal variação em que foram submetidos os modelos foi a simulação do problema com os termos espúrios e sem os termos espúrios causadores de erros. Essa variação foi importante para fazer comparações entre os dois resultados e assim entender qual influência desses termos nos resultados.					<B>Resultados</B>: A tendência fica clara nas análises, pois os valores da análise dotada de erro se distância da análise livre de erros. Isso mostra como é grande a influência de erros na solução e a importância da remoção desses erros, que são causados pelos termos espúrios.					<B>Conclusões</B>: Durante a implementação computacional foram encontrados problemas, que foram solucionados posteriormente. A partir disso foram feitas simulações e os resultados mostraram a grande influência que os termos espúrios tem sobre os resultados. Portanto, a notação strain gradient se mostrou muito eficiente para análise de compósitos laminados.				</P>
			</div><B>Palavras-chave</B>: Compósitos laminados. Elementos serendipity. Análise de erros. Notação strain gradient.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Laminated composites are very important in structural engineering due to its applications in airplane, ship and car structures, and in reinforced concrete structures among others.  In this study of laminated composites, finite element analyses based on strain gradient notation are employed to study the behavior of such structures. Strain gradient notation allows for the identification and elimination of spurious terms which cause numerical errors prior to the computational implementation.					<B>Objectives</B>: The primary objective of this work is to evaluate stresses, strains and displacements in laminated composite structures.					<B>Methods</B>: Different problems were chosen to be analyzed such as cantilever beams and simply-supported plates. Boundary conditions, applied loading and mesh refinement were varied. However, the most important issue is the analysis of each model using elements with spurious terms and then elements without spurious terms, which is essential to explain the effects of such terms in the quality of the results.					<B>Results</B>: It is observed that results provided by the model containing spurious terms are far from results provided by the correct model. This shows how great is the influence of spurious terms in the numerical solution, and how important is the removal of those terms.					<B>Conclusion</B>: Operational problems were encountered during the computational implementation stage. Having solved that, numerical simulations were performed, and the results obtained showed the great influence of spurious terms in the results. Therefore, the strain gradient notation proved itself very efficient for the analysis of laminated composites.				</P>
			</div><B>Keywords</B>: Laminated composites. Serendipity elements. Spurious terms. Strain gradient notation.</td>
