			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The oral leukoplakia , the most common malignancy presents a potentially subjective histologic grading that is used to determine the risk of malignant transformation ( WHO 2005) . In this sense , the binary classification system attempts to make playable but not subjective classification. The identification of patients at low risk profile with injuries is interesting to help decide the clinical management .					<B>Objectives</B>: This study aimed to determine the profile of patients with leukoplakia with low risk of malignancy.					<B>Methods</B>: Thirty-two cases of oral leukoplakia were identified and histological sections stained with hematoxylin and eosin were classified according aa gradation epithelial dysplasia of the WHO ( 2005) and then in the binary system by means of an optical microscope . The gender data , age and location were collected from biopsy chips.					<B>Results</B>: Twenty patients had low-risk injuries. The average age was 57 years, the most affected gender was female ( 55.0 %) and the most frequent location was the buccal mucosa (40%) .					<B>Conclusion</B>: Within the sampling limitations , the patient profile with low-risk leukoplakia patients is , in the sixth decade of life, women, with lesions located in the oral mucosa . This information may help decide the clinical management .				</P>
