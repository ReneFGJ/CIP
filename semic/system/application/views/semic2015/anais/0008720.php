			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A leucoplasia bucal, a mais frequente lesão potencialmente maligna apresenta uma gradação histológica subjetiva que é utilizada para determinação do risco de transformação maligna (OMS-2005). Nesse sentido, o sistema binário de classificação tenta tornar a classificação reproduzível e não subjetiva. A identificação do perfil de pacientes com lesões de baixo risco é interessante para auxiliar na decisão da conduta clínica.					<B>Objetivo</B>: Este estudo objetivou verificar o perfil de pacientes com leucoplasia com baixo risco de malignização.					<B>Metodologia</B>: Trinta e dois casos de leucoplasia bucal foram identificados e secções histológicas corados por hematoxilina e eosina foram classificados de acordo com a a gradação de displasia epitelial da OMS (2005) e em seguida no sistema binário, por meio de microscópio ótico. Os dados gênero, idade e localização foram coletados das fichas de biópsia.					<B>Resultados</B>: Vinte pacientes apresentaram lesões de baixo risco. A média de idade foi de 57 anos, o gênero mais acometido foi o feminino (55,0%) e a localização mais frequente foi a mucosa jugal (40%).					<B>Conclusões</B>: Dentro das limitações amostrais, o perfil do paciente com leucoplasia de baixo risco é pacientes, na sexta década de vida, gênero feminino, com lesões localizadas em mucosa jugal. Essas informações podem auxiliar na decisão da conduta clínica.				</P>
			</div><B>Palavras-chave</B>: leucoplasia, gênero, idade, localização			<BR>
