			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A interface cérebro-computador (ICC, do inglês brain-computer interfaces, BCIs) tem emergido como uma área promissora de pesquisa, isto se dá por conta da sua capacidade em fornecer aos seus usuários a possibilidade de comunicação e controle que não dependem de músculos.					<B>Objetivo</B>: Logo, este projeto traz uma abordagem para um sistema de comunicação entre uma pessoa e um veículo aéreo não tripulado, também conhecido como drone. No qual, através de um encefalograma, capta-se sinapses, troca de informações entre neurônios, que são convertidas em vetores numéricos e posteriormente classificadas por um algoritmo.					<B>Metodologia</B>: Esta abordagem pode ser promissora para o projeto de sistemas que possam ajudar pessoas com limitações vinculadas de coordenação motora, tais como em casos de pessoas tetraplégicas ou quadriplégicas, que possuem paralisia em todas as quatro extremidades, superiores e inferiores, juntamente à musculatura do tronco, mas que possuem atividade cerebral. Além disso, o projeto tem um intuito de utilizar algoritmos de otimização e mais especificamente redes neurais artificiais para classificar os sinais que serão recebidos através de um eletroencefalograma e, desta forma, gerar resultados para o controle de um veículo aéreo não tripulado.					<B>Resultados</B>: 					<B>Conclusões</B>: A classificação é realizada por uma Rede Neural Artificial (RNA), um modelo adaptativo inspirado no próprio cérebro humano, que busca padrões entre os dados de entrada gerando como saída comandos pré-definidos para o drone. Esta abordagem pode ser uma base promissora para o projeto de sistemas que possam ajudar pessoas com limitações vinculadas de coordenação motora, tais como em casos de pessoas tetraplégicas ou quadriplégicas, que possuem paralisia em todas as quatro extremidades, superiores e inferiores, juntamente à musculatura do tronco, mas que possuem atividade cerebral, onde através da utilização de ICCs estimula-se o cérebro das mesmas de forma a controlar objetos ou executar determinadas funções.				</P>
			</div><B>Palavras-chave</B>: Eletroencefalograma. interface cérebro-computador. veículos aéreos não-tripulados.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The brain-computer interface (BCIs) has emerged as a promising area of research, it happens because of their ability to provide their users to communication and control possibility, which do not depend of their muscles.					<B>Objectives</B>: Therefore, this project brings an approach for a communication system between a person and an unmanned aerial vehicle, also known as drone. Whereupon, through an encephalogram, it captures synapses, information exchange between neurons, which are converted into numerical vectors and subsequently classified by an algorithm.					<B>Methods</B>: Classification is performed by an Artificial Neural Network (ANN), an adaptive model inspired by the human brain itself, seeking patterns among the input data generating as output pre-defined commands to the drone.					<B>Results</B>: 					<B>Conclusion</B>: This approach may be a promising basis for system design which can help people with bound constraints motor coordination, such as in cases of quadriplegia persons having paralysis of all four limbs, upper and lower, along the musculature stem, but have cerebral activity, which through the use of ICCs stimulates the brain is the same in order to control objects or perform certain functions.				</P>
			</div><B>Keywords</B>: Electroencephalogram. brain-computer interface. unmanned aerial vehicles.</td>
