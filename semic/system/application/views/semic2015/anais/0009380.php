			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Os espaços públicos de lazer são locais privilegiados para vivências e encontros significativos dentro dos centros urbanos. Em Curitiba, as Academias ao Ar livre (AAL) são estruturas que, dentre outros, busca incentivar a pratica de hábitos sadios.					<B>Objetivo</B>: Nesse sentido, os objetivos deste estudo são os de avaliar a qualidade e segurança das ruas em torno das AAL e verificar como essas variáveis podem se relacionar com o perfil dos usuários desses espaços.					<B>Metodologia</B>: Para tanto, foi realizado um estudo descritivo transversal com instrumentos e protocolos de observação validados e já utilizados em outros estudos.					<B>Resultados</B>: Os resultados mostram que as ruas em torno das AAl pesquisadas apresentam uma razoável qualidade porém poucos itens que ofereçam segurança aos pedestre. Com relação ao perfil dos usuários, as análises estatísticas apontaram haver diferenças significativas entre o nível de atividade física, gênero e idade dos usuários com as variáveis de segurança e qualidade das ruas entorno das AAL.					<B>Conclusões</B>: Devido ao investimento público e o aumento significativo desses espaços em vários centros urbanos, cabem maiores estudos que busquem elucidar a melhor utilização dos mesmos.				</P>
			</div><B>Palavras-chave</B>: Espaços Públicos, Lazer, Atividade Física			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Public leisure spaces are prime locations for experiences and significant meetings in towns. In Curitiba, the free to air Academies (AAL) are structures that, among others, seeks to encourage the practice of healthy habits.					<B>Objectives</B>: In this sense, the objectives of this study are to evaluate the quality and safety of the streets around the AAL and see how these variables can relate to the profile of users of these spaces.					<B>Methods</B>: Therefore, we performed a cross-sectional study with validated instruments and observation protocols have already been used in other studies.					<B>Results</B>: The results show that the streets around the AAL surveyed have a reasonable quality but few items that provide security to pedestrians. Regarding the profile of the users, the statistical analysis showed significant differences between the level of physical activity, gender and age of users with the security variables and quality of the surrounding streets of AAL.					<B>Conclusion</B>: Due to public investment and the significant increase in these spaces in various urban centers, fit larger studies that seek to elucidate the best use of them.				</P>
			</div><B>Keywords</B>: Public spaces; Lazer;  physical activity</td>
