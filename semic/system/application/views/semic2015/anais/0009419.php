			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A lactoferrina é uma glicoproteína presente na imunidade inata, que desempenha papel importante na proteção das mucosas contra diversos tipos de microrganismos patogênicos. Muitos fungos filamentosos encontrados no ambiente podem ser patogênicos, causando principalmente micoses cutâneas podendo causar também micoses sistêmicas, constituindo um sério problema de saúde pública.					<B>Objetivo</B>: O propósito deste estudo foi verificar a ação fungicida da lactoferrina em isolados clínicos de fungos filamentosos. Aspergilus fumigatus, Trichosporon asahii, Rhizopus oryzae e  Malassezia furfur.					<B>Metodologia</B>: As placas de microdiluição em caldo foram confeccionadas manualmente utilizando concentrações de lactoferrina diluídas em RPMI 1640. Na primeira coluna de cada placa, foram pipetados 200µL da solução de lactoferrina na concentração de 50mg/mL, em seguida, realizadas 8 microdiluições seriadas de 1:2 em RPMI 1640, por último, adicionou-se 100µL de inóculo em cada poço. Após serem inoculadas, as placas foram incubadas à 35°C, em ar ambiente, por um período de 24 horas e submetidas à leitura visual e espectrofotométrica para determinar as concentrações inibitórias mínimas. Todo o experimento foi realizado em duplicata.					<B>Resultados</B>: Os resultados mostraram um efeito fungicida em concentrações medianas de lactoferrina, e um fenômeno chamado efeito paradoxal em concentrações mais elevadas de lactoferrina.					<B>Conclusões</B>: O efeito paradoxal foi descrito como o crescimento celular em concentrações acima do MIC da lactoferrina, sendo um fenômeno não muito bem explicado em fungos filamentosos, então, estudos futuros devem ser realizados para melhor elucida-los.				</P>
			</div><B>Palavras-chave</B>: Lactoferrina. Fungo filamentoso. Efeito paradoxal			<BR>
