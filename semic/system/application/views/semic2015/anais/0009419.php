			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Lactoferrin is a glycoprotein existing in the innate immunity system. It plays an important role in protecting mucosal tissues against several types of pathogenic microorganisms. Many filamentous fungi found in the environment may be pathogenic and they can cause skin and systemic mycoses which are a serious public health issue.					<B>Objectives</B>: The aim of this study is to verify lactoferrin´s antifungal effect in clinical isolates of filamentous fungi strains such as Aspergilus fumigatus, Trichosporon asahii, Rhizopus oryzae and Malassezia furfur.					<B>Methods</B>: The minimum inhibitory concentrations were obtained by microdilution technique, using RPMI 1640 supplemented with 2% glucose. In the first column there was pipetted 200 µL from the lactoferrin soluton in a 50mg/mL then 8 serial microdilutions  of 1:2 in RPNI 1640 and, by last, adding 100 µL of an innocuous in each poll. After inoculated, the plates were incubated at 35°C, in room air, by a 24 hours period and submitted to visual perusal and spectrophotometric reading to determinate the minimal inhibitory concentrations. Tests were conducted in duplicates.					<B>Results</B>: Results showed an antifungal effect in median concentrations of lactoferrin and a phenomenon called paradoxical effect in lactoferrin´s more elevated concentrations.					<B>Conclusion</B>: The paradoxical effect was described as cellular growth in concentrations above lactoferrin´s MIC, which is not well explained among filamentous fungi. Hence, future studies should be conducted to better understand the mechanisms involved.				</P>
