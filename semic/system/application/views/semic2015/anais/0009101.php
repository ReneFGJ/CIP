			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O tema deste trabalho é a Lei 10.639/03 e as repercussões político-acadêmicas da implementação dela nas universidades.					<B>Objetivo</B>: O objetivo do estudo é analisar as consequências da implementação da lei no âmbito da educação de nível superior.					<B>Metodologia</B>: Recorreu-se a pesquisa bibliográfica, para obtenção dos dados necessários ao andamento da reflexão; os conteúdos disponibilizados consistiam de ensaios, estudos empíricos e entrevistas, dentre outros.					<B>Resultados</B>: A análise evidenciou dados estatísticos que demonstraram um notável índice dos cotistas, reconfigurando o perfil dos discentes na rotina acadêmica. Uma das consequências deu-se no desempenho acadêmico, pois, as notas e médias dos discentes subiram no período estudado. Observou-se que setores sociais refratários às cotas raciais alegam que este mecanismo lhes rouba as vagas a que teriam direito por mérito. Contudo, esta opinião não se sustenta pois os governos do PT ampliaram significativamente as vagas públicas no ensino superior. Além disso, a quantidade de discentes cotistas é baixa em relação às demandas do povo negro e não absorve a totalidade das novas vagas disponibilizadas na rede pública universitária. Portanto, a acusação de que, os (as) discentes cotistas sejam ladrões e ladras de vagas consiste em vergonhosa perversidade racista.					<B>Conclusões</B>: Concluiu-se que os pontos positivos que as cotas produziram no âmbito social foi a ampliação do debate em relação ao racismo brasileiro, uma relevante contribuição no enfrentamento do mito da democracia racial.				</P>
			</div><B>Palavras-chave</B>: Cotas. Educação Superior. Lei 10.639/03. Inclusão Social. Meritocracia.			<BR>
