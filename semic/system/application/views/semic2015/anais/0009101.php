			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The theme of this work is the Law 10.639 / 03 and the political and academic repercussions of implementing it in the universities.					<B>Objectives</B>: The objective of the study is to analyze the consequences of the implementation of the law in higher education.					<B>Methods</B>: Resorted to literature, to obtain the data necessary for the progress of reflection; the available contents consisted of tests, empirical studies and interviews, among others.					<B>Results</B>: The analysis showed statistics that showed a remarkable rate of shareholders, reconfiguring the profile of students in the academic routine. One of the consequences was seen in academic performance because, notes and averages of students rose during the study period. It was observed that refractory social sectors to racial quotas claim that this mechanism robs them of the positions to which they are entitled on merit. However, this view does not hold as governments PT significantly expanded public places in higher education. In addition, the amount of shareholders students is low in relation to the demands of black people and does not absorb all of the new jobs available in the university public. So the accusation that the (as) shareholders students are thieves and robbers spaces consists of shameful racist wickedness.					<B>Conclusion</B>: It was concluded that the positives that quotas produced in the social sphere was the broadening of the debate regarding the Brazilian racism, a relevant contribution to the myth of fighting racial democracy.				</P>
