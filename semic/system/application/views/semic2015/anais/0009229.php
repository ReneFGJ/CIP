			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Introduction: The Bovine Viral Diarrhea (BVD) is a disease of cattle caused by a virus Flaviridae family, the Pestivirus genus and can affect other ruminants. The BVD virus causes biggies economic losses in herd and has worldwide distribution. Animals may be infected with the virus of Bovine Viral Diarrhea transiently or born persistently infected (PI).					<B>Objectives</B>: Considering the limited knowledge of the prevalence of the agent in the region, the objective of this study was to determine the occurrence of anti-virus Bovine Viral Diarrhea antibodies in dairy cattle farms belonging to the city of Toledo, PR, Brazil.					<B>Methods</B>: To determine the prevalence of antibodies, 57 herds were randomly selected among the 24 dairy micro municipality, resulting in a total of 385 samples of blood through the openInfo 6.0 program. Sera were obtained by centrifugation and analyzed by enzyme linked immunosorbent assay technique (ELISA - IDEXX Laboratories®) where samples with suspect results were retested.					<B>Results</B>: Of the samples analyzed, 122 (31.77 %) had antibodies against the virus of bovine viral diarrhea. The farms which proceeded to collect samples, 38 (66.67 %) were positive to the etiologic agent searched.					<B>Conclusion</B>: Conclusion: The results of this study showed that the virus Bovine Viral Diarrhea is widely distributed among dairy cattle in the regio, is highly found in the studied properties, showing that prophylactic measures should be instituted in the studied cattle.				</P>
