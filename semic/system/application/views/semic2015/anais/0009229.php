			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Introdução: A diarreia viral bovina (BVD) é uma enfermidade dos bovinos, causada por um vírus da família Flaviridae, do gênero Pestivirus e pode acometer também outros ruminantes. O vírus da BVD causa grandes perdas econômicas nos rebanhos e tem distribuição mundial. Os animais podem ser infectados com o vírus da diarreia viral bovina transitoriamente ou nascerem persistentemente infectados (PI).					<B>Objetivo</B>: Considerando o conhecimento limitado da prevalência do agente na região, o objetivo do presente estudo foi determinar a ocorrência de anticorpos anti-vírus da Diarreia Viral Bovina em bovinos leiteiros pertencentes a propriedades rurais do município de Toledo, PR, Brasil.					<B>Metodologia</B>: Para determinar a prevalência de anticorpos, foram selecionados aleatoriamente 57 rebanhos dentre as 24 microrregiões leiteiras do município, resultando em um total de 385 amostras de sangue através do programa OpenInfo 6.0. Os soros foram obtidos através da centrifugação e analisados por meio da técnica de ensaio imunoenzimático indireto (ELISA – IDEXX Laboratories®) onde as amostras que apresentaram resultados suspeitos foram retestadas.					<B>Resultados</B>: Das amostras analisadas, 122 (31,77%) apresentaram anticorpos contra o vírus da diarreia viral bovina. Das propriedades onde procedeu a coleta de amostras, 38 (66,67%) foram positivas ao agente etiológico pesquisado.					<B>Conclusões</B>: Os resultados obtidos neste estudo evidenciaram que o vírus da Diarreia Viral Bovina está amplamente distribuído entre os bovinos leiteiros da região, sendo altamente encontrado nas propriedades estudadas, mostrando assim que medidas profiláticas devem ser instituídas nos bovinos estudados.				</P>
