			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O presente estudo propõe-se um tema de grande atualidade, muito evidenciado no pós-Concílio Vaticano II pelos últimos Papas, teólogos e sociólogos: a família. Intitulado como: “Incursões canônico-pastorais sobre o sacramento do matrimonio: Análise dos relatórios de nulidade matrimonial no Tribunal Eclesiástico Interdiocesano de Maringá no período de 12/04/2007 a 31/12/2013”					<B>Objetivo</B>: realizar a analise dos dados quanti-qualitativos dos relatórios enviados à Assinatura Apostólica, Tribunal romano competente para acompanhamento dos tribunais no mundo, a fim de levantar os três principais cânones que justificam as sentenças positivas. Num segundo momento o estudo versa sobre o atual conceito de matrimônio apresentado pelo Código de Direito Canônico de 1983 e apresenta a relação entre matrimônio-família afirmando que a segunda só existe em detrimento da primeira segundo a legislação Canônica.					<B>Metodologia</B>: Método quali-quantitativo das sentenças.					<B>Resultados</B>: 					<B>Conclusões</B>: Apresenta algumas ações pastorais para a evangelização das famílias e oferece como novidade os três elementos que validaram a nulidade matrimonial, como “sinais dos tempos” e ponto de partida para um nova ação evangelizadora.				</P>
			</div><B>Palavras-chave</B>: Matrimônio. Família. Nulidade Matrimonial.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: This research has a very current theme, very evident in the post-Vatican II by recent
Popes, theologians and sociologists: the family. Titled as "canonical and pastoral incursions on the sacrament of marriage: analysis of matrimonial nullity reports the Inter-diocesan Ecclesiastical Tribunal of Maringa in the period 12/04/2007 to 31/12/2013"					<B>Objectives</B>: To carry out the analysis of quanti-qualitatives data of reports sent to the Apostolic Signature, competent Roman Tribunal for monitoring of courts in the world, in order to discover the main three canons that justify the positive sentences. Secondly the study deals with the current concept of marriage presented by the 1983 Code of Canon Law and shows the relationship between marriage-family stating that the second exists only at the expense of the first according to Canon law.					<B>Methods</B>: analysis of quanti-qualitatives sentences					<B>Results</B>: 					<B>Conclusion</B>: it presents some pastoral activities for evangelization of families and offers a novelty the three elements that validated the matrimonial nullity as "signs of the times" and starting point for a new evangelization.				</P>
			</div><B>Keywords</B>: Marriage. Family. Matrimonial nullity.</td>
