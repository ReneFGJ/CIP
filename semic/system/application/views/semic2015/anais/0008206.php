			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: It is possible to observe a number of complications in positioning the concept of pragmatic anthropology (1798) in the context of Kantian system.Where in one moment it is characterized as reformulation or reorientation of empirical psychology, and in other occupying similar position next to a critical science.					<B>Objectives</B>: To be possible to discuss this placement, this paper carried out a brief research on the development of the Kantian theory of knowledge, which sets out the conditions of possibility of all science.					<B>Methods</B>: Then performed an analysis forward to the course that psychology took in its constitution as heuristic science, since its object cannot be given or built on possible experience. After this a description of pragmatic anthropology was carried out by analyzing the convergences and divergences compared to empirical psychology.					<B>Results</B>: Finally, raised the features that link an analogy between the empirical psychology to pragmatic anthropology, resulting in its possibility of constitution as a science.					<B>Conclusion</B>: 				</P>
