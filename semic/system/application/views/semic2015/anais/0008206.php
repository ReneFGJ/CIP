			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: É possível observar uma série de complicações no posicionamento do conceito de antropologia pragmática (1798) no âmbito do sistema kantiano. No qual ora esta é caracterizada como reformulação ou reorientação da psicologia empírica, ora ocupando posição análoga ao lado de uma ciência crítica.					<B>Objetivo</B>: Para que seja possível discutir esta colocação, neste trabalho fora realizada uma breve investigação sobre a elaboração da teoria do conhecimento Kantiana, na qual são estabelecidas as condições de possibilidade de toda ciência.					<B>Metodologia</B>: Em seguida realizado uma análise frente ao curso que a psicologia tomou em sua constituição enquanto ciência heurística, uma vez que o seu objeto não pode ser dado ou construído na experiência possível. Após isto fora executada uma descrição da antropologia pragmática, analisando as convergências e divergências frente à psicologia empírica.					<B>Resultados</B>: Por fim, levantadas as características que apontam uma analogia entre a psicologia empírica à antropologia pragmática, resultando em sua possibilidade de constituição enquanto ciência.					<B>Conclusões</B>: 				</P>
			</div><B>Palavras-chave</B>: Antropologia. psicologia. Kant			<BR>
