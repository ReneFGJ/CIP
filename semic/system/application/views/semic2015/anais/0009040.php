			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The celiac disease, also known as gluten intolerance is caused by the toxic effect of prolamin proteins that are part of the gluten. His treatment is the total restriction of food intake with gluten, reflecting the disappearance of the symptoms manifested.					<B>Objectives</B>: The objective was to develop and optimize formulations for preparation of pizza crust, pre baked, gluten-free.					<B>Methods</B>: From preliminary tests was selected a base composition of 100% of flour (rice flour, corn starch and potato starch), plus 8% sugar, 1% salt, 5% margarine, 4% yeast 0.5% carboxymethylcellulose and 0.5% monoglycerides of fatty acids, which varied in proportion dried egg and water, from 5 to 15% and from 60 to 90%, respectively, using a factorial design. The answers specific volume, water activity, pH, titratable acidity, moisture, sensory analysis of acceptance (taste, appearance and overall acceptability) and microbiological analysis (yeast and mold count) were evaluated statistically, presenting mathematical models.					<B>Results</B>: Response models that showed up significant (p [e]lt;0.05) and coefficient of determination (R2) greater than 0.8 were plotted in response surface graphics, its results were superimposed and the optimal graph region, choice of the proportions of independent variables is selected based on the highest overall sensory acceptance. This region chose two different proportions of dried egg and water and a sensory analysis was performed preferably between them.					<B>Conclusion</B>: The preferred sample by the evaluators was 80.7% with the ratio of 15% dehydrated egg and 84% of water with respect to the base formulation.				</P>
