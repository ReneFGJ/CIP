			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A doença celíaca, também conhecida como intolerância ao glúten é causada pelo efeito tóxico das prolaminas, proteínas que fazem parte do glúten. Seu tratamento é a restrição total do consumo de alimentos com glúten, o que reflete no desaparecimento dos sintomas manifestados.					<B>Objetivo</B>: O objetivo do trabalho foi desenvolver e otimizar formulações para preparo de massa de pizza, pré assada, isenta de glúten.					<B>Metodologia</B>: .  A partir de testes preliminares foi selecionada uma formulação base com 100% de farinhas (farinha de arroz, amido de milho e fécula de batata), acrescidas de 8% de açúcar, 1% de sal, 5% de margarina, 4% de fermento, 0,5% de carboximetilcelulose e 0,5% monoglicerídeos de ácidos graxos, onde foram variados a proporção de ovo desidratado e água, de 5 a 15% e de 60 a 90%, respectivamente, utilizando um delineamento fatorial. As respostas de volume específico, atividade de água, pH, acidez titulável, umidade, análise sensorial de aceitação (sabor, aparência e aceitabilidade geral) e análise microbiológica (contagem de bolores e leveduras), foram avaliadas estatisticamente, apresentando modelos matemáticos.					<B>Resultados</B>: Os modelos de resposta que apresentaram-se significativos (p[e]lt;0,05) e com coeficiente de determinação (R2) superiores a 0,8 foram plotados em gráficos de superfície de resposta, seus resultados foram sobrepostos e a região ótima do gráfico, para escolha das proporções das variáveis independentes, foi selecionada com base na maior aceitação sensorial geral. Dessa região foram escolhidas duas diferentes proporções de ovo desidratado e de água e foi realizada uma análise sensorial de preferência entre as duas.					<B>Conclusões</B>: A amostra preferida por 80,7% dos avaliadores foi com a proporção de 15% de ovo desidratado e 84% de água, com relação à formulação base.				</P>
			</div><B>Palavras-chave</B>: desenvolvimento e otimização, massa de pizza, isento de gluten			<BR>
