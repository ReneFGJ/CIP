			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: This research project analyses current academic discourse on the subject of the condition of homeless individuals. After the Brazilian Psychiatric Reform, the network of mental health care has undergone many transformations to the assistance of people with mental disorders.					<B>Objectives</B>: Therefore, we seek to understand how this issue is being addressed in the scientific community, in view of these changes. From the analyzed publications, it is recognized who this population is in front of its historical and social background, and especially as they are currently treated by the new psychosocial devices that are concerned with the mental health of homeless population.					<B>Methods</B>: The results consisted in understanding the reality in which this population lives, shown through the analysis of academic publications, from 1992 to 2015, even though they are still very scarce in relation to this issue.					<B>Results</B>: Thus, the reflection that we approach involves the position of researchers reporting primarily the reality of public policies, which leads sometimes to include and other times to exclude these citizens from the access to health care. Many health services refuse to treat homeless people, because to treat their healths would be necessary that they leave the streets. Professionals in this area have difficulty seeing these homeless people as individuals.					<B>Conclusion</B>: We conclude, therefore, that even with the new health system devices, the homeless population has a huge difficulty to have access to this network, and consequently it is necessary a larger production of research in this area, so the street population can have greater visibility.				</P>
			</div><B>Keywords</B>: Homeless. Academic discourse. Mental health.</td>
