			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Esta pesquisa trata da análise do discurso acadêmico vigente acerca do morador em situação de rua. Após a Reforma Psiquiátrica Brasileira, a rede de atenção em saúde mental passou por muitas transformações no atendimento de pessoas com transtornos mentais.					<B>Objetivo</B>: Portanto, busca-se compreender como este tema está sendo analisado pela comunidade científica, em vista destas mudanças. A partir das publicações analisadas, se reconhece quem é esta população, diante de sua origem histórica e social e, principalmente, como são atualmente tratados pelos novos dispositivos psicossociais que ocupam-se da saúde mental da população de rua.					<B>Metodologia</B>: Os resultados consistiram em compreender a realidade em que vive a população, mostrada através da metodologia de análise das publicações acadêmicas, de 1992 a 2015, mesmo que ainda muito escassas em relação à este tema.					<B>Resultados</B>: Assim, a reflexão que aqui abordamos envolve a posição dos pesquisadores, que relatam, principalmente, a realidade das políticas públicas, da qual acaba por vezes à incluir e outras por excluir estes cidadãos do acesso à saúde.  Muitos serviços de saúde recusam-se a atender moradores de rua pois, para tratar a saúde dos mesmos, seria preciso que eles deixassem a própria rua. Os profissionais desta área têm dificuldade em olhar para os moradores como pessoas singulares.					<B>Conclusões</B>: Conclui-se, portanto, que, mesmo com os novos dispositivos do sistema de saúde, ainda há grande dificuldade por parte dos moradores de rua de terem acesso à esta rede. Desta forma, faz-se necessária uma maior produção de pesquisas nesta área, para que a população de rua possa ter maior visibilidade.				</P>
			</div><B>Palavras-chave</B>: Morador de rua. Discurso acadêmico. Saúde mental.			<BR>
