			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A literatura afro-brasileira vem há anos buscando um lugar de destaque e reconhecimento na literatura. As mulheres negras batalham por um espaço no qual  possam ter os mesmos direitos que as mulheres de outras etnias, deixando de lado a vida de escravas e donas de casa, para se tornarem autoras de sua própria história.					<B>Objetivo</B>: Assim, nossa pesquisa tem por objetivo principal a análise de textos de autoria feminina da literatura afro-brasileira nos períodos pós-abolicionista e contemporâneo selecionados da obra literária Antropologia de Poesia Afro-Brasileira: 150 anos de consciência negra no Brasil, organizada por Zilá Bernd (2011), a fim de analisarmos a construção da identidade da mulher afrodescendente na literatura em um período relevante da história nacional e para a expressão da multiculturalidade.					<B>Metodologia</B>: Realizamos uma pesquisa bibliográfica a fim de analisarmos os termos identidade, alteridade e multiculturalidade. Estudamos obras relacionadas às temáticas da identidade e da alteridade e à literatura afro-brasileira e, pela leitura atenta dos textos, produzimos resumos e debates, a fim de obtermos uma melhor compreensão dos conceitos mais relevantes para a pesquisa.					<B>Resultados</B>: Vítimas da invisibilidade, a maioria das escritoras negras não são conhecidas. Elas se utilizam da literatura como afirmação de sua identidade, visto que podem se expressar sem medo de serem caladas ou reprimidas.					<B>Conclusões</B>: Buscamos com essa pesquisa, mostrar a importância da obra destas mulheres e sua contribuição para a literatura, bem como divulgar seus trabalhos, que são ainda pouco conhecidos devido aos preconceitos da sociedade.				</P>
			</div><B>Palavras-chave</B>: Mulher afro-brasileira. Literatura. Identidade. Multiculturalidade.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: African-Brazilian writers have been trying to find for centuries a place of recognition in mainstream literature. Black women have struggled for a space of their own, so that they can have the same rights as women from other ethnic groups, leaving aside slavery and the life of housewives, in order to become authors of their history.					<B>Objectives</B>: Therefore, this research aims at analyzing texts of feminine authorship, produced by African-Brazilian women during post-abolitionist and contemporary periods, selected from Antropologia de Poesia Afro-Brasileira: 150 anos de consciência negra no Brasil, organized by Zilá Bernd (2011). The main objective is to investigate the construction of the identity of the African woman in literature at relevant periods of Brazilian national history and for the expression of multiculturalism.					<B>Methods</B>: We have sought support for this research on bibliographical references to analyze the issues of identity, alterity and multiculturalism. We have studied works related to African-Brazilian literature and we have produced debates and reflection based on theoretical readings in order to obtain a better understanding of concepts relevant for this research.					<B>Results</B>: Victims of invisibility, most black women writers are not known. They write and tell stories as a means of asserting their identity, as they might express themselves without fear of being repressed.					<B>Conclusion</B>: Through our research, we sought to show the importance of the works produced by those women writers and their contribution to literature, as well to make their works visible to the academic community, considering the prejudice they still face in society nowadays.				</P>
			</div><B>Keywords</B>: African-Brazilian woman. Literature. Identity. Multiculturalism.</td>
