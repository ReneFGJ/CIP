			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A anemia é um achado comum em indivíduos portadores de Doença Renal Crônica (DRC) e se associa a redução da qualidade de vida, déficit cognitivo, prevalência de doença cardiovascular, risco de hospitalização e mortalidade nessa população.					<B>Objetivo</B>: Avaliar os fatores associados à anemia em indivíduos com DRC em tratamento conservador.					<B>Metodologia</B>: Neste estudo de corte em pacientes prevalentes do ambulatório de Nefrologia Geral do Hospital Nossa Senhora da Luz da PUCPR, entre agosto e novembro de 2014, foram dosados níveis de hemoglobina e hematócrito, Volume Corpuscular Médio (VCM), Hemoglobina Corpuscular Média (HCM), ferro, ferritina, saturação de transferrina, B12, ácido fólico, paratormônio e foi estimada a Taxa de Filtração Glomerular (TFG).					<B>Resultados</B>: Foram avaliados 94 pacientes (Idade: 66,2 ± 12,6 anos; 55,3% homens; 84% brancos; 53,2% diabéticos; 92,6% hipertensos; TFG: 31,8±14,4mL/min). Nefropatia diabética e nefroesclerose hipertensiva foram os diagnósticos etiológicos mais prevalentes de DRC (27,7% e 19,1%, respectivamente). Anemia foi observada em 53,2% dos pacientes. Os níveis de hemoglobina estiveram associados TFG (r=0,487; p[e]lt;0,0001), HCM (r=0,283, p[e]lt;0,006), microalbuminúria (r=-0,321, p=0,008), saturação de transferrina (r=0,288, p=0,043) e PTH (r=-0,407, p=0,001). Análise de regressão multivariada identificou a TFG como único preditor independente dos níveis de hemoglobina ([e]#946;=0,052; IC95%=0,013-0,091; p=0,011).					<B>Conclusões</B>: A TFG foi um preditor independente de anemia, mesmo em fases mais precoces da DRC e fatores carências não pareceram contribuir para a anemia observada na população estudada.				</P>
			</div><B>Palavras-chave</B>: Doença renal crônica. Anemia. Taxa de filtração glomerulae.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Anemia is a common finding in individuals with CKD being associated with reduced quality of life, cognitive impairment, prevalence of cardiovascular disease, risk of hospitalization and mortality in this population					<B>Objectives</B>: In this cohort study, patients were chosen in the general nephrology clinic of the Hospital Nossa Senhora da Luz - PUC-PR, between August and November 2014.					<B>Methods</B>: The data acquired was: levels of hemoglobin and hematocrit, mean corpuscular volume (MCV), hemoglobin mean corpuscular (MCH), iron, ferritin, transferrin saturation, B12, folic acid, parathyroid hormone and was estimated glomerular filtration rate (GFR).					<B>Results</B>: : We studied 94 patients (age: 66,2 ± 12,6 years; 55.3% male, 84% white, 53.2% diabetics, 92.6% were hypertensive; TFG: 31.8 ± 14,4mL / min). Diabetic nephropathy and hypertensive nephrosclerosis were the etiologic diagnosis more prevalent leading to CKD (27.7% and 19.1%, respectively). Anemia was observed in 53.2% of patients. Hemoglobin levels were directly associated with GFR (r=0,487; p[e]lt;0,0001), MHC (r=0,283, p[e]lt;0,006), microalbuminuria (r=-0,321, p=0,008), transferrin saturation (r=0,288, p=0,043) and PTH (r=-0,407, p=0,001). Multivariate regression analysis identified the GFR as the only independent predictor of hemoglobin levels ([e]#946;=0,052; IC95%=0,013-0,091; p=0,011).					<B>Conclusion</B>: GFR was an independent predictor of anemia, even at early stages of CKD, and deficiency of nutricional factors did not seem to contribute to the anemia observed in the study population.				</P>
			</div><B>Keywords</B>: Chronic kidney disease. Anemia. Glomerular filtration rate.</td>
