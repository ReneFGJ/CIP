			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Os antimicrobianos são utilizados há muitos anos na suinocultura intensiva, a fim de melhorar a sanidade dos animais e, consequentemente, a produção. Dentre os antimicrobianos que podem ser utilizados como aditivos melhoradores de desempenho encontra-se a colistina.					<B>Objetivo</B>: O objetivo do presente trabalho foi avaliar o efeito da colistina, utilizada como aditivo melhorador de desempenho, sobre a resposta imune de leitões na fase de creche.					<B>Metodologia</B>: Para isso, foram utilizados 144 leitões recém-desmamados, divididos em seis tratamentos: Grupo Controle (ração sem aditivos), Grupo Tilosina 22ppm (ração com 22ppm de tilosina), Grupo Colistina 20ppm (ração com 20ppm de colistina), Grupo Colistina 40ppm (ração com 40ppm de colistina), Grupo Colistina 50ppm (ração com 50ppm de colistina) e Grupo Colistina 60ppm (ração com 60ppm de colistina). Amostras de sangue foram coletadas de todas as repetições de cada tratamento no momento do alojamento e aos sete, 21, 28 e 35 dias desde o início do experimento, as quais foram processadas pela técnica de citometria de fluxo.					<B>Resultados</B>: Os resultados demonstraram que a colistina modulou o porcentual de linfócitos T auxiliares (CD4+), razão entre os linfócitos T CD4 e CD8 (Razão CD4:CD8), células duplo-marcadas (CD4+CD8+), macrófagos e linfócitos B.					<B>Conclusões</B>: Conclui-se, portanto, que a colistina, utilizada em baixas doses como aditivo melhorador de desempenho, foi capaz de modular a resposta imune de leitões na fase de creche.				</P>
			</div><B>Palavras-chave</B>: Citometria, linfócitos, leitões, tilosina, antibióticos.			<BR>
