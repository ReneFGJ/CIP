			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Antimicrobials are used for a long time in the intensive swine production, with the purpose to increase  animal’s sanity and, consequently, the production. One of the antimicrobials that can be used as feed additive is the colistin.					<B>Objectives</B>: This study aimed to evaluate the effect of colistin, used as feed additive, in the immune response of nursery pigs.					<B>Methods</B>: For this, 144 weaned pigs were used, allotted to 6 treatments: control group (feed without additive), tylosin group 22ppm (feed with 22ppm of tylosin), colistin group 20ppm (feed with 20ppm of colistin), colistin group 40ppm (feed with 40ppm of colistin), colistin group 50ppm (feed with 50ppm of colistin) and colistin group 60ppm (feed with 60ppm of colistin). Blood samples were collected from all repetitions of each treatment at the day of housing, and  7, 21, 28 and 35 days from the begin of the experiment, which was processed by the flow cytometry technic.					<B>Results</B>: Results showed that colistin modulated the percentual of lymphocytes T helper (CD4+), ratio between CD4+ and CD8+ T lymphocytes (Ratio CD4:CD8), double-marked cells (CD4+CD8+), macrophages and B lymphocytes.					<B>Conclusion</B>: This study concluded that colistin, used as feed additive, is capable to modulate the immune response of nursery pigs.				</P>
			</div><B>Keywords</B>: Cytometry, lymphocytes, pigs, tylosin, antimicrobials.</td>
