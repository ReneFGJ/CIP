			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A cárie dentária é uma das doenças que mais afeta a população em nível mundial. Um dos tratamentos para esta doença é o tratamento restaurador atraumático (ART). O ART tem como principal característica viabilizar o controle clínico da doença mesmo fora do ambiente ambulatorial.					<B>Objetivo</B>: A proposta desta pesquisa foi avaliar a longevidade das restaurações em ART em detentas da Penitenciaria Feminina do Paraná após 4 meses da execução da técnica.					<B>Metodologia</B>: Um estudo experimental foi realizado com as detentas da Penitenciária Feminina do Paraná em dois momentos. Restaurações utilizando a técnica de ART foram conduzidas em todas as detentas que apresentassem lesões de cárie de acordo com critérios de inclusão. Por fim, quatro meses após, as restaurações de ART foram avaliadas de acordo com o critério ART.					<B>Resultados</B>: O total da amostra foi de 61 detentas. A média de idade foi de 32 anos (DP=1,22). A média do índice CPO-D foi de 18,69. O total de dentes restaurados foi de 103, sendo que após quatro meses na avaliação observou-se que 66% estavam com restauração presente e satisfatória.					<B>Conclusões</B>: Houve um resultado significante quando observamos a longevidade da técnica. Após quatro meses, a maioria das restaurações estavam adequadas (presentes e satisfatórias).				</P>
			</div><B>Palavras-chave</B>: TRATAMENTO RESTAURADOR ATRAUMÁTICO. CÁRIE. MULHERES CONFINADAS			<BR>
