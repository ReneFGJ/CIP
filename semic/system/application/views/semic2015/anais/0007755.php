			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Dental caries is a disease that affects the population worldwide. One of the treatments for this disease, Atraumatic restorative treatment (ART). The ART´s main characteristic enable the control of the disease even outside the outpatient setting.					<B>Objectives</B>: The purpose of this study was to evaluate the longevity of restorations ART inmates in the Paraná Women´s Penitentiary after 4 months from the technical implementation.					<B>Methods</B>: An experimental study was conducted with the inmates of the Paraná Women´s Penitentiary in two stages. Restorations using the ART technique were conducted on all inmates who presented carious lesions according to inclusion criteria. Finally, four months after the ART restorations were evaluated according to the criteria ART.					<B>Results</B>: The total sample was 61 inmates. The average age was 32 years (SD = 1.22). The average DMFT index was 18.69. The total number of teeth was restored 103, and three months after the evaluation was observed that 66% were present and satisfactory restoration.					<B>Conclusion</B>: There was a significant result when we look at the longevity of the technique. After three months, most of the restorations were appropriate (present and satisfactory).				</P>
