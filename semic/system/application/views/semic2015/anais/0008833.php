			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Uma região metropolitana ao ser instituída tem como finalidade a viabilização, a organização, a execução e o planejamento das funções públicas de interesse
comum a todos os seus membros. Uma vez configurada a regionalização, não se trata mais de um sistema de saneamento básico isolado, mas de um sistema de saneamento básico integralizado. Os serviços públicos de saneamento básico estão diretamente relacionados com a saúde pública. Esta, por sua vez, é parte de um conjunto de direitos sociais, que têm como inspiração o valor da igualdade entre as pessoas. Por isso, o presente propósito acadêmico tem por objetivo responder de modo mediato à seguinte questão: a quem compete o serviço público de saneamento básico em regiões metropolitanas? Por outro lado, o objetivo geral se desdobra em dois objetivos específicos no que se refere ao serviço de saneamento básico. São eles: (i) investigar os referenciais normativos, a doutrina e a jurisprudência do Supremo Tribunal Federal aplicáveis à questão, (ii) avaliar a titularidade dos serviços de saneamento básico em regiões metropolitanas.					<B>Objetivo</B>: O presente propósito acadêmico tem por objetivo responder de modo
mediato à seguinte questão: a quem compete o serviço público de saneamento
