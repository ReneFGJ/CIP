			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O desenvolvimento tecnológico possibilitou o aprimoramento da instrumentação geotécnica ao longo dos últimos anos e viabilizou o uso da tecnologia da fibra óptica para monitoramentos geotécnicos, possibilitando a utilização de cabos de fibra óptica como uma tecnologia avançada de obtenção de dados geotécnicos.					<B>Objetivo</B>: A presente pesquisa focou a instalação, a realização de testes em campo e o monitoramento de uma instrumentação geotécnica baseada na tecnologia de fibra óptica para medição do nível d’água no solo.					<B>Metodologia</B>: A pesquisa apresenta uma análise da comparação dos resultados de monitoramento de nível d’água de uma encosta com a utilização de dois métodos. Os mesmos consistem em medidores de nível d’água convencionais (PIU) e em medidores de nível d’água que utilizam a tecnologia de fibra óptica. A encosta monitorada localiza-se na Serra do Mar, no município de Morretes-PR, onde atravessam dutos de insumos da Petrobras.					<B>Resultados</B>: Os valores de nível d’água obtidos com os dois métodos de monitoramento utilizados na pesquisa encontram-se dentro do erro negligenciável de ±0,5m.					<B>Conclusões</B>: O monitoramento geotécnico utilizando a tecnologia de fibra óptica mostrou-se promissor, com leituras dentro do erro estimado, principalmente em locais onde há ocorrência de riscos associados. O sucesso da pesquisa deve-se também a possibilidade de realizar monitoramentos remotos, aliando ainda monitoramento de grandes áreas e uma maior aquisição de dados, favorecendo ágeis e possíveis tomadas de medidas preventivas e corretivas.				</P>
			</div><B>Palavras-chave</B>: Medidos de nível d´água. fibra óptica. monitoramento geotécnico			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The technological development has enabled the improvement of geotechnical instrumentation over the past few years and enabled the use of the fiber optic technology for geotechnical monitoring, enabling the use of fiber optic cables as an advanced technology to obtain geotechnical data.					<B>Objectives</B>: This study focused on the installation, testing and monitoring in the field of geotechnical instrumentation based on fiber optic technology for measuring the soil water level.					<B>Methods</B>: The study presents an analysis comparing the level of monitoring results of water on a slope by using two methods. They consist the conventional water level meters (PIU) and water level meters that uses fiber optical technology. The slope monitored is located in the Serra do Mar, in the city of Morretes, Parana State, where its crosses a Petrobras pipeline.					<B>Results</B>: The water level values obtained with the two monitoring methods used in the study are negligible within error of ± 0.5m.					<B>Conclusion</B>: The geotechnical monitoring using fiber optic technology has shown promise, with readings within the estimated error, especially in places where there is occurrence of associated risks. Their success is also due to the ability to conduct remote monitoring, even combining monitoring of large areas and increased data acquisition, favoring nimble and possible outlets preventive and corrective measures.				</P>
			</div><B>Keywords</B>: Water level meter. optic fiber. geotechnical monitoring</td>
