			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: ESTUDO DO EFEITO DO CILOSTAZOL EM MODELO
EXPERIMENTAL DE FERIMENTO DESCOLANTE DE
MEMBROS DE RATOS					<B>Objetivo</B>: O fluxo insuficiente de sangue é uma das causas de necrose em
retalhos cutâneos. Cilostazol um inibidor da fosfodiesterase III que aumenta os
níveis intracelulares de AMPc causa vasodilatação. Dessa forma é esperado
que o cilostazol aumente a viabilidade dos retalhos cutâneos produzidos.					<B>Metodologia</B>: Trabalho experimental duplo cego randomizado. Foram utilizados 30
ratos, divididos em grupos de controle com solução fisiológica diariamente e o
grupo estudo com cilostazol por 7 dias com posterior avaliação. Após 7 dias
foram avaliados os retalhos no membro posterior do animal com o
dopplerfluxometro para avaliação da vascularização tecidual, e via o programa
image j a área de necrose do retalho produzido					<B>Resultados</B>: Foi encontrada
uma diferença estatística entre o grupo controle e o grupo cilostazol. A área de
necrose foi menor no grupo com o uso de cilostazol. O aporte sanguíneo foi
aumentado em todas as regiões do retalho cutâneo.					<B>Conclusões</B>: O cilostazol
reduz significativamente a área de necrose de retalhos cutâneos em membros
posteriores de ratos, bem como aumenta o fluxo sanguíneo da região.				</P>
			</div><B>Palavras-chave</B>: Modelo experimental - Ferimentos - Cilostazol			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Insufficient blood flow is one of the causes of necrosis of skin
flaps. Cilostazol, an inhibitor of phosphodiesterase III that increases intracellular
levels of cAMP causes vasodilation. This way, it is expected that cilostazol
increases the viability of skin flaps produced.					<B>Objectives</B>: Evaluate the effect of cilostazol medication in the evolution of
avulsed patchwork, using a degloving experimental model.					<B>Methods</B>: Randomized double-blind experimental work. 30 rats were divided
into Control group with saline solution daily and the Study group with cilostazol
for 7 days with further evaluation. After seven days were evaluated by Doppler
the flaps on the back of the animal limbs for assessing tissue vascularity, and
by the Image J Program to check the necrotic area.					<B>Results</B>: A statistical difference between the control group and the cilostazol
group has been found. The necrotic area was smaller in the group using
cilostazol than in the control group. The blood supply has been increased in all
regions of the skin flap.					<B>Conclusion</B>: The cilostazol significantly reduces the area of necrosis of skin
flaps in hind limbs of rats and increases blood flow in the region.				</P>
