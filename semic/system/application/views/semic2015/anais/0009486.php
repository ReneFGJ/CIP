			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Os gases intestinais prejudicam a qualidade das imagens, em ultrassonografias abdominais.
Especialmente em cães, os protocolos de preparo sugeridos são variados e pouco estudados.					<B>Objetivo</B>: Realizar uma comparação da eficácia entre dois preparos (jejum e jejum somado à dimeticona) para melhora da visualização do exame ultrassonográfico abdominal completo e do pâncreas.					<B>Metodologia</B>: 40 cães foram utilizados neste estudo. Houve a divisão em 2 grupos (dieta 1 – jejum; dieta 2 – jejum somado à dimeticona). As imagens do pâncreas foram obtidas a partir do decúbito lateral esquerdo. Um escore de 1 a 3 (1 – Ruim, 2 – Regular e 3 – Bom) mensurou separadamente a qualidade do exame completo, lobo esquerdo, direito e corpo do pâncreas.					<B>Resultados</B>: Para ambas as dietas, houve resultado acima de 50% de boas imagens do exame completo e do lobo direito do pâncreas. Para ambas as dietas, houve resultado acima de 90% de imagens ruins do lobo esquerdo e corpo do pâncreas.					<B>Conclusões</B>: A boa visualização do exame abdominal completo tanto para animais submetidos à dimeticona como para aqueles sem a medicação refuta a necessidade deste medicamento para o exame abdominal completo.				</P>
			</div><B>Palavras-chave</B>: Simeticona. ultrassonografia. veterinária			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Intestinal gas results in low quality image in abdominal ultrasound in domestic animals. Several preparations protocols, different doses and intervals, was proposed but not adequately studied at this time, especially in dogs.					<B>Objectives</B>: Accomplish an efficacy comparison between 2 preparations (fasting and fasting added to dimethicone) in improving the complete abdominal ultrasound exam and pancreas preview.					<B>Methods</B>: 40 dogs were enrolled in this study. They were separated in 2 groups (diet 1 – fasting; diet 2 – fasting added to dimethicone). The pancreas images were taken from left lateral decubitus. A score of 1 to 3 (1 – Bad, 2 – Fair and 3 – Good) measured separately the quality of the complete exam, left lobe, right and body of the pancreas.					<B>Results</B>: In both diets, there were results above 50% of good images of the complete exam and pancreas’s right lobe. In both diets, there were results above 90% of bad images of the right lobe and body of the pancreas.					<B>Conclusion</B>: The good abdominal exam preview as well submitted animals to the dimethicone as those without the medication refute this medicine requirement to the abdominal ultrasound exam.				</P>
