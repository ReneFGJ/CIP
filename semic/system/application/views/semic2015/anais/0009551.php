			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O milho (Zea mays L) é uma cultura de grande importância no cenário econômico mundial, requerendo nutrientes na quantidade adequada para ter bom rendimento. O nitrogênio é o nutriente mais exigido pela cultura, influenciando diretamente na produtividade, porém existem questionamentos quanto a época adequada de aplicação.					<B>Objetivo</B>: O trabalho teve como objetivo avaliar diferentes épocas de incorporação de nitrogênio na forma de sulfato de amônio em pré semeadura na cultura do milho.					<B>Metodologia</B>: O trabalho foi conduzido na fazenda experimental da Pontifícia Universidade Católica do Paraná, Campus Toledo, PR. O delineamento experimental utilizado foi o de blocos ao acaso, com 9 tratamentos (42;35;28;21;14 e 7 dias antes da semeadura e dose total na semeadura, e em cobertura nos estádios fenológicos V4 e V8), com quatro repetições. A dose utilizada foi de 150 Kg.ha-1 de N tanto quando incorporado em pré semeadura, quanto em cobertura , utilizando sulfato de amônio.					<B>Resultados</B>: Para as variáveis analisadas não foram constatadas diferenças significativas na análise de variância, nos componentes de rendimento do milho . A incorporação de N em pré semeadura não difere estatisticamente da aplicação de N em cobertura, promovendo rendimento similares nas condições avaliadas.					<B>Conclusões</B>: Não foi possível detectar diferença entre os periodos, existindo necessidade de aprofundamento nos fatores que influenciam a eficiência e eficácia da adubação nitrogenada quando aplicada em pré-semeadura.				</P>
			</div><B>Palavras-chave</B>: antecipação de adubação, Zea mays L, adubação de nitrogênio.			<BR>
