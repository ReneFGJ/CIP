			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Corn (Zea mays L.) is a crop of great importance in the global economy, requiring nutrients in adequate amount to have good income. Nitrogen is the nutrient most required by culture, influencing directly on the productivity, but there are questions as to the appropriate time of application.					<B>Objectives</B>: The study aimed to evaluate different nitrogen incorporation times in the form of ammonium sulfate in pre seeding in corn.					<B>Methods</B>: The work was conducted at the experimental farm of the Pontifical Catholic University of Paraná, Campus Toledo, PR. The experimental design was randomized blocks, with nine treatments (42; 35; 28; 21, 14 and 7 days before sowing and total dose at sowing, and coverage growth stages V4 and V8) with four replications . The dose used was 150 Kg.ha N-1 when incorporated into both pre seeding, as for coverage using ammonium sulfate.					<B>Results</B>: or variables no significant differences were observed in the analysis of variance, the components of corn yield. The incorporation of N in pre seeding does not differ statistically from the application of nitrogen in promoting similar performance in the evaluated conditions.					<B>Conclusion</B>: Could not detect difference between the periods, there need to deepen the factors that influence the efficiency and effectiveness of nitrogen fertilization when applied in pre-sowing.				</P>
			</div><B>Keywords</B>: anticipation of fertilization, Zea mays L. nitrogen fertilization.</td>
