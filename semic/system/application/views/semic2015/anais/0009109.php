			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A relevância de se compreender a trajetória de construção de políticas públicas para o desenvolvimento turístico de acordo com os preceitos de sustentabilidade ambiental, social, cultural e econômica se dá pela necessidade de planejar suas atividades as quais devem estar respaldadas politicamente. Diante destes aspectos, a pesquisa pautou-se em autores que discutem o turismo e a política como atividade que pode contribuir como estratégia de sustentabilidade em especial ao compor comitês de planejamento participativo com representação civil e na construção de políticas públicas que contemplem a diversificação econômica mas que promovam a conservação do meio ambiente bem como a valorização social e cultural das comunidades envolvidas.					<B>Objetivo</B>: Como objetivo geral teve-se a proposta de analisar estratégias de planejamento participativo considerando as políticas públicas brasileiras para o turismo  sob a perspectiva histórico-institucional. Para tanto, estabeleceu-se como objetivo específico: diagnosticar pontos fortes e fracos, oportunidades e ameaças do Programa Nacional de Regionalização do Turismo previsto para atender as 05 regiões brasileiras.					<B>Metodologia</B>: Por isso, questiona-se a existência de uma política do turismo efetiva a partir da análise diagnóstica qualitativa do Programa de Regionalização do Turismo no Brasil no intuito de identificar possíveis consequências positivas ou negativas.					<B>Resultados</B>: 					<B>Conclusões</B>: 				</P>
