			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The ability to walk is one of the most striking features among patients who have suffered a stroke. Relearn or regain, even partially, this capability becomes target established for these patients in order to execute their daily activities independently.					<B>Objectives</B>: Evaluate the effect of a physical therapy protocol with body weight support on quality of life of post-stroke patients, through the application of SF36 questionnaire.					<B>Methods</B>: Participants were divided into two groups: control group (CG) and study group (EG). The GC only received conventional physical therapy offered by Ana Carolina Moura Xavier Rehabilitation Hospital in the city of Curitiba-PR, where the study was conducted, and the GE received in addition to conventional therapy, 20 additional minutes of locomotor training with partial support weight (TLSP) for 12 weeks.The evaluations of the quality of life of participants were held at the beginning of treatment (AV1), in the seventh week (AV2) and another at the end, in the twelfth week (AV3).					<B>Results</B>: The average scores of control group were 53.3, 58.0 and 61.4 on AV1, AV2 and AV3, respectivelly. These average scores of estudy group were 54.7, 58.0 and 62.6.					<B>Conclusion</B>: Quality live improved in both groups in a similar fashion. Therefore, as far as quality life is concern, locomotor training is no superior than conventional therapy.				</P>
