			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A capacidade de deambular é uma das características mais marcantes entre os pacientes que sofreram um acidente vascular encefálico – AVE. Reaprender ou recuperar, ainda que parcialmente, tal capacidade, torna-se meta fixa para estes pacientes, no sentido de reunir funções que o deixem novamente com possibilidades de retomar suas atividades cotidianas com certa autonomia.					<B>Objetivo</B>: Avaliar o efeito de um protocolo fisioterapêutico com suporte de peso corporal sobre a qualidade de vida de pacientes pós- AVE, por meio da aplicação do questionário SF36.					<B>Metodologia</B>: Os participantes foram alocados em dois grupos: grupo controle (GC) e grupo estudo (GE). O GC recebeu apenas a fisioterapia convencional ofertada pelo Centro Hospitalar de Reabilitação Ana Carolina Moura Xavier na cidade de Curitiba, PR, onde o estudo foi realizado, e o GE recebeu, além da fisioterapia convencional, 20 minutos adicionais de treino locomotor com suporte parcial de peso (TLSP) durante 12 semanas. As avaliações da qualidade de vida dos participantes foram realizadas no início do tratamento (AV1), na sétima semana (AV2) e outra ao término, na décima segunda semana (AV3)					<B>Resultados</B>: As pontuações médias do questionário, no GC, foram de 53,3, 58,0 e 61,4, nas avaliações AV1, Av2 e AV3, respectivamente. No GE, as mesmas pontuações foram de 54,7, 58,0 e 62,6.					<B>Conclusões</B>: Os pacientes submetidos ao tratamento convencional oferecido pelo hospital e os pacientes que receberam o complemento proposto pelo projeto, obtiveram avanços semelhantes em relação à qualidade de vida, avaliada com o questionário SF-36.				</P>
			</div><B>Palavras-chave</B>: AVC. marcha. fisioterapia. qualidade de vida			<BR>
