			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: This study aims to analyse the way population of the neighborhood Cidade Industrial de Curitiba (CIC) react by facing the themes shown in the journalistic program Brasil Urgente. The program has two versions displayed in Curitiba television, the first one about city informations and the second one about national news. Both of them are broadcast on TV for all states of Brazil from the capital São Paulo.					<B>Objectives</B>: This study shows how the inhabitants of the Industrial City of Curitiba neighborhood - one chosen to record a high rate of murders, according to data released by the Department of Public Security of the State of Paraná (SESP, 2014) - react with respect to the content of the news Brazil Urgent news program, in its two versions circulated in Curitiba.As a specific goal this study will show whether this portion of the hearing condone the same views that appear in the program.					<B>Methods</B>: The qualitative research was developed  following  the instruments: analysis of Brazil Urgent program and focus group.
Three editions of each program were recorded, analyzed and assisted. The analysis of the goals were two: to understand how the program dialogued with the viewer and find that journalistic formats formed its contents. The focus group was conducted with residents of the CIC neighborhood in Curitiba. The participants showed a great rejection of some participants in the program, which was not expected by this researcher					<B>Results</B>: This analysis reveals the journalistic formats found in this TV program, the way communication is done with its public and how part of this public reacts to the issues broadcast in the program. The Brazil Urgent TV show and the inhabitants of the CIC appear to have different views on the subject they have in common: urban violence.					<B>Conclusion</B>: 				</P>
