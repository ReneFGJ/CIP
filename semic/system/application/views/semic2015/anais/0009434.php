			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: Este trabalho analisa o modo com que os moradores do bairro Cidade Industrial de Curitiba (CIC) reagem em relação aos temas que formam o conteúdo do programa jornalístico Brasil Urgente, que possui duas versões exibidas em Curitiba, uma com informações da cidade e outra veiculada para todo o país, transmitida a partir da capital paulista.					<B>Objetivo</B>: O objetivo é  mostrar como os moradores do bairro Cidade Industrial de Curitiba – Esse escolhido por registrar um elevado índice de homicídios dolosos, segundo dados divulgados pela Secretaria de Segurança Pública do Estado do Paraná (SESP,2014) – reagem com relação ao conteúdo noticioso do programa jornalístico Brasil Urgente, em suas duas versões veiculadas em Curitiba.Como objetivo específico este estudo mostrará se essa parcela da audiência compactua das mesmas opiniões que são exibidas no programa.					<B>Metodologia</B>: A presente pesquisa de abordagem qualitativa foi desenvolvida pelos seguintes instrumentos: análise do programa Brasil Urgente e grupo focal.
Três edições de cada programa foram gravadas, assistidas e analisadas. Os objetivos da análise eram dois: entender como o programa dialogava com o telespectador e descobrir que formatos jornalísticos formavam seu conteúdo. 
O grupo focal foi realizado com moradores do bairro CIC em Curitiba. Os participantes revelaram uma grande rejeição de alguns participantes pelo programa, algo que não era esperado por estes pesquisadores.					<B>Resultados</B>: Tal análise revela os formatos jornalísticos encontrados no programa, o modo com que dialoga com a audiência e como uma parcela dessa audiência reage aos temas do programa. O programa de TV Brasil Urgente e os moradores da CIC parecem ter visões diferentes sobre o tema que possuem em comum: a violência urbana.					<B>Conclusões</B>: 				</P>
			</div><B>Palavras-chave</B>: Jornalismo. sensacionalismo. recepção.			<BR>
