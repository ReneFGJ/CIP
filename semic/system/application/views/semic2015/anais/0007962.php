			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: A prevalência de obesidade infantil cresceu muito nas últimas décadas. Consequentemente, patologias associadas à obesidade, como diabetes e hipertensão (HAS), apresentam aumento de incidência em faixas etárias cada vez mais jovens. Apesar de estar demonstrada a associação entre a obesidade avaliada pelo índice de massa corpórea (IMC) e elevação da pressão arterial (PA), não está claro se outros índices antropométricos também apresentam esta associação em crianças, em especial quando o IMC é normal.					<B>Objetivo</B>: Avaliar a associação entre medida de cintura e elevação da PA em crianças com IMC normal.					<B>Metodologia</B>: Análise transversal de 5037 escolares entre 6 e 11 anos, de ambos os sexos, frequentadoras de escolas públicas e privadas. Crianças com IMC abaixo ou acima dos valores da normalidade foram excluídas. A medida de cintura foi categorizada por quartil para cada faixa de idade. A significância estatística foi estabelecida em 5%.					<B>Resultados</B>: Foram incluídas 3.417 crianças com IMC normal. A prevalência de pressão arterial elevada foi de 10,7%. Nas crianças com medida de cintura no quartil mais baixo para a idade, a prevalência de PA elevada foi de 8,1%. Essa prevalência aumentou nos quartis mais altos, sendo 10,6% no segundo, 12,4% no terceiro e 12,1% no quartil mais alto (p= 0,01). Mesmo na vigência de IMC normal, neste grupo, estar no quartil mais alto para cintura esteve associado a chance 57% maior de apresentar elevação da PA em comparação com as crianças com medida de cintura no quartil mais baixo. (Q4 vs Q1; OR 1,57 – IC 95% 1,14 – 2,17).					<B>Conclusões</B>: Em crianças, o aumento da medida de cintura está associada à elevação da PA mesmo quando IMC é normal.				</P>
			</div><B>Palavras-chave</B>: Obesidade. hipertensão			<BR>
