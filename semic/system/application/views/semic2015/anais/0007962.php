			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The prevalence of childhood obesity increased significantly in the last decades. Consequently, diseases associated with obesity such as diabetes and hypertension have an increased incidence in age becoming younger. Despite being shown the association between obesity measured by body mass index (BMI) and elevated blood pressure (BP), it is unclear whether other anthropometric indices also show this association in children, especially when the BMI is normal.					<B>Objectives</B>: To evaluate the association between waist circumference and elevated BP in children with normal BMI.					<B>Methods</B>: Cross-sectional analysis of 5037 children between 6 and 11 years old, of both sexes, who attend public and private schools. Children with BMI above or below the normal values were excluded. The waist measurement was categorized by quartile for each group of age. Statistical significance was set at 5 %.					<B>Results</B>: The study included 3.417 children with normal BMI. The prevalence of high blood pressure was 10.7%. In children with waist measurement in the lowest quartile for age, the prevalence of high BP was 8.1%. This prevalence increased in the highest quartiles, being 10.6% in the second, 12.4% in the third, and 12.1% in the highest quartile (p = 0.01). Even in the presence of normal BMI, in this group, being in the highest quartile for waist was associated with 57 % greater chance of presenting BP elevation compared to children with waist measurement in the lowest quartile. (Q4 vs Q1, OR 1.57 - 95 % 1,14 – 2,17)					<B>Conclusion</B>: In children, increased waist circumference is associated with elevated BP even when BMI is normal				</P>
