			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O desenvolvimento de tecnologias que utilizem fontes renováveis de energia torna-se atrativas tanto ambientalmente como socialmente, pois possibilitam a criação de fontes de suprimento descentralizadas e em pequena escala, e isto, torna-se fundamental para o desenvolvimento sustentável para países desenvolvidos e para os que estão em desenvolvimento.					<B>Objetivo</B>: O objetivo deste trabalho é o estudo da biodigestão anaeróbica utilizando diferentes materiais orgânicos para a produção de biogás, como esterco de suínos, bovinos, e aves, que será realizado nos laboratórios da PUCPR, Campus Toledo. Através deste estudo, pretende-se determinar qual a melhor matéria-prima para a biodigestão anaeróbica.					<B>Metodologia</B>: O trabalho encontrou diversas dificuldades, como a demora da coleta de matéria orgânica dos biodigestores, devido a operação de limpeza dos mesmos na propriedade rural, e a adequação da escala do biodigestor para que produzisse quantidades significativas de biogás. Desta forma, biodigestores formados por galões de 20 litros foram a melhor solução para a produção de biogás.					<B>Resultados</B>: Devido a todas as dificuldades encontradas, só foi possível a realização de uma bateira de experimentos, sendo realizada em duplicata para os efluentes de aves, bovinos e suínos. Os resultados mostraram que o efluente que mais produziu biogás foi o de suíno, seguido dos bovinos e das aves.					<B>Conclusões</B>: Para que uma melhor conclusão seja feita, será necessário a quantinuidade do projeto, acrescentando outros fatores importantes no experimento, como a análise laboratorial dos efluentes, controle de pH e realização de várias baterias de experimentação.				</P>
			</div><B>Palavras-chave</B>: Digestão anaeróbica. Biogás. Material orgânico.			<BR>
			<BR>
			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: The development of technologies using renewable sources of energy becomes attractive both environmentally and socially as they allow for the creation of decentralized sources of supply and small scale, and this is fundamental for sustainable development for developed countries and for the that they are in development.					<B>Objectives</B>: The objective of this work is the study of anaerobic digestion using different organic materials for biogas production, as swine manure , cattle, and poultry , which will be held in the PUCPR laboratories , Campus Toledo. Through this study , we intend to determine the best feedstock for anaerobic digestion.					<B>Methods</B>: The study found several difficulties , such as the delay in the collection of organic matter digesters , because the cleaning operation on the same farm , and the adequacy of the scale digester to produce significant amounts of biogas. Thus , digesters formed by gallons of 20 liters were the best solution for the production of biogas.					<B>Results</B>: Because of all the difficulties , it was only possible to hold a Bateira experiments being carried out in duplicate for effluents from poultry, cattle and pigs. The results showed that the produced biogas effluent was more pig , followed by cattle and poultry.					<B>Conclusion</B>: For a better conclusion is made , you need to quantinuidade project , adding other important factors in the experiment , such as laboratory analysis of discharge , pH control and carrying out various experiments batteries.				</P>
