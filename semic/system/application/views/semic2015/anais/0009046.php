			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O fibrinogênio é uma proteína de fase aguda sintetizada pelo fígado, cuja concentração plasmática eleva-se sob a ação estimuladora das interleucinas (IL-1 e 6) e do fator de necrose tecidual liberado pelo processo inflamatório. Durante o processo de inflamação aguda, a concentração plasmática desta substância aumenta por vários dias, atingindo um pico entre o quinto e sétimo dia. A resposta da fase aguda refere-se a uma reação complexa e não específica do animal que ocorre após qualquer lesão tissular.					<B>Objetivo</B>: O projeto teve como objetivo dosar o fibrinogênio no plasma sanguíneo de cães machos e fêmeas submetidos a cirurgia eletiva de orquiectomia e ovariohisterectomia.					<B>Metodologia</B>: Foi realizada a coleta do sangue em tubos com EDTA, em 5 diferentes momentos, M0 no pré operatório, M1 no pós operatório imediato, M2 24 horas após a cirurgia, M3 e M4 cinco e sete dias após a cirurgia.					<B>Resultados</B>: Os resultados encontrados em média foram: Pré operatório 2,85 (±1,10a), pós operatório imediato 3,15 (±1,42a), 24 horas após a cirurgia 4,00 (±1,43b), 5º dia após a cirurgia 3,55 (±1,40ab) e no 7º dia após a cirurgia 4,05 (±2,85b. Estas alterações foram estatisticamente significativas.					<B>Conclusões</B>: Conclui-se que as cirurgias, mesmo eletivas, alteram o nível plasmático de fibrinogênio e isto pode estar relacionado ao trauma cirúrgico.				</P>
			</div><B>Palavras-chave</B>: Fibrinogenio. cirurgia eletiva. cão.			<BR>
