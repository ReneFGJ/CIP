			</td><td width="2%">&nbsp;</td><td width="44%"> Abstract
			<div style="text-align:justify;">
				<P>
					<B>Introduction</B>: Fibrinogen is an acute phase protein synthesized by the liver, whose plasma concentration rises under the stimulating action of interleukins (IL-1 and 6) and tissue necrosis factor released by the inflammatory process. During the process of acute inflammation, the plasma concentration of this substance increases over several days, peaking between the fifth and seventh day. The acute phase response refers to a complex reaction and not specific to any animal that occurs after tissue injury.					<B>Objectives</B>: The project was designed to determine the fibrinogen in the blood plasma of male and female dogs undergoing elective surgery orchiectomy and ovariohysterectomy.					<B>Methods</B>: Blood colect was performed in tubes with EDTA in 5 different times , M0 preoperatively , immediately after surgery M1 , M2 24 hours after surgery, M3 and M4 five seven days after surgery.					<B>Results</B>: The results on average were: Pre operative 2.85 ( ± 1,10a ) , immediate postoperative 3.15 ( ± 1,42a ), 24 hours after surgery 4.00 ( ± 1,43b ) , 5th day after surgery 3.55 ( ± 1,40ab ) and on the 7th day after surgery 4.05 ( ± 2,85b . These changes were statistically significant.					<B>Conclusion</B>: Conclude that surgery , by choice , change the plasma levels of fibrinogen and this may be related to surgical trauma.				</P>
