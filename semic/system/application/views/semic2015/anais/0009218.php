			<td width="44%"> Resumo
			<div style="text-align:justify;">
				<P>
					<B>Introdução</B>: O progresso tecnológico dos meios e canais de comunicação, plataformas de acesso e propagação da informação orientou o mundo a uma renovada experiência relacional com a publicidade e as diferentes manifestações artísticas e culturais. Essa direção comum de contato remete a uma sociedade convivendo, participando e atuando a partir de uma convergência de interelações midiáticas, culturais e de consumo de informação. Cada passo tendendo à ampliação do acesso e participação nas novas tecnologias midiáticas, desencadeia-se significativas transformações nos cenários culturais, artísticos e publicitário (JENKINS, 2009).					<B>Objetivo</B>: A proposta desta pesquisa é a procura pela capacidade de percepção de conhecimento do público, observando os critérios de cobertura, frequência, continuidade, impacto e interatividade em relação aos bens culturais, artísticos e publicitários de consumo.					<B>Metodologia</B>: A base científica é estruturada no estudo teórico dos temas envolvidos: mídias sociais, ciberespaço, relevância cultural e artística, publicidade, interatividade, cultura da convergência e a contemporaneidade tecnológica. Os estudos foram organizados por meio de pesquisa bibliográfica, investigação empírica e metodologias qualitativa e quantitativa.					<B>Resultados</B>: Completou a pesquisa, a definição de indicadores de avaliação e seleção de case e sua posterior análise e ajuizamento.					<B>Conclusões</B>: As avaliações e análises presentes neste trabalho possibilitaram entender, ainda de que forma pouco aprofundada – devido às limitações de tempo e acesso ao planejamento de comunicação das empresas e campanhas analisadas – as relações humanas e suas diversas manifestações com o universo midiático e informacional
contemporâneo. Estas, abalizadas em estratégias de comunicação, estão impressas em atitudes coletivas, sentimento de pertencimento e criação conjunta.				</P>
