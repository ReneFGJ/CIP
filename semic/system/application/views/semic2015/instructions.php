<h1>NORMAS GERAIS PARA PARTICIPAÇÃO NO XXII SEMIC</h1>
<div align="left">
	<p align="justify">
		Para localizar o código do trabalho, o aluno deverá acessar o site do XXII SEMIC (www.pucpr.br/semic) entrar no link em Sumário Geral, apertar CTRL + F, abrirá uma caixa de "localizar", deverá ser digitado o sobrenome do autor em maiúsculo e apertar Enter, automaticamente será remitido ao projeto. O aluno deverá marcar este código, pois ele será o identificador do seu trabalho.
	</p>
	<p align="justify">
		Para as apresentações em inglês os códigos são identificados com letras "i" na frente<br /> 
		Para as apresentações dos trabalhos PIBITI todos são identificados com a letra "T" ao final do código. 
	</p>
	
	<p align="justify">
		<p>Exemplo:<br/>
		<ul>
			<li>
			   <strong>INTERNACIONAL:</strong><ul><li><b>iEPR07</b> (trabalho de engenharia de produção, em inglês, número 07)</li></ul>
			</li>
			<br />
			<li>
				 <strong>PIBIC:</strong> <ul><li><b>CCOMP44</b> (trabalho PIBIC de ciência da computação, número 44)</li></ul>
			</li>
			<br />
			<li>
				 <strong>PIBITI: </strong><ul><li><b>EPR08T</b> (trabalho PIBITI de engenharia de produção, número 08)</li></ul>
			</li>
			<br />
			<li>
				 <strong>PIBIC Jr:</strong> <ul><li><b>medvet01Jr</b> (trabalho PIBIC JUNIOR de medicina veterinária, número 01)</li></ul>
			</li>
			<br />
			<li>
				 <strong>MOBILIDADE NACIONAL:</strong> <ul><li><b>MOB02</b> (trabalho PIBIC ou PIBITI Mobilidade Nacional, número 02)</li></ul>
			</li>			
		</ul>
	</p>
	<br />
	<p align="justify">
	Depois de localizado o código do trabalho, na aba Programação, o aluno encontrará uma tabela com todas as informações da programação do evento, incluindo a sala de sua apresentação oral. O aluno deverá localizar o seu código na tabela, usando CTRL + F e digitando o código localizado em sumário geral. 
	</p>
			
	<p align="justify">
	O aluno deverá participar de todo o bloco em que seu trabalho está incluído, por exemplo: das 8h00 às 09h15. Não será permitida a entrada do aluno na sala após o início das apresentações ou apenas para a sua apresentação. O aluno deverá utilizar uma apresentação de Power Point (modelo encontrado no site do PIBIC) como material de apoio.
	</p>
	
	<p align="justify">
	Para sua segurança envie uma cópia para o seu e-mail. Assim evitará problemas de arquivo corrompido na hora da apresentação. Não é necessário entregar para a Coordenação antecipadamente.
	</p>
			
	<p align="justify">
	Os alunos que optaram pela apresentação oral internacional, deverão fazer os slides em inglês, seguindo as mesmas normas acima. 
	</p>
			
	<p align="justify">
	A apresentação oral deverá ter no máximo 15 minutos, mais 05 minutos de perguntas e respostas para uma banca de dois avaliadores. 
	</p>
					
	<p align="justify">
	A equipe de apoio do evento está terminantemente proibida de auxiliar o aluno na troca de slides no momento da apresentação e na instalação da apresentação no computador da sala de apresentação. Assim, o aluno deverá pedir a um colega que o auxilie.
	</p>
			
	<p align="justify">
	Para fazer a transferência de arquivo para o computador da sala, é necessária a chegada antecipada (intervalo de apresentações), uma vez que a partir da hora que o avaliador chamar o aluno para apresentar, o tempo já estará contando. 
	</p>
					
