	<br />
	<p align="justify">
		O aluno deverá participar de todo o bloco em que seu trabalho está incluído, por exemplo: das 8h00 às 09h15. Não será permitida a entrada do aluno na sala após o início das apresentações ou apenas para a sua apresentação. O aluno deverá utilizar uma apresentação de Power Point (modelo encontrado no site do <a href="http://www.pucpr.br/pesquisacientifica/iniciacaocientifica/modelosdedocumentos.php" target="_new">PIBIC</a>) como material de apoio.
	</p>
	<p align="justify">
		Para sua segurança envie uma cópia para o seu e-mail. Assim evitará problemas de arquivo corrompido na hora da apresentação. Não é necessário entregar para a Coordenação antecipadamente.
	</p>
	<p align="justify">
		Os alunos que optaram pela apresentação oral internacional, deverão fazer os slides em inglês, seguindo as mesmas normas acima.
	</p>
	<p align="justify">
		A apresentação oral deverá ter no <font color = "#8b1217">máximo 10 minutos</font>, mais <font color = "#8b1217">05 minutos de perguntas e respostas</font> para uma banca de dois avaliadores.
	</p>
	<p align="justify">
		A equipe de apoio do evento está terminantemente proibida de auxiliar o aluno na troca de slides no momento da apresentação e na instalação da apresentação no computador da sala de apresentação. Assim, o aluno deverá pedir a um colega que o auxilie.
	</p>
	<p align="justify">
		Para fazer a transferência de arquivo para o computador da sala, é necessária a chegada antecipada (no intervalo de apresentações), uma vez que a partir da hora que o avaliador chamar o aluno para apresentar, o tempo já estará contando.
	</p>
	<br />
	<p>
		<strong>PRESENÇA</strong>
	</p>
	<p align="justify">
		É obrigatória a presença do orientador durante a apresentação oral do aluno. Caso ele esteja impossibilitado de comparecer, é necessário que envie um representante com um ofício de justificativa da ausência. Este ofício deverá ser entregue para o avaliador que estiver presidindo a mesa, bem como enviado uma cópia para a Coordenação da IC.
	</p>
	<br />
	<p>
		<strong>PARTICIPAÇÃO</strong>
	</p>
	<p align="justify">
		O aluno que quiser participar como ouvinte deverá assistir obrigatoriamente pelo menos 50% das apresentações oral, palestras, minicurso e/ou mesa redonda.
		*** Sessões de pôster NÃO contabilizam como frequência para completar os 50% de participação no evento.
	<p align="justify">
		Alunos que apresentarão seus trabalhos no evento receberão a declaração de conclusão e participação no programa PIBIC/PIBITI.
	</p>
	</p>
	<br />
	<p>
		<strong>DECLARAÇÃO</strong>
	</p>
	<p align="justify">
		O certificado será disponibilizado no site do evento a partir do dia 25/10/2015, e poderá ser obtido no painel do usuário. Somente usuários com credenciamento efetivo terão direito ao certificado de participação, portanto todos os alunos deverão obrigatoriamente efetuar o credenciamento a partir do dia 05/10/2015 conforme instruções.
	</p>
