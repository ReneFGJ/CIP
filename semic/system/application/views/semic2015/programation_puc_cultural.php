		<p><strong>TRANSCENDENDO NA ESTRELA - CONSTELAÇÕES SISTÊMICAS</strong></p>
		<p>Vivência grupal conduzida pelas consteladoras Marusa Helenna da Graça e Vera Boeing na Casa Estrela, o local mágico no marco zero do campus da PUCPR.</p>
		<p>Com o tema “Um Lugar para os Excluídos – Somos Todos Um”, a Vivência é baseada nos conceitos e na técnica de Bert Hellinger, o criador das Constelações Familiares. Marusa Gonçalves e Vera Boeing são nomes locais de referência no Brasil em Constelações Sistêmicas.</p>
		
		<p>A Casa Estrela, patrimônio da Arquitetura local, restaurada e reedificada pela PUCPR, única no mundo por sua peculiaridade na intenção da forma, foi construída manualmente em madeira por Augusto Gonçalves de Castro (perito contador) e sua esposa Dionízia Azulay no início da década de 30. A família Castro era Esperantista, Teosofista e adepta da Fraternidade Universal.</p>
		
