<h1>PROGRAMAÇÃO CIENTÍFICA</h1>
<div align="left">
	<p>
		<h2><strong>Palestra</strong></h2>
	</p>
	<p>
		<strong>Título:</strong> "Um mundo Mais Humano por meio da pesquisa"
		<br>
		<strong>Conferencista:</strong> Profa. Dra. Jucimeri Isolda Silveira - PUCPR
	</p>
	<ul>
		<li>
			06/10 - terça-feira - 10h
		</li>
		<li>
			09/10 - sexta-feira - 10h
		</li>
	</ul>
	<BR>
	<p>
		<strong>Resumo</strong>
	</p>
	<p align="justify">
		Esta comunicação parte da reflexão crítica sobre os desafios da pesquisa e difusão de uma cultura
		em direitos humanos, num contexto de globalização com efeitos na manuteção e no
		aprofundamento da desigualdade social, e das práticas de intolerância e de violência, com
		diversas violações de direitos humanos. A Pontifícia Universidade Católica do Paraná vem
		estruturando a área estratégica em direitos humanos, processo indutor, ativador e articulador
		da pesquisa e de um conjunto de atividades, que explicitem o protagonimo de docentes e estudantes,
		e a relevância pública da universidade. Para tanto é preciso romper a departamentalização e a
		fragmentação, atuar de modo interdisciplinar na produção de pesquisas, com incidência no ensino
		e na extensão, em sintonia com os valores do densenvolvimento sustentável e dos direitos humanos,
		em benefício da sociedade. Processo que requer ambientes dialógicos, integradores, para a
		construção de novos saberes, novas práxis, alternativas que favoreçam a convivência pacífica, ética,
		responsável, solidária e igualitária, visando à distribuição equânime de bens, riqueza e poder, a
		valorização da diversidade e o reconhecimento das diferenças. As práxis transformadoras exigem
		reflexão/ação, a mediação crítica entre teoria, prática e ética, tendo como base os valores
		ético-políticos, alimentados por conhecimentos relevantes, explicativos e transformadores,
		na direção de um mundo efetivamente mais humano.
	</p>
	<p>
		Palavas-chave: direitos humanos, pesquisa, práxis.
	</p>
	<br />
	<p>
		<strong>Jucimeri Isolda Silveira</strong>
	</p>
	<p align="justify">
		É doutora em Serviço Social pela PUCSP, mestre em Sociologia pela UFPR, professora do Curso de Serviço Social e do Programa de Pós-Graduação em Direitos Humanos e Políticas Públicas, coordenadora do Núcleo de Direitos Humanos. Possui publicações em políticas públicas, especialmente em assistência social. Foi consultora pelo Programa das Nações Unidas para o Desenvolvimento junto ao Ministério de Desenvolvimento Social e Combate à Fome. Possui experiência em direitos humanos e políticas púbicas, na área de planejamento, gestão pública e trabalho social.
	</p>
	<br />
	<p>
		<h2><strong>Palestra de Encerramento</strong></h2>
	</p>
	<p>
		<strong>Título:</strong> “DIREITOS HUMANOS, CIDADES E DESENVOLVIMENTO SUSTENTÁVEL <strong>
		<br />
		Palestrante:</strong> Prof. Dr. Harry Alberto Bollmann - PUCPR
	</p>
	<p align="justify">
		Vivemos em uma época de incertezas, onde até mesmo os direitos humanos mais elementares precisam ser reconquistados dentro da perturbada e impessoal sociedade moderna. Ao mesmo tempo, a constante produção teórica do utopismo das cidades justas como um local de realização da plena condição humana, bem como da equilibrada convivência com o meio ambiente e suas funções, acentua uma realidade diacrônica entre o discurso e a ação, entre o sonho e a realidade. A cidade de hoje já não é mais vivenciada e nem entendida como antes. Elos de pertencimento e de respeito se romperam. A cidade se transformou mais em um objeto de consumo, ou um objeto orientado para o consumo dos bens e serviços nela transacionados. Para alguns mais saudosistas, a cidade (antiga) está morta, embora persista em um estado alienado, como um ente difuso e amorfo, pontuado por inúmeras crises. Neste ponto, a universidade precisa desempenhar seu duplo papel: o de pensar e de discutir os fundamentos de um novo projeto urbano sustentável que consiga reconectar as pessoas com o seu espaço vital, sem esquecer o de buscar as soluções emergenciais para as crises da atualidade.
	</p>
	<br />
	<p>
		<strong>Prof. Dr. Harry Alberto Bollmann</strong>
	</p>
	<p align="justify">
