			<h1>Apresentação</h1>

			<P>
				Este documento é um desdobramento do compromisso assumido pela Rede Marista Internacional das Instituições de Educação Superior, realizado no seu quinto encontro, em 2006, na Cidade do México, que visou fortalecer a educação marista com o intuito de formar "bons cristãos e virtuosos cidadãos", como queria o nosso fundador São Marcelino Champagnat, além de reunir educadores de várias partes do mundo em um programa de atividades para uma rica troca de experiências.
			</P>
			<P>
				O objetivo deste material é possibilitar, por meio do Instituto dos Irmãos Maristas, a internacionalização de nossas práticas de Ação Pastoral em nossas universidades. Assim, a construção colaborativa nos auxiliará a conhecer as diferentes realidades mundiais e a criar uma grande rede de experiências nesse segmento. Sabemos que algumas universidades possuem importantes conhecimentos e atuação em pastoral universitária, bem como em outras vemos que esta prática ainda é incipiente.
