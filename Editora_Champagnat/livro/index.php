			<h1>Presentation</h1>

			<P>
				This document is an unfolding of the compromise assumed by the Marist International Network of Institutions of Higher Education, which took place in its fifth encounter, in Mexico City, 2006, which undertook to strengthen marist education with the objective of forming "good Christians and virtuous citizens", as our founder Saint Marcellin Champagnat wished, besides gathering educators of various parts of the world in a program of activities for a rich exchange of experiences.
			</P>
			<P>
				The focus of this material is to enable the internationalization of our practices of pastoral action in the universities through the Institute of the Marist Brothers. Thus, a collaborative construction will help us to know the different world realities and to create a great network of experiences in this segment. We know that some universities possess important knowledge and performance in university pastoral, but we also know that in other universities this practice is still incipient.
