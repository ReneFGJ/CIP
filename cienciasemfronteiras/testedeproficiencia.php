				 	<h3>Curiosidades sobre os testes de proficiência</h3>
				 	<h4>TOEFL</h4>
				 	<p>Por padrão, o resultado do TOEFL sai de Princeton, New Jersey e demora de 4 a 6 semanas para chegar. 
				 		Porém, devido a problemas com o correio brasileiro, a ETS abriu uma excessão ao Ciência sem
				 		Fronteiras e está disponibilizando o PDF do resultado, porém não sabemos se essa função vai
				 		continuar nos próximos editais.</p>
				 	<h4>IELTS</h4>
				 	<p>O resultado do IELTS sai de São Paulo, então chega com poucas semanas na casa do aluno. É uma opção
				 		"mais rápida" para aqueles que farão o teste perto da data de inscrição.</p>
			
