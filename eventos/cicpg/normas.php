<B>Confecção do Pôster</B>
<P>O aluno deve fazer o pôster a partir do modelo disponível na página do PIBIC (www.pucpr.br/ic em PIBIC e modelos de documentos) no formato PowerPoint (.ppt). O texto do pôster deve ser legível a uma distância de, pelo menos, 1 m. O pôster deve ter 1,20 cm de altura e 90 cm de largura. No pôster deve constar: título, nomes dos autores, curso dos autores e dados da pesquisa. Utilizar o mínimo de texto, pode ser colocado figuras, fotos, tabelas, gráficos e esquemas possíveis. Organizar as informações de modo que as ideias centrais do trabalho sejam facilmente apreendidas e utilizar todos os recursos disponíveis para o pôster despertar o interesse do público. </P>

<P>É obrigatório que o título do trabalho no pôster seja idêntico ao título do resumo submetido ao evento. É proibida a apresentação de trabalhos impressos sem características de um pôster, bem como de pôsteres que não obedeçam às normas gerais de tamanho e legibilidade. </p>
<P>O aluno de pós-graduação irá apresentar seu trabalho no formato de pôster ou oral, de acordo com a escolha do programa. As regras de apresentação são iguais aos alunos de graduação incluindo o modelo do pôster. O custo da impressão do pôster é dos autores do projeto. A impressão do pôster deverá ser feita na gráfica da preferência dos autores do projeto, no material desejado. As normas e modelos de pôster são iguais para PIBIC, PIBITI, ICV, ITV, PIBIC Jr e Mostra de Pesquisa PG.</P>

