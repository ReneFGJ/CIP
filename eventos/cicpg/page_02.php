	<h1>Apresentação</h1>

	<div class="texto-apresentacao">
					<p>
						O <strong>3° Congresso Sul Brasileiro de Iniciação Científica e Pós-Graduação</strong> será realizado em Curitiba, entre os dias 04 e 06 
						de Novembro de 2014. O objetivo do congresso é promover o intercâmbio de conhecimento e práticas entre instituições, 
						pesquisadores e alunos de iniciação científica, de iniciação tecnológica, de mestrado e de doutorado, colocando em pauta 
						a pesquisa e a inovação. A divulgação das pesquisas realizadas por esses alunos orientados por pesquisadores experientes 
						valorizará a atuação dos alunos de graduação e pós-graduação como também busca motivar novos alunos para participarem da 
						atividade de investigação científica. Esta edição do evento quer integrar a ciência, a arte e a cultura. O evento é 
						<strong>gratuito e aberto a estudantes de quaisquer Instituições de Ensino Superior e Institutos de Pesquisa</strong>.
					</p>
					<p>
						Simultaneamente, a Pontifícia Universidade Católica (PUCPR) estará realizando seu <strong>XXII Seminário de Iniciação Cientifica 
						e Tecnológica</strong>. Serão apresentados os trabalhos dos alunos envolvidos nos programas <a href="http://www.pucpr.br/pesquisacientifica/iniciacaocientifica/pibic.php" target="_blank">PIBIC</a>, <a href="http://www.pucpr.br/pesquisacientifica/iniciacaocientifica/pibiti.php" target="_blank">PIBITI</a> e <a href="http://www.pucpr.br/pesquisacientifica/iniciacaocientifica/pibicjr.php" target="_blank">PIBIC Jr</a>, 
						desenvolvidos ao longo de 12 meses de atividades.
