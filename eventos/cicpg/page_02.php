				<h1>Apresentação / Presentation</h1>
				<P>
					O III Congresso Sul Brasileiro de Iniciação Científica e Pós-Graduação será realizado em Curitiba, entre os dias 04 e 06 de Novembro de 2014. O objetivo do congresso é promover o intercâmbio de conhecimento e práticas entre instituições, pesquisadores e bolsistas de Iniciação Científica, colocando em pauta pesquisa e inovação. Além de elevar a valorização de alunos de graduação e Pós-Graduação, e divulgar pesquisas desenvolvidas por eles sob a orientação de professores-pesquisadores e por fim estimular a adesão de alunos à atividade de investigação. Esta edição do evento busca integrar ciência, arte e cultura. O evento é gratuito e aberto a estudantes de quaisquer Instituição de Ensino Superior.
				</P>
				<P>
					Simultaneamente, a Pontifícia Universidade Católica (PUCPR) estará realizando seu XXII Seminário de Iniciação Cientifica (SEMIC) e a III Seminário de Iniciação Tecnológica e Inovação. Serão exibidos os trabalhos de Iniciação Científica desenvolvidos pelos alunos de graduação ao longo do ano e proporciona momentos de aprendizado e de contribuição de bolsistas de Iniciação Científica da PUCPR.
