			<p>Descrição:</p>
 		<p><strong>Parte 1:</strong><br />
			- Apresentação; <br />
			- Introdução - Por que escrever?<br />
			- Fundamentação lógica e argumentação;<br />
			- Diferenças entre veículos: conferências/seminários, revistas, divulgação científica;<br />
			- ética e boas práticas na pesquisa;<br />
			- Como escrever um artigo agradável e instigante;<br />
			- Manter a atenção e motivação do leitor - criar expectativas;<br />
			- Exigir menos da memória do leitor - reduzir o tempo de leitura;<br />
			- Estabelecer uma progressão para uma leitura fluída - impulsionar a leitura.</p>


