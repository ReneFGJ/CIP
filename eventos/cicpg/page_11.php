	<h1><span>Política de adesão</span></h1>
	<h3 class="titulo-politica-adesao">Política de adesão ao 3° Congresso Sul Brasileiro de Iniciação Científica e Pós-Graduação</h3>
	<table width="900" align="center">
		<tr>
			<td>
			<P>
				Para aderir, ao 3° Congresso Sul Brasileiro de Iniciação Científica e Pós-Graduação, as Instituição de Ensino Superior (IES) e Institutos de Pesquisa deverão enviar e-mail para cicpg@pucpr.br informando o número de alunos em Iniciação Científica e alunos de Pós-Graduação <I>stricto sensu</I> até <B>22 de setembro de 2014</B>. Ao aderir ao evento a IES garante a participação do aluno gratuitamente no Congresso por meio da cota destinada a cada IES.
			</P><h4>Sobre cotas</h4>
			<P>
				A cota de cada IES ou Instituto de Pesquisa será determinada de acordo com o número de alunos de IC (iniciação cientifica) e de PG (pós-graduação <i>stricto sensu</i>). A Instituição fica responsável em indicar os estudantes participantes por meio do envio de uma listagem com nome, CPF e modalidade (IC, IT, mestrado ou doutorado) para a organização do evento.
            </P>
<a href="http://www2.pucpr.br/reol/eventos/cicpg/page_submit.php">Área de submissão</a>

	<P>1)	Os alunos selecionados pelas instituições de ensino Superior e Institutos de Pesquisa que fizeram a adesão ao evento deverão digitar o CPF para ter acesso a área de inscrição. Na tela de inscrição o aluno deve completar os dados pessoais e em seguida devera proceder a submissão do trabalho. O trabalho está na qualidade de representante da instituição, uma vez que já foi avaliado por mérito.
Caso não encontre o seu nome, você deve fazer contato com a pessoa responsável de sua IES ou Instituto de Pesquisa para que confirme sua indicação junto a organização do evento.</P>
	<P>2)	Alunos de IES ou Institutos de Pesquisa que não fizeram a adesão ao evento mas querem submeter trabalhos, poderão participar incluindo todas as informações nos respectivos campos. Os trabalhos serão avaliados por mérito e o autor receberá a resposta do aceite ou não.</P>
    <P>3)	Clique <a><a href="http://www2.pucpr.br/reol/eventos/cicpg/index.php#page14">aqui</a></a> para saber se sua IES ou Institutos de Pesquisa que fez adesão ao evento e obter o respectivo contato.</P>

