				<P>
					Para o III Congresso Sul Brasileiro de Iniciação Científica e Pós-Graduação são consideradas duas modalidades de submissão, a primeira para apresentação oral e outra para pôster.
				</P>
				<P>
					Somente os melhores trabalhos conforme avaliação por mérito serão indicados para apresentação oral, o restante para pôster, desde que aprovada.
				</P>
				<P>
					O evento ocorrerá nos dias 4, 5 e 6 de novembro de 2014 a Pontifícia Universidade Católica do Paraná sediará o III Congresso Sul Brasileiro de Iniciação Cientifica e Pós-Graduação junto ao XXII SEMIC.
				</P>
				<P>
					Para submeter trabalhos clique <A href="index.php#page03">aqui</A>.
				<P>
					<B>Modalidade Oral</B>
				</P>

				<P>
					Indicações Gerais
				</P>

				<P>
					Os resumos expandidos podem ser encaminhados em português, podendo ser aceitos trabalhos em inglês. Abreviações oficiais poderão ser empregadas somente após uma primeira menção completa. Deverão constar nos resumos: nome dos autores, titulação, filiação institucional, e-mail.
				</P>
				<P>
					Os resumos serão encaminhados para Comissão de avaliadores, que serão julgados. Os organizadores coordenam as informações entre os autores e revisores, cabendo-lhes a decisão final sobre quais resumo serão publicados com base nas recomendações feitas pelos revisores. Quando recusados, os resumos serão devolvidos com a justificativa da comissão organizadora. São observados os seguintes critérios: consistência e rigor teórico conceitual; articulação entre objetivo, fundamentação teórico metodológica, análise e conclusão de dados; bibliografia pertinente e atualizada; originalidade e contribuições para a área; correção de linguagem e respeito às normas técnicas - ABNT.
				</P>
				<P>
					O acompanhamento do processo poderá ser realizado pelo site do evento, sendo os resultados expressos como: Aceito e  Não aceito. A aprovação dos resumos estendidos pelos autores é definida no momento da submissão. Somente um dos autores deverá submeter o trabalho.
				</P>
				<P>
					Os trabalhos devem ser enviados com revisão de português, e ou inglês, sendo a responsabilidade do autor.
				</P>

				<P>
					<B>FORMA E APRESENTAÇÃO ORAL DOS ORIGINAIS</B>
				</P>

				<P>
					Os trabalhos em apresentação oral deverão tem um resumo entre 200 e 400 palavras,  podendo ser em Inglês ou Português. O resumo deve conter entre três e cinco palavras chaves, retiradas de um tesauro ou vocabulário específico da área do conhecimento.
				</P>
				<P>
					O restante do trabalho deve ser na modalidade de resumos expandidos, digitado em Word for Windows ou similar, em fonte Times New Roman, tamanho 12 pt, espaçamento entre linhas de 1,5, formato A4, não devendo ultrapassar 3 laudas incluindo as referências, ilustrações, figuras, gráficos, quadros e tabelas.
				</P>
				As ilustrações, figuras, gráficos, quadros e  tabelas etc., devem ser limitadas ao mínimo indispensável (observando que o título também deverá ser indicado no texto onde a ilustração será inserida), identificadas e numeradas consecutivamente em algarismos arábicos com extensão .jpg ou .tif e resolução mínima de 300 dpi. </P>
				<P>
					Na montagem das tabelas, seguir as Normas de apresentação tabular, estabelecidas pelo Conselho Nacional de Estatística e publicadas pelo IBGE em 1979. No preparo do resumo deverá ser observada a seguinte estrutura:
				</P>

				<P>
					<B>Cabeçalho</B>
				</P>

				<P>
					Título do resumo em português (Inicial em maiúscula, restante em letras minúsculas, a menos que seja nome próprio, em negrito, fonte Times New Roman, tamanho 12 pt, centralizado), subtítulo em letras minúsculas (exceção para nome próprio), em inglês (obrigatório) (somente a primeira letra do título em maiúscula, as demais palavras em letras minúsculas - exceção para nome próprio), em itálico, fonte Times New Roman, tamanho 12 pt, centralizado). O título deve conter no máximo 12 palavras, sendo suficientemente específico e descritivo.
				</P>
				<P>
					Nome(s) do(s) autor(es) deve(m) estar abaixo do título e apresentar: afiliação institucional/vínculo, cidade, estado, país e e-mail.
				</P>

				<P>
					<B>Corpo do resumo estendido</B>
				</P>
				<P>
					<B>Introdução</B>: Contém o propósito do estudo, de maneira concisa, e descrever quais os avanços que foram alcançados com a pesquisa. Inclui a contextualização do problema por meio de revisão bibliográfica. A introdução não deve incluir dados ou conclusões do trabalho em questão.
				</P>
				<P>
					<B>Metodologia</B>: Apresenta a trajetória metodológica em sua abordagem, procedimentos de investigação e análise dos dados. A fundamentação e argumentação teórico metodológica deve ser pertinente ao tema abordado e possibilitar uma análise densa e aprofundada. A literatura deve expressar referenciais atualizados da área.
				</P>
				<P>
					<B>Conclusão ou Considerações finais</B>: Apontam as contribuições ao propósito do texto relacionando-as ao conhecimento já existente, ampliando e aprofundando as discussões. Incluir as citações indispensáveis para embasar o estudo.
				</P>
				<P>
					<B>Referências</B>: Somente as citações que figuram no texto devem ser referenciadas. As referências no final do trabalho devem ser organizadas e apresentadas em ordem alfabética de acordo com o sobrenome do primeiro autor. Para elaboração das referências, deve ser observada a norma NBR 6023 da ABNT, ago. 2002. Não utilizar abreviações nem siglas dos nomes das instituições. Deve-se evitar ao máximo a citação de citação.
				</P>

				<P>
					O estudante fica livre para incorporação de novas seções como agradecimento, matérias e métodos entre outros.
				</P>

				<P>
					<B>Modalidade Pôster
				</P></B>
				<P>
					Utilize o template disponível no site do evento, o banner deve ser impresso em tamanho 90x120cm colorido, utilizando apenas uma página. No banner deve ter os campos de: Introdução, Objetivo(s), Metodologia, Discussão e Resultado(s), Conclusão ou Considerações, e um breve quando com as referências utilizadas no Banner.
				</P>
				<P>
					Todo acompanhamento da submissão será feito pelo site.
				</P>
