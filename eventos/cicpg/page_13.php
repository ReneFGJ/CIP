				<B>Érico Veríssimo (1905-1975)</B>
				<BR>
                Foi um dos escritores brasileiros mais populares do século XX, "Olhai os Lírio do Campo", é sua obra prima. Foi um dos melhores romancistas brasileiros. Fez parte do segundo tempo modernista. Recebeu o "Prêmio Machado de Assis" com a obra "Música ao Longe" e o "Prêmio Graça Aranha" com "Caminhos Cruzados".<BR>
                <BR>
                Escreveu 33 livros, teve diversas de suas obras adaptadas para TV e filmes. Seus livros foram traduzidos para o alemão, espanhol, finlandês, francês, holandês, húngaro, indonésio, inglês, italiano, japonês, norueguês, polonês, romeno, russo, sueco e tcheco.
