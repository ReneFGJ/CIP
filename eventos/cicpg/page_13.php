			<BR><B>Paulo Leminski (1944-1989)</B>
			<BR>
            Foi um escritor, poeta, crítico literário, tradutor e professor. Era, também, faixa preta de judô. Leminski tornou-se reconhecido por ter inventado seu próprio jeito para escrever poesias, fazendo trocadilhos ou brincando com ditados populares. Paulo Leminski foi também professor de História e de Redação em cursos pré-vestibulares, além de professor de judô.<BR>
            <BR>  Leminski teve poemas e textos publicados em diversas revistas, escreveu letras de músicas com uma grande influência de MPB (Música Popular Brasileira) chegando até a fazer pareceria com Caetano Veloso.
            <BR>
			<BR><B>Helena Kolody (1912-2004)</B>
			<BR>
            Foi uma poetisa, se tornou uma das poetisas mais importantes do Paraná, e praticava principalmente o haicai, que é uma forma poética de origem japonesa, cuja característica é a concisão, ou seja, a arte de dizer o máximo com o mínimo. Foi a primeira mulher a publicar haicais no Brasil, em 1941.<BR>
            <BR>
            Foi admirada por poetas como Carlos Drummond de Andrade e Paulo Leminski, sendo que, com esse último, teve uma grande relação de amizade pessoal e literária.<BR>
            <BR>
            Foi eleita em 1991 para a Academia Paranaense de Letras. E em 2003 recebeu o título de "Doutora Honoris Causa" pela Universidade Federal do Paraná.
