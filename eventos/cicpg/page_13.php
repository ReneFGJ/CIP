				<B>Zilda Arns (1934-2010)</B>
				<BR>
                Foi uma médica pediatra e sanitarista brasileira, fundadora e coordenadora internacional da Pastoral da Criança e da Pastoral da Pessoa Idosa, organismos de ação social da Conferência Nacional dos Bispos do Brasil (CNBB). Recebeu diversas menções especiais e títulos de cidadã honorária no país. Da mesma forma, à Pastoral da Criança foram concedidos diversos prêmios pelo trabalho que vem sendo desenvolvido desde a sua fundação. Em 2012 numa seleção por um formato internacional, Arns foi eleita a 17° maior brasileira de todos os tempos. <BR>
                <BR>
                Zilda Arns encontrava-se em Porto Príncipe, em missão humanitária, para introduzir a Pastoral da Criança no país. No dia 12 de janeiro de 2010, pouco depois de proferir uma palestra para cerca de 15 religiosos de Cuba, o país foi atingido por um violento terremoto. A Dra. Zilda foi uma das vítimas da catástrofe.
                <BR>
				<BR>
				<B>Cristovão Tezza </B>
				<BR>
                É um romancista e professor universitário brasileiro, embora tenha nascido em Lages-SC, Cristóvão Tezza mudou-se para Curitiba, no Paraná, com oito anos de idade. Esta cidade é cenário de boa parte de sua literatura, em que personagens visitam ruas e pontos turísticos. <BR>
                <BR>
                Doutor em Literatura Brasileira, Tezza é professor de Linguística na Universidade Federal do Paraná. Já publicou dez romances, em algumas declarações ele afirma que “só uns quatro ou cinco escritores brasileiros poderiam viver só dos livros” e, por esse motivo, é professor. Ganhou o prêmio da Academia Brasileira de Letras de melhor romance brasileiro de 2004, pelo seu livro “O fotógrafo”. Foi considerado pela revista Época um dos 100 brasileiros mais influentes do ano de 2009.
                É também colunista quinzenal da Folha de S. Paulo.
