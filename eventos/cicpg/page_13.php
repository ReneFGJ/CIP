			<BR><B>Poty Lazzaroto (1924-1998)</B>
            <BR>
            Foi um desenhista, gravurista, ceramista e muralista brasileiro, estudou pintura na Escola Nacional de Belas Artes no Rio de Janeiro. Freqüenta curso de gravura com Carlos Oswald (1882-1971) no Liceu de Artes e Ofícios do Rio de Janeiro. Em 1946, viaja para Paris, onde permanece por um ano. Estuda litografia na École Supérieure des Beaux-Arts, com bolsa do governo francês. Em 1950, funda, juntamente com Flávio Motta (1916), a Escola Livre de Artes Plásticas, na qual leciona desenho e gravura. Nessa época organiza o primeiro curso de gravura do Museu de Arte de São Paulo Assis Chateaubriand (Masp). Organiza, ao longo da década de 1950, cursos sobre gravura em Curitiba, Salvador e Recife. Desde os anos 1960 tem destaque como muralista, com diversas obras em edifícios públicos e particulares no país e no exterior. Tem relevante atuação como ilustrador de obras literárias como as de Jorge Amado, Graciliano Ramos, Euclides da Cunha e Dalton Trevisan, entre outros.<BR>
            <BR>É autor dos livros A Propósito de Figurinhas, de 1986, e Curitiba, de Nós, de 1989, em parceria com Valêncio Xavier Niculitcheff. Executa diversos murais, como o da Casa do Brasil, em Paris, 1950, e o painel para o Memorial da América Latina, São Paulo, 1988. A partir dos anos 1980 são lançadas várias publicações sobre sua produção, entre elas: Poty Ilustrador, de Antônio Houaiss (1915-1999), em 1988; Poty: Trilhos, Trilhas e Traços, de Valêncio Xavier Niculitcheff, em 1994, e Poty: o lirismo dos anos 90, de Regina Casillo, em 2000.<BR>
            <BR>
            Obteve medalha de ouro e Prêmio de Viagem ao País na secção de Artes Gráficas do Salão Nacional de Arte Moderna.
