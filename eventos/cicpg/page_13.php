				<B>Cruz e Sousa (1861-1898) </B>
				<BR>
                Filho de escravos alforriados foi um poeta brasileiro, com a alcunha de Dante Negro ou Cisne Negro, foi um dos precursores do simbolismo no Brasil. João da Cruz desde pequeno recebeu a tutela e uma educação refinada de seu ex-senhor, aprendeu francês, latim e grego, além de Matemática e Ciências Naturais.<BR>
                <BR>
                Dirigiu o jornal Tribuna Popular, no qual combateu a escravidão e o preconceito racial, foi recusado como promotor de Laguna-SC por ser negro. Em 1885 lançou o primeiro livro, Tropos e Fantasias em parceria com Virgílio Várzea. Cinco anos depois foi para o Rio de Janeiro, onde trabalhou como arquivista na Estrada de Ferro Central do Brasil, colaborando também com o jornal Folha Popular. Em 1893, publica Missal (prosa poética baudelairiana) e em agosto, Broquéis (poesia), dando início ao Simbolismo no Brasil que se estende até 1922. Em novembro desse mesmo ano casou-se com Gavita Gonçalves, também negra, com quem teve quatro filhos, todos mortos prematuramente por tuberculose. Morreu em Minas Gerais, na cidade de Antonio Carlos, vítima também da tuberculose.<BR>
                <BR>
                Cruz e Sousa é um dos patronos da Academia Catarinense de Letras, representando a cadeira número 15.<BR>
