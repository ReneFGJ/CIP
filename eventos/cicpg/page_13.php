				<B>Victor Meirelles de Lima (1832-1903)</B>
				<BR>
				Nasceu em Nossa Senhora do Desterro, atual Florianópolis,
				pintor, desenhista e professor, começou sua trajetória precocemente,
				realizando paisagens da cidade. Frequentou a Academia Imperial de Belas Artes,
				no Rio de Janeiro e aos vinte anos, conquistou o Prêmio Especial de Viagem à Europa.
				De 1853 a 1861, viveu primeiro na Itália e em seguida na França, onde se dedicou
				ao estudo e ao trabalho. Foi professor honorário da Academia Imperial de Belas Artes,
				onde ensinou pintura histórica e professor do Liceu de Artes e Ofícios,
				no Rio de Janeiro. Autor de quadros históricos, retratos, panoramas e
				da mais popular das telas brasileiras, "Primeira Missa no Brasil" , exposta no Salão de Paris em 1861 (obra pertencente ao acervo do Museu Nacional de Belas Artes/IBRAM/MinC), Victor Meirelles deixou um extraordinário acervo, minuciosos esboços, estudos em papel e óleos sobre tela. O artista faleceu no Rio de Janeiro em 22 de fevereiro de 1903.
