				<B>Juarez Machado</B>
                <BR>
                É um artista plástico brasileiro, é pintor, escultor, desenhista, caricaturista, mímico, designer, cenógrafo, escritor, fotógrafo e ator.<BR>
                <BR>
                Estudou na Escola de Música e Belas-Artes do Paraná, em 1964 realizou sua 1ª mostra individual na Galeria Cocaco, de Curitiba, iniciando uma carreira de grande sucesso. Mudou-se para o Rio de Janeiro em 1966, onde residiu por vinte anos. Através de seus desenhos de humor, projeta-se nacionalmente. Além do desenho e da pintura, fez incursões pela mímica, cenografia, programação visual, ilustração e escultura. Foi chargista dos principais jornais brasileiros e mímico no programa Fantástico, da TV Globo. No final dos anos 70 voltou-se totalmente para a pintura. Em 1971 realizou os desenhos de abertura do filme Uma Pantera em Minha Cama.<BR>
                <BR>
                Pretendendo internacionalizar seu trabalho, em 1978 Juarez viajou a Nova Iorque, Londres e, finalmente, foi para Paris, onde fixou residência em 1986 e montou ateliê.
                Juarez Machado recebeu inúmeros prêmios, tanto no Brasil como no exterior. Tem feito exposições frequentes nos Estados Unidos e na Europa.
