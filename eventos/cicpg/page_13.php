				<B>Elis Regina (1945-1982)</B>
				<BR>
                Foi uma cantora brasileira, conhecida por sua presença de palco, sua voz e sua personalidade. Com os sucessos de Falso Brilhante e Transversal do Tempo, ela inovou os espetáculos musicais no país e era capaz de demonstrar emoções tão contrárias, como a melancolia e a felicidade, numa mesma apresentação ou numa mesma música.<BR>
                <BR>
                Em 2013, foi eleita a segunda melhor voz da música brasileira pela revista Rolling Stone Brasil, superada apenas por Tim Maia. Elis foi citada também na lista dos maiores artistas da música brasileira, ficando na 14ª posição, sendo a mulher mais bem colocada. Em novembro de 2013 estreou um musical em sua homenagem Elis, a musical.
