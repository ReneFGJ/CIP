		<h6>O que é?</h6>
 		<p><strong>Conte para nós quem é o cientista que serve de inspiração para a sua carreira!</strong></p>
		<p>O Concurso “Meu Cientista Favorito” traz a oportunidade de você, aluno, conhecer e refletir sobre a carreira, 
			desafios e conquistas de cientistas em diferentes áreas de conhecimento e compartilhar suas reflexões sobre estes 
			protagonistas da ciência. Escreva um ensaio sobre aquele pesquisador que tem grande influência
			na sua carreira.</p>
		
		<p>Os ensaios selecionados serão apresentados à comunidade acadêmica no período do 3º Congresso Sul Brasileiro 
			de Iniciação Científica e Pós-Graduação (3º CIC e PG, www.pucpr.br/cicpg), que acontecerá na PUCPR, Campus Curitiba, 
			nos dias 4, 5 e 6 de novembro de 2014.</p>

		<h6>Como Participar?</h6>
		<p>Inscreva seu trabalho através do email <strong>meucientistafavorito@pucpr.br</strong>, mandando o seu ensaio e o 
			comprovante de matrícula.</p>

