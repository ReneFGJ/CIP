			<div id="duvidas-frequentes">
				<h4>1. Como faço para me inscrever no programa?</h4>
				<p>O aluno candidato deverá inscrever-se no site do Ciência Sem Fronteiras no <a href="http://www.cienciasemfronteiras.gov.br/">site do Governo</a>. Deverá ser feita também a inscrição pelo site da <a href="http://www2.pucpr.br/reol/cienciasemfronteiras/">PUCPR do CsF</a>. É necessária a inscrição nesses dois momentos para que toda e qualquer informação sobre a candidatura do aluno seja disponibilizada a coordenação da Iniciação Científica (PIBIC). Sendo assim, poderemos acompanhar o processo de inscrição e aprovação de cada aluno para assim esclarecer qualquer dúvida que se faça necessária.</p>
					
				<h4>2. Quem inscreve os candidatos? O coordenador ou o próprio estudante?</h4>
				<p>O coordenador é responsável pela seleção dos candidatos e comunicação aos mesmos do resultado da seleção. Após isso, o candidato deverá se inscrever na <a href="http://www.cienciasemfronteiras.gov.br/">página do programa</a> , preenchendo os formulários de inscrição e enviando as documentações pertinentes.</p>
					
				<h4>3. Como funciona a indicação dos candidatos pela universidade brasileira?</h4>
				<p>A universidade deve publicar amplamente a chamada pública que estabelece o programa e divulgar o respectivo método de seleção dos candidatos. Os aprovados no processo de seleção da IES deverão preencher o formulário da CAPES/CNPq, disponível no site do programa. Após o preenchimento, no prazo estipulado pela Capes, o coordenador do programa deverá homologar as inscrições dentro da lista de aprovados pela IES. Os candidatos homologados serão classificados pela CAPES/CNPq.</p>
				
				<h4>4. O meu curso de graduação está incluso nas áreas prioritárias?</h4>
				<p>Consulte a relação das <a href="areascursos.php">áreas prioritárias</a> do Ciência sem Fronteiras.</p>
				
				<h4>5. Há exigência de proficiência em língua estrangeira? Quais exames de proficiência em língua serão aceitos?</h4>
				<p>A exigência será feita conforme acordo realizado entre as agências (CNPq e CAPES) e a instituição de ensino estrangeira. As instituições de ensino superior brasileiras estão sendo estimuladas a oferecer cursos intensivos de língua estrangeira para os potenciais candidatos à bolsa que tenham dificuldade no idioma utilizado na instituição de destino. O ensino superior em alguns países como: Alemanha, China e Coréia, é total ou parcialmente realizado em língua inglesa, possibilitando a candidatura de estudantes e pesquisadores com domínio de língua inglesa para esses países. A CAPES irá oferecer cursos de inglês à distância de alta qualidade para os estudantes brasileiros já aceitos no programa. O acesso a cursos de curta duração também poderá ser negociado, conforme a necessidade. No sentido de facilitar a vinda de estudantes e pesquisadores do exterior para o Brasil, as Embaixadas e Consulados brasileiros estudam a possibilidade de promover Cursos de Português no exterior. Grupos locais ajudarão na inserção de estrangeiros, facilitando a superação da barreira do idioma nesses casos. Estudantes que não alcançarem a nota mínima exigida nos testes de proficiência também poderão realizar cursos de idioma no exterior conforme avaliação feita pela CAPES e CNPq. <a href="testeproficiencia.php"Veja quais são os testes de proficiência solicitados></a></p>
				
				<h4>6. Quais instituições no exterior podem participar do programa?</h4>
				<p>As instituições de excelência no exterior estão firmando acordos com os repreentantes Educacionais de cada país ou com o CNPq e a CAPES.</p>
				
				<h4>7. Quais são as áreas prioritárias que posso concorrer?</h4>
				<p>Temas e áreas de interesse: Engenharias e demais áreas tecnológicas; Ciências Exatas e da Terra: Física, Química, Biologia e Geociências, Ciências Biomédicas e da Saúde; Computação e tecnologias da informação; Tecnologia Aeroespacial; Fármacos; Produção Agrícola Sustentável; Petróleo, Gás e Carvão Mineral; Energias Renováveis; Tecnologia Mineral; Biotecnologia; Nanotecnologia e Novos materiais; Tecnologias de Prevenção e Mitigação de Desastres Naturais; Biodiversidade e Bioprospecção; Ciências do Mar; Indústria criativa; Novas Tecnologias de Engenharia Construtiva; Formação de Tecnólogos.</p>
				
				<h4>8. Quem decide se o curso do candidato está dentro das áreas prioritárias?</h4>
				<p>A universidade brasileira analisa se o curso de graduação do candidato está ou não incluído entre as áreas prioritárias.</p>
				
				<h4>9. Os estudantes e pesquisadores beneficiados só poderão trabalhar dentro das áreas prioritárias do CsF?</h4>
				<p>Sim. Tanto estudantes como pesquisadores deverão seguir as áreas prioritárias, constantes do Programa.</p>
				
				<h4>10. Ainda não tenho o resultado original do exame de proficiência, posso enviar depois?</h4>
				<p>Caso não possua o teste com a pontuação exigida, a CAPES/CNPq aceitará como documento o comprovante de inscrição para realização da prova, que deverá ser anexado à inscrição on-line no lugar do documento "teste de proficiência". A nota mínima necessária deverá ser consultada no Edital de cada país. O candidato , caso ainda não possua a pontuação exigida pela CAPES/CNPq, poderá apresentar o comprovante de inscrição no teste de proficiência.</p>
				
				<h4>11. Ainda não concluí a graduação. Posso participar do programa?</h4>
				<p>Sim, desde que tenha concluído de 20% a no máximo 90% do curso na instituição de ensino brasileira e esteja devidamente matriculado. Neste caso, a aluno pode pleitear a bolsa Graduação Sanduíche no Exterior (SWG) para fazer intercâmbio de 6 a 12 meses no exterior,de acordo com cada Chamada.</p>
				
				<h4>12. Sou aluno PIBIC, como devo proceder?</h4>
				<p>Você deverá comunicar o setor responsável (PIBIC) sobre a sua aprovação para que a bolsa seja cancelada no momento em que for aceito pela Universidade destino. o CNPq não autoriza o acumulo de bolsas, portanto no momento da sua aprovação a bolsa PIBIC deverá ser suspensa.</p>
				
				<h4>13. Onde devo anexar os documentos exigidos na inscrição?</h4>
				<p>Os documentos da inscrição exigidos na chamada pública devem ser anexados dentro do formulário de inscrição disponível no link "formulário de inscrições da Capes". O documentos devem ser anexados em formato PDF.</p>
				
				<h4>14. A documentação exigida pode ser enviada por email?</h4>
				<p>Não. As documentações exigidas deverão ser enviadas pelo site da Capes, na página do programa, respeitando o respectivo calendário. Lembrando que todos os documentos devem estar no formato PDF.</p>
				
							
				<h4>15. Tenho bolsa auxílio da PUC, ProUNI ou FIES. Como devo proceder?</h4>
				<p>Você deverá comunicar o setor do PIBIC caso tenha qualquer auxílio para cursar a graduação na Universidade, seja Bolsa PUC, FIES ou ProUNI, o mesmo deverá ser suspenso pelo setor (PIBIC) e o aluno deverá comparecer ao SIGA para assinar o termo de "suspensão temporária", assim a bolsa não será cancelada tendo o aluno direito de reativá-la no seu retorno à PUC.</p>
				
				<h4>16. Qual o valor da bolsa dos alunos que vão para o exterior?</h4>
				<p>No site do Ciência Sem Fronteiras na chamada de cada País tem os valores específicos. Os valores variam de um País para o outro.</p>
				
				<h4>17. Quando ficarei sabendo para qual instituição irei?</h4>
				<p>Após a divulgação do resultado das aplicações nas universidades estrangeiras dos candidatos selecionados pelos institutos intermediários, quando for disponibilizada a resposta da aplicação, o candidato será comunicado por e-mail e/ou correios em qual Universidade foi aceito, no qual será esclarecido o procedimento para os passos posteriores.</p>
				
				<h4>18. Posso escolher a universidade para ingresso?</h4>
				<p>Os institutos que intermedeiam as universidades estrangeiras e as agências responsáveis pelo programa Ciência sem Fronteiras serão os responsáveis por aplicar as universidades, utilizando a pertinência quanto às áreas prioritárias do Programa Ciência sem Fronteiras.</p>
