				<h4>9. Does the beneficiary students and researchers in the Science Without Borders Program are only able to work inside the priority fields?</h4>
				<p>Yes. Students and researchers must follow the priority fields.</p>
				
				<h4>10. I still don´t have the TOEFL. Can I send my score later?</h4>
<P>If you don´t have the document with the TOEFL score, CAPES will accpet the inscription receipt, that must be attached in the online inscription replacing the proficiency score exam. The student must present the TOEFL results with a 79 score or if the student don´t own the demanded score by the CAPES, must present the TOEFL inscription receipt.</P>				
				
				<h4>11. I still didn´t finish my undergraduation, can I participate the program?</h4>
<p>Yes, on condition that you are regularly matriculated and have concluded from 20% to 90% of your undergraduation course in a brazilian instituition. In this case, the student can dispute the scholarship to study abroad of 6 to 12 months, according to the call.</p>				
				
				<h4>12.  I am a PIBIC student, how do I proceed?</h4>
<p>You must communicate the PIBIC about your approval, so the scholarship will be canceled in the moment you are accepted by the university abroad. CNPq doesn´t authorize the scholarship accumulation, so in the moment you are approved, your PIBIC scholarship will be suspended.</p>				
				
				<h4>13. Where do I attach the demanded documents in the inscription?</h4>
<p>The demanded documents must be attached inside the inscription former available in the link "formulário de inscrições da Capes". The same must be attached in PDF.</p>				
				
				<h4>14. Does the demanded documentation can be send by email?</h4>
<p>No. The demanded documentation must be send over the Capes website, in the program webpage, respecting the schedule. The documents must be in PDF format.</p>				
							
				<h4>15. I have a scholarship from PUC, ProUNI or FIES, how do I proceed?</h4>
<p>You must communicate PIBIC if you have any kind of scholarship to attend your undergraduation, even if it is PUC scholarship, FIES or ProUNI. It must be suspended by the PIBIC and the student must go to SIGA to sign the "temporary suspension" term, so the scholarship won´t be cancelated and the student will have the chance to reactivate it when come back to PUC.</p>				
				
				<h4>16. Which is the amount to the students that travel abroad?</h4>
<p>Inside the calls of each country, in the Science Without Boarders website, is written the specific amount of each country. The amount varies from a country to another.</p>				
				
