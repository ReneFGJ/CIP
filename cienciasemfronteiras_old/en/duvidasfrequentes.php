				<h4>1. How do I apply to Science without Borders?</h4>
				<p>The student should apply in Science Without Borders through this link: http://www.cienciasemfronteiras.gov.br/. The student must apply also in <a href="http://www2.pucpr.br/reol/cienciasemfronteiras">PUCPR website</a>. The subscription is necessary in these two ways because the PIBIC must have all the stundent inscription information. So, we can follow up the inscription process and aprove of each student to clarify any needed doubts.</p>
					
				<h4>2. Who inscribes the students? The coordinator or the student?</h4>
<p>The coordinator is responsible for the selection and in communicate the results to the student. After that, the student must apply in the Capes and IIE webpage, filling out the formers and sending the documents.</p>					
				
				<h4>3. How do the references work coming from the Brazilian universities?</h4>
<p>The university must publish a public call that stablish the program and post the student selection method. Those who are approved in the IES selection process should fill out the Capes form (it is available in IIE program homepage). After that, during the Capes deadline, the coordinator must homologate the inscription inside the IES approved students list. The students will be classified by the Capes.</p>				
				
				<h4>4. Does my degree course is included in the priority fields?</h4>
<p>The list of fields considered priority in Science Without Boarders can be viewed in the webpages program.</p>				
				
				<h4>5. Is there any proficiency language asked? Which proficiency exams are allowed?</h4>
<p>The agencies (CNPq and CAPES) along with the foreign instituitions will demand the proficiency exams. The brazilian instituitions are being stimulated in offering intensive language courses to the potentials students candidates, those who has difficults in learning the idiom used in the instituition abroad. Some countries like Germany, China and Korea do theirs classes partial in English, enabling the students and researchers who has a proficiency in this language to apply to these countries. CAPES will offer high quality english course with distance learning to approved brazilian students in the program. Short idioms courses can be negotiated, if necessary. The brazilian Ambassy and Consulate are looking forward to create Portuguese courses to make easier the students and researchers from other countries coming to Brazil. Local groups will help in the foreign insertion, making easy the language wall in these cases. Students who doesn´t get the minimum score in the proficiency exam will also study the laguage abroad, if CAPES and CNPq judge necessary.</p>				
				
				<h4>6. Which instituitions abroad can participate in the program?</h4>
<p>The excellence instituitions abroad are stablishing deals with Educational Representatives from each country or with the CNPq and CAPES.</p>				
				
